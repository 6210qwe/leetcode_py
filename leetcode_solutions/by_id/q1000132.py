"""
LCP 26. 导航装置 - 小扣参加的秋日市集景区共有 $N$ 个景点，景点编号为 $1$~$N$。景点内设有 $N-1$ 条双向道路，使所有景点形成了一个二叉树结构，根结点记为 `root`，景点编号即为节点值。 由于秋日市集景区的结构特殊，游客很容易迷路，主办方决定在景区的若干个景点设置导航装置，按照所在景点编号升序排列后定义装置编号为 1 ~ M。导航装置向游客发送数据，数据内容为列表 `[游客与装置 1 的相对距离,游客与装置 2 的相对距离,...,游客与装置 M 的相对距离]`。由于游客根据导航装置发送的信息来确认位置，因此主办方需保证游客在每个景点接收的数据信息皆不相同。请返回主办方最少需要设置多少个导航装置。 **示例 1：** >输入：`root = [1,2,null,3,4]` > >输出：`2` > >解释：在景点 1、3 或景点 1、4 或景点 3、4 设置导航装置。 > >![image.png](https://pic.leetcode.cn/1597996812-tqrgwu-image.png){:height="250px"} **示例 2：** >输入：`root = [1,2,3,4]` > >输出：`1` > >解释：在景点 3、4 设置导航装置皆可。 > >![image.png](https://pic.leetcode.cn/1597996826-EUQRyz-image.png){:height="200px"} **提示：** - `2 <= N <= 50000` - 二叉树的非空节点值为 `1~N` 的一个排列。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）遍历二叉树，通过动态规划计算每个节点的状态，并确定最少需要的导航装置数量。

算法步骤:
1. 定义状态：对于每个节点，定义三个状态：
   - `dp[0]`：当前节点没有导航装置，且其子节点也没有导航装置。
   - `dp[1]`：当前节点没有导航装置，但至少有一个子节点有导航装置。
   - `dp[2]`：当前节点有导航装置。
2. 递归遍历二叉树，计算每个节点的状态。
3. 通过合并子节点的状态，更新当前节点的状态。
4. 最终结果取根节点的最小值。

关键点:
- 使用动态规划的思想，通过递归计算每个节点的状态。
- 合并子节点状态时，需要考虑各种可能的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(N)，其中 N 是二叉树的节点数，因为每个节点只被访问一次。
空间复杂度: O(H)，其中 H 是二叉树的高度，这是递归调用栈的空间开销。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def dfs(node: TreeNode) -> List[int]:
    if not node:
        return [0, float('inf'), float('inf')]
    
    left = dfs(node.left)
    right = dfs(node.right)
    
    # 当前节点没有导航装置，且子节点也没有导航装置
    dp0 = min(left[1] + right[1], left[2] + right[2], left[2] + right[1], left[1] + right[2])
    # 当前节点没有导航装置，但至少有一个子节点有导航装置
    dp1 = min(left[0] + right[1], left[1] + right[0], left[0] + right[2], left[2] + right[0])
    # 当前节点有导航装置
    dp2 = 1 + min(left[0], left[1], left[2]) + min(right[0], right[1], right[2])
    
    return [dp0, dp1, dp2]

class Solution:
    def navigation(self, root: TreeNode) -> int:
        result = dfs(root)
        return min(result)

Solution = create_solution(Solution)