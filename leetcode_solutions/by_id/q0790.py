"""
775. 全局倒置与局部倒置 - 给你一个长度为 n 的整数数组 nums ，表示由范围 [0, n - 1] 内所有整数组成的一个排列。 全局倒置 的数目等于满足下述条件不同下标对 (i, j) 的数目： * 0 <= i < j < n * nums[i] > nums[j] 局部倒置 的数目等于满足下述条件的下标 i 的数目： * 0 <= i < n - 1 * nums[i] > nums[i + 1] 当数组 nums 中 全局倒置 的数量等于 局部倒置 的数量时，返回 true ；否则，返回 false 。 示例 1： 输入：nums = [1,0,2] 输出：true 解释：有 1 个全局倒置，和 1 个局部倒置。 示例 2： 输入：nums = [1,2,0] 输出：false 解释：有 2 个全局倒置，和 1 个局部倒置。 提示： * n == nums.length * 1 <= n <= 105 * 0 <= nums[i] < n * nums 中的所有整数 互不相同 * nums 是范围 [0, n - 1] 内所有数字组成的一个排列
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 如果全局倒置的数量等于局部倒置的数量，那么对于每个元素 nums[i]，它最多只能偏离其原始位置 i 两个单位。

算法步骤:
1. 遍历数组，检查每个元素是否在其原始位置或其相邻位置。
2. 如果某个元素偏离其原始位置超过两个单位，则返回 False。
3. 如果遍历完整个数组都没有发现这种情况，则返回 True。

关键点:
- 通过检查每个元素与其索引的差值来判断是否存在非局部倒置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_ideal_permutation(nums: List[int]) -> bool:
    """
    函数式接口 - 判断全局倒置的数量是否等于局部倒置的数量
    """
    for i in range(len(nums)):
        if abs(nums[i] - i) > 1:
            return False
    return True


Solution = create_solution(is_ideal_permutation)