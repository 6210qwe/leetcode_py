"""
1621. 大小为 K 的不重叠线段的数目 - 给你一维空间的 n 个点，其中第 i 个点（编号从 0 到 n-1）位于 x = i 处，请你找到 恰好 k 个不重叠 线段且每个线段至少覆盖两个点的方案数。线段的两个端点必须都是 整数坐标 。这 k 个线段不需要全部覆盖全部 n 个点，且它们的端点 可以 重合。 请你返回 k 个不重叠线段的方案数。由于答案可能很大，请将结果对 109 + 7 取余 后返回。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/10/17/ex1.png] 输入：n = 4, k = 2 输出：5 解释： 如图所示，两个线段分别用红色和蓝色标出。 上图展示了 5 种不同的方案 {(0,2),(2,3)}，{(0,1),(1,3)}，{(0,1),(2,3)}，{(1,2),(2,3)}，{(0,1),(1,2)} 。 示例 2： 输入：n = 3, k = 1 输出：3 解释：总共有 3 种不同的方案 {(0,1)}, {(0,2)}, {(1,2)} 。 示例 3： 输入：n = 30, k = 7 输出：796297179 解释：画 7 条线段的总方案数为 3796297200 种。将这个数对 109 + 7 取余得到 796297179 。 示例 4： 输入：n = 5, k = 3 输出：7 示例 5： 输入：n = 3, k = 2 输出：1 提示： * 2 <= n <= 1000 * 1 <= k <= n-1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 为在前 i 个点中选择 j 个不重叠线段的方案数。通过递推关系可以计算出最终结果。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][j] 表示在前 i 个点中选择 j 个不重叠线段的方案数。
2. 基本情况：dp[0][0] = 1，表示在 0 个点中选择 0 个线段的方案数为 1。
3. 递推公式：dp[i][j] = dp[i-1][j] + dp[i-1][j-1] + (i-1) * dp[i-1][j]，其中 dp[i-1][j] 表示不使用第 i 个点的情况，dp[i-1][j-1] 表示使用第 i 个点作为新线段的起点的情况，(i-1) * dp[i-1][j] 表示将第 i 个点添加到现有线段的情况。
4. 最终结果为 dp[n][k]。

关键点:
- 动态规划的状态转移方程。
- 对结果取模 10^9 + 7。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)
空间复杂度: O(n * k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def number_of_sets(n: int, k: int) -> int:
    MOD = 10**9 + 7
    dp = [[0] * (k + 1) for _ in range(n + 1)]
    dp[0][0] = 1

    for i in range(1, n + 1):
        for j in range(1, min(i, k) + 1):
            dp[i][j] = (dp[i - 1][j] + dp[i - 1][j - 1] + (i - 1) * dp[i - 1][j]) % MOD

    return dp[n][k]


Solution = create_solution(number_of_sets)