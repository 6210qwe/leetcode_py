"""
2571. 将整数减少到零需要的最少操作数 - 给你一个正整数 n ，你可以执行下述操作 任意 次： * n 加上或减去 2 的某个 幂 返回使 n 等于 0 需要执行的 最少 操作数。 如果 x == 2i 且其中 i >= 0 ，则数字 x 是 2 的幂。 示例 1： 输入：n = 39 输出：3 解释：我们可以执行下述操作： - n 加上 20 = 1 ，得到 n = 40 。 - n 减去 23 = 8 ，得到 n = 32 。 - n 减去 25 = 32 ，得到 n = 0 。 可以证明使 n 等于 0 需要执行的最少操作数是 3 。 示例 2： 输入：n = 54 输出：3 解释：我们可以执行下述操作： - n 加上 21 = 2 ，得到 n = 56 。 - n 加上 23 = 8 ，得到 n = 64 。 - n 减去 26 = 64 ，得到 n = 0 。 使 n 等于 0 需要执行的最少操作数是 3 。 提示： * 1 <= n <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，通过位运算找到最近的 2 的幂，并决定是加还是减。

算法步骤:
1. 初始化操作计数器 `operations` 为 0。
2. 当 `n` 不为 0 时，进行以下操作：
   - 找到最接近 `n` 的 2 的幂 `power_of_two`。
   - 如果 `n` 与 `power_of_two` 的差值小于 `n` 与 `power_of_two + power_of_two` 的差值，则 `n` 减去 `power_of_two`，否则 `n` 加上 `power_of_two`。
   - 增加操作计数器 `operations`。
3. 返回操作计数器 `operations`。

关键点:
- 使用位运算快速找到最接近 `n` 的 2 的幂。
- 通过比较差值来决定是加还是减。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_reduce_to_zero(n: int) -> int:
    """
    函数式接口 - 返回将整数 n 减少到 0 需要的最少操作数
    """
    operations = 0
    while n != 0:
        # 找到最接近 n 的 2 的幂
        power_of_two = 1
        while power_of_two * 2 <= n:
            power_of_two *= 2
        
        # 决定是加还是减
        if n - power_of_two < power_of_two - (n - power_of_two):
            n -= power_of_two
        else:
            n += power_of_two
        
        operations += 1
    
    return operations


Solution = create_solution(min_operations_to_reduce_to_zero)