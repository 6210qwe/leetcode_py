def number_complement(num: int) -> int:
    """
    函数式接口 - 数字的补数
    
    实现思路:
    找到最高位，构造掩码，异或得到补数。
    
    Args:
        num: 输入整数
        
    Returns:
        补数
        
    Example:
        >>> number_complement(5)
        2
    """
    # 找到最高位
    mask = 1
    while mask <= num:
        mask <<= 1
    # mask-1得到全1掩码
    return num ^ (mask - 1)


