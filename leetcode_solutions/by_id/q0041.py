"""
41. 缺失的第一个正数 - 给你一个未排序的整数数组 nums ，请你找出其中没有出现的最小的正整数。 请你实现时间复杂度为 O(n) 并且只使用常数级别额外空间的解决方案。 示例 1： 输入：nums = [1,2,0] 输出：3 解释：范围 [1,2] 中的数字都在数组中。 示例 2： 输入：nums = [3,4,-1,1] 输出：2 解释：1 在数组中，但 2 没有。 示例 3： 输入：nums = [7,8,9,11,12] 输出：1 解释：最小的正数 1 没有出现。 提示： * 1 <= nums.length <= 105 * -231 <= nums[i] <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用数组本身作为哈希表，将数字i放到位置i-1

算法步骤:
1. 遍历数组，将数字i放到位置i-1（如果i在[1, n]范围内）
2. 再次遍历数组，找到第一个位置i，如果nums[i] != i+1，返回i+1
3. 如果都匹配，返回n+1

关键点:
- 使用数组本身作为哈希表，O(1)空间
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组两次
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def first_missing_positive(nums: List[int]) -> int:
    """
    函数式接口 - 缺失的第一个正数
    
    实现思路:
    使用数组本身作为哈希表，将数字i放到位置i-1。
    
    Args:
        nums: 未排序的整数数组
        
    Returns:
        没有出现的最小的正整数
        
    Example:
        >>> first_missing_positive([3,4,-1,1])
        2
    """
    n = len(nums)
    
    # 将数字i放到位置i-1
    for i in range(n):
        while 1 <= nums[i] <= n and nums[nums[i] - 1] != nums[i]:
            nums[nums[i] - 1], nums[i] = nums[i], nums[nums[i] - 1]
    
    # 找到第一个不匹配的位置
    for i in range(n):
        if nums[i] != i + 1:
            return i + 1
    
    return n + 1


