"""
2441. 与对应负数同时存在的最大正整数 - 给你一个 不包含 任何零的整数数组 nums ，找出自身与对应的负数都在数组中存在的最大正整数 k 。 返回正整数 k ，如果不存在这样的整数，返回 -1 。 示例 1： 输入：nums = [-1,2,-3,3] 输出：3 解释：3 是数组中唯一一个满足题目要求的 k 。 示例 2： 输入：nums = [-1,10,6,7,-7,1] 输出：7 解释：数组中存在 1 和 7 对应的负数，7 的值更大。 示例 3： 输入：nums = [-10,8,6,7,-2,-3] 输出：-1 解释：不存在满足题目要求的 k ，返回 -1 。 提示： * 1 <= nums.length <= 1000 * -1000 <= nums[i] <= 1000 * nums[i] != 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合来存储数组中的元素，并检查每个正数的负数是否存在。

算法步骤:
1. 创建一个空的哈希集合。
2. 遍历数组，将每个元素加入哈希集合。
3. 再次遍历数组，对于每个正数，检查其负数是否在哈希集合中，如果是则更新结果。
4. 返回结果，如果没有找到符合条件的正整数则返回 -1。

关键点:
- 使用哈希集合可以在 O(1) 时间内检查某个元素是否存在。
- 只需要一次遍历即可完成所有操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要两次遍历数组。
空间复杂度: O(n)，哈希集合最多存储 n 个元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_max_k(nums: List[int]) -> int:
    """
    函数式接口 - 找出自身与对应的负数都在数组中存在的最大正整数 k
    """
    # 创建一个哈希集合
    num_set = set()
    
    # 将数组中的每个元素加入哈希集合
    for num in nums:
        num_set.add(num)
    
    # 初始化结果为 -1
    result = -1
    
    # 遍历数组，检查每个正数的负数是否在哈希集合中
    for num in nums:
        if num > 0 and -num in num_set:
            result = max(result, num)
    
    return result


Solution = create_solution(find_max_k)