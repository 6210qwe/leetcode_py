"""
2243. 计算字符串的数字和 - 给你一个由若干数字（0 - 9）组成的字符串 s ，和一个整数。 如果 s 的长度大于 k ，则可以执行一轮操作。在一轮操作中，需要完成以下工作： 1. 将 s 拆分 成长度为 k 的若干 连续数字组 ，使得前 k 个字符都分在第一组，接下来的 k 个字符都分在第二组，依此类推。注意，最后一个数字组的长度可以小于 k 。 2. 用表示每个数字组中所有数字之和的字符串来 替换 对应的数字组。例如，"346" 会替换为 "13" ，因为 3 + 4 + 6 = 13 。 3. 合并 所有组以形成一个新字符串。如果新字符串的长度大于 k 则重复第一步。 返回在完成所有轮操作后的 s 。 示例 1： 输入：s = "11111222223", k = 3 输出："135" 解释： - 第一轮，将 s 分成："111"、"112"、"222" 和 "23" 。 接着，计算每一组的数字和：1 + 1 + 1 = 3、1 + 1 + 2 = 4、2 + 2 + 2 = 6 和 2 + 3 = 5 。 这样，s 在第一轮之后变成 "3" + "4" + "6" + "5" = "3465" 。 - 第二轮，将 s 分成："346" 和 "5" 。 接着，计算每一组的数字和：3 + 4 + 6 = 13 、5 = 5 。 这样，s 在第二轮之后变成 "13" + "5" = "135" 。 现在，s.length <= k ，所以返回 "135" 作为答案。 示例 2： 输入：s = "00000000", k = 3 输出："000" 解释： 将 "000", "000", and "00". 接着，计算每一组的数字和：0 + 0 + 0 = 0 、0 + 0 + 0 = 0 和 0 + 0 = 0 。 s 变为 "0" + "0" + "0" = "000" ，其长度等于 k ，所以返回 "000" 。 提示： * 1 <= s.length <= 100 * 2 <= k <= 100 * s 仅由数字（0 - 9）组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过不断将字符串按 k 长度分组，并计算每组的数字和，直到字符串长度不大于 k 为止。

算法步骤:
1. 定义一个辅助函数 `sum_digits` 来计算一个字符串中所有数字的和。
2. 使用一个循环，当字符串长度大于 k 时，将其按 k 长度分组，并计算每组的数字和，然后合并这些和形成新的字符串。
3. 重复上述过程，直到字符串长度不大于 k 为止。

关键点:
- 使用切片操作来分组字符串。
- 使用辅助函数来计算每组的数字和。
- 使用循环来处理多轮操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串 s 的长度。最坏情况下，每轮操作都需要遍历整个字符串。
空间复杂度: O(n)，用于存储新的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_digits(group: str) -> str:
    """计算一个字符串中所有数字的和，并返回结果字符串"""
    return str(sum(int(digit) for digit in group))


def solution_function_name(s: str, k: int) -> str:
    """
    函数式接口 - 计算字符串的数字和
    """
    while len(s) > k:
        new_s = ""
        for i in range(0, len(s), k):
            group = s[i:i + k]
            new_s += sum_digits(group)
        s = new_s
    return s


Solution = create_solution(solution_function_name)