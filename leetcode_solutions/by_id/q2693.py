"""
核心思想: 通过贪心算法选择价格最高的物品，直到装满背包。

算法步骤:
1. 将物品按价格从高到低排序。
2. 依次选择价格最高的物品，直到背包容量达到上限。

关键点:
- 使用贪心算法，每次选择当前价格最高的物品。
- 排序的时间复杂度为 O(n log n)，遍历选择的时间复杂度为 O(n)。
"""

