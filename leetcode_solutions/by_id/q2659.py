"""
2595. 奇偶位数 - 给你一个 正 整数 n 。 用 even 表示在 n 的二进制形式（下标从 0 开始）中值为 1 的偶数下标的个数。 用 odd 表示在 n 的二进制形式（下标从 0 开始）中值为 1 的奇数下标的个数。 请注意，在数字的二进制表示中，位下标的顺序 从右到左。 返回整数数组 answer ，其中 answer = [even, odd] 。 示例 1： 输入：n = 50 输出：[1,2] 解释： 50 的二进制表示是 110010。 在下标 1，4，5 对应的值为 1。 示例 2： 输入：n = 2 输出：[0,1] 解释： 2 的二进制表示是 10。 只有下标 1 对应的值为 1。 提示： * 1 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算来统计偶数和奇数下标位上的1的数量。

算法步骤:
1. 初始化两个计数器 even 和 odd 分别用于记录偶数和奇数下标位上的1的数量。
2. 通过位运算逐位检查 n 的每一位，如果是1则根据其下标更新相应的计数器。
3. 返回结果 [even, odd]。

关键点:
- 使用位运算高效地逐位检查 n 的每一位。
- 通过 & 1 操作判断当前位是否为1。
- 通过右移操作遍历每一位。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 因为需要遍历 n 的每一位，而 n 的位数是 log n。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(n: int) -> List[int]:
    """
    函数式接口 - 计算 n 的二进制表示中值为 1 的偶数和奇数下标的个数。
    """
    even, odd = 0, 0
    bit_index = 0
    
    while n > 0:
        if n & 1:
            if bit_index % 2 == 0:
                even += 1
            else:
                odd += 1
        n >>= 1
        bit_index += 1
    
    return [even, odd]

