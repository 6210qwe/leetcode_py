"""
面试题 08.01. 三步问题 - 三步问题。有个小孩正在上楼梯，楼梯有 n 阶台阶，小孩一次可以上 1 阶、2 阶或 3 阶。实现一种方法，计算小孩有多少种上楼梯的方式。结果可能很大，你需要对结果模 1000000007。 示例 1： 输入：n = 3 输出：4 说明：有四种走法 示例 2： 输入：n = 5 输出：13 提示: 1. n 范围在[1, 1000000]之间
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们可以定义一个数组 dp，其中 dp[i] 表示到达第 i 阶台阶的方法数。状态转移方程为 dp[i] = (dp[i-1] + dp[i-2] + dp[i-3]) % 1000000007。

算法步骤:
1. 初始化 dp 数组，dp[0] = 1, dp[1] = 1, dp[2] = 2。
2. 从第 3 阶开始，使用状态转移方程计算 dp[i]。
3. 返回 dp[n]。

关键点:
- 使用动态规划减少重复计算。
- 对结果取模以防止溢出。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 计算小孩有多少种上楼梯的方式。
    """
    if n == 0:
        return 1
    if n == 1:
        return 1
    if n == 2:
        return 2

    MOD = 1000000007
    dp = [0] * (n + 1)
    dp[0], dp[1], dp[2] = 1, 1, 2

    for i in range(3, n + 1):
        dp[i] = (dp[i - 1] + dp[i - 2] + dp[i - 3]) % MOD

    return dp[n]


Solution = create_solution(solution_function_name)