"""
1745. 分割回文串 IV - 给你一个字符串 s ，如果可以将它分割成三个 非空 回文子字符串，那么返回 true ，否则返回 false 。 当一个字符串正着读和反着读是一模一样的，就称其为 回文字符串 。 示例 1： 输入：s = "abcbdd" 输出：true 解释："abcbdd" = "a" + "bcb" + "dd"，三个子字符串都是回文的。 示例 2： 输入：s = "bcbddxy" 输出：false 解释：s 没办法被分割成 3 个回文子字符串。 提示： * 3 <= s.length <= 2000 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划预处理所有可能的回文子串，然后通过双指针检查是否存在三个非空回文子串。

算法步骤:
1. 使用动态规划预处理所有可能的回文子串。
2. 使用双指针检查是否存在三个非空回文子串。

关键点:
- 动态规划预处理回文子串。
- 双指针检查三个非空回文子串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n^2)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> bool:
    """
    函数式接口 - 判断字符串是否可以分割成三个非空回文子字符串
    """
    n = len(s)
    if n < 3:
        return False

    # 动态规划预处理所有可能的回文子串
    dp = [[False] * n for _ in range(n)]
    for i in range(n):
        dp[i][i] = True
    for length in range(2, n + 1):
        for i in range(n - length + 1):
            j = i + length - 1
            if s[i] == s[j]:
                dp[i][j] = (length == 2) or dp[i + 1][j - 1]

    # 使用双指针检查是否存在三个非空回文子串
    for i in range(1, n - 1):
        for j in range(i, n - 1):
            if dp[0][i - 1] and dp[i][j] and dp[j + 1][n - 1]:
                return True
    return False


Solution = create_solution(solution_function_name)