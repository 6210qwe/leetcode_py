"""
2124. 检查是否所有 A 都在 B 之前 - 给你一个 仅 由字符 'a' 和 'b' 组成的字符串 s 。如果字符串中 每个 'a' 都出现在 每个 'b' 之前，返回 true ；否则，返回 false 。 示例 1： 输入：s = "aaabbb" 输出：true 解释： 'a' 位于下标 0、1 和 2 ；而 'b' 位于下标 3、4 和 5 。 因此，每个 'a' 都出现在每个 'b' 之前，所以返回 true 。 示例 2： 输入：s = "abab" 输出：false 解释： 存在一个 'a' 位于下标 2 ，而一个 'b' 位于下标 1 。 因此，不能满足每个 'a' 都出现在每个 'b' 之前，所以返回 false 。 示例 3： 输入：s = "bbb" 输出：true 解释： 不存在 'a' ，因此可以视作每个 'a' 都出现在每个 'b' 之前，所以返回 true 。 提示： * 1 <= s.length <= 100 * s[i] 为 'a' 或 'b'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串，检查是否存在 'b' 出现在 'a' 之前的情况。

算法步骤:
1. 初始化一个标志变量 found_b 为 False。
2. 遍历字符串 s 中的每个字符：
   - 如果当前字符是 'b'，将 found_b 置为 True。
   - 如果当前字符是 'a' 且 found_b 为 True，返回 False。
3. 如果遍历完整个字符串都没有发现 'a' 出现在 'b' 之后的情况，返回 True。

关键点:
- 通过一次遍历即可完成检查，时间复杂度为 O(n)。
- 使用常数空间存储标志变量，空间复杂度为 O(1)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_string(s: str) -> bool:
    """
    检查字符串 s 是否满足所有 'a' 都出现在所有 'b' 之前。
    """
    found_b = False
    for char in s:
        if char == 'b':
            found_b = True
        elif char == 'a' and found_b:
            return False
    return True


Solution = create_solution(check_string)