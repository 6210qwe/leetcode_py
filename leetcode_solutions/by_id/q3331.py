"""
3065. 超过阈值的最少操作数 I - 给你一个下标从 0 开始的整数数组 nums 和一个整数 k 。 一次操作中，你可以删除 nums 中的最小元素。 你需要使数组中的所有元素都大于或等于 k ，请你返回需要的 最少 操作次数。 示例 1： 输入：nums = [2,11,10,1,3], k = 10 输出：3 解释：第一次操作后，nums 变为 [2, 11, 10, 3] 。 第二次操作后，nums 变为 [11, 10, 3] 。 第三次操作后，nums 变为 [11, 10] 。 此时，数组中的所有元素都大于等于 10 ，所以我们停止操作。 使数组中所有元素都大于等于 10 需要的最少操作次数为 3 。 示例 2： 输入：nums = [1,1,2,4,9], k = 1 输出：0 解释：数组中的所有元素都大于等于 1 ，所以不需要对 nums 做任何操作。 示例 3： 输入：nums = [1,1,2,4,9], k = 9 输出：4 解释：nums 中只有一个元素大于等于 9 ，所以需要执行 4 次操作。 提示： * 1 <= nums.length <= 50 * 1 <= nums[i] <= 109 * 1 <= k <= 109 * 输入保证至少有一个满足 nums[i] >= k 的下标 i 存在。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对数组进行排序，然后遍历数组找到第一个大于等于 k 的元素的位置，该位置之前的元素数量即为需要删除的元素数量。

算法步骤:
1. 对数组进行排序。
2. 遍历排序后的数组，找到第一个大于等于 k 的元素的位置。
3. 返回该位置之前的元素数量作为结果。

关键点:
- 排序后可以快速找到需要删除的元素数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 其中 n 是数组的长度，排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1) - 除了输入数组外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_operations_to_exceed_threshold(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算使数组中所有元素都大于等于 k 所需的最少操作次数
    """
    # 对数组进行排序
    nums.sort()
    
    # 遍历排序后的数组，找到第一个大于等于 k 的元素的位置
    for i, num in enumerate(nums):
        if num >= k:
            return i
    
    # 如果没有找到大于等于 k 的元素，返回数组长度
    return len(nums)


Solution = create_solution(minimum_operations_to_exceed_threshold)