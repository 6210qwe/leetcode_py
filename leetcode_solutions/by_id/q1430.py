# ============================================================================
# 问题描述
# ============================================================================
"""
2269. 找到一个数字的 K 美丽值 - 一个整数 num 的 k 美丽值定义为 num 中符合以下条件的 子字符串 数目：
* 子字符串长度为 k 。
* 子字符串能整除 num 。
给你整数 num 和 k ，请你返回 num 的 k 美丽值。
注意：
* 允许有 前缀 0 。
* 0 不能整除任何值。
一个 子字符串 是一个字符串里的连续一段字符序列。

示例 1：
输入：num = 240, k = 2
输出：2
解释：以下是 num 里长度为 k 的子字符串：
- "240" 中的 "24" ：24 能整除 240 。
- "240" 中的 "40" ：40 能整除 240 。
所以，k 美丽值为 2 。

示例 2：
输入：num = 430043, k = 2
输出：2
解释：以下是 num 里长度为 k 的子字符串：
- "430043" 中的 "43" ：43 能整除 430043 。
- "430043" 中的 "30" ：30 不能整除 430043 。
- "430043" 中的 "00" ：0 不能整除 430043 。
- "430043" 中的 "04" ：4 不能整除 430043 。
- "430043" 中的 "43" ：43 能整除 430043 。
所以，k 美丽值为 2 。

提示：
* 1 <= num <= 10^9
* 1 <= k <= num.length （将 num 视为字符串）
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口遍历 num 的所有长度为 k 的子字符串，并检查这些子字符串是否能整除 num。

算法步骤:
1. 将 num 转换为字符串，以便于处理子字符串。
2. 初始化一个计数器来记录 k 美丽值。
3. 使用滑动窗口遍历 num 的所有长度为 k 的子字符串。
4. 对于每个子字符串，将其转换为整数并检查是否能整除 num。
5. 如果能整除，则增加计数器。
6. 返回计数器的值。

关键点:
- 使用滑动窗口可以高效地遍历所有长度为 k 的子字符串。
- 注意处理前缀 0 和 0 不能整除的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 num 的长度。我们只需要遍历一次 num 的所有长度为 k 的子字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_k_beauty(num: int, k: int) -> int:
    """
    函数式接口 - 计算 num 的 k 美丽值
    """
    num_str = str(num)
    n = len(num_str)
    count = 0
    
    for i in range(n - k + 1):
        substring = num_str[i:i + k]
        sub_num = int(substring)
        if sub_num != 0 and num % sub_num == 0:
            count += 1
    
    return count


Solution = create_solution(find_k_beauty)