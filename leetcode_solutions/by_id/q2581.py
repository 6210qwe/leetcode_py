"""
2491. 划分技能点相等的团队 - 给你一个正整数数组 skill ，数组长度为 偶数 n ，其中 skill[i] 表示第 i 个玩家的技能点。将所有玩家分成 n / 2 个 2 人团队，使每一个团队的技能点之和 相等 。 团队的 化学反应 等于团队中玩家的技能点 乘积 。 返回所有团队的 化学反应 之和，如果无法使每个团队的技能点之和相等，则返回 -1 。 示例 1： 输入：skill = [3,2,5,1,3,4] 输出：22 解释： 将玩家分成 3 个团队 (1, 5), (2, 4), (3, 3) ，每个团队的技能点之和都是 6 。 所有团队的化学反应之和是 1 * 5 + 2 * 4 + 3 * 3 = 5 + 8 + 9 = 22 。 示例 2： 输入：skill = [3,4] 输出：12 解释： 两个玩家形成一个团队，技能点之和是 7 。 团队的化学反应是 3 * 4 = 12 。 示例 3： 输入：skill = [1,1,2,3] 输出：-1 解释： 无法将玩家分成每个团队技能点都相等的若干个 2 人团队。 提示： * 2 <= skill.length <= 105 * skill.length 是偶数 * 1 <= skill[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和双指针来找到每对技能点之和相等的组合。

算法步骤:
1. 计算所有技能点的总和，并确定每个团队的技能点目标值。
2. 对技能点数组进行排序。
3. 使用双指针从两端向中间遍历，检查每对技能点之和是否等于目标值。
4. 如果所有对的技能点之和都相等，则计算并返回化学反应之和；否则返回 -1。

关键点:
- 通过排序和双指针确保每对技能点之和相等。
- 检查每对技能点之和是否等于目标值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def divide_players(skill: List[int]) -> int:
    """
    函数式接口 - 将玩家分成技能点之和相等的团队，并返回所有团队的化学反应之和。
    """
    n = len(skill)
    if n % 2 != 0:
        return -1
    
    total_skill = sum(skill)
    target_sum = total_skill // (n // 2)
    
    skill.sort()
    
    left, right = 0, n - 1
    chemistry_sum = 0
    
    while left < right:
        if skill[left] + skill[right] != target_sum:
            return -1
        chemistry_sum += skill[left] * skill[right]
        left += 1
        right -= 1
    
    return chemistry_sum


Solution = create_solution(divide_players)