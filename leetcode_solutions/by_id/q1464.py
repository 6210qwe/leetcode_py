"""
1338. 数组大小减半 - 给你一个整数数组 arr。你可以从中选出一个整数集合，并删除这些整数在数组中的每次出现。 返回 至少 能删除数组中的一半整数的整数集合的最小大小。 示例 1： 输入：arr = [3,3,3,3,5,5,5,2,2,7] 输出：2 解释：选择 {3,7} 使得结果数组为 [5,5,5,2,2]、长度为 5（原数组长度的一半）。 大小为 2 的可行集合有 {3,5},{3,2},{5,2}。 选择 {2,7} 是不可行的，它的结果数组为 [3,3,3,3,5,5,5]，新数组长度大于原数组的二分之一。 示例 2： 输入：arr = [7,7,7,7,7,7] 输出：1 解释：我们只能选择集合 {7}，结果数组为空。 提示： * 1 <= arr.length <= 105 * arr.length 为偶数 * 1 <= arr[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和计数器来找到最少的整数集合，使得删除这些整数后数组长度减半。

算法步骤:
1. 使用 Counter 统计每个元素的频率。
2. 将频率从高到低排序。
3. 依次选择频率最高的元素，直到删除的元素数量达到数组长度的一半。

关键点:
- 使用 Counter 进行频率统计。
- 按频率从高到低排序，优先选择频率高的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，使用了额外的存储空间来存储频率。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import Counter

def solution_function_name(arr: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 统计每个元素的频率
    freq = Counter(arr)
    
    # 按频率从高到低排序
    sorted_freq = sorted(freq.values(), reverse=True)
    
    # 计算需要删除的元素数量
    target = len(arr) // 2
    count = 0
    result = 0
    
    for f in sorted_freq:
        if count >= target:
            break
        count += f
        result += 1
    
    return result

