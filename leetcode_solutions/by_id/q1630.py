"""
1523. 在区间范围内统计奇数数目 - 给你两个非负整数 low 和 high 。请你返回 low 和 high 之间（包括二者）奇数的数目。 示例 1： 输入：low = 3, high = 7 输出：3 解释：3 到 7 之间奇数数字为 [3,5,7] 。 示例 2： 输入：low = 8, high = 10 输出：1 解释：8 到 10 之间奇数数字为 [9] 。 提示： * 0 <= low <= high <= 10^9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过数学公式直接计算区间内的奇数个数。

算法步骤:
1. 计算区间内所有数字的总数。
2. 如果 low 是奇数，则将总数加 1。
3. 返回总数的一半，即为奇数的个数。

关键点:
- 通过数学公式避免遍历区间内的每个数字，从而提高效率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_odds(low: int, high: int) -> int:
    """
    函数式接口 - 计算给定区间内的奇数个数
    """
    # 计算区间内所有数字的总数
    total_numbers = high - low + 1
    
    # 如果 low 是奇数，则将总数加 1
    if low % 2 == 1:
        total_numbers += 1
    
    # 返回总数的一半，即为奇数的个数
    return total_numbers // 2


Solution = create_solution(count_odds)