# ============================================================================
# 问题描述
# ============================================================================
"""
985. 查询后的偶数和 - 给出一个整数数组 A 和一个查询数组 queries。 对于第 i 次查询，有 val = queries[i][0], index = queries[i][1]，我们会把 val 加到 A[index] 上。然后，第 i 次查询的答案是 A 中偶数值的和。 （此处给定的 index = queries[i][1] 是从 0 开始的索引，每次查询都会永久修改数组 A。） 返回所有查询的答案。你的答案应当以数组 answer 给出，answer[i] 为第 i 次查询的答案。 示例： 输入：A = [1,2,3,4], queries = [[1,0],[-3,1],[-4,0],[2,3]] 输出：[8,6,2,4] 解释： 开始时，数组为 [1,2,3,4]。 将 1 加到 A[0] 上之后，数组为 [2,2,3,4]，偶数值之和为 2 + 2 + 4 = 8。 将 -3 加到 A[1] 上之后，数组为 [2,-1,3,4]，偶数值之和为 2 + 4 = 6。 将 -4 加到 A[0] 上之后，数组为 [-2,-1,3,4]，偶数值之和为 -2 + 4 = 2。 将 2 加到 A[3] 上之后，数组为 [-2,-1,3,6]，偶数值之和为 -2 + 6 = 4。 提示： 1. 1 <= A.length <= 10000 2. -10000 <= A[i] <= 10000 3. 1 <= queries.length <= 10000 4. -10000 <= queries[i][0] <= 10000 5. 0 <= queries[i][1] < A.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过预先计算初始的偶数和，并在每次查询后更新偶数和，避免重复计算。

算法步骤:
1. 计算初始数组 A 中所有偶数的和。
2. 对于每个查询，根据查询值和目标索引更新数组 A，并更新偶数和。
3. 如果原来的值是偶数，则从偶数和中减去；如果新的值是偶数，则加到偶数和中。
4. 将每次查询后的偶数和记录到结果数组中。

关键点:
- 预先计算初始偶数和，减少重复计算。
- 动态更新偶数和，避免每次都重新计算整个数组的偶数和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是数组 A 的长度，m 是查询数组 queries 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_even_after_queries(nums: List[int], queries: List[List[int]]) -> List[int]:
    """
    函数式接口 - 根据查询数组 queries 更新数组 nums，并返回每次查询后的偶数和。
    """
    # 计算初始偶数和
    even_sum = sum(num for num in nums if num % 2 == 0)
    
    # 结果数组
    result = []
    
    # 处理每个查询
    for val, index in queries:
        old_val = nums[index]
        new_val = old_val + val
        
        # 更新数组
        nums[index] = new_val
        
        # 更新偶数和
        if old_val % 2 == 0:
            even_sum -= old_val
        if new_val % 2 == 0:
            even_sum += new_val
        
        # 记录当前查询后的偶数和
        result.append(even_sum)
    
    return result


Solution = create_solution(sum_even_after_queries)