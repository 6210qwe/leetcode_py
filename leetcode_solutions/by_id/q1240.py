"""
1140. 石子游戏 II - Alice 和 Bob 继续他们的石子游戏。许多堆石子 排成一行，每堆都有正整数颗石子 piles[i]。游戏以谁手中的石子最多来决出胜负。 Alice 和 Bob 轮流进行，Alice 先开始。最初，M = 1。 在每个玩家的回合中，该玩家可以拿走剩下的 前 X 堆的所有石子，其中 1 <= X <= 2M。然后，令 M = max(M, X)。 游戏一直持续到所有石子都被拿走。 假设 Alice 和 Bob 都发挥出最佳水平，返回 Alice 可以得到的最大数量的石头。 示例 1： 输入：piles = [2,7,9,4,4] 输出：10 解释：如果一开始 Alice 取了一堆，Bob 取了两堆，然后 Alice 再取两堆。Alice 可以得到 2 + 4 + 4 = 10 堆。 如果 Alice 一开始拿走了两堆，那么 Bob 可以拿走剩下的三堆。在这种情况下，Alice 得到 2 + 7 = 9 堆。返回 10，因为它更大。 示例 2: 输入：piles = [1,2,3,4,5,100] 输出：104 提示： * 1 <= piles.length <= 100 * 1 <= piles[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。定义 dp[i][m] 表示从第 i 堆石子开始，当前 M 为 m 时，当前玩家可以获得的最大石子数。

算法步骤:
1. 计算前缀和数组，方便快速计算任意区间的石子总数。
2. 初始化 dp 数组，dp[i][m] 表示从第 i 堆石子开始，当前 M 为 m 时，当前玩家可以获得的最大石子数。
3. 从后向前遍历每一堆石子，对于每一个可能的 M 值，计算当前玩家可以获得的最大石子数。
4. 返回 dp[0][1]，即从第 0 堆石子开始，初始 M 为 1 时，Alice 可以获得的最大石子数。

关键点:
- 使用前缀和数组优化区间求和操作。
- 动态规划的状态转移方程为 dp[i][m] = max(dp[i][m], prefix_sum[i] - dp[j][max(m, x)] for x in range(1, 2 * m + 1))。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3)，其中 n 是 piles 的长度。因为我们需要遍历所有可能的 (i, m) 对，并且在每个 (i, m) 对中需要遍历最多 2 * m 次。
空间复杂度: O(n^2)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def stone_game_ii(piles: List[int]) -> int:
    n = len(piles)
    if n == 0:
        return 0

    # 计算前缀和数组
    prefix_sum = [0] * (n + 1)
    for i in range(n):
        prefix_sum[i + 1] = prefix_sum[i] + piles[i]

    # 初始化 dp 数组
    dp = [[0] * (n + 1) for _ in range(n)]

    # 从后向前遍历每一堆石子
    for i in range(n - 1, -1, -1):
        for m in range(1, n + 1):
            for x in range(1, min(2 * m, n - i) + 1):
                j = i + x
                dp[i][m] = max(dp[i][m], prefix_sum[j] - prefix_sum[i] - dp[j][max(m, x)])

    return dp[0][1]


Solution = create_solution(stone_game_ii)