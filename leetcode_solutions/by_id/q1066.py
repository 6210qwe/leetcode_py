# ============================================================================
# 问题描述
# ============================================================================
"""
给定一个递增数组 A，如果 i 满足 A[i] == i，则称 i 为数组 A 的不动点。返回数组 A 中的最小不动点。
如果没有不动点，返回 -1。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来寻找最小的不动点。

算法步骤:
1. 初始化左右指针 left 和 right，分别指向数组的起始和末尾。
2. 进行二分查找：
   - 计算中间位置 mid。
   - 如果 A[mid] == mid，则更新结果 res 并继续在左半部分查找更小的不动点。
   - 如果 A[mid] < mid，则在右半部分查找。
   - 如果 A[mid] > mid，则在左半部分查找。
3. 返回结果 res，如果没有找到不动点则返回 -1。

关键点:
- 使用二分查找来优化查找过程，时间复杂度为 O(log n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)，其中 n 是数组的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(A: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    left, right = 0, len(A) - 1
    res = -1
    
    while left <= right:
        mid = (left + right) // 2
        if A[mid] == mid:
            res = mid
            right = mid - 1  # 继续在左半部分查找更小的不动点
        elif A[mid] < mid:
            left = mid + 1
        else:
            right = mid - 1
    
    return res


Solution = create_solution(solution_function_name)