"""
核心思想: 检查给定数字的所有前缀和后缀是否都是质数。

算法步骤:
1. 将数字转换为字符串，以便于生成前缀和后缀。
2. 检查每个前缀和后缀是否是质数。
3. 如果所有前缀和后缀都是质数，则返回 True，否则返回 False。

关键点:
- 使用高效的质数检查方法。
- 生成前缀和后缀时，避免重复计算。
"""

