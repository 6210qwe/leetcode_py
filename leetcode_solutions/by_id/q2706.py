from typing import List

def min_score(nums: List[int]) -> int:
    """
    函数式接口 - 返回修改 nums 中至多两个元素的值后，可以得到的最小分数。
    """
    # 对数组进行排序
    nums.sort()
    
    # 计算前三个元素之间的差值
    min_diff1 = nums[2] - nums[0]
    
    # 计算最后三个元素之间的差值
    min_diff2 = nums[-1] - nums[-3]
    
    # 返回这些差值中的最小值
    return min(min_diff1, min_diff2)

