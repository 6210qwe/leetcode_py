"""
1414. 和为 K 的最少斐波那契数字数目 - 给你数字 k ，请你返回和为 k 的斐波那契数字的最少数目，其中，每个斐波那契数字都可以被使用多次。 斐波那契数字定义为： * F1 = 1 * F2 = 1 * Fn = Fn-1 + Fn-2 ， 其中 n > 2 。 数据保证对于给定的 k ，一定能找到可行解。 示例 1： 输入：k = 7 输出：2 解释：斐波那契数字为：1，1，2，3，5，8，13，…… 对于 k = 7 ，我们可以得到 2 + 5 = 7 。 示例 2： 输入：k = 10 输出：2 解释：对于 k = 10 ，我们可以得到 2 + 8 = 10 。 示例 3： 输入：k = 19 输出：3 解释：对于 k = 19 ，我们可以得到 1 + 5 + 13 = 19 。 提示： * 1 <= k <= 10^9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从最大的斐波那契数开始减去，直到 k 为 0。

算法步骤:
1. 生成所有小于等于 k 的斐波那契数。
2. 从最大的斐波那契数开始，尽可能多地减去该数，更新 k。
3. 重复步骤 2，直到 k 为 0。

关键点:
- 生成斐波那契数列时，只需生成到大于 k 的第一个数即可。
- 每次选择当前最大的斐波那契数进行减法操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log k)
空间复杂度: O(log k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_min_fibonacci_numbers(k: int) -> int:
    """
    函数式接口 - 返回和为 k 的斐波那契数字的最少数目
    """
    # 生成所有小于等于 k 的斐波那契数
    fibs = []
    a, b = 1, 1
    while a <= k:
        fibs.append(a)
        a, b = b, a + b
    
    # 从最大的斐波那契数开始，尽可能多地减去该数，更新 k
    count = 0
    for fib in reversed(fibs):
        if k >= fib:
            k -= fib
            count += 1
        if k == 0:
            break
    
    return count


Solution = create_solution(find_min_fibonacci_numbers)