"""
1799. N 次操作后的最大分数和 - 给你 nums ，它是一个大小为 2 * n 的正整数数组。你必须对这个数组执行 n 次操作。 在第 i 次操作时（操作编号从 1 开始），你需要： * 选择两个元素 x 和 y 。 * 获得分数 i * gcd(x, y) 。 * 将 x 和 y 从 nums 中删除。 请你返回 n 次操作后你能获得的分数和最大为多少。 函数 gcd(x, y) 是 x 和 y 的最大公约数。 示例 1： 输入：nums = [1,2] 输出：1 解释：最优操作是： (1 * gcd(1, 2)) = 1 示例 2： 输入：nums = [3,4,6,8] 输出：11 解释：最优操作是： (1 * gcd(3, 6)) + (2 * gcd(4, 8)) = 3 + 8 = 11 示例 3： 输入：nums = [1,2,3,4,5,6] 输出：14 解释：最优操作是： (1 * gcd(1, 5)) + (2 * gcd(2, 4)) + (3 * gcd(3, 6)) = 1 + 4 + 9 = 14 提示： * 1 <= n <= 7 * nums.length == 2 * n * 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和状态压缩来解决这个问题。

算法步骤:
1. 计算所有可能的数字对的最大公约数，并存储在一个二维数组中。
2. 使用一个递归函数来计算在当前状态下，剩余操作次数所能获得的最大分数。
3. 通过状态压缩技术，用一个整数表示哪些数字已经被使用。
4. 递归地尝试所有可能的数字对组合，并更新最大分数。

关键点:
- 使用状态压缩来表示已经使用的数字。
- 通过缓存递归结果来避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n * n^2)
空间复杂度: O(2^n * n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
from math import gcd
from functools import lru_cache

def maxScore(nums: List[int]) -> int:
    n = len(nums) // 2
    gcds = [[gcd(nums[i], nums[j]) for j in range(i + 1, 2 * n)] for i in range(2 * n)]
    
    @lru_cache(None)
    def dp(state, op):
        if op > n:
            return 0
        max_score = 0
        for i in range(2 * n):
            if state & (1 << i) == 0:
                for j in range(i + 1, 2 * n):
                    if state & (1 << j) == 0:
                        new_state = state | (1 << i) | (1 << j)
                        score = op * gcds[i][j - (i + 1)] + dp(new_state, op + 1)
                        max_score = max(max_score, score)
        return max_score
    
    return dp(0, 1)

Solution = create_solution(maxScore)