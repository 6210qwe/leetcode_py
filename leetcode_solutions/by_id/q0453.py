def minimum_moves_to_equal_array_elements(nums: List[int]) -> int:
    """
    函数式接口 - 最小操作次数使数组元素相等
    
    实现思路:
    每次让n-1个元素加1，等价于让1个元素减1。
    所以答案是所有元素与最小值的差之和。
    
    Args:
        nums: 整数数组
        
    Returns:
        最小操作次数
        
    Example:
        >>> minimum_moves_to_equal_array_elements([1,2,3])
        3
    """
    min_val = min(nums)
    return sum(nums) - min_val * len(nums)


