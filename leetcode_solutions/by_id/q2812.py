"""
2769. 找出最大的可达成数字 - 给你两个整数 num 和 t 。如果整数 x 可以在执行下述操作 不超过 t 次的情况下变为与 num 相等，则称其为 可达成数字 ： * 每次操作将 x 的值增加或减少 1 ，同时可以选择将 num 的值增加或减少 1 。 返回所有可达成数字中的 最大 值 x。 示例 1： 输入：num = 4, t = 1 输出：6 解释： 执行下述操作可以使最大可达成数字等于 num ： * 最大可达成数字减少 1 ，同时 num 增加 1 。 示例 2： 输入：num = 3, t = 2 输出：7 解释： 执行两次下述操作可以使最大可达成数字等于 num ： * 最大可达成数字减少 1 ，同时 num 增加 1。 提示： * 1 <= num, t <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过每次操作将 x 的值增加或减少 1，同时可以选择将 num 的值增加或减少 1，我们可以计算出 x 的最大值。

算法步骤:
1. 计算 x 的最大值，即 num + 2 * t。
2. 返回计算结果。

关键点:
- 每次操作可以将 x 的值增加 1，并且可以将 num 的值增加 1，因此 x 的最大值为 num + 2 * t。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 只需要常数时间进行计算。
空间复杂度: O(1) - 只需要常数空间存储结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: int, t: int) -> int:
    """
    函数式接口 - 计算并返回最大可达成数字 x。
    """
    # 计算 x 的最大值
    max_achievable_number = num + 2 * t
    return max_achievable_number


