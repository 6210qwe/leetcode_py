"""
LCR 187. 破冰游戏 - 社团共有 num 位成员参与破冰游戏，编号为 0 ~ num-1。成员们按照编号顺序围绕圆桌而坐。社长抽取一个数字 target，从 0 号成员起开始计数，排在第 target 位的成员离开圆桌，且成员离开后从下一个成员开始计数。请返回游戏结束时最后一位成员的编号。 示例 1： 输入：num = 7, target = 4 输出：1 示例 2： 输入：num = 12, target = 5 输出：0 提示： * 1 <= num <= 10^5 * 1 <= target <= 10^6
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用约瑟夫环问题的数学解法

算法步骤:
1. 定义一个递归函数 f(n, m)，表示 n 个人玩游戏，每次删除第 m 个人，最后剩下的那个人的编号。
2. 递归终止条件：当 n 为 1 时，返回 0。
3. 递归公式：f(n, m) = (f(n-1, m) + m) % n。

关键点:
- 递归公式推导：假设 f(n-1, m) 的结果是 x，则对于 n 个人的情况，x 会移动 m 个位置，再对 n 取模。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: int, target: int) -> int:
    """
    函数式接口 - 使用约瑟夫环问题的数学解法
    """
    def f(n: int, m: int) -> int:
        if n == 1:
            return 0
        return (f(n - 1, m) + m) % n

    return f(num, target)


Solution = create_solution(solution_function_name)