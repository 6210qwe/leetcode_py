"""
1637. 两点之间不包含任何点的最宽垂直区域 - 给你 n 个二维平面上的点 points ，其中 points[i] = [xi, yi] ，请你返回两点之间内部不包含任何点的 最宽垂直区域 的宽度。 垂直区域 的定义是固定宽度，而 y 轴上无限延伸的一块区域（也就是高度为无穷大）。 最宽垂直区域 为宽度最大的一个垂直区域。 请注意，垂直区域 边上 的点 不在 区域内。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/10/31/points3.png] 输入：points = [[8,7],[9,9],[7,4],[9,7]] 输出：1 解释：红色区域和蓝色区域都是最优区域。 示例 2： 输入：points = [[3,1],[9,0],[1,0],[1,4],[5,3],[8,8]] 输出：3 提示： * n == points.length * 2 <= n <= 105 * points[i].length == 2 * 0 <= xi, yi <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对点按 x 坐标进行排序，然后遍历排序后的点，计算相邻点之间的 x 坐标差值，找到最大的差值。

算法步骤:
1. 按 x 坐标对点进行排序。
2. 遍历排序后的点，计算相邻点之间的 x 坐标差值。
3. 找到最大的 x 坐标差值，即为最宽垂直区域的宽度。

关键点:
- 排序后只需比较相邻点的 x 坐标差值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 主要由排序操作决定。
空间复杂度: O(1) - 除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def widest_vertical_area(points: List[List[int]]) -> int:
    """
    函数式接口 - 计算两点之间不包含任何点的最宽垂直区域的宽度
    """
    # 按 x 坐标对点进行排序
    points.sort(key=lambda p: p[0])
    
    # 初始化最大宽度
    max_width = 0
    
    # 遍历排序后的点，计算相邻点之间的 x 坐标差值
    for i in range(1, len(points)):
        width = points[i][0] - points[i - 1][0]
        max_width = max(max_width, width)
    
    return max_width


Solution = create_solution(widest_vertical_area)