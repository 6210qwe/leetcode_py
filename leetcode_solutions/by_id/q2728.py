"""
2679. 矩阵中的和 - 给你一个下标从 0 开始的二维整数数组 nums 。一开始你的分数为 0 。你需要执行以下操作直到矩阵变为空： 1. 矩阵中每一行选取最大的一个数，并删除它。如果一行中有多个最大的数，选择任意一个并删除。 2. 在步骤 1 删除的所有数字中找到最大的一个数字，将它添加到你的 分数 中。 请你返回最后的 分数 。 示例 1： 输入：nums = [[7,2,1],[6,4,2],[6,5,3],[3,2,1]] 输出：15 解释：第一步操作中，我们删除 7 ，6 ，6 和 3 ，将分数增加 7 。下一步操作中，删除 2 ，4 ，5 和 2 ，将分数增加 5 。最后删除 1 ，2 ，3 和 1 ，将分数增加 3 。所以总得分为 7 + 5 + 3 = 15 。 示例 2： 输入：nums = [[1]] 输出：1 解释：我们删除 1 并将分数增加 1 ，所以返回 1 。 提示： * 1 <= nums.length <= 300 * 1 <= nums[i].length <= 500 * 0 <= nums[i][j] <= 103
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 每次从每一行中选择最大的数，然后在这些数中选择最大的数加入分数。重复这个过程直到矩阵为空。

算法步骤:
1. 对每一行进行降序排序。
2. 逐列遍历矩阵，每次选择当前列中最大的数加入分数。
3. 重复步骤2直到所有列都被处理完。

关键点:
- 通过预先对每一行进行降序排序，可以简化后续的选择过程。
- 每次选择当前列中最大的数，确保每一步都选择了当前最优解。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n * log(n))，其中 m 是矩阵的行数，n 是矩阵的列数。每一行的排序时间复杂度是 O(n * log(n))，共需要对 m 行进行排序。
空间复杂度: O(1)，除了输入和输出外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[List[int]]) -> int:
    """
    函数式接口 - 计算矩阵中的和
    """
    # 对每一行进行降序排序
    for row in nums:
        row.sort(reverse=True)
    
    score = 0
    # 逐列遍历矩阵
    for col in range(len(nums[0])):
        max_val = 0
        for row in nums:
            max_val = max(max_val, row[col])
        score += max_val
    
    return score


Solution = create_solution(solution_function_name)