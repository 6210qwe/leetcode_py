"""
172. 阶乘后的零 - 给定一个整数 n ，返回 n! 结果中尾随零的数量。 提示 n! = n * (n - 1) * (n - 2) * ... * 3 * 2 * 1 示例 1： 输入：n = 3 输出：0 解释：3! = 6 ，不含尾随 0 示例 2： 输入：n = 5 输出：1 解释：5! = 120 ，有一个尾随 0 示例 3： 输入：n = 0 输出：0 提示： * 0 <= n <= 104 进阶：你可以设计并实现对数时间复杂度的算法来解决此问题吗？
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 尾随零由因子10产生，10=2×5，统计因子5的个数（2的个数总是更多）

算法步骤:
1. 尾随零的数量等于因子5的个数
2. 统计1到n中所有5的倍数、25的倍数、125的倍数...
3. 累加这些数量

关键点:
- 因子5的个数决定尾随零的数量
- 时间复杂度O(logn)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(logn) - 以5为底的对数
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def factorial_trailing_zeroes(n: int) -> int:
    """
    函数式接口 - 计算阶乘后的零
    
    实现思路:
    尾随零由因子10产生，统计因子5的个数。
    
    Args:
        n: 正整数
        
    Returns:
        n!中尾随零的数量
        
    Example:
        >>> factorial_trailing_zeroes(5)
        1
    """
    count = 0
    
    while n > 0:
        n //= 5
        count += n
    
    return count


