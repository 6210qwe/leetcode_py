"""
412. Fizz Buzz - 给你一个整数 n ，返回一个字符串数组 answer（下标从 1 开始），其中： * answer[i] == "FizzBuzz" 如果 i 同时是 3 和 5 的倍数。 * answer[i] == "Fizz" 如果 i 是 3 的倍数。 * answer[i] == "Buzz" 如果 i 是 5 的倍数。 * answer[i] == i （以字符串形式）如果上述条件全不满足。 示例 1： 输入：n = 3 输出：["1","2","Fizz"] 示例 2： 输入：n = 5 输出：["1","2","Fizz","4","Buzz"] 示例 3： 输入：n = 15 输出：["1","2","Fizz","4","Buzz","Fizz","7","8","Fizz","Buzz","11","Fizz","13","14","FizzBuzz"] 提示： * 1 <= n <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历1到n，根据是否能被3和5整除返回对应字符串

算法步骤:
1. 遍历1到n
2. 如果i同时是3和5的倍数，返回"FizzBuzz"
3. 如果i是3的倍数，返回"Fizz"
4. 如果i是5的倍数，返回"Buzz"
5. 否则返回i的字符串形式

关键点:
- 注意判断顺序：先判断3和5的倍数
- 时间复杂度O(n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历1到n
空间复杂度: O(n) - 结果数组空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def fizz_buzz(n: int) -> List[str]:
    """
    函数式接口 - Fizz Buzz
    
    实现思路:
    遍历1到n，根据是否能被3和5整除返回对应字符串。
    
    Args:
        n: 整数
        
    Returns:
        字符串数组
        
    Example:
        >>> fizz_buzz(3)
        ['1', '2', 'Fizz']
    """
    result = []
    for i in range(1, n + 1):
        if i % 15 == 0:
            result.append("FizzBuzz")
        elif i % 3 == 0:
            result.append("Fizz")
        elif i % 5 == 0:
            result.append("Buzz")
        else:
            result.append(str(i))
    return result


