"""
2126. 摧毁小行星 - 给你一个整数 mass ，它表示一颗行星的初始质量。再给你一个整数数组 asteroids ，其中 asteroids[i] 是第 i 颗小行星的质量。 你可以按 任意顺序 重新安排小行星的顺序，然后让行星跟它们发生碰撞。如果行星碰撞时的质量 大于等于 小行星的质量，那么小行星被 摧毁 ，并且行星会 获得 这颗小行星的质量。否则，行星将被摧毁。 如果所有小行星 都 能被摧毁，请返回 true ，否则返回 false 。 示例 1： 输入：mass = 10, asteroids = [3,9,19,5,21] 输出：true 解释：一种安排小行星的方式为 [9,19,5,3,21] ： - 行星与质量为 9 的小行星碰撞。新的行星质量为：10 + 9 = 19 - 行星与质量为 19 的小行星碰撞。新的行星质量为：19 + 19 = 38 - 行星与质量为 5 的小行星碰撞。新的行星质量为：38 + 5 = 43 - 行星与质量为 3 的小行星碰撞。新的行星质量为：43 + 3 = 46 - 行星与质量为 21 的小行星碰撞。新的行星质量为：46 + 21 = 67 所有小行星都被摧毁。 示例 2： 输入：mass = 5, asteroids = [4,9,23,4] 输出：false 解释： 行星无论如何没法获得足够质量去摧毁质量为 23 的小行星。 行星把别的小行星摧毁后，质量为 5 + 4 + 9 + 4 = 22 。 它比 23 小，所以无法摧毁最后一颗小行星。 提示： * 1 <= mass <= 105 * 1 <= asteroids.length <= 105 * 1 <= asteroids[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，先从小行星中选择质量最小的进行碰撞，逐步增加行星的质量。

算法步骤:
1. 对小行星质量数组进行排序。
2. 初始化行星质量为给定的初始质量。
3. 遍历排序后的小行星数组，逐一尝试摧毁小行星：
   - 如果当前行星质量大于等于小行星质量，则摧毁小行星，并增加行星质量。
   - 否则，返回 False，表示无法摧毁所有小行星。
4. 如果遍历完所有小行星，返回 True。

关键点:
- 排序后的小行星数组确保每次碰撞都能尽可能多地增加行星质量。
- 通过贪心策略，确保行星质量增长最大化。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是小行星的数量。排序操作的时间复杂度是 O(n log n)，后续遍历操作是 O(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_destroy_asteroids(mass: int, asteroids: List[int]) -> bool:
    """
    判断是否可以摧毁所有小行星。
    
    :param mass: 行星的初始质量。
    :param asteroids: 小行星的质量数组。
    :return: 如果可以摧毁所有小行星，返回 True；否则返回 False。
    """
    # 对小行星质量数组进行排序
    asteroids.sort()
    
    # 遍历排序后的小行星数组
    for asteroid in asteroids:
        if mass >= asteroid:
            # 如果当前行星质量大于等于小行星质量，则摧毁小行星，并增加行星质量
            mass += asteroid
        else:
            # 否则，返回 False，表示无法摧毁所有小行星
            return False
    
    # 如果遍历完所有小行星，返回 True
    return True


Solution = create_solution(can_destroy_asteroids)