"""
2807. 在链表中插入最大公约数 - 给你一个链表的头 head ，每个结点包含一个整数值。 在相邻结点之间，请你插入一个新的结点，结点值为这两个相邻结点值的 最大公约数 。 请你返回插入之后的链表。 两个数的 最大公约数 是可以被两个数字整除的最大正整数。 示例 1： [https://assets.leetcode.com/uploads/2023/07/18/ex1_copy.png] 输入：head = [18,6,10,3] 输出：[18,6,6,2,10,1,3] 解释：第一幅图是一开始的链表，第二幅图是插入新结点后的图（蓝色结点为新插入结点）。 - 18 和 6 的最大公约数为 6 ，插入第一和第二个结点之间。 - 6 和 10 的最大公约数为 2 ，插入第二和第三个结点之间。 - 10 和 3 的最大公约数为 1 ，插入第三和第四个结点之间。 所有相邻结点之间都插入完毕，返回链表。 示例 2： [https://assets.leetcode.com/uploads/2023/07/18/ex2_copy1.png] 输入：head = [7] 输出：[7] 解释：第一幅图是一开始的链表，第二幅图是插入新结点后的图（蓝色结点为新插入结点）。 没有相邻结点，所以返回初始链表。 提示： * 链表中结点数目在 [1, 5000] 之间。 * 1 <= Node.val <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历链表，在每对相邻节点之间插入它们的最大公约数。

算法步骤:
1. 定义一个辅助函数 `gcd` 来计算两个数的最大公约数。
2. 遍历链表，对于每对相邻节点，计算它们的最大公约数，并插入一个新的节点。
3. 返回修改后的链表头节点。

关键点:
- 使用欧几里得算法计算最大公约数。
- 在遍历链表时，确保正确地插入新节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是链表的长度。我们只需要遍历链表一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.solution import create_solution

def gcd(a: int, b: int) -> int:
    """计算两个数的最大公约数"""
    while b:
        a, b = b, a % b
    return a

def insert_gcds(head: Optional[ListNode]) -> Optional[ListNode]:
    """
    在链表中插入最大公约数
    """
    if not head or not head.next:
        return head

    current = head
    while current and current.next:
        next_node = current.next
        gcd_value = gcd(current.val, next_node.val)
        new_node = ListNode(gcd_value)
        current.next = new_node
        new_node.next = next_node
        current = next_node

    return head

Solution = create_solution(insert_gcds)