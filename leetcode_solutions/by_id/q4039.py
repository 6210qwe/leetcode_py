"""
3697. 计算十进制表示 - 给你一个 正整数 n。 如果一个正整数可以表示为 1 到 9 的单个数字和 10 的非负整数次幂的乘积，则称这个整数是一个 10 进制分量。例如，500、30 和 7 是 10 进制分量 ，而 537、102 和 11 则不是。 请将 n 表示为若干 仅由 10 进制分量组成的和，且使用的 10 进制分量个数 最少 。 返回一个包含这些 10 进制分量 的数组，并按分量大小 降序 排列。 示例 1： 输入：n = 537 输出：[500,30,7] 解释： 我们可以将 537 表示为500 + 30 + 7。无法用少于 3 个 10 进制分量表示 537。 示例 2： 输入：n = 102 输出：[100,2] 解释： 我们可以将 102 表示为100 + 2。102 不是一个 10 进制分量，因此需要 2 个 10 进制分量。 示例 3： 输入：n = 6 输出：[6] 解释： 6 是一个 10 进制分量。 提示： * 1 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将整数 n 按位拆解，每个位上的数字乘以相应的 10 的幂次，得到 10 进制分量。

算法步骤:
1. 初始化一个空列表 result 用于存储结果。
2. 从最高位到最低位遍历 n 的每一位。
3. 对于每一位，计算其对应的 10 进制分量，并将其添加到 result 中。
4. 返回 result。

关键点:
- 通过取模和整除操作逐位处理 n。
- 每一位的 10 进制分量是该位上的数字乘以 10 的相应幂次。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 因为我们在处理 n 的每一位，而 n 的位数是 log n。
空间复杂度: O(log n) - 结果列表的长度最多为 n 的位数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> List[int]:
    """
    函数式接口 - 将整数 n 表示为若干 10 进制分量组成的和，并返回这些分量的列表。
    """
    result = []
    power_of_ten = 1
    while n > 0:
        digit = n % 10
        if digit > 0:
            result.append(digit * power_of_ten)
        n //= 10
        power_of_ten *= 10
    return result[::-1]


Solution = create_solution(solution_function_name)