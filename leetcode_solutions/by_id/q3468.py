"""
3210. 找出加密后的字符串 - 给你一个字符串 s 和一个整数 k。请你使用以下算法加密字符串： * 对于字符串 s 中的每个字符 c，用字符串中 c 后面的第 k 个字符替换 c（以循环方式）。 返回加密后的字符串。 示例 1： 输入： s = "dart", k = 3 输出： "tdar" 解释： * 对于 i = 0，'d' 后面的第 3 个字符是 't'。 * 对于 i = 1，'a' 后面的第 3 个字符是 'd'。 * 对于 i = 2，'r' 后面的第 3 个字符是 'a'。 * 对于 i = 3，'t' 后面的第 3 个字符是 'r'。 示例 2： 输入： s = "aaa", k = 1 输出： "aaa" 解释： 由于所有字符都相同，加密后的字符串也将相同。 提示： * 1 <= s.length <= 100 * 1 <= k <= 104 * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用字符串切片和拼接来实现循环替换。

算法步骤:
1. 计算实际的偏移量 `k % len(s)`，确保 k 不超过字符串长度。
2. 将字符串分成两部分：前半部分和后半部分。
3. 拼接后半部分和前半部分，得到加密后的字符串。

关键点:
- 使用字符串切片操作可以高效地实现循环替换。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。字符串切片和拼接操作的时间复杂度为 O(n)。
空间复杂度: O(n)，需要额外的空间来存储切片后的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_encrypted_string(s: str, k: int) -> str:
    """
    函数式接口 - 实现加密字符串的函数
    """
    n = len(s)
    k = k % n  # 计算实际的偏移量
    return s[-k:] + s[:-k]  # 拼接后半部分和前半部分


Solution = create_solution(find_encrypted_string)