"""
858. 镜面反射 - 有一个特殊的正方形房间，每面墙上都有一面镜子。除西南角以外，每个角落都放有一个接受器，编号为 0， 1，以及 2。 正方形房间的墙壁长度为 p，一束激光从西南角射出，首先会与东墙相遇，入射点到接收器 0 的距离为 q 。 返回光线最先遇到的接收器的编号（保证光线最终会遇到一个接收器）。 示例 1： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/06/18/reflection.png] 输入：p = 2, q = 1 输出：2 解释：这条光线在第一次被反射回左边的墙时就遇到了接收器 2 。 示例 2： 输入：p = 3, q = 1 输入：1 提示： * 1 <= q <= p <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过数学方法找到光线首次遇到接收器的位置。

算法步骤:
1. 计算光线在水平方向和垂直方向上的步数，直到光线到达某个接收器。
2. 通过计算最小公倍数 (LCM) 和最大公约数 (GCD) 来确定光线的路径。
3. 根据路径判断光线最终遇到的接收器编号。

关键点:
- 使用 GCD 和 LCM 来简化计算。
- 通过奇偶性判断光线最终遇到的接收器编号。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log(min(p, q))) - 主要由 GCD 计算决定。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
import math

def solution_function_name(p: int, q: int) -> int:
    """
    函数式接口 - 实现镜面反射问题的最优解法
    """
    # 计算 p 和 q 的最大公约数
    gcd = math.gcd(p, q)
    
    # 计算 p 和 q 的最小公倍数
    lcm = (p * q) // gcd
    
    # 判断光线最终遇到的接收器编号
    if (lcm // p) % 2 == 0:
        return 2
    else:
        if (lcm // q) % 2 == 0:
            return 0
        else:
            return 1

Solution = create_solution(solution_function_name)