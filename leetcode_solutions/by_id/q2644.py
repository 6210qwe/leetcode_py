"""
核心思想: 通过位运算技巧，我们可以发现所有子序列和的按位或结果等于数组中所有元素的按位或结果。

算法步骤:
1. 初始化一个变量 `result` 为 0。
2. 遍历数组中的每个元素，将当前元素与 `result` 进行按位或操作。
3. 返回最终的 `result`。

关键点:
- 任何子序列的和都可以通过选择某些元素的组合来表示，因此所有子序列和的按位或结果等同于数组中所有元素的按位或结果。
"""

