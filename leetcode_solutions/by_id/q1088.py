# ============================================================================
# 问题描述
# ============================================================================
"""
给定一个年份 year 和月份 month，返回这个月份的天数。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断给定的月份和年份，确定该月的天数。

算法步骤:
1. 检查输入的月份是否在 1 到 12 之间，如果不在则返回 -1。
2. 根据月份判断该月的天数：
   - 31 天的月份：1, 3, 5, 7, 8, 10, 12
   - 30 天的月份：4, 6, 9, 11
   - 2 月需要判断是否是闰年，如果是闰年则有 29 天，否则 28 天。
3. 判断闰年的条件：能被 4 整除但不能被 100 整除，或者能被 400 整除。

关键点:
- 闰年的判断条件。
- 月份的天数规则。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def number_of_days_in_month(year: int, month: int) -> int:
    """
    函数式接口 - 返回给定年份和月份的天数
    """
    if not 1 <= month <= 12:
        return -1

    # 31 天的月份
    if month in [1, 3, 5, 7, 8, 10, 12]:
        return 31
    # 30 天的月份
    elif month in [4, 6, 9, 11]:
        return 30
    # 2 月
    else:
        # 判断是否是闰年
        if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
            return 29
        else:
            return 28


Solution = create_solution(number_of_days_in_month)