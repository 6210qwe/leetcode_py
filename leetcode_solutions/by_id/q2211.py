"""
2090. 半径为 k 的子数组平均值 - 给你一个下标从 0 开始的数组 nums ，数组中有 n 个整数，另给你一个整数 k 。 半径为 k 的子数组平均值 是指：nums 中一个以下标 i 为 中心 且 半径 为 k 的子数组中所有元素的平均值，即下标在 i - k 和 i + k 范围（含 i - k 和 i + k）内所有元素的平均值。如果在下标 i 前或后不足 k 个元素，那么 半径为 k 的子数组平均值 是 -1 。 构建并返回一个长度为 n 的数组 avgs ，其中 avgs[i] 是以下标 i 为中心的子数组的 半径为 k 的子数组平均值 。 x 个元素的 平均值 是 x 个元素相加之和除以 x ，此时使用截断式 整数除法 ，即需要去掉结果的小数部分。 * 例如，四个元素 2、3、1 和 5 的平均值是 (2 + 3 + 1 + 5) / 4 = 11 / 4 = 2.75，截断后得到 2 。 示例 1： [https://assets.leetcode.com/uploads/2021/11/07/eg1.png] 输入：nums = [7,4,3,9,1,8,5,2,6], k = 3 输出：[-1,-1,-1,5,4,4,-1,-1,-1] 解释： - avg[0]、avg[1] 和 avg[2] 是 -1 ，因为在这几个下标前的元素数量都不足 k 个。 - 中心为下标 3 且半径为 3 的子数组的元素总和是：7 + 4 + 3 + 9 + 1 + 8 + 5 = 37 。 使用截断式 整数除法，avg[3] = 37 / 7 = 5 。 - 中心为下标 4 的子数组，avg[4] = (4 + 3 + 9 + 1 + 8 + 5 + 2) / 7 = 4 。 - 中心为下标 5 的子数组，avg[5] = (3 + 9 + 1 + 8 + 5 + 2 + 6) / 7 = 4 。 - avg[6]、avg[7] 和 avg[8] 是 -1 ，因为在这几个下标后的元素数量都不足 k 个。 示例 2： 输入：nums = [100000], k = 0 输出：[100000] 解释： - 中心为下标 0 且半径 0 的子数组的元素总和是：100000 。 avg[0] = 100000 / 1 = 100000 。 示例 3： 输入：nums = [8], k = 100000 输出：[-1] 解释： - avg[0] 是 -1 ，因为在下标 0 前后的元素数量均不足 k 。 提示： * n == nums.length * 1 <= n <= 105 * 0 <= nums[i], k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来计算每个子数组的和，并更新结果数组。

算法步骤:
1. 初始化结果数组 `avgs`，长度与 `nums` 相同，初始值全部为 -1。
2. 如果 `k` 为 0，直接返回 `nums`。
3. 计算初始窗口的和 `window_sum`，窗口大小为 `2 * k + 1`。
4. 从 `k` 到 `n - k` 遍历数组，更新 `window_sum` 并计算当前窗口的平均值。
5. 将平均值存储在 `avgs` 中对应的位置。

关键点:
- 使用滑动窗口避免重复计算子数组的和。
- 注意边界条件，确保窗口不会越界。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1) （不考虑输出数组）
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def get_averages(nums: List[int], k: int) -> List[int]:
    n = len(nums)
    avgs = [-1] * n
    
    if k == 0:
        return nums
    
    if 2 * k + 1 > n:
        return avgs
    
    window_sum = sum(nums[:2 * k + 1])
    
    for i in range(k, n - k):
        avgs[i] = window_sum // (2 * k + 1)
        if i + k + 1 < n:
            window_sum += nums[i + k + 1] - nums[i - k]
    
    return avgs


Solution = create_solution(get_averages)