"""
2937. 使三个字符串相等 - 给你三个字符串 s1、s2 和 s3。 你可以根据需要对这三个字符串执行以下操作 任意次数 。 在每次操作中，你可以选择其中一个长度至少为 2 的字符串 并删除其 最右位置上 的字符。 如果存在某种方法能够使这三个字符串相等，请返回使它们相等所需的 最小 操作次数；否则，返回 -1。 示例 1： 输入：s1 = "abc"，s2 = "abb"，s3 = "ab" 输出：2 解释：对 s1 和 s2 进行一次操作后，可以得到三个相等的字符串。 可以证明，不可能用少于两次操作使它们相等。 示例 2： 输入：s1 = "dac"，s2 = "bac"，s3 = "cac" 输出：-1 解释：因为 s1 和 s2 的最左位置上的字母不相等，所以无论进行多少次操作，它们都不可能相等。因此答案是 -1 。 提示： * 1 <= s1.length, s2.length, s3.length <= 100 * s1、s2 和 s3 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过从左到右逐个比较字符，找到最长的公共前缀。然后计算每个字符串需要删除的字符数，使得它们都变成这个公共前缀。

算法步骤:
1. 初始化一个变量 `i` 用于记录最长公共前缀的长度。
2. 从左到右逐个比较三个字符串的字符，直到找到第一个不同的字符或达到某个字符串的末尾。
3. 计算每个字符串需要删除的字符数，即每个字符串的长度减去最长公共前缀的长度。
4. 返回总的操作次数，如果找不到公共前缀则返回 -1。

关键点:
- 通过逐个比较字符来找到最长公共前缀。
- 计算每个字符串需要删除的字符数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是三个字符串中最短的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def make_three_strings_equal(s1: str, s2: str, s3: str) -> int:
    """
    函数式接口 - 使三个字符串相等
    """
    # 找到最长的公共前缀
    i = 0
    while i < len(s1) and i < len(s2) and i < len(s3) and s1[i] == s2[i] == s3[i]:
        i += 1
    
    # 如果没有公共前缀，返回 -1
    if i == 0:
        return -1
    
    # 计算每个字符串需要删除的字符数
    operations = (len(s1) - i) + (len(s2) - i) + (len(s3) - i)
    
    return operations


Solution = create_solution(make_three_strings_equal)