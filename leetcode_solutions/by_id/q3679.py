"""
核心思想: 使用位运算和二分查找来找到最大尺寸的子数组。

算法步骤:
1. 初始化两个指针 left 和 right，分别指向数组的起始位置。
2. 使用一个变量 max_size 来记录当前找到的最大尺寸。
3. 使用位运算来检查当前子数组是否满足条件。
4. 如果满足条件，更新 max_size 并移动右指针扩大窗口；否则，移动左指针缩小窗口。
5. 重复步骤 3-4 直到遍历完整个数组。

关键点:
- 使用位运算来快速判断子数组是否满足条件。
- 使用二分查找来优化窗口的移动。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
