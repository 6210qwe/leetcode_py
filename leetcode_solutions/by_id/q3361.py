def solution_function_name(s: str) -> str:
    """
    函数式接口 - 替换字符可以得到的最晚时间
    """
    # 将字符串分割成小时和分钟部分
    hour, minute = s[:2], s[3:]

    # 处理小时部分
    if hour[0] == '?':
        if hour[1] == '?' or int(hour[1]) <= 3:
            hour = '2' + (hour[1] if hour[1] != '?' else '3')
        else:
            hour = '1' + hour[1]
    if hour[1] == '?':
        hour = hour[0] + ('9' if hour[0] == '1' else '3')

    # 处理分钟部分
    if minute[0] == '?':
        minute = '5' + minute[1]
    if minute[1] == '?':
        minute = minute[0] + '9'

    return f"{hour}:{minute}"


Solution = create_solution(solution_function_name)