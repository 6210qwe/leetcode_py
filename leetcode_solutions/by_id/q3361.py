"""
3114. 替换字符可以得到的最晚时间 - 给你一个字符串 s，表示一个 12 小时制的时间格式，其中一些数字（可能没有）被 "?" 替换。 12 小时制时间格式为 "HH:MM" ，其中 HH 的取值范围为 00 至 11，MM 的取值范围为 00 至 59。最早的时间为 00:00，最晚的时间为 11:59。 你需要将 s 中的 所有 "?" 字符替换为数字，使得结果字符串代表的时间是一个 有效 的 12 小时制时间，并且是可能的 最晚 时间。 返回结果字符串。 示例 1： 输入： s = "1?:?4" 输出： "11:54" 解释： 通过替换 "?" 字符，可以得到的最晚12小时制时间是 "11:54"。 示例 2： 输入： s = "0?:5?" 输出： "09:59" 解释： 通过替换 "?" 字符，可以得到的最晚12小时制时间是 "09:59"。 提示： * s.length == 5 * s[2] 是字符 ":" * 除 s[2] 外，其他字符都是数字或 "?" * 输入保证在替换 "?" 字符后至少存在一个介于 "00:00" 和 "11:59" 之间的时间。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 枚举所有可能的替换，找到最晚的时间。

算法步骤:
1. 将字符串分割成小时和分钟部分。
2. 对小时部分进行处理，确保其在 00 到 11 之间。
3. 对分钟部分进行处理，确保其在 00 到 59 之间。
4. 返回拼接后的最晚时间。

关键点:
- 通过枚举所有可能的替换，找到最晚的时间。
- 确保小时和分钟部分都在有效范围内。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 固定长度的字符串处理。
空间复杂度: O(1) - 只使用常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
