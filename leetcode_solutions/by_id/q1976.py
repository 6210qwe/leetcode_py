def split_string(s: str) -> bool:
    def backtrack(index: int, prev: int) -> bool:
        if index == len(s):
            return True

        for i in range(index + 1, len(s) + 1):
            num = int(s[index:i])
            if prev == -1 or (prev - num == 1 and (i == len(s) or s[i] != '0')):
                if backtrack(i, num):
                    return True
        return False

    return backtrack(0, -1)


