"""
3131. 找出与数组相加的整数 I - 给你两个长度相等的数组 nums1 和 nums2。 数组 nums1 中的每个元素都与变量 x 所表示的整数相加。如果 x 为负数，则表现为元素值的减少。 在与 x 相加后，nums1 和 nums2 相等 。当两个数组中包含相同的整数，并且这些整数出现的频次相同时，两个数组 相等 。 返回整数 x 。 示例 1: 输入：nums1 = [2,6,4], nums2 = [9,7,5] 输出：3 解释： 与 3 相加后，nums1 和 nums2 相等。 示例 2: 输入：nums1 = [10], nums2 = [5] 输出：-5 解释： 与 -5 相加后，nums1 和 nums2 相等。 示例 3: 输入：nums1 = [1,1,1,1], nums2 = [1,1,1,1] 输出：0 解释： 与 0 相加后，nums1 和 nums2 相等。 提示： * 1 <= nums1.length == nums2.length <= 100 * 0 <= nums1[i], nums2[i] <= 1000 * 测试用例以这样的方式生成：存在一个整数 x，使得 nums1 中的每个元素都与 x 相加后，nums1 与 nums2 相等。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过比较两个数组中对应元素的差值来找到整数 x。

算法步骤:
1. 计算 nums1 和 nums2 中任意一对对应元素的差值。
2. 返回该差值作为结果。

关键点:
- 由于题目保证存在一个整数 x 使得 nums1 中的每个元素都与 x 相加后，nums1 与 nums2 相等，因此只需要计算一次差值即可。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_integer_added_to_array(nums1: List[int], nums2: List[int]) -> int:
    """
    函数式接口 - 找出与数组相加的整数 x
    """
    # 通过比较两个数组中第一个元素的差值来找到整数 x
    return nums2[0] - nums1[0]


Solution = create_solution(find_integer_added_to_array)