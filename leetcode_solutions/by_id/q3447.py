"""
3174. 清除数字 - 给你一个字符串 s 。 你的任务是重复以下操作删除 所有 数字字符： * 删除 第一个数字字符 以及它左边 最近 的 非数字 字符。 请你返回删除所有数字字符以后剩下的字符串。 注意，该操作不能对左侧没有任何非数字字符的数字执行。 示例 1： 输入：s = "abc" 输出："abc" 解释： 字符串中没有数字。 示例 2： 输入：s = "cb34" 输出："" 解释： 一开始，我们对 s[2] 执行操作，s 变为 "c4" 。 然后对 s[1] 执行操作，s 变为 "" 。 提示： * 1 <= s.length <= 100 * s 只包含小写英文字母和数字字符。 * 输入保证所有数字都可以按以上操作被删除。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来存储非数字字符，并在遇到数字时弹出栈顶元素。

算法步骤:
1. 初始化一个空栈。
2. 遍历字符串 s 中的每个字符：
   - 如果字符是非数字字符，将其压入栈。
   - 如果字符是数字字符，从栈中弹出一个非数字字符（如果栈不为空）。
3. 将栈中的字符拼接成最终结果字符串。

关键点:
- 使用栈来存储非数字字符，确保在遇到数字时可以正确弹出最近的非数字字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。每个字符只处理一次。
空间复杂度: O(n)，最坏情况下栈中会存储所有的非数字字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> str:
    """
    函数式接口 - 实现清除数字的操作
    """
    stack = []
    for char in s:
        if char.isdigit():
            if stack:
                stack.pop()
        else:
            stack.append(char)
    return ''.join(stack)


Solution = create_solution(solution_function_name)