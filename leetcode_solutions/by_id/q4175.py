def smallest_all_ones_multiple(k: int) -> int:
    """
    函数式接口 - 找到最小的全 1 数字，使其能被 k 整除，并返回其长度。
    """
    if k % 2 == 0 or k % 5 == 0:
        return -1  # 如果 k 是 2 或 5 的倍数，则不存在这样的 n

    num = 1
    length = 1
    while num % k != 0:
        num = (num * 10 + 1) % k
        length += 1
        if length > k:  # 如果长度超过 k，则返回 -1
            return -1
    return length


