"""
1968. 构造元素不等于两相邻元素平均值的数组 - 给你一个 下标从 0 开始 的数组 nums ，数组由若干 互不相同的 整数组成。你打算重新排列数组中的元素以满足：重排后，数组中的每个元素都 不等于 其两侧相邻元素的 平均值 。 更公式化的说法是，重新排列的数组应当满足这一属性：对于范围 1 <= i < nums.length - 1 中的每个 i ，(nums[i-1] + nums[i+1]) / 2 不等于 nums[i] 均成立 。 返回满足题意的任一重排结果。 示例 1： 输入：nums = [1,2,3,4,5] 输出：[1,2,4,5,3] 解释： i=1, nums[i] = 2, 两相邻元素平均值为 (1+4) / 2 = 2.5 i=2, nums[i] = 4, 两相邻元素平均值为 (2+5) / 2 = 3.5 i=3, nums[i] = 5, 两相邻元素平均值为 (4+3) / 2 = 3.5 示例 2： 输入：nums = [6,2,0,9,7] 输出：[9,7,6,2,0] 解释： i=1, nums[i] = 7, 两相邻元素平均值为 (9+6) / 2 = 7.5 i=2, nums[i] = 6, 两相邻元素平均值为 (7+2) / 2 = 4.5 i=3, nums[i] = 2, 两相邻元素平均值为 (6+0) / 2 = 3 提示： * 3 <= nums.length <= 105 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过交替放置排序后的前一半和后一半元素来确保每个元素都不等于其两侧相邻元素的平均值。

算法步骤:
1. 对数组进行排序。
2. 创建一个新的数组，交替放置排序后的前一半和后一半元素。
3. 返回新数组。

关键点:
- 通过交替放置排序后的前一半和后一半元素，可以确保每个元素都不等于其两侧相邻元素的平均值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度，因为排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，需要额外的空间来存储新的数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> List[int]:
    """
    函数式接口 - 通过交替放置排序后的前一半和后一半元素来确保每个元素都不等于其两侧相邻元素的平均值。
    """
    # 对数组进行排序
    nums.sort()
    
    # 创建一个新的数组，交替放置排序后的前一半和后一半元素
    n = len(nums)
    result = [0] * n
    left, right = 0, (n - 1) // 2 + 1
    for i in range(n):
        if i % 2 == 0:
            result[i] = nums[left]
            left += 1
        else:
            result[i] = nums[right]
            right += 1
    
    return result


Solution = create_solution(solution_function_name)