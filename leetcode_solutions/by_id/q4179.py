"""
3798. 最大的偶数 - 给你一个仅由字符'1'和'2'组成的字符串s。 你可以删除字符串s中的任意数量的字符，但必须保持剩余字符的顺序不变。 返回可以表示 偶数 整数的 最大结果字符串 。如果不存在这样的字符串，则返回空字符串""。 示例 1： 输入: s = "1112" 输出: "1112" 解释: 该字符串已经表示了最大的偶数，因此不需要删除任何字符。 示例 2： 输入: s = "221" 输出: "22" 解释: 删除'1'后，可以得到最大的偶数，即 22。 示例 3： 输入: s = "1" 输出: "" 解释: 无法通过删除字符得到偶数。 提示： * 1 <= s.length <= 100 * s 仅由字符 '1' 和 '2' 组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从右到左遍历字符串，找到第一个出现的 '2'，并将其作为偶数的最后一位。如果找不到 '2'，则返回空字符串。

算法步骤:
1. 从右到左遍历字符串，找到第一个 '2' 的位置。
2. 如果找到了 '2'，则返回从字符串开头到该位置的子字符串。
3. 如果没有找到 '2'，则返回空字符串。

关键点:
- 从右到左遍历，确保找到的第一个 '2' 是最右边的 '2'，从而保证结果是最大的偶数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。最坏情况下需要遍历整个字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> str:
    """
    函数式接口 - 返回可以表示最大偶数的结果字符串
    """
    # 从右到左遍历字符串，找到第一个 '2'
    for i in range(len(s) - 1, -1, -1):
        if s[i] == '2':
            return s[:i + 1]
    
    # 如果没有找到 '2'，返回空字符串
    return ""


Solution = create_solution(solution_function_name)