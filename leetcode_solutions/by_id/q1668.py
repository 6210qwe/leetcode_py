"""
1542. 找出最长的超赞子字符串 - 给你一个字符串 s 。请返回 s 中最长的 超赞子字符串 的长度。 「超赞子字符串」需满足满足下述两个条件： * 该字符串是 s 的一个非空子字符串 * 进行任意次数的字符交换后，该字符串可以变成一个回文字符串 示例 1： 输入：s = "3242415" 输出：5 解释："24241" 是最长的超赞子字符串，交换其中的字符后，可以得到回文 "24142" 示例 2： 输入：s = "12345678" 输出：1 示例 3： 输入：s = "213123" 输出：6 解释："213123" 是最长的超赞子字符串，交换其中的字符后，可以得到回文 "231132" 示例 4： 输入：s = "00" 输出：2 提示： * 1 <= s.length <= 10^5 * s 仅由数字组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位掩码和哈希表来记录每个前缀的状态，并查找最长的子字符串。

算法步骤:
1. 初始化一个哈希表 `prefix`，用于存储每个状态第一次出现的位置。
2. 初始化状态 `state` 为 0，并将初始状态 (0, -1) 存入哈希表。
3. 遍历字符串 `s`，对于每个字符，更新状态 `state`。
4. 检查当前状态是否已经在哈希表中出现过，如果是，则计算当前子字符串的长度。
5. 检查与当前状态只有一个位不同的状态是否在哈希表中出现过，如果是，则计算当前子字符串的长度。
6. 更新哈希表 `prefix`，记录当前状态的位置。
7. 返回最长的超赞子字符串的长度。

关键点:
- 使用位掩码来表示字符的奇偶性。
- 使用哈希表来记录每个状态第一次出现的位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)（因为状态数最多为 1024）
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution(s: str) -> int:
    """
    函数式接口 - 找出最长的超赞子字符串
    """
    # 初始化哈希表，记录每个状态第一次出现的位置
    prefix = {0: -1}
    state = 0
    max_length = 0
    
    for i, char in enumerate(s):
        # 更新状态
        state ^= 1 << int(char)
        
        # 如果当前状态已经出现过，计算子字符串长度
        if state in prefix:
            max_length = max(max_length, i - prefix[state])
        else:
            prefix[state] = i
        
        # 检查与当前状态只有一个位不同的状态
        for j in range(10):
            new_state = state ^ (1 << j)
            if new_state in prefix:
                max_length = max(max_length, i - prefix[new_state])
    
    return max_length


Solution = create_solution(solution)