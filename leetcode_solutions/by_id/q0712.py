"""
712. 两个字符串的最小ASCII删除和 - 给定两个字符串s1 和 s2，返回 使两个字符串相等所需删除字符的 ASCII 值的最小和 。 示例 1: 输入: s1 = "sea", s2 = "eat" 输出: 231 解释: 在 "sea" 中删除 "s" 并将 "s" 的值(115)加入总和。 在 "eat" 中删除 "t" 并将 116 加入总和。 结束时，两个字符串相等，115 + 116 = 231 就是符合条件的最小和。 示例 2: 输入: s1 = "delete", s2 = "leet" 输出: 403 解释: 在 "delete" 中删除 "dee" 字符串变成 "let"， 将 100[d]+101[e]+101[e] 加入总和。在 "leet" 中删除 "e" 将 101[e] 加入总和。 结束时，两个字符串都等于 "let"，结果即为 100+101+101+101 = 403 。 如果改为将两个字符串转换为 "lee" 或 "eet"，我们会得到 433 或 417 的结果，比答案更大。 提示: * 1 <= s1.length, s2.length <= 1000 * s1 和 s2 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义一个二维数组 dp，其中 dp[i][j] 表示使 s1[:i] 和 s2[:j] 相等所需的最小 ASCII 删除和。

算法步骤:
1. 初始化一个 (m+1) x (n+1) 的二维数组 dp，其中 m 和 n 分别是 s1 和 s2 的长度。
2. 填充 dp 数组的第一行和第一列，表示一个字符串为空时需要删除另一个字符串的所有字符。
3. 对于 dp[i][j]，如果 s1[i-1] == s2[j-1]，则 dp[i][j] = dp[i-1][j-1]；否则，dp[i][j] = min(dp[i-1][j] + ord(s1[i-1]), dp[i][j-1] + ord(s2[j-1]))。
4. 最终结果存储在 dp[m][n] 中。

关键点:
- 动态规划的状态转移方程：dp[i][j] = min(dp[i-1][j] + ord(s1[i-1]), dp[i][j-1] + ord(s2[j-1])) 当 s1[i-1] != s2[j-1] 时。
- 初始化 dp 数组的第一行和第一列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是 s1 和 s2 的长度。
空间复杂度: O(m * n)，使用了一个 (m+1) x (n+1) 的二维数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_delete_sum(s1: str, s2: str) -> int:
    """
    函数式接口 - 计算使两个字符串相等所需的最小 ASCII 删除和
    """
    m, n = len(s1), len(s2)
    
    # 初始化 dp 数组
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    
    # 填充 dp 数组的第一行
    for j in range(1, n + 1):
        dp[0][j] = dp[0][j - 1] + ord(s2[j - 1])
    
    # 填充 dp 数组的第一列
    for i in range(1, m + 1):
        dp[i][0] = dp[i - 1][0] + ord(s1[i - 1])
    
    # 填充 dp 数组的其余部分
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if s1[i - 1] == s2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1]
            else:
                dp[i][j] = min(dp[i - 1][j] + ord(s1[i - 1]), dp[i][j - 1] + ord(s2[j - 1]))
    
    return dp[m][n]


Solution = create_solution(minimum_delete_sum)