def count_good_numbers(n: int) -> int:
    """
    函数式接口 - 计算长度为 n 的好数字的总数
    """
    MOD = 10**9 + 7

    def power(x: int, y: int) -> int:
        result = 1
        while y > 0:
            if y % 2 == 1:
                result = (result * x) % MOD
            x = (x * x) % MOD
            y //= 2
        return result

    even_count = (n + 1) // 2
    odd_count = n // 2
    return (power(5, even_count) * power(4, odd_count)) % MOD


