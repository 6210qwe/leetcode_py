"""
核心思想: 使用快速幂算法计算结果

算法步骤:
1. 计算偶数位和奇数位的数量。
2. 使用快速幂算法计算 (5^偶数位) * (4^奇数位) % (10^9 + 7)。

关键点:
- 快速幂算法可以高效地计算大指数的幂次。
"""

