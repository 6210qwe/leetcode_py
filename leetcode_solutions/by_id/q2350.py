"""
2239. 找到最接近 0 的数字 - 给你一个长度为 n 的整数数组 nums ，请你返回 nums 中最 接近 0 的数字。如果有多个答案，请你返回它们中的 最大值 。 示例 1： 输入：nums = [-4,-2,1,4,8] 输出：1 解释： -4 到 0 的距离为 |-4| = 4 。 -2 到 0 的距离为 |-2| = 2 。 1 到 0 的距离为 |1| = 1 。 4 到 0 的距离为 |4| = 4 。 8 到 0 的距离为 |8| = 8 。 所以，数组中距离 0 最近的数字为 1 。 示例 2： 输入：nums = [2,-1,1] 输出：1 解释：1 和 -1 都是距离 0 最近的数字，所以返回较大值 1 。 提示： * 1 <= n <= 1000 * -105 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 一次遍历数组，找到绝对值最小且最大的数。

算法步骤:
1. 初始化两个变量 `closest` 和 `min_distance`，分别用于存储当前最接近 0 的数和其距离。
2. 遍历数组，对于每个数，计算其绝对值。
3. 如果当前数的绝对值小于 `min_distance`，或者等于 `min_distance` 但当前数大于 `closest`，则更新 `closest` 和 `min_distance`。
4. 返回 `closest`。

关键点:
- 通过一次遍历数组，可以在 O(n) 时间内找到最接近 0 的数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_closest_number(nums: List[int]) -> int:
    """
    函数式接口 - 找到最接近 0 的数字
    """
    closest = None
    min_distance = float('inf')
    
    for num in nums:
        distance = abs(num)
        if distance < min_distance or (distance == min_distance and num > closest):
            closest = num
            min_distance = distance
    
    return closest


Solution = create_solution(find_closest_number)