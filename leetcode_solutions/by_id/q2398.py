"""
2319. 判断矩阵是否是一个 X 矩阵 - 如果一个正方形矩阵满足下述 全部 条件，则称之为一个 X 矩阵 ： 1. 矩阵对角线上的所有元素都 不是 0 2. 矩阵中所有其他元素都是 0 给你一个大小为 n x n 的二维整数数组 grid ，表示一个正方形矩阵。如果 grid 是一个 X 矩阵 ，返回 true ；否则，返回 false 。 示例 1： [https://assets.leetcode.com/uploads/2022/05/03/ex1.jpg] 输入：grid = [[2,0,0,1],[0,3,1,0],[0,5,2,0],[4,0,0,2]] 输出：true 解释：矩阵如上图所示。 X 矩阵应该满足：绿色元素（对角线上）都不是 0 ，红色元素都是 0 。 因此，grid 是一个 X 矩阵。 示例 2： [https://assets.leetcode.com/uploads/2022/05/03/ex2.jpg] 输入：grid = [[5,7,0],[0,3,1],[0,5,0]] 输出：false 解释：矩阵如上图所示。 X 矩阵应该满足：绿色元素（对角线上）都不是 0 ，红色元素都是 0 。 因此，grid 不是一个 X 矩阵。 提示： * n == grid.length == grid[i].length * 3 <= n <= 100 * 0 <= grid[i][j] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 检查矩阵的主对角线和副对角线上的元素是否都不为0，并且其他元素是否都为0。

算法步骤:
1. 遍历矩阵的每个元素。
2. 检查主对角线和副对角线上的元素是否都不为0。
3. 检查其他元素是否都为0。
4. 如果所有条件都满足，返回True；否则返回False。

关键点:
- 通过一次遍历完成检查，避免多次遍历矩阵。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中n是矩阵的边长。我们需要遍历整个矩阵。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_x_matrix(grid: List[List[int]]) -> bool:
    """
    函数式接口 - 判断矩阵是否是一个X矩阵
    """
    n = len(grid)
    
    for i in range(n):
        for j in range(n):
            if i == j or i + j == n - 1:
                # 检查对角线上的元素是否不为0
                if grid[i][j] == 0:
                    return False
            else:
                # 检查其他元素是否为0
                if grid[i][j] != 0:
                    return False
    
    return True


Solution = create_solution(is_x_matrix)