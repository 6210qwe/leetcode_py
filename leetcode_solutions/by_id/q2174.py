"""
核心思想: 枚举可能的数值平衡数，并找到第一个大于 n 的数值平衡数。

算法步骤:
1. 定义一个函数 `is_balanced` 来判断一个数是否是数值平衡数。
2. 从 n+1 开始逐个检查每个数，直到找到一个数值平衡数。

关键点:
- 使用字符串操作来统计每个数位的出现次数。
- 通过枚举和检查来找到最小的数值平衡数。
"""

