"""
1589. 所有排列中的最大和 - 有一个整数数组 nums ，和一个查询数组 requests ，其中 requests[i] = [starti, endi] 。第 i 个查询求 nums[starti] + nums[starti + 1] + ... + nums[endi - 1] + nums[endi] 的结果 ，starti 和 endi 数组索引都是 从 0 开始 的。 你可以任意排列 nums 中的数字，请你返回所有查询结果之和的最大值。 由于答案可能会很大，请你将它对 109 + 7 取余 后返回。 示例 1： 输入：nums = [1,2,3,4,5], requests = [[1,3],[0,1]] 输出：19 解释：一个可行的 nums 排列为 [2,1,3,4,5]，并有如下结果： requests[0] -> nums[1] + nums[2] + nums[3] = 1 + 3 + 4 = 8 requests[1] -> nums[0] + nums[1] = 2 + 1 = 3 总和为：8 + 3 = 11。 一个总和更大的排列为 [3,5,4,2,1]，并有如下结果： requests[0] -> nums[1] + nums[2] + nums[3] = 5 + 4 + 2 = 11 requests[1] -> nums[0] + nums[1] = 3 + 5 = 8 总和为： 11 + 8 = 19，这个方案是所有排列中查询之和最大的结果。 示例 2： 输入：nums = [1,2,3,4,5,6], requests = [[0,1]] 输出：11 解释：一个总和最大的排列为 [6,5,4,3,2,1] ，查询和为 [11]。 示例 3： 输入：nums = [1,2,3,4,5,10], requests = [[0,2],[1,3],[1,1]] 输出：47 解释：一个和最大的排列为 [4,10,5,3,2,1] ，查询结果分别为 [19,18,10]。 提示： * n == nums.length * 1 <= n <= 105 * 0 <= nums[i] <= 105 * 1 <= requests.length <= 105 * requests[i].length == 2 * 0 <= starti <= endi < n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算每个位置被查询的次数，然后将这些次数与 nums 中的元素进行排序，使得查询次数最多的元素对应 nums 中最大的元素，从而最大化查询结果之和。

算法步骤:
1. 计算每个位置被查询的次数。
2. 将 nums 和查询次数分别排序。
3. 将查询次数最多的元素与 nums 中最大的元素相乘，累加得到最大和。

关键点:
- 使用差分数组来高效计算每个位置被查询的次数。
- 对 nums 和查询次数进行排序，确保最大值对应最大查询次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + m log m)，其中 n 是 nums 的长度，m 是 requests 的长度。排序操作的时间复杂度为 O(n log n) 和 O(m log m)。
空间复杂度: O(n)，用于存储差分数组和查询次数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def max_sum_range_query(nums: List[int], requests: List[List[int]]) -> int:
    MOD = 10**9 + 7
    n = len(nums)
    count = [0] * (n + 1)
    
    # 计算每个位置被查询的次数
    for start, end in requests:
        count[start] += 1
        count[end + 1] -= 1
    
    # 差分数组转换为前缀和数组
    for i in range(1, n):
        count[i] += count[i - 1]
    
    # 去掉最后一个多余的元素
    count.pop()
    
    # 对 nums 和查询次数进行排序
    nums.sort()
    count.sort()
    
    # 计算最大和
    max_sum = sum(num * cnt for num, cnt in zip(nums, count)) % MOD
    
    return max_sum

