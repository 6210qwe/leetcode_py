"""
核心思想: 使用贪心算法，优先使用 cost2 操作来减少总开销。

算法步骤:
1. 计算每个元素与最大值之间的差值。
2. 如果 cost2 < 2 * cost1，则优先使用 cost2 操作来增加多个元素。
3. 否则，直接使用 cost1 操作来增加单个元素。
4. 计算总开销并取模。

关键点:
- 优先使用 cost2 操作来减少总开销。
- 使用前缀和来快速计算多个元素的增量。
"""

