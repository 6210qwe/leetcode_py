# ============================================================================
# 问题描述
# ============================================================================
"""
3803. 统计残差前缀 - 给你一个仅由小写英文字母组成的字符串 s。 如果字符串 s 的某个 前缀 中 不同字符的数量 等于 len(prefix) % 3，则该前缀被称为残差前缀（residue）。 返回字符串 s 中 残差前缀 的数量。 字符串的 前缀 是一个 非空子字符串，从字符串的开头起始并延伸到任意位置。 示例 1： 输入: s = "abc" 输出: 2 解释: * 前缀 "a" 有 1 个不同字符，且长度模 3 为 1，因此它是一个残差前缀。 * 前缀 "ab" 有 2 个不同字符，且长度模 3 为 2，因此它是一个残差前缀。 * 前缀 "abc" 不满足条件，因此不是残差前缀。 因此，答案是 2。 示例 2： 输入: s = "dd" 输出: 1 解释: * 前缀 "d" 有 1 个不同字符，且长度模 3 为 1，因此它是一个残差前缀。 * 前缀 "dd" 有 1 个不同字符，但长度模 3 为 2，因此它不是残差前缀。 因此，答案是 1。 示例 3： 输入: s = "bob" 输出: 2 解释: * 前缀 "b" 有 1 个不同字符，且长度模 3 为 1，因此它是一个残差前缀。 * 前缀 "bo" 有 2 个不同字符，且长度模 3 为 2，因此它是一个残差前缀。 * 前缀 "bob" 不满足条件。 因此，答案是 2。 提示： * 1 <= s.length <= 100 * s 仅包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合来记录当前前缀中的不同字符，并检查其数量是否等于当前前缀长度模 3。

算法步骤:
1. 初始化一个哈希集合来存储当前前缀中的不同字符。
2. 遍历字符串 s 的每个字符，更新哈希集合。
3. 对于每个前缀，检查不同字符的数量是否等于当前前缀长度模 3。
4. 如果满足条件，增加计数器。
5. 返回计数器的值。

关键点:
- 使用哈希集合来高效地记录和检查不同字符的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，因为字母表的大小是固定的（26个小写字母），所以哈希集合的大小也是固定的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_residue_prefixes(s: str) -> int:
    """
    函数式接口 - 统计残差前缀
    """
    # 初始化计数器
    count = 0
    # 初始化哈希集合
    seen = set()
    
    # 遍历字符串 s
    for i, char in enumerate(s):
        # 将当前字符加入哈希集合
        seen.add(char)
        # 检查不同字符的数量是否等于当前前缀长度模 3
        if len(seen) == (i + 1) % 3:
            count += 1
    
    return count


Solution = create_solution(count_residue_prefixes)