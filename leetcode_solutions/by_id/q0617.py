# 问题描述
# ============================================================================
"""
617. 合并二叉树 - 给你两棵二叉树： root1 和 root2 。 想象一下，当你将其中一棵覆盖到另一棵之上时，两棵树上的一些节点将会重叠（而另一些不会）。你需要将这两棵树合并成一棵新二叉树。合并的规则是：如果两个节点重叠，那么将这两个节点的值相加作为合并后节点的新值；否则，不为 null 的节点将直接作为新二叉树的节点。 返回合并后的二叉树。 注意: 合并过程必须从两个树的根节点开始。 示例 1： [https://assets.leetcode.com/uploads/2021/02/05/merge.jpg] 输入：root1 = [1,3,2,5], root2 = [2,1,3,null,4,null,7] 输出：[3,4,5,5,4,null,7] 示例 2： 输入：root1 = [1], root2 = [1,2] 输出：[2,2] 提示： * 两棵树中的节点数目在范围 [0, 2000] 内 * -104 <= Node.val <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 来遍历两棵树，并在遍历时合并节点。

算法步骤:
1. 如果两个节点都存在，则将它们的值相加，并递归地合并它们的左子树和右子树。
2. 如果其中一个节点不存在，则返回另一个节点。

关键点:
- 使用递归方法进行深度优先搜索。
- 在每个节点处处理合并逻辑。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是两棵树中节点数的总和。每个节点最多被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def merge_trees(root1: Optional[TreeNode], root2: Optional[TreeNode]) -> Optional[TreeNode]:
    """
    合并两棵二叉树。
    """
    if not root1:
        return root2
    if not root2:
        return root1
    
    # 合并当前节点
    root1.val += root2.val
    
    # 递归合并左子树
    root1.left = merge_trees(root1.left, root2.left)
    
    # 递归合并右子树
    root1.right = merge_trees(root1.right, root2.right)
    
    return root1

Solution = create_solution(merge_trees)