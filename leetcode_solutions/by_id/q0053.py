"""
53. 最大子数组和 - 给你一个整数数组 nums ，请你找出一个具有最大和的连续子数组（子数组最少包含一个元素），返回其最大和。 子数组 是数组中的一个连续部分。 示例 1： 输入：nums = [-2,1,-3,4,-1,2,1,-5,4] 输出：6 解释：连续子数组 [4,-1,2,1] 的和最大，为 6 。 示例 2： 输入：nums = [1] 输出：1 示例 3： 输入：nums = [5,4,-1,7,8] 输出：23 提示： * 1 <= nums.length <= 105 * -104 <= nums[i] <= 104 进阶：如果你已经实现复杂度为 O(n) 的解法，尝试使用更为精妙的 分治法 求解。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划（Kadane算法），维护以当前位置结尾的最大子数组和

算法步骤:
1. 初始化：max_sum = nums[0], current_sum = nums[0]
2. 从索引1开始遍历数组：
   - 对于每个元素nums[i]：
     * current_sum = max(nums[i], current_sum + nums[i])
     * 如果current_sum > max_sum，更新max_sum
3. 返回max_sum

关键点:
- Kadane算法的核心：如果当前子数组和为负，则重新开始
- 只需要一次遍历，时间复杂度O(n)
- 空间复杂度O(1)，只使用常数额外空间
- 也可以使用分治法，但Kadane算法更简洁高效
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 只需一次遍历
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def max_sub_array(nums: List[int]) -> int:
    """
    函数式接口 - Kadane算法（动态规划）
    
    实现思路:
    使用Kadane算法，维护以当前位置结尾的最大子数组和，一次遍历即可完成。
    
    Args:
        nums: 整数数组
        
    Returns:
        最大和的连续子数组的和
        
    Example:
        >>> max_sub_array([-2, 1, -3, 4, -1, 2, 1, -5, 4])
        6
    """
    if not nums:
        return 0
    
    max_sum = nums[0]
    current_sum = nums[0]
    
    for i in range(1, len(nums)):
        # 如果当前子数组和为负，则重新开始
        current_sum = max(nums[i], current_sum + nums[i])
        max_sum = max(max_sum, current_sum)
    
    return max_sum


