"""
1318. 或运算的最小翻转次数 - 给你三个正整数 a、b 和 c。 你可以对 a 和 b 的二进制表示进行位翻转操作，返回能够使按位或运算 a OR b == c 成立的最小翻转次数。 「位翻转操作」是指将一个数的二进制表示任何单个位上的 1 变成 0 或者 0 变成 1 。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/01/11/sample_3_1676.png] 输入：a = 2, b = 6, c = 5 输出：3 解释：翻转后 a = 1 , b = 4 , c = 5 使得 a OR b == c 示例 2： 输入：a = 4, b = 2, c = 7 输出：1 示例 3： 输入：a = 1, b = 2, c = 3 输出：0 提示： * 1 <= a <= 10^9 * 1 <= b <= 10^9 * 1 <= c <= 10^9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过逐位比较 a, b 和 c 的二进制表示，计算需要翻转的次数。

算法步骤:
1. 初始化翻转次数为 0。
2. 遍历 a, b 和 c 的每一位，从最低位到最高位。
3. 对于每一位，如果 c 的该位为 1，则 a 和 b 中至少有一个位为 1 才能满足条件，否则需要翻转。
4. 如果 c 的该位为 0，则 a 和 b 的该位都必须为 0，否则需要翻转。
5. 返回总的翻转次数。

关键点:
- 使用位运算逐位比较 a, b 和 c。
- 计算需要翻转的次数时，考虑 a 和 b 的每一位是否满足条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 因为整数的位数是固定的（最多 32 位）。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_flips_to_make_a_or_b_equal_to_c(a: int, b: int, c: int) -> int:
    """
    函数式接口 - 计算使 a OR b == c 成立的最小翻转次数
    """
    flips = 0
    for i in range(32):
        bit_a = (a >> i) & 1
        bit_b = (b >> i) & 1
        bit_c = (c >> i) & 1
        if bit_c == 0:
            # c 的该位为 0，a 和 b 的该位都必须为 0
            flips += (bit_a + bit_b)
        else:
            # c 的该位为 1，a 和 b 中至少有一个位为 1
            if bit_a == 0 and bit_b == 0:
                flips += 1
    return flips


Solution = create_solution(min_flips_to_make_a_or_b_equal_to_c)