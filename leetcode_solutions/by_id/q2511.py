"""
2522. 将字符串分割成值不超过 K 的子字符串 - 给你一个字符串 s ，它每一位都是 1 到 9 之间的数字组成，同时给你一个整数 k 。 如果一个字符串 s 的分割满足以下条件，我们称它是一个 好 分割： * s 中每个数位 恰好 属于一个子字符串。 * 每个子字符串的值都小于等于 k 。 请你返回 s 所有的 好 分割中，子字符串的 最少 数目。如果不存在 s 的 好 分割，返回 -1 。 注意： * 一个字符串的 值 是这个字符串对应的整数。比方说，"165462" 的值为 165462 ，"1" 的值是 1 。 * 子字符串 是字符串中一段连续的字符序列。 示例 1： 输入：s = "165462", k = 60 输出：4 解释：我们将字符串分割成子字符串 "16" ，"54" ，"6" 和 "2" 。每个子字符串的值都小于等于 k = 60 。 不存在小于 4 个子字符串的好分割。 示例 2： 输入：s = "238182", k = 5 输出：-1 解释：这个字符串不存在好分割。 提示： * 1 <= s.length <= 105 * s[i] 是 '1' 到 '9' 之间的数字。 * 1 <= k <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从左到右遍历字符串，尽可能多地将字符合并成一个子字符串，直到该子字符串的值超过 k。

算法步骤:
1. 初始化计数器 `count` 为 0，用于记录子字符串的数量。
2. 初始化当前子字符串的值 `current_value` 为 0。
3. 从左到右遍历字符串 s：
   - 将当前字符转换为整数并累加到 `current_value`。
   - 如果 `current_value` 超过 k，则增加计数器 `count`，并将 `current_value` 重置为当前字符的值。
   - 如果在遍历过程中发现某个字符的值本身就大于 k，则直接返回 -1。
4. 最后，如果 `current_value` 不为空，则增加计数器 `count`。
5. 返回计数器 `count`。

关键点:
- 使用贪心算法，尽可能多地将字符合并成一个子字符串。
- 在遍历过程中，如果发现某个字符的值本身就大于 k，则直接返回 -1。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历一次字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_partitions(s: str, k: int) -> int:
    """
    函数式接口 - 返回字符串 s 的所有好分割中，子字符串的最少数目。
    """
    count = 0
    current_value = 0
    
    for char in s:
        digit = int(char)
        if digit > k:
            return -1
        if current_value * 10 + digit > k:
            count += 1
            current_value = digit
        else:
            current_value = current_value * 10 + digit
    
    if current_value > 0:
        count += 1
    
    return count


Solution = create_solution(min_partitions)