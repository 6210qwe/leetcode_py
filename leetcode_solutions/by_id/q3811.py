"""
3498. 字符串的反转度 - 给你一个字符串 s，计算其 反转度。 反转度的计算方法如下： 1. 对于每个字符，将其在 反转 字母表中的位置（'a' = 26, 'b' = 25, ..., 'z' = 1）与其在字符串中的位置（下标从1 开始）相乘。 2. 将这些乘积加起来，得到字符串中所有字符的和。 返回 反转度。 示例 1： 输入： s = "abc" 输出： 148 解释： 字母 反转字母表中的位置 字符串中的位置 乘积 'a' 26 1 26 'b' 25 2 50 'c' 24 3 72 反转度是 26 + 50 + 72 = 148 。 示例 2： 输入： s = "zaza" 输出： 160 解释： 字母 反转字母表中的位置 字符串中的位置 乘积 'z' 1 1 1 'a' 26 2 52 'z' 1 3 3 'a' 26 4 104 反转度是 1 + 52 + 3 + 104 = 160 。 提示： * 1 <= s.length <= 1000 * s 仅包含小写字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算每个字符在反转字母表中的位置，并将其与字符在字符串中的位置相乘，然后将所有乘积累加。

算法步骤:
1. 初始化一个变量 `reverse_degree` 为 0，用于存储最终的反转度。
2. 遍历字符串 `s`，对于每个字符：
   - 计算其在反转字母表中的位置。
   - 将其位置与其在字符串中的位置相乘。
   - 将乘积累加到 `reverse_degree` 中。
3. 返回 `reverse_degree`。

关键点:
- 使用 ASCII 码来快速计算字符在反转字母表中的位置。
- 通过遍历字符串一次完成计算，时间复杂度为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算字符串的反转度
    """
    reverse_degree = 0
    for i, char in enumerate(s):
        # 计算字符在反转字母表中的位置
        reverse_pos = 26 - (ord(char) - ord('a'))
        # 将其位置与其在字符串中的位置相乘
        reverse_degree += reverse_pos * (i + 1)
    return reverse_degree


Solution = create_solution(solution_function_name)