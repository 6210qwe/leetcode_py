"""
2148. 元素计数 - 给你一个整数数组 nums ，统计并返回在 nums 中同时至少具有一个严格较小元素和一个严格较大元素的元素数目。 示例 1： 输入：nums = [11,7,2,15] 输出：2 解释：元素 7 ：严格较小元素是元素 2 ，严格较大元素是元素 11 。 元素 11 ：严格较小元素是元素 7 ，严格较大元素是元素 15 。 总计有 2 个元素都满足在 nums 中同时存在一个严格较小元素和一个严格较大元素。 示例 2： 输入：nums = [-3,3,3,90] 输出：2 解释：元素 3 ：严格较小元素是元素 -3 ，严格较大元素是元素 90 。 由于有两个元素的值为 3 ，总计有 2 个元素都满足在 nums 中同时存在一个严格较小元素和一个严格较大元素。 提示： * 1 <= nums.length <= 100 * -105 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到数组中的最小值和最大值，然后遍历数组，统计既大于最小值又小于最大值的元素个数。

算法步骤:
1. 找到数组中的最小值和最大值。
2. 遍历数组，统计既大于最小值又小于最大值的元素个数。

关键点:
- 通过一次遍历找到最小值和最大值。
- 再次遍历数组进行计数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要两次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_elements(nums: List[int]) -> int:
    """
    函数式接口 - 统计在 nums 中同时至少具有一个严格较小元素和一个严格较大元素的元素数目。
    """
    if len(nums) < 3:
        return 0

    min_val = min(nums)
    max_val = max(nums)

    count = 0
    for num in nums:
        if min_val < num < max_val:
            count += 1

    return count


Solution = create_solution(count_elements)