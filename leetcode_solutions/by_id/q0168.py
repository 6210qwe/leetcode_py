"""
168. Excel 表列名称 - 给你一个整数 columnNumber ，返回它在 Excel 表中相对应的列名称。 例如： A -> 1 B -> 2 C -> 3 ... Z -> 26 AA -> 27 AB -> 28 ... 示例 1： 输入：columnNumber = 1 输出："A" 示例 2： 输入：columnNumber = 28 输出："AB" 示例 3： 输入：columnNumber = 701 输出："ZY" 示例 4： 输入：columnNumber = 2147483647 输出："FXSHRXW" 提示： * 1 <= columnNumber <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 26进制转换，注意这里是从1开始而不是0

算法步骤:
1. 这是一个26进制系统，但是从1开始（A=1, Z=26）
2. 每次取模26，但需要减1后再取模（因为从1开始）
3. 将结果转换为对应字母
4. 反转字符串得到最终结果

关键点:
- 注意26进制从1开始，需要减1处理
- 时间复杂度O(logn)，空间复杂度O(logn)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(logn) - 以26为底的对数
空间复杂度: O(logn) - 存储结果字符串
"""

# ============================================================================
# 代码实现
# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def excel_sheet_column_title(columnNumber: int) -> str:
    """
    函数式接口 - Excel表列名称
    
    实现思路:
    26进制转换，注意这里是从1开始而不是0。
    
    Args:
        columnNumber: 列序号
        
    Returns:
        对应的列名称
        
    Example:
        >>> excel_sheet_column_title(28)
        'AB'
    """
    result = []
    
    while columnNumber > 0:
        columnNumber -= 1  # 因为从1开始，需要减1
        result.append(chr(ord('A') + columnNumber % 26))
        columnNumber //= 26
    
    return ''.join(reversed(result))


