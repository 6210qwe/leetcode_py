# 问题描述
# ============================================================================
"""
932. 漂亮数组 - 如果长度为 n 的数组 nums 满足下述条件，则认为该数组是一个 漂亮数组 ： * nums 是由范围 [1, n] 的整数组成的一个排列。 * 对于每个 0 <= i < j < n ，均不存在下标 k（i < k < j）使得 2 * nums[k] == nums[i] + nums[j] 。 给你整数 n ，返回长度为 n 的任一 漂亮数组 。本题保证对于给定的 n 至少存在一个有效答案。 示例 1 ： 输入：n = 4 输出：[2,1,4,3] 示例 2 ： 输入：n = 5 输出：[3,1,2,5,4] 提示： * 1 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用分治法构造漂亮数组

算法步骤:
1. 如果 n 为 1，直接返回 [1]
2. 将 n 分解为两个子问题，分别求解奇数部分和偶数部分
3. 合并两个子问题的结果，构造最终的漂亮数组

关键点:
- 奇数部分和偶数部分的构造可以递归地进行
- 通过合并奇数部分和偶数部分来避免出现 2 * nums[k] == nums[i] + nums[j] 的情况
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)
空间复杂度: O(n log n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> List[int]:
    """
    函数式接口 - 使用分治法构造漂亮数组
    """
    def beautiful_array(n: int) -> List[int]:
        if n == 1:
            return [1]
        
        # 递归构造奇数部分和偶数部分
        odd_part = [x * 2 - 1 for x in beautiful_array((n + 1) // 2)]
        even_part = [x * 2 for x in beautiful_array(n // 2)]
        
        # 合并奇数部分和偶数部分
        return odd_part + even_part

    return beautiful_array(n)


Solution = create_solution(solution_function_name)