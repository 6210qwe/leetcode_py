"""
1913. 两个数对之间的最大乘积差 - 两个数对 (a, b) 和 (c, d) 之间的 乘积差 定义为 (a * b) - (c * d) 。 * 例如，(5, 6) 和 (2, 7) 之间的乘积差是 (5 * 6) - (2 * 7) = 16 。 给你一个整数数组 nums ，选出四个 不同的 下标 w、x、y 和 z ，使数对 (nums[w], nums[x]) 和 (nums[y], nums[z]) 之间的 乘积差 取到 最大值 。 返回以这种方式取得的乘积差中的 最大值 。 示例 1： 输入：nums = [5,6,2,7,4] 输出：34 解释：可以选出下标为 1 和 3 的元素构成第一个数对 (6, 7) 以及下标 2 和 4 构成第二个数对 (2, 4) 乘积差是 (6 * 7) - (2 * 4) = 34 示例 2： 输入：nums = [4,2,5,9,7,4,8] 输出：64 解释：可以选出下标为 3 和 6 的元素构成第一个数对 (9, 8) 以及下标 1 和 5 构成第二个数对 (2, 4) 乘积差是 (9 * 8) - (2 * 4) = 64 提示： * 4 <= nums.length <= 104 * 1 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历找到数组中最大的两个数和最小的两个数，然后计算它们的乘积差。

算法步骤:
1. 初始化四个变量来存储最大的两个数和最小的两个数。
2. 遍历数组，更新这四个变量。
3. 计算并返回最大乘积差。

关键点:
- 一次遍历即可找到所需的四个数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要一次遍历来找到所需的四个数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_product_difference(nums: List[int]) -> int:
    """
    函数式接口 - 计算两个数对之间的最大乘积差
    """
    # 初始化最大的两个数和最小的两个数
    max1, max2 = float('-inf'), float('-inf')
    min1, min2 = float('inf'), float('inf')

    # 遍历数组，更新最大和最小的两个数
    for num in nums:
        if num > max1:
            max1, max2 = num, max1
        elif num > max2:
            max2 = num

        if num < min1:
            min1, min2 = num, min1
        elif num < min2:
            min2 = num

    # 计算并返回最大乘积差
    return (max1 * max2) - (min1 * min2)


Solution = create_solution(max_product_difference)