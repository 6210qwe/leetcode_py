"""
3818. 移除前缀使数组严格递增 - 给你一个整数数组 nums。 你需要从 nums 中 恰好 移除一个前缀（可以为空）。 返回一个整数，表示被移除的前缀的 最小 长度，使得剩余的数组 严格递增 。 数组的 前缀 是从数组的起始位置开始，一直延伸到任意位置的子数组。 如果一个数组的每个元素都严格大于它的前一个元素（若存在），则称该数组 严格递增 。 示例 1： 输入： nums = [1,-1,2,3,3,4,5] 输出： 4 解释： 移除前缀 prefix = [1, -1, 2, 3] 后，剩余数组为 [3, 4, 5]，严格递增。 示例 2： 输入： nums = [4,3,-2,-5] 输出： 3 解释： 移除前缀 prefix = [4, 3, -2] 后，剩余数组为 [-5]，严格递增。 示例 3： 输入： nums = [1,2,3,4] 输出： 0 解释： 数组 nums = [1, 2, 3, 4] 已经是严格递增的，因此移除空前缀即可。 提示： * 1 <= nums.length <= 105 * -109 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法找到最小的前缀长度，使得剩余数组严格递增。

算法步骤:
1. 初始化两个指针 `i` 和 `j`，分别指向数组的起始位置。
2. 使用指针 `i` 遍历数组，同时使用指针 `j` 记录当前严格递增子数组的起始位置。
3. 如果当前元素 `nums[i]` 小于或等于 `nums[i-1]`，更新 `j` 为 `i`。
4. 最后返回 `j` 作为需要移除的前缀长度。

关键点:
- 使用双指针方法可以在 O(n) 时间复杂度内找到最小前缀长度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_prefix_removal(nums: List[int]) -> int:
    """
    函数式接口 - 找到最小的前缀长度，使得剩余数组严格递增。
    """
    n = len(nums)
    j = 0  # 记录当前严格递增子数组的起始位置
    for i in range(1, n):
        if nums[i] <= nums[i - 1]:
            j = i  # 更新 j 为 i
    return j


Solution = create_solution(min_prefix_removal)