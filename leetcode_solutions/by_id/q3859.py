"""
3536. 两个数字的最大乘积 - 给定一个正整数 n。 返回 任意两位数字 相乘所得的 最大 乘积。 注意：如果某个数字在 n 中出现多次，你可以多次使用该数字。 示例 1： 输入： n = 31 输出： 3 解释： * n 的数字是 [3, 1]。 * 任意两位数字相乘的结果为：3 * 1 = 3。 * 最大乘积为 3。 示例 2： 输入： n = 22 输出： 4 解释： * n 的数字是 [2, 2]。 * 任意两位数字相乘的结果为：2 * 2 = 4。 * 最大乘积为 4。 示例 3： 输入： n = 124 输出： 8 解释： * n 的数字是 [1, 2, 4]。 * 任意两位数字相乘的结果为：1 * 2 = 2, 1 * 4 = 4, 2 * 4 = 8。 * 最大乘积为 8。 提示： * 10 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将整数 n 转换为字符串，然后遍历所有可能的两位数字组合，找到最大乘积。

算法步骤:
1. 将整数 n 转换为字符串。
2. 初始化最大乘积为 0。
3. 遍历字符串中的每个字符，将其转换为整数。
4. 对于每个字符，再次遍历字符串中的每个字符，计算两两之间的乘积，并更新最大乘积。

关键点:
- 使用双重循环遍历所有可能的两位数字组合。
- 可以重复使用相同的数字。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d^2)，其中 d 是 n 的位数。因为我们需要遍历所有可能的两位数字组合。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_product_of_two_digits(n: int) -> int:
    """
    函数式接口 - 返回给定正整数 n 的任意两位数字相乘所得的最大乘积。
    """
    # 将整数 n 转换为字符串
    digits = str(n)
    
    # 初始化最大乘积为 0
    max_product = 0
    
    # 遍历字符串中的每个字符
    for i in range(len(digits)):
        for j in range(i, len(digits)):
            # 计算两两之间的乘积
            product = int(digits[i]) * int(digits[j])
            # 更新最大乘积
            max_product = max(max_product, product)
    
    return max_product


Solution = create_solution(max_product_of_two_digits)