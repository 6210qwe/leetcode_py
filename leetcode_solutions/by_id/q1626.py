# ============================================================================
# 问题描述
# ============================================================================
"""
1502. 判断能否形成等差数列 - 给你一个数字数组 arr 。 如果一个数列中，任意相邻两项的差总等于同一个常数，那么这个数列就称为 等差数列 。 如果可以重新排列数组形成等差数列，请返回 true ；否则，返回 false 。 示例 1： 输入：arr = [3,5,1] 输出：true 解释：对数组重新排序得到 [1,3,5] 或者 [5,3,1] ，任意相邻两项的差分别为 2 或 -2 ，可以形成等差数列。 示例 2： 输入：arr = [1,2,4] 输出：false 解释：无法通过重新排序得到等差数列。 提示： * 2 <= arr.length <= 1000 * -10^6 <= arr[i] <= 10^6
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序后检查相邻元素的差是否相等来判断是否可以形成等差数列。

算法步骤:
1. 对数组进行排序。
2. 计算前两个元素的差值作为公差。
3. 遍历排序后的数组，检查每对相邻元素的差值是否等于公差。

关键点:
- 排序后，如果所有相邻元素的差值都相等，则可以形成等差数列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 主要由排序操作决定。
空间复杂度: O(1) - 除了输入数组外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_make_arithmetic_progression(arr: List[int]) -> bool:
    """
    函数式接口 - 判断是否可以重新排列数组形成等差数列
    """
    # 对数组进行排序
    arr.sort()
    
    # 计算前两个元素的差值作为公差
    if len(arr) < 2:
        return False
    common_difference = arr[1] - arr[0]
    
    # 遍历排序后的数组，检查每对相邻元素的差值是否等于公差
    for i in range(2, len(arr)):
        if arr[i] - arr[i - 1] != common_difference:
            return False
    
    return True


Solution = create_solution(can_make_arithmetic_progression)