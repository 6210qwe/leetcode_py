def minimum_moves_to_equal_array_elements_ii(nums: List[int]) -> int:
    """
    函数式接口 - 最小操作次数使数组元素相等 II
    
    实现思路:
    中位数使所有元素到它的距离和最小。
    
    Args:
        nums: 整数数组
        
    Returns:
        最小操作次数
        
    Example:
        >>> minimum_moves_to_equal_array_elements_ii([1,2,3])
        2
    """
    nums.sort()
    n = len(nums)
    median = nums[n // 2]
    
    return sum(abs(num - median) for num in nums)


