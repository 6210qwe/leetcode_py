# ============================================================================
# 问题描述
# ============================================================================
"""
2000. 反转单词前缀 - 给你一个下标从 0 开始的字符串 word 和一个字符 ch 。找出 ch 第一次出现的下标 i ，反转 word 中从下标 0 开始、直到下标 i 结束（含下标 i ）的那段字符。如果 word 中不存在字符 ch ，则无需进行任何操作。 * 例如，如果 word = "abcdefd" 且 ch = 'd' ，那么你应该 反转 从下标 0 开始、直到下标 3 结束（含下标 3）的这段字符。结果字符串将会是 "dcbaefd" 。 返回 结果字符串 。 示例 1： 输入：word = "abcdefd", ch = 'd' 输出："dcbaefd" 解释："d" 第一次出现在下标 3 。 反转从下标 0 到下标 3（含下标 3）的这段字符，结果字符串是 "dcbaefd" 。 示例 2： 输入：word = "xyxzxe", ch = 'z' 输出："zxyxxe" 解释："z" 第一次也是唯一一次出现是在下标 3 。 反转从下标 0 到下标 3（含下标 3）的这段字符，结果字符串是 "zxyxxe" 。 示例 3： 输入：word = "abcd", ch = 'z' 输出："abcd" 解释："z" 不存在于 word 中。 无需执行反转操作，结果字符串是 "abcd" 。 提示： * 1 <= word.length <= 250 * word 由小写英文字母组成 * ch 是一个小写英文字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到字符 ch 在字符串 word 中第一次出现的位置，然后反转从开头到该位置的子字符串。

算法步骤:
1. 遍历字符串 word，找到字符 ch 第一次出现的位置 i。
2. 如果找到了字符 ch，反转从下标 0 到下标 i 的子字符串。
3. 将反转后的子字符串与剩余部分拼接起来，返回结果。

关键点:
- 使用切片操作来反转子字符串。
- 如果字符 ch 不存在于 word 中，直接返回原字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 word 的长度。最坏情况下需要遍历整个字符串。
空间复杂度: O(n)，因为字符串切片和拼接操作会创建新的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def reverse_prefix_of_word(word: str, ch: str) -> str:
    """
    函数式接口 - 反转单词前缀
    """
    # 找到字符 ch 第一次出现的位置
    index = word.find(ch)
    
    # 如果找到了字符 ch
    if index != -1:
        # 反转从下标 0 到下标 index 的子字符串
        prefix = word[:index + 1][::-1]
        # 拼接反转后的前缀和剩余部分
        return prefix + word[index + 1:]
    
    # 如果字符 ch 不存在于 word 中，直接返回原字符串
    return word


Solution = create_solution(reverse_prefix_of_word)