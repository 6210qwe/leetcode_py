"""
核心思想: 使用回溯法检查每个数的平方是否可以分割成若干连续子字符串，且这些子字符串对应的整数值之和等于该数。

算法步骤:
1. 初始化惩罚数为 0。
2. 对于从 1 到 n 的每个数 i，计算其平方 s。
3. 使用回溯法检查 s 是否可以分割成若干连续子字符串，且这些子字符串对应的整数值之和等于 i。
4. 如果可以，则将 i 的平方加到惩罚数中。
5. 返回最终的惩罚数。

关键点:
- 使用回溯法递归地检查每个可能的分割方式。
- 通过累加子字符串的整数值来判断是否满足条件。
"""

