# ============================================================================
# 问题描述
# ============================================================================
"""
31. 下一个排列 - 整数数组的一个 排列 就是将其所有成员以序列或线性顺序排列。 * 例如，arr = [1,2,3] ，以下这些都可以视作 arr 的排列：[1,2,3]、[1,3,2]、[3,1,2]、[2,3,1] 。 整数数组的 下一个排列 是指其整数的下一个字典序更大的排列。更正式地，如果数组的所有排列根据其字典顺序从小到大排列在一个容器中，那么数组的 下一个排列 就是在这个有序容器中排在它后面的那个排列。如果不存在下一个更大的排列，那么这个数组必须重排为字典序最小的排列（即，其元素按升序排列）。 * 例如，arr = [1,2,3] 的下一个排列是 [1,3,2] 。 * 类似地，arr = [2,3,1] 的下一个排列是 [3,1,2] 。 * 而 arr = [3,2,1] 的下一个排列是 [1,2,3] ，因为 [3,2,1] 不存在一个字典序更大的排列。 给你一个整数数组 nums ，找出 nums 的下一个排列。 必须 原地 [https://baike.baidu.com/item/%E5%8E%9F%E5%9C%B0%E7%AE%97%E6%B3%95]修改，只允许使用额外常数空间。 示例 1： 输入：nums = [1,2,3] 输出：[1,3,2] 示例 2： 输入：nums = [3,2,1] 输出：[1,2,3] 示例 3： 输入：nums = [1,1,5] 输出：[1,5,1] 提示： * 1 <= nums.length <= 100 * 0 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从右往左找到第一个降序的位置，然后交换并反转

算法步骤:
1. 从右往左找到第一个满足nums[i] < nums[i+1]的位置i
2. 如果找不到，说明数组是降序排列，直接反转整个数组
3. 如果找到了，从右往左找到第一个大于nums[i]的位置j
4. 交换nums[i]和nums[j]
5. 反转nums[i+1:]部分

关键点:
- 下一个排列需要找到字典序更大的排列
- 从右往左找第一个降序位置，这是可以增大的位置
- 交换后需要反转后面的部分，使其成为最小排列
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 最多遍历数组两次，n为数组长度
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def next_permutation(nums: List[int]) -> None:
    """
    函数式接口 - 双指针法实现
    
    实现思路:
    从右往左找到第一个降序的位置，然后交换并反转，得到下一个排列。
    
    Args:
        nums: 整数数组（原地修改）
        
    Returns:
        None（原地修改数组）
        
    Example:
        >>> nums = [1,2,3]
        >>> next_permutation(nums)
        >>> nums
        [1, 3, 2]
    """
    n = len(nums)
    if n <= 1:
        return
    
    # 从右往左找到第一个满足nums[i] < nums[i+1]的位置
    i = n - 2
    while i >= 0 and nums[i] >= nums[i + 1]:
        i -= 1
    
    # 如果找到了，从右往左找到第一个大于nums[i]的位置
    if i >= 0:
        j = n - 1
        while j > i and nums[j] <= nums[i]:
            j -= 1
        # 交换nums[i]和nums[j]
        nums[i], nums[j] = nums[j], nums[i]
    
    # 反转nums[i+1:]部分
    left, right = i + 1, n - 1
    while left < right:
        nums[left], nums[right] = nums[right], nums[left]
        left += 1
        right -= 1


