"""
3572. 选择不同 X 值三元组使 Y 值之和最大 - 给你两个整数数组 x 和 y，长度均为 n。你必须选择三个 不同 的下标 i ，j 和 k，满足以下条件： * x[i] != x[j] * x[j] != x[k] * x[k] != x[i] 你的目标是在满足这些条件下 最大化 y[i] + y[j] + y[k] 的值。返回通过选择这样一组三元组下标所能获得的 最大 可能和。 如果不存在这样的三元组，返回 -1。 示例 1： 输入：x = [1,2,1,3,2], y = [5,3,4,6,2] 输出：14 解释： * 选择 i = 0（x[i] = 1，y[i] = 5），j = 1（x[j] = 2，y[j] = 3），k = 3（x[k] = 3，y[k] = 6）。 * 选出的三个 x 中的值互不相同。5 + 3 + 6 = 14 是我们能获得的最大值。因此输出为 14。 示例 2： 输入：x = [1,2,1,2], y = [4,5,6,7] 输出：-1 解释： * x 中只有两个不同的值。因此输出为 -1。 提示： * n == x.length == y.length * 3 <= n <= 105 * 1 <= x[i], y[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个 x 值对应的最大 y 值，然后使用堆（优先队列）找到最大的三个 y 值。

算法步骤:
1. 使用哈希表记录每个 x 值对应的最大 y 值。
2. 将哈希表中的 y 值存入一个列表中。
3. 对列表进行排序，取最大的三个 y 值。
4. 如果找到的 y 值少于三个，返回 -1；否则返回这三个 y 值的和。

关键点:
- 使用哈希表去重并记录最大 y 值。
- 使用排序找到最大的三个 y 值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 x 和 y 的长度。主要时间开销在排序上。
空间复杂度: O(n)，哈希表和列表的空间开销。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(x: List[int], y: List[int]) -> int:
    """
    函数式接口 - 选择不同 X 值三元组使 Y 值之和最大
    """
    # 使用哈希表记录每个 x 值对应的最大 y 值
    max_y = {}
    for i in range(len(x)):
        if x[i] not in max_y or y[i] > max_y[x[i]]:
            max_y[x[i]] = y[i]

    # 将哈希表中的 y 值存入一个列表中
    y_values = list(max_y.values())

    # 对列表进行排序，取最大的三个 y 值
    y_values.sort(reverse=True)
    if len(y_values) < 3:
        return -1
    else:
        return sum(y_values[:3])


Solution = create_solution(solution_function_name)