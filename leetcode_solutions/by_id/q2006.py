"""
1894. 找到需要补充粉笔的学生编号 - 一个班级里有 n 个学生，编号为 0 到 n - 1 。每个学生会依次回答问题，编号为 0 的学生先回答，然后是编号为 1 的学生，以此类推，直到编号为 n - 1 的学生，然后老师会重复这个过程，重新从编号为 0 的学生开始回答问题。 给你一个长度为 n 且下标从 0 开始的整数数组 chalk 和一个整数 k 。一开始粉笔盒里总共有 k 支粉笔。当编号为 i 的学生回答问题时，他会消耗 chalk[i] 支粉笔。如果剩余粉笔数量 严格小于 chalk[i] ，那么学生 i 需要 补充 粉笔。 请你返回需要 补充 粉笔的学生 编号 。 示例 1： 输入：chalk = [5,1,5], k = 22 输出：0 解释：学生消耗粉笔情况如下： - 编号为 0 的学生使用 5 支粉笔，然后 k = 17 。 - 编号为 1 的学生使用 1 支粉笔，然后 k = 16 。 - 编号为 2 的学生使用 5 支粉笔，然后 k = 11 。 - 编号为 0 的学生使用 5 支粉笔，然后 k = 6 。 - 编号为 1 的学生使用 1 支粉笔，然后 k = 5 。 - 编号为 2 的学生使用 5 支粉笔，然后 k = 0 。 编号为 0 的学生没有足够的粉笔，所以他需要补充粉笔。 示例 2： 输入：chalk = [3,4,1,2], k = 25 输出：1 解释：学生消耗粉笔情况如下： - 编号为 0 的学生使用 3 支粉笔，然后 k = 22 。 - 编号为 1 的学生使用 4 支粉笔，然后 k = 18 。 - 编号为 2 的学生使用 1 支粉笔，然后 k = 17 。 - 编号为 3 的学生使用 2 支粉笔，然后 k = 15 。 - 编号为 0 的学生使用 3 支粉笔，然后 k = 12 。 - 编号为 1 的学生使用 4 支粉笔，然后 k = 8 。 - 编号为 2 的学生使用 1 支粉笔，然后 k = 7 。 - 编号为 3 的学生使用 2 支粉笔，然后 k = 5 。 - 编号为 0 的学生使用 3 支粉笔，然后 k = 2 。 编号为 1 的学生没有足够的粉笔，所以他需要补充粉笔。 提示： * chalk.length == n * 1 <= n <= 105 * 1 <= chalk[i] <= 105 * 1 <= k <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来快速找到需要补充粉笔的学生。

算法步骤:
1. 计算粉笔消耗的前缀和。
2. 对 k 取模以减少不必要的循环。
3. 使用二分查找找到第一个使得前缀和大于等于 k 的位置。

关键点:
- 使用前缀和可以快速计算出某个位置的累计消耗。
- 通过取模操作减少 k 的值，避免多次循环。
- 二分查找可以在 O(log n) 时间内找到需要补充粉笔的学生。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + log n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_student_to_replace(chalk: List[int], k: int) -> int:
    """
    返回需要补充粉笔的学生编号。

    :param chalk: 每个学生消耗的粉笔数量
    :param k: 初始粉笔数量
    :return: 需要补充粉笔的学生编号
    """
    # 计算前缀和
    prefix_sum = [0]
    for c in chalk:
        prefix_sum.append(prefix_sum[-1] + c)

    # 对 k 取模
    k %= prefix_sum[-1]

    # 使用二分查找找到第一个使得前缀和大于等于 k 的位置
    left, right = 0, len(prefix_sum) - 1
    while left < right:
        mid = (left + right) // 2
        if prefix_sum[mid] > k:
            right = mid
        else:
            left = mid + 1

    return left - 1


Solution = create_solution(find_student_to_replace)