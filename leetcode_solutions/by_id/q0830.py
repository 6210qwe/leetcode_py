"""
812. 最大三角形面积 - 给你一个由 X-Y 平面上的点组成的数组 points ，其中 points[i] = [xi, yi] 。从其中取任意三个不同的点组成三角形，返回能组成的最大三角形的面积。与真实值误差在 10-5 内的答案将会视为正确答案。 示例 1： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/04/1027.png] 输入：points = [[0,0],[0,1],[1,0],[0,2],[2,0]] 输出：2.00000 解释：输入中的 5 个点如上图所示，红色的三角形面积最大。 示例 2： 输入：points = [[1,0],[0,0],[0,1]] 输出：0.50000 提示： * 3 <= points.length <= 50 * -50 <= xi, yi <= 50 * 给出的所有点 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用海伦公式计算三角形面积，并通过三重循环枚举所有可能的三角形。

算法步骤:
1. 定义一个函数来计算三角形的面积。
2. 使用三重循环枚举所有可能的三个点组合。
3. 计算每个三角形的面积，并记录最大面积。

关键点:
- 使用海伦公式计算三角形面积。
- 通过三重循环枚举所有可能的三角形。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3)，其中 n 是 points 的长度。因为我们需要枚举所有的三个点组合。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def largest_triangle_area(points: List[List[int]]) -> float:
    """
    函数式接口 - 计算最大三角形面积
    """
    def area(p1, p2, p3):
        # 使用海伦公式计算三角形面积
        x1, y1 = p1
        x2, y2 = p2
        x3, y3 = p3
        return 0.5 * abs(x1 * (y2 - y3) + x2 * (y3 - y1) + x3 * (y1 - y2))

    max_area = 0.0
    n = len(points)
    for i in range(n):
        for j in range(i + 1, n):
            for k in range(j + 1, n):
                max_area = max(max_area, area(points[i], points[j], points[k]))
    return max_area


Solution = create_solution(largest_triangle_area)