"""
1130. 叶值的最小代价生成树 - 给你一个正整数数组 arr，考虑所有满足以下条件的二叉树： * 每个节点都有 0 个或是 2 个子节点。 * 数组 arr 中的值与树的中序遍历中每个叶节点的值一一对应。 * 每个非叶节点的值等于其左子树和右子树中叶节点的最大值的乘积。 在所有这样的二叉树中，返回每个非叶节点的值的最小可能总和。这个和的值是一个 32 位整数。 如果一个节点有 0 个子节点，那么该节点为叶节点。 示例 1： [https://assets.leetcode.com/uploads/2021/08/10/tree1.jpg] 输入：arr = [6,2,4] 输出：32 解释：有两种可能的树，第一种的非叶节点的总和为 36 ，第二种非叶节点的总和为 32 。 示例 2： [https://assets.leetcode.com/uploads/2021/08/10/tree2.jpg] 输入：arr = [4,11] 输出：44 提示： * 2 <= arr.length <= 40 * 1 <= arr[i] <= 15 * 答案保证是一个 32 位带符号整数，即小于 231 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调栈来维护当前处理的元素，并在每次弹出元素时计算非叶节点的值。

算法步骤:
1. 初始化一个栈，用于存储当前处理的元素。
2. 遍历数组 arr，对于每个元素：
   - 如果当前元素大于栈顶元素，则弹出栈顶元素，并计算非叶节点的值。
   - 将当前元素压入栈中。
3. 最后，处理栈中剩余的元素，计算非叶节点的值。

关键点:
- 使用单调栈来维护当前处理的元素，确保每次弹出元素时计算非叶节点的值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 arr 的长度。每个元素最多只会被压入和弹出栈一次。
空间复杂度: O(n)，栈的空间复杂度为 O(n)。
"""

# ============================================================================
