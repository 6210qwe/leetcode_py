"""
1800. 最大升序子数组和 - 给你一个正整数组成的数组 nums ，返回 nums 中一个 严格递增子数组 的最大可能元素和。 子数组是数组中的一个连续数字序列。 示例 1： 输入：nums = [10,20,30,5,10,50] 输出：65 解释：[5,10,50] 是元素和最大的升序子数组，最大元素和为 65 。 示例 2： 输入：nums = [10,20,30,40,50] 输出：150 解释：[10,20,30,40,50] 是元素和最大的升序子数组，最大元素和为 150 。 示例 3： 输入：nums = [12,17,15,13,10,11,12] 输出：33 解释：[10,11,12] 是元素和最大的升序子数组，最大元素和为 33 。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历找到所有严格递增的子数组，并计算其和，最后返回最大和。

算法步骤:
1. 初始化当前子数组和 `current_sum` 和最大子数组和 `max_sum`。
2. 遍历数组 `nums`：
   - 如果当前元素大于前一个元素，则将其加入当前子数组和 `current_sum`。
   - 否则，更新最大子数组和 `max_sum`，并重置当前子数组和 `current_sum` 为当前元素。
3. 最后更新一次最大子数组和 `max_sum`。

关键点:
- 通过一次遍历找到所有严格递增的子数组，并计算其和。
- 保持当前子数组和 `current_sum` 和最大子数组和 `max_sum` 的更新。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。我们只需要一次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_ascending_sum(nums: List[int]) -> int:
    """
    函数式接口 - 返回 nums 中一个严格递增子数组的最大可能元素和。
    """
    if not nums:
        return 0

    current_sum = max_sum = nums[0]

    for i in range(1, len(nums)):
        if nums[i] > nums[i - 1]:
            current_sum += nums[i]
        else:
            max_sum = max(max_sum, current_sum)
            current_sum = nums[i]

    max_sum = max(max_sum, current_sum)

    return max_sum


Solution = create_solution(max_ascending_sum)