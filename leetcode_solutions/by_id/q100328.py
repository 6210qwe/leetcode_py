from leetcode_solutions.utils.solution import create_solution


def reverse_words(message: str) -> str:
    """
    函数式接口 - 实现字符串中的单词反转
    """
    # 去除首尾空格
    message = message.strip()
    
    if not message:
        return ""
    
    n = len(message)
    i = n - 1
    j = n - 1
    result = []
    
    while i >= 0:
        # 找到单词的结尾
        while i >= 0 and message[i] != ' ':
            i -= 1
        # 添加单词
        result.append(message[i + 1:j + 1])
        # 跳过空格
        while i >= 0 and message[i] == ' ':
            i -= 1
        j = i
    
    return ' '.join(result)


Solution = create_solution(reverse_words)