"""
核心思想: 使用滑动窗口来计算从每个位置开始的最小翻转次数。

算法步骤:
1. 计算从每个位置开始的两种交替字符串（以 '0' 开头和以 '1' 开头）的最小翻转次数。
2. 通过滑动窗口技术，更新每个位置的翻转次数。
3. 返回最小的翻转次数。

关键点:
- 使用滑动窗口技术可以高效地计算每个位置的翻转次数。
- 通过前缀和数组来优化翻转次数的计算。
"""

