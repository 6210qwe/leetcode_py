"""
面试题 04.08. 首个共同祖先 - 设计并实现一个算法，找出二叉树中某两个节点的第一个共同祖先。不得将其他的节点存储在另外的数据结构中。注意：这不一定是二叉搜索树。 例如，给定如下二叉树: root = [3,5,1,6,2,0,8,null,null,7,4] 3 / \ 5 1 / \ / \ 6 2 0 8 / \ 7 4 示例 1： 输入：root = [3,5,1,6,2,0,8,null,null,7,4], p = 5, q = 1 输出：3 解释：节点 5 和节点 1 的最近公共祖先是节点 3。 示例 2： 输入：root = [3,5,1,6,2,0,8,null,null,7,4], p = 5, q = 4 输出：5 解释：节点 5 和节点 4 的最近公共祖先是节点 5。因为根据定义最近公共祖先节点可以为节点本身。 提示： 所有节点的值都是唯一的。 p、q 为不同节点且均存在于给定的二叉树中。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用递归的方法来查找两个节点的最近公共祖先。

算法步骤:
1. 如果当前节点是 None 或者等于 p 或 q，则返回当前节点。
2. 递归查找左子树和右子树。
3. 如果左右子树都找到了 p 或 q，则当前节点是最近公共祖先。
4. 如果只有一边找到了 p 或 q，则返回找到的那个节点。

关键点:
- 递归地查找左右子树。
- 如果左右子树都找到了 p 或 q，则当前节点是最近公共祖先。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数，因为每个节点最多被访问一次。
空间复杂度: O(h)，其中 h 是二叉树的高度，这是由于递归调用栈的深度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def lowest_common_ancestor(root: 'TreeNode', p: 'TreeNode', q: 'TreeNode') -> 'TreeNode':
    if not root or root == p or root == q:
        return root
    
    left = lowest_common_ancestor(root.left, p, q)
    right = lowest_common_ancestor(root.right, p, q)
    
    if left and right:
        return root
    return left if left else right

Solution = create_solution(lowest_common_ancestor)