"""
2810. 故障键盘 - 你的笔记本键盘存在故障，每当你在上面输入字符 'i' 时，它会反转你所写的字符串。而输入其他字符则可以正常工作。 给你一个下标从 0 开始的字符串 s ，请你用故障键盘依次输入每个字符。 返回最终笔记本屏幕上输出的字符串。 示例 1： 输入：s = "string" 输出："rtsng" 解释： 输入第 1 个字符后，屏幕上的文本是："s" 。 输入第 2 个字符后，屏幕上的文本是："st" 。 输入第 3 个字符后，屏幕上的文本是："str" 。 因为第 4 个字符是 'i' ，屏幕上的文本被反转，变成 "rts" 。 输入第 5 个字符后，屏幕上的文本是："rtsn" 。 输入第 6 个字符后，屏幕上的文本是： "rtsng" 。 因此，返回 "rtsng" 。 示例 2： 输入：s = "poiinter" 输出："ponter" 解释： 输入第 1 个字符后，屏幕上的文本是："p" 。 输入第 2 个字符后，屏幕上的文本是："po" 。 因为第 3 个字符是 'i' ，屏幕上的文本被反转，变成 "op" 。 因为第 4 个字符是 'i' ，屏幕上的文本被反转，变成 "po" 。 输入第 5 个字符后，屏幕上的文本是："pon" 。 输入第 6 个字符后，屏幕上的文本是："pont" 。 输入第 7 个字符后，屏幕上的文本是："ponte" 。 输入第 8 个字符后，屏幕上的文本是："ponter" 。 因此，返回 "ponter" 。 提示： * 1 <= s.length <= 100 * s 由小写英文字母组成 * s[0] != 'i'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串并根据字符 'i' 的出现来决定是否反转当前结果字符串。

算法步骤:
1. 初始化一个空的结果字符串。
2. 遍历输入字符串 s 中的每个字符。
3. 如果当前字符是 'i'，则反转结果字符串。
4. 否则，将当前字符追加到结果字符串中。
5. 返回最终的结果字符串。

关键点:
- 使用一个标志变量来记录是否需要反转字符串。
- 在遍历过程中动态更新结果字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历整个字符串一次。
空间复杂度: O(n)，结果字符串的长度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def faulty_keyboard(s: str) -> str:
    """
    函数式接口 - 实现故障键盘的功能
    """
    result = []
    for char in s:
        if char == 'i':
            result.reverse()
        else:
            result.append(char)
    return ''.join(result)


