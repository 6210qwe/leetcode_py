# ============================================================================
# 问题描述
# ============================================================================
"""
2086. 喂食仓鼠的最小食物桶数 - 给你一个下标从 0 开始的字符串 hamsters ，其中 hamsters[i] 要么是： * 'H' 表示有一个仓鼠在下标 i ，或者 * '.' 表示下标 i 是空的。 你将要在空的位置上添加一定数量的食物桶来喂养仓鼠。如果仓鼠的左边或右边至少有一个食物桶，就可以喂食它。更正式地说，如果你在位置 i - 1 或者 i + 1 放置一个食物桶，就可以喂养位置为 i 处的仓鼠。 在 空的位置 放置食物桶以喂养所有仓鼠的前提下，请你返回需要的 最少 食物桶数。如果无解请返回 -1 。 示例 1： [https://pic.leetcode.cn/1710141378-bfEGUX-image.png] 输入：hamsters = "H..H" 输出：2 解释： 我们可以在下标为 1 和 2 处放食物桶。 可以发现如果我们只放置 1 个食物桶，其中一只仓鼠将得不到喂养。 示例 2： [https://pic.leetcode.cn/1710141384-oLAScv-image.png] 输入：street = ".H.H." 输出：1 解释： 我们可以在下标为 2 处放置一个食物桶。 示例 3： 输入：street = ".HHH." 输出：-1 解释： 如果我们如图那样在每个空位放置食物桶，下标 2 处的仓鼠将吃不到食物。 提示： * 1 <= hamsters.length <= 105 * hamsters[i] 要么是 'H' ，要么是 '.' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，尽量在两个相邻的仓鼠之间放置一个食物桶，以减少所需的食物桶数量。

算法步骤:
1. 遍历字符串，检查每个位置。
2. 如果当前位置是 'H' 且前一个位置也是 'H'，则无法放置食物桶，返回 -1。
3. 如果当前位置是 'H' 且下一个位置是 'H'，则跳过当前 'H'，继续检查下一个位置。
4. 如果当前位置是 'H' 且下一个位置是 '.'，则在下一个位置放置一个食物桶，并跳过下一个位置。
5. 如果当前位置是 'H' 且下一个位置是 'H'，则在当前位置放置一个食物桶。
6. 统计所需的食物桶数量。

关键点:
- 尽量在两个相邻的仓鼠之间放置一个食物桶。
- 避免在两个连续的 'H' 之间没有空位的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度，因为我们需要遍历整个字符串一次。
空间复杂度: O(1)，因为我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(hamsters: str) -> int:
    """
    函数式接口 - 返回最少的食物桶数
    """
    n = len(hamsters)
    buckets = 0
    i = 0
    
    while i < n:
        if hamsters[i] == 'H':
            if i > 0 and hamsters[i - 1] == 'H':
                return -1  # 两个连续的 'H' 之间没有空位
            if i + 1 < n and hamsters[i + 1] == '.':
                buckets += 1
                i += 2  # 跳过下一个位置
            elif i + 1 < n and hamsters[i + 1] == 'H':
                buckets += 1
                i += 1  # 跳过当前 'H'
            else:
                buckets += 1
                i += 1
        else:
            i += 1
    
    return buckets


Solution = create_solution(solution_function_name)