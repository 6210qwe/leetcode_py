"""
核心思想: 使用前缀和后缀的乘积来计算每个可能的 x 值。

算法步骤:
1. 计算前缀乘积和后缀乘积。
2. 使用哈希表记录每个前缀乘积出现的次数。
3. 遍历后缀乘积，计算每个可能的 x 值，并更新结果数组。

关键点:
- 使用模运算来处理大数乘积。
- 通过哈希表优化查找前缀乘积的时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(k + n)
"""

# ============================================================================
