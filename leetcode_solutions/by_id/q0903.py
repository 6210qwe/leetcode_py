def rand10():
    """
    函数式接口 - 生成 [1,10] 范围内的均匀随机整数
    """
    while True:
        # 生成一个范围在 [1, 49] 的均匀随机整数
        num = (rand7() - 1) * 7 + rand7()
        # 如果生成的整数在 [1, 40] 范围内，则将其映射到 [1, 10] 范围内并返回
        if num <= 40:
            return (num - 1) % 10 + 1

