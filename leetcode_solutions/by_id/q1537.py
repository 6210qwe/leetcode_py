"""
1422. 分割字符串的最大得分 - 给你一个由若干 0 和 1 组成的字符串 s ，请你计算并返回将该字符串分割成两个 非空 子字符串（即 左 子字符串和 右 子字符串）所能获得的最大得分。 「分割字符串的得分」为 左 子字符串中 0 的数量加上 右 子字符串中 1 的数量。 示例 1： 输入：s = "011101" 输出：5 解释： 将字符串 s 划分为两个非空子字符串的可行方案有： 左子字符串 = "0" 且 右子字符串 = "11101"，得分 = 1 + 4 = 5 左子字符串 = "01" 且 右子字符串 = "1101"，得分 = 1 + 3 = 4 左子字符串 = "011" 且 右子字符串 = "101"，得分 = 1 + 2 = 3 左子字符串 = "0111" 且 右子字符串 = "01"，得分 = 1 + 1 = 2 左子字符串 = "01110" 且 右子字符串 = "1"，得分 = 2 + 1 = 3 示例 2： 输入：s = "00111" 输出：5 解释：当 左子字符串 = "00" 且 右子字符串 = "111" 时，我们得到最大得分 = 2 + 3 = 5 示例 3： 输入：s = "1111" 输出：3 提示： * 2 <= s.length <= 500 * 字符串 s 仅由字符 '0' 和 '1' 组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来计算每个可能的分割点的得分，并找到最大得分。

算法步骤:
1. 计算整个字符串中 '1' 的总数。
2. 遍历字符串，从第一个字符到倒数第二个字符，计算每个分割点的得分。
3. 更新最大得分。

关键点:
- 使用前缀和来高效计算每个分割点的得分。
- 通过一次遍历即可完成计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历字符串两次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_score_after_split(s: str) -> int:
    """
    函数式接口 - 计算并返回将字符串分割成两个非空子字符串所能获得的最大得分。
    """
    # 计算整个字符串中 '1' 的总数
    total_ones = s.count('1')
    
    # 初始化变量
    max_score = 0
    left_zeros = 0
    
    # 遍历字符串，从第一个字符到倒数第二个字符
    for i in range(len(s) - 1):
        if s[i] == '0':
            left_zeros += 1
        else:
            total_ones -= 1
        
        # 计算当前分割点的得分
        current_score = left_zeros + total_ones
        # 更新最大得分
        max_score = max(max_score, current_score)
    
    return max_score


Solution = create_solution(max_score_after_split)