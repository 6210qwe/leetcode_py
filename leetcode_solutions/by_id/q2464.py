"""
2380. 二进制字符串重新安排顺序需要的时间 - 给你一个二进制字符串 s 。在一秒之中，所有 子字符串 "01" 同时 被替换成 "10" 。这个过程持续进行到没有 "01" 存在。 请你返回完成这个过程所需要的秒数。 示例 1： 输入：s = "0110101" 输出：4 解释： 一秒后，s 变成 "1011010" 。 再过 1 秒后，s 变成 "1101100" 。 第三秒过后，s 变成 "1110100" 。 第四秒后，s 变成 "1111000" 。 此时没有 "01" 存在，整个过程花费 4 秒。 所以我们返回 4 。 示例 2： 输入：s = "11100" 输出：0 解释： s 中没有 "01" 存在，整个过程花费 0 秒。 所以我们返回 0 。 提示： * 1 <= s.length <= 1000 * s[i] 要么是 '0' ，要么是 '1' 。 进阶： 你能以 O(n) 的时间复杂度解决这个问题吗？
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一次遍历来计算所需的秒数。

算法步骤:
1. 初始化两个变量：`seconds` 用于记录所需的时间，`zeros` 用于记录当前遇到的 '0' 的数量。
2. 遍历字符串 `s`：
   - 如果遇到 '0'，增加 `zeros` 计数。
   - 如果遇到 '1'，更新 `seconds` 为 `max(seconds, zeros)`。
3. 返回 `seconds`。

关键点:
- 通过维护 '0' 的计数来确定每个 '1' 需要移动的最大步数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def time_needed_to_rearrange(s: str) -> int:
    """
    函数式接口 - 计算二进制字符串重新排列所需的时间
    """
    seconds = 0
    zeros = 0
    for char in s:
        if char == '0':
            zeros += 1
        else:
            seconds = max(seconds, zeros)
    return seconds


Solution = create_solution(time_needed_to_rearrange)