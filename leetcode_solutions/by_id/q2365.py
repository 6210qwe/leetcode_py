"""
2278. 字母在字符串中的百分比 - 给你一个字符串 s 和一个字符 letter ，返回在 s 中等于 letter 字符所占的 百分比 ，向下取整到最接近的百分比。 示例 1： 输入：s = "foobar", letter = "o" 输出：33 解释： 等于字母 'o' 的字符在 s 中占到的百分比是 2 / 6 * 100% = 33% ，向下取整，所以返回 33 。 示例 2： 输入：s = "jjjj", letter = "k" 输出：0 解释： 等于字母 'k' 的字符在 s 中占到的百分比是 0% ，所以返回 0 。 提示： * 1 <= s.length <= 100 * s 由小写英文字母组成 * letter 是一个小写英文字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算字符串中指定字符的数量，然后计算其占总长度的百分比。

算法步骤:
1. 遍历字符串，统计指定字符的出现次数。
2. 计算该字符占总长度的百分比，并向下取整。

关键点:
- 使用一次遍历来统计字符数量，时间复杂度为 O(n)。
- 计算百分比时使用整数除法，确保结果向下取整。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def percentage_of_letter_in_string(s: str, letter: str) -> int:
    """
    函数式接口 - 计算字符串中指定字符的百分比
    """
    # 统计指定字符的出现次数
    count = sum(1 for char in s if char == letter)
    
    # 计算百分比并向下取整
    return (count * 100) // len(s)


Solution = create_solution(percentage_of_letter_in_string)