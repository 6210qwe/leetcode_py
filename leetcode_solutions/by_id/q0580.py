def solution_function_name():
    """
    函数式接口 - 实现 SQL 查询来统计每个专业的学生人数
    """
    # SQL 查询实现
    query = """
    SELECT d.dept_name, COUNT(s.student_id) AS student_number
    FROM student s
    INNER JOIN department d ON s.dept_id = d.dept_id
    GROUP BY d.dept_name
    ORDER BY d.dept_name;
    """
    return query

