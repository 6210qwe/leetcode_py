# ============================================================================
# 问题描述
# ============================================================================
"""
1343. 大小为 K 且平均值大于等于阈值的子数组数目 - 给你一个整数数组 arr 和两个整数 k 和 threshold 。 请你返回长度为 k 且平均值大于等于 threshold 的子数组数目。 示例 1： 输入：arr = [2,2,2,2,5,5,5,8], k = 3, threshold = 4 输出：3 解释：子数组 [2,5,5],[5,5,5] 和 [5,5,8] 的平均值分别为 4，5 和 6 。其他长度为 3 的子数组的平均值都小于 4 （threshold 的值)。 示例 2： 输入：arr = [11,13,17,23,29,31,7,5,2,3], k = 3, threshold = 5 输出：6 解释：前 6 个长度为 3 的子数组平均值都大于 5 。注意平均值不是整数。 提示： * 1 <= arr.length <= 105 * 1 <= arr[i] <= 104 * 1 <= k <= arr.length * 0 <= threshold <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来计算每个长度为 k 的子数组的和，并检查其平均值是否大于等于阈值。

算法步骤:
1. 初始化一个变量 `window_sum` 来存储当前窗口的和。
2. 遍历数组，从第 0 个元素到第 k-1 个元素，计算初始窗口的和。
3. 检查初始窗口的平均值是否大于等于阈值，如果是则计数器加一。
4. 从第 k 个元素开始，使用滑动窗口更新窗口和，每次移动窗口时减去窗口最左边的元素并加上新进入窗口的元素。
5. 每次更新窗口和后，检查新的窗口平均值是否大于等于阈值，如果是则计数器加一。
6. 返回计数器的值。

关键点:
- 使用滑动窗口可以将时间复杂度降低到 O(n)。
- 通过维护窗口和而不是每次都重新计算和，可以进一步优化性能。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def number_of_subarrays(arr: List[int], k: int, threshold: int) -> int:
    """
    函数式接口 - 计算大小为 K 且平均值大于等于阈值的子数组数目
    """
    window_sum = 0
    count = 0
    
    # 计算初始窗口的和
    for i in range(k):
        window_sum += arr[i]
    
    # 检查初始窗口的平均值
    if window_sum / k >= threshold:
        count += 1
    
    # 使用滑动窗口更新窗口和
    for i in range(k, len(arr)):
        window_sum += arr[i] - arr[i - k]
        if window_sum / k >= threshold:
            count += 1
    
    return count


Solution = create_solution(number_of_subarrays)