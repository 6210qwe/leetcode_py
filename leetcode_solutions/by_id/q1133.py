def last_substring_in_lexicographical_order(s: str) -> str:
    """
    函数式接口 - 找出字符串 s 的所有子串并按字典序排列，返回排在最后的那个子串。
    """
    n = len(s)
    i, j, k = 0, 1, 0
    
    while j + k < n:
        if s[i + k] == s[j + k]:
            k += 1
            continue
        elif s[i + k] < s[j + k]:
            i = max(i + k + 1, j)
            j = i + 1
        else:
            j = j + k + 1
        k = 0
    
    return s[i:]


