"""
2460. 对数组执行操作 - 给你一个下标从 0 开始的数组 nums ，数组大小为 n ，且由 非负 整数组成。 你需要对数组执行 n - 1 步操作，其中第 i 步操作（从 0 开始计数）要求对 nums 中第 i 个元素执行下述指令： * 如果 nums[i] == nums[i + 1] ，则 nums[i] 的值变成原来的 2 倍，nums[i + 1] 的值变成 0 。否则，跳过这步操作。 在执行完 全部 操作后，将所有 0 移动 到数组的 末尾 。 * 例如，数组 [1,0,2,0,0,1] 将所有 0 移动到末尾后变为 [1,2,1,0,0,0] 。 返回结果数组。 注意 操作应当 依次有序 执行，而不是一次性全部执行。 示例 1： 输入：nums = [1,2,2,1,1,0] 输出：[1,4,2,0,0,0] 解释：执行以下操作： - i = 0: nums[0] 和 nums[1] 不相等，跳过这步操作。 - i = 1: nums[1] 和 nums[2] 相等，nums[1] 的值变成原来的 2 倍，nums[2] 的值变成 0 。数组变成 [1,4,0,1,1,0] 。 - i = 2: nums[2] 和 nums[3] 不相等，所以跳过这步操作。 - i = 3: nums[3] 和 nums[4] 相等，nums[3] 的值变成原来的 2 倍，nums[4] 的值变成 0 。数组变成 [1,4,0,2,0,0] 。 - i = 4: nums[4] 和 nums[5] 相等，nums[4] 的值变成原来的 2 倍，nums[5] 的值变成 0 。数组变成 [1,4,0,2,0,0] 。 执行完所有操作后，将 0 全部移动到数组末尾，得到结果数组 [1,4,2,0,0,0] 。 示例 2： 输入：nums = [0,1] 输出：[1,0] 解释：无法执行任何操作，只需要将 0 移动到末尾。 提示： * 2 <= nums.length <= 2000 * 0 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一次遍历处理数组中的元素，并在遍历过程中将非零元素移动到前面，最后将剩余位置填充为 0。

算法步骤:
1. 初始化一个指针 `non_zero_pos` 用于记录非零元素的位置。
2. 遍历数组，对于每个元素：
   - 如果当前元素和下一个元素相等，则将当前元素的值变成原来的 2 倍，并将下一个元素的值设为 0。
   - 如果当前元素不为 0，则将其移动到 `non_zero_pos` 位置，并更新 `non_zero_pos`。
3. 将 `non_zero_pos` 之后的所有位置填充为 0。

关键点:
- 通过一次遍历同时处理数组中的元素和移动非零元素，避免了多次遍历。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要一次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def apply_operations(nums: List[int]) -> List[int]:
    """
    函数式接口 - 对数组执行操作
    """
    non_zero_pos = 0  # 用于记录非零元素的位置
    
    for i in range(len(nums)):
        if i < len(nums) - 1 and nums[i] == nums[i + 1]:
            nums[i] *= 2
            nums[i + 1] = 0
        
        if nums[i] != 0:
            nums[non_zero_pos] = nums[i]
            non_zero_pos += 1
    
    for i in range(non_zero_pos, len(nums)):
        nums[i] = 0
    
    return nums


Solution = create_solution(apply_operations)