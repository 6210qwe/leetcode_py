"""
3281. 范围内整数的最大得分 - 给你一个整数数组 start 和一个整数 d，代表 n 个区间 [start[i], start[i] + d]。 你需要选择 n 个整数，其中第 i 个整数必须属于第 i 个区间。所选整数的 得分 定义为所选整数两两之间的 最小 绝对差。 返回所选整数的 最大可能得分 。 示例 1： 输入： start = [6,0,3], d = 2 输出： 4 解释： 可以选择整数 8, 0 和 4 获得最大可能得分，得分为 min(|8 - 0|, |8 - 4|, |0 - 4|)，等于 4。 示例 2： 输入： start = [2,6,13,13], d = 5 输出： 5 解释： 可以选择整数 2, 7, 13 和 18 获得最大可能得分，得分为 min(|2 - 7|, |2 - 13|, |2 - 18|, |7 - 13|, |7 - 18|, |13 - 18|)，等于 5。 提示： * 2 <= start.length <= 105 * 0 <= start[i] <= 109 * 0 <= d <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法选择每个区间的最小值，然后计算这些最小值之间的最小绝对差。

算法步骤:
1. 将每个区间的左端点加上 d，得到每个区间的右端点。
2. 对所有区间的右端点进行排序。
3. 计算相邻两个右端点之间的最小差值，即为所选整数的最大可能得分。

关键点:
- 选择每个区间的最小值可以确保最小绝对差最大化。
- 通过排序和遍历，可以高效地找到最小绝对差。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 start 的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(start: List[int], d: int) -> int:
    """
    函数式接口 - 实现
    """
    # 计算每个区间的右端点
    end_points = [s + d for s in start]
    
    # 对右端点进行排序
    end_points.sort()
    
    # 初始化最小绝对差
    min_diff = float('inf')
    
    # 计算相邻两个右端点之间的最小差值
    for i in range(1, len(end_points)):
        min_diff = min(min_diff, end_points[i] - end_points[i - 1])
    
    return min_diff


Solution = create_solution(solution_function_name)