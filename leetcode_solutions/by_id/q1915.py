"""
1790. 仅执行一次字符串交换能否使两个字符串相等 - 给你长度相等的两个字符串 s1 和 s2 。一次 字符串交换 操作的步骤如下：选出某个字符串中的两个下标（不必不同），并交换这两个下标所对应的字符。 如果对 其中一个字符串 执行 最多一次字符串交换 就可以使两个字符串相等，返回 true ；否则，返回 false 。 示例 1： 输入：s1 = "bank", s2 = "kanb" 输出：true 解释：例如，交换 s2 中的第一个和最后一个字符可以得到 "bank" 示例 2： 输入：s1 = "attack", s2 = "defend" 输出：false 解释：一次字符串交换无法使两个字符串相等 示例 3： 输入：s1 = "kelb", s2 = "kelb" 输出：true 解释：两个字符串已经相等，所以不需要进行字符串交换 示例 4： 输入：s1 = "abcd", s2 = "dcba" 输出：false 提示： * 1 <= s1.length, s2.length <= 100 * s1.length == s2.length * s1 和 s2 仅由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过比较两个字符串的字符差异来判断是否可以通过一次交换使它们相等。

算法步骤:
1. 检查两个字符串是否完全相同，如果相同则直接返回 True。
2. 遍历两个字符串，记录所有不同的字符对。
3. 如果不同字符对的数量不等于 2，则返回 False。
4. 检查这两个不同字符对是否可以通过交换使字符串相等。

关键点:
- 只有当两个字符串中有且仅有两个位置的字符不同，并且这两个字符可以互换时，才可以通过一次交换使两个字符串相等。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们需要遍历字符串一次来找到所有不同的字符对。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s1: str, s2: str) -> bool:
    """
    函数式接口 - 判断是否可以通过一次交换使两个字符串相等
    """
    if s1 == s2:
        return True

    diff = []
    for i in range(len(s1)):
        if s1[i] != s2[i]:
            diff.append((s1[i], s2[i]))
            if len(diff) > 2:
                return False

    return len(diff) == 2 and diff[0] == diff[1][::-1]


Solution = create_solution(solution_function_name)