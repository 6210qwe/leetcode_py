"""
3325. 字符至少出现 K 次的子字符串 I - 给你一个字符串 s 和一个整数 k，在 s 的所有子字符串中，请你统计并返回 至少有一个 字符 至少出现 k 次的子字符串总数。 子字符串 是字符串中的一个连续、 非空 的字符序列。 示例 1： 输入： s = "abacb", k = 2 输出： 4 解释： 符合条件的子字符串如下： * "aba"（字符 'a' 出现 2 次）。 * "abac"（字符 'a' 出现 2 次）。 * "abacb"（字符 'a' 出现 2 次）。 * "bacb"（字符 'b' 出现 2 次）。 示例 2： 输入： s = "abcde", k = 1 输出： 15 解释： 所有子字符串都有效，因为每个字符至少出现一次。 提示： * 1 <= s.length <= 3000 * 1 <= k <= s.length * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和哈希表来统计每个子字符串中字符的频率，并检查是否有字符至少出现 k 次。

算法步骤:
1. 初始化左右指针和哈希表，用于记录当前窗口内字符的频率。
2. 移动右指针扩展窗口，更新哈希表。
3. 检查当前窗口内是否有字符至少出现 k 次，如果有，则计算以当前左指针为起点的有效子字符串数量。
4. 移动左指针收缩窗口，更新哈希表。
5. 重复步骤 2-4 直到右指针遍历完整个字符串。

关键点:
- 使用滑动窗口动态维护子字符串的字符频率。
- 通过哈希表高效统计字符频率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串 s 的长度。最坏情况下，每个字符都需要作为起始点进行一次完整的遍历。
空间复杂度: O(1)，哈希表的大小最多为 26（小写字母的数量）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_substrings_with_k_frequency(s: str, k: int) -> int:
    """
    函数式接口 - 统计字符串 s 中至少有一个字符至少出现 k 次的子字符串总数。
    """
    def at_least_k_freq(freq, k):
        return any(v >= k for v in freq.values())

    n = len(s)
    result = 0

    for i in range(n):
        freq = {}
        for j in range(i, n):
            freq[s[j]] = freq.get(s[j], 0) + 1
            if at_least_k_freq(freq, k):
                result += 1

    return result


Solution = create_solution(count_substrings_with_k_frequency)