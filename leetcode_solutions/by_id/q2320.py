"""
2200. 找出数组中的所有 K 近邻下标 - 给你一个下标从 0 开始的整数数组 nums 和两个整数 key 和 k 。K 近邻下标 是 nums 中的一个下标 i ，并满足至少存在一个下标 j 使得 |i - j| <= k 且 nums[j] == key 。 以列表形式返回按 递增顺序 排序的所有 K 近邻下标。 示例 1： 输入：nums = [3,4,9,1,3,9,5], key = 9, k = 1 输出：[1,2,3,4,5,6] 解释：因此，nums[2] == key 且 nums[5] == key。 - 对下标 0 ，|0 - 2| > k 且 |0 - 5| > k，所以不存在 j 使得 |0 - j| <= k 且 nums[j] == key。所以 0 不是一个 K 近邻下标。 - 对下标 1 ，|1 - 2| <= k 且 nums[2] == key，所以 1 是一个 K 近邻下标。 - 对下标 2 ，|2 - 2| <= k 且 nums[2] == key，所以 2 是一个 K 近邻下标。 - 对下标 3 ，|3 - 2| <= k 且 nums[2] == key，所以 3 是一个 K 近邻下标。 - 对下标 4 ，|4 - 5| <= k 且 nums[5] == key，所以 4 是一个 K 近邻下标。 - 对下标 5 ，|5 - 5| <= k 且 nums[5] == key，所以 5 是一个 K 近邻下标。 - 对下标 6 ，|6 - 5| <= k 且 nums[5] == key，所以 6 是一个 K 近邻下标。 因此，按递增顺序返回 [1,2,3,4,5,6] 。 示例 2： 输入：nums = [2,2,2,2,2], key = 2, k = 2 输出：[0,1,2,3,4] 解释：对 nums 的所有下标 i ，总存在某个下标 j 使得 |i - j| <= k 且 nums[j] == key，所以每个下标都是一个 K 近邻下标。 因此，返回 [0,1,2,3,4] 。 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= 1000 * key 是数组 nums 中的一个整数 * 1 <= k <= nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针来遍历数组，找到所有符合条件的下标。

算法步骤:
1. 初始化一个空的结果列表 `result`。
2. 遍历数组 `nums`，记录所有等于 `key` 的下标到 `key_indices` 列表中。
3. 再次遍历数组 `nums`，对于每个下标 `i`，检查是否存在 `key_indices` 中的下标 `j` 使得 `|i - j| <= k`。
4. 如果存在，则将 `i` 添加到 `result` 列表中。
5. 返回 `result` 列表。

关键点:
- 使用双指针来高效地查找符合条件的下标。
- 通过预处理 `key_indices` 列表来减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。我们需要遍历数组两次，每次遍历的时间复杂度是 O(n)。
空间复杂度: O(n)，最坏情况下 `key_indices` 列表的长度可能与 `nums` 的长度相同。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_k_distant_indices(nums: List[int], key: int, k: int) -> List[int]:
    """
    函数式接口 - 找出数组中的所有 K 近邻下标
    """
    result = []
    key_indices = [i for i, num in enumerate(nums) if num == key]
    
    for i in range(len(nums)):
        for j in key_indices:
            if abs(i - j) <= k:
                result.append(i)
                break
    
    return result


Solution = create_solution(find_k_distant_indices)