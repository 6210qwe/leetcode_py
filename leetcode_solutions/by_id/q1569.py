"""
1458. 两个子序列的最大点积 - 给你两个数组 nums1 和 nums2 。 请你返回 nums1 和 nums2 中两个长度相同的 非空 子序列的最大点积。 数组的非空子序列是通过删除原数组中某些元素（可能一个也不删除）后剩余数字组成的序列，但不能改变数字间相对顺序。比方说，[2,3,5] 是 [1,2,3,4,5] 的一个子序列而 [1,5,3] 不是。 示例 1： 输入：nums1 = [2,1,-2,5], nums2 = [3,0,-6] 输出：18 解释：从 nums1 中得到子序列 [2,-2] ，从 nums2 中得到子序列 [3,-6] 。 它们的点积为 (2*3 + (-2)*(-6)) = 18 。 示例 2： 输入：nums1 = [3,-2], nums2 = [2,-6,7] 输出：21 解释：从 nums1 中得到子序列 [3] ，从 nums2 中得到子序列 [7] 。 它们的点积为 (3*7) = 21 。 示例 3： 输入：nums1 = [-1,-1], nums2 = [1,1] 输出：-1 解释：从 nums1 中得到子序列 [-1] ，从 nums2 中得到子序列 [1] 。 它们的点积为 -1 。 提示： * 1 <= nums1.length, nums2.length <= 500 * -1000 <= nums1[i], nums2[i] <= 1000 点积： 定义 a = [a1, a2,…, an] 和 b = [b1, b2,…, bn] 的点积为： \mathbf{a}\cdot \mathbf{b} = \sum_{i=1}^n a_ib_i = a_1b_1 + a_2b_2 + \cdots + a_nb_n [https://pic.leetcode.cn/1666164309-PBJMQp-image.png] 这里的 Σ 指示总和符号。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示 nums1 的前 i 个元素和 nums2 的前 j 个元素的最大点积。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][j] 表示 nums1 的前 i 个元素和 nums2 的前 j 个元素的最大点积。
2. 遍历 nums1 和 nums2，更新 dp 数组。
3. 对于每个 dp[i][j]，我们有以下几种选择：
   - 不选择 nums1[i-1] 和 nums2[j-1]，则 dp[i][j] = max(dp[i-1][j], dp[i][j-1])。
   - 选择 nums1[i-1] 和 nums2[j-1]，则 dp[i][j] = max(dp[i][j], dp[i-1][j-1] + nums1[i-1] * nums2[j-1])。
4. 最终结果保存在 dp[m][n] 中，其中 m 和 n 分别是 nums1 和 nums2 的长度。

关键点:
- 处理边界情况，确保 dp 数组初始化正确。
- 动态规划的状态转移方程要正确。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是 nums1 和 nums2 的长度。
空间复杂度: O(m * n)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums1: List[int], nums2: List[int]) -> int:
    """
    函数式接口 - 计算两个数组的非空子序列的最大点积
    """
    m, n = len(nums1), len(nums2)
    dp = [[float('-inf')] * (n + 1) for _ in range(m + 1)]
    
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            dp[i][j] = max(
                dp[i - 1][j],
                dp[i][j - 1],
                dp[i - 1][j - 1] + nums1[i - 1] * nums2[j - 1],
                nums1[i - 1] * nums2[j - 1]
            )
    
    return dp[m][n]


Solution = create_solution(solution_function_name)