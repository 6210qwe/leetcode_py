"""
核心思想: 使用双指针找到需要交换的字符位置，然后生成下一个排列并重新构建回文串。

算法步骤:
1. 将输入字符串转换为列表，以便进行原地修改。
2. 使用双指针从中间向两边遍历，找到第一个不满足递增顺序的位置。
3. 从该位置开始，找到右侧比当前字符大的最小字符，并交换它们。
4. 将右侧部分反转，使其成为最小的字典序排列。
5. 重新构建回文串。

关键点:
- 使用双指针找到需要交换的字符位置。
- 生成下一个排列并重新构建回文串。
"""

# ============================================================================
