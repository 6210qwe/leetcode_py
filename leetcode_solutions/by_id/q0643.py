"""
643. 子数组最大平均数 I - 给你一个由 n 个元素组成的整数数组 nums 和一个整数 k 。 请你找出平均数最大且 长度为 k 的连续子数组，并输出该最大平均数。 任何误差小于 10-5 的答案都将被视为正确答案。 示例 1： 输入：nums = [1,12,-5,-6,50,3], k = 4 输出：12.75 解释：最大平均数 (12-5-6+50)/4 = 51/4 = 12.75 示例 2： 输入：nums = [5], k = 1 输出：5.00000 提示： * n == nums.length * 1 <= k <= n <= 105 * -104 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来计算长度为 k 的子数组的和，并在滑动过程中更新最大和。

算法步骤:
1. 初始化第一个窗口的和。
2. 滑动窗口，每次移动一步，更新当前窗口的和。
3. 在每次滑动过程中，更新最大和。
4. 最后返回最大和除以 k 的结果。

关键点:
- 使用滑动窗口技术，可以在 O(n) 时间复杂度内解决问题。
- 只需要常数级别的额外空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int], k: int) -> float:
    """
    函数式接口 - 计算长度为 k 的子数组的最大平均数
    """
    if not nums or k <= 0:
        return 0.0

    n = len(nums)
    if k > n:
        return 0.0

    # 初始化第一个窗口的和
    window_sum = sum(nums[:k])
    max_sum = window_sum

    # 滑动窗口
    for i in range(k, n):
        window_sum += nums[i] - nums[i - k]
        max_sum = max(max_sum, window_sum)

    return max_sum / k


Solution = create_solution(solution_function_name)