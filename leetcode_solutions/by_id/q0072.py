"""
72. 编辑距离 - 给你两个单词 word1 和 word2， 请返回将 word1 转换成 word2 所使用的最少操作数 。 你可以对一个单词进行如下三种操作： * 插入一个字符 * 删除一个字符 * 替换一个字符 示例 1： 输入：word1 = "horse", word2 = "ros" 输出：3 解释： horse -> rorse (将 'h' 替换为 'r') rorse -> rose (删除 'r') rose -> ros (删除 'e') 示例 2： 输入：word1 = "intention", word2 = "execution" 输出：5 解释： intention -> inention (删除 't') inention -> enention (将 'i' 替换为 'e') enention -> exention (将 'n' 替换为 'x') exention -> exection (将 'n' 替换为 'c') exection -> execution (插入 'u') 提示： * 0 <= word1.length, word2.length <= 500 * word1 和 word2 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划，dp[i][j]表示word1的前i个字符转换为word2的前j个字符的最少操作数

算法步骤:
1. 初始化：dp[0][j] = j（插入j个字符），dp[i][0] = i（删除i个字符）
2. 状态转移：
   - 如果word1[i-1] == word2[j-1]：dp[i][j] = dp[i-1][j-1]（无需操作）
   - 否则：dp[i][j] = 1 + min(
       dp[i-1][j]（删除），
       dp[i][j-1]（插入），
       dp[i-1][j-1]（替换）
     )
3. 返回dp[m][n]

关键点:
- 可以优化空间复杂度为O(n)，使用一维数组
- 三种操作：插入、删除、替换
- 时间复杂度O(m*n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m*n) - m和n分别为两个字符串的长度
空间复杂度: O(n) - 优化后只使用一维数组
"""

# ============================================================================
# 代码实现
# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def min_distance(word1: str, word2: str) -> int:
    """
    函数式接口 - 动态规划（空间优化）
    
    实现思路:
    使用动态规划计算编辑距离，优化空间复杂度为O(n)。
    
    Args:
        word1: 第一个单词
        word2: 第二个单词
        
    Returns:
        将word1转换成word2的最少操作数
        
    Example:
        >>> min_distance("horse", "ros")
        3
    """
    m, n = len(word1), len(word2)
    
    # 使用一维数组
    dp = list(range(n + 1))
    
    for i in range(1, m + 1):
        prev = dp[0]
        dp[0] = i
        for j in range(1, n + 1):
            temp = dp[j]
            if word1[i - 1] == word2[j - 1]:
                dp[j] = prev
            else:
                dp[j] = 1 + min(dp[j], dp[j - 1], prev)
            prev = temp
    
    return dp[n]


