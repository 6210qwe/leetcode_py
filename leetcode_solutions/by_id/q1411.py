"""
1290. 二进制链表转整数 - 给你一个单链表的引用结点 head。链表中每个结点的值不是 0 就是 1。已知此链表是一个整数数字的二进制表示形式。 请你返回该链表所表示数字的 十进制值 。 最高位 在链表的头部。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2019/12/15/graph-1.png] 输入：head = [1,0,1] 输出：5 解释：二进制数 (101) 转化为十进制数 (5) 示例 2： 输入：head = [0] 输出：0 提示： * 链表不为空。 * 链表的结点总数不超过 30。 * 每个结点的值不是 0 就是 1。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历链表，将二进制数转换为十进制数。

算法步骤:
1. 初始化结果变量 `result` 为 0。
2. 遍历链表，对于每个节点，将当前结果左移一位（相当于乘以 2），然后加上当前节点的值。
3. 返回最终结果。

关键点:
- 使用位运算来高效地进行二进制到十进制的转换。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是链表的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.solution import create_solution


def convert_binary_number(head: Optional[ListNode]) -> int:
    """
    函数式接口 - 将二进制链表转换为十进制整数
    """
    result = 0
    current = head
    while current:
        result = (result << 1) | current.val
        current = current.next
    return result


Solution = create_solution(convert_binary_number)