"""
核心思想: 使用集合来记录有销售记录的卖家，并找出没有销售记录的卖家。

算法步骤:
1. 从 `Sales` 表中提取所有有销售记录的卖家 ID，并存入一个集合。
2. 从 `Seller` 表中提取所有卖家 ID，并存入另一个集合。
3. 通过集合差集操作找出没有销售记录的卖家 ID。
4. 返回这些卖家的详细信息。

关键点:
- 使用集合操作来提高查找效率。
- 确保返回的卖家信息符合题目要求。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 `Sales` 表的行数，m 是 `Seller` 表的行数。
空间复杂度: O(n + m)，用于存储两个集合。
"""

