"""
3550. 数位和等于下标的最小下标 - 给你一个整数数组 nums 。 返回满足 nums[i] 的数位和（每一位数字相加求和）等于 i 的 最小 下标 i 。 如果不存在满足要求的下标，返回 -1 。 示例 1： 输入：nums = [1,3,2] 输出：2 解释： * nums[2] = 2，其数位和等于 2 ，与其下标 i = 2 相等。因此，输出为 2 。 示例 2： 输入：nums = [1,10,11] 输出：1 解释： * nums[1] = 10，其数位和等于 1 + 0 = 1，与其下标 i = 1 相等。 * nums[2] = 11，其数位和等于是 1 + 1 = 2，与其下标 i = 2 相等。 * 由于下标 1 是满足要求的最小下标，输出为 1 。 示例 3： 输入：nums = [1,2,3] 输出：-1 解释： * 由于不存在满足要求的下标，输出为 -1 。 提示： * 1 <= nums.length <= 100 * 0 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，计算每个元素的数位和，并检查是否等于其下标。

算法步骤:
1. 定义一个辅助函数 `digit_sum` 来计算一个整数的数位和。
2. 遍历数组 `nums`，对于每个元素 `nums[i]`，计算其数位和。
3. 检查数位和是否等于当前下标 `i`，如果是，则返回该下标。
4. 如果遍历完数组后没有找到满足条件的下标，返回 -1。

关键点:
- 使用辅助函数简化数位和的计算。
- 一次遍历数组，时间复杂度为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def digit_sum(num: int) -> int:
    """计算一个整数的数位和"""
    return sum(int(digit) for digit in str(num))


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找到数位和等于下标的最小下标
    """
    for i, num in enumerate(nums):
        if digit_sum(num) == i:
            return i
    return -1


Solution = create_solution(solution_function_name)