"""
1018. 可被 5 整除的二进制前缀 - 给定一个二进制数组 nums ( 索引从0开始 )。 我们将xi 定义为其二进制表示形式为子数组 nums[0..i] (从最高有效位到最低有效位)。 * 例如，如果 nums =[1,0,1] ，那么 x0 = 1, x1 = 2, 和 x2 = 5。 返回布尔值列表 answer，只有当 xi 可以被 5 整除时，答案 answer[i] 为 true，否则为 false。 示例 1： 输入：nums = [0,1,1] 输出：[true,false,false] 解释： 输入数字为 0, 01, 011；也就是十进制中的 0, 1, 3 。只有第一个数可以被 5 整除，因此 answer[0] 为 true 。 示例 2： 输入：nums = [1,1,1] 输出：[false,false,false] 提示： * 1 <= nums.length <= 105 * nums[i] 仅为 0 或 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一个变量来累积当前的二进制前缀，并在每次迭代中检查其是否能被 5 整除。

算法步骤:
1. 初始化一个变量 `current` 为 0，用于存储当前的二进制前缀。
2. 遍历数组 `nums`，对于每个元素：
   - 更新 `current` 为 `current * 2 + num`。
   - 检查 `current` 是否能被 5 整除，并将结果添加到答案列表中。
   - 对 `current` 取模 5，以防止溢出。

关键点:
- 使用取模操作来保持 `current` 的大小在合理范围内。
- 通过累积和取模操作，可以在 O(1) 空间复杂度内解决问题。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> List[bool]:
    """
    函数式接口 - 判断二进制前缀是否能被 5 整除
    """
    current = 0
    result = []
    
    for num in nums:
        current = (current * 2 + num) % 5
        result.append(current == 0)
    
    return result


Solution = create_solution(solution_function_name)