# 问题描述
# ============================================================================
"""
3472. 至多 K 次操作后的最长回文子序列 - 给你一个字符串 s 和一个整数 k。 在一次操作中，你可以将任意位置的字符替换为字母表中相邻的字符（字母表是循环的，因此 'z' 的下一个字母是 'a'）。例如，将 'a' 替换为下一个字母结果是 'b'，将 'a' 替换为上一个字母结果是 'z'；同样，将 'z' 替换为下一个字母结果是 'a'，替换为上一个字母结果是 'y'。 返回在进行 最多 k 次操作后，s 的 最长回文子序列 的长度。 子序列 是一个 非空 字符串，可以通过删除原字符串中的某些字符（或不删除任何字符）并保持剩余字符的相对顺序得到。 回文 是正着读和反着读都相同的字符串。 示例 1： 输入: s = "abced", k = 2 输出: 3 解释: * 将 s[1] 替换为下一个字母，得到 "acced"。 * 将 s[4] 替换为上一个字母，得到 "accec"。 子序列 "ccc" 形成一个长度为 3 的回文，这是最长的回文子序列。 示例 2： 输入: s = "aaazzz", k = 4 输出: 6 解释: * 将 s[0] 替换为上一个字母，得到 "zaazzz"。 * 将 s[4] 替换为下一个字母，得到 "zaazaz"。 * 将 s[3] 替换为下一个字母，得到 "zaaaaz"。 整个字符串形成一个长度为 6 的回文。 提示: * 1 <= s.length <= 200 * 1 <= k <= 200 * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j][k] 表示从 s[i] 到 s[j] 且最多进行 k 次操作后的最长回文子序列的长度。

算法步骤:
1. 初始化一个三维数组 dp，其中 dp[i][j][k] 表示从 s[i] 到 s[j] 且最多进行 k 次操作后的最长回文子序列的长度。
2. 如果 i > j，dp[i][j][k] = 0。
3. 如果 i == j，dp[i][j][k] = 1。
4. 如果 s[i] == s[j]，dp[i][j][k] = dp[i+1][j-1][k] + 2。
5. 如果 s[i] != s[j]，我们需要考虑两种情况：
   - 不进行替换操作：dp[i][j][k] = max(dp[i+1][j][k], dp[i][j-1][k])。
   - 进行替换操作：dp[i][j][k] = max(dp[i][j][k], dp[i+1][j-1][k-1] + 2)。

关键点:
- 动态规划的状态转移方程。
- 处理边界条件。
- 使用递归和记忆化搜索来优化时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2 * k)，其中 n 是字符串 s 的长度，k 是最大操作次数。
空间复杂度: O(n^2 * k)，用于存储动态规划的状态。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_palindromic_subsequence(s: str, k: int) -> int:
    n = len(s)
    dp = [[[0] * (k + 1) for _ in range(n)] for _ in range(n)]

    def get_diff(c1, c2):
        return min(abs(ord(c1) - ord(c2)), 26 - abs(ord(c1) - ord(c2)))

    def dfs(i, j, k):
        if i > j:
            return 0
        if i == j:
            return 1
        if dp[i][j][k] != 0:
            return dp[i][j][k]
        
        if s[i] == s[j]:
            dp[i][j][k] = dfs(i + 1, j - 1, k) + 2
        else:
            dp[i][j][k] = max(dfs(i + 1, j, k), dfs(i, j - 1, k))
            if k > 0:
                diff = get_diff(s[i], s[j])
                if diff <= k:
                    dp[i][j][k] = max(dp[i][j][k], dfs(i + 1, j - 1, k - diff) + 2)
        
        return dp[i][j][k]

    return dfs(0, n - 1, k)


Solution = create_solution(longest_palindromic_subsequence)