"""
171. Excel 表列序号 - 给你一个字符串 columnTitle ，表示 Excel 表格中的列名称。返回 该列名称对应的列序号 。 例如： A -> 1 B -> 2 C -> 3 ... Z -> 26 AA -> 27 AB -> 28 ... 示例 1: 输入: columnTitle = "A" 输出: 1 示例 2: 输入: columnTitle = "AB" 输出: 28 示例 3: 输入: columnTitle = "ZY" 输出: 701 提示： * 1 <= columnTitle.length <= 7 * columnTitle 仅由大写英文组成 * columnTitle 在范围 ["A", "FXSHRXW"] 内
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 26进制转10进制，从右到左计算

算法步骤:
1. 从右到左遍历字符串
2. 每个字符转换为对应的数字（A=1, B=2, ..., Z=26）
3. 乘以26的相应次方，累加结果

关键点:
- 26进制转10进制，注意从1开始
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - n为字符串长度
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def excel_sheet_column_number(columnTitle: str) -> int:
    """
    函数式接口 - Excel表列序号
    
    实现思路:
    26进制转10进制，从右到左计算。
    
    Args:
        columnTitle: 列名称字符串
        
    Returns:
        对应的列序号
        
    Example:
        >>> excel_sheet_column_number("AB")
        28
    """
    result = 0
    
    for char in columnTitle:
        result = result * 26 + (ord(char) - ord('A') + 1)
    
    return result


