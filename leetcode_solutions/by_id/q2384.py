"""
2236. 判断根结点是否等于子结点之和 - 给你一个 二叉树 的根结点 root，该二叉树由恰好 3 个结点组成：根结点、左子结点和右子结点。 如果根结点值等于两个子结点值之和，返回 true ，否则返回 false 。 示例 1： [https://assets.leetcode.com/uploads/2022/04/08/graph3drawio.png] 输入：root = [10,4,6] 输出：true 解释：根结点、左子结点和右子结点的值分别是 10 、4 和 6 。 由于 10 等于 4 + 6 ，因此返回 true 。 示例 2： [https://assets.leetcode.com/uploads/2022/04/08/graph3drawio-1.png] 输入：root = [5,3,1] 输出：false 解释：根结点、左子结点和右子结点的值分别是 5 、3 和 1 。 由于 5 不等于 3 + 1 ，因此返回 false 。 提示： * 树只包含根结点、左子结点和右子结点 * -100 <= Node.val <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 检查根结点的值是否等于其左子结点和右子结点的值之和。

算法步骤:
1. 获取根结点的值。
2. 获取左子结点和右子结点的值。
3. 比较根结点的值是否等于左子结点和右子结点的值之和。

关键点:
- 由于树只有三个结点，可以直接访问左右子结点的值进行比较。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 只需要常数时间进行计算。
空间复杂度: O(1) - 只需要常数空间存储变量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def check_root_sum(root: Optional[TreeNode]) -> bool:
    """
    函数式接口 - 检查根结点的值是否等于其左子结点和右子结点的值之和。
    """
    if not root or not root.left or not root.right:
        return False
    return root.val == (root.left.val + root.right.val)

Solution = create_solution(check_root_sum)