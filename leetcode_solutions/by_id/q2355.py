"""
2274. 不含特殊楼层的最大连续楼层数 - Alice 管理着一家公司，并租用大楼的部分楼层作为办公空间。Alice 决定将一些楼层作为 特殊楼层 ，仅用于放松。 给你两个整数 bottom 和 top ，表示 Alice 租用了从 bottom 到 top（含 bottom 和 top 在内）的所有楼层。另给你一个整数数组 special ，其中 special[i] 表示 Alice 指定用于放松的特殊楼层。 返回不含特殊楼层的 最大 连续楼层数。 示例 1： 输入：bottom = 2, top = 9, special = [4,6] 输出：3 解释：下面列出的是不含特殊楼层的连续楼层范围： - (2, 3) ，楼层数为 2 。 - (5, 5) ，楼层数为 1 。 - (7, 9) ，楼层数为 3 。 因此，返回最大连续楼层数 3 。 示例 2： 输入：bottom = 6, top = 8, special = [7,6,8] 输出：0 解释：每层楼都被规划为特殊楼层，所以返回 0 。 提示 * 1 <= special.length <= 105 * 1 <= bottom <= special[i] <= top <= 109 * special 中的所有值 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和遍历找到最大连续非特殊楼层。

算法步骤:
1. 将特殊楼层列表 special 排序。
2. 初始化变量 `max_gap` 为 0，记录最大连续非特殊楼层数。
3. 计算从 `bottom` 到第一个特殊楼层之间的楼层数。
4. 遍历排序后的特殊楼层列表，计算相邻特殊楼层之间的楼层数。
5. 计算从最后一个特殊楼层到 `top` 之间的楼层数。
6. 返回 `max_gap`。

关键点:
- 通过排序和遍历找到最大连续非特殊楼层。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 special 的长度，因为排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def max_consecutive_floors(bottom: int, top: int, special: List[int]) -> int:
    """
    函数式接口 - 返回不含特殊楼层的最大连续楼层数
    """
    # 对特殊楼层进行排序
    special.sort()
    
    # 初始化最大连续楼层数
    max_gap = 0
    
    # 计算从 bottom 到第一个特殊楼层之间的楼层数
    max_gap = max(max_gap, special[0] - bottom)
    
    # 遍历排序后的特殊楼层列表，计算相邻特殊楼层之间的楼层数
    for i in range(1, len(special)):
        max_gap = max(max_gap, special[i] - special[i - 1] - 1)
    
    # 计算从最后一个特殊楼层到 top 之间的楼层数
    max_gap = max(max_gap, top - special[-1])
    
    return max_gap

