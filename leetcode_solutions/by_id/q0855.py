# ============================================================================
# 问题描述
# ============================================================================
"""
828. 统计子串中的唯一字符 - 我们定义了一个函数 countUniqueChars(s) 来统计字符串 s 中的唯一字符，并返回唯一字符的个数。 例如：s = "LEETCODE" ，则其中 "L", "T","C","O","D" 都是唯一字符，因为它们只出现一次，所以 countUniqueChars(s) = 5 。 本题将会给你一个字符串 s ，我们需要返回 countUniqueChars(t) 的总和，其中 t 是 s 的子字符串。输入用例保证返回值为 32 位整数。 注意，某些子字符串可能是重复的，但你统计时也必须算上这些重复的子字符串（也就是说，你必须统计 s 的所有子字符串中的唯一字符）。 示例 1： 输入: s = "ABC" 输出: 10 解释: 所有可能的子串为："A","B","C","AB","BC" 和 "ABC"。 其中，每一个子串都由独特字符构成。 所以其长度总和为：1 + 1 + 1 + 2 + 2 + 3 = 10 示例 2： 输入: s = "ABA" 输出: 8 解释: 除了 countUniqueChars("ABA") = 1 之外，其余与示例 1 相同。 示例 3： 输入：s = "LEETCODE" 输出：92 提示： * 1 <= s.length <= 105 * s 只包含大写英文字符
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和哈希表来记录每个字符的位置，从而计算每个字符对子字符串贡献的唯一字符数量。

算法步骤:
1. 初始化一个字典 `last_seen` 来记录每个字符上次出现的位置。
2. 初始化一个数组 `dp`，其中 `dp[i]` 表示以 `s[i]` 结尾的所有子字符串中唯一字符的数量。
3. 遍历字符串 `s`，对于每个字符 `s[i]`：
   - 计算 `dp[i]` 的值，基于 `dp[i-1]` 和 `last_seen` 字典。
   - 更新 `last_seen` 字典。
4. 返回 `dp` 数组的总和。

关键点:
- 使用 `last_seen` 字典来记录每个字符上次出现的位置，以便快速计算 `dp[i]`。
- 动态规划的思想，通过 `dp[i]` 依赖于 `dp[i-1]` 来减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，虽然使用了 `last_seen` 字典，但由于字符集是固定的（大写英文字母），因此空间复杂度是常数级别的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 统计字符串 s 的所有子字符串中的唯一字符数量。
    """
    if not s:
        return 0

    last_seen = {}
    dp = [0] * len(s)
    total_unique_chars = 0

    for i in range(len(s)):
        if s[i] in last_seen:
            dp[i] = dp[i - 1] - (last_seen[s[i]] - i + 1)
        else:
            dp[i] = dp[i - 1] + i + 1
        last_seen[s[i]] = i
        total_unique_chars += dp[i]

    return total_unique_chars


Solution = create_solution(solution_function_name)