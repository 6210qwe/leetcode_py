"""
896. 单调数列 - 如果数组是单调递增或单调递减的，那么它是 单调 的。 如果对于所有 i <= j，nums[i] <= nums[j]，那么数组 nums 是单调递增的。 如果对于所有 i <= j，nums[i] >= nums[j]，那么数组 nums 是单调递减的。 当给定的数组 nums 是单调数组时返回 true，否则返回 false。 示例 1： 输入：nums = [1,2,2,3] 输出：true 示例 2： 输入：nums = [6,5,4,4] 输出：true 示例 3： 输入：nums = [1,3,2] 输出：false 提示： * 1 <= nums.length <= 105 * -105 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历数组来判断数组是否单调递增或单调递减。

算法步骤:
1. 初始化两个标志变量 `increasing` 和 `decreasing` 为 True。
2. 遍历数组，比较相邻元素：
   - 如果当前元素大于下一个元素，则将 `increasing` 置为 False。
   - 如果当前元素小于下一个元素，则将 `decreasing` 置为 False。
3. 如果遍历结束后，`increasing` 或 `decreasing` 仍为 True，则数组是单调的。

关键点:
- 通过一次遍历同时检查单调递增和单调递减。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度，因为只需要一次遍历。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


def isMonotonic(nums: List[int]) -> bool:
    """
    判断数组是否是单调数组。
    """
    increasing = decreasing = True
    
    for i in range(len(nums) - 1):
        if nums[i] > nums[i + 1]:
            increasing = False
        if nums[i] < nums[i + 1]:
            decreasing = False
    
    return increasing or decreasing


