"""
2333. 最小差值平方和 - 给你两个下标从 0 开始的整数数组 nums1 和 nums2 ，长度为 n 。 数组 nums1 和 nums2 的 差值平方和 定义为所有满足 0 <= i < n 的 (nums1[i] - nums2[i])2 之和。 同时给你两个正整数 k1 和 k2 。你可以将 nums1 中的任意元素 +1 或者 -1 至多 k1 次。类似的，你可以将 nums2 中的任意元素 +1 或者 -1 至多 k2 次。 请你返回修改数组 nums1 至多 k1 次且修改数组 nums2 至多 k2 次后的最小 差值平方和 。 注意：你可以将数组中的元素变成 负 整数。 示例 1： 输入：nums1 = [1,2,3,4], nums2 = [2,10,20,19], k1 = 0, k2 = 0 输出：579 解释：nums1 和 nums2 中的元素不能修改，因为 k1 = 0 和 k2 = 0 。 差值平方和为：(1 - 2)2 + (2 - 10)2 + (3 - 20)2 + (4 - 19)2 = 579 。 示例 2： 输入：nums1 = [1,4,10,12], nums2 = [5,8,6,9], k1 = 1, k2 = 1 输出：43 解释：一种得到最小差值平方和的方式为： - 将 nums1[0] 增加一次。 - 将 nums2[2] 增加一次。 最小差值平方和为： (2 - 5)2 + (4 - 8)2 + (10 - 7)2 + (12 - 9)2 = 43 。 注意，也有其他方式可以得到最小差值平方和，但没有得到比 43 更小答案的方案。 提示： * n == nums1.length == nums2.length * 1 <= n <= 105 * 0 <= nums1[i], nums2[i] <= 105 * 0 <= k1, k2 <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，通过优先队列来处理最大的差值平方和。

算法步骤:
1. 计算初始的差值平方和，并记录每个差值的绝对值。
2. 使用一个最大堆来存储这些差值的绝对值。
3. 从堆中取出最大的差值，尽可能地减少这个差值，直到不能再减少为止。
4. 更新差值平方和，并继续处理下一个最大的差值，直到用完所有的操作次数。

关键点:
- 使用最大堆来高效地找到并处理最大的差值。
- 通过贪心策略，每次尽量减少最大的差值，以达到最小化总差值平方和的目的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def min_sum_of_squared_difference(nums1: List[int], nums2: List[int], k1: int, k2: int) -> int:
    """
    返回修改数组 nums1 至多 k1 次且修改数组 nums2 至多 k2 次后的最小差值平方和。
    """
    # 计算初始的差值平方和
    diff_squares = []
    total_diff = 0
    for num1, num2 in zip(nums1, nums2):
        diff = abs(num1 - num2)
        diff_squares.append(-diff)  # 使用负数来构建最大堆
        total_diff += diff ** 2
    
    # 构建最大堆
    heapq.heapify(diff_squares)
    
    # 总的操作次数
    total_operations = k1 + k2
    
    # 贪心地减少最大的差值
    while total_operations > 0 and diff_squares:
        max_diff = -heapq.heappop(diff_squares)
        if max_diff == 0:
            break
        # 尽可能地减少这个差值
        operations = min(total_operations, max_diff)
        max_diff -= operations
        total_operations -= operations
        # 更新差值平方和
        total_diff -= (max_diff + operations) ** 2 - max_diff ** 2
        # 将更新后的差值重新放入堆中
        heapq.heappush(diff_squares, -max_diff)
    
    return total_diff

