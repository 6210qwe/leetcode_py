"""
核心思想: 根据三角形的三边长度判断其类型（等边、等腰或普通）。

算法步骤:
1. 检查输入的三条边是否能构成一个三角形。
2. 如果能构成三角形，根据边长关系判断其类型：
   - 如果三条边相等，则为等边三角形。
   - 如果两条边相等，则为等腰三角形。
   - 否则为普通三角形。

关键点:
- 判断三角形的条件：任意两边之和大于第三边。
- 判断等边和等腰三角形的条件。
"""

