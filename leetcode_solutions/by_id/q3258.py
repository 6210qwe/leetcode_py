def classify_triangle(a: int, b: int, c: int) -> str:
    """
    函数式接口 - 根据三角形的三边长度分类三角形
    """
    # 检查是否能构成三角形
    if a + b <= c or a + c <= b or b + c <= a:
        return "Not a triangle"

    # 判断三角形类型
    if a == b == c:
        return "Equilateral"
    elif a == b or b == c or a == c:
        return "Isosceles"
    else:
        return "Scalene"


