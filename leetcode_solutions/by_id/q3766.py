"""
3627. 中位数之和的最大值 - 给你一个整数数组 nums，其长度可以被 3 整除。 你需要通过多次操作将数组清空。在每一步操作中，你可以从数组中选择任意三个元素，计算它们的 中位数 ，并将这三个元素从数组中移除。 奇数长度数组的 中位数 定义为数组按非递减顺序排序后位于中间的元素。 返回通过所有操作得到的 中位数之和的最大值 。 示例 1： 输入： nums = [2,1,3,2,1,3] 输出： 5 解释： * 第一步，选择下标为 2、4 和 5 的元素，它们的中位数是 3。移除这些元素后，nums 变为 [2, 1, 2]。 * 第二步，选择下标为 0、1 和 2 的元素，它们的中位数是 2。移除这些元素后，nums 变为空数组。 因此，中位数之和为 3 + 2 = 5。 示例 2： 输入： nums = [1,1,10,10,10,10] 输出： 20 解释： * 第一步，选择下标为 0、2 和 3 的元素，它们的中位数是 10。移除这些元素后，nums 变为 [1, 10, 10]。 * 第二步，选择下标为 0、1 和 2 的元素，它们的中位数是 10。移除这些元素后，nums 变为空数组。 因此，中位数之和为 10 + 10 = 20。 提示： * 1 <= nums.length <= 5 * 105 * nums.length % 3 == 0 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 为了最大化中位数之和，我们需要尽可能选择较大的中位数。可以通过对数组进行排序，然后从大到小选择每组三个元素中的中间元素来实现。

算法步骤:
1. 对数组进行排序。
2. 从排序后的数组中每隔两个元素选择一个元素，累加这些元素的值。

关键点:
- 通过对数组进行排序，确保每次选择的中位数都是当前可能的最大值。
- 选择每组三个元素中的中间元素，以最大化中位数之和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序操作的时间复杂度。
空间复杂度: O(1) - 除了输入和输出外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算中位数之和的最大值
    """
    # 对数组进行排序
    nums.sort()
    
    # 从排序后的数组中每隔两个元素选择一个元素，累加这些元素的值
    total_median_sum = sum(nums[-(i+1)] for i in range(0, len(nums), 3))
    
    return total_median_sum


Solution = create_solution(solution_function_name)