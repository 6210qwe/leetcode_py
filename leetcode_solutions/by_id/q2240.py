# ============================================================================
# 问题描述
# ============================================================================
"""
2121. 相同元素的间隔之和 - 给你一个下标从 0 开始、由 n 个整数组成的数组 arr 。 arr 中两个元素的 间隔 定义为它们下标之间的 绝对差 。更正式地，arr[i] 和 arr[j] 之间的间隔是 |i - j| 。 返回一个长度为 n 的数组 intervals ，其中 intervals[i] 是 arr[i] 和 arr 中每个相同元素（与 arr[i] 的值相同）的 间隔之和 。 注意：|x| 是 x 的绝对值。 示例 1： 输入：arr = [2,1,3,1,2,3,3] 输出：[4,2,7,2,4,4,5] 解释： - 下标 0 ：另一个 2 在下标 4 ，|0 - 4| = 4 - 下标 1 ：另一个 1 在下标 3 ，|1 - 3| = 2 - 下标 2 ：另两个 3 在下标 5 和 6 ，|2 - 5| + |2 - 6| = 7 - 下标 3 ：另一个 1 在下标 1 ，|3 - 1| = 2 - 下标 4 ：另一个 2 在下标 0 ，|4 - 0| = 4 - 下标 5 ：另两个 3 在下标 2 和 6 ，|5 - 2| + |5 - 6| = 4 - 下标 6 ：另两个 3 在下标 2 和 5 ，|6 - 2| + |6 - 5| = 5 示例 2： 输入：arr = [10,5,10,10] 输出：[5,0,3,4] 解释： - 下标 0 ：另两个 10 在下标 2 和 3 ，|0 - 2| + |0 - 3| = 5 - 下标 1 ：只有这一个 5 在数组中，所以到相同元素的间隔之和是 0 - 下标 2 ：另两个 10 在下标 0 和 3 ，|2 - 0| + |2 - 3| = 3 - 下标 3 ：另两个 10 在下标 0 和 2 ，|3 - 0| + |3 - 2| = 4 提示： * n == arr.length * 1 <= n <= 105 * 1 <= arr[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两次遍历，分别计算每个元素左边和右边相同元素的间隔之和。

算法步骤:
1. 初始化一个字典 `left_sum` 和 `right_sum`，用于存储每个元素左边和右边的间隔之和。
2. 第一次遍历数组，计算每个元素左边相同元素的间隔之和，并存储在 `left_sum` 中。
3. 第二次遍历数组，计算每个元素右边相同元素的间隔之和，并存储在 `right_sum` 中。
4. 最后，将 `left_sum` 和 `right_sum` 相加得到结果。

关键点:
- 使用字典记录每个元素上次出现的位置，以便计算间隔之和。
- 通过两次遍历，分别处理左边和右边的间隔之和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要两次遍历数组。
空间复杂度: O(n)，使用了两个字典来存储每个元素的间隔之和。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def get_intervals(arr: List[int]) -> List[int]:
    n = len(arr)
    left_sum = [0] * n
    right_sum = [0] * n
    last_seen_left = {}
    last_seen_right = {}

    # 计算左边的间隔之和
    for i in range(n):
        if arr[i] in last_seen_left:
            left_sum[i] = left_sum[last_seen_left[arr[i]]] + (i - last_seen_left[arr[i]]) * (last_seen_left[arr[i]] + 1)
        last_seen_left[arr[i]] = i

    # 计算右边的间隔之和
    for i in range(n - 1, -1, -1):
        if arr[i] in last_seen_right:
            right_sum[i] = right_sum[last_seen_right[arr[i]]] + (last_seen_right[arr[i]] - i) * (n - last_seen_right[arr[i]])
        last_seen_right[arr[i]] = i

    # 合并左边和右边的间隔之和
    result = [left_sum[i] + right_sum[i] for i in range(n)]
    return result

Solution = get_intervals