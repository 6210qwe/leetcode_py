def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询来查找得分最高的学生
    """
    # SQL 查询语句
    query = """
    SELECT student_id, score
    FROM (
        SELECT student_id, score,
               RANK() OVER (ORDER BY score DESC) as rank
        FROM scores
    ) ranked_scores
    WHERE rank = 1
    """
    return query


Solution = create_solution(solution_function_name)