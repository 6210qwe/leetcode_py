"""
941. 有效的山脉数组 - 给定一个整数数组 arr，如果它是有效的山脉数组就返回 true，否则返回 false。 让我们回顾一下，如果 arr 满足下述条件，那么它是一个山脉数组： * arr.length >= 3 * 在 0 < i < arr.length - 1 条件下，存在 i 使得： * arr[0] < arr[1] < ... arr[i-1] < arr[i] * arr[i] > arr[i+1] > ... > arr[arr.length - 1] [https://assets.leetcode.com/uploads/2019/10/20/hint_valid_mountain_array.png] 示例 1： 输入：arr = [2,1] 输出：false 示例 2： 输入：arr = [3,5,5] 输出：false 示例 3： 输入：arr = [0,3,2,1] 输出：true 提示： * 1 <= arr.length <= 104 * 0 <= arr[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历数组来判断是否满足山脉数组的条件。

算法步骤:
1. 初始化两个指针，一个从左向右遍历，一个从右向左遍历。
2. 左指针找到第一个不满足递增条件的位置。
3. 右指针找到第一个不满足递减条件的位置。
4. 如果左指针和右指针相遇且不在数组的两端，则数组是有效的山脉数组。

关键点:
- 通过一次遍历即可完成判断，时间复杂度为 O(n)。
- 不需要额外的空间，空间复杂度为 O(1)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int]) -> bool:
    """
    函数式接口 - 判断给定数组是否为有效的山脉数组
    """
    n = len(arr)
    if n < 3:
        return False
    
    left = 0
    # 找到第一个不满足递增条件的位置
    while left + 1 < n and arr[left] < arr[left + 1]:
        left += 1
    
    right = n - 1
    # 找到第一个不满足递减条件的位置
    while right > 0 and arr[right - 1] > arr[right]:
        right -= 1
    
    # 如果左指针和右指针相遇且不在数组的两端，则数组是有效的山脉数组
    return left == right and left != 0 and right != n - 1


Solution = create_solution(solution_function_name)