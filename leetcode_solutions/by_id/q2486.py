"""
2404. 出现最频繁的偶数元素 - 给你一个整数数组 nums ，返回出现最频繁的偶数元素。 如果存在多个满足条件的元素，只需要返回 最小 的一个。如果不存在这样的元素，返回 -1 。 示例 1： 输入：nums = [0,1,2,2,4,4,1] 输出：2 解释： 数组中的偶数元素为 0、2 和 4 ，在这些元素中，2 和 4 出现次数最多。 返回最小的那个，即返回 2 。 示例 2： 输入：nums = [4,4,4,9,2,4] 输出：4 解释：4 是出现最频繁的偶数元素。 示例 3： 输入：nums = [29,47,21,41,13,37,25,7] 输出：-1 解释：不存在偶数元素。 提示： * 1 <= nums.length <= 2000 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个偶数的出现次数，然后找到出现次数最多的偶数。如果有多个偶数出现次数相同，则返回最小的那个。

算法步骤:
1. 初始化一个哈希表来记录每个偶数的出现次数。
2. 遍历数组，更新哈希表。
3. 找到出现次数最多的偶数。如果有多个偶数出现次数相同，则返回最小的那个。
4. 如果没有偶数元素，返回 -1。

关键点:
- 使用哈希表记录每个偶数的出现次数。
- 在遍历哈希表时，同时考虑出现次数和元素值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def most_frequent_even(nums: List[int]) -> int:
    """
    函数式接口 - 返回出现最频繁的偶数元素
    """
    # 哈希表记录每个偶数的出现次数
    count = {}
    for num in nums:
        if num % 2 == 0:
            if num in count:
                count[num] += 1
            else:
                count[num] = 1
    
    # 找到出现次数最多的偶数
    max_count = 0
    result = -1
    for num, freq in count.items():
        if freq > max_count or (freq == max_count and num < result):
            max_count = freq
            result = num
    
    return result


Solution = create_solution(most_frequent_even)