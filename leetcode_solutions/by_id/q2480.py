"""
2395. 和相等的子数组 - 给你一个下标从 0 开始的整数数组 nums ，判断是否存在 两个 长度为 2 的子数组且它们的 和 相等。注意，这两个子数组起始位置的下标必须 不相同 。 如果这样的子数组存在，请返回 true，否则返回 false 。 子数组 是一个数组中一段连续非空的元素组成的序列。 示例 1： 输入：nums = [4,2,4] 输出：true 解释：元素为 [4,2] 和 [2,4] 的子数组有相同的和 6 。 示例 2： 输入：nums = [1,2,3,4,5] 输出：false 解释：没有长度为 2 的两个子数组和相等。 示例 3： 输入：nums = [0,0,0] 输出：true 解释：子数组 [nums[0],nums[1]] 和 [nums[1],nums[2]] 的和相等，都为 0 。 注意即使子数组的元素相同，这两个子数组也视为不相同的子数组，因为它们在原数组中的起始位置不同。 提示： * 2 <= nums.length <= 1000 * -109 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来记录每个长度为2的子数组的和，如果发现某个和已经存在于哈希表中，则说明存在两个和相等的子数组。

算法步骤:
1. 初始化一个空的哈希表 seen_sums。
2. 遍历数组 nums，对于每个元素 nums[i]，计算其与下一个元素 nums[i+1] 的和。
3. 检查这个和是否已经在 seen_sums 中，如果在则返回 True。
4. 如果不在 seen_sums 中，则将这个和加入 seen_sums。
5. 如果遍历完数组后没有找到和相等的子数组，则返回 False。

关键点:
- 使用哈希表来记录和，可以快速检查是否存在重复的和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 nums 的长度。我们只需要遍历数组一次。
空间复杂度: O(n)，最坏情况下哈希表需要存储 n-1 个不同的和。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> bool:
    """
    函数式接口 - 判断是否存在两个长度为2的子数组且它们的和相等
    """
    seen_sums = set()
    
    for i in range(len(nums) - 1):
        current_sum = nums[i] + nums[i + 1]
        if current_sum in seen_sums:
            return True
        seen_sums.add(current_sum)
    
    return False


Solution = create_solution(solution_function_name)