"""
1608. 特殊数组的特征值 - 给你一个非负整数数组 nums 。如果存在一个数 x ，使得 nums 中恰好有 x 个元素 大于或者等于 x ，那么就称 nums 是一个 特殊数组 ，而 x 是该数组的 特征值 。 注意： x 不必 是 nums 的中的元素。 如果数组 nums 是一个 特殊数组 ，请返回它的特征值 x 。否则，返回 -1 。可以证明的是，如果 nums 是特殊数组，那么其特征值 x 是 唯一的 。 示例 1： 输入：nums = [3,5] 输出：2 解释：有 2 个元素（3 和 5）大于或等于 2 。 示例 2： 输入：nums = [0,0] 输出：-1 解释：没有满足题目要求的特殊数组，故而也不存在特征值 x 。 如果 x = 0，应该有 0 个元素 >= x，但实际有 2 个。 如果 x = 1，应该有 1 个元素 >= x，但实际有 0 个。 如果 x = 2，应该有 2 个元素 >= x，但实际有 0 个。 x 不能取更大的值，因为 nums 中只有两个元素。 示例 3： 输入：nums = [0,4,3,0,4] 输出：3 解释：有 3 个元素大于或等于 3 。 示例 4： 输入：nums = [3,6,7,7,0] 输出：-1 提示： * 1 <= nums.length <= 100 * 0 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 先对数组进行排序，然后从大到小遍历数组，找到第一个满足条件的 x。

算法步骤:
1. 对数组进行排序。
2. 从大到小遍历数组，检查每个可能的 x 值。
3. 如果当前元素大于或等于 x，并且 x 的计数与 x 相等，则返回 x。
4. 如果遍历完数组没有找到满足条件的 x，则返回 -1。

关键点:
- 排序后可以从大到小遍历，减少不必要的比较。
- 使用计数器来记录大于或等于当前 x 的元素数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序操作的时间复杂度。
空间复杂度: O(1) - 除了输入数组外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def special_array(nums: List[int]) -> int:
    """
    函数式接口 - 找到特殊数组的特征值
    """
    # 对数组进行排序
    nums.sort()
    
    n = len(nums)
    for x in range(n + 1):
        # 二分查找第一个大于等于 x 的位置
        left, right = 0, n
        while left < right:
            mid = (left + right) // 2
            if nums[mid] >= x:
                right = mid
            else:
                left = mid + 1
        
        # 检查是否有 x 个元素大于或等于 x
        if n - left == x:
            return x
    
    return -1

