# ============================================================================
# 问题描述
# ============================================================================
"""
780. 到达终点 - 给定四个整数 sx , sy ，tx 和 ty，如果通过一系列的转换可以从起点 (sx, sy) 到达终点 (tx, ty)，则返回 true，否则返回 false。 从点 (x, y) 可以转换到 (x, x+y) 或者 (x+y, y)。 示例 1: 输入: sx = 1, sy = 1, tx = 3, ty = 5 输出: true 解释: 可以通过以下一系列转换从起点转换到终点： (1, 1) -> (1, 2) (1, 2) -> (3, 2) (3, 2) -> (3, 5) 示例 2: 输入: sx = 1, sy = 1, tx = 2, ty = 2 输出: false 示例 3: 输入: sx = 1, sy = 1, tx = 1, ty = 1 输出: true 提示: * 1 <= sx, sy, tx, ty <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从终点 (tx, ty) 反向推导到起点 (sx, sy)。每次将较大的坐标减去较小的坐标，直到其中一个坐标小于等于起点坐标。

算法步骤:
1. 如果 tx < sx 或 ty < sy，直接返回 False。
2. 如果 tx == sx 且 ty == sy，返回 True。
3. 如果 tx > ty，将 tx 减去若干个 ty，使得 tx 小于等于 sx。
4. 如果 ty > tx，将 ty 减去若干个 tx，使得 ty 小于等于 sy。
5. 重复上述步骤，直到找到解或无法继续。

关键点:
- 通过反向推导，可以有效减少不必要的计算。
- 使用模运算来快速减小坐标值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log(max(tx, ty))) - 每次操作都会显著减少较大的坐标值。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def reaching_points(sx: int, sy: int, tx: int, ty: int) -> bool:
    """
    函数式接口 - 从终点 (tx, ty) 反向推导到起点 (sx, sy)，判断是否可以到达。
    """
    while tx >= sx and ty >= sy:
        if tx == sx and ty == sy:
            return True
        if tx > ty:
            if ty > sy:
                tx %= ty
            else:
                return (tx - sx) % ty == 0
        else:
            if tx > sx:
                ty %= tx
            else:
                return (ty - sy) % tx == 0
    return False


Solution = create_solution(reaching_points)