"""
2375. 根据模式串构造最小数字 - 给你下标从 0 开始、长度为 n 的字符串 pattern ，它包含两种字符，'I' 表示 上升 ，'D' 表示 下降 。 你需要构造一个下标从 0 开始长度为 n + 1 的字符串，且它要满足以下条件： * num 包含数字 '1' 到 '9' ，其中每个数字 至多 使用一次。 * 如果 pattern[i] == 'I' ，那么 num[i] < num[i + 1] 。 * 如果 pattern[i] == 'D' ，那么 num[i] > num[i + 1] 。 请你返回满足上述条件字典序 最小 的字符串 num。 示例 1： 输入：pattern = "IIIDIDDD" 输出："123549876" 解释： 下标 0 ，1 ，2 和 4 处，我们需要使 num[i] < num[i+1] 。 下标 3 ，5 ，6 和 7 处，我们需要使 num[i] > num[i+1] 。 一些可能的 num 的值为 "245639871" ，"135749862" 和 "123849765" 。 "123549876" 是满足条件最小的数字。 注意，"123414321" 不是可行解因为数字 '1' 使用次数超过 1 次。 示例 2： 输入：pattern = "DDD" 输出："4321" 解释： 一些可能的 num 的值为 "9876" ，"7321" 和 "8742" 。 "4321" 是满足条件最小的数字。 提示： * 1 <= pattern.length <= 8 * pattern 只包含字符 'I' 和 'D' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和栈来构造最小的数字。

算法步骤:
1. 初始化一个空栈 `stack` 和结果列表 `result`。
2. 遍历 `pattern` 字符串，同时维护一个计数器 `num` 从 1 开始。
3. 对于每个字符：
   - 将当前 `num` 压入栈中。
   - 如果当前字符是 'I' 或者已经遍历到 `pattern` 的末尾：
     - 将栈中的所有元素依次弹出并加入 `result`。
4. 最后将 `result` 转换为字符串并返回。

关键点:
- 使用栈来处理连续的 'D' 字符，确保在遇到 'I' 时可以逆序输出。
- 通过贪心算法确保每次选择的数字尽可能小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 pattern 的长度。每个字符只处理一次。
空间复杂度: O(n)，栈的最大深度为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_number(pattern: str) -> str:
    """
    函数式接口 - 构造满足 pattern 条件的最小数字。
    """
    stack = []
    result = []
    num = 1
    
    for i, char in enumerate(pattern + 'I'):
        stack.append(num)
        if char == 'I' or i == len(pattern):
            while stack:
                result.append(stack.pop())
        num += 1
    
    return ''.join(map(str, result))


Solution = create_solution(smallest_number)