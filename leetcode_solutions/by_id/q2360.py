"""
2272. 最大波动的子字符串 - 字符串的 波动 定义为子字符串中出现次数 最多 的字符次数与出现次数 最少 的字符次数之差。 给你一个字符串 s ，它只包含小写英文字母。请你返回 s 里所有 子字符串的 最大波动 值。 子字符串 是一个字符串的一段连续字符序列。 示例 1： 输入：s = "aababbb" 输出：3 解释： 所有可能的波动值和它们对应的子字符串如以下所示： - 波动值为 0 的子字符串："a" ，"aa" ，"ab" ，"abab" ，"aababb" ，"ba" ，"b" ，"bb" 和 "bbb" 。 - 波动值为 1 的子字符串："aab" ，"aba" ，"abb" ，"aabab" ，"ababb" ，"aababbb" 和 "bab" 。 - 波动值为 2 的子字符串："aaba" ，"ababbb" ，"abbb" 和 "babb" 。 - 波动值为 3 的子字符串 "babbb" 。 所以，最大可能波动值为 3 。 示例 2： 输入：s = "abcde" 输出：0 解释： s 中没有字母出现超过 1 次，所以 s 中每个子字符串的波动值都是 0 。 提示： * 1 <= s.length <= 104 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算每对字符的最大波动值。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][j] 表示以字符 i 为最大频率字符，字符 j 为最小频率字符的子字符串的最大波动值。
2. 遍历字符串 s，对于每一对字符 (i, j)，更新 dp[i][j] 的值。
3. 对于每个字符 c，更新当前字符 c 作为最大频率字符和最小频率字符的情况。
4. 记录并更新最大波动值。

关键点:
- 使用动态规划来避免重复计算。
- 通过遍历所有字符对来确保找到最大波动值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * 26^2) = O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(26 * 26) = O(1)，因为字符集大小是固定的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算字符串 s 的最大波动值
    """
    if not s:
        return 0

    n = len(s)
    max_variance = 0
    dp = [[-n for _ in range(26)] for _ in range(26)]

    for i in range(n):
        char_idx = ord(s[i]) - ord('a')
        for j in range(26):
            if j == char_idx:
                continue
            dp[char_idx][j] = max(dp[char_idx][j] + 1, 1)
            dp[j][char_idx] = max(dp[j][char_idx] - 1, -1)
            if dp[char_idx][j] > 0:
                max_variance = max(max_variance, dp[char_idx][j])

    return max_variance


Solution = create_solution(solution_function_name)