# 问题描述
# ============================================================================
"""
781. 森林中的兔子 - 森林中有未知数量的兔子。提问其中若干只兔子 "还有多少只兔子与你（指被提问的兔子）颜色相同?" ，将答案收集到一个整数数组 answers 中，其中 answers[i] 是第 i 只兔子的回答。 给你数组 answers ，返回森林中兔子的最少数量。 示例 1： 输入：answers = [1,1,2] 输出：5 解释： 两只回答了 "1" 的兔子可能有相同的颜色，设为红色。 之后回答了 "2" 的兔子不会是红色，否则他们的回答会相互矛盾。 设回答了 "2" 的兔子为蓝色。 此外，森林中还应有另外 2 只蓝色兔子的回答没有包含在数组中。 因此森林中兔子的最少数量是 5 只：3 只回答的和 2 只没有回答的。 示例 2： 输入：answers = [10,10,10] 输出：11 提示： * 1 <= answers.length <= 1000 * 0 <= answers[i] < 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个回答的数量，然后计算每种回答对应的最小兔子数量。

算法步骤:
1. 使用哈希表统计每个回答的数量。
2. 对于每个回答，计算其对应的最小兔子数量，并累加到总数量中。

关键点:
- 同一种回答的最大组数为 (count + answer) // (answer + 1)，其中 count 是该回答的数量。
- 每组兔子的数量为 answer + 1。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 answers 的长度。我们需要遍历数组并进行哈希表操作。
空间复杂度: O(n)，哈希表的空间复杂度为 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(answers: List[int]) -> int:
    """
    函数式接口 - 计算森林中兔子的最少数量
    """
    from collections import Counter
    
    # 统计每个回答的数量
    count = Counter(answers)
    
    # 计算最小兔子数量
    total_rabbits = 0
    for answer, cnt in count.items():
        # 计算每种回答对应的最小兔子数量
        groups = (cnt + answer) // (answer + 1)
        total_rabbits += groups * (answer + 1)
    
    return total_rabbits


Solution = create_solution(solution_function_name)