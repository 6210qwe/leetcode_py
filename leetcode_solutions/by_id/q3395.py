"""
3138. 同位字符串连接的最小长度 - 给你一个字符串 s ，它由某个字符串 t 和若干 t 的 同位字符串 连接而成。 请你返回字符串 t 的 最小 可能长度。 同位字符串 指的是重新排列一个字符串的字母得到的另外一个字符串。例如，"aab"，"aba" 和 "baa" 是 "aab" 的同位字符串。 示例 1： 输入：s = "abba" 输出：2 解释： 一个可能的字符串 t 为 "ba" 。 示例 2： 输入：s = "cdef" 输出：4 解释： 一个可能的字符串 t 为 "cdef" ，注意 t 可能等于 s 。 示例 3： 输入：s = "abcbcacabbaccba" 输出：3 提示： * 1 <= s.length <= 105 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计字符频率，并通过最大公约数来确定最小的 t 的长度。

算法步骤:
1. 统计字符串 s 中每个字符的出现次数。
2. 找到所有字符出现次数的最大公约数。
3. 返回该最大公约数作为 t 的最小可能长度。

关键点:
- 使用 `math.gcd` 函数来计算最大公约数。
- 通过遍历字符频率数组来找到所有频率的最大公约数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log k)，其中 n 是字符串 s 的长度，k 是字符集的大小（最多 26 个字母）。
空间复杂度: O(1)，因为字符集的大小是固定的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
import math
from collections import Counter

def solution_function_name(s: str) -> int:
    """
    函数式接口 - 实现
    """
    # 统计字符串 s 中每个字符的出现次数
    char_count = Counter(s)
    
    # 初始化最大公约数为第一个字符的频率
    gcd_val = list(char_count.values())[0]
    
    # 计算所有字符频率的最大公约数
    for count in char_count.values():
        gcd_val = math.gcd(gcd_val, count)
    
    return gcd_val

Solution = create_solution(solution_function_name)