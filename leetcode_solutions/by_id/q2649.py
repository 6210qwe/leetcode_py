"""
核心思想: 通过数学公式计算 n 分钟后被染色的格子数目。

算法步骤:
1. 计算 n 分钟后被染色的格子数目，公式为：总数 = 2 * n * (n - 1) + 1

关键点:
- 利用数学公式直接计算结果，避免模拟过程。
"""

