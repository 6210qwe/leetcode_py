"""
1598. 文件夹操作日志搜集器 - 每当用户执行变更文件夹操作时，LeetCode 文件系统都会保存一条日志记录。 下面给出对变更操作的说明： * "../" ：移动到当前文件夹的父文件夹。如果已经在主文件夹下，则 继续停留在当前文件夹 。 * "./" ：继续停留在当前文件夹。 * "x/" ：移动到名为 x 的子文件夹中。题目数据 保证总是存在文件夹 x 。 给你一个字符串列表 logs ，其中 logs[i] 是用户在 ith 步执行的操作。 文件系统启动时位于主文件夹，然后执行 logs 中的操作。 执行完所有变更文件夹操作后，请你找出 返回主文件夹所需的最小步数 。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/09/26/sample_11_1957.png] 输入：logs = ["d1/","d2/","../","d21/","./"] 输出：2 解释：执行 "../" 操作变更文件夹 2 次，即可回到主文件夹 示例 2： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/09/26/sample_22_1957.png] 输入：logs = ["d1/","d2/","./","d3/","../","d31/"] 输出：3 示例 3： 输入：logs = ["d1/","../","../","../"] 输出：0 提示： * 1 <= logs.length <= 103 * 2 <= logs[i].length <= 10 * logs[i] 包含小写英文字母，数字，'.' 和 '/' * logs[i] 符合语句中描述的格式 * 文件夹名称由小写英文字母和数字组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来模拟文件夹的进出操作。

算法步骤:
1. 初始化一个栈，用于存储当前所在的文件夹层级。
2. 遍历 logs 列表：
   - 如果是 "../"，并且栈不为空，则弹出栈顶元素。
   - 如果是 "./"，则继续遍历下一个元素。
   - 如果是 "x/"，则将 "x" 压入栈中。
3. 最后返回栈的大小，即为返回主文件夹所需的最小步数。

关键点:
- 使用栈来模拟文件夹的进出操作。
- 处理 "../" 时需要判断栈是否为空。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 logs 的长度。我们需要遍历整个 logs 列表。
空间复杂度: O(n)，在最坏情况下，所有的操作都是进入子文件夹，栈的大小会达到 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(logs: List[str]) -> int:
    """
    函数式接口 - 计算返回主文件夹所需的最小步数
    """
    stack = []
    for log in logs:
        if log == "../":
            if stack:
                stack.pop()
        elif log != "./":
            stack.append(log)
    return len(stack)


Solution = create_solution(solution_function_name)