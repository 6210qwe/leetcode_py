"""
2916. 子数组不同元素数目的平方和 II - 给你一个下标从 0 开始的整数数组 nums 。 定义 nums 一个子数组的 不同计数 值如下： * 令 nums[i..j] 表示 nums 中所有下标在 i 到 j 范围内的元素构成的子数组（满足 0 <= i <= j < nums.length ），那么我们称子数组 nums[i..j] 中不同值的数目为 nums[i..j] 的不同计数。 请你返回 nums 中所有子数组的 不同计数 的 平方 和。 由于答案可能会很大，请你将它对 109 + 7 取余 后返回。 子数组指的是一个数组里面一段连续 非空 的元素序列。 示例 1： 输入：nums = [1,2,1] 输出：15 解释：六个子数组分别为： [1]: 1 个互不相同的元素。 [2]: 1 个互不相同的元素。 [1]: 1 个互不相同的元素。 [1,2]: 2 个互不相同的元素。 [2,1]: 2 个互不相同的元素。 [1,2,1]: 2 个互不相同的元素。 所有不同计数的平方和为 12 + 12 + 12 + 22 + 22 + 22 = 15 。 示例 2： 输入：nums = [2,2] 输出：3 解释：三个子数组分别为： [2]: 1 个互不相同的元素。 [2]: 1 个互不相同的元素。 [2,2]: 1 个互不相同的元素。 所有不同计数的平方和为 12 + 12 + 12 = 3 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和和哈希表来记录每个元素上次出现的位置，从而快速计算出每个子数组的不同元素数目。

算法步骤:
1. 初始化变量：模数 MOD = 10^9 + 7，前缀和数组 prefix_sum，结果 result = 0。
2. 遍历数组 nums，对于每个元素 nums[i]：
   - 更新前缀和数组 prefix_sum。
   - 使用哈希表 last_seen 记录每个元素上次出现的位置。
   - 计算当前子数组的不同元素数目，并更新结果 result。
3. 返回结果 result。

关键点:
- 使用前缀和和哈希表来高效计算每个子数组的不同元素数目。
- 通过遍历数组并更新前缀和和哈希表，可以在线性时间内完成计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 nums 的长度。每个元素只处理一次。
空间复杂度: O(n)，需要额外的空间来存储前缀和数组和哈希表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def sum_of_squares(nums: List[int]) -> int:
    MOD = 10**9 + 7
    n = len(nums)
    prefix_sum = [0] * (n + 1)
    last_seen = {}
    result = 0

    for i in range(n):
        # 更新前缀和
        prefix_sum[i + 1] = prefix_sum[i] + 1
        if nums[i] in last_seen:
            # 如果元素之前出现过，更新前缀和
            prefix_sum[i + 1] -= prefix_sum[last_seen[nums[i]]]
        # 更新哈希表
        last_seen[nums[i]] = i + 1
        # 计算当前子数组的不同元素数目
        distinct_count = prefix_sum[i + 1]
        # 更新结果
        result = (result + distinct_count * distinct_count) % MOD

    return result

