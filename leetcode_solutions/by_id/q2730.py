"""
2680. 最大或值 - 给你一个下标从 0 开始长度为 n 的整数数组 nums 和一个整数 k 。每一次操作中，你可以选择一个数并将它乘 2 。 你最多可以进行 k 次操作，请你返回 nums[0] | nums[1] | ... | nums[n - 1] 的最大值。 a | b 表示两个整数 a 和 b 的 按位或 运算。 示例 1： 输入：nums = [12,9], k = 1 输出：30 解释：如果我们对下标为 1 的元素进行操作，新的数组为 [12,18] 。此时得到最优答案为 12 和 18 的按位或运算的结果，也就是 30 。 示例 2： 输入：nums = [8,1,2], k = 2 输出：35 解释：如果我们对下标 0 处的元素进行操作，得到新数组 [32,1,2] 。此时得到最优答案为 32|1|2 = 35 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109 * 1 <= k <= 15
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法选择最优的元素进行乘2操作，以最大化最终的按位或结果。

算法步骤:
1. 计算初始的按位或结果。
2. 对每个元素，计算将其乘2^k后的按位或结果，并记录最大值。

关键点:
- 通过预计算每个元素的前缀和后缀按位或结果，可以在O(1)时间内计算出某个元素乘2^k后的按位或结果。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def maximum_or(nums: List[int], k: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    n = len(nums)
    prefix_or = [0] * (n + 1)
    suffix_or = [0] * (n + 1)

    # 计算前缀按位或
    for i in range(n):
        prefix_or[i + 1] = prefix_or[i] | nums[i]

    # 计算后缀按位或
    for i in range(n - 1, -1, -1):
        suffix_or[i] = suffix_or[i + 1] | nums[i]

    max_or = 0
    for i in range(n):
        # 计算当前元素乘2^k后的按位或结果
        current_or = (prefix_or[i] | (nums[i] << k) | suffix_or[i + 1])
        max_or = max(max_or, current_or)

    return max_or


