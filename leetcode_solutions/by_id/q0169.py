"""
169. 多数元素 - 给定一个大小为 n 的数组 nums ，返回其中的多数元素。多数元素是指在数组中出现次数 大于 ⌊ n/2 ⌋ 的元素。 你可以假设数组是非空的，并且给定的数组总是存在多数元素。 示例 1： 输入：nums = [3,2,3] 输出：3 示例 2： 输入：nums = [2,2,1,1,1,2,2] 输出：2 提示： * n == nums.length * 1 <= n <= 5 * 104 * -109 <= nums[i] <= 109 * 输入保证数组中一定有一个多数元素。 进阶：尝试设计时间复杂度为 O(n)、空间复杂度为 O(1) 的算法解决此问题。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用Boyer-Moore投票算法，找到出现次数超过一半的元素

算法步骤:
1. 初始化候选元素和计数器
2. 遍历数组：
   - 如果计数器为0，更新候选元素
   - 如果当前元素等于候选元素，计数器加1
   - 否则计数器减1
3. 返回候选元素（题目保证存在多数元素）

关键点:
- Boyer-Moore投票算法，O(1)空间
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 只需遍历一次数组
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def majority_element(nums: List[int]) -> int:
    """
    函数式接口 - 找出多数元素
    
    实现思路:
    使用Boyer-Moore投票算法，找到出现次数超过一半的元素。
    
    Args:
        nums: 整数数组
        
    Returns:
        多数元素
        
    Example:
        >>> majority_element([3, 2, 3])
        3
    """
    candidate = None
    count = 0
    
    for num in nums:
        if count == 0:
            candidate = num
        count += 1 if num == candidate else -1
    
    return candidate


