"""
1103. 分糖果 II - 排排坐，分糖果。 我们买了一些糖果 candies，打算把它们分给排好队的 n = num_people 个小朋友。 给第一个小朋友 1 颗糖果，第二个小朋友 2 颗，依此类推，直到给最后一个小朋友 n 颗糖果。 然后，我们再回到队伍的起点，给第一个小朋友 n + 1 颗糖果，第二个小朋友 n + 2 颗，依此类推，直到给最后一个小朋友 2 * n 颗糖果。 重复上述过程（每次都比上一次多给出一颗糖果，当到达队伍终点后再次从队伍起点开始），直到我们分完所有的糖果。注意，就算我们手中的剩下糖果数不够（不比前一次发出的糖果多），这些糖果也会全部发给当前的小朋友。 返回一个长度为 num_people、元素之和为 candies 的数组，以表示糖果的最终分发情况（即 ans[i] 表示第 i 个小朋友分到的糖果数）。 示例 1： 输入：candies = 7, num_people = 4 输出：[1,2,3,1] 解释： 第一次，ans[0] += 1，数组变为 [1,0,0,0]。 第二次，ans[1] += 2，数组变为 [1,2,0,0]。 第三次，ans[2] += 3，数组变为 [1,2,3,0]。 第四次，ans[3] += 1（因为此时只剩下 1 颗糖果），最终数组变为 [1,2,3,1]。 示例 2： 输入：candies = 10, num_people = 3 输出：[5,2,3] 解释： 第一次，ans[0] += 1，数组变为 [1,0,0]。 第二次，ans[1] += 2，数组变为 [1,2,0]。 第三次，ans[2] += 3，数组变为 [1,2,3]。 第四次，ans[0] += 4，最终数组变为 [5,2,3]。 提示： * 1 <= candies <= 10^9 * 1 <= num_people <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用数学方法计算每个小朋友分到的糖果数。

算法步骤:
1. 初始化一个长度为 num_people 的数组 result，用于存储每个小朋友分到的糖果数。
2. 使用一个变量 current_candy 来表示当前要分发的糖果数，初始值为 1。
3. 使用一个变量 remaining_candies 来表示剩余的糖果数，初始值为 candies。
4. 循环分发糖果，直到剩余的糖果数为 0：
   - 计算当前小朋友应该分到的糖果数，如果剩余的糖果数小于 current_candy，则将剩余的所有糖果都分给当前小朋友。
   - 更新剩余的糖果数。
   - 更新 current_candy，每次增加 1。
   - 更新 result 数组中当前小朋友的糖果数。
5. 返回 result 数组。

关键点:
- 通过循环分发糖果，确保每个小朋友都能按顺序分到正确的糖果数。
- 使用数学方法避免不必要的循环，提高效率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(sqrt(candies))，因为每次分发的糖果数是递增的，总次数大约为 sqrt(candies)。
空间复杂度: O(num_people)，需要一个长度为 num_people 的数组来存储结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def distribute_candies(candies: int, num_people: int) -> List[int]:
    """
    函数式接口 - 分糖果
    """
    result = [0] * num_people
    current_candy = 1
    remaining_candies = candies
    index = 0

    while remaining_candies > 0:
        # 计算当前小朋友应该分到的糖果数
        if remaining_candies >= current_candy:
            result[index % num_people] += current_candy
            remaining_candies -= current_candy
        else:
            result[index % num_people] += remaining_candies
            remaining_candies = 0
        # 更新 current_candy 和索引
        current_candy += 1
        index += 1

    return result

