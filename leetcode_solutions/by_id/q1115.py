"""
1037. 有效的回旋镖 - 给定一个数组 points ，其中 points[i] = [xi, yi] 表示 X-Y 平面上的一个点，如果这些点构成一个 回旋镖 则返回 true 。 回旋镖 定义为一组三个点，这些点 各不相同 且 不在一条直线上 。 示例 1： 输入：points = [[1,1],[2,3],[3,2]] 输出：true 示例 2： 输入：points = [[1,1],[2,2],[3,3]] 输出：false 提示： * points.length == 3 * points[i].length == 2 * 0 <= xi, yi <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断三个点是否共线。如果三个点共线，则它们的斜率相等。

算法步骤:
1. 计算前两个点 (P1, P2) 的斜率。
2. 计算后两个点 (P2, P3) 的斜率。
3. 比较两个斜率，如果不相等，则这三个点不共线，构成回旋镖。

关键点:
- 使用斜率公式 (y2 - y1) / (x2 - x1)，为了避免除法运算带来的精度问题，可以使用乘法形式 (y2 - y1) * (x3 - x2) != (y3 - y2) * (x2 - x1) 来判断。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_boomerang(points: List[List[int]]) -> bool:
    """
    判断给定的三个点是否构成一个回旋镖。

    :param points: 三个点的坐标列表
    :return: 如果这三个点构成回旋镖则返回 True，否则返回 False
    """
    (x1, y1), (x2, y2), (x3, y3) = points
    # 使用乘法形式避免除法运算带来的精度问题
    return (y2 - y1) * (x3 - x2) != (y3 - y2) * (x2 - x1)


Solution = create_solution(is_boomerang)