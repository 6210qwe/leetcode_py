"""
540. 有序数组中的单一元素 - 给你一个仅由整数组成的有序数组，其中每个元素都会出现两次，唯有一个数只会出现一次。 请你找出并返回只出现一次的那个数。 你设计的解决方案必须满足 O(log n) 时间复杂度和 O(1) 空间复杂度。 示例 1: 输入: nums = [1,1,2,3,3,4,4,8,8] 输出: 2 示例 2: 输入: nums = [3,3,7,7,10,11,11] 输出: 10 提示: * 1 <= nums.length <= 105 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到唯一出现一次的元素。

算法步骤:
1. 初始化左右指针 left 和 right。
2. 进行二分查找：
   - 计算中间位置 mid。
   - 如果 mid 是偶数且 nums[mid] == nums[mid + 1]，则目标元素在右半部分。
   - 如果 mid 是奇数且 nums[mid] == nums[mid - 1]，则目标元素在右半部分。
   - 否则，目标元素在左半部分。
3. 当 left 和 right 相遇时，left 指向的就是唯一出现一次的元素。

关键点:
- 通过检查 mid 与相邻元素的关系来确定目标元素的位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 使用二分查找找到唯一出现一次的元素
    """
    left, right = 0, len(nums) - 1
    
    while left < right:
        mid = (left + right) // 2
        
        # 确保 mid 是偶数
        if mid % 2 == 1:
            mid -= 1
        
        # 检查 mid 与相邻元素的关系
        if nums[mid] == nums[mid + 1]:
            left = mid + 2
        else:
            right = mid
    
    return nums[left]


Solution = create_solution(solution_function_name)