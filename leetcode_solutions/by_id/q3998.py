"""
3674. 数组元素相等的最小操作次数 - 给你一个长度为 n 的整数数组 nums。 在一次操作中，可以选择任意子数组 nums[l...r] （0 <= l <= r < n），并将该子数组中的每个元素 替换 为所有元素的 按位与（bitwise AND）结果。 返回使数组 nums 中所有元素相等所需的最小操作次数。 子数组 是数组中连续的、非空的元素序列。 示例 1： 输入： nums = [1,2] 输出： 1 解释： 选择 nums[0...1]：(1 AND 2) = 0，因此数组变为 [0, 0]，所有元素在一次操作后相等。 示例 2： 输入： nums = [5,5,5] 输出： 0 解释： nums 本身是 [5, 5, 5]，所有元素已经相等，因此不需要任何操作。 提示： * 1 <= n == nums.length <= 100 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过按位与操作找到数组中所有元素的公共前缀，然后计算需要的操作次数。

算法步骤:
1. 计算整个数组的按位与结果。
2. 如果按位与结果等于数组中的所有元素，则不需要任何操作。
3. 否则，需要一次操作将整个数组替换为按位与结果。

关键点:
- 通过按位与操作找到数组中所有元素的公共前缀。
- 只需要一次操作即可将整个数组替换为按位与结果。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 返回使数组 nums 中所有元素相等所需的最小操作次数
    """
    # 计算整个数组的按位与结果
    and_result = nums[0]
    for num in nums[1:]:
        and_result &= num
    
    # 如果按位与结果等于数组中的所有元素，则不需要任何操作
    if and_result == nums[0]:
        return 0
    
    # 否则，需要一次操作将整个数组替换为按位与结果
    return 1


Solution = create_solution(solution_function_name)