"""
1021. 删除最外层的括号 - 有效括号字符串为空 ""、"(" + A + ")" 或 A + B ，其中 A 和 B 都是有效的括号字符串，+ 代表字符串的连接。 * 例如，""，"()"，"(())()" 和 "(()(()))" 都是有效的括号字符串。 如果有效字符串 s 非空，且不存在将其拆分为 s = A + B 的方法，我们称其为原语（primitive），其中 A 和 B 都是非空有效括号字符串。 给出一个非空有效字符串 s，考虑将其进行原语化分解，使得：s = P_1 + P_2 + ... + P_k，其中 P_i 是有效括号字符串原语。 对 s 进行原语化分解，删除分解中每个原语字符串的最外层括号，返回 s 。 示例 1： 输入：s = "(()())(())" 输出："()()()" 解释： 输入字符串为 "(()())(())"，原语化分解得到 "(()())" + "(())"， 删除每个部分中的最外层括号后得到 "()()" + "()" = "()()()"。 示例 2： 输入：s = "(()())(())(()(()))" 输出："()()()()(())" 解释： 输入字符串为 "(()())(())(()(()))"，原语化分解得到 "(()())" + "(())" + "(()(()))"， 删除每个部分中的最外层括号后得到 "()()" + "()" + "()(())" = "()()()()(())"。 示例 3： 输入：s = "()()" 输出："" 解释： 输入字符串为 "()()"，原语化分解得到 "()" + "()"， 删除每个部分中的最外层括号后得到 "" + "" = ""。 提示： * 1 <= s.length <= 105 * s[i] 为 '(' 或 ')' * s 是一个有效括号字符串
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来记录括号的嵌套层次，当遇到一个完整的原语时，去掉最外层的括号。

算法步骤:
1. 初始化一个空栈和一个结果字符串。
2. 遍历输入字符串 s 中的每个字符：
   - 如果当前字符是 '(', 将其压入栈中。
   - 如果当前字符是 ')', 弹出栈顶元素。
   - 如果栈不为空，则将当前字符添加到结果字符串中。
3. 返回结果字符串。

关键点:
- 使用栈来记录括号的嵌套层次。
- 当栈为空时，表示遇到了一个完整的原语，此时不将当前字符添加到结果字符串中。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历一次字符串。
空间复杂度: O(n)，在最坏情况下，栈的大小可能达到字符串长度的一半。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_outer_parentheses(s: str) -> str:
    """
    函数式接口 - 删除最外层的括号
    """
    stack = []
    result = []
    for char in s:
        if char == '(':
            if stack:
                result.append(char)
            stack.append(char)
        elif char == ')':
            stack.pop()
            if stack:
                result.append(char)
    return ''.join(result)


Solution = create_solution(remove_outer_parentheses)