"""
2974. 最小数字游戏 - 你有一个下标从 0 开始、长度为 偶数 的整数数组 nums ，同时还有一个空数组 arr 。Alice 和 Bob 决定玩一个游戏，游戏中每一轮 Alice 和 Bob 都会各自执行一次操作。游戏规则如下： * 每一轮，Alice 先从 nums 中移除一个 最小 元素，然后 Bob 执行同样的操作。 * 接着，Bob 会将移除的元素添加到数组 arr 中，然后 Alice 也执行同样的操作。 * 游戏持续进行，直到 nums 变为空。 返回结果数组 arr 。 示例 1： 输入：nums = [5,4,2,3] 输出：[3,2,5,4] 解释：第一轮，Alice 先移除 2 ，然后 Bob 移除 3 。然后 Bob 先将 3 添加到 arr 中，接着 Alice 再将 2 添加到 arr 中。于是 arr = [3,2] 。 第二轮开始时，nums = [5,4] 。Alice 先移除 4 ，然后 Bob 移除 5 。接着他们都将元素添加到 arr 中，arr 变为 [3,2,5,4] 。 示例 2： 输入：nums = [2,5] 输出：[5,2] 解释：第一轮，Alice 先移除 2 ，然后 Bob 移除 5 。然后 Bob 先将 5 添加到 arr 中，接着 Alice 再将 2 添加到 arr 中。于是 arr = [5,2] 。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100 * nums.length % 2 == 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用排序和双指针来模拟游戏过程。

算法步骤:
1. 对数组 nums 进行排序。
2. 使用两个指针分别指向当前最小和次小的元素。
3. 依次从 nums 中取出最小和次小的元素，并按 Bob 和 Alice 的顺序添加到结果数组 arr 中。
4. 重复上述步骤直到 nums 为空。

关键点:
- 通过排序，可以确保每次都能快速找到最小和次小的元素。
- 使用双指针可以简化代码逻辑。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度，主要由排序操作决定。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_number_game(nums: List[int]) -> List[int]:
    """
    函数式接口 - 实现最小数字游戏
    """
    # 对数组进行排序
    nums.sort()
    
    # 初始化结果数组
    arr = []
    
    # 使用双指针模拟游戏过程
    for i in range(0, len(nums), 2):
        # Bob 先添加次小的元素
        arr.append(nums[i + 1])
        # Alice 再添加最小的元素
        arr.append(nums[i])
    
    return arr


Solution = create_solution(minimum_number_game)