"""
1234. 替换子串得到平衡字符串 - 有一个只含有 'Q', 'W', 'E', 'R' 四种字符，且长度为 n 的字符串。 假如在该字符串中，这四个字符都恰好出现 n/4 次，那么它就是一个「平衡字符串」。 给你一个这样的字符串 s，请通过「替换一个子串」的方式，使原字符串 s 变成一个「平衡字符串」。 你可以用和「待替换子串」长度相同的 任何 其他字符串来完成替换。 请返回待替换子串的最小可能长度。 如果原字符串自身就是一个平衡字符串，则返回 0。 示例 1： 输入：s = "QWER" 输出：0 解释：s 已经是平衡的了。 示例 2： 输入：s = "QQWE" 输出：1 解释：我们需要把一个 'Q' 替换成 'R'，这样得到的 "RQWE" (或 "QRWE") 是平衡的。 示例 3： 输入：s = "QQQW" 输出：2 解释：我们可以把前面的 "QQ" 替换成 "ER"。 示例 4： 输入：s = "QQQQ" 输出：3 解释：我们可以替换后 3 个 'Q'，使 s = "QWER"。 提示： * 1 <= s.length <= 10^5 * s.length 是 4 的倍数 * s 中只含有 'Q', 'W', 'E', 'R' 四种字符
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到最短的子串，使得替换这个子串后整个字符串变成平衡字符串。

算法步骤:
1. 计算每个字符的目标数量（即 n/4）。
2. 使用滑动窗口技术，从左到右遍历字符串，记录当前窗口内每个字符的数量。
3. 当窗口内的字符数量满足目标数量时，尝试缩小窗口以找到最短的子串。
4. 更新最小长度。

关键点:
- 使用滑动窗口来动态调整子串的长度。
- 通过计数器来判断当前窗口是否满足平衡条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def balancedString(s: str) -> int:
    n = len(s)
    target = n // 4
    count = {'Q': 0, 'W': 0, 'E': 0, 'R': 0}
    
    # 计算初始字符频率
    for char in s:
        count[char] += 1
    
    # 如果已经是平衡字符串
    if all(count[char] == target for char in 'QWER'):
        return 0
    
    left = 0
    min_len = n
    for right in range(n):
        count[s[right]] -= 1
        
        # 尝试缩小窗口
        while all(count[char] <= target for char in 'QWER'):
            min_len = min(min_len, right - left + 1)
            count[s[left]] += 1
            left += 1
    
    return min_len


Solution = create_solution(balancedString)