"""
2451. 差值数组不同的字符串 - 给你一个字符串数组 words ，每一个字符串长度都相同，令所有字符串的长度都为 n 。 每个字符串 words[i] 可以被转化为一个长度为 n - 1 的 差值整数数组 difference[i] ，其中对于 0 <= j <= n - 2 有 difference[i][j] = words[i][j+1] - words[i][j] 。注意两个字母的差值定义为它们在字母表中 位置 之差，也就是说 'a' 的位置是 0 ，'b' 的位置是 1 ，'z' 的位置是 25 。 * 比方说，字符串 "acb" 的差值整数数组是 [2 - 0, 1 - 2] = [2, -1] 。 words 中所有字符串 除了一个字符串以外 ，其他字符串的差值整数数组都相同。你需要找到那个不同的字符串。 请你返回 words中 差值整数数组 不同的字符串。 示例 1： 输入：words = ["adc","wzy","abc"] 输出："abc" 解释： - "adc" 的差值整数数组是 [3 - 0, 2 - 3] = [3, -1] 。 - "wzy" 的差值整数数组是 [25 - 22, 24 - 25]= [3, -1] 。 - "abc" 的差值整数数组是 [1 - 0, 2 - 1] = [1, 1] 。 不同的数组是 [1, 1]，所以返回对应的字符串，"abc"。 示例 2： 输入：words = ["aaa","bob","ccc","ddd"] 输出："bob" 解释：除了 "bob" 的差值整数数组是 [13, -13] 以外，其他字符串的差值整数数组都是 [0, 0] 。 提示： * 3 <= words.length <= 100 * n == words[i].length * 2 <= n <= 20 * words[i] 只含有小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个差值数组出现的次数，找到唯一一个出现一次的差值数组对应的字符串。

算法步骤:
1. 定义一个函数 `get_difference` 来计算字符串的差值数组。
2. 使用哈希表记录每个差值数组出现的次数。
3. 遍历哈希表，找到唯一一个出现一次的差值数组对应的字符串。

关键点:
- 使用哈希表记录差值数组及其对应的字符串。
- 找到唯一一个出现一次的差值数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是字符串数组的长度，n 是每个字符串的长度。
空间复杂度: O(m * n)，用于存储差值数组及其对应的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def get_difference(s: str) -> List[int]:
    """计算字符串的差值数组"""
    return [ord(s[i + 1]) - ord(s[i]) for i in range(len(s) - 1)]


def solution_function_name(words: List[str]) -> str:
    """
    函数式接口 - 找到差值数组不同的字符串
    """
    diff_count = {}
    
    for word in words:
        diff = tuple(get_difference(word))
        if diff in diff_count:
            diff_count[diff].append(word)
        else:
            diff_count[diff] = [word]
    
    for diff, words in diff_count.items():
        if len(words) == 1:
            return words[0]

Solution = create_solution(solution_function_name)