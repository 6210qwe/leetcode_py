"""
核心思想: 使用二分查找和前缀和来找到最小的整体不适宜度。

算法步骤:
1. 初始化二分查找的上下界。
2. 对于每个中间值，使用前缀和数组检查是否可以通过修改某些指令来达到该中间值。
3. 如果可以，则更新上界；否则，更新下界。
4. 最终返回下界作为结果。

关键点:
- 使用二分查找来缩小搜索范围。
- 使用前缀和数组来快速计算子数组的和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log S)，其中 n 是 operate 的长度，S 是 operate 中的最大值。
空间复杂度: O(n)，用于存储前缀和数组。
"""

# ============================================================================
