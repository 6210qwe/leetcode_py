"""
502. IPO - 假设 力扣（LeetCode）即将开始 IPO 。为了以更高的价格将股票卖给风险投资公司，力扣 希望在 IPO 之前开展一些项目以增加其资本。 由于资源有限，它只能在 IPO 之前完成最多 k 个不同的项目。帮助 力扣 设计完成最多 k 个不同项目后得到最大总资本的方式。 给你 n 个项目。对于每个项目 i ，它都有一个纯利润 profits[i] ，和启动该项目需要的最小资本 capital[i] 。 最初，你的资本为 w 。当你完成一个项目时，你将获得纯利润，且利润将被添加到你的总资本中。 总而言之，从给定项目中选择 最多 k 个不同项目的列表，以 最大化最终资本 ，并输出最终可获得的最多资本。 答案保证在 32 位有符号整数范围内。 示例 1： 输入：k = 2, w = 0, profits = [1,2,3], capital = [0,1,1] 输出：4 解释： 由于你的初始资本为 0，你仅可以从 0 号项目开始。 在完成后，你将获得 1 的利润，你的总资本将变为 1。 此时你可以选择开始 1 号或 2 号项目。 由于你最多可以选择两个项目，所以你需要完成 2 号项目以获得最大的资本。 因此，输出最后最大化的资本，为 0 + 1 + 3 = 4。 示例 2： 输入：k = 3, w = 0, profits = [1,2,3], capital = [0,1,2] 输出：6 提示： * 1 <= k <= 105 * 0 <= w <= 109 * n == profits.length * n == capital.length * 1 <= n <= 105 * 0 <= profits[i] <= 104 * 0 <= capital[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法结合优先队列（最大堆）来选择当前可以启动的项目中利润最大的项目。

算法步骤:
1. 将所有项目按照启动资本从小到大排序。
2. 使用一个最大堆来存储当前可以启动的项目的利润。
3. 每次从最大堆中取出利润最大的项目，并更新资本。
4. 如果当前资本足够启动新的项目，则将这些项目的利润加入最大堆。
5. 重复上述步骤直到完成 k 个项目或没有更多可以启动的项目。

关键点:
- 使用最大堆来高效地获取当前可以启动的项目中利润最大的项目。
- 通过排序和堆操作来确保每次选择的都是最优解。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是项目的数量。排序操作的时间复杂度是 O(n log n)，堆操作的时间复杂度是 O(log n)。
空间复杂度: O(n)，用于存储项目和最大堆。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def findMaximizedCapital(k: int, w: int, profits: List[int], capital: List[int]) -> int:
    """
    函数式接口 - 实现 IPO 项目选择的最大化资本
    """
    # 将项目按启动资本从小到大排序
    projects = sorted(zip(capital, profits))
    
    max_heap = []
    current_capital = w
    index = 0
    
    for _ in range(k):
        # 将当前资本可以启动的所有项目加入最大堆
        while index < len(projects) and projects[index][0] <= current_capital:
            heapq.heappush(max_heap, -projects[index][1])
            index += 1
        
        # 如果最大堆为空，表示没有更多可以启动的项目
        if not max_heap:
            break
        
        # 从最大堆中取出利润最大的项目
        current_capital += -heapq.heappop(max_heap)
    
    return current_capital

