# ============================================================================
# 问题描述
# ============================================================================
"""
3324. 出现在屏幕上的字符串序列 - 给你一个字符串 target。 Alice 将会使用一种特殊的键盘在她的电脑上输入 target，这个键盘 只有两个 按键： * 按键 1：在屏幕上的字符串后追加字符 'a'。 * 按键 2：将屏幕上字符串的 最后一个 字符更改为英文字母表中的 下一个 字符。例如，'c' 变为 'd'，'z' 变为 'a'。 注意，最初屏幕上是一个空字符串 ""，所以她 只能 按按键 1。 请你考虑按键次数 最少 的情况，按字符串出现顺序，返回 Alice 输入 target 时屏幕上出现的所有字符串列表。 示例 1： 输入： target = "abc" 输出： ["a","aa","ab","aba","abb","abc"] 解释： Alice 按键的顺序如下： * 按下按键 1，屏幕上的字符串变为 "a"。 * 按下按键 1，屏幕上的字符串变为 "aa"。 * 按下按键 2，屏幕上的字符串变为 "ab"。 * 按下按键 1，屏幕上的字符串变为 "aba"。 * 按下按键 2，屏幕上的字符串变为 "abb"。 * 按下按键 2，屏幕上的字符串变为 "abc"。 示例 2： 输入： target = "he" 输出： ["a","b","c","d","e","f","g","h","ha","hb","hc","hd","he"] 提示： * 1 <= target.length <= 400 * target 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过模拟按键操作，逐步构建目标字符串，并记录每一步生成的字符串。

算法步骤:
1. 初始化当前字符串为空字符串。
2. 遍历目标字符串的每个字符，逐步构建当前字符串。
3. 对于每个字符，先通过按键 1 追加 'a'，然后通过按键 2 将最后一个字符变为目标字符。
4. 记录每一步生成的字符串。

关键点:
- 通过按键 1 和按键 2 的组合，逐步构建目标字符串。
- 记录每一步生成的字符串，确保按顺序返回。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是目标字符串的长度。最坏情况下，每次按键 2 都需要遍历之前的所有字符。
空间复杂度: O(n^2)，存储所有生成的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(target: str) -> List[str]:
    """
    函数式接口 - 返回 Alice 输入 target 时屏幕上出现的所有字符串列表
    """
    result = []
    current = ""
    
    for char in target:
        # 追加 'a' 直到当前字符串的长度等于目标字符的位置
        while len(current) < len(target):
            current += 'a'
            result.append(current)
        
        # 将最后一个字符更改为目标字符
        while current[-1] != char:
            current = current[:-1] + chr((ord(current[-1]) - ord('a') + 1) % 26 + ord('a'))
            result.append(current)
    
    return result


Solution = create_solution(solution_function_name)