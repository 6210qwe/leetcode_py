"""
3216. 交换后字典序最小的字符串 - 给你一个仅由数字组成的字符串 s，在最多交换一次 相邻 且具有相同 奇偶性 的数字后，返回可以得到的字典序最小的字符串。 如果两个数字都是奇数或都是偶数，则它们具有相同的奇偶性。例如，5 和 9、2 和 4 奇偶性相同，而 6 和 9 奇偶性不同。 示例 1： 输入： s = "45320" 输出： "43520" 解释： s[1] == '5' 和 s[2] == '3' 都具有相同的奇偶性，交换它们可以得到字典序最小的字符串。 示例 2： 输入： s = "001" 输出： "001" 解释： 无需进行交换，因为 s 已经是字典序最小的。 提示： * 2 <= s.length <= 100 * s 仅由数字组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串找到第一个可以交换的位置，使得交换后的字符串字典序最小。

算法步骤:
1. 遍历字符串，找到第一个可以交换的位置。
2. 交换相邻且具有相同奇偶性的数字，使得交换后的字符串字典序最小。
3. 返回交换后的字符串。

关键点:
- 只需要一次遍历即可找到最优解。
- 交换时确保相邻且具有相同奇偶性的数字。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> str:
    """
    函数式接口 - 交换后字典序最小的字符串
    """
    n = len(s)
    for i in range(n - 1):
        if (int(s[i]) % 2 == int(s[i + 1]) % 2) and s[i] > s[i + 1]:
            # 交换相邻且具有相同奇偶性的数字
            return s[:i] + s[i + 1] + s[i] + s[i + 2:]
    return s


Solution = create_solution(solution_function_name)