def solution_function_name():
    """
    函数式接口 - 返回财富大于等于 50000 的客户数量
    """
    # SQL 查询语句
    query = """
    SELECT COUNT(*) AS rich_customers_count
    FROM Customers
    WHERE wealth >= 50000;
    """
    return query


