"""
1053. 交换一次的先前排列 - 给你一个正整数数组 arr（可能存在重复的元素），请你返回可在 一次交换（交换两数字 arr[i] 和 arr[j] 的位置）后得到的、按字典序排列小于 arr 的最大排列。 如果无法这么操作，就请返回原数组。 示例 1： 输入：arr = [3,2,1] 输出：[3,1,2] 解释：交换 2 和 1 示例 2： 输入：arr = [1,1,5] 输出：[1,1,5] 解释：已经是最小排列 示例 3： 输入：arr = [1,9,4,6,7] 输出：[1,7,4,6,9] 解释：交换 9 和 7 提示： * 1 <= arr.length <= 104 * 1 <= arr[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从右向左找到第一个降序对 (i, i+1)，然后在 i+1 到末尾中找到比 arr[i] 小的最大值进行交换。

算法步骤:
1. 从右向左遍历数组，找到第一个降序对 (i, i+1)。
2. 在 i+1 到末尾中找到比 arr[i] 小的最大值的位置 j。
3. 交换 arr[i] 和 arr[j]。
4. 返回结果。

关键点:
- 找到第一个降序对 (i, i+1)。
- 在 i+1 到末尾中找到比 arr[i] 小的最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int]) -> List[int]:
    """
    函数式接口 - 实现
    """
    n = len(arr)
    if n < 2:
        return arr

    # 从右向左找到第一个降序对 (i, i+1)
    i = n - 2
    while i >= 0 and arr[i] <= arr[i + 1]:
        i -= 1

    if i == -1:
        return arr

    # 在 i+1 到末尾中找到比 arr[i] 小的最大值的位置 j
    j = n - 1
    while j > i and arr[j] >= arr[i]:
        j -= 1

    # 交换 arr[i] 和 arr[j]
    arr[i], arr[j] = arr[j], arr[i]

    return arr


Solution = create_solution(solution_function_name)