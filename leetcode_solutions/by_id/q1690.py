"""
1567. 乘积为正数的最长子数组长度 - 给你一个整数数组 nums ，请你求出乘积为正数的最长子数组的长度。 一个数组的子数组是由原数组中零个或者更多个连续数字组成的数组。 请你返回乘积为正数的最长子数组长度。 示例 1： 输入：nums = [1,-2,-3,4] 输出：4 解释：数组本身乘积就是正数，值为 24 。 示例 2： 输入：nums = [0,1,-2,-3,-4] 输出：3 解释：最长乘积为正数的子数组为 [1,-2,-3] ，乘积为 6 。 注意，我们不能把 0 也包括到子数组中，因为这样乘积为 0 ，不是正数。 示例 3： 输入：nums = [-1,-2,-3,0,1] 输出：2 解释：乘积为正数的最长子数组是 [-1,-2] 或者 [-2,-3] 。 提示： * 1 <= nums.length <= 10^5 * -10^9 <= nums[i] <= 10^9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来维护当前子数组的正数和负数长度。

算法步骤:
1. 初始化两个变量 `pos` 和 `neg`，分别表示以当前元素结尾的乘积为正数和负数的最长子数组长度。
2. 遍历数组，根据当前元素的正负性更新 `pos` 和 `neg`。
3. 如果当前元素为正数，则 `pos` 增加 1，如果 `neg` 不为 0，则 `neg` 也增加 1。
4. 如果当前元素为负数，则交换 `pos` 和 `neg` 的值，并将 `neg` 增加 1。
5. 如果当前元素为 0，则重置 `pos` 和 `neg` 为 0。
6. 在每次更新后，记录最大 `pos` 值。

关键点:
- 动态规划的状态转移方程取决于当前元素的正负性。
- 通过维护 `pos` 和 `neg` 来避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历一次数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 返回乘积为正数的最长子数组长度
    """
    if not nums:
        return 0

    max_len = 0
    pos = 0
    neg = 0

    for num in nums:
        if num > 0:
            pos += 1
            if neg > 0:
                neg += 1
        elif num < 0:
            pos, neg = neg + 1 if neg > 0 else 0, pos + 1
        else:
            pos = 0
            neg = 0
        max_len = max(max_len, pos)

    return max_len


Solution = create_solution(solution_function_name)