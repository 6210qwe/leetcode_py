"""
1890. 2020年最后一次登录 - 表: Logins +----------------+----------+ | 列名 | 类型 | +----------------+----------+ | user_id | int | | time_stamp | datetime | +----------------+----------+ (user_id, time_stamp) 是这个表的主键(具有唯一值的列的组合)。 每一行包含的信息是user_id 这个用户的登录时间。 编写解决方案以获取在 2020 年登录过的所有用户的本年度 最后一次 登录时间。结果集 不 包含 2020 年没有登录过的用户。 返回的结果集可以按 任意顺序 排列。 返回结果格式如下例。 示例 1: 输入： Logins 表: +---------+---------------------+ | user_id | time_stamp | +---------+---------------------+ | 6 | 2020-06-30 15:06:07 | | 6 | 2021-04-21 14:06:06 | | 6 | 2019-03-07 00:18:15 | | 8 | 2020-02-01 05:10:53 | | 8 | 2020-12-30 00:46:50 | | 2 | 2020-01-16 02:49:50 | | 2 | 2019-08-25 07:59:08 | | 14 | 2019-07-14 09:00:00 | | 14 | 2021-01-06 11:59:59 | +---------+---------------------+ 输出： +---------+---------------------+ | user_id | last_stamp | +---------+---------------------+ | 6 | 2020-06-30 15:06:07 | | 8 | 2020-12-30 00:46:50 | | 2 | 2020-01-16 02:49:50 | +---------+---------------------+ 解释： 6号用户登录了3次，但是在2020年仅有一次，所以结果集应包含此次登录。 8号用户在2020年登录了2次，一次在2月，一次在12月，所以，结果集应该包含12月的这次登录。 2号用户登录了2次，但是在2020年仅有一次，所以结果集应包含此次登录。 14号用户在2020年没有登录，所以结果集不应包含。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 查询来过滤出 2020 年的登录记录，并找到每个用户的最后一次登录时间。

算法步骤:
1. 过滤出 2020 年的登录记录。
2. 对每个用户，找到其最后一次登录的时间。

关键点:
- 使用 `WHERE` 子句过滤出 2020 年的记录。
- 使用 `GROUP BY` 和 `MAX` 函数找到每个用户的最后一次登录时间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 Logins 表中的记录数。这是因为 SQL 引擎可能需要对数据进行排序和分组操作。
空间复杂度: O(n)，因为我们需要存储 2020 年的所有登录记录。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(logins: List[List[str]]) -> List[List[str]]:
    """
    函数式接口 - 获取 2020 年每个用户的最后一次登录时间
    """
    from collections import defaultdict
    from datetime import datetime

    # 创建一个字典来存储每个用户的最后一次登录时间
    last_login = defaultdict(lambda: datetime.min)

    for user_id, time_stamp in logins:
        time_stamp = datetime.fromisoformat(time_stamp)
        if time_stamp.year == 2020 and time_stamp > last_login[user_id]:
            last_login[user_id] = time_stamp

    # 将结果转换为列表
    result = [[user_id, time_stamp.isoformat(sep=' ', timespec='seconds')] for user_id, time_stamp in last_login.items() if time_stamp != datetime.min]

    return result


Solution = create_solution(solution_function_name)