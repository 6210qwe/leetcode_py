"""
2164. 对奇偶下标分别排序 - 给你一个下标从 0 开始的整数数组 nums 。根据下述规则重排 nums 中的值： 1. 按 非递增 顺序排列 nums 奇数下标 上的所有值。 * 举个例子，如果排序前 nums = [4,1,2,3] ，对奇数下标的值排序后变为 [4,3,2,1] 。奇数下标 1 和 3 的值按照非递增顺序重排。 2. 按 非递减 顺序排列 nums 偶数下标 上的所有值。 * 举个例子，如果排序前 nums = [4,1,2,3] ，对偶数下标的值排序后变为 [2,1,4,3] 。偶数下标 0 和 2 的值按照非递减顺序重排。 返回重排 nums 的值之后形成的数组。 示例 1： 输入：nums = [4,1,2,3] 输出：[2,3,4,1] 解释： 首先，按非递增顺序重排奇数下标（1 和 3）的值。 所以，nums 从 [4,1,2,3] 变为 [4,3,2,1] 。 然后，按非递减顺序重排偶数下标（0 和 2）的值。 所以，nums 从 [4,1,2,3] 变为 [2,3,4,1] 。 因此，重排之后形成的数组是 [2,3,4,1] 。 示例 2： 输入：nums = [2,1] 输出：[2,1] 解释： 由于只有一个奇数下标和一个偶数下标，所以不会发生重排。 形成的结果数组是 [2,1] ，和初始数组一样。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 分别提取奇数下标和偶数下标的值，进行排序后再放回原数组。

算法步骤:
1. 提取奇数下标和偶数下标的值。
2. 对奇数下标的值进行非递增排序。
3. 对偶数下标的值进行非递减排序。
4. 将排序后的值放回原数组。

关键点:
- 使用列表切片提取奇数和偶数下标的值。
- 使用 Python 内置的 `sorted` 函数进行排序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(n)，需要额外的空间存储奇数和偶数下标的值。
"""

# ============================================================================
# 代码实现
