"""
1200. 最小绝对差 - 给你个整数数组 arr，其中每个元素都 不相同。 请你找到所有具有最小绝对差的元素对，并且按升序的顺序返回。 每对元素对 [a,b] 如下： * a , b 均为数组 arr 中的元素 * a < b * b - a 等于 arr 中任意两个元素的最小绝对差 示例 1： 输入：arr = [4,2,1,3] 输出：[[1,2],[2,3],[3,4]] 示例 2： 输入：arr = [1,3,6,10,15] 输出：[[1,3]] 示例 3： 输入：arr = [3,8,-10,23,19,-4,-14,27] 输出：[[-14,-10],[19,23],[23,27]] 提示： * 2 <= arr.length <= 10^5 * -10^6 <= arr[i] <= 10^6
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 先对数组进行排序，然后遍历相邻元素对，找到最小绝对差的元素对。

算法步骤:
1. 对数组进行排序。
2. 初始化最小绝对差为无穷大。
3. 遍历排序后的数组，计算相邻元素的差值，更新最小绝对差。
4. 再次遍历数组，将所有具有最小绝对差的元素对加入结果列表。

关键点:
- 排序后相邻元素的差值是可能的最小绝对差。
- 只需要一次遍历即可找到所有具有最小绝对差的元素对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 主要由排序操作决定。
空间复杂度: O(1) - 除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def minimum_abs_difference(arr: List[int]) -> List[List[int]]:
    """
    函数式接口 - 找到所有具有最小绝对差的元素对，并且按升序的顺序返回。
    """
    # 对数组进行排序
    arr.sort()
    
    # 初始化最小绝对差为无穷大
    min_diff = float('inf')
    
    # 遍历排序后的数组，计算相邻元素的差值，更新最小绝对差
    for i in range(len(arr) - 1):
        diff = arr[i + 1] - arr[i]
        if diff < min_diff:
            min_diff = diff
    
    # 再次遍历数组，将所有具有最小绝对差的元素对加入结果列表
    result = []
    for i in range(len(arr) - 1):
        if arr[i + 1] - arr[i] == min_diff:
            result.append([arr[i], arr[i + 1]])
    
    return result

