"""
2970. 统计移除递增子数组的数目 I - 给你一个下标从 0 开始的 正 整数数组 nums 。 如果 nums 的一个子数组满足：移除这个子数组后剩余元素 严格递增 ，那么我们称这个子数组为 移除递增 子数组。比方说，[5, 3, 4, 6, 7] 中的 [3, 4] 是一个移除递增子数组，因为移除该子数组后，[5, 3, 4, 6, 7] 变为 [5, 6, 7] ，是严格递增的。 请你返回 nums 中 移除递增 子数组的总数目。 注意 ，剩余元素为空的数组也视为是递增的。 子数组 指的是一个数组中一段非空且连续的元素序列。 示例 1： 输入：nums = [1,2,3,4] 输出：10 解释：10 个移除递增子数组分别为：[1], [2], [3], [4], [1,2], [2,3], [3,4], [1,2,3], [2,3,4] 和 [1,2,3,4]。移除任意一个子数组后，剩余元素都是递增的。注意，空数组不是移除递增子数组。 示例 2： 输入：nums = [6,5,7,8] 输出：7 解释：7 个移除递增子数组分别为：[5], [6], [5,7], [6,5], [5,7,8], [6,5,7] 和 [6,5,7,8] 。 nums 中只有这 7 个移除递增子数组。 示例 3： 输入：nums = [8,7,6,6] 输出：3 解释：3 个移除递增子数组分别为：[8,7,6], [7,6,6] 和 [8,7,6,6] 。注意 [8,7] 不是移除递增子数组因为移除 [8,7] 后 nums 变为 [6,6] ，它不是严格递增的。 提示： * 1 <= nums.length <= 50 * 1 <= nums[i] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针和枚举来找到所有可能的移除递增子数组。

算法步骤:
1. 初始化两个指针 left 和 right，分别指向数组的起始和末尾。
2. 从左到右移动 left 指针，直到找到第一个不满足严格递增的位置。
3. 从右到左移动 right 指针，直到找到第一个不满足严格递增的位置。
4. 枚举所有可能的移除子数组，检查移除后的数组是否严格递增。
5. 计算并返回移除递增子数组的总数目。

关键点:
- 使用双指针找到不满足严格递增的位置。
- 通过枚举和检查来确定移除子数组后的数组是否严格递增。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是数组的长度。最坏情况下需要枚举所有子数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_incremovable_subarrays(nums: List[int]) -> int:
    """
    函数式接口 - 统计移除递增子数组的数目
    """
    n = len(nums)
    count = 0

    # 找到第一个不满足严格递增的位置
    left = 0
    while left < n - 1 and nums[left] < nums[left + 1]:
        left += 1

    # 如果整个数组已经是严格递增的
    if left == n - 1:
        return (n * (n + 1)) // 2

    # 找到最后一个不满足严格递增的位置
    right = n - 1
    while right > 0 and nums[right] > nums[right - 1]:
        right -= 1

    # 枚举所有可能的移除子数组
    for i in range(left + 1):
        for j in range(right, n):
            if i >= j:
                count += 1
            else:
                if all(nums[k] < nums[k + 1] for k in range(i, j - 1)):
                    count += 1

    return count


Solution = create_solution(count_incremovable_subarrays)