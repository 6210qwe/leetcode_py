"""
908. 最小差值 I - 给你一个整数数组 nums，和一个整数 k 。 在一个操作中，您可以选择 0 <= i < nums.length 的任何索引 i 。将 nums[i] 改为 nums[i] + x ，其中 x 是一个范围为 [-k, k] 的任意整数。对于每个索引 i ，最多 只能 应用 一次 此操作。 nums 的 分数 是 nums 中最大和最小元素的差值。 在对 nums 中的每个索引最多应用一次上述操作后，返回 nums 的最低 分数 。 示例 1： 输入：nums = [1], k = 0 输出：0 解释：分数是 max(nums) - min(nums) = 1 - 1 = 0。 示例 2： 输入：nums = [0,10], k = 2 输出：6 解释：将 nums 改为 [2,8]。分数是 max(nums) - min(nums) = 8 - 2 = 6。 示例 3： 输入：nums = [1,3,6], k = 3 输出：0 解释：将 nums 改为 [4,4,4]。分数是 max(nums) - min(nums) = 4 - 4 = 0。 提示： * 1 <= nums.length <= 104 * 0 <= nums[i] <= 104 * 0 <= k <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过调整数组中的每个元素，使得数组的最大值和最小值之间的差值尽可能小。

算法步骤:
1. 找到数组中的最大值和最小值。
2. 计算最大值减去 k 和最小值加上 k 后的差值。
3. 如果这个差值小于等于 0，则返回 0，否则返回这个差值。

关键点:
- 通过调整最大值和最小值，可以使得数组中的所有元素尽可能接近。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度，因为我们需要遍历数组来找到最大值和最小值。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def smallestRangeI(nums: List[int], k: int) -> int:
    """
    函数式接口 - 返回调整后的数组的最小分数
    """
    # 找到数组中的最大值和最小值
    max_val = max(nums)
    min_val = min(nums)
    
    # 计算调整后的最大值和最小值
    adjusted_max = max_val - k
    adjusted_min = min_val + k
    
    # 计算调整后的差值
    diff = adjusted_max - adjusted_min
    
    # 如果差值小于等于 0，则返回 0，否则返回差值
    return max(diff, 0)

