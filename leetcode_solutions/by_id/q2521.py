"""
2435. 矩阵中和能被 K 整除的路径 - 给你一个下标从 0 开始的 m x n 整数矩阵 grid 和一个整数 k 。你从起点 (0, 0) 出发，每一步只能往 下 或者往 右 ，你想要到达终点 (m - 1, n - 1) 。 请你返回路径和能被 k 整除的路径数目，由于答案可能很大，返回答案对 109 + 7 取余 的结果。 示例 1： [https://assets.leetcode.com/uploads/2022/08/13/image-20220813183124-1.png] 输入：grid = [[5,2,4],[3,0,5],[0,7,2]], k = 3 输出：2 解释：有两条路径满足路径上元素的和能被 k 整除。 第一条路径为上图中用红色标注的路径，和为 5 + 2 + 4 + 5 + 2 = 18 ，能被 3 整除。 第二条路径为上图中用蓝色标注的路径，和为 5 + 3 + 0 + 5 + 2 = 15 ，能被 3 整除。 示例 2： [https://assets.leetcode.com/uploads/2022/08/17/image-20220817112930-3.png] 输入：grid = [[0,0]], k = 5 输出：1 解释：红色标注的路径和为 0 + 0 = 0 ，能被 5 整除。 示例 3： [https://assets.leetcode.com/uploads/2022/08/12/image-20220812224605-3.png] 输入：grid = [[7,3,4,9],[2,3,6,2],[2,3,7,0]], k = 1 输出：10 解释：每个数字都能被 1 整除，所以每一条路径的和都能被 k 整除。 提示： * m == grid.length * n == grid[i].length * 1 <= m, n <= 5 * 104 * 1 <= m * n <= 5 * 104 * 0 <= grid[i][j] <= 100 * 1 <= k <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来记录从起点到每个位置的路径和模 k 的值及其数量。

算法步骤:
1. 初始化一个三维数组 dp，其中 dp[i][j][r] 表示从起点 (0, 0) 到达 (i, j) 且路径和模 k 为 r 的路径数目。
2. 初始化 dp[0][0][grid[0][0] % k] 为 1，表示从起点出发的路径和模 k 的值。
3. 遍历矩阵，对于每个位置 (i, j)，更新 dp[i][j][r]，其中 r 是从上方或左方到达 (i, j) 的路径和模 k 的值。
4. 最终结果是 dp[m-1][n-1][0]，即从起点到终点且路径和模 k 为 0 的路径数目。

关键点:
- 使用三维数组 dp 来记录路径和模 k 的值及其数量。
- 通过动态规划避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n * k)
空间复杂度: O(m * n * k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def count_paths_divisible_by_k(grid: List[List[int]], k: int) -> int:
    MOD = 10**9 + 7
    m, n = len(grid), len(grid[0])
    
    # 初始化 dp 数组
    dp = [[[0 for _ in range(k)] for _ in range(n)] for _ in range(m)]
    dp[0][0][grid[0][0] % k] = 1
    
    # 动态规划填充 dp 数组
    for i in range(m):
        for j in range(n):
            for r in range(k):
                if i > 0:
                    dp[i][j][(r + grid[i][j]) % k] += dp[i-1][j][r]
                    dp[i][j][(r + grid[i][j]) % k] %= MOD
                if j > 0:
                    dp[i][j][(r + grid[i][j]) % k] += dp[i][j-1][r]
                    dp[i][j][(r + grid[i][j]) % k] %= MOD
    
    return dp[m-1][n-1][0]

Solution = create_solution(count_paths_divisible_by_k)