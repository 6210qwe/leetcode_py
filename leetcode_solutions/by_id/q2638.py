from typing import List

def numberOfSubarraysHavingEvenProduct(nums: List[int]) -> int:
    """
    计算乘积为偶数的子数组数量
    """
    even_count = 0
    odd_count = 0
    result = 0
    
    for num in nums:
        if num % 2 == 0:
            even_count += 1
            result += even_count
        else:
            even_count, odd_count = odd_count + even_count, even_count
            result += even_count
    
    return result

