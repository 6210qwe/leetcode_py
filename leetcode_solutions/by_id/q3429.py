"""
3151. 特殊数组 I - 如果数组的每一对相邻元素都是两个奇偶性不同的数字，则该数组被认为是一个 特殊数组。换句话说，每一对中的元素 必须 有一个是偶数，另一个是奇数。 你有一个整数数组 nums。如果 nums 是一个 特殊数组 ，返回 true，否则返回 false。 示例 1： 输入：nums = [1] 输出：true 解释： 只有一个元素，所以答案为 true。 示例 2： 输入：nums = [2,1,4] 输出：true 解释： 只有两对相邻元素： (2,1) 和 (1,4)，它们都包含了奇偶性不同的数字，因此答案为 true。 示例 3： 输入：nums = [4,3,1,6] 输出：false 解释： nums[1] 和 nums[2] 都是奇数。因此答案为 false。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，检查每一对相邻元素的奇偶性是否不同。

算法步骤:
1. 遍历数组，从第一个元素到倒数第二个元素。
2. 对于每一对相邻元素，检查它们的奇偶性是否不同。
3. 如果发现任何一对相邻元素的奇偶性相同，则返回 False。
4. 如果遍历完整个数组都没有发现奇偶性相同的相邻元素，则返回 True。

关键点:
- 使用异或运算来判断两个数的奇偶性是否不同。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历一次数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_special_array(nums: List[int]) -> bool:
    """
    函数式接口 - 判断数组是否为特殊数组
    """
    for i in range(len(nums) - 1):
        if (nums[i] % 2) == (nums[i + 1] % 2):
            return False
    return True


Solution = create_solution(is_special_array)