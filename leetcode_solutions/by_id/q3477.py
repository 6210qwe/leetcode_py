"""
3192. 使二进制数组全部等于 1 的最少操作次数 II - 给你一个二进制数组 nums 。 你可以对数组执行以下操作 任意 次（也可以 0 次）： * 选择数组中 任意 一个下标 i ，并将从下标 i 开始一直到数组末尾 所有 元素 反转 。 反转 一个元素指的是将它的值从 0 变 1 ，或者从 1 变 0 。 请你返回将 nums 中所有元素变为 1 的 最少 操作次数。 示例 1： 输入：nums = [0,1,1,0,1] 输出：4 解释： 我们可以执行以下操作： * 选择下标 i = 1 执行操作，得到 nums = [0,0,0,1,0] 。 * 选择下标 i = 0 执行操作，得到 nums = [1,1,1,0,1] 。 * 选择下标 i = 4 执行操作，得到 nums = [1,1,1,0,0] 。 * 选择下标 i = 3 执行操作，得到 nums = [1,1,1,1,1] 。 示例 2： 输入：nums = [1,0,0,0] 输出：1 解释： 我们可以执行以下操作： * 选择下标 i = 1 执行操作，得到 nums = [1,1,1,1] 。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，每次找到最左边的 0 并将其及其后的所有元素反转，直到所有元素都变为 1。

算法步骤:
1. 初始化操作次数为 0。
2. 遍历数组，找到最左边的 0。
3. 如果找到 0，则将其及其后的所有元素反转，并增加操作次数。
4. 重复步骤 2 和 3，直到数组中没有 0。

关键点:
- 每次只反转最左边的 0 及其后的所有元素，这样可以确保每次操作都是最优的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个元素最多被访问两次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_one(nums: List[int]) -> int:
    """
    函数式接口 - 返回将 nums 中所有元素变为 1 的最少操作次数
    """
    operations = 0
    n = len(nums)
    
    for i in range(n):
        if nums[i] == 0:
            # 反转从 i 到末尾的所有元素
            for j in range(i, n):
                nums[j] = 1 - nums[j]
            operations += 1
    
    return operations


Solution = create_solution(min_operations_to_one)