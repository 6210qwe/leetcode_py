"""
核心思想: 使用双指针来比较两个数组，找到第一个不同的元素。

算法步骤:
1. 初始化两个指针 i 和 j，分别指向两个数组的起始位置。
2. 逐个比较两个数组的元素，如果发现不同，则返回 "sensor1" 或 "sensor2"。
3. 如果遍历完所有元素都没有发现不同，则返回 -1。

关键点:
- 使用双指针可以同时遍历两个数组，时间复杂度为 O(n)。
- 只需要常数级别的额外空间。
"""

