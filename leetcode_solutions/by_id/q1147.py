"""
1072. 按列翻转得到最大值等行数 - 给定 m x n 矩阵 matrix 。 你可以从中选出任意数量的列并翻转其上的 每个 单元格。（即翻转后，单元格的值从 0 变成 1，或者从 1 变为 0 。） 返回 经过一些翻转后，行内所有值都相等的最大行数 。 示例 1： 输入：matrix = [[0,1],[1,1]] 输出：1 解释：不进行翻转，有 1 行所有值都相等。 示例 2： 输入：matrix = [[0,1],[1,0]] 输出：2 解释：翻转第一列的值之后，这两行都由相等的值组成。 示例 3： 输入：matrix = [[0,0,0],[0,0,1],[1,1,0]] 输出：2 解释：翻转前两列的值之后，后两行由相等的值组成。 提示： * m == matrix.length * n == matrix[i].length * 1 <= m, n <= 300 * matrix[i][j] == 0 或 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来记录每行的模式及其出现次数。对于每一行，我们可以通过将每个元素与第一个元素异或来生成一个模式字符串。这样，相同的行在翻转后会有相同的模式。

算法步骤:
1. 初始化一个哈希表来记录每种模式的出现次数。
2. 遍历每一行，生成该行的模式字符串。
3. 更新哈希表中该模式的出现次数。
4. 找出哈希表中最大值，即为最大行数。

关键点:
- 通过异或操作生成模式字符串，确保相同行在翻转后具有相同的模式。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是矩阵的行数，n 是矩阵的列数。我们需要遍历整个矩阵。
空间复杂度: O(m * n)，在最坏情况下，哈希表中可能存储所有不同的模式。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_equal_rows_after_flips(matrix: List[List[int]]) -> int:
    """
    函数式接口 - 计算经过一些翻转后，行内所有值都相等的最大行数
    """
    from collections import defaultdict

    pattern_count = defaultdict(int)

    for row in matrix:
        # 生成模式字符串
        pattern = tuple(row[0] ^ val for val in row)
        pattern_count[pattern] += 1

    # 找出最大行数
    return max(pattern_count.values())


Solution = create_solution(max_equal_rows_after_flips)