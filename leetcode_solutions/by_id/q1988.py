"""
1877. 数组中最大数对和的最小值 - 一个数对 (a,b) 的 数对和 等于 a + b 。最大数对和 是一个数对数组中最大的 数对和 。 * 比方说，如果我们有数对 (1,5) ，(2,3) 和 (4,4)，最大数对和 为 max(1+5, 2+3, 4+4) = max(6, 5, 8) = 8 。 给你一个长度为 偶数 n 的数组 nums ，请你将 nums 中的元素分成 n / 2 个数对，使得： * nums 中每个元素 恰好 在 一个 数对中，且 * 最大数对和 的值 最小 。 请你在最优数对划分的方案下，返回最小的 最大数对和 。 示例 1： 输入：nums = [3,5,2,3] 输出：7 解释：数组中的元素可以分为数对 (3,3) 和 (5,2) 。 最大数对和为 max(3+3, 5+2) = max(6, 7) = 7 。 示例 2： 输入：nums = [3,5,4,2,4,6] 输出：8 解释：数组中的元素可以分为数对 (3,5)，(4,4) 和 (6,2) 。 最大数对和为 max(3+5, 4+4, 6+2) = max(8, 8, 8) = 8 。 提示： * n == nums.length * 2 <= n <= 105 * n 是 偶数 。 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，将数组从小到大排序后，使用双指针从两端向中间配对，以最小化最大数对和。

算法步骤:
1. 对数组进行排序。
2. 使用双指针从两端向中间配对，计算每对数的和。
3. 记录所有数对和的最大值。

关键点:
- 通过排序和双指针方法，确保每对数的和尽可能小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)，后续遍历操作为 O(n)。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimize_max_pair_sum(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 对数组进行排序
    nums.sort()
    
    # 初始化最大数对和
    max_pair_sum = 0
    
    # 使用双指针从两端向中间配对
    left, right = 0, len(nums) - 1
    while left < right:
        pair_sum = nums[left] + nums[right]
        max_pair_sum = max(max_pair_sum, pair_sum)
        left += 1
        right -= 1
    
    return max_pair_sum


Solution = create_solution(minimize_max_pair_sum)