"""
1979. 找出数组的最大公约数 - 给你一个整数数组 nums ，返回数组中最大数和最小数的 最大公约数 。 两个数的 最大公约数 是能够被两个数整除的最大正整数。 示例 1： 输入：nums = [2,5,6,9,10] 输出：2 解释： nums 中最小的数是 2 nums 中最大的数是 10 2 和 10 的最大公约数是 2 示例 2： 输入：nums = [7,5,6,8,3] 输出：1 解释： nums 中最小的数是 3 nums 中最大的数是 8 3 和 8 的最大公约数是 1 示例 3： 输入：nums = [3,3] 输出：3 解释： nums 中最小的数是 3 nums 中最大的数是 3 3 和 3 的最大公约数是 3 提示： * 2 <= nums.length <= 1000 * 1 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用欧几里得算法（辗转相除法）来计算最大公约数。

算法步骤:
1. 找到数组中的最小值和最大值。
2. 使用欧几里得算法计算这两个数的最大公约数。

关键点:
- 欧几里得算法的时间复杂度为 O(log(min(a, b)))，其中 a 和 b 是两个数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + log(min(a, b)))，其中 n 是数组的长度，a 和 b 分别是数组中的最小值和最大值。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_gcd(nums: List[int]) -> int:
    """
    计算数组中最大数和最小数的最大公约数
    """
    # 找到数组中的最小值和最大值
    min_num = min(nums)
    max_num = max(nums)

    # 使用欧几里得算法计算最大公约数
    def gcd(a: int, b: int) -> int:
        while b != 0:
            a, b = b, a % b
        return a

    return gcd(min_num, max_num)


Solution = create_solution(find_gcd)