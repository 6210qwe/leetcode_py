"""
804. 唯一摩尔斯密码词 - 国际摩尔斯密码定义一种标准编码方式，将每个字母对应于一个由一系列点和短线组成的字符串， 比如: * 'a' 对应 ".-" ， * 'b' 对应 "-..." ， * 'c' 对应 "-.-." ，以此类推。 为了方便，所有 26 个英文字母的摩尔斯密码表如下： [".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."] 给你一个字符串数组 words ，每个单词可以写成每个字母对应摩尔斯密码的组合。 * 例如，"cab" 可以写成 "-.-..--..." ，(即 "-.-." + ".-" + "-..." 字符串的结合)。我们将这样一个连接过程称作 单词翻译 。 对 words 中所有单词进行单词翻译，返回不同 单词翻译 的数量。 示例 1： 输入: words = ["gin", "zen", "gig", "msg"] 输出: 2 解释: 各单词翻译如下: "gin" -> "--...-." "zen" -> "--...-." "gig" -> "--...--." "msg" -> "--...--." 共有 2 种不同翻译, "--...-." 和 "--...--.". 示例 2： 输入：words = ["a"] 输出：1 提示： * 1 <= words.length <= 100 * 1 <= words[i].length <= 12 * words[i] 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表存储摩尔斯密码表，并使用集合来存储不同的摩尔斯密码翻译结果。

算法步骤:
1. 初始化摩尔斯密码表。
2. 遍历每个单词，将其转换为摩尔斯密码。
3. 将每个摩尔斯密码翻译结果存入集合中。
4. 返回集合的大小，即不同摩尔斯密码翻译的数量。

关键点:
- 使用集合来去重，确保每个摩尔斯密码翻译结果是唯一的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是 words 的长度，m 是每个单词的平均长度。
空间复杂度: O(n * m)，用于存储摩尔斯密码翻译结果的集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(words: List[str]) -> int:
    """
    函数式接口 - 实现唯一摩尔斯密码词
    """
    # 摩尔斯密码表
    morse_code = [".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."]
    
    # 存储不同的摩尔斯密码翻译结果
    unique_morse_codes = set()
    
    # 遍历每个单词
    for word in words:
        # 将单词转换为摩尔斯密码
        morse_word = ''.join(morse_code[ord(char) - ord('a')] for char in word)
        # 将摩尔斯密码翻译结果存入集合
        unique_morse_codes.add(morse_word)
    
    # 返回不同摩尔斯密码翻译的数量
    return len(unique_morse_codes)

