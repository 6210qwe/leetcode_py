"""
395. 至少有 K 个重复字符的最长子串 - 给你一个字符串 s 和一个整数 k ，请你找出 s 中的最长子串， 要求该子串中的每一字符出现次数都不少于 k 。返回这一子串的长度。 如果不存在这样的子字符串，则返回 0。 示例 1： 输入：s = "aaabb", k = 3 输出：3 解释：最长子串为 "aaa" ，其中 'a' 重复了 3 次。 示例 2： 输入：s = "ababbc", k = 2 输出：5 解释：最长子串为 "ababb" ，其中 'a' 重复了 2 次， 'b' 重复了 3 次。 提示： * 1 <= s.length <= 104 * s 仅由小写英文字母组成 * 1 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 分治：如果某个字符在整个区间内出现次数小于 k，则这个字符不可能出现在合法子串中，可以用它将区间分割。

算法步骤:
1. 定义递归函数 dfs(l,r) 表示在 s[l..r] 区间内的答案。
2. 若区间长度小于 k，则不可能存在合法子串，返回 0。
3. 统计 s[l..r] 区间内每个字符的出现次数。
4. 从左到右扫描该区间，找到任意一个次数小于 k 的字符 ch，将区间按 ch 分成左右两段，递归求解两段的最大值。
5. 如果区间内所有字符次数都不小于 k，则整个区间都是合法子串，返回 r-l+1。

关键点:
- 通过“非法字符切分”大幅减少搜索空间，避免对所有子串暴力检查。
- 字符集只有 26 个小写字母，计数开销很小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: 最坏 O(26 * n) 级别，n 为字符串长度；每层递归统计一次频率，总体在可接受范围内。
空间复杂度: O(26 + 递归深度)，递归深度最坏 O(n)，但平均远小于此。
"""

