def longest_substring_with_at_least_k_repeating_characters(s: str, k: int) -> int:
    """
    返回每个字符出现次数都不少于 k 的最长子串长度。

    使用分治：对于出现次数 < k 的字符，将字符串按该字符分割，递归求解两边。
    """

    def dfs(l: int, r: int) -> int:
        if r - l + 1 < k:
            return 0
        freq = [0] * 26
        for i in range(l, r + 1):
            freq[ord(s[i]) - 97] += 1
        for i in range(l, r + 1):
            if freq[ord(s[i]) - 97] < k:
                return max(dfs(l, i - 1), dfs(i + 1, r))
        return r - l + 1

    return dfs(0, len(s) - 1)


