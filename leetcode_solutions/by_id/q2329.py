"""
2233. K 次增加后的最大乘积 - 给你一个非负整数数组 nums 和一个整数 k 。每次操作，你可以选择 nums 中 任一 元素并将它 增加 1 。 请你返回 至多 k 次操作后，能得到的 nums的 最大乘积 。由于答案可能很大，请你将答案对 109 + 7 取余后返回。 示例 1： 输入：nums = [0,4], k = 5 输出：20 解释：将第一个数增加 5 次。 得到 nums = [5, 4] ，乘积为 5 * 4 = 20 。 可以证明 20 是能得到的最大乘积，所以我们返回 20 。 存在其他增加 nums 的方法，也能得到最大乘积。 示例 2： 输入：nums = [6,3,3,2], k = 2 输出：216 解释：将第二个数增加 1 次，将第四个数增加 1 次。 得到 nums = [6, 4, 3, 3] ，乘积为 6 * 4 * 3 * 3 = 216 。 可以证明 216 是能得到的最大乘积，所以我们返回 216 。 存在其他增加 nums 的方法，也能得到最大乘积。 提示： * 1 <= nums.length, k <= 105 * 0 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用最小堆来维护当前最小的元素，并在每次操作中将其增加1，直到完成k次操作。

算法步骤:
1. 将所有元素加入最小堆。
2. 进行k次操作，每次从堆中取出最小元素，增加1后再放回堆中。
3. 最后计算所有元素的乘积，并对10^9 + 7取余。

关键点:
- 使用最小堆来高效地找到并更新最小元素。
- 对结果取模以防止溢出。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + k log n)，其中n是数组长度，k是操作次数。建堆的时间复杂度是O(n log n)，每次操作的时间复杂度是O(log n)。
空间复杂度: O(n)，用于存储堆。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 将所有元素加入最小堆
    min_heap = nums[:]
    heapq.heapify(min_heap)
    
    # 进行k次操作
    for _ in range(k):
        # 取出最小元素并增加1
        smallest = heapq.heappop(min_heap)
        heapq.heappush(min_heap, smallest + 1)
    
    # 计算所有元素的乘积并对10^9 + 7取余
    mod = 10**9 + 7
    result = 1
    for num in min_heap:
        result = (result * num) % mod
    
    return result

