"""
814. 二叉树剪枝 - 给你二叉树的根结点 root ，此外树的每个结点的值要么是 0 ，要么是 1 。 返回移除了所有不包含 1 的子树的原二叉树。 节点 node 的子树为 node 本身加上所有 node 的后代。 示例 1： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/06/1028_2.png] 输入：root = [1,null,0,0,1] 输出：[1,null,0,null,1] 解释： 只有红色节点满足条件“所有不包含 1 的子树”。 右图为返回的答案。 示例 2： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/06/1028_1.png] 输入：root = [1,0,1,0,0,0,1] 输出：[1,null,1,null,1] 示例 3： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/05/1028.png] 输入：root = [1,1,0,1,1,0,1,0] 输出：[1,1,0,1,1,null,1] 提示： * 树中节点的数目在范围 [1, 200] 内 * Node.val 为 0 或 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）递归地遍历树，剪掉所有不包含 1 的子树。

算法步骤:
1. 定义一个递归函数 `prune_tree`，该函数接受一个节点作为参数。
2. 如果当前节点为空，直接返回 None。
3. 递归地调用 `prune_tree` 处理当前节点的左子树和右子树。
4. 如果处理后的左子树或右子树为空且节点值为 0，则将该节点设为 None。
5. 返回处理后的当前节点。

关键点:
- 递归地处理每个节点，并根据其子树是否包含 1 来决定是否剪枝。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点都被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def prune_tree(root: Optional[TreeNode]) -> Optional[TreeNode]:
    """
    函数式接口 - 二叉树剪枝
    """
    if not root:
        return None
    
    # 递归地剪枝左子树和右子树
    root.left = prune_tree(root.left)
    root.right = prune_tree(root.right)
    
    # 如果当前节点值为 0 且左右子树都为空，则剪掉该节点
    if root.val == 0 and not root.left and not root.right:
        return None
    
    return root


Solution = create_solution(prune_tree)