"""
核心思想: 使用哈希表记录每个元素减去k后的差值出现的位置，然后通过双指针法找到最长的连续子数组。

算法步骤:
1. 初始化一个哈希表 `diff_indices` 来记录每个差值出现的位置。
2. 遍历数组 `nums`，计算每个元素减去 `k` 后的差值，并将其位置记录在 `diff_indices` 中。
3. 使用双指针法遍历 `diff_indices`，找到最长的连续子数组。
4. 返回最长的连续子数组的长度。

关键点:
- 使用哈希表记录差值出现的位置，可以快速找到连续的子数组。
- 双指针法可以高效地找到最长的连续子数组。
"""

