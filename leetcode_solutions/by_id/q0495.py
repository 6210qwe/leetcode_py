"""
核心思想: 计算每次攻击的中毒时间，如果两次攻击之间的时间间隔小于中毒持续时间，则只计算实际中毒时间；否则，累加每次攻击的完整中毒时间。

算法步骤:
1. 初始化总中毒时间为 0。
2. 遍历 timeSeries 数组，对于每个时间点：
   - 如果是第一次攻击，直接加上完整的中毒时间。
   - 否则，计算当前攻击与上一次攻击的时间间隔，如果间隔小于中毒持续时间，则加上实际中毒时间；否则，加上完整的中毒时间。
3. 返回总中毒时间。

关键点:
- 注意边界条件，确保每次攻击的中毒时间正确累加。
- 优化时间和空间复杂度，只需遍历一次数组。
"""

