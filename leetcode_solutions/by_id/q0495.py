def teemo_attacking(time_series: List[int], duration: int) -> int:
    """
    函数式接口 - 计算艾希处于中毒状态的总秒数
    
    实现思路:
    计算每次攻击的中毒时间，如果两次攻击之间的时间间隔小于中毒持续时间，则只计算实际中毒时间；否则，累加每次攻击的完整中毒时间。

    Args:
        time_series: 提莫攻击的时间序列
        duration: 中毒持续时间
        
    Returns:
        艾希处于中毒状态的总秒数
        
    Example:
        >>> teemo_attacking([1, 4], 2)
        4
        >>> teemo_attacking([1, 2], 2)
        3
    """
    total_duration = 0
    n = len(time_series)
    
    for i in range(n):
        if i == 0:
            total_duration += duration
        else:
            interval = time_series[i] - time_series[i - 1]
            if interval < duration:
                total_duration += interval
            else:
                total_duration += duration
    
    return total_duration


