"""
3726. 移除十进制表示中的所有零 - 给你一个正整数n。 返回一个整数，该整数是将 n 的十进制表示中所有的零都移除后得到的结果。 示例 1： 输入： n = 1020030 输出： 123 解释： 从 1020030 中移除所有的零后，得到 123。 示例 2： 输入： n = 1 输出： 1 解释： 1 的十进制表示中没有零，因此结果为 1。 提示： * 1 <= n <= 1015
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将整数转换为字符串，移除其中的 '0'，再将结果转换回整数。

算法步骤:
1. 将整数 n 转换为字符串。
2. 使用字符串的 replace 方法移除所有的 '0'。
3. 将处理后的字符串转换回整数并返回。

关键点:
- 利用字符串操作来简化移除零的过程。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d)，其中 d 是 n 的十进制表示的位数。
空间复杂度: O(d)，因为需要存储转换后的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 移除十进制表示中的所有零
    """
    # 将整数转换为字符串
    n_str = str(n)
    # 移除字符串中的所有 '0'
    n_str_no_zeros = n_str.replace('0', '')
    # 将处理后的字符串转换回整数
    result = int(n_str_no_zeros)
    return result


Solution = create_solution(solution_function_name)