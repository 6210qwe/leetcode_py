"""
LCR 191. 按规则计算统计结果 - 为了深入了解这些生物群体的生态特征，你们进行了大量的实地观察和数据采集。数组 arrayA 记录了各个生物群体数量数据，其中 arrayA[i] 表示第 i 个生物群体的数量。请返回一个数组 arrayB，该数组为基于数组 arrayA 中的数据计算得出的结果，其中 arrayB[i] 表示将第 i 个生物群体的数量从总体中排除后的其他数量的乘积。 示例 1： 输入：arrayA = [2, 4, 6, 8, 10] 输出：[1920, 960, 640, 480, 384] 提示： * 所有元素乘积之和不会溢出 32 位整数 * arrayA.length <= 100000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个数组分别存储每个位置左侧和右侧所有元素的乘积，然后将这两个数组对应位置的值相乘得到结果。

算法步骤:
1. 初始化两个数组 left 和 right，长度与 arrayA 相同，用于存储每个位置左侧和右侧所有元素的乘积。
2. 从左到右遍历 arrayA，计算每个位置左侧所有元素的乘积并存储在 left 数组中。
3. 从右到左遍历 arrayA，计算每个位置右侧所有元素的乘积并存储在 right 数组中。
4. 将 left 和 right 数组对应位置的值相乘得到结果数组 arrayB。

关键点:
- 使用两个数组分别存储左侧和右侧的乘积，避免重复计算。
- 时间复杂度和空间复杂度都为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arrayA: List[int]) -> List[int]:
    """
    函数式接口 - 计算排除每个位置后的乘积
    """
    n = len(arrayA)
    if n == 0:
        return []

    # 初始化 left 和 right 数组
    left = [1] * n
    right = [1] * n

    # 计算每个位置左侧所有元素的乘积
    for i in range(1, n):
        left[i] = left[i - 1] * arrayA[i - 1]

    # 计算每个位置右侧所有元素的乘积
    for i in range(n - 2, -1, -1):
        right[i] = right[i + 1] * arrayA[i + 1]

    # 计算结果数组
    result = [left[i] * right[i] for i in range(n)]
    return result


Solution = create_solution(solution_function_name)