"""
3069. 将元素分配到两个数组中 I - 给你一个下标从 1 开始、包含 不同 整数的数组 nums ，数组长度为 n 。 你需要通过 n 次操作，将 nums 中的所有元素分配到两个数组 arr1 和 arr2 中。在第一次操作中，将 nums[1] 追加到 arr1 。在第二次操作中，将 nums[2] 追加到 arr2 。之后，在第 i 次操作中： * 如果 arr1 的最后一个元素 大于 arr2 的最后一个元素，就将 nums[i] 追加到 arr1 。否则，将 nums[i] 追加到 arr2 。 通过连接数组 arr1 和 arr2 形成数组 result 。例如，如果 arr1 == [1,2,3] 且 arr2 == [4,5,6] ，那么 result = [1,2,3,4,5,6] 。 返回数组 result 。 示例 1： 输入：nums = [2,1,3] 输出：[2,3,1] 解释：在前两次操作后，arr1 = [2] ，arr2 = [1] 。 在第 3 次操作中，由于 arr1 的最后一个元素大于 arr2 的最后一个元素（2 > 1），将 nums[3] 追加到 arr1 。 3 次操作后，arr1 = [2,3] ，arr2 = [1] 。 因此，连接形成的数组 result 是 [2,3,1] 。 示例 2： 输入：nums = [5,4,3,8] 输出：[5,3,4,8] 解释：在前两次操作后，arr1 = [5] ，arr2 = [4] 。 在第 3 次操作中，由于 arr1 的最后一个元素大于 arr2 的最后一个元素（5 > 4），将 nums[3] 追加到 arr1 ，因此 arr1 变为 [5,3] 。 在第 4 次操作中，由于 arr2 的最后一个元素大于 arr1 的最后一个元素（4 > 3），将 nums[4] 追加到 arr2 ，因此 arr2 变为 [4,8] 。 4 次操作后，arr1 = [5,3] ，arr2 = [4,8] 。 因此，连接形成的数组 result 是 [5,3,4,8] 。 提示： * 3 <= n <= 50 * 1 <= nums[i] <= 100 * nums中的所有元素都互不相同。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组并根据条件将元素分配到两个数组中。

算法步骤:
1. 初始化两个空数组 arr1 和 arr2。
2. 将 nums[0] 追加到 arr1，将 nums[1] 追加到 arr2。
3. 从第三个元素开始遍历 nums 数组：
   - 如果 arr1 的最后一个元素大于 arr2 的最后一个元素，将当前元素追加到 arr1。
   - 否则，将当前元素追加到 arr2。
4. 最后，将 arr1 和 arr2 连接起来形成结果数组 result 并返回。

关键点:
- 通过比较 arr1 和 arr2 的最后一个元素来决定当前元素的分配。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 遍历一次数组。
空间复杂度: O(n) - 使用了两个额外的数组来存储元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def distribute_elements_into_two_arrays(nums: List[int]) -> List[int]:
    """
    函数式接口 - 将元素分配到两个数组中 I
    """
    arr1 = [nums[0]]
    arr2 = [nums[1]]

    for num in nums[2:]:
        if arr1[-1] > arr2[-1]:
            arr1.append(num)
        else:
            arr2.append(num)

    return arr1 + arr2


Solution = create_solution(distribute_elements_into_two_arrays)