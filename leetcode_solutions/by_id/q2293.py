"""
2186. 制造字母异位词的最小步骤数 II - 给你两个字符串 s 和 t 。在一步操作中，你可以给 s 或者 t 追加 任一字符 。 返回使 s 和 t 互为 字母异位词 所需的最少步骤数。 字母异位词 指字母相同但是顺序不同（或者相同）的字符串。 示例 1： 输入：s = "leetcode", t = "coats" 输出：7 解释： - 执行 2 步操作，将 "as" 追加到 s = "leetcode" 中，得到 s = "leetcodeas" 。 - 执行 5 步操作，将 "leede" 追加到 t = "coats" 中，得到 t = "coatsleede" 。 "leetcodeas" 和 "coatsleede" 互为字母异位词。 总共用去 2 + 5 = 7 步。 可以证明，无法用少于 7 步操作使这两个字符串互为字母异位词。 示例 2： 输入：s = "night", t = "thing" 输出：0 解释：给出的字符串已经互为字母异位词。因此，不需要任何进一步操作。 提示： * 1 <= s.length, t.length <= 2 * 105 * s 和 t 由小写英文字符组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个字符在 s 和 t 中的出现次数，然后计算需要追加的字符数量。

算法步骤:
1. 使用两个哈希表分别记录 s 和 t 中每个字符的出现次数。
2. 计算 s 和 t 中字符的差异，累加需要追加的字符数量。

关键点:
- 使用哈希表高效统计字符频率。
- 通过字符频率差异计算所需追加的字符数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 和 m 分别是 s 和 t 的长度。
空间复杂度: O(1)，因为字符集大小固定为 26 个字母。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_steps_to_make_anagrams(s: str, t: str) -> int:
    """
    函数式接口 - 计算使 s 和 t 互为字母异位词所需的最少步骤数
    """
    # 记录 s 和 t 中每个字符的出现次数
    count_s = [0] * 26
    count_t = [0] * 26
    
    for char in s:
        count_s[ord(char) - ord('a')] += 1
    for char in t:
        count_t[ord(char) - ord('a')] += 1
    
    # 计算需要追加的字符数量
    steps = 0
    for i in range(26):
        steps += abs(count_s[i] - count_t[i])
    
    return steps


Solution = create_solution(min_steps_to_make_anagrams)