"""
3345. 最小可整除数位乘积 I - 给你两个整数 n 和 t 。请你返回大于等于 n 的 最小 整数，且该整数的 各数位之积 能被 t 整除。 示例 1： 输入：n = 10, t = 2 输出：10 解释： 10 的数位乘积为 0 ，可以被 2 整除，所以它是大于等于 10 且满足题目要求的最小整数。 示例 2： 输入：n = 15, t = 3 输出：16 解释： 16 的数位乘积为 6 ，可以被 3 整除，所以它是大于等于 15 且满足题目要求的最小整数。 提示： * 1 <= n <= 100 * 1 <= t <= 10
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从 n 开始逐个检查每个整数，直到找到一个其数位乘积能被 t 整除的整数。

算法步骤:
1. 定义一个辅助函数 `digit_product` 来计算一个整数的数位乘积。
2. 从 n 开始逐个检查每个整数，使用 `digit_product` 函数计算其数位乘积。
3. 如果数位乘积能被 t 整除，则返回该整数。
4. 否则，继续检查下一个整数。

关键点:
- 使用一个简单的循环来逐个检查整数。
- 使用一个辅助函数来计算数位乘积，提高代码的可读性和复用性。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 在最坏情况下，可能需要检查从 n 到 100 的所有整数。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def digit_product(num: int) -> int:
    """计算一个整数的数位乘积"""
    product = 1
    while num > 0:
        product *= num % 10
        num //= 10
    return product


def solution_function_name(n: int, t: int) -> int:
    """
    函数式接口 - 返回大于等于 n 的最小整数，且该整数的各数位之积能被 t 整除。
    """
    # 从 n 开始逐个检查每个整数
    current = n
    while True:
        if digit_product(current) % t == 0:
            return current
        current += 1


Solution = create_solution(solution_function_name)