"""
3133. 数组最后一个元素的最小值 - 给你两个整数 n 和 x 。你需要构造一个长度为 n 的 正整数 数组 nums ，对于所有 0 <= i < n - 1 ，满足 nums[i + 1] 大于 nums[i] ，并且数组 nums 中所有元素的按位 AND 运算结果为 x 。 返回 nums[n - 1] 可能的 最小 值。 示例 1： 输入：n = 3, x = 4 输出：6 解释： 数组 nums 可以是 [4,5,6] ，最后一个元素为 6 。 示例 2： 输入：n = 2, x = 7 输出：15 解释： 数组 nums 可以是 [7,15] ，最后一个元素为 15 。 提示： * 1 <= n, x <= 108
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 为了使数组中的所有元素按位 AND 运算结果为 x，我们需要确保每个元素都包含 x 的所有位。我们可以从 x 开始，逐步增加每个元素，同时确保它们的按位 AND 结果为 x。

算法步骤:
1. 初始化第一个元素为 x。
2. 从第二个元素开始，逐步增加每个元素，确保每个元素都大于前一个元素，并且按位 AND 结果为 x。
3. 计算最后一个元素的值。

关键点:
- 确保每个元素都包含 x 的所有位。
- 逐步增加每个元素，确保它们的按位 AND 结果为 x。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, x: int) -> int:
    """
    函数式接口 - 实现
    """
    # 初始化第一个元素为 x
    current = x
    
    # 逐步增加每个元素
    for i in range(1, n):
        # 找到下一个大于当前元素且按位 AND 结果为 x 的元素
        current += 1
        while current & x != x:
            current += 1
    
    return current


Solution = create_solution(solution_function_name)