"""
核心思想: 通过贪心算法构造最长的新字符串。首先尽量使用 "AB" 字符串，然后交替使用 "AA" 和 "BB" 字符串以避免出现 "AAA" 或 "BBB"。

算法步骤:
1. 计算 "AB" 字符串的数量。
2. 计算 "AA" 和 "BB" 字符串的数量，取较小值并交替使用。
3. 如果 "AA" 和 "BB" 数量不相等，额外使用一个剩余的 "AA" 或 "BB" 字符串。

关键点:
- 优先使用 "AB" 字符串，因为它不会导致 "AAA" 或 "BBB"。
- 交替使用 "AA" 和 "BB" 字符串以避免连续相同的字符。
- 最后处理剩余的 "AA" 或 "BB" 字符串。
"""

