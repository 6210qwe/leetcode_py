def solution_function_name(x: int, y: int, z: int) -> int:
    """
    函数式接口 - 返回新字符串的最大可能长度
    """
    # 使用所有的 "AB" 字符串
    max_length = z * 2
    
    # 交替使用 "AA" 和 "BB" 字符串
    min_pairs = min(x, y)
    max_length += min_pairs * 4
    
    # 如果 "AA" 和 "BB" 数量不相等，额外使用一个剩余的 "AA" 或 "BB" 字符串
    if x != y:
        max_length += 2
    
    return max_length

