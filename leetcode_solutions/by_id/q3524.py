from typing import List

def min_flips_to_palindromic_grid(grid: List[List[int]]) -> int:
    """
    函数式接口 - 返回最少翻转次数，使得矩阵中所有行和列都是回文的，且矩阵中 1 的数目可以被 4 整除。
    """
    m, n = len(grid), len(grid[0])
    flips = 0
    ones_count = 0

    # 逐行检查
    for row in grid:
        for i in range(n // 2):
            if row[i] != row[n - 1 - i]:
                flips += 1
        ones_count += sum(row)

    # 逐列检查
    for j in range(n):
        for i in range(m // 2):
            if grid[i][j] != grid[m - 1 - i][j]:
                flips += 1

    # 调整 1 的数量
    ones_count %= 4
    if ones_count == 1 or ones_count == 3:
        flips += 1
    elif ones_count == 2:
        flips += 2

    return flips

