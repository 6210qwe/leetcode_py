"""
核心思想: 通过逐行和逐列检查，计算最少翻转次数，使得每行和每列都成为回文，并且确保矩阵中 1 的数量可以被 4 整除。

算法步骤:
1. 初始化翻转次数为 0。
2. 逐行检查，计算每一行变成回文所需的最小翻转次数。
3. 逐列检查，计算每一列变成回文所需的最小翻转次数。
4. 计算矩阵中 1 的总数，并调整翻转次数，使得 1 的数量可以被 4 整除。

关键点:
- 逐行和逐列检查时，只需要比较前半部分和后半部分的对应位置。
- 调整 1 的数量时，可以通过翻转某些位置来实现。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)
空间复杂度: O(1)
"""

