"""
1859. 将句子排序 - 一个 句子 指的是一个序列的单词用单个空格连接起来，且开头和结尾没有任何空格。每个单词都只包含小写或大写英文字母。 我们可以给一个句子添加 从 1 开始的单词位置索引 ，并且将句子中所有单词 打乱顺序 。 * 比方说，句子 "This is a sentence" 可以被打乱顺序得到 "sentence4 a3 is2 This1" 或者 "is2 sentence4 This1 a3" 。 给你一个 打乱顺序 的句子 s ，它包含的单词不超过 9 个，请你重新构造并得到原本顺序的句子。 示例 1： 输入：s = "is2 sentence4 This1 a3" 输出："This is a sentence" 解释：将 s 中的单词按照初始位置排序，得到 "This1 is2 a3 sentence4" ，然后删除数字。 示例 2： 输入：s = "Myself2 Me1 I4 and3" 输出："Me Myself and I" 解释：将 s 中的单词按照初始位置排序，得到 "Me1 Myself2 and3 I4" ，然后删除数字。 提示： * 2 <= s.length <= 200 * s 只包含小写和大写英文字母、空格以及从 1 到 9 的数字。 * s 中单词数目为 1 到 9 个。 * s 中的单词由单个空格分隔。 * s 不包含任何前导或者后缀空格。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用字典来存储每个单词及其位置索引，然后按索引排序并重建句子。

算法步骤:
1. 将句子拆分成单词列表。
2. 使用字典存储每个单词及其位置索引。
3. 按位置索引对单词进行排序。
4. 重建句子并删除位置索引。

关键点:
- 使用字典存储单词及其位置索引。
- 按位置索引排序。
- 重建句子时删除位置索引。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是单词的数量（最多 9 个），排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(n)，需要额外的空间来存储字典和排序后的单词列表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sort_sentence(s: str) -> str:
    """
    函数式接口 - 将打乱顺序的句子重新排序
    """
    # 将句子拆分成单词列表
    words = s.split()
    
    # 使用字典存储每个单词及其位置索引
    word_dict = {}
    for word in words:
        index = int(word[-1])  # 获取单词的位置索引
        word_dict[index] = word[:-1]  # 存储单词（去掉位置索引）
    
    # 按位置索引对单词进行排序
    sorted_words = [word_dict[i] for i in range(1, len(words) + 1)]
    
    # 重建句子
    return ' '.join(sorted_words)


Solution = create_solution(sort_sentence)