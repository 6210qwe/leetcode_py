"""
1047. 删除字符串中的所有相邻重复项 - 给出由小写字母组成的字符串 s，重复项删除操作会选择两个相邻且相同的字母，并删除它们。 在 s 上反复执行重复项删除操作，直到无法继续删除。 在完成所有重复项删除操作后返回最终的字符串。答案保证唯一。 示例： 输入："abbaca" 输出："ca" 解释： 例如，在 "abbaca" 中，我们可以删除 "bb" 由于两字母相邻且相同，这是此时唯一可以执行删除操作的重复项。之后我们得到字符串 "aaca"，其中又只有 "aa" 可以执行重复项删除操作，所以最后的字符串为 "ca"。 提示： 1. 1 <= s.length <= 105 2. s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来处理相邻重复项

算法步骤:
1. 初始化一个空栈。
2. 遍历字符串 s 中的每个字符：
   - 如果栈不为空且当前字符与栈顶字符相同，则弹出栈顶字符。
   - 否则，将当前字符压入栈中。
3. 最终栈中的字符即为删除所有相邻重复项后的结果。

关键点:
- 使用栈来存储非重复字符，确保相邻重复项被移除。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。每个字符最多只会被压入和弹出栈一次。
空间复杂度: O(n)，在最坏情况下，栈中可能会存储所有的字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_duplicates(s: str) -> str:
    """
    函数式接口 - 删除字符串中的所有相邻重复项
    """
    stack = []
    for char in s:
        if stack and stack[-1] == char:
            stack.pop()
        else:
            stack.append(char)
    return ''.join(stack)


Solution = create_solution(remove_duplicates)