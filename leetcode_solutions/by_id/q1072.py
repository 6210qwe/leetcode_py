"""
1019. 链表中的下一个更大节点 - 给定一个长度为 n 的链表 head 对于列表中的每个节点，查找下一个 更大节点 的值。也就是说，对于每个节点，找到它旁边的第一个节点的值，这个节点的值 严格大于 它的值。 返回一个整数数组 answer ，其中 answer[i] 是第 i 个节点( 从1开始 )的下一个更大的节点的值。如果第 i 个节点没有下一个更大的节点，设置 answer[i] = 0 。 示例 1： [https://assets.leetcode.com/uploads/2021/08/05/linkedlistnext1.jpg] 输入：head = [2,1,5] 输出：[5,5,0] 示例 2： [https://assets.leetcode.com/uploads/2021/08/05/linkedlistnext2.jpg] 输入：head = [2,7,4,3,5] 输出：[7,0,5,5,0] 提示： * 链表中节点数为 n * 1 <= n <= 104 * 1 <= Node.val <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调栈来解决这个问题。我们可以在遍历链表的同时维护一个单调递减栈，栈中存储的是节点的值和索引。当遇到一个比栈顶元素大的节点时，更新栈顶元素的答案，并将栈顶元素出栈。

算法步骤:
1. 初始化一个空的结果数组 `result` 和一个单调递减栈 `stack`。
2. 遍历链表，对于每个节点：
   - 如果栈不为空且当前节点的值大于栈顶元素的值，则更新栈顶元素的答案，并将栈顶元素出栈。
   - 将当前节点的值和索引压入栈中。
3. 遍历结束后，栈中剩余的元素都是没有找到下一个更大节点的节点，将它们的答案设为 0。

关键点:
- 使用单调栈来维护节点的值和索引。
- 在遍历链表时，通过比较当前节点的值和栈顶元素的值来更新答案。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是链表的长度。每个节点最多只会被压入和弹出栈一次。
空间复杂度: O(n)，最坏情况下，栈中会存储所有的节点。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def next_larger_nodes(head: Optional[ListNode]) -> List[int]:
    """
    函数式接口 - 返回链表中每个节点的下一个更大节点的值
    """
    result = []
    stack = []
    index = 0
    
    while head:
        # 更新栈中所有小于当前节点值的节点的答案
        while stack and stack[-1][1] < head.val:
            _, idx = stack.pop()
            result[idx] = head.val
        
        # 将当前节点的值和索引压入栈中
        stack.append((head.val, index))
        result.append(0)  # 初始化当前节点的答案为 0
        head = head.next
        index += 1
    
    return result


Solution = create_solution(next_larger_nodes)