"""
330. 按要求补齐数组 - 给定一个已排序的正整数数组 nums ，和一个正整数 n 。从 [1, n] 区间内选取任意个数字补充到 nums 中，使得 [1, n] 区间内的任何数字都可以用 nums 中某几个数字的和来表示。 请返回 满足上述要求的最少需要补充的数字个数 。 示例 1: 输入: nums = [1,3], n = 6 输出: 1 解释: 根据 nums 里现有的组合 [1], [3], [1,3]，可以得出 1, 3, 4。 现在如果我们将 2 添加到 nums 中， 组合变为: [1], [2], [3], [1,3], [2,3], [1,2,3]。 其和可以表示数字 1, 2, 3, 4, 5, 6，能够覆盖 [1, 6] 区间里所有的数。 所以我们最少需要添加一个数字。 示例 2: 输入: nums = [1,5,10], n = 20 输出: 2 解释: 我们需要添加 [2,4]。 示例 3: 输入: nums = [1,2,2], n = 5 输出: 0 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= 104 * nums 按 升序排列 * 1 <= n <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 贪心，维护当前能表示的最大值

算法步骤:
1. 维护当前能表示的最大值miss
2. 如果nums[i] <= miss，更新miss = miss + nums[i]
3. 否则添加miss，更新miss = miss * 2

关键点:
- 贪心策略
- 时间复杂度O(m + log n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m + log n) - m为数组长度
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_patches(nums: List[int], n: int) -> int:
    """
    函数式接口 - 按要求补齐数组
    
    实现思路:
    贪心：维护当前能表示的最大值。
    
    Args:
        nums: 已排序的正整数数组
        n: 目标范围
        
    Returns:
        最少需要补充的数字个数
        
    Example:
        >>> min_patches([1,3], 6)
        1
    """
    patches = 0
    miss = 1
    i = 0
    
    while miss <= n:
        if i < len(nums) and nums[i] <= miss:
            miss += nums[i]
            i += 1
        else:
            miss += miss
            patches += 1
    
    return patches


