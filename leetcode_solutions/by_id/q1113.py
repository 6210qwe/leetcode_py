# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过滑动窗口找到最小和最大移动次数。

算法步骤:
1. 对石子位置进行排序。
2. 使用滑动窗口找到最小移动次数。
3. 计算最大移动次数。

关键点:
- 最小移动次数可以通过滑动窗口找到。
- 最大移动次数可以通过计算最远距离得到。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 stones 的长度，主要由排序操作决定。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
