def is_unique(s: str) -> bool:
    """
    函数式接口 - 判断字符串 s 中的所有字符是否全都不同
    """
    checker = 0
    for char in s:
        val = ord(char) - ord('a')
        if (checker & (1 << val)) > 0:
            return False
        checker |= (1 << val)
    return True


Solution = create_solution(is_unique)