# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用递归的方法计算二叉树的最大深度。

算法步骤:
1. 如果当前节点为空，返回深度为0。
2. 递归计算左子树和右子树的最大深度。
3. 返回左右子树最大深度加1（当前节点）。

关键点:
- 递归调用时，每次递归都计算当前节点的左右子树的最大深度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数，因为每个节点都被访问一次。
空间复杂度: O(h)，其中 h 是二叉树的高度，这是由于递归调用栈的深度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def calculate_tree_depth(root: Optional[TreeNode]) -> int:
    """
    函数式接口 - 计算二叉树的最大深度
    """
    if root is None:
        return 0
    left_depth = calculate_tree_depth(root.left)
    right_depth = calculate_tree_depth(root.right)
    return max(left_depth, right_depth) + 1

Solution = create_solution(calculate_tree_depth)