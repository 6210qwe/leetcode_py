"""
129. 求根节点到叶节点数字之和 - 给你一个二叉树的根节点 root ，树中每个节点都存放有一个 0 到 9 之间的数字。 每条从根节点到叶节点的路径都代表一个数字： * 例如，从根节点到叶节点的路径 1 -> 2 -> 3 表示数字 123 。 计算从根节点到叶节点生成的 所有数字之和 。 叶节点 是指没有子节点的节点。 示例 1： [https://assets.leetcode.com/uploads/2021/02/19/num1tree.jpg] 输入：root = [1,2,3] 输出：25 解释： 从根到叶子节点路径 1->2 代表数字 12 从根到叶子节点路径 1->3 代表数字 13 因此，数字总和 = 12 + 13 = 25 示例 2： [https://assets.leetcode.com/uploads/2021/02/19/num2tree.jpg] 输入：root = [4,9,0,5,1] 输出：1026 解释： 从根到叶子节点路径 4->9->5 代表数字 495 从根到叶子节点路径 4->9->1 代表数字 491 从根到叶子节点路径 4->0 代表数字 40 因此，数字总和 = 495 + 491 + 40 = 1026 提示： * 树中节点的数目在范围 [1, 1000] 内 * 0 <= Node.val <= 9 * 树的深度不超过 10
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 递归DFS，从根节点到叶节点，累加路径数字

算法步骤:
1. 递归遍历树，维护当前路径数字
2. 到达叶节点时，将路径数字加入总和
3. 路径数字 = 父节点数字 * 10 + 当前节点值

关键点:
- 递归DFS，累加路径数字
- 时间复杂度O(n)，空间复杂度O(h)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历所有节点
空间复杂度: O(h) - h为树的高度
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_root_to_leaf_numbers(root: Optional[TreeNode]) -> int:
    """
    函数式接口 - 求根节点到叶节点数字之和
    
    实现思路:
    递归DFS，从根节点到叶节点，累加路径数字。
    
    Args:
        root: 二叉树根节点
        
    Returns:
        所有从根节点到叶节点生成的数字之和
        
    Example:
        >>> root = TreeNode.from_list([1,2,3])
        >>> sum_root_to_leaf_numbers(root)
        25
    """
    def dfs(node: Optional[TreeNode], current_sum: int) -> int:
        """递归函数"""
        if not node:
            return 0
        
        current_sum = current_sum * 10 + node.val
        
        # 叶节点
        if not node.left and not node.right:
            return current_sum
        
        # 递归左右子树
        return dfs(node.left, current_sum) + dfs(node.right, current_sum)
    
    return dfs(root, 0)


