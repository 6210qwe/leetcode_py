"""
1647. 字符频次唯一的最小删除次数 - 如果字符串 s 中 不存在 两个不同字符 频次 相同的情况，就称 s 是 优质字符串 。 给你一个字符串 s，返回使 s 成为 优质字符串 需要删除的 最小 字符数。 字符串中字符的 频次 是该字符在字符串中的出现次数。例如，在字符串 "aab" 中，'a' 的频次是 2，而 'b' 的频次是 1 。 示例 1： 输入：s = "aab" 输出：0 解释：s 已经是优质字符串。 示例 2： 输入：s = "aaabbbcc" 输出：2 解释：可以删除两个 'b' , 得到优质字符串 "aaabcc" 。 另一种方式是删除一个 'b' 和一个 'c' ，得到优质字符串 "aaabbc" 。 示例 3： 输入：s = "ceabaacb" 输出：2 解释：可以删除两个 'c' 得到优质字符串 "eabaab" 。 注意，只需要关注结果字符串中仍然存在的字符。（即，频次为 0 的字符会忽略不计。） 提示： * 1 <= s.length <= 105 * s 仅含小写英文字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和哈希表来记录每个字符的频次，并通过排序和调整频次来确保每个字符的频次唯一。

算法步骤:
1. 计算每个字符的频次并存储在哈希表中。
2. 将频次按降序排序。
3. 遍历排序后的频次列表，如果当前频次大于或等于前一个频次，则将当前频次减一，并增加删除计数。
4. 重复步骤3直到所有频次都唯一。

关键点:
- 使用哈希表记录字符频次。
- 通过排序和调整频次来确保每个字符的频次唯一。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + k log k)，其中 n 是字符串长度，k 是字符种类数（最多26个字母）。
空间复杂度: O(k)，用于存储字符频次。
"""

