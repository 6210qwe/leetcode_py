"""
1646. 获取生成数组中的最大值 - 给你一个整数 n 。按下述规则生成一个长度为 n + 1 的数组 nums ： * nums[0] = 0 * nums[1] = 1 * 当 2 <= 2 * i <= n 时，nums[2 * i] = nums[i] * 当 2 <= 2 * i + 1 <= n 时，nums[2 * i + 1] = nums[i] + nums[i + 1] 返回生成数组 nums 中的 最大 值。 示例 1： 输入：n = 7 输出：3 解释：根据规则： nums[0] = 0 nums[1] = 1 nums[(1 * 2) = 2] = nums[1] = 1 nums[(1 * 2) + 1 = 3] = nums[1] + nums[2] = 1 + 1 = 2 nums[(2 * 2) = 4] = nums[2] = 1 nums[(2 * 2) + 1 = 5] = nums[2] + nums[3] = 1 + 2 = 3 nums[(3 * 2) = 6] = nums[3] = 2 nums[(3 * 2) + 1 = 7] = nums[3] + nums[4] = 2 + 1 = 3 因此，nums = [0,1,1,2,1,3,2,3]，最大值 3 示例 2： 输入：n = 2 输出：1 解释：根据规则，nums[0]、nums[1] 和 nums[2] 之中的最大值是 1 示例 3： 输入：n = 3 输出：2 解释：根据规则，nums[0]、nums[1]、nums[2] 和 nums[3] 之中的最大值是 2 提示： * 0 <= n <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过动态规划生成数组，并在生成过程中记录最大值。

算法步骤:
1. 初始化一个长度为 n + 1 的数组 nums，其中 nums[0] = 0, nums[1] = 1。
2. 从 2 到 n 遍历，根据规则填充 nums 数组。
3. 在填充过程中，维护一个变量 max_value 来记录当前的最大值。
4. 返回 max_value。

关键点:
- 动态规划生成数组并记录最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
