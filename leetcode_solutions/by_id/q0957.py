"""
921. 使括号有效的最少添加 - 只有满足下面几点之一，括号字符串才是有效的： * 它是一个空字符串，或者 * 它可以被写成 AB （A 与 B 连接）, 其中 A 和 B 都是有效字符串，或者 * 它可以被写作 (A)，其中 A 是有效字符串。 给定一个括号字符串 s ，在每一次操作中，你都可以在字符串的任何位置插入一个括号 * 例如，如果 s = "()))" ，你可以插入一个开始括号为 "(()))" 或结束括号为 "())))" 。 返回 为使结果字符串 s 有效而必须添加的最少括号数。 示例 1： 输入：s = "())" 输出：1 示例 2： 输入：s = "(((" 输出：3 提示： * 1 <= s.length <= 1000 * s 只包含 '(' 和 ')' 字符。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来匹配括号，并计算需要添加的括号数量。

算法步骤:
1. 初始化一个栈和一个计数器。
2. 遍历字符串 s：
   - 如果遇到 '(', 将其压入栈中。
   - 如果遇到 ')':
     * 如果栈不为空，弹出栈顶元素（表示匹配成功）。
     * 如果栈为空，增加计数器（表示需要添加一个 '(' 来匹配这个 ')'）。
3. 最后，栈中剩余的 '(' 数量加上计数器的值即为需要添加的最少括号数。

关键点:
- 使用栈来匹配括号。
- 计数器记录未匹配的 ')'。
- 栈中剩余的 '(' 数量也需要添加相应的 ')'。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
