"""
3467. 将数组按照奇偶性转化 - 给你一个整数数组 nums。请你按照以下顺序 依次 执行操作，转换 nums： 1. 将每个偶数替换为 0。 2. 将每个奇数替换为 1。 3. 按 非递减 顺序排序修改后的数组。 执行完这些操作后，返回结果数组。 示例 1: 输入：nums = [4,3,2,1] 输出：[0,0,1,1] 解释： * 将偶数（4 和 2）替换为 0，将奇数（3 和 1）替换为 1。现在，nums = [0, 1, 0, 1]。 * 按非递减顺序排序 nums，得到 nums = [0, 0, 1, 1]。 示例 2: 输入：nums = [1,5,1,4,2] 输出：[0,0,1,1,1] 解释： * 将偶数（4 和 2）替换为 0，将奇数（1, 5 和 1）替换为 1。现在，nums = [1, 1, 1, 0, 0]。 * 按非递减顺序排序 nums，得到 nums = [0, 0, 1, 1, 1]。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组将偶数替换为 0，奇数替换为 1，然后对数组进行排序。

算法步骤:
1. 遍历数组，将偶数替换为 0，奇数替换为 1。
2. 对数组进行排序。

关键点:
- 使用列表推导式简化代码。
- 利用 Python 的内置排序函数 `sorted` 进行排序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，除了输入和输出外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def transform_array_by_parity(nums: List[int]) -> List[int]:
    """
    函数式接口 - 将数组按照奇偶性转化
    """
    # 将偶数替换为 0，奇数替换为 1
    transformed_nums = [0 if num % 2 == 0 else 1 for num in nums]
    # 对数组进行排序
    sorted_nums = sorted(transformed_nums)
    return sorted_nums


Solution = create_solution(transform_array_by_parity)