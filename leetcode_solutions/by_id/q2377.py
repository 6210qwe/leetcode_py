"""
2283. 判断一个数的数字计数是否等于数位的值 - 给你一个下标从 0 开始长度为 n 的字符串 num ，它只包含数字。 如果对于 每个 0 <= i < n 的下标 i ，都满足数位 i 在 num 中出现了 num[i]次，那么请你返回 true ，否则返回 false 。 示例 1： 输入：num = "1210" 输出：true 解释： num[0] = '1' 。数字 0 在 num 中出现了一次。 num[1] = '2' 。数字 1 在 num 中出现了两次。 num[2] = '1' 。数字 2 在 num 中出现了一次。 num[3] = '0' 。数字 3 在 num 中出现了零次。 "1210" 满足题目要求条件，所以返回 true 。 示例 2： 输入：num = "030" 输出：false 解释： num[0] = '0' 。数字 0 应该出现 0 次，但是在 num 中出现了两次。 num[1] = '3' 。数字 1 应该出现 3 次，但是在 num 中出现了零次。 num[2] = '0' 。数字 2 在 num 中出现了 0 次。 下标 0 和 1 都违反了题目要求，所以返回 false 。 提示： * n == num.length * 1 <= n <= 10 * num 只包含数字。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个数字的出现次数，然后检查每个位置的数字是否符合要求。

算法步骤:
1. 初始化一个长度为 10 的数组 `count`，用于记录每个数字在字符串 `num` 中的出现次数。
2. 遍历字符串 `num`，更新 `count` 数组。
3. 再次遍历字符串 `num`，检查每个位置的数字是否等于 `count` 数组中对应位置的值。
4. 如果所有位置都满足条件，则返回 `True`，否则返回 `False`。

关键点:
- 使用固定大小的数组 `count` 来记录每个数字的出现次数，避免使用额外的空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 `num` 的长度。我们需要遍历字符串两次。
空间复杂度: O(1)，我们只使用了一个固定大小的数组 `count`。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: str) -> bool:
    """
    函数式接口 - 判断一个数的数字计数是否等于数位的值
    """
    # 初始化一个长度为 10 的数组来记录每个数字的出现次数
    count = [0] * 10
    
    # 遍历字符串 num，更新 count 数组
    for digit in num:
        count[int(digit)] += 1
    
    # 再次遍历字符串 num，检查每个位置的数字是否等于 count 数组中对应位置的值
    for i, digit in enumerate(num):
        if int(digit) != count[i]:
            return False
    
    return True


Solution = create_solution(solution_function_name)