def solution_function_name(date: str) -> str:
    """
    函数式接口 - 将给定的日期字符串从 "yyyy-mm-dd" 格式转换为 "dd-mm-yyyy" 格式。
    """
    # 分割日期字符串
    parts = date.split('-')
    
    # 重新组合成 "dd-mm-yyyy" 格式
    new_date = f"{parts[2]}-{parts[1]}-{parts[0]}"
    
    return new_date


