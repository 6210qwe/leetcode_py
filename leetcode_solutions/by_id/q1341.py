"""
1221. 分割平衡字符串 - 平衡字符串 中，'L' 和 'R' 字符的数量是相同的。 给你一个平衡字符串 s，请你将它分割成尽可能多的子字符串，并满足： * 每个子字符串都是平衡字符串。 返回可以通过分割得到的平衡字符串的 最大数量 。 示例 1： 输入：s = "RLRRLLRLRL" 输出：4 解释：s 可以分割为 "RL"、"RRLL"、"RL"、"RL" ，每个子字符串中都包含相同数量的 'L' 和 'R' 。 示例 2： 输入：s = "RLRRRLLRLL" 输出：2 解释：s 可以分割为 "RL"、"RRRLLRLL"，每个子字符串中都包含相同数量的 'L' 和 'R' 。 注意，s 无法分割为 "RL"、"RR"、"RL"、"LR"、"LL" 因为第 2 个和第 5 个子字符串不是平衡字符串。 示例 3： 输入：s = "LLLLRRRR" 输出：1 解释：s 只能保持原样 "LLLLRRRR" 。 提示： * 2 <= s.length <= 1000 * s[i] = 'L' 或 'R' * s 是一个 平衡 字符串
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，遍历字符串并维护一个计数器，当计数器归零时，表示找到了一个平衡字符串。

算法步骤:
1. 初始化计数器 `balance` 为 0。
2. 遍历字符串 `s`：
   - 如果当前字符是 'L'，则 `balance` 减 1。
   - 如果当前字符是 'R'，则 `balance` 加 1。
   - 当 `balance` 为 0 时，表示找到了一个平衡字符串，计数器 `count` 加 1。
3. 返回 `count` 作为结果。

关键点:
- 使用单个变量 `balance` 来跟踪 'L' 和 'R' 的平衡状态。
- 每次 `balance` 归零时，增加平衡字符串的计数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 返回通过分割得到的平衡字符串的最大数量
    """
    balance = 0
    count = 0
    
    for char in s:
        if char == 'L':
            balance -= 1
        else:
            balance += 1
        
        if balance == 0:
            count += 1
    
    return count


Solution = create_solution(solution_function_name)