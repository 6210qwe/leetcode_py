from typing import List

def jump_game_ix(nums: List[int]) -> List[int]:
    n = len(nums)
    ans = [max(nums)] * n
    stack = []

    # 从左到右处理
    for i in range(n):
        while stack and nums[stack[-1]] < nums[i]:
            ans[stack.pop()] = max(ans[stack[-1]], nums[i])
        stack.append(i)

    stack.clear()

    # 从右到左处理
    for i in range(n - 1, -1, -1):
        while stack and nums[stack[-1]] > nums[i]:
            ans[stack.pop()] = max(ans[stack[-1]], nums[i])
        stack.append(i)

    return ans

