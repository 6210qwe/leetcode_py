"""
核心思想: 使用单调栈来维护可以跳跃到的最大值。

算法步骤:
1. 初始化结果数组 `ans`，长度与 `nums` 相同，初始值为 `nums` 的最大值。
2. 使用两个单调栈分别处理从左到右和从右到左的跳跃。
3. 从左到右遍历 `nums`，使用单调递减栈来找到可以跳跃到的最大值。
4. 从右到左遍历 `nums`，使用单调递增栈来找到可以跳跃到的最大值。
5. 更新结果数组 `ans`。

关键点:
- 使用单调栈来高效地找到可以跳跃到的最大值。
- 从左到右和从右到左分别处理两种跳跃规则。
"""

