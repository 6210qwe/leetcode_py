"""
2696. 删除子串后的字符串最小长度 - 给你一个仅由 大写 英文字符组成的字符串 s 。 你可以对此字符串执行一些操作，在每一步操作中，你可以从 s 中删除 任一个 "AB" 或 "CD" 子字符串。 通过执行操作，删除所有 "AB" 和 "CD" 子串，返回可获得的最终字符串的 最小 可能长度。 注意，删除子串后，重新连接出的字符串可能会产生新的 "AB" 或 "CD" 子串。 示例 1： 输入：s = "ABFCACDB" 输出：2 解释：你可以执行下述操作： - 从 "ABFCACDB" 中删除子串 "AB"，得到 s = "FCACDB" 。 - 从 "FCACDB" 中删除子串 "CD"，得到 s = "FCAB" 。 - 从 "FCAB" 中删除子串 "AB"，得到 s = "FC" 。 最终字符串的长度为 2 。 可以证明 2 是可获得的最小长度。 示例 2： 输入：s = "ACBBD" 输出：5 解释：无法执行操作，字符串长度不变。 提示： * 1 <= s.length <= 100 * s 仅由大写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来处理字符串中的 "AB" 和 "CD" 子串。每次遇到 "A" 或 "C" 时将其压入栈中，遇到 "B" 或 "D" 时检查栈顶元素是否为 "A" 或 "C"，如果是则弹出栈顶元素，否则将当前字符压入栈中。

算法步骤:
1. 初始化一个空栈。
2. 遍历字符串 s 的每个字符：
   - 如果当前字符是 "B" 且栈顶元素是 "A"，则弹出栈顶元素。
   - 如果当前字符是 "D" 且栈顶元素是 "C"，则弹出栈顶元素。
   - 否则，将当前字符压入栈中。
3. 最后，栈中的字符即为删除所有 "AB" 和 "CD" 子串后的结果，返回栈的长度。

关键点:
- 使用栈来处理字符串中的 "AB" 和 "CD" 子串。
- 每次遇到 "B" 或 "D" 时检查栈顶元素，决定是否弹出栈顶元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。每个字符最多只会被压入和弹出栈一次。
空间复杂度: O(n)，在最坏情况下，栈中可能存储所有字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_length_after_removals(s: str) -> int:
    """
    函数式接口 - 返回删除所有 "AB" 和 "CD" 子串后的字符串最小长度
    """
    stack = []
    for char in s:
        if char == "B" and stack and stack[-1] == "A":
            stack.pop()
        elif char == "D" and stack and stack[-1] == "C":
            stack.pop()
        else:
            stack.append(char)
    return len(stack)


Solution = create_solution(min_length_after_removals)