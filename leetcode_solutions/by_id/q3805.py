"""
3499. 操作后最大活跃区段数 I - 给你一个长度为 n 的二进制字符串 s，其中： * '1' 表示一个 活跃 区段。 * '0' 表示一个 非活跃 区段。 你可以执行 最多一次操作 来最大化 s 中的活跃区段数量。在一次操作中，你可以： * 将一个被 '0' 包围的连续 '1' 区块转换为全 '0'。 * 然后，将一个被 '1' 包围的连续 '0' 区块转换为全 '1'。 返回在执行最优操作后，s 中的 最大 活跃区段数。 注意：处理时需要在 s 的两侧加上 '1' ，即 t = '1' + s + '1'。这些加上的 '1' 不会影响最终的计数。 示例 1： 输入： s = "01" 输出： 1 解释： 因为没有被 '0' 包围的 '1' 区块，因此无法进行有效操作。最大活跃区段数为 1。 示例 2： 输入： s = "0100" 输出： 4 解释： * 字符串 "0100" → 两端加上 '1' 后得到 "101001" 。 * 选择 "0100"，"101001" → "100001" → "111111" 。 * 最终的字符串去掉两端的 '1' 后为 "1111" 。最大活跃区段数为 4。 示例 3： 输入： s = "1000100" 输出： 7 解释： * 字符串 "1000100" → 两端加上 '1' 后得到 "110001001" 。 * 选择 "000100"，"110001001" → "110000001" → "111111111"。 * 最终的字符串去掉两端的 '1' 后为 "1111111"。最大活跃区段数为 7。 示例 4： 输入： s = "01010" 输出： 4 解释： * 字符串 "01010" → 两端加上 '1' 后得到 "1010101"。 * 选择 "010"，"1010101" → "1000101" → "1111101"。 * 最终的字符串去掉两端的 '1' 后为 "11110"。最大活跃区段数为 4。 提示： * 1 <= n == s.length <= 105 * s[i] 仅包含 '0' 或 '1'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历找到所有被 '0' 包围的 '1' 区块和被 '1' 包围的 '0' 区块，并计算在进行一次操作后的最大活跃区段数。

算法步骤:
1. 在字符串 s 的两侧加上 '1'，形成新的字符串 t。
2. 遍历字符串 t，记录每个 '1' 区块和 '0' 区块的起始和结束位置。
3. 对于每个被 '0' 包围的 '1' 区块，计算将其转换为 '0' 并将相邻的 '0' 区块转换为 '1' 后的最大活跃区段数。
4. 返回最大活跃区段数。

关键点:
- 通过一次遍历记录所有区块的位置。
- 计算每个可能的操作后的最大活跃区段数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 在字符串 s 的两侧加上 '1'，形成新的字符串 t
    t = '1' + s + '1'
    n = len(t)
    
    # 初始化变量
    max_active = 0
    active_count = 0
    start = 0
    
    for i in range(n):
        if t[i] == '1':
            if t[start] == '0':
                # 计算将当前 '0' 区块转换为 '1' 后的最大活跃区段数
                max_active = max(max_active, active_count + (i - start))
            else:
                # 当前 '1' 区块的长度
                active_count += (i - start)
            start = i
        elif i == n - 1 and t[start] == '1':
            # 计算最后一个 '1' 区块的长度
            active_count += (i - start + 1)
    
    return max_active + active_count


Solution = create_solution(solution_function_name)