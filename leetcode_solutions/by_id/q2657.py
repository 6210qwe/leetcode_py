"""
2589. 完成所有任务的最少时间 - 你有一台电脑，它可以 同时 运行无数个任务。给你一个二维整数数组 tasks ，其中 tasks[i] = [starti, endi, durationi] 表示第 i 个任务需要在 闭区间 时间段 [starti, endi] 内运行 durationi 个整数时间点（但不需要连续）。 当电脑需要运行任务时，你可以打开电脑，如果空闲时，你可以将电脑关闭。 请你返回完成所有任务的情况下，电脑最少需要运行多少秒。 示例 1： 输入：tasks = [[2,3,1],[4,5,1],[1,5,2]] 输出：2 解释： - 第一个任务在闭区间 [2, 2] 运行。 - 第二个任务在闭区间 [5, 5] 运行。 - 第三个任务在闭区间 [2, 2] 和 [5, 5] 运行。 电脑总共运行 2 个整数时间点。 示例 2： 输入：tasks = [[1,3,2],[2,5,3],[5,6,2]] 输出：4 解释： - 第一个任务在闭区间 [2, 3] 运行 - 第二个任务在闭区间 [2, 3] 和 [5, 5] 运行。 - 第三个任务在闭区间 [5, 6] 运行。 电脑总共运行 4 个整数时间点。 提示： * 1 <= tasks.length <= 2000 * tasks[i].length == 3 * 1 <= starti, endi <= 2000 * 1 <= durationi <= endi - starti + 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和优先队列来最小化电脑运行时间。

算法步骤:
1. 按任务的结束时间对任务进行排序。
2. 使用一个优先队列来记录当前正在运行的任务，并维护一个计数器来记录已运行的时间点。
3. 遍历每个任务，尝试将其安排在已有任务的空闲时间段内。
4. 如果无法安排在空闲时间段内，则延长电脑的运行时间以满足任务需求。

关键点:
- 通过优先队列来高效管理任务的开始和结束时间。
- 通过贪心策略尽可能地利用已有的运行时间点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是任务的数量。排序操作的时间复杂度为 O(n log n)，而后续遍历和优先队列操作的时间复杂度为 O(n log n)。
空间复杂度: O(n)，优先队列的空间复杂度为 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def minimum_time_to_complete_tasks(tasks: List[List[int]]) -> int:
    # 按任务的结束时间排序
    tasks.sort(key=lambda x: x[1])
    
    # 优先队列，存储任务的结束时间和持续时间
    pq = []
    total_time = 0
    
    for start, end, duration in tasks:
        while pq and start > pq[0][0]:
            _, d = heapq.heappop(pq)
            duration -= min(d, start - pq[0][0])
        
        if duration > 0:
            heapq.heappush(pq, (end, duration))
            total_time += duration
    
    return total_time

