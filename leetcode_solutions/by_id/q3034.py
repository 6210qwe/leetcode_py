"""
2848. 与车相交的点 - 给你一个下标从 0 开始的二维整数数组 nums 表示汽车停放在数轴上的坐标。对于任意下标 i，nums[i] = [starti, endi] ，其中 starti 是第 i 辆车的起点，endi 是第 i 辆车的终点。 返回数轴上被车 任意部分 覆盖的整数点的数目。 示例 1： 输入：nums = [[3,6],[1,5],[4,7]] 输出：7 解释：从 1 到 7 的所有点都至少与一辆车相交，因此答案为 7 。 示例 2： 输入：nums = [[1,3],[5,8]] 输出：7 解释：1、2、3、5、6、7、8 共计 7 个点满足至少与一辆车相交，因此答案为 7 。 提示： * 1 <= nums.length <= 100 * nums[i].length == 2 * 1 <= starti <= endi <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用差分数组来计算每个点的覆盖情况

算法步骤:
1. 初始化一个长度为 101 的差分数组 diff，用于记录每个点的覆盖变化。
2. 遍历每辆车的起始点和结束点，更新差分数组。
3. 通过前缀和计算每个点的实际覆盖情况。
4. 统计覆盖点的数量。

关键点:
- 使用差分数组可以高效地处理区间更新问题。
- 通过前缀和计算最终的覆盖情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 nums 的长度，m 是数轴的最大范围（本题中为 100）。
空间复杂度: O(m)，用于存储差分数组和前缀和数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums: List[List[int]]) -> int:
    """
    函数式接口 - 计算与车相交的点的数量
    """
    # 初始化差分数组
    diff = [0] * 101
    
    # 更新差分数组
    for start, end in nums:
        diff[start] += 1
        diff[end + 1] -= 1
    
    # 通过前缀和计算实际覆盖情况
    prefix_sum = 0
    count = 0
    for i in range(101):
        prefix_sum += diff[i]
        if prefix_sum > 0:
            count += 1
    
    return count

