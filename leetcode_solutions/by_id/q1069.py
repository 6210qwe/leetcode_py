def confusingNumber(n: int) -> bool:
    """
    判断一个数是否是易混淆数。
    
    :param n: 输入的整数
    :return: 如果是易混淆数返回 True，否则返回 False
    """
    # 旋转180度后的数字映射
    rotation_map = {0: 0, 1: 1, 6: 9, 8: 8, 9: 6}
    original = n
    rotated = 0
    
    while n > 0:
        digit = n % 10
        if digit not in rotation_map:
            return False
        rotated = rotated * 10 + rotation_map[digit]
        n //= 10
    
    return rotated != original

