"""
1663. 具有给定数值的最小字符串 - 小写字符 的 数值 是它在字母表中的位置（从 1 开始），因此 a 的数值为 1 ，b 的数值为 2 ，c 的数值为 3 ，以此类推。 字符串由若干小写字符组成，字符串的数值 为各字符的数值之和。例如，字符串 "abe" 的数值等于 1 + 2 + 5 = 8 。 给你两个整数 n 和 k 。返回 长度 等于 n 且 数值 等于 k 的 字典序最小 的字符串。 注意，如果字符串 x 在字典排序中位于 y 之前，就认为 x 字典序比 y 小，有以下两种情况： * x 是 y 的一个前缀； * 如果 i 是 x[i] != y[i] 的第一个位置，且 x[i] 在字母表中的位置比 y[i] 靠前。 示例 1： 输入：n = 3, k = 27 输出："aay" 解释：字符串的数值为 1 + 1 + 25 = 27，它是数值满足要求且长度等于 3 字典序最小的字符串。 示例 2： 输入：n = 5, k = 73 输出："aaszz" 提示： * 1 <= n <= 105 * n <= k <= 26 * n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从后向前构建字符串，尽量使用较小的字符。

算法步骤:
1. 初始化一个空的结果字符串 `result`。
2. 从后向前遍历字符串的位置，计算当前剩余的数值 `k`。
3. 在每个位置上，选择尽可能大的字符，但不能超过剩余的数值 `k`。
4. 将选择的字符添加到结果字符串中，并更新剩余的数值 `k`。
5. 最后返回结果字符串。

关键点:
- 从后向前构建字符串，确保每次选择的字符尽可能大，从而保证字典序最小。
- 每次选择字符时，需要确保剩余的数值 `k` 足够分配给前面的字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
