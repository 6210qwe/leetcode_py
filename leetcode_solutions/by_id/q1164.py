"""
核心思想: 利用等差数列的性质，通过求和公式计算出完整的等差数列的和，然后减去实际数组的和，即可得到缺失的数字。

算法步骤:
1. 计算等差数列的公差 d。
2. 计算等差数列的总和 sum_complete。
3. 计算实际数组的总和 sum_actual。
4. 缺失的数字为 sum_complete - sum_actual。

关键点:
- 通过等差数列的求和公式可以快速计算出完整的等差数列的和。
- 通过一次遍历计算实际数组的和。
"""

