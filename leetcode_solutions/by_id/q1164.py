def find_missing_number(arr: List[int]) -> int:
    """
    函数式接口 - 找到等差数列中缺失的数字
    """
    n = len(arr)
    total_diff = (arr[-1] - arr[0]) // n
    expected_sum = (n + 1) * (arr[0] + arr[-1]) // 2
    actual_sum = sum(arr)
    return expected_sum - actual_sum


