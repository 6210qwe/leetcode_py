def sum_to_n(n: int) -> int:
    """
    计算从 1 到 n 的累加和
    """
    return n and n + sum_to_n(n - 1) or 0


def solution_function_name(target: int) -> int:
    """
    函数式接口 - 计算从 1 到 target 的累加和
    """
    return sum_to_n(target)


