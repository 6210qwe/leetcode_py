"""
2442. 反转之后不同整数的数目 - 给你一个由 正 整数组成的数组 nums 。 你必须取出数组中的每个整数，反转其中每个数位，并将反转后得到的数字添加到数组的末尾。这一操作只针对 nums 中原有的整数执行。 返回结果数组中 不同 整数的数目。 示例 1： 输入：nums = [1,13,10,12,31] 输出：6 解释：反转每个数字后，结果数组是 [1,13,10,12,31,1,31,1,21,13] 。 反转后得到的数字添加到数组的末尾并按斜体加粗表示。注意对于整数 10 ，反转之后会变成 01 ，即 1 。 数组中不同整数的数目为 6（数字 1、10、12、13、21 和 31）。 示例 2： 输入：nums = [2,2,2] 输出：1 解释：反转每个数字后，结果数组是 [2,2,2,2,2,2] 。 数组中不同整数的数目为 1（数字 2）。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来存储原始数组和反转后的数组中的所有不同整数。

算法步骤:
1. 初始化一个集合 `seen` 来存储所有不同的整数。
2. 遍历数组 `nums`，将每个整数及其反转后的整数添加到 `seen` 集合中。
3. 返回 `seen` 集合的大小，即不同整数的数量。

关键点:
- 使用集合来自动去重。
- 反转整数时，可以将其转换为字符串进行反转，然后再转换回整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。每个整数的反转操作是 O(1) 的。
空间复杂度: O(n)，最坏情况下，所有的整数都是不同的，需要存储 n 个整数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_distinct_integers(nums: List[int]) -> int:
    """
    函数式接口 - 计算反转之后不同整数的数目
    """
    seen = set()
    
    for num in nums:
        seen.add(num)
        seen.add(int(str(num)[::-1]))
    
    return len(seen)


Solution = create_solution(count_distinct_integers)