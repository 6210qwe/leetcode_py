# ============================================================================
# 问题描述
# ============================================================================
"""
995. K 连续位的最小翻转次数 - 给定一个二进制数组 nums 和一个整数 k 。 k位翻转 就是从 nums 中选择一个长度为 k 的 子数组 ，同时把子数组中的每一个 0 都改成 1 ，把子数组中的每一个 1 都改成 0 。 返回数组中不存在 0 所需的最小 k位翻转 次数。如果不可能，则返回 -1 。 子数组 是数组的 连续 部分。 示例 1： 输入：nums = [0,1,0], K = 1 输出：2 解释：先翻转 A[0]，然后翻转 A[2]。 示例 2： 输入：nums = [1,1,0], K = 2 输出：-1 解释：无论我们怎样翻转大小为 2 的子数组，我们都不能使数组变为 [1,1,1]。 示例 3： 输入：nums = [0,0,0,1,0,1,1,0], K = 3 输出：3 解释： 翻转 A[0],A[1],A[2]: A变成 [1,1,1,1,0,1,1,0] 翻转 A[4],A[5],A[6]: A变成 [1,1,1,1,1,0,0,0] 翻转 A[5],A[6],A[7]: A变成 [1,1,1,1,1,1,1,1] 提示： * 1 <= nums.length <= 105 * 1 <= k <= nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和差分数组来记录翻转操作。

算法步骤:
1. 初始化一个差分数组 `diff` 和一个当前翻转次数 `flip_count`。
2. 遍历数组 `nums`，对于每个元素：
   - 更新当前翻转次数 `flip_count`。
   - 如果当前元素加上翻转次数后是 0，则需要进行翻转操作。
   - 在差分数组 `diff` 中记录翻转操作，并更新翻转次数。
3. 如果遍历过程中发现无法完成翻转（即剩余长度小于 k），则返回 -1。
4. 返回总的翻转次数。

关键点:
- 使用差分数组记录翻转操作，避免重复计算。
- 通过滑动窗口维护当前翻转次数，确保每次操作的有效性。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。每个元素只处理一次。
空间复杂度: O(1)，差分数组 `diff` 的大小是固定的，不随输入规模增加而增加。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def min_k_bit_flips(nums: List[int], k: int) -> int:
    n = len(nums)
    diff = [0] * (n + 1)
    flip_count = 0
    result = 0
    
    for i in range(n):
        flip_count += diff[i]
        if (nums[i] + flip_count) % 2 == 0:
            if i + k > n:
                return -1
            result += 1
            flip_count += 1
            diff[i + k] -= 1
    
    return result

Solution = min_k_bit_flips