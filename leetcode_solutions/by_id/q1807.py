"""
1689. 十-二进制数的最少数目 - 如果一个十进制数字不含任何前导零，且每一位上的数字不是 0 就是 1 ，那么该数字就是一个 十-二进制数 。例如，101 和 1100 都是 十-二进制数，而 112 和 3001 不是。 给你一个表示十进制整数的字符串 n ，返回和为 n 的 十-二进制数 的最少数目。 示例 1： 输入：n = "32" 输出：3 解释：10 + 11 + 11 = 32 示例 2： 输入：n = "82734" 输出：8 示例 3： 输入：n = "27346209830709182346" 输出：9 提示： * 1 <= n.length <= 105 * n 仅由数字组成 * n 不含任何前导零并总是表示正整数
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 最少的十-二进制数的数量等于字符串 n 中的最大数字。

算法步骤:
1. 遍历字符串 n，找到其中的最大数字。
2. 返回最大数字作为结果。

关键点:
- 十-二进制数的每一位只能是 0 或 1，因此要达到目标和 n，需要的最少十-二进制数的数量就是 n 中的最大数字。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 n 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_partitions(n: str) -> int:
    """
    函数式接口 - 返回和为 n 的十-二进制数的最少数目
    """
    # 找到字符串 n 中的最大数字
    max_digit = 0
    for digit in n:
        max_digit = max(max_digit, int(digit))
    
    return max_digit


Solution = create_solution(min_partitions)