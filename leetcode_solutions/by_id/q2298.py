"""
2180. 统计各位数字之和为偶数的整数个数 - 给你一个正整数 num ，请你统计并返回 小于或等于 num 且各位数字之和为 偶数 的正整数的数目。 正整数的 各位数字之和 是其所有位上的对应数字相加的结果。 示例 1： 输入：num = 4 输出：2 解释： 只有 2 和 4 满足小于等于 4 且各位数字之和为偶数。 示例 2： 输入：num = 30 输出：14 解释： 只有 14 个整数满足小于等于 30 且各位数字之和为偶数，分别是： 2、4、6、8、11、13、15、17、19、20、22、24、26 和 28 。 提示： * 1 <= num <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对于每个整数，计算其各位数字之和，并检查是否为偶数。

算法步骤:
1. 初始化计数器 count 为 0。
2. 遍历从 1 到 num 的所有整数。
3. 对于每个整数，计算其各位数字之和。
4. 如果各位数字之和为偶数，则增加计数器 count。
5. 返回计数器 count。

关键点:
- 使用模运算和整除运算来逐位提取数字。
- 使用一个辅助函数来计算各位数字之和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * d)，其中 n 是 num 的值，d 是 num 的位数。最坏情况下，d 为 log10(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_even_digit_sum(number: int) -> bool:
    """检查一个整数的各位数字之和是否为偶数"""
    digit_sum = 0
    while number > 0:
        digit_sum += number % 10
        number //= 10
    return digit_sum % 2 == 0


def solution_function_name(num: int) -> int:
    """
    函数式接口 - 统计小于或等于 num 且各位数字之和为偶数的正整数的数目
    """
    count = 0
    for i in range(1, num + 1):
        if is_even_digit_sum(i):
            count += 1
    return count


Solution = create_solution(solution_function_name)