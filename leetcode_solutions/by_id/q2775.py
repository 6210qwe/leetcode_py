"""
2648. 生成斐波那契数列 - 请你编写一个生成器函数，并返回一个可以生成 斐波那契数列 的生成器对象。 斐波那契数列 的递推公式为 Xn = Xn-1 + Xn-2 。 这个数列的前几个数字是 0, 1, 1, 2, 3, 5, 8, 13 。 示例 1： 输入：callCount = 5 输出：[0,1,1,2,3] 解释： const gen = fibGenerator(); gen.next().value; // 0 gen.next().value; // 1 gen.next().value; // 1 gen.next().value; // 2 gen.next().value; // 3 示例 2： 输入：callCount = 0 输出：[] 解释：gen.next() 永远不会被调用，所以什么也不会输出 提示： * 0 <= callCount <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用生成器来生成斐波那契数列。

算法步骤:
1. 初始化两个变量 a 和 b 分别为 0 和 1。
2. 在每次生成器迭代中，先产生当前的 a 值。
3. 更新 a 和 b 的值，a 变为 b，b 变为 a + b。

关键点:
- 生成器在每次迭代时只保存当前和下一个斐波那契数，因此空间复杂度很低。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 每次生成一个斐波那契数的时间复杂度是常数级别的。
空间复杂度: O(1) - 生成器只保存了两个变量 a 和 b。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Generator


def fibGenerator() -> Generator[int, None, None]:
    """
    生成斐波那契数列的生成器函数。
    """
    a, b = 0, 1
    while True:
        yield a
        a, b = b, a + b


Solution = fibGenerator