"""
LCR 182. 动态口令 - 某公司门禁密码使用动态口令技术。初始密码为字符串 password，密码更新均遵循以下步骤： * 设定一个正整数目标值 target * 将 password 前 target 个字符按原顺序移动至字符串末尾 请返回更新后的密码字符串。 示例 1： 输入: password = "s3cur1tyC0d3", target = 4 输出: "r1tyC0d3s3cu" 示例 2： 输入: password = "lrloseumgh", target = 6 输出: "umghlrlose" 提示： * 1 <= target < password.length <= 10000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过字符串切片操作来实现旋转。

算法步骤:
1. 将字符串分为两部分：前 target 个字符和剩余的字符。
2. 将这两部分拼接起来，形成新的字符串。

关键点:
- 使用 Python 的字符串切片操作可以高效地完成字符串的分割和拼接。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。字符串切片操作的时间复杂度是 O(n)。
空间复杂度: O(n)，因为我们需要创建一个新的字符串来存储结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(password: str, target: int) -> str:
    """
    函数式接口 - 实现动态口令更新
    """
    # 使用字符串切片操作来实现旋转
    return password[target:] + password[:target]


Solution = create_solution(solution_function_name)