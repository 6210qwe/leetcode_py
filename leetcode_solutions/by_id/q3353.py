"""
3083. 字符串及其反转中是否存在同一子字符串 - 给你一个字符串 s ，请你判断字符串 s 是否存在一个长度为 2 的子字符串，在 s 反转后的字符串中也出现。 如果存在这样的子字符串，返回 true；如果不存在，返回 false 。 示例 1： 输入：s = "leetcode" 输出：true 解释：子字符串 "ee" 的长度为 2，它也出现在 reverse(s) == "edocteel" 中。 示例 2： 输入：s = "abcba" 输出：true 解释：所有长度为 2 的子字符串 "ab"、"bc"、"cb"、"ba" 也都出现在 reverse(s) == "abcba" 中。 示例 3： 输入：s = "abcd" 输出：false 解释：字符串 s 中不存在满足「在其反转后的字符串中也出现」且长度为 2 的子字符串。 提示： * 1 <= s.length <= 100 * 字符串 s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合存储所有长度为 2 的子字符串，然后检查这些子字符串是否在反转后的字符串中出现。

算法步骤:
1. 创建一个哈希集合来存储字符串 s 中所有长度为 2 的子字符串。
2. 遍历字符串 s，提取所有长度为 2 的子字符串并将其添加到哈希集合中。
3. 反转字符串 s 并检查哈希集合中的每个子字符串是否在反转后的字符串中出现。
4. 如果找到一个匹配的子字符串，返回 true；否则，返回 false。

关键点:
- 使用哈希集合来存储子字符串，以实现 O(1) 时间复杂度的查找。
- 反转字符串后，直接检查哈希集合中的子字符串是否存在于反转后的字符串中。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历字符串一次来提取子字符串，并再次遍历来检查反转后的字符串。
空间复杂度: O(n)，哈希集合最多存储 n-1 个子字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> bool:
    """
    函数式接口 - 判断字符串 s 是否存在一个长度为 2 的子字符串，在 s 反转后的字符串中也出现。
    """
    # 创建一个哈希集合来存储所有长度为 2 的子字符串
    substrings = set()
    
    # 遍历字符串 s，提取所有长度为 2 的子字符串并将其添加到哈希集合中
    for i in range(len(s) - 1):
        substrings.add(s[i:i+2])
    
    # 反转字符串 s
    reversed_s = s[::-1]
    
    # 检查哈希集合中的每个子字符串是否在反转后的字符串中出现
    for substring in substrings:
        if substring in reversed_s:
            return True
    
    return False


Solution = create_solution(solution_function_name)