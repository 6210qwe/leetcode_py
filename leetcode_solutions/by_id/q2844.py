"""
2778. 特殊元素平方和 - 给你一个下标从 1 开始、长度为 n 的整数数组 nums 。 对 nums 中的元素 nums[i] 而言，如果 n 能够被 i 整除，即 n % i == 0 ，则认为 nums[i] 是一个 特殊元素 。 返回 nums 中所有 特殊元素 的 平方和 。 示例 1： 输入：nums = [1,2,3,4] 输出：21 解释：nums 中共有 3 个特殊元素：nums[1]，因为 4 被 1 整除；nums[2]，因为 4 被 2 整除；以及 nums[4]，因为 4 被 4 整除。 因此，nums 中所有特殊元素的平方和等于 nums[1] * nums[1] + nums[2] * nums[2] + nums[4] * nums[4] = 1 * 1 + 2 * 2 + 4 * 4 = 21 。 示例 2： 输入：nums = [2,7,1,19,18,3] 输出：63 解释：nums 中共有 4 个特殊元素：nums[1]，因为 6 被 1 整除；nums[2] ，因为 6 被 2 整除；nums[3]，因为 6 被 3 整除；以及 nums[6]，因为 6 被 6 整除。 因此，nums 中所有特殊元素的平方和等于 nums[1] * nums[1] + nums[2] * nums[2] + nums[3] * nums[3] + nums[6] * nums[6] = 2 * 2 + 7 * 7 + 1 * 1 + 3 * 3 = 63 。 提示： * 1 <= nums.length == n <= 50 * 1 <= nums[i] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，检查每个索引是否是数组长度的因数，如果是，则将该位置的元素平方并累加到结果中。

算法步骤:
1. 初始化一个变量 `result` 用于存储平方和。
2. 遍历数组 `nums`，对于每个索引 `i`（从 1 开始），检查 `n % (i + 1) == 0` 是否成立。
3. 如果成立，将 `nums[i]` 的平方累加到 `result` 中。
4. 返回 `result`。

关键点:
- 索引从 1 开始，因此在遍历时需要将 `i` 加 1。
- 使用 `n % (i + 1) == 0` 来判断当前索引是否是数组长度的因数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历一次数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_squares_of_special_elements(nums: List[int]) -> int:
    """
    函数式接口 - 计算数组中所有特殊元素的平方和
    """
    n = len(nums)
    result = 0
    for i in range(n):
        if n % (i + 1) == 0:
            result += nums[i] ** 2
    return result


