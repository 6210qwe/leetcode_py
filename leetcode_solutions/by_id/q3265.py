"""
3026. 最大好子数组和 - 给你一个长度为 n 的数组 nums 和一个 正 整数 k 。 如果 nums 的一个子数组中，第一个元素和最后一个元素 差的绝对值恰好 为 k ，我们称这个子数组为 好 的。换句话说，如果子数组 nums[i..j] 满足 |nums[i] - nums[j]| == k ，那么它是一个好子数组。 请你返回 nums 中 好 子数组的 最大 和，如果没有好子数组，返回 0 。 示例 1： 输入：nums = [1,2,3,4,5,6], k = 1 输出：11 解释：好子数组中第一个元素和最后一个元素的差的绝对值必须为 1 。好子数组有 [1,2] ，[2,3] ，[3,4] ，[4,5] 和 [5,6] 。最大子数组和为 11 ，对应的子数组为 [5,6] 。 示例 2： 输入：nums = [-1,3,2,4,5], k = 3 输出：11 解释：好子数组中第一个元素和最后一个元素的差的绝对值必须为 3 。好子数组有 [-1,3,2] 和 [2,4,5] 。最大子数组和为 11 ，对应的子数组为 [2,4,5] 。 示例 3： 输入：nums = [-1,-2,-3,-4], k = 2 输出：-6 解释：好子数组中第一个元素和最后一个元素的差的绝对值必须为 2 。好子数组有 [-1,-2,-3] 和 [-2,-3,-4] 。最大子数组和为 -6 ，对应的子数组为 [-1,-2,-3] 。 提示： * 2 <= nums.length <= 105 * -109 <= nums[i] <= 109 * 1 <= k <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和和哈希表来记录每个可能的起点，并在遍历过程中找到满足条件的子数组。

算法步骤:
1. 初始化前缀和数组 `prefix_sum` 和哈希表 `seen`。
2. 遍历数组 `nums`，计算当前前缀和。
3. 对于每个元素 `nums[j]`，检查 `nums[j] + k` 和 `nums[j] - k` 是否在 `seen` 中。
4. 如果存在，更新最大子数组和。
5. 更新 `seen`，记录当前前缀和及其索引。

关键点:
- 使用前缀和和哈希表来高效地查找满足条件的子数组。
- 通过哈希表记录每个可能的起点，避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def maximum_good_subarray_sum(nums: List[int], k: int) -> int:
    """
    函数式接口 - 返回 nums 中 好 子数组的最大和
    """
    n = len(nums)
    prefix_sum = 0
    seen = {}
    max_sum = float('-inf')
    
    for j in range(n):
        prefix_sum += nums[j]
        
        if nums[j] + k in seen:
            max_sum = max(max_sum, prefix_sum - seen[nums[j] + k])
        
        if nums[j] - k in seen:
            max_sum = max(max_sum, prefix_sum - seen[nums[j] - k])
        
        if nums[j] not in seen:
            seen[nums[j]] = prefix_sum
    
    return max_sum if max_sum != float('-inf') else 0


Solution = create_solution(maximum_good_subarray_sum)