# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和计数排序来找到最长的连续子序列。

算法步骤:
1. 使用计数数组统计每个魔力值的出现次数。
2. 遍历计数数组，找到最长的连续子序列。

关键点:
- 使用计数数组可以快速统计每个魔力值的出现次数。
- 通过遍历计数数组，可以找到最长的连续子序列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + k)，其中 n 是 runes 的长度，k 是魔力值的范围（10^4）。
空间复杂度: O(k)，用于存储计数数组。
"""

# ============================================================================
