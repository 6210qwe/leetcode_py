from typing import List

def solution_function_name(runes: List[int]) -> int:
    """
    函数式接口 - 返回能够携带的符文最大数量
    """
    # 计数数组，统计每个魔力值的出现次数
    count = [0] * 10001
    for rune in runes:
        count[rune] += 1
    
    max_length = 0
    current_length = 0
    
    # 遍历计数数组，找到最长的连续子序列
    for i in range(10001):
        if count[i] > 0:
            current_length += count[i]
        else:
            max_length = max(max_length, current_length)
            current_length = 0
    
    # 最后一次连续子序列的长度
    max_length = max(max_length, current_length)
    
    return max_length

