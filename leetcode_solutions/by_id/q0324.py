from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def wiggle_sort(nums: List[int]) -> None:
    """
    函数式接口 - 摆动排序 II
    
    实现思路:
    排序后，将较大的一半放在偶数位置，较小的一半放在奇数位置。
    
    Args:
        nums: 整数数组（原地修改）
        
    Returns:
        None（原地修改）
        
    Example:
        >>> nums = [1,5,1,1,6,4]
        >>> wiggle_sort(nums)
        >>> nums
        [1, 6, 1, 5, 1, 4]
    """
    sorted_nums = sorted(nums)
    n = len(nums)
    mid = (n + 1) // 2
    
    # 较小的一半放在奇数位置，较大的一半放在偶数位置
    # 从后往前填充，避免覆盖
    j = mid - 1  # 较小的一半
    k = n - 1    # 较大的一半
    
    for i in range(n):
        if i % 2 == 0:
            nums[i] = sorted_nums[j]
            j -= 1
        else:
            nums[i] = sorted_nums[k]
            k -= 1


