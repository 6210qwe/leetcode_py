"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i] 表示使用 i 次操作最多可以得到多少个 'A'。

算法步骤:
1. 初始化 dp 数组，dp[0] = 1，表示没有操作时只有一个 'A'。
2. 对于每一个 i，从 1 到 n，尝试所有可能的操作序列，更新 dp[i]。
3. 最后返回 dp[n]。

关键点:
- 使用一个嵌套循环来尝试所有可能的操作序列。
- 通过复制和粘贴操作来最大化 'A' 的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n)
"""

# ============================================================================
