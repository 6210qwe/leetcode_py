"""
1573. 分割字符串的方案数 - 给你一个二进制串 s （一个只包含 0 和 1 的字符串），我们可以将 s 分割成 3 个 非空 字符串 s1, s2, s3 （s1 + s2 + s3 = s）。 请你返回分割 s 的方案数，满足 s1，s2 和 s3 中字符 '1' 的数目相同。 由于答案可能很大，请将它对 10^9 + 7 取余后返回。 示例 1： 输入：s = "10101" 输出：4 解释：总共有 4 种方法将 s 分割成含有 '1' 数目相同的三个子字符串。 "1|010|1" "1|01|01" "10|10|1" "10|1|01" 示例 2： 输入：s = "1001" 输出：0 示例 3： 输入：s = "0000" 输出：3 解释：总共有 3 种分割 s 的方法。 "0|0|00" "0|00|0" "00|0|0" 示例 4： 输入：s = "100100010100110" 输出：12 提示： * s[i] == '0' 或者 s[i] == '1' * 3 <= s.length <= 10^5
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算每个 '1' 出现的位置，然后根据这些位置计算分割方案数。

算法步骤:
1. 统计字符串中 '1' 的总数。
2. 如果 '1' 的总数不是 3 的倍数，直接返回 0。
3. 找到每一段 '1' 的起始和结束位置。
4. 根据这些位置计算可以分割的方案数。

关键点:
- 通过计算 '1' 的位置来确定分割点。
- 使用组合数学公式计算分割方案数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算分割字符串的方案数
    """
    MOD = 10**9 + 7
    n = len(s)
    ones = [i for i, c in enumerate(s) if c == '1']
    m = len(ones)
    
    if m % 3 != 0:
        return 0
    
    if m == 0:
        return (n - 1) * (n - 2) // 2 % MOD
    
    ways = 1
    for i in range(m // 3, 2 * m // 3):
        ways *= (ones[i + 1] - ones[i])
        ways %= MOD
    
    return ways


Solution = create_solution(solution_function_name)