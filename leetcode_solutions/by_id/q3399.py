"""
3132. 找出与数组相加的整数 II - 给你两个整数数组 nums1 和 nums2。 从 nums1 中移除两个元素，并且所有其他元素都与变量 x 所表示的整数相加。如果 x 为负数，则表现为元素值的减少。 执行上述操作后，nums1 和 nums2 相等 。当两个数组中包含相同的整数，并且这些整数出现的频次相同时，两个数组 相等 。 返回能够实现数组相等的 最小 整数 x 。 示例 1: 输入：nums1 = [4,20,16,12,8], nums2 = [14,18,10] 输出：-2 解释： 移除 nums1 中下标为 [0,4] 的两个元素，并且每个元素与 -2 相加后，nums1 变为 [18,14,10] ，与 nums2 相等。 示例 2: 输入：nums1 = [3,5,5,3], nums2 = [7,7] 输出：2 解释： 移除 nums1 中下标为 [0,3] 的两个元素，并且每个元素与 2 相加后，nums1 变为 [7,7] ，与 nums2 相等。 提示： * 3 <= nums1.length <= 200 * nums2.length == nums1.length - 2 * 0 <= nums1[i], nums2[i] <= 1000 * 测试用例以这样的方式生成：存在一个整数 x，nums1 中的每个元素都与 x 相加后，再移除两个元素，nums1 可以与 nums2 相等。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针和排序来找到最小的整数 x，使得在移除 nums1 中的两个元素后，nums1 和 nums2 相等。

算法步骤:
1. 对 nums1 和 nums2 进行排序。
2. 使用双指针遍历 nums1 和 nums2，找到可能的 x 值。
3. 检查移除两个元素后的 nums1 是否与 nums2 相等。
4. 返回最小的 x 值。

关键点:
- 通过排序和双指针可以高效地找到可能的 x 值。
- 需要检查移除两个元素后的 nums1 是否与 nums2 相等。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums1 的长度。排序的时间复杂度是 O(n log n)，双指针遍历的时间复杂度是 O(n)。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_integer_added_to_array(nums1: List[int], nums2: List[int]) -> int:
    """
    找出与数组相加的整数 II
    :param nums1: 第一个整数数组
    :param nums2: 第二个整数数组
    :return: 能够实现数组相等的最小整数 x
    """
    nums1.sort()
    nums2.sort()
    
    for i in range(len(nums1)):
        for j in range(i + 1, len(nums1)):
            x = nums2[0] - nums1[i]
            if all(nums1[k] + x == nums2[k - (k > i) - (k > j)] for k in range(len(nums1)) if k != i and k != j):
                return x
    return 0  # 理论上不会到达这里，因为题目保证存在解

Solution = create_solution(find_integer_added_to_array)