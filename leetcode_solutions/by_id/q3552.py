# ============================================================================
# 问题描述
# ============================================================================
"""
3260. 找出最大的 N 位 K 回文数 - 给你两个 正整数 n 和 k。 如果整数 x 满足以下全部条件，则该整数是一个 k 回文数： * x 是一个 回文数。 * x 可以被 k 整除。 以字符串形式返回 最大的 n 位 k 回文数。 注意，该整数 不 含前导零。 示例 1： 输入： n = 3, k = 5 输出： "595" 解释： 595 是最大的 3 位 k 回文数。 示例 2： 输入： n = 1, k = 4 输出： "8" 解释： 1 位 k 回文数只有 4 和 8。 示例 3： 输入： n = 5, k = 6 输出： "89898" 提示： * 1 <= n <= 105 * 1 <= k <= 9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过构造回文数并检查其是否能被 k 整除来找到最大的 n 位 k 回文数。

算法步骤:
1. 从最大的 n 位数开始，逐步减少直到找到一个满足条件的回文数。
2. 构造回文数时，先生成前半部分，然后翻转得到后半部分，拼接成完整的回文数。
3. 检查生成的回文数是否能被 k 整除。
4. 如果找到满足条件的回文数，返回其字符串形式；否则继续减小。

关键点:
- 通过构造回文数并检查其是否能被 k 整除来找到最大的 n 位 k 回文数。
- 从最大的 n 位数开始逐步减少，确保找到的是最大的满足条件的回文数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(10^n) - 在最坏情况下，需要检查所有的 n 位数。
空间复杂度: O(n) - 用于存储生成的回文数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, k: int) -> str:
    """
    函数式接口 - 返回最大的 n 位 k 回文数
    """
    def is_palindrome(num: int) -> bool:
        return str(num) == str(num)[::-1]

    for i in range(10**n - 1, 10**(n-1) - 1, -1):
        if i % k == 0 and is_palindrome(i):
            return str(i)

    return ""


Solution = create_solution(solution_function_name)