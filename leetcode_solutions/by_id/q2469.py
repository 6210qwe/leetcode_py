"""
2389. 和有限的最长子序列 - 给你一个长度为 n 的整数数组 nums ，和一个长度为 m 的整数数组 queries 。 返回一个长度为 m 的数组 answer ，其中 answer[i] 是 nums 中 元素之和小于等于 queries[i] 的 子序列 的 最大 长度 。 子序列 是由一个数组删除某些元素（也可以不删除）但不改变剩余元素顺序得到的一个数组。 示例 1： 输入：nums = [4,5,2,1], queries = [3,10,21] 输出：[2,3,4] 解释：queries 对应的 answer 如下： - 子序列 [2,1] 的和小于或等于 3 。可以证明满足题目要求的子序列的最大长度是 2 ，所以 answer[0] = 2 。 - 子序列 [4,5,1] 的和小于或等于 10 。可以证明满足题目要求的子序列的最大长度是 3 ，所以 answer[1] = 3 。 - 子序列 [4,5,2,1] 的和小于或等于 21 。可以证明满足题目要求的子序列的最大长度是 4 ，所以 answer[2] = 4 。 示例 2： 输入：nums = [2,3,4,5], queries = [1] 输出：[0] 解释：空子序列是唯一一个满足元素和小于或等于 1 的子序列，所以 answer[0] = 0 。 提示： * n == nums.length * m == queries.length * 1 <= n, m <= 1000 * 1 <= nums[i], queries[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和前缀和来找到每个查询对应的最长子序列长度。

算法步骤:
1. 对 nums 进行排序。
2. 计算 nums 的前缀和。
3. 对于每个查询，使用二分查找找到前缀和中小于等于查询值的最大索引。

关键点:
- 排序使得我们可以贪心地选择最小的元素来构建子序列。
- 前缀和用于快速计算子序列的和。
- 二分查找用于高效地找到满足条件的最大索引。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + m log n)，其中 n 是 nums 的长度，m 是 queries 的长度。排序的时间复杂度是 O(n log n)，计算前缀和是 O(n)，每次二分查找是 O(log n)。
空间复杂度: O(n)，存储前缀和需要 O(n) 的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def longest_subsequence_with_limited_sum(nums: List[int], queries: List[int]) -> List[int]:
    """
    函数式接口 - 返回每个查询对应的最长子序列长度
    """
    # 对 nums 进行排序
    nums.sort()
    
    # 计算前缀和
    prefix_sum = [0]
    for num in nums:
        prefix_sum.append(prefix_sum[-1] + num)
    
    # 对每个查询进行二分查找
    def binary_search(target: int) -> int:
        left, right = 0, len(prefix_sum) - 1
        while left < right:
            mid = (left + right + 1) // 2
            if prefix_sum[mid] <= target:
                left = mid
            else:
                right = mid - 1
        return left
    
    result = []
    for query in queries:
        index = binary_search(query)
        result.append(index)
    
    return result

