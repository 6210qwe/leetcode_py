"""
521. 最长特殊序列 Ⅰ - 给你两个字符串 a 和 b，请返回 这两个字符串中 最长的特殊序列 的长度。如果不存在，则返回 -1 。 「最长特殊序列」 定义如下：该序列为 某字符串独有的最长子序列（即不能是其他字符串的子序列） 。 字符串 s 的子序列是在从 s 中删除任意数量的字符后可以获得的字符串。 * 例如，"abc" 是 "aebdc" 的子序列，因为删除 "aebdc" 中斜体加粗的字符可以得到 "abc" 。 "aebdc" 的子序列还包括 "aebdc" 、 "aeb" 和 "" (空字符串)。 示例 1： 输入: a = "aba", b = "cdc" 输出: 3 解释: 最长特殊序列可为 "aba" (或 "cdc")，两者均为自身的子序列且不是对方的子序列。 示例 2： 输入：a = "aaa", b = "bbb" 输出：3 解释: 最长特殊序列是 "aaa" 和 "bbb" 。 示例 3： 输入：a = "aaa", b = "aaa" 输出：-1 解释: 字符串 a 的每个子序列也是字符串 b 的每个子序列。同样，字符串 b 的每个子序列也是字符串 a 的子序列。 提示： * 1 <= a.length, b.length <= 100 * a 和 b 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 如果两个字符串不相等，那么较长的字符串就是最长的特殊序列。如果两个字符串相等，则没有最长的特殊序列。

算法步骤:
1. 比较两个字符串是否相等。
2. 如果不相等，返回较长字符串的长度。
3. 如果相等，返回 -1。

关键点:
- 判断两个字符串是否相等。
- 返回较长字符串的长度或 -1。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(a: str, b: str) -> int:
    """
    函数式接口 - 返回两个字符串中最长的特殊序列的长度
    """
    if a == b:
        return -1
    return max(len(a), len(b))


Solution = create_solution(solution_function_name)