"""
2765. 最长交替子数组 - 给你一个下标从 0 开始的整数数组 nums 。如果 nums 中长度为 m 的子数组 s 满足以下条件，我们称它是一个 交替子数组 ： * m 大于 1 。 * s1 = s0 + 1 。 * 下标从 0 开始的子数组 s 与数组 [s0, s1, s0, s1,...,s(m-1) % 2] 一样。也就是说，s1 - s0 = 1 ，s2 - s1 = -1 ，s3 - s2 = 1 ，s4 - s3 = -1 ，以此类推，直到 s[m - 1] - s[m - 2] = (-1)m 。 请你返回 nums 中所有 交替 子数组中，最长的长度，如果不存在交替子数组，请你返回 -1 。 子数组是一个数组中一段连续 非空 的元素序列。 示例 1： 输入：nums = [2,3,4,3,4] 输出：4 解释：交替子数组有 [2,3]，[3,4]，[3,4,3] 和 [3,4,3,4]。最长的子数组为 [3,4,3,4]，长度为 4。 示例 2： 输入：nums = [4,5,6] 输出：2 解释：[4,5] 和 [5,6] 是仅有的两个交替子数组。它们长度都为 2 。 提示： * 2 <= nums.length <= 100 * 1 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到最长的交替子数组。

算法步骤:
1. 初始化变量 `max_length` 为 -1，用于记录最长交替子数组的长度。
2. 遍历数组，使用双指针方法来找到每个可能的交替子数组。
3. 对于每个起始点，检查后续元素是否满足交替条件，并更新 `max_length`。

关键点:
- 使用双指针方法来遍历数组，确保时间复杂度最优。
- 通过比较相邻元素的差值来判断是否满足交替条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个元素最多被访问两次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_alternating_subarray(nums: List[int]) -> int:
    """
    返回数组中所有交替子数组中最长的长度，如果不存在交替子数组，则返回 -1。
    """
    max_length = -1
    n = len(nums)
    
    for i in range(n):
        if i + 1 < n and nums[i + 1] == nums[i] + 1:
            length = 2
            diff = -1
            j = i + 2
            while j < n and nums[j] == nums[j - 1] + diff:
                length += 1
                diff *= -1
                j += 1
            max_length = max(max_length, length)
    
    return max_length


Solution = create_solution(longest_alternating_subarray)