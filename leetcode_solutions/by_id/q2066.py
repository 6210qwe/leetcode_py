# 问题描述
# ============================================================================
"""
1936. 新增的最少台阶数 - 给你一个 严格递增 的整数数组 rungs ，用于表示梯子上每一台阶的 高度 。当前你正站在高度为 0 的地板上，并打算爬到最后一个台阶。 另给你一个整数 dist 。每次移动中，你可以到达下一个距离你当前位置（地板或台阶）不超过 dist 高度的台阶。当然，你也可以在任何正 整数 高度处插入尚不存在的新台阶。 返回爬到最后一阶时必须添加到梯子上的 最少 台阶数。 示例 1： 输入：rungs = [1,3,5,10], dist = 2 输出：2 解释： 现在无法到达最后一阶。 在高度为 7 和 8 的位置增设新的台阶，以爬上梯子。 梯子在高度为 [1,3,5,7,8,10] 的位置上有台阶。 示例 2： 输入：rungs = [3,6,8,10], dist = 3 输出：0 解释： 这个梯子无需增设新台阶也可以爬上去。 示例 3： 输入：rungs = [3,4,6,7], dist = 2 输出：1 解释： 现在无法从地板到达梯子的第一阶。 在高度为 1 的位置增设新的台阶，以爬上梯子。 梯子在高度为 [1,3,4,6,7] 的位置上有台阶。 示例 4： 输入：rungs = [5], dist = 10 输出：0 解释：这个梯子无需增设新台阶也可以爬上去。 提示： * 1 <= rungs.length <= 105 * 1 <= rungs[i] <= 109 * 1 <= dist <= 109 * rungs 严格递增
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，遍历每个台阶，计算需要添加的台阶数。

算法步骤:
1. 初始化当前高度为 0。
2. 遍历每个台阶，计算当前台阶与前一个台阶之间的高度差。
3. 如果高度差大于 dist，则计算需要添加的台阶数，并更新当前高度。
4. 返回总的添加台阶数。

关键点:
- 通过计算高度差来确定需要添加的台阶数。
- 使用贪心算法确保每次添加的台阶数最少。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 rungs 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def add_rungs(rungs: List[int], dist: int) -> int:
    """
    函数式接口 - 计算需要添加的最少台阶数
    """
    current_height = 0
    added_rungs = 0
    
    for rung in rungs:
        if rung - current_height > dist:
            added_rungs += (rung - current_height - 1) // dist
        current_height = rung
    
    return added_rungs


Solution = create_solution(add_rungs)