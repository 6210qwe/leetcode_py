"""
801. 使序列递增的最小交换次数 - 我们有两个长度相等且不为空的整型数组 nums1 和 nums2 。在一次操作中，我们可以交换 nums1[i] 和 nums2[i]的元素。 * 例如，如果 nums1 = [1,2,3,8] ， nums2 =[5,6,7,4] ，你可以交换 i = 3 处的元素，得到 nums1 =[1,2,3,4] 和 nums2 =[5,6,7,8] 。 返回 使 nums1 和 nums2 严格递增 所需操作的最小次数 。 数组 arr 严格递增 且 arr[0] < arr[1] < arr[2] < ... < arr[arr.length - 1] 。 注意： * 用例保证可以实现操作。 示例 1: 输入: nums1 = [1,3,5,4], nums2 = [1,2,3,7] 输出: 1 解释: 交换 A[3] 和 B[3] 后，两个数组如下: A = [1, 3, 5, 7] ， B = [1, 2, 3, 4] 两个数组均为严格递增的。 示例 2: 输入: nums1 = [0,3,5,8,9], nums2 = [2,1,4,6,9] 输出: 1 提示: * 2 <= nums1.length <= 105 * nums2.length == nums1.length * 0 <= nums1[i], nums2[i] <= 2 * 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义两个状态：
- dp[i][0] 表示到第 i 个位置为止，不交换 nums1[i] 和 nums2[i] 的最小交换次数。
- dp[i][1] 表示到第 i 个位置为止，交换 nums1[i] 和 nums2[i] 的最小交换次数。

算法步骤:
1. 初始化 dp 数组，dp[0][0] = 0, dp[0][1] = 1。
2. 遍历数组，对于每个位置 i，根据 nums1 和 nums2 的值更新 dp[i][0] 和 dp[i][1]。
3. 最终结果是 dp[n-1][0] 和 dp[n-1][1] 中的最小值。

关键点:
- 动态规划的状态转移方程需要考虑当前元素和前一个元素的关系。
- 通过维护两个状态，可以有效地计算出最小交换次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历一次数组。
空间复杂度: O(n)，我们需要一个大小为 n*2 的 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_swap(nums1: List[int], nums2: List[int]) -> int:
    n = len(nums1)
    dp = [[float('inf')] * 2 for _ in range(n)]
    dp[0][0] = 0
    dp[0][1] = 1

    for i in range(1, n):
        if nums1[i] > nums1[i - 1] and nums2[i] > nums2[i - 1]:
            dp[i][0] = dp[i - 1][0]
            dp[i][1] = dp[i - 1][1] + 1
        if nums1[i] > nums2[i - 1] and nums2[i] > nums1[i - 1]:
            dp[i][0] = min(dp[i][0], dp[i - 1][1])
            dp[i][1] = min(dp[i][1], dp[i - 1][0] + 1)

    return min(dp[n - 1][0], dp[n - 1][1])


Solution = create_solution(min_swap)