"""
883. 三维形体投影面积 - 在 n x n 的网格 grid 中，我们放置了一些与 x，y，z 三轴对齐的 1 x 1 x 1 立方体。 每个值 v = grid[i][j] 表示有一列 v 个正方体叠放在格子 (i, j) 上。 现在，我们查看这些立方体在 xy 、yz 和 zx 平面上的投影。 投影 就像影子，将 三维 形体映射到一个 二维 平面上。从顶部、前面和侧面看立方体时，我们会看到“影子”。 返回 所有三个投影的总面积 。 示例 1： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/08/02/shadow.png] 输入：[[1,2],[3,4]] 输出：17 解释：这里有该形体在三个轴对齐平面上的三个投影(“阴影部分”)。 示例 2: 输入：grid = [[2]] 输出：5 示例 3： 输入：[[1,0],[0,2]] 输出：8 提示： * n == grid.length == grid[i].length * 1 <= n <= 50 * 0 <= grid[i][j] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算三个方向的投影面积。

算法步骤:
1. 计算顶视图投影面积：遍历整个网格，统计非零单元格的数量。
2. 计算前视图投影面积：遍历每一行，取每行的最大值。
3. 计算侧视图投影面积：遍历每一列，取每列的最大值。

关键点:
- 顶视图投影面积是所有非零单元格的数量。
- 前视图投影面积是每一行的最大值之和。
- 侧视图投影面积是每一列的最大值之和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是网格的边长。我们需要遍历整个网格来计算各个投影面积。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def projection_area(grid: List[List[int]]) -> int:
    """
    函数式接口 - 计算三维形体的投影面积
    """
    n = len(grid)
    top_view = sum(cell > 0 for row in grid for cell in row)
    front_view = sum(max(row) for row in grid)
    side_view = sum(max(grid[row][col] for row in range(n)) for col in range(n))
    
    return top_view + front_view + side_view


Solution = create_solution(projection_area)