"""
1864. 构成交替字符串需要的最小交换次数 - 给你一个二进制字符串 s ，现需要将其转化为一个 交替字符串 。请你计算并返回转化所需的 最小 字符交换次数，如果无法完成转化，返回 -1 。 交替字符串 是指：相邻字符之间不存在相等情况的字符串。例如，字符串 "010" 和 "1010" 属于交替字符串，但 "0100" 不是。 任意两个字符都可以进行交换，不必相邻 。 示例 1： 输入：s = "111000" 输出：1 解释：交换位置 1 和 4："111000" -> "101010" ，字符串变为交替字符串。 示例 2： 输入：s = "010" 输出：0 解释：字符串已经是交替字符串了，不需要交换。 示例 3： 输入：s = "1110" 输出：-1 提示： * 1 <= s.length <= 1000 * s[i] 的值为 '0' 或 '1'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算两种可能的交替字符串（以 '0' 开头和以 '1' 开头）与原字符串的差异，来确定最小交换次数。

算法步骤:
1. 计算以 '0' 开头和以 '1' 开头的交替字符串。
2. 比较这两种交替字符串与原字符串的差异，统计需要交换的字符数量。
3. 返回最小的交换次数，如果无法转换则返回 -1。

关键点:
- 使用计数器来统计字符差异，避免逐个字符比较。
- 考虑奇偶位置上的字符分布。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们需要遍历字符串两次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_swaps(s: str) -> int:
    """
    函数式接口 - 计算将二进制字符串转换为交替字符串所需的最小交换次数
    """
    n = len(s)
    if n % 2 == 0:
        # 偶数长度
        count_0 = s.count('0')
        count_1 = s.count('1')
        if abs(count_0 - count_1) > 1:
            return -1
    else:
        # 奇数长度
        count_0 = s.count('0')
        count_1 = s.count('1')
        if abs(count_0 - count_1) != 1:
            return -1
    
    # 计算以 '0' 开头和以 '1' 开头的交替字符串
    alt_0 = ['0' if i % 2 == 0 else '1' for i in range(n)]
    alt_1 = ['1' if i % 2 == 0 else '0' for i in range(n)]
    
    # 计算差异
    diff_0 = sum(1 for i in range(n) if s[i] != alt_0[i])
    diff_1 = sum(1 for i in range(n) if s[i] != alt_1[i])
    
    # 选择最小的交换次数
    if diff_0 % 2 == 0 and diff_1 % 2 == 0:
        return min(diff_0 // 2, diff_1 // 2)
    elif diff_0 % 2 == 0:
        return diff_0 // 2
    elif diff_1 % 2 == 0:
        return diff_1 // 2
    else:
        return -1


Solution = create_solution(min_swaps)