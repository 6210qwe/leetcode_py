"""
1498. 满足条件的子序列数目 - 给你一个整数数组 nums 和一个整数 target 。 请你统计并返回 nums 中能满足其最小元素与最大元素的 和 小于或等于 target 的 非空 子序列的数目。 由于答案可能很大，请将结果对 109 + 7 取余后返回。 示例 1： 输入：nums = [3,5,6,7], target = 9 输出：4 解释：有 4 个子序列满足该条件。 [3] -> 最小元素 + 最大元素 <= target (3 + 3 <= 9) [3,5] -> (3 + 5 <= 9) [3,5,6] -> (3 + 6 <= 9) [3,6] -> (3 + 6 <= 9) 示例 2： 输入：nums = [3,3,6,8], target = 10 输出：6 解释：有 6 个子序列满足该条件。（nums 中可以有重复数字） [3] , [3] , [3,3], [3,6] , [3,6] , [3,3,6] 示例 3： 输入：nums = [2,3,3,4,6,7], target = 12 输出：61 解释：共有 63 个非空子序列，其中 2 个不满足条件（[6,7], [7]） 有效序列总数为（63 - 2 = 61） 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 106 * 1 <= target <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针和二分查找来高效地找到满足条件的子序列。

算法步骤:
1. 对数组进行排序。
2. 使用双指针遍历数组，左指针从左到右移动，右指针从右到左移动。
3. 对于每个左指针位置，找到右指针的位置，使得 nums[left] + nums[right] <= target。
4. 计算从 left 到 right 之间的子序列数量，并累加到结果中。
5. 由于结果可能很大，每次累加时对 10^9 + 7 取余。

关键点:
- 排序后的数组可以使用双指针高效地找到满足条件的子序列。
- 使用快速幂计算 2 的幂次，以避免大数溢出。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度。排序的时间复杂度是 O(n log n)，双指针遍历的时间复杂度是 O(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import bisect

MOD = 10**9 + 7

def quick_pow(base: int, exp: int, mod: int) -> int:
    """快速幂计算 base^exp % mod"""
    result = 1
    while exp > 0:
        if exp % 2 == 1:
            result = (result * base) % mod
        base = (base * base) % mod
        exp //= 2
    return result

def solution_function_name(nums: List[int], target: int) -> int:
    """
    函数式接口 - 返回满足条件的子序列数目
    """
    nums.sort()
    n = len(nums)
    result = 0
    
    for left in range(n):
        right = bisect.bisect_right(nums, target - nums[left]) - 1
        if right >= left:
            result += quick_pow(2, right - left, MOD)
            result %= MOD
    
    return result

