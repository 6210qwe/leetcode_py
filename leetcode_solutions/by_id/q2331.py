"""
2248. 多个数组求交集 - 给你一个二维整数数组 nums ，其中 nums[i] 是由 不同 正整数组成的一个非空数组，按 升序排列 返回一个数组，数组中的每个元素在 nums 所有数组 中都出现过。 示例 1： 输入：nums = [[3,1,2,4,5],[1,2,3,4],[3,4,5,6]] 输出：[3,4] 解释： nums[0] = [3,1,2,4,5]，nums[1] = [1,2,3,4]，nums[2] = [3,4,5,6]，在 nums 中每个数组中都出现的数字是 3 和 4 ，所以返回 [3,4] 。 示例 2： 输入：nums = [[1,2,3],[4,5,6]] 输出：[] 解释： 不存在同时出现在 nums[0] 和 nums[1] 的整数，所以返回一个空列表 [] 。 提示： * 1 <= nums.length <= 1000 * 1 <= sum(nums[i].length) <= 1000 * 1 <= nums[i][j] <= 1000 * nums[i] 中的所有值 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数器来记录每个数字在所有数组中出现的次数，然后找出出现次数等于数组数量的数字。

算法步骤:
1. 初始化一个计数器，用于记录每个数字在所有数组中出现的次数。
2. 遍历每个数组，更新计数器。
3. 遍历计数器，找出出现次数等于数组数量的数字。
4. 返回这些数字，并按升序排列。

关键点:
- 使用计数器来高效统计每个数字的出现次数。
- 只保留出现次数等于数组数量的数字。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是数组的数量，m 是每个数组的平均长度。
空间复杂度: O(k)，其中 k 是所有数组中不同数字的数量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import Counter

def intersection_of_multiple_arrays(nums: List[List[int]]) -> List[int]:
    """
    函数式接口 - 求多个数组的交集
    """
    # 初始化计数器
    counter = Counter()
    
    # 遍历每个数组，更新计数器
    for arr in nums:
        counter.update(arr)
    
    # 找出出现次数等于数组数量的数字
    result = [num for num, count in counter.items() if count == len(nums)]
    
    # 返回结果并按升序排列
    return sorted(result)

