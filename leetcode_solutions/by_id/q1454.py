"""
1332. 删除回文子序列 - 给你一个字符串 s，它仅由字母 'a' 和 'b' 组成。每一次删除操作都可以从 s 中删除一个回文 子序列。 返回删除给定字符串中所有字符（字符串为空）的最小删除次数。 「子序列」定义：如果一个字符串可以通过删除原字符串某些字符而不改变原字符顺序得到，那么这个字符串就是原字符串的一个子序列。 「回文」定义：如果一个字符串向后和向前读是一致的，那么这个字符串就是一个回文。 示例 1： 输入：s = "ababa" 输出：1 解释：字符串本身就是回文序列，只需要删除一次。 示例 2： 输入：s = "abb" 输出：2 解释："abb" -> "bb" -> "". 先删除回文子序列 "a"，然后再删除 "bb"。 示例 3： 输入：s = "baabb" 输出：2 解释："baabb" -> "b" -> "". 先删除回文子序列 "baab"，然后再删除 "b"。 提示： * 1 <= s.length <= 1000 * s 仅包含字母 'a' 和 'b'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断字符串是否为回文，如果是，则只需一次删除；如果不是，则需要两次删除。

算法步骤:
1. 检查字符串是否为回文。
2. 如果是回文，返回 1。
3. 如果不是回文，返回 2。

关键点:
- 回文字符串的判断。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度，因为我们需要遍历字符串来检查是否为回文。
空间复杂度: O(1)，因为我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 返回删除给定字符串中所有字符（字符串为空）的最小删除次数。
    """
    # 检查字符串是否为回文
    def is_palindrome(s: str) -> bool:
        left, right = 0, len(s) - 1
        while left < right:
            if s[left] != s[right]:
                return False
            left += 1
            right -= 1
        return True

    if is_palindrome(s):
        return 1
    else:
        return 2


Solution = create_solution(solution_function_name)