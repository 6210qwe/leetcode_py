"""
2344. 使数组可以被整除的最少删除次数 - 给你两个正整数数组 nums 和 numsDivide 。你可以从 nums 中删除任意数目的元素。 请你返回使 nums 中 最小 元素可以整除 numsDivide 中所有元素的 最少 删除次数。如果无法得到这样的元素，返回 -1 。 如果 y % x == 0 ，那么我们说整数 x 整除 y 。 示例 1： 输入：nums = [2,3,2,4,3], numsDivide = [9,6,9,3,15] 输出：2 解释： [2,3,2,4,3] 中最小元素是 2 ，它无法整除 numsDivide 中所有元素。 我们从 nums 中删除 2 个大小为 2 的元素，得到 nums = [3,4,3] 。 [3,4,3] 中最小元素为 3 ，它可以整除 numsDivide 中所有元素。 可以证明 2 是最少删除次数。 示例 2： 输入：nums = [4,3,6], numsDivide = [8,2,6,10] 输出：-1 解释： 我们想 nums 中的最小元素可以整除 numsDivide 中的所有元素。 没有任何办法可以达到这一目的。 提示： * 1 <= nums.length, numsDivide.length <= 105 * 1 <= nums[i], numsDivide[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用最大公约数 (GCD) 来找到 numsDivide 中所有元素的 GCD，然后在 nums 中找到能整除该 GCD 的最小元素。

算法步骤:
1. 计算 numsDivide 中所有元素的最大公约数 (GCD)。
2. 对 nums 进行排序。
3. 遍历排序后的 nums，找到第一个能整除 GCD 的元素。
4. 返回该元素在原数组中的索引作为删除次数，如果找不到则返回 -1。

关键点:
- 使用 GCD 来简化问题。
- 排序后遍历以找到最小的满足条件的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + m log k)，其中 n 是 nums 的长度，m 是 numsDivide 的长度，k 是 numsDivide 中的最大值。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import math

def min_deletions_to_make_array_divisible(nums: List[int], nums_divide: List[int]) -> int:
    """
    函数式接口 - 使数组可以被整除的最少删除次数
    """
    # 计算 numsDivide 中所有元素的最大公约数 (GCD)
    gcd = nums_divide[0]
    for num in nums_divide[1:]:
        gcd = math.gcd(gcd, num)
    
    # 对 nums 进行排序
    nums.sort()
    
    # 遍历排序后的 nums，找到第一个能整除 GCD 的元素
    for i, num in enumerate(nums):
        if gcd % num == 0:
            return i
    
    # 如果找不到则返回 -1
    return -1

