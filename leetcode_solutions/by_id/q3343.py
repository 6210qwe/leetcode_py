"""
核心思想: 使用排列组合来计算各位数字都不同的数字个数。

算法步骤:
1. 计算从 0 到 n 的所有数字中，各位数字都不同的数字个数。
2. 对于每一位数字，可以选择的数字是 10 个（0-9），但第一位不能为 0。
3. 使用排列组合公式计算每种情况下的不同数字个数。

关键点:
- 使用排列组合公式 A(n, k) = n! / (n-k)! 来计算不同数字的排列数。
- 考虑到第一位不能为 0，需要单独处理。
"""

