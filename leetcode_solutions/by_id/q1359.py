"""
1238. 循环码排列 - 给你两个整数 n 和 start。你的任务是返回任意 (0,1,2,,...,2^n-1) 的排列 p，并且满足： * p[0] = start * p[i] 和 p[i+1] 的二进制表示形式只有一位不同 * p[0] 和 p[2^n -1] 的二进制表示形式也只有一位不同 示例 1： 输入：n = 2, start = 3 输出：[3,2,0,1] 解释：这个排列的二进制表示是 (11,10,00,01) 所有的相邻元素都有一位是不同的，另一个有效的排列是 [3,1,0,2] 示例 2： 输入：n = 3, start = 2 输出：[2,6,7,5,4,0,1,3] 解释：这个排列的二进制表示是 (010,110,111,101,100,000,001,011) 提示： * 1 <= n <= 16 * 0 <= start < 2^n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用格雷码生成循环排列

算法步骤:
1. 生成长度为 2^n 的格雷码序列。
2. 将格雷码序列中的每个元素与 start 进行异或操作，得到最终的排列。

关键点:
- 格雷码的生成方法：从 0 到 2^n-1 的所有整数，通过 i ^ (i >> 1) 生成。
- 通过异或操作将格雷码序列转换为以 start 开头的序列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n)
空间复杂度: O(2^n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, start: int) -> List[int]:
    """
    函数式接口 - 生成以 start 开头的循环码排列
    """
    # 生成格雷码序列
    gray_code = [i ^ (i >> 1) for i in range(1 << n)]
    
    # 通过异或操作将格雷码序列转换为以 start 开头的序列
    result = [num ^ start for num in gray_code]
    
    return result


Solution = create_solution(solution_function_name)