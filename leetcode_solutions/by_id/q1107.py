# ============================================================================
# 问题描述
# ============================================================================
"""
给定一个二进制数组 data，你需要找到一个子数组，使得子数组中的所有 1 都连续在一起。返回最少需要交换的次数。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到包含最多 1 的子数组，然后计算需要交换的次数。

算法步骤:
1. 计算数组中 1 的总数 k。
2. 使用滑动窗口技术找到长度为 k 的子数组中包含最多 1 的子数组。
3. 计算该子数组中 0 的数量，即为需要交换的次数。

关键点:
- 滑动窗口的大小为 k（数组中 1 的总数）。
- 通过滑动窗口找到包含最多 1 的子数组。
- 交换次数等于该子数组中 0 的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_swaps(data: List[int]) -> int:
    """
    函数式接口 - 实现最少交换次数来组合所有的 1
    """
    # 计算数组中 1 的总数
    k = sum(data)
    
    # 初始化滑动窗口
    max_ones = ones_in_window = sum(data[:k])
    for i in range(k, len(data)):
        ones_in_window += data[i] - data[i - k]
        max_ones = max(max_ones, ones_in_window)
    
    # 计算需要交换的次数
    return k - max_ones


Solution = create_solution(min_swaps)