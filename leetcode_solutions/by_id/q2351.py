"""
2240. 买钢笔和铅笔的方案数 - 给你一个整数 total ，表示你拥有的总钱数。同时给你两个整数 cost1 和 cost2 ，分别表示一支钢笔和一支铅笔的价格。你可以花费你部分或者全部的钱，去买任意数目的两种笔。 请你返回购买钢笔和铅笔的 不同方案数目 。 示例 1： 输入：total = 20, cost1 = 10, cost2 = 5 输出：9 解释：一支钢笔的价格为 10 ，一支铅笔的价格为 5 。 - 如果你买 0 支钢笔，那么你可以买 0 ，1 ，2 ，3 或者 4 支铅笔。 - 如果你买 1 支钢笔，那么你可以买 0 ，1 或者 2 支铅笔。 - 如果你买 2 支钢笔，那么你没法买任何铅笔。 所以买钢笔和铅笔的总方案数为 5 + 3 + 1 = 9 种。 示例 2： 输入：total = 5, cost1 = 10, cost2 = 10 输出：1 解释：钢笔和铅笔的价格都为 10 ，都比拥有的钱数多，所以你没法购买任何文具。所以只有 1 种方案：买 0 支钢笔和 0 支铅笔。 提示： * 1 <= total, cost1, cost2 <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过枚举购买钢笔的数量，计算剩余的钱可以购买多少支铅笔，从而得到总的方案数。

算法步骤:
1. 初始化方案数为 0。
2. 枚举购买钢笔的数量 i，从 0 到 total // cost1。
3. 对于每个 i，计算剩余的钱数 remaining = total - i * cost1。
4. 计算可以购买铅笔的最大数量 j = remaining // cost2。
5. 将 j + 1（包括不买铅笔的情况）加到方案数中。
6. 返回总方案数。

关键点:
- 通过枚举钢笔的数量，避免了双重循环，提高了效率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(total // cost1)，其中 total 是总钱数，cost1 是钢笔的价格。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(total: int, cost1: int, cost2: int) -> int:
    """
    函数式接口 - 计算购买钢笔和铅笔的不同方案数
    """
    # 初始化方案数
    ways = 0
    
    # 枚举购买钢笔的数量
    for i in range(total // cost1 + 1):
        remaining = total - i * cost1
        # 计算可以购买铅笔的最大数量
        j = remaining // cost2
        # 将 j + 1 加到方案数中
        ways += j + 1
    
    return ways


Solution = create_solution(solution_function_name)