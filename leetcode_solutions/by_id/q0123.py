from typing import List
from leetcode_solutions.utils.solution import create_solution


def best_time_to_buy_and_sell_stock_iii(prices: List[int]) -> int:
    """
    函数式接口 - 买卖股票的最佳时机III
    
    实现思路:
    动态规划，最多完成两笔交易，使用四个状态：第一次买入、第一次卖出、第二次买入、第二次卖出。
    
    Args:
        prices: 股票价格数组
        
    Returns:
        最大利润
        
    Example:
        >>> best_time_to_buy_and_sell_stock_iii([3,3,5,0,0,3,1,4])
        6
    """
    buy1 = buy2 = float('-inf')
    sell1 = sell2 = 0
    
    for price in prices:
        buy1 = max(buy1, -price)
        sell1 = max(sell1, buy1 + price)
        buy2 = max(buy2, sell1 - price)
        sell2 = max(sell2, buy2 + price)
    
    return sell2


