"""
面试题 17.04. 消失的数字 - 数组nums包含从0到n的所有整数，但其中缺了一个。请编写代码找出那个缺失的整数。你有办法在O(n)时间内完成吗？ 注意：本题相对书上原题稍作改动 示例 1： 输入：[3,0,1] 输出：2 示例 2： 输入：[9,6,4,2,3,5,7,0,1] 输出：8
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用异或操作来找到缺失的数字

算法步骤:
1. 初始化一个变量 `missing` 为 0。
2. 遍历数组 `nums`，对每个元素和其索引进行异或操作，并将结果与 `missing` 进行异或操作。
3. 最后，`missing` 将是缺失的数字。

关键点:
- 异或操作的性质：a ^ a = 0 和 a ^ 0 = a
- 通过遍历数组并将其索引和值进行异或操作，可以消除所有出现的数字，剩下的就是缺失的数字
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找出缺失的数字
    """
    missing = len(nums)
    for i, num in enumerate(nums):
        missing ^= i ^ num
    return missing


Solution = create_solution(solution_function_name)