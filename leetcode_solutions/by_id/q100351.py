# ============================================================================
# 问题描述
# ============================================================================
"""
面试题 16.10. 生存人数 - 给定 N 个人的出生年份和死亡年份，第 i 个人的出生年份为 birth[i]，死亡年份为 death[i]，实现一个方法以计算生存人数最多的年份。 你可以假设所有人都出生于 1900 年至 2000 年（含 1900 和 2000 ）之间。如果一个人在某一年的任意时期处于生存状态，那么他应该被纳入那一年的统计中。例如，生于 1908 年、死于 1909 年的人应当被列入 1908 年和 1909 年的计数。 如果有多个年份生存人数相同且均为最大值，输出其中最小的年份。 示例： 输入： birth = [1900, 1901, 1950] death = [1948, 1951, 2000] 输出： 1901 提示： * 0 < birth.length == death.length <= 10000 * birth[i] <= death[i]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用差分数组来记录每年的人口变化，然后通过前缀和计算每年的生存人数。

算法步骤:
1. 初始化一个长度为101的差分数组，用于记录从1900年到2000年的每年人口变化。
2. 遍历每个人的出生年份和死亡年份，更新差分数组。
3. 计算前缀和，得到每年的实际生存人数。
4. 找出生存人数最多的年份。

关键点:
- 使用差分数组可以高效地记录每年的人口变化。
- 前缀和可以快速计算每年的生存人数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 birth 和 death 数组的长度，m 是年份范围（101）。
空间复杂度: O(m)，需要一个长度为101的差分数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_living_year(birth: List[int], death: List[int]) -> int:
    """
    函数式接口 - 计算生存人数最多的年份
    """
    # 初始化差分数组
    diff = [0] * 101
    
    # 更新差分数组
    for b in birth:
        diff[b - 1900] += 1
    for d in death:
        diff[d - 1900 + 1] -= 1
    
    # 计算前缀和
    population = 0
    max_population = 0
    max_year = 1900
    for i in range(101):
        population += diff[i]
        if population > max_population:
            max_population = population
            max_year = 1900 + i
    
    return max_year


Solution = create_solution(max_living_year)