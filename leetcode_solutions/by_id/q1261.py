"""
1156. 单字符重复子串的最大长度 - 如果字符串中的所有字符都相同，那么这个字符串是单字符重复的字符串。 给你一个字符串 text，你只能交换其中两个字符一次或者什么都不做，然后得到一些单字符重复的子串。返回其中最长的子串的长度。 示例 1： 输入：text = "ababa" 输出：3 示例 2： 输入：text = "aaabaaa" 输出：6 示例 3： 输入：text = "aaabbaaa" 输出：4 示例 4： 输入：text = "aaaaa" 输出：5 示例 5： 输入：text = "abcdef" 输出：1 提示： * 1 <= text.length <= 20000 * text 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和计数器来找到最长的单字符重复子串，并考虑一次交换的可能性。

算法步骤:
1. 初始化一个计数器 `count` 来记录每个字符的出现次数。
2. 使用滑动窗口遍历字符串，维护当前窗口内的字符和其最大连续长度。
3. 对于每个字符，计算其在窗口内的最大连续长度，并检查是否可以通过一次交换来增加长度。
4. 更新结果为当前窗口内最大连续长度的最大值。

关键点:
- 使用滑动窗口来高效地找到最长的单字符重复子串。
- 考虑一次交换的可能性来增加子串的长度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)（因为只有小写字母，所以计数器的空间是固定的）
"""

# ============================================================================
