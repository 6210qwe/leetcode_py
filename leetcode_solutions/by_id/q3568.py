"""
3270. 求出数字答案 - 给你三个 正 整数 num1 ，num2 和 num3 。 数字 num1 ，num2 和 num3 的数字答案 key 是一个四位数，定义如下： * 一开始，如果有数字 少于 四位数，给它补 前导 0 。 * 答案 key 的第 i 个数位（1 <= i <= 4）为 num1 ，num2 和 num3 第 i 个数位中的 最小 值。 请你返回三个数字 没有 前导 0 的数字答案。 示例 1： 输入：num1 = 1, num2 = 10, num3 = 1000 输出：0 解释： 补前导 0 后，num1 变为 "0001" ，num2 变为 "0010" ，num3 保持不变，为 "1000" 。 * 数字答案 key 的第 1 个数位为 min(0, 0, 1) 。 * 数字答案 key 的第 2 个数位为 min(0, 0, 0) 。 * 数字答案 key 的第 3 个数位为 min(0, 1, 0) 。 * 数字答案 key 的第 4 个数位为 min(1, 0, 0) 。 所以数字答案为 "0000" ，也就是 0 。 示例 2： 输入： num1 = 987, num2 = 879, num3 = 798 输出：777 示例 3： 输入：num1 = 1, num2 = 2, num3 = 3 输出：1 提示： * 1 <= num1, num2, num3 <= 9999
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将每个数字转换为四位字符串，然后逐位比较并取最小值。

算法步骤:
1. 将 num1, num2, num3 转换为四位字符串，不足四位的前面补零。
2. 初始化一个空字符串 result 用于存储结果。
3. 对每一位进行比较，取最小值并添加到 result 中。
4. 将 result 转换为整数并返回。

关键点:
- 使用 zfill(4) 方法将数字转换为四位字符串。
- 逐位比较并取最小值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 固定的四位数操作。
空间复杂度: O(1) - 使用常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num1: int, num2: int, num3: int) -> int:
    """
    函数式接口 - 返回三个正整数 num1, num2, num3 的数字答案 key。
    """
    # 将数字转换为四位字符串
    str_num1 = str(num1).zfill(4)
    str_num2 = str(num2).zfill(4)
    str_num3 = str(num3).zfill(4)

    # 初始化结果字符串
    result = ""

    # 逐位比较并取最小值
    for i in range(4):
        result += min(str_num1[i], str_num2[i], str_num3[i])

    # 将结果字符串转换为整数并返回
    return int(result)


Solution = create_solution(solution_function_name)