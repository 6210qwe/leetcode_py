"""
1539. 第 k 个缺失的正整数 - 给你一个 严格升序排列 的正整数数组 arr 和一个整数 k 。 请你找到这个数组里第 k 个缺失的正整数。 示例 1： 输入：arr = [2,3,4,7,11], k = 5 输出：9 解释：缺失的正整数包括 [1,5,6,8,9,10,12,13,...] 。第 5 个缺失的正整数为 9 。 示例 2： 输入：arr = [1,2,3,4], k = 2 输出：6 解释：缺失的正整数包括 [5,6,7,...] 。第 2 个缺失的正整数为 6 。 提示： * 1 <= arr.length <= 1000 * 1 <= arr[i] <= 1000 * 1 <= k <= 1000 * 对于所有 1 <= i < j <= arr.length 的 i 和 j 满足 arr[i] < arr[j] 进阶： 你可以设计一个时间复杂度小于 O(n) 的算法解决此问题吗？
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到第 k 个缺失的正整数。

算法步骤:
1. 初始化左右指针 left 和 right，分别指向 0 和 len(arr) - 1。
2. 在二分查找的过程中，计算中间位置 mid，并计算从 1 到 arr[mid] 之间的缺失正整数的数量。
3. 如果缺失的正整数数量小于 k，则说明第 k 个缺失的正整数在 mid 右侧，更新 left。
4. 否则，更新 right。
5. 最后，根据 left 的位置计算出第 k 个缺失的正整数。

关键点:
- 使用二分查找来减少查找范围，提高效率。
- 计算缺失的正整数数量时，使用公式 (arr[mid] - 1) - mid。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_kth_missing_positive(arr: List[int], k: int) -> int:
    """
    函数式接口 - 找到第 k 个缺失的正整数
    """
    left, right = 0, len(arr) - 1
    
    while left <= right:
        mid = (left + right) // 2
        missing = arr[mid] - mid - 1
        
        if missing < k:
            left = mid + 1
        else:
            right = mid - 1
    
    # 计算第 k 个缺失的正整数
    return left + k


Solution = create_solution(find_kth_missing_positive)