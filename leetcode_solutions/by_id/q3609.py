def solution_function_name(params):
    """
    函数式接口 - 实现最优解法
    """
    # SQL 查询实现
    query = """
    SELECT 
        product_id, 
        CASE 
            WHEN min_discounted_price IS NOT NULL THEN price * (1 - min_discounted_price) 
            ELSE price 
        END AS final_price
    FROM (
        SELECT 
            product_id, 
            price, 
            MIN(discount) OVER (PARTITION BY product_id) AS min_discounted_price
        FROM products
    ) AS subquery
    """
    return query

