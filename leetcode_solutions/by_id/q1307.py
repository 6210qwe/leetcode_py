import math

def gcd(x, y):
    while y:
        x, y = y, x % y
    return x

def lcm(x, y):
    return x * y // gcd(x, y)

def count_ugly(num, a, b, c):
    ab = lcm(a, b)
    ac = lcm(a, c)
    bc = lcm(b, c)
    abc = lcm(ab, c)
    return (num // a + num // b + num // c 
            - num // ab - num // ac - num // bc 
            + num // abc)

def nthUglyNumber(n: int, a: int, b: int, c: int) -> int:
    left, right = 1, 2 * 10**9
    while left < right:
        mid = (left + right) // 2
        if count_ugly(mid, a, b, c) < n:
            left = mid + 1
        else:
            right = mid
    return left

Solution = create_solution(nthUglyNumber)