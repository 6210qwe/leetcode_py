def string_compression(chars: List[str]) -> int:
    """
    函数式接口 - 压缩字符数组
    
    实现思路:
    使用双指针方法遍历字符数组，并在原地修改数组以实现压缩。
    
    Args:
        chars (List[str]): 输入的字符数组
        
    Returns:
        int: 压缩后的字符数组的新长度
        
    Example:
        >>> string_compression(["a","a","b","b","c","c","c"])
        6
    """
    n = len(chars)
    if n == 0:
        return 0

    write = 0
    read = 0

    while read < n:
        current_char = chars[read]
        count = 0

        # 计算当前字符的连续出现次数
        while read < n and chars[read] == current_char:
            read += 1
            count += 1

        # 写入当前字符
        chars[write] = current_char
        write += 1

        # 如果当前字符的连续出现次数大于1，则写入计数
        if count > 1:
            for digit in str(count):
                chars[write] = digit
                write += 1

    return write


