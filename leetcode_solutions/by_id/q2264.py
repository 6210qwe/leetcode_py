"""
2160. 拆分数位后四位数字的最小和 - 给你一个四位 正 整数 num 。请你使用 num 中的 数位 ，将 num 拆成两个新的整数 new1 和 new2 。new1 和 new2 中可以有 前导 0 ，且 num 中 所有 数位都必须使用。 * 比方说，给你 num = 2932 ，你拥有的数位包括：两个 2 ，一个 9 和一个 3 。一些可能的 [new1, new2] 数对为 [22, 93]，[23, 92]，[223, 9] 和 [2, 329] 。 请你返回可以得到的 new1 和 new2 的 最小 和。 示例 1： 输入：num = 2932 输出：52 解释：可行的 [new1, new2] 数对为 [29, 23] ，[223, 9] 等等。 最小和为数对 [29, 23] 的和：29 + 23 = 52 。 示例 2： 输入：num = 4009 输出：13 解释：可行的 [new1, new2] 数对为 [0, 49] ，[490, 0] 等等。 最小和为数对 [4, 9] 的和：4 + 9 = 13 。 提示： * 1000 <= num <= 9999
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将四位数的每一位拆分出来，然后将这些数字从小到大排序。将最小的两位数组合成一个数，将最大的两位数组合成另一个数，这样可以保证两个数的和最小。

算法步骤:
1. 将 num 转换为字符串，以便逐位处理。
2. 将字符串中的每一位字符转换为整数，并存储在一个列表中。
3. 对列表进行排序。
4. 从排序后的列表中取出前两位和后两位，分别组合成两个新的整数。
5. 返回这两个新整数的和。

关键点:
- 通过排序确保最小的两位数和最大的两位数组合在一起，从而使得总和最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 因为输入固定为四位数，所以排序操作的时间复杂度是常数级别的。
空间复杂度: O(1) - 只使用了常数级别的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_sum(num: int) -> int:
    """
    函数式接口 - 计算四位数 num 拆分成两个新整数的最小和
    """
    # 将 num 转换为字符串，以便逐位处理
    digits = [int(digit) for digit in str(num)]
    
    # 对数字进行排序
    digits.sort()
    
    # 组合成两个新的整数
    new1 = digits[0] * 10 + digits[2]
    new2 = digits[1] * 10 + digits[3]
    
    # 返回两个新整数的和
    return new1 + new2


Solution = create_solution(minimum_sum)