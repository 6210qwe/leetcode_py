"""
LCR 169. 招式拆解 II - 某套连招动作记作仅由小写字母组成的序列 arr，其中 arr[i] 第 i 个招式的名字。请返回第一个只出现一次的招式名称，如不存在请返回空格。 示例 1： 输入：arr = "abbccdeff" 输出：'a' 示例 2： 输入：arr = "ccdd" 输出：' ' 限制： 0 <= arr.length <= 50000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个字符的出现次数，然后再次遍历字符串找到第一个出现一次的字符。

算法步骤:
1. 遍历字符串，使用哈希表记录每个字符的出现次数。
2. 再次遍历字符串，找到第一个出现次数为1的字符并返回。
3. 如果没有找到这样的字符，返回空格。

关键点:
- 使用哈希表可以在 O(1) 时间内完成字符出现次数的记录和查询。
- 两次遍历字符串可以确保找到第一个只出现一次的字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。需要两次遍历字符串。
空间复杂度: O(1)，因为哈希表的大小最多为 26（小写字母的数量）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: str) -> str:
    """
    函数式接口 - 返回第一个只出现一次的字符
    """
    # 使用哈希表记录每个字符的出现次数
    char_count = {}
    for char in arr:
        if char in char_count:
            char_count[char] += 1
        else:
            char_count[char] = 1
    
    # 找到第一个出现次数为1的字符
    for char in arr:
        if char_count[char] == 1:
            return char
    
    # 如果没有找到这样的字符，返回空格
    return ' '


Solution = create_solution(solution_function_name)