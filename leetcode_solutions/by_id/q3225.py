# ============================================================================
# 问题描述
# ============================================================================
"""
2958. 最多 K 个重复元素的最长子数组 - 给你一个整数数组 nums 和一个整数 k 。 一个元素 x 在数组中的 频率 指的是它在数组中的出现次数。 如果一个数组中所有元素的频率都 小于等于 k ，那么我们称这个数组是 好 数组。 请你返回 nums 中 最长好 子数组的长度。 子数组 指的是一个数组中一段连续非空的元素序列。 示例 1： 输入：nums = [1,2,3,1,2,3,1,2], k = 2 输出：6 解释：最长好子数组是 [1,2,3,1,2,3] ，值 1 ，2 和 3 在子数组中的频率都没有超过 k = 2 。[2,3,1,2,3,1] 和 [3,1,2,3,1,2] 也是好子数组。 最长好子数组的长度为 6 。 示例 2： 输入：nums = [1,2,1,2,1,2,1,2], k = 1 输出：2 解释：最长好子数组是 [1,2] ，值 1 和 2 在子数组中的频率都没有超过 k = 1 。[2,1] 也是好子数组。 最长好子数组的长度为 2 。 示例 3： 输入：nums = [5,5,5,5,5,5,5], k = 4 输出：4 解释：最长好子数组是 [5,5,5,5] ，值 5 在子数组中的频率没有超过 k = 4 。 最长好子数组的长度为 4 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109 * 1 <= k <= nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到满足条件的最长子数组。

算法步骤:
1. 初始化两个指针 left 和 right，分别表示滑动窗口的左右边界。
2. 使用一个哈希表 freq 来记录当前窗口内每个元素的频率。
3. 移动右指针 right，将新元素加入窗口，并更新其频率。
4. 如果某个元素的频率超过了 k，则移动左指针 left，直到该元素的频率不超过 k。
5. 记录每次满足条件时的窗口长度，并更新最大长度。

关键点:
- 使用滑动窗口技术，确保窗口内的所有元素频率不超过 k。
- 通过哈希表记录频率，快速判断和更新频率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 nums 的长度。每个元素最多被访问两次（一次通过右指针，一次通过左指针）。
空间复杂度: O(m)，其中 m 是数组 nums 中不同元素的数量。哈希表的空间取决于不同元素的数量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 返回 nums 中最长好子数组的长度
    """
    left, right = 0, 0
    freq = {}
    max_length = 0
    
    while right < len(nums):
        # 更新右指针对应元素的频率
        if nums[right] in freq:
            freq[nums[right]] += 1
        else:
            freq[nums[right]] = 1
        
        # 如果当前窗口不满足条件，移动左指针
        while freq[nums[right]] > k:
            freq[nums[left]] -= 1
            left += 1
        
        # 更新最大长度
        max_length = max(max_length, right - left + 1)
        right += 1
    
    return max_length


Solution = create_solution(solution_function_name)