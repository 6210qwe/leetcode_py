from typing import List

def is_prime(n: int) -> bool:
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True

def find_prime_pairs(n: int) -> List[List[int]]:
    primes = [i for i in range(2, n + 1) if is_prime(i)]
    result = []
    for x in primes:
        y = n - x
        if y < x:
            break
        if y in primes:
            result.append([x, y])
    return result

