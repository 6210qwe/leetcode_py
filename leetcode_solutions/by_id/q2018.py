"""
1889. 装包裹的最小浪费空间 - 给你 n 个包裹，你需要把它们装在箱子里，每个箱子装一个包裹。总共有 m 个供应商提供 不同尺寸 的箱子（每个规格都有无数个箱子）。如果一个包裹的尺寸 小于等于 一个箱子的尺寸，那么这个包裹就可以放入这个箱子之中。 包裹的尺寸用一个整数数组 packages 表示，其中 packages[i] 是第 i 个包裹的尺寸。供应商用二维数组 boxes 表示，其中 boxes[j] 是第 j 个供应商提供的所有箱子尺寸的数组。 你想要选择 一个供应商 并只使用该供应商提供的箱子，使得 总浪费空间最小 。对于每个装了包裹的箱子，我们定义 浪费的 空间等于 箱子的尺寸 - 包裹的尺寸 。总浪费空间 为 所有 箱子中浪费空间的总和。 * 比方说，如果你想要用尺寸数组为 [4,8] 的箱子装下尺寸为 [2,3,5] 的包裹，你可以将尺寸为 2 和 3 的两个包裹装入两个尺寸为 4 的箱子中，同时把尺寸为 5 的包裹装入尺寸为 8 的箱子中。总浪费空间为 (4-2) + (4-3) + (8-5) = 6 。 请你选择 最优 箱子供应商，使得 总浪费空间最小 。如果 无法 将所有包裹放入箱子中，请你返回 -1 。由于答案可能会 很大 ，请返回它对 109 + 7 取余 的结果。 示例 1： 输入：packages = [2,3,5], boxes = [[4,8],[2,8]] 输出：6 解释：选择第一个供应商最优，用两个尺寸为 4 的箱子和一个尺寸为 8 的箱子。 总浪费空间为 (4-2) + (4-3) + (8-5) = 6 。 示例 2： 输入：packages = [2,3,5], boxes = [[1,4],[2,3],[3,4]] 输出：-1 解释：没有箱子能装下尺寸为 5 的包裹。 示例 3： 输入：packages = [3,5,8,10,11,12], boxes = [[12],[11,9],[10,5,14]] 输出：9 解释：选择第三个供应商最优，用两个尺寸为 5 的箱子，两个尺寸为 10 的箱子和两个尺寸为 14 的箱子。 总浪费空间为 (5-3) + (5-5) + (10-8) + (10-10) + (14-11) + (14-12) = 9 。 提示： * n == packages.length * m == boxes.length * 1 <= n <= 105 * 1 <= m <= 105 * 1 <= packages[i] <= 105 * 1 <= boxes[j].length <= 105 * 1 <= boxes[j][k] <= 105 * sum(boxes[j].length) <= 105 * boxes[j] 中的元素 互不相同 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找和前缀和来计算每个供应商的最小浪费空间。

算法步骤:
1. 对包裹进行排序。
2. 计算包裹的前缀和。
3. 对每个供应商的箱子进行排序。
4. 使用二分查找找到每个箱子可以容纳的最大包裹索引。
5. 计算每个供应商的总浪费空间，并记录最小值。

关键点:
- 使用前缀和快速计算某个范围内的包裹总和。
- 使用二分查找快速找到每个箱子可以容纳的最大包裹索引。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + m * k log n)，其中 n 是包裹数量，m 是供应商数量，k 是每个供应商箱子的数量。
空间复杂度: O(n)，用于存储前缀和数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import bisect

def min_wasted_space(packages: List[int], boxes: List[List[int]]) -> int:
    MOD = 10**9 + 7
    n = len(packages)
    
    # 对包裹进行排序
    packages.sort()
    
    # 计算包裹的前缀和
    prefix_sum = [0] * (n + 1)
    for i in range(n):
        prefix_sum[i + 1] = prefix_sum[i] + packages[i]
    
    min_waste = float('inf')
    
    for box_sizes in boxes:
        box_sizes.sort()
        
        if box_sizes[-1] < packages[-1]:
            continue  # 如果最大的箱子也无法装下最大的包裹，则跳过
        
        waste = 0
        prev_index = 0
        
        for box_size in box_sizes:
            # 使用二分查找找到当前箱子可以容纳的最大包裹索引
            index = bisect.bisect_right(packages, box_size)
            if index > prev_index:
                total_package_size = prefix_sum[index] - prefix_sum[prev_index]
                waste += (index - prev_index) * box_size - total_package_size
                prev_index = index
        
        min_waste = min(min_waste, waste)
    
    return min_waste % MOD if min_waste != float('inf') else -1

