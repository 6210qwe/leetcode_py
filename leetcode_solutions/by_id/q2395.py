"""
2311. 小于等于 K 的最长二进制子序列 - 给你一个二进制字符串 s 和一个正整数 k 。 请你返回 s 的 最长 子序列的长度，且该子序列对应的 二进制 数字小于等于 k 。 注意： * 子序列可以有 前导 0 。 * 空字符串视为 0 。 * 子序列 是指从一个字符串中删除零个或者多个字符后，不改变顺序得到的剩余字符序列。 示例 1： 输入：s = "1001010", k = 5 输出：5 解释：s 中小于等于 5 的最长子序列是 "00010" ，对应的十进制数字是 2 。 注意 "00100" 和 "00101" 也是可行的最长子序列，十进制分别对应 4 和 5 。 最长子序列的长度为 5 ，所以返回 5 。 示例 2： 输入：s = "00101001", k = 1 输出：6 解释："000001" 是 s 中小于等于 1 的最长子序列，对应的十进制数字是 1 。 最长子序列的长度为 6 ，所以返回 6 。 提示： * 1 <= s.length <= 1000 * s[i] 要么是 '0' ，要么是 '1' 。 * 1 <= k <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和位运算来找到最长的二进制子序列。

算法步骤:
1. 初始化结果变量 `result` 为 0。
2. 从右到左遍历字符串 `s`，使用变量 `current_value` 来记录当前子序列的值。
3. 如果当前字符是 '0'，直接将其计入结果 `result`。
4. 如果当前字符是 '1'，检查将其加入 `current_value` 后是否仍然小于等于 `k`。如果是，则将其计入结果 `result` 并更新 `current_value`。
5. 返回结果 `result`。

关键点:
- 从右到左遍历字符串，确保我们总是先处理低位。
- 使用位运算来高效地更新 `current_value`。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

