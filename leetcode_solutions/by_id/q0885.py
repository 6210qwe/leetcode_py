"""
855. 考场就座 - 在考场里，有 n 个座位排成一行，编号为 0 到 n - 1。 当学生进入考场后，他必须坐在离最近的人最远的座位上。如果有多个这样的座位，他会坐在编号最小的座位上。(另外，如果考场里没有人，那么学生就坐在 0 号座位上。) 设计一个模拟所述考场的类。 实现 ExamRoom 类： * ExamRoom(int n) 用座位的数量 n 初始化考场对象。 * int seat() 返回下一个学生将会入座的座位编号。 * void leave(int p) 指定坐在座位 p 的学生将离开教室。保证座位 p 上会有一位学生。 示例 1： 输入： ["ExamRoom", "seat", "seat", "seat", "seat", "leave", "seat"] [[10], [], [], [], [], [4], []] 输出： [null, 0, 9, 4, 2, null, 5] 解释： ExamRoom examRoom = new ExamRoom(10); examRoom.seat(); // 返回 0，房间里没有人，学生坐在 0 号座位。 examRoom.seat(); // 返回 9，学生最后坐在 9 号座位。 examRoom.seat(); // 返回 4，学生最后坐在 4 号座位。 examRoom.seat(); // 返回 2，学生最后坐在 2 号座位。 examRoom.leave(4); examRoom.seat(); // 返回 5，学生最后坐在 5 号座位。 提示： 1. 1 <= n <= 109 2. 保证有学生正坐在座位 p 上。 3. seat 和 leave 最多被调用 104 次。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用有序集合来维护当前所有学生的座位，并使用优先队列来管理空闲区间。

算法步骤:
1. 初始化时，创建一个有序集合 `seats` 来存储当前所有学生的座位。
2. `seat` 方法中，首先检查是否有学生在考场中：
   - 如果没有学生，直接返回 0。
   - 如果有学生，计算每个相邻座位之间的最大距离，并选择距离最大的位置。
3. `leave` 方法中，从有序集合中移除指定的学生座位，并更新优先队列中的空闲区间。

关键点:
- 使用有序集合来高效地插入和删除学生座位。
- 使用优先队列来管理空闲区间，确保每次都能快速找到距离最大的位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 有序集合的插入和删除操作。
空间复杂度: O(n) - 有序集合和优先队列的空间开销。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from sortedcontainers import SortedList
import heapq

class ExamRoom:

    def __init__(self, n: int):
        self.n = n
        self.seats = SortedList()
        self.pq = []

    def seat(self) -> int:
        if not self.seats:
            self.seats.add(0)
            return 0
        
        # 计算每个相邻座位之间的最大距离
        max_distance = self.seats[0]
        for i in range(len(self.seats) - 1):
            distance = (self.seats[i + 1] - self.seats[i]) // 2
            if distance > max_distance:
                max_distance = distance
                seat = (self.seats[i] + self.seats[i + 1]) // 2
        
        if self.n - 1 - self.seats[-1] > max_distance:
            seat = self.n - 1
        
        self.seats.add(seat)
        return seat

    def leave(self, p: int) -> None:
        self.seats.remove(p)

