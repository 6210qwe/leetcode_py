def solution_function_name(params):
    """
    函数式接口 - 实现
    """
    # SQL 查询实现最优解法
    query = """
    SELECT product_id, store, SUM(quantity) AS total_quantity
    FROM sales
    GROUP BY product_id, store
    ORDER BY product_id, store;
    """
    return query

