"""
1442. 形成两个异或相等数组的三元组数目 - 给你一个整数数组 arr 。 现需要从数组中取三个下标 i、j 和 k ，其中 (0 <= i < j <= k < arr.length) 。 a 和 b 定义如下： * a = arr[i] ^ arr[i + 1] ^ ... ^ arr[j - 1] * b = arr[j] ^ arr[j + 1] ^ ... ^ arr[k] 注意：^ 表示 按位异或 操作。 请返回能够令 a == b 成立的三元组 (i, j , k) 的数目。 示例 1： 输入：arr = [2,3,1,6,7] 输出：4 解释：满足题意的三元组分别是 (0,1,2), (0,2,2), (2,3,4) 以及 (2,4,4) 示例 2： 输入：arr = [1,1,1,1,1] 输出：10 示例 3： 输入：arr = [2,3] 输出：0 示例 4： 输入：arr = [1,3,5,7,9] 输出：3 示例 5： 输入：arr = [7,11,12,9,5,2,7,17,22] 输出：8 提示： * 1 <= arr.length <= 300 * 1 <= arr[i] <= 10^8
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀异或和来简化计算。

算法步骤:
1. 计算前缀异或和数组。
2. 使用哈希表记录每个前缀异或和出现的位置。
3. 遍历数组，对于每个位置 k，计算所有可能的 (i, j) 对，使得 a == b。

关键点:
- 前缀异或和可以快速计算任意子数组的异或值。
- 使用哈希表记录前缀异或和出现的位置，可以快速找到满足条件的 (i, j) 对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_triplets(arr: List[int]) -> int:
    """
    函数式接口 - 计算能够令 a == b 成立的三元组 (i, j, k) 的数目
    """
    n = len(arr)
    prefix_xor = [0] * (n + 1)
    
    # 计算前缀异或和
    for i in range(n):
        prefix_xor[i + 1] = prefix_xor[i] ^ arr[i]
    
    # 使用哈希表记录前缀异或和出现的位置
    xor_count = {}
    for i, xor in enumerate(prefix_xor):
        if xor not in xor_count:
            xor_count[xor] = []
        xor_count[xor].append(i)
    
    result = 0
    # 遍历数组，计算所有可能的 (i, j) 对
    for k in range(1, n + 1):
        for i in xor_count[prefix_xor[k]]:
            if i < k:
                result += k - i - 1
    
    return result


Solution = create_solution(count_triplets)