"""
2869. 收集元素的最少操作次数 - 给你一个正整数数组 nums 和一个整数 k 。 一次操作中，你可以将数组的最后一个元素删除，将该元素添加到一个集合中。 请你返回收集元素 1, 2, ..., k 需要的 最少操作次数 。 示例 1： 输入：nums = [3,1,5,4,2], k = 2 输出：4 解释：4 次操作后，集合中的元素依次添加了 2 ，4 ，5 和 1 。此时集合中包含元素 1 和 2 ，所以答案为 4 。 示例 2： 输入：nums = [3,1,5,4,2], k = 5 输出：5 解释：5 次操作后，集合中的元素依次添加了 2 ，4 ，5 ，1 和 3 。此时集合中包含元素 1 到 5 ，所以答案为 5 。 示例 3： 输入：nums = [3,2,5,3,1], k = 3 输出：4 解释：4 次操作后，集合中的元素依次添加了 1 ，3 ，5 和 2 。此时集合中包含元素 1 到 3 ，所以答案为 4 。 提示： * 1 <= nums.length <= 50 * 1 <= nums[i] <= nums.length * 1 <= k <= nums.length * 输入保证你可以收集到元素 1, 2, ..., k 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来跟踪已经收集到的元素，并从数组末尾开始遍历，直到收集到所有需要的元素。

算法步骤:
1. 初始化一个集合 `collected` 来存储已经收集到的元素。
2. 从数组的末尾开始向前遍历，每次将当前元素添加到集合中。
3. 如果集合中已经包含了所有需要的元素（即集合的大小等于 k），则返回操作次数。

关键点:
- 从数组末尾开始遍历可以确保操作次数最少。
- 使用集合来快速检查是否已经收集到所有需要的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。最坏情况下需要遍历整个数组。
空间复杂度: O(k)，用于存储已经收集到的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_collect_elements(nums: List[int], k: int) -> int:
    """
    函数式接口 - 返回收集元素 1, 2, ..., k 需要的最少操作次数
    """
    collected = set()
    operations = 0
    
    # 从数组末尾开始向前遍历
    for i in range(len(nums) - 1, -1, -1):
        if nums[i] <= k:
            collected.add(nums[i])
        operations += 1
        if len(collected) == k:
            break
    
    return operations


Solution = create_solution(min_operations_to_collect_elements)