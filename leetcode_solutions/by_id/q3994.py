"""
3663. 出现频率最低的数字 - 给你一个整数 n，找出在其十进制表示中出现频率 最低 的数字。如果多个数字的出现频率相同，则选择 最小 的那个数字。 以整数形式返回所选的数字。 数字 x 的出现频率是指它在 n 的十进制表示中的出现次数。 示例 1: 输入： n = 1553322 输出： 1 解释： 在 n 中，出现频率最低的数字是 1，它只出现了一次。所有其他数字都出现了两次。 示例 2: 输入： n = 723344511 输出： 2 解释： 在 n 中，出现频率最低的数字是 7、2 和 5，它们都只出现了一次。 提示: * 1 <= n <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个数字的出现频率，然后找到出现频率最低且最小的数字。

算法步骤:
1. 将整数 n 转换为字符串，以便逐位处理。
2. 使用哈希表统计每个数字的出现频率。
3. 遍历哈希表，找到出现频率最低且最小的数字。

关键点:
- 使用哈希表进行频率统计。
- 遍历哈希表时，同时考虑频率和数字大小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d)，其中 d 是 n 的十进制表示的位数。
空间复杂度: O(1)，因为哈希表的大小最多为 10（0 到 9）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_least_frequent_digit(n: int) -> int:
    """
    函数式接口 - 找出整数 n 十进制表示中出现频率最低的数字。
    """
    # 将整数 n 转换为字符串
    n_str = str(n)
    
    # 使用哈希表统计每个数字的出现频率
    frequency = [0] * 10
    for digit in n_str:
        frequency[int(digit)] += 1
    
    # 初始化最小频率和结果
    min_frequency = float('inf')
    result = None
    
    # 遍历哈希表，找到出现频率最低且最小的数字
    for digit in range(10):
        if frequency[digit] > 0 and frequency[digit] < min_frequency:
            min_frequency = frequency[digit]
            result = digit
    
    return result


Solution = create_solution(find_least_frequent_digit)