"""
3224. 使差值相等的最少数组改动次数 - 给你一个长度为 n 的整数数组 nums ，n 是 偶数 ，同时给你一个整数 k 。 你可以对数组进行一些操作。每次操作中，你可以将数组中 任一 元素替换为 0 到 k 之间的 任一 整数。 执行完所有操作以后，你需要确保最后得到的数组满足以下条件： * 存在一个整数 X ，满足对于所有的 (0 <= i < n) 都有 abs(a[i] - a[n - i - 1]) = X 。 请你返回满足以上条件 最少 修改次数。 示例 1： 输入：nums = [1,0,1,2,4,3], k = 4 输出：2 解释： 我们可以执行以下操作： * 将 nums[1] 变为 2 ，结果数组为 nums = [1,2,1,2,4,3] 。 * 将 nums[3] 变为 3 ，结果数组为 nums = [1,2,1,3,4,3] 。 整数 X 为 2 。 示例 2： 输入：nums = [0,1,2,3,3,6,5,4], k = 6 输出：2 解释： 我们可以执行以下操作： * 将 nums[3] 变为 0 ，结果数组为 nums = [0,1,2,0,3,6,5,4] 。 * 将 nums[4] 变为 4 ，结果数组为 nums = [0,1,2,0,4,6,5,4] 。 整数 X 为 4 。 提示： * 2 <= n == nums.length <= 105 * n 是偶数。 * 0 <= nums[i] <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针和贪心算法来找到最小的修改次数。

算法步骤:
1. 初始化两个指针 i 和 j 分别指向数组的开头和结尾。
2. 对于每个 i 和 j，计算它们之间的差值 diff，并记录在哈希表中。
3. 对于每个可能的差值 diff，计算需要的修改次数。
4. 返回最小的修改次数。

关键点:
- 使用哈希表记录每个差值出现的次数。
- 贪心地选择最小的修改次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_array_changes(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算使差值相等的最少数组改动次数
    """
    n = len(nums)
    diff_count = [0] * (k + 1)

    for i in range(n // 2):
        diff = abs(nums[i] - nums[n - 1 - i])
        diff_count[min(diff, k - diff)] += 1

    min_changes = float('inf')
    for diff, count in enumerate(diff_count):
        changes = n // 2 - count
        if changes < min_changes:
            min_changes = changes

    return min_changes


Solution = create_solution(min_array_changes)