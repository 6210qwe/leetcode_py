from typing import List
from leetcode_solutions.utils.solution import create_solution


def pascals_triangle_ii(rowIndex: int) -> List[int]:
    """
    函数式接口 - 返回杨辉三角的第rowIndex行
    
    实现思路:
    使用滚动数组优化空间，从后往前更新避免覆盖。
    
    Args:
        rowIndex: 行索引（从0开始）
        
    Returns:
        杨辉三角的第rowIndex行
        
    Example:
        >>> pascals_triangle_ii(3)
        [1, 3, 3, 1]
    """
    row = [1] * (rowIndex + 1)
    
    for i in range(2, rowIndex + 1):
        for j in range(i - 1, 0, -1):
            row[j] += row[j - 1]
    
    return row


