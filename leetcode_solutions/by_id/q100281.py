# ============================================================================
# 问题描述
# ============================================================================
"""
LCR 130. 衣橱整理 - 家居整理师将待整理衣橱划分为 m x n 的二维矩阵 grid，其中 grid[i][j] 代表一个需要整理的格子。整理师自 grid[0][0] 开始 逐行逐列 地整理每个格子。 整理规则为：在整理过程中，可以选择 向右移动一格 或 向下移动一格，但不能移动到衣柜之外。同时，不需要整理 digit(i) + digit(j) > cnt 的格子，其中 digit(x) 表示数字 x 的各数位之和。 请返回整理师 总共需要整理多少个格子。 示例 1： 输入：m = 4, n = 7, cnt = 5 输出：18 提示： * 1 <= n, m <= 100 * 0 <= cnt <= 20
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用广度优先搜索（BFS）来遍历所有可以整理的格子。

算法步骤:
1. 初始化一个队列，将起点 (0, 0) 加入队列。
2. 使用一个集合 visited 来记录已经访问过的格子，避免重复访问。
3. 定义一个辅助函数 `digit_sum` 来计算一个数的数位之和。
4. 从队列中取出一个格子 (i, j)，如果该格子满足条件且未被访问过，则将其加入 visited 集合，并将其四个方向上的相邻格子加入队列。
5. 重复步骤 4，直到队列为空。

关键点:
- 使用 BFS 可以确保按层遍历，避免重复访问。
- 使用集合 visited 来记录已访问的格子，避免重复访问。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，最坏情况下需要遍历整个矩阵。
空间复杂度: O(m * n)，最坏情况下需要存储整个矩阵的访问状态。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(m: int, n: int, k: int) -> int:
    """
    函数式接口 - 返回整理师总共需要整理的格子数
    """
    def digit_sum(x: int) -> int:
        """计算一个数的数位之和"""
        return sum(int(digit) for digit in str(x))

    # 初始化队列和访问集合
    queue = [(0, 0)]
    visited = set([(0, 0)])

    while queue:
        i, j = queue.pop(0)
        for di, dj in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            ni, nj = i + di, j + dj
            if 0 <= ni < m and 0 <= nj < n and (ni, nj) not in visited and digit_sum(ni) + digit_sum(nj) <= k:
                visited.add((ni, nj))
                queue.append((ni, nj))

    return len(visited)


Solution = create_solution(solution_function_name)