"""
121. 买卖股票的最佳时机 - 给定一个数组 prices ，它的第 i 个元素 prices[i] 表示一支给定股票第 i 天的价格。 你只能选择 某一天 买入这只股票，并选择在 未来的某一个不同的日子 卖出该股票。设计一个算法来计算你所能获取的最大利润。 返回你可以从这笔交易中获取的最大利润。如果你不能获取任何利润，返回 0 。 示例 1： 输入：[7,1,5,3,6,4] 输出：5 解释：在第 2 天（股票价格 = 1）的时候买入，在第 5 天（股票价格 = 6）的时候卖出，最大利润 = 6-1 = 5 。 注意利润不能是 7-1 = 6, 因为卖出价格需要大于买入价格；同时，你不能在买入前卖出股票。 示例 2： 输入：prices = [7,6,4,3,1] 输出：0 解释：在这种情况下, 没有交易完成, 所以最大利润为 0。 提示： * 1 <= prices.length <= 105 * 0 <= prices[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，记录最低买入价格和最大利润

算法步骤:
1. 初始化最低价格为第一个价格，最大利润为0
2. 遍历价格数组：
   - 更新最低买入价格
   - 计算当前卖出能获得的利润，更新最大利润
3. 返回最大利润

关键点:
- 一次遍历即可完成
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 只需遍历一次数组
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def best_time_to_buy_and_sell_stock(prices: List[int]) -> int:
    """
    函数式接口 - 计算买卖股票的最大利润
    
    实现思路:
    遍历数组，记录最低买入价格和最大利润。
    
    Args:
        prices: 股票价格数组
        
    Returns:
        最大利润
        
    Example:
        >>> best_time_to_buy_and_sell_stock([7, 1, 5, 3, 6, 4])
        5
    """
    if not prices:
        return 0
    
    min_price = prices[0]
    max_profit = 0
    
    for price in prices[1:]:
        min_price = min(min_price, price)
        max_profit = max(max_profit, price - min_price)
    
    return max_profit


