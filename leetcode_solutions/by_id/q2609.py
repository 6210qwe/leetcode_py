"""
2521. 数组乘积中的不同质因数数目 - 给你一个正整数数组 nums ，对 nums 所有元素求积之后，找出并返回乘积中 不同质因数 的数目。 注意： * 质数 是指大于 1 且仅能被 1 及自身整除的数字。 * 如果 val2 / val1 是一个整数，则整数 val1 是另一个整数 val2 的一个因数。 示例 1： 输入：nums = [2,4,3,7,10,6] 输出：4 解释： nums 中所有元素的乘积是：2 * 4 * 3 * 7 * 10 * 6 = 10080 = 25 * 32 * 5 * 7 。 共有 4 个不同的质因数，所以返回 4 。 示例 2： 输入：nums = [2,4,8,16] 输出：1 解释： nums 中所有元素的乘积是：2 * 4 * 8 * 16 = 1024 = 210 。 共有 1 个不同的质因数，所以返回 1 。 提示： * 1 <= nums.length <= 104 * 2 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过分解每个数的质因数，并使用集合来存储不同的质因数。

算法步骤:
1. 初始化一个空集合 `prime_factors` 来存储不同的质因数。
2. 遍历数组中的每个数，对其进行质因数分解。
3. 将分解得到的质因数加入集合 `prime_factors`。
4. 返回集合 `prime_factors` 的大小。

关键点:
- 使用集合来存储不同的质因数，确保每个质因数只被记录一次。
- 通过遍历每个数的质因数分解，确保所有质因数都被找到。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * sqrt(m))，其中 n 是数组的长度，m 是数组中的最大值。每个数的质因数分解的时间复杂度为 O(sqrt(m))。
空间复杂度: O(k)，其中 k 是不同的质因数的数量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def distinct_prime_factors(nums: List[int]) -> int:
    """
    函数式接口 - 返回数组乘积中的不同质因数数目
    """
    def prime_factors(n: int) -> set:
        """返回 n 的所有质因数"""
        factors = set()
        # 分解出所有的 2
        while n % 2 == 0:
            factors.add(2)
            n //= 2
        # 分解出所有的奇数质因数
        for i in range(3, int(n**0.5) + 1, 2):
            while n % i == 0:
                factors.add(i)
                n //= i
        # 如果 n 是一个大于 2 的质数
        if n > 2:
            factors.add(n)
        return factors

    # 使用集合来存储不同的质因数
    prime_factors_set = set()
    for num in nums:
        prime_factors_set.update(prime_factors(num))
    
    return len(prime_factors_set)


Solution = create_solution(distinct_prime_factors)