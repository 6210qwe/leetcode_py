def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询来统计每天的新用户数量。
    """
    # 实现最优解法
    query = """
    SELECT 
        DATE_FORMAT(register_date, '%Y-%m-%d') AS register_date,
        COUNT(user_id) AS user_count
    FROM 
        users
    GROUP BY 
        register_date
    ORDER BY 
        register_date;
    """
    return query


