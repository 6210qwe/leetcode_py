"""
852. 山脉数组的峰顶索引 - 给定一个长度为 n 的整数 山脉 数组 arr ，其中的值递增到一个 峰值元素 然后递减。 返回峰值元素的下标。 你必须设计并实现时间复杂度为 O(log(n)) 的解决方案。 示例 1： 输入：arr = [0,1,0] 输出：1 示例 2： 输入：arr = [0,2,1,0] 输出：1 示例 3： 输入：arr = [0,10,5,2] 输出：1 提示： * 3 <= arr.length <= 105 * 0 <= arr[i] <= 106 * 题目数据 保证 arr 是一个山脉数组
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到峰值元素的下标。

算法步骤:
1. 初始化左右指针 left 和 right，分别指向数组的起始和末尾。
2. 进行二分查找：
   - 计算中间位置 mid。
   - 如果 arr[mid] > arr[mid + 1]，说明峰值在左侧或就是 mid，更新 right = mid。
   - 否则，峰值在右侧，更新 left = mid + 1。
3. 当 left == right 时，left 即为峰值元素的下标。

关键点:
- 通过比较中间位置及其右侧的元素来确定峰值的位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log(n))
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def peak_index_in_mountain_array(arr: List[int]) -> int:
    """
    函数式接口 - 找到山脉数组的峰值元素的下标
    """
    left, right = 0, len(arr) - 1
    
    while left < right:
        mid = (left + right) // 2
        if arr[mid] > arr[mid + 1]:
            right = mid
        else:
            left = mid + 1
    
    return left


