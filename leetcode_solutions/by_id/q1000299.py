"""
LCR 046. 二叉树的右视图 - 给定一个二叉树的 根节点 root，想象自己站在它的右侧，按照从顶部到底部的顺序，返回从右侧所能看到的节点值。 示例 1： [https://assets.leetcode.com/uploads/2021/02/14/tree.jpg] 输入: [1,2,3,null,5,null,4] 输出: [1,3,4] 示例 2： 输入: [1,null,3] 输出: [1,3] 示例 3： 输入: [] 输出: [] 提示： * 二叉树的节点个数的范围是 [0,100] * -100 <= Node.val <= 100 注意：本题与主站 199 题相同：https://leetcode.cn/problems/binary-tree-right-side-view/ [https://leetcode.cn/problems/binary-tree-right-side-view/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用层次遍历（广度优先搜索）来获取每一层的最右侧节点。

算法步骤:
1. 初始化一个队列，将根节点加入队列。
2. 开始层次遍历：
   - 对于当前层的所有节点，依次处理它们的子节点。
   - 记录当前层的最后一个节点值。
3. 返回记录的所有最右侧节点值。

关键点:
- 使用队列进行层次遍历。
- 每次只记录当前层的最后一个节点值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数。每个节点只会被访问一次。
空间复杂度: O(n)，队列中最多会存储所有节点。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def right_side_view(root: Optional[TreeNode]) -> List[int]:
    """
    函数式接口 - 返回二叉树的右视图
    """
    if not root:
        return []

    result = []
    queue = [root]

    while queue:
        level_size = len(queue)
        for i in range(level_size):
            node = queue.pop(0)
            if i == level_size - 1:
                result.append(node.val)
            if node.left:
                queue.append(node.left)
            if node.right:
                queue.append(node.right)

    return result

Solution = create_solution(right_side_view)