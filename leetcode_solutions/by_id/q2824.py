"""
2729. 判断一个数是否迷人 - 给你一个三位数整数 n 。 如果经过以下修改得到的数字 恰好 包含数字 1 到 9 各一次且不包含任何 0 ，那么我们称数字 n 是 迷人的 ： * 将 n 与数字 2 * n 和 3 * n 连接 。 如果 n 是迷人的，返回 true，否则返回 false 。 连接 两个数字表示把它们首尾相接连在一起。比方说 121 和 371 连接得到 121371 。 示例 1： 输入：n = 192 输出：true 解释：我们将数字 n = 192 ，2 * n = 384 和 3 * n = 576 连接，得到 192384576 。这个数字包含 1 到 9 恰好各一次。 示例 2： 输入：n = 100 输出：false 解释：我们将数字 n = 100 ，2 * n = 200 和 3 * n = 300 连接，得到 100200300 。这个数字不符合上述条件。 提示： * 100 <= n <= 999
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将 n, 2 * n, 3 * n 连接成一个字符串，检查该字符串是否恰好包含 1 到 9 各一次且不包含 0。

算法步骤:
1. 计算 2 * n 和 3 * n。
2. 将 n, 2 * n, 3 * n 连接成一个字符串。
3. 检查该字符串是否恰好包含 1 到 9 各一次且不包含 0。

关键点:
- 使用集合来检查字符串中的字符是否满足条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 因为 n 的范围是固定的（100 到 999），所以操作次数是常数级的。
空间复杂度: O(1) - 使用的空间也是常数级的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_fascinating(n: int) -> bool:
    """
    函数式接口 - 判断一个数是否迷人
    """
    # 计算 2 * n 和 3 * n
    str_n = str(n)
    str_2n = str(2 * n)
    str_3n = str(3 * n)
    
    # 将 n, 2 * n, 3 * n 连接成一个字符串
    concatenated_str = str_n + str_2n + str_3n
    
    # 检查该字符串是否恰好包含 1 到 9 各一次且不包含 0
    return len(concatenated_str) == 9 and set(concatenated_str) == {'1', '2', '3', '4', '5', '6', '7', '8', '9'}


Solution = create_solution(is_fascinating)