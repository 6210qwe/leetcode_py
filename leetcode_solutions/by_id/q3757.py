import re

def solution_function_name(products: List[str]) -> List[str]:
    """
    函数式接口 - 查找具有三个连续数字的产品
    """
    # 使用正则表达式匹配具有三个连续数字的产品名称
    pattern = re.compile(r'\d{3}')
    result = [product for product in products if pattern.search(product)]
