# 问题描述
# ============================================================================
"""
1541. 平衡括号字符串的最少插入次数 - 给你一个括号字符串 s ，它只包含字符 '(' 和 ')' 。一个括号字符串被称为平衡的当它满足： * 任何左括号 '(' 必须对应两个连续的右括号 '))' 。 * 左括号 '(' 必须在对应的连续两个右括号 '))' 之前。 比方说 "())"， "())(())))" 和 "(())())))" 都是平衡的， ")()"， "()))" 和 "(()))" 都是不平衡的。 你可以在任意位置插入字符 '(' 和 ')' 使字符串平衡。 请你返回让 s 平衡的最少插入次数。 示例 1： 输入：s = "(()))" 输出：1 解释：第二个左括号有与之匹配的两个右括号，但是第一个左括号只有一个右括号。我们需要在字符串结尾额外增加一个 ')' 使字符串变成平衡字符串 "(())))" 。 示例 2： 输入：s = "())" 输出：0 解释：字符串已经平衡了。 示例 3： 输入：s = "))())(" 输出：3 解释：添加 '(' 去匹配最开头的 '))' ，然后添加 ''))' 去匹配最后一个 '(' 。 示例 4： 输入：s = "((((((" 输出：12 解释：添加 12 个 ')' 得到平衡字符串。 示例 5： 输入：s = ")))))))" 输出：5 解释：在字符串开头添加 4 个 '(' 并在结尾添加 1 个 ')' ，字符串变成平衡字符串 "(((())))))))" 。 提示： * 1 <= s.length <= 10^5 * s 只包含 '(' 和 ')' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈的思想和贪心算法来解决这个问题。

算法步骤:
1. 初始化需要插入的括号数 `insertions` 为 0，以及当前需要匹配的右括号数量 `right_needed` 为 0。
2. 遍历字符串 `s`：
   - 如果遇到左括号 `(`，则 `right_needed` 增加 2。
   - 如果遇到右括号 `)`，则 `right_needed` 减少 2。
   - 如果 `right_needed` 为负数，说明右括号多于左括号，需要插入左括号并更新 `insertions` 和 `right_needed`。
   - 如果 `right_needed` 为奇数，说明需要一个额外的右括号来匹配，更新 `insertions` 和 `right_needed`。
3. 最后，如果 `right_needed` 不为 0，说明还需要插入一些右括号来平衡字符串，更新 `insertions`。

关键点:
- 使用 `right_needed` 来跟踪当前需要匹配的右括号数量。
- 通过贪心算法确保每次插入最少的括号。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_insertions(s: str) -> int:
    """
    计算使括号字符串平衡的最少插入次数。
    """
    insertions = 0
    right_needed = 0
    
    for char in s:
        if char == '(':
            right_needed += 2
            # 如果 right_needed 为奇数，说明需要一个额外的右括号
            if right_needed % 2 == 1:
                insertions += 1
                right_needed -= 1
        else:  # char == ')'
            right_needed -= 1
            # 如果 right_needed 为负数，说明右括号多于左括号
            if right_needed < 0:
                insertions += 1
                right_needed = 1  # 重置 right_needed 为 1，因为需要一个左括号来匹配
            # 如果 right_needed 为奇数，说明需要一个额外的右括号
            if right_needed % 2 == 1:
                insertions += 1
                right_needed -= 1
    
    # 最后，如果 right_needed 不为 0，说明还需要插入一些右括号来平衡字符串
    insertions += right_needed
    
    return insertions


Solution = create_solution(min_insertions)