"""
128. 最长连续序列 - 给定一个未排序的整数数组 nums ，找出数字连续的最长序列（不要求序列元素在原数组中连续）的长度。 请你设计并实现时间复杂度为 O(n) 的算法解决此问题。 示例 1： 输入：nums = [100,4,200,1,3,2] 输出：4 解释：最长数字连续序列是 [1, 2, 3, 4]。它的长度为 4。 示例 2： 输入：nums = [0,3,7,2,5,8,4,6,0,1] 输出：9 示例 3： 输入：nums = [1,0,1,2] 输出：3 提示： * 0 <= nums.length <= 105 * -109 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合，只从序列的起始位置开始扩展

算法步骤:
1. 将所有数字放入集合
2. 遍历数组，如果num-1不在集合中，说明是序列起始位置
3. 从起始位置开始扩展，计算序列长度

关键点:
- 只从序列起始位置扩展，避免重复计算
- 时间复杂度O(n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 每个数字最多被访问两次
空间复杂度: O(n) - 哈希集合空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def longest_consecutive_sequence(nums: List[int]) -> int:
    """
    函数式接口 - 最长连续序列
    
    实现思路:
    使用哈希集合，只从序列的起始位置开始扩展。
    
    Args:
        nums: 未排序的整数数组
        
    Returns:
        数字连续的最长序列的长度
        
    Example:
        >>> longest_consecutive_sequence([100,4,200,1,3,2])
        4
    """
    if not nums:
        return 0
    
    num_set = set(nums)
    max_length = 0
    
    for num in num_set:
        # 只从序列起始位置开始扩展
        if num - 1 not in num_set:
            current_num = num
            current_length = 1
            
            while current_num + 1 in num_set:
                current_num += 1
                current_length += 1
            
            max_length = max(max_length, current_length)
    
    return max_length


