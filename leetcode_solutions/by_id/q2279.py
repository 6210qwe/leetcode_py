# 问题描述
# ============================================================================
"""
2178. 拆分成最多数目的正偶数之和 - 给你一个整数 finalSum 。请你将它拆分成若干个 互不相同 的正偶数之和，且拆分出来的正偶数数目 最多 。 * 比方说，给你 finalSum = 12 ，那么这些拆分是 符合要求 的（互不相同的正偶数且和为 finalSum）：(2 + 10) ，(2 + 4 + 6) 和 (4 + 8) 。它们中，(2 + 4 + 6) 包含最多数目的整数。注意 finalSum 不能拆分成 (2 + 2 + 4 + 4) ，因为拆分出来的整数必须互不相同。 请你返回一个整数数组，表示将整数拆分成 最多 数目的正偶数数组。如果没有办法将 finalSum 进行拆分，请你返回一个 空 数组。你可以按 任意 顺序返回这些整数。 示例 1： 输入：finalSum = 12 输出：[2,4,6] 解释：以下是一些符合要求的拆分：(2 + 10)，(2 + 4 + 6) 和 (4 + 8) 。 (2 + 4 + 6) 为最多数目的整数，数目为 3 ，所以我们返回 [2,4,6] 。 [2,6,4] ，[6,2,4] 等等也都是可行的解。 示例 2： 输入：finalSum = 7 输出：[] 解释：没有办法将 finalSum 进行拆分。 所以返回空数组。 示例 3： 输入：finalSum = 28 输出：[6,8,2,12] 解释：以下是一些符合要求的拆分：(2 + 26)，(6 + 8 + 2 + 12) 和 (4 + 24) 。 (6 + 8 + 2 + 12) 有最多数目的整数，数目为 4 ，所以我们返回 [6,8,2,12] 。 [10,2,4,12] ，[6,2,4,16] 等等也都是可行的解。 提示： * 1 <= finalSum <= 1010
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从最小的正偶数开始逐步累加，直到达到或超过 finalSum。

算法步骤:
1. 如果 finalSum 是奇数，直接返回空数组。
2. 初始化一个空列表 result 来存储结果。
3. 从 2 开始，逐步增加偶数，并将其加入 result 列表中，同时从 finalSum 中减去该偶数。
4. 当剩余的 finalSum 小于当前偶数时，将剩余的 finalSum 加到最后一个元素上。
5. 返回 result 列表。

关键点:
- 从最小的正偶数开始逐步累加，确保每次添加的偶数都是唯一的。
- 当剩余的 finalSum 小于当前偶数时，将剩余的 finalSum 加到最后一个元素上，确保最终结果的和等于 finalSum。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(sqrt(finalSum)) - 因为我们是从 2 开始逐步增加偶数，最坏情况下需要增加到 sqrt(finalSum)。
空间复杂度: O(sqrt(finalSum)) - 结果列表的长度最多为 sqrt(finalSum)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(final_sum: int) -> List[int]:
    """
    函数式接口 - 将 finalSum 拆分成最多数目的正偶数之和
    """
    if final_sum % 2 != 0:
        return []

    result = []
    current_even = 2

    while final_sum >= current_even:
        result.append(current_even)
        final_sum -= current_even
        current_even += 2

    # 如果 finalSum 还有剩余，加到最后一个元素上
    if final_sum > 0:
        result[-1] += final_sum

    return result


Solution = create_solution(solution_function_name)