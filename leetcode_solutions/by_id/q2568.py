"""
核心思想: 使用深度优先搜索 (DFS) 计算每个子树的代表数量，并计算所需的油耗。

算法步骤:
1. 构建邻接表表示树。
2. 使用 DFS 从首都开始遍历树，计算每个子树的代表数量。
3. 在回溯过程中，计算每个子树到其父节点的油耗，并累加总油耗。

关键点:
- 使用 DFS 计算每个子树的代表数量。
- 计算每个子树到其父节点的油耗时，考虑每辆车的座位数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是城市的数量。每个节点和边都只访问一次。
空间复杂度: O(n)，存储邻接表和递归调用栈的空间。
"""

