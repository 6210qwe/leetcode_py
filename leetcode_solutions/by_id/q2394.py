"""
2302. 统计得分小于 K 的子数组数目 - 一个数组的 分数 定义为数组之和 乘以 数组的长度。 * 比方说，[1, 2, 3, 4, 5] 的分数为 (1 + 2 + 3 + 4 + 5) * 5 = 75 。 给你一个正整数数组 nums 和一个整数 k ，请你返回 nums 中分数 严格小于 k 的 非空整数子数组数目。 子数组 是数组中的一个连续元素序列。 示例 1： 输入：nums = [2,1,4,3,5], k = 10 输出：6 解释： 有 6 个子数组的分数小于 10 ： - [2] 分数为 2 * 1 = 2 。 - [1] 分数为 1 * 1 = 1 。 - [4] 分数为 4 * 1 = 4 。 - [3] 分数为 3 * 1 = 3 。 - [5] 分数为 5 * 1 = 5 。 - [2,1] 分数为 (2 + 1) * 2 = 6 。 注意，子数组 [1,4] 和 [4,3,5] 不符合要求，因为它们的分数分别为 10 和 36，但我们要求子数组的分数严格小于 10 。 示例 2： 输入：nums = [1,1,1], k = 5 输出：5 解释： 除了 [1,1,1] 以外每个子数组分数都小于 5 。 [1,1,1] 分数为 (1 + 1 + 1) * 3 = 9 ，大于 5 。 所以总共有 5 个子数组得分小于 5 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 105 * 1 <= k <= 1015
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来维护一个子数组，使其分数严格小于 k。

算法步骤:
1. 初始化两个指针 left 和 right，分别指向当前子数组的起始和结束位置。
2. 初始化一个变量 total 来记录当前子数组的和。
3. 使用 right 指针遍历数组：
   - 将当前元素加入 total。
   - 如果当前子数组的分数大于等于 k，则移动 left 指针，直到子数组的分数小于 k。
   - 计算当前子数组的贡献，即 right - left + 1，并累加到结果中。

关键点:
- 使用滑动窗口来动态调整子数组的大小，确保其分数始终小于 k。
- 通过移动 left 指针来缩小子数组的范围，从而降低分数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个指针最多遍历一次数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_subarrays_with_score_less_than_k(nums: List[int], k: int) -> int:
    """
    函数式接口 - 统计得分小于 K 的子数组数目
    """
    n = len(nums)
    left = 0
    total = 0
    result = 0
    
    for right in range(n):
        total += nums[right]
        
        while (total * (right - left + 1)) >= k and left <= right:
            total -= nums[left]
            left += 1
        
        result += (right - left + 1)
    
    return result


Solution = create_solution(count_subarrays_with_score_less_than_k)