"""
2293. 极大极小游戏 - 给你一个下标从 0 开始的整数数组 nums ，其长度是 2 的幂。 对 nums 执行下述算法： 1. 设 n 等于 nums 的长度，如果 n == 1 ，终止 算法过程。否则，创建 一个新的整数数组 newNums ，新数组长度为 n / 2 ，下标从 0 开始。 2. 对于满足 0 <= i < n / 2 的每个 偶数 下标 i ，将 newNums[i] 赋值 为 min(nums[2 * i], nums[2 * i + 1]) 。 3. 对于满足 0 <= i < n / 2 的每个 奇数 下标 i ，将 newNums[i] 赋值 为 max(nums[2 * i], nums[2 * i + 1]) 。 4. 用 newNums 替换 nums 。 5. 从步骤 1 开始 重复 整个过程。 执行算法后，返回 nums 中剩下的那个数字。 示例 1： [https://assets.leetcode.com/uploads/2022/04/13/example1drawio-1.png] 输入：nums = [1,3,5,2,4,8,2,2] 输出：1 解释：重复执行算法会得到下述数组。 第一轮：nums = [1,5,4,2] 第二轮：nums = [1,4] 第三轮：nums = [1] 1 是最后剩下的那个数字，返回 1 。 示例 2： 输入：nums = [3] 输出：3 解释：3 就是最后剩下的数字，返回 3 。 提示： * 1 <= nums.length <= 1024 * 1 <= nums[i] <= 109 * nums.length 是 2 的幂
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过不断缩小数组的大小，最终找到最后一个元素。

算法步骤:
1. 检查数组长度是否为1，如果是则直接返回该元素。
2. 创建一个新的数组 newNums，长度为原数组的一半。
3. 遍历原数组，对于偶数下标取最小值，奇数下标取最大值，填充到 newNums。
4. 用 newNums 替换 nums，重复上述步骤直到数组长度为1。

关键点:
- 通过不断缩小数组的大小，最终找到最后一个元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度。每次操作将数组长度减半，总共需要进行 log n 次操作。
空间复杂度: O(1)，因为我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现极大极小游戏
    """
    while len(nums) > 1:
        new_nums = []
        for i in range(len(nums) // 2):
            if i % 2 == 0:
                new_nums.append(min(nums[2 * i], nums[2 * i + 1]))
            else:
                new_nums.append(max(nums[2 * i], nums[2 * i + 1]))
        nums = new_nums
    return nums[0]

