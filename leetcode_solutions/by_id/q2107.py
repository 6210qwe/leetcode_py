"""
1980. 找出不同的二进制字符串 - 给你一个字符串数组 nums ，该数组由 n 个 互不相同 的二进制字符串组成，且每个字符串长度都是 n 。请你找出并返回一个长度为 n 且 没有出现 在 nums 中的二进制字符串。如果存在多种答案，只需返回 任意一个 即可。 示例 1： 输入：nums = ["01","10"] 输出："11" 解释："11" 没有出现在 nums 中。"00" 也是正确答案。 示例 2： 输入：nums = ["00","01"] 输出："11" 解释："11" 没有出现在 nums 中。"10" 也是正确答案。 示例 3： 输入：nums = ["111","011","001"] 输出："101" 解释："101" 没有出现在 nums 中。"000"、"010"、"100"、"110" 也是正确答案。 提示： * n == nums.length * 1 <= n <= 16 * nums[i].length == n * nums[i] 为 '0' 或 '1' * nums 中的所有字符串 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用对角线法构造一个不在 nums 中的二进制字符串。

算法步骤:
1. 初始化一个空字符串 result。
2. 遍历 nums 中的每个字符串，对于第 i 个字符串，取其第 i 位字符的反字符（即 '0' 变 '1'，'1' 变 '0'），并将其添加到 result 中。
3. 返回 result。

关键点:
- 对角线法确保生成的字符串与 nums 中的每一个字符串至少有一个位置不同。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_unique_binary_string(nums: List[str]) -> str:
    """
    函数式接口 - 使用对角线法构造一个不在 nums 中的二进制字符串。
    """
    n = len(nums)
    result = []
    for i in range(n):
        if nums[i][i] == '0':
            result.append('1')
        else:
            result.append('0')
    return ''.join(result)


Solution = create_solution(find_unique_binary_string)