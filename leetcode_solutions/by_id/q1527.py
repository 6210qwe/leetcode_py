"""
1411. 给 N x 3 网格图涂色的方案数 - 你有一个 n x 3 的网格图 grid ，你需要用 红，黄，绿 三种颜色之一给每一个格子上色，且确保相邻格子颜色不同（也就是有相同水平边或者垂直边的格子颜色不同）。 给你网格图的行数 n 。 请你返回给 grid 涂色的方案数。由于答案可能会非常大，请你返回答案对 10^9 + 7 取余的结果。 示例 1： 输入：n = 1 输出：12 解释：总共有 12 种可行的方法： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/04/12/e1.png] 示例 2： 输入：n = 2 输出：54 示例 3： 输入：n = 3 输出：246 示例 4： 输入：n = 7 输出：106494 示例 5： 输入：n = 5000 输出：30228214 提示： * n == grid.length * grid[i].length == 3 * 1 <= n <= 5000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算每行的涂色方案数。我们定义两种状态：
- a 表示当前行的三个格子颜色都不同的方案数。
- b 表示当前行有两个格子颜色相同，另一个格子颜色不同的方案数。

算法步骤:
1. 初始化第一行的 a 和 b。
2. 通过递推公式计算每一行的 a 和 b。
3. 最后一行的 a 和 b 之和即为最终结果。

关键点:
- 递推公式：a[i] = (3 * b[i-1]) % MOD, b[i] = (2 * (a[i-1] + b[i-1])) % MOD
- 初始值：a[0] = 6, b[0] = 6
- 结果：(a[n-1] + b[n-1]) % MOD
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 计算 n x 3 网格图的涂色方案数
    """
    MOD = 10**9 + 7
    if n == 1:
        return 12
    
    a, b = 6, 6
    for i in range(2, n + 1):
        new_a = (3 * b) % MOD
        new_b = (2 * (a + b)) % MOD
        a, b = new_a, new_b
    
    return (a + b) % MOD


Solution = create_solution(solution_function_name)