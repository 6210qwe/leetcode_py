"""
1260. 二维网格迁移 - 给你一个 m 行 n 列的二维网格 grid 和一个整数 k。你需要将 grid 迁移 k 次。 每次「迁移」操作将会引发下述活动： * 位于 grid[i][j]（j < n - 1）的元素将会移动到 grid[i][j + 1]。 * 位于 grid[i][n - 1] 的元素将会移动到 grid[i + 1][0]。 * 位于 grid[m - 1][n - 1] 的元素将会移动到 grid[0][0]。 请你返回 k 次迁移操作后最终得到的 二维网格。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2019/11/16/e1-1.png] 输入：grid = [[1,2,3],[4,5,6],[7,8,9]], k = 1 输出：[[9,1,2],[3,4,5],[6,7,8]] 示例 2： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2019/11/16/e2-1.png] 输入：grid = [[3,8,1,9],[19,7,2,5],[4,6,11,10],[12,0,21,13]], k = 4 输出：[[12,0,21,13],[3,8,1,9],[19,7,2,5],[4,6,11,10]] 示例 3： 输入：grid = [[1,2,3],[4,5,6],[7,8,9]], k = 9 输出：[[1,2,3],[4,5,6],[7,8,9]] 提示： * m == grid.length * n == grid[i].length * 1 <= m <= 50 * 1 <= n <= 50 * -1000 <= grid[i][j] <= 1000 * 0 <= k <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将二维网格展平为一维数组，然后进行循环移位，再重新构造成二维网格。

算法步骤:
1. 计算总元素数量 total_elements = m * n。
2. 将二维网格展平为一维数组 flat_grid。
3. 对一维数组进行 k 次循环移位。
4. 将移位后的一维数组重新构造成二维网格。

关键点:
- 使用取模运算来处理循环移位。
- 重新构造二维网格时注意行列索引的计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是行数，n 是列数。
空间复杂度: O(m * n)，需要额外的空间存储展平后的一维数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def shift_2d_grid(grid: List[List[int]], k: int) -> List[List[int]]:
    """
    函数式接口 - 将二维网格迁移 k 次
    """
    m, n = len(grid), len(grid[0])
    total_elements = m * n
    k = k % total_elements  # 处理 k 超过总元素数量的情况

    # 将二维网格展平为一维数组
    flat_grid = [grid[i // n][i % n] for i in range(total_elements)]

    # 对一维数组进行 k 次循环移位
    shifted_flat_grid = flat_grid[-k:] + flat_grid[:-k]

    # 重新构造成二维网格
    result_grid = [[shifted_flat_grid[i * n + j] for j in range(n)] for i in range(m)]
    return result_grid


Solution = create_solution(shift_2d_grid)