"""
1638. 统计只差一个字符的子串数目 - 给你两个字符串 s 和 t ，请你找出 s 中的非空子串的数目，这些子串满足替换 一个不同字符 以后，是 t 串的子串。换言之，请你找到 s 和 t 串中 恰好 只有一个字符不同的子字符串对的数目。 比方说， "computer" and "computation" 只有一个字符不同： 'e'/'a' ，所以这一对子字符串会给答案加 1 。 请你返回满足上述条件的不同子字符串对数目。 一个 子字符串 是一个字符串中连续的字符。 示例 1： 输入：s = "aba", t = "baba" 输出：6 解释：以下为只相差 1 个字符的 s 和 t 串的子字符串对： ("aba", "baba") ("aba", "baba") ("aba", "baba") ("aba", "baba") ("aba", "baba") ("aba", "baba") 加粗部分分别表示 s 和 t 串选出来的子字符串。 示例 2： 输入：s = "ab", t = "bb" 输出：3 解释：以下为只相差 1 个字符的 s 和 t 串的子字符串对： ("ab", "bb") ("ab", "bb") ("ab", "bb") 加粗部分分别表示 s 和 t 串选出来的子字符串。 示例 3： 输入：s = "a", t = "a" 输出：0 示例 4： 输入：s = "abe", t = "bbc" 输出：10 提示： * 1 <= s.length, t.length <= 100 * s 和 t 都只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来记录每个前缀和后缀的出现次数，然后通过枚举每个可能的中心位置来计算满足条件的子字符串对。

算法步骤:
1. 初始化两个哈希表 `prefix` 和 `suffix` 来记录前缀和后缀的出现次数。
2. 遍历字符串 `s` 和 `t` 的每个字符，对于每个字符，计算从当前字符到字符串末尾的所有子字符串，并更新 `prefix` 和 `suffix`。
3. 再次遍历字符串 `s` 和 `t` 的每个字符，对于每个字符，计算从字符串开头到当前字符的所有子字符串，并更新 `prefix` 和 `suffix`。
4. 对于每个字符，如果 `s[i]` 不等于 `t[i]`，则计算以 `i` 为中心的满足条件的子字符串对的数量。

关键点:
- 使用哈希表来记录前缀和后缀的出现次数，可以高效地计算满足条件的子字符串对。
- 通过两次遍历字符串，分别处理前缀和后缀，可以避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串的长度。需要遍历字符串并计算所有子字符串。
空间复杂度: O(n^2)，使用了两个哈希表来记录前缀和后缀的出现次数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_substrings_that_differ_by_one_character(s: str, t: str) -> int:
    """
    函数式接口 - 计算两个字符串中恰好只有一个字符不同的子字符串对的数目
    """
    n = len(s)
    m = len(t)
    prefix = {}
    suffix = {}
    count = 0

    # 计算后缀
    for i in range(n):
        for j in range(m):
            if s[i] != t[j]:
                suffix[(i, j)] = 1
            else:
                suffix[(i, j)] = suffix.get((i + 1, j + 1), 0) + 1

    # 计算前缀
    for i in range(n - 1, -1, -1):
        for j in range(m - 1, -1, -1):
            if s[i] != t[j]:
                prefix[(i, j)] = 1
            else:
                prefix[(i, j)] = prefix.get((i - 1, j - 1), 0) + 1

    # 计算满足条件的子字符串对
    for i in range(n):
        for j in range(m):
            if s[i] != t[j]:
                count += (prefix.get((i - 1, j - 1), 0) + 1) * (suffix.get((i + 1, j + 1), 0) + 1)

    return count


