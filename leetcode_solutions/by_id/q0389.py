def find_the_difference(s: str, t: str) -> str:
    """
    在字符串 t 中找出被额外添加的那个字符。

    使用异或的性质：相同字符两两异或为 0，最终剩下的就是被添加的字符。
    """
    x = 0
    for ch in s:
        x ^= ord(ch)
    for ch in t:
        x ^= ord(ch)
    return chr(x)


