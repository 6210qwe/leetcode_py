"""
2871. 将数组分割成最多数目的子数组 - 给你一个只包含 非负 整数的数组 nums 。 我们定义满足 l <= r 的子数组 nums[l..r] 的分数为 nums[l] AND nums[l + 1] AND ... AND nums[r] ，其中 AND 是按位与运算。 请你将数组分割成一个或者更多子数组，满足： * 每个 元素都 只 属于一个子数组。 * 子数组分数之和尽可能 小 。 请你在满足以上要求的条件下，返回 最多 可以得到多少个子数组。 一个 子数组 是一个数组中一段连续的元素。 示例 1： 输入：nums = [1,0,2,0,1,2] 输出：3 解释：我们可以将数组分割成以下子数组： - [1,0] 。子数组分数为 1 AND 0 = 0 。 - [2,0] 。子数组分数为 2 AND 0 = 0 。 - [1,2] 。子数组分数为 1 AND 2 = 0 。 分数之和为 0 + 0 + 0 = 0 ，是我们可以得到的最小分数之和。 在分数之和为 0 的前提下，最多可以将数组分割成 3 个子数组。所以返回 3 。 示例 2： 输入：nums = [5,7,1,3] 输出：1 解释：我们可以将数组分割成一个子数组：[5,7,1,3] ，分数为 1 ，这是可以得到的最小总分数。 在总分数为 1 的前提下，最多可以将数组分割成 1 个子数组。所以返回 1 。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，尽量在每个子数组的分数为0时进行分割。

算法步骤:
1. 初始化变量 `current_and` 为第一个元素，并初始化子数组计数器 `subarray_count` 为1。
2. 遍历数组，从第二个元素开始：
   - 更新 `current_and` 为当前元素与 `current_and` 的按位与结果。
   - 如果 `current_and` 为0，说明当前子数组的分数为0，可以进行分割，增加子数组计数器，并重置 `current_and` 为当前元素。
3. 返回子数组计数器 `subarray_count`。

关键点:
- 通过按位与操作来判断当前子数组的分数是否为0。
- 在分数为0时进行分割，确保子数组数量最大化。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def max_subarrays(nums: List[int]) -> int:
    """
    函数式接口 - 返回最多可以得到多少个子数组
    """
    if not nums:
        return 0

    current_and = nums[0]
    subarray_count = 1

    for num in nums[1:]:
        current_and &= num
        if current_and == 0:
            subarray_count += 1
            current_and = num

    return subarray_count

