"""
核心思想: 使用前序遍历（根-左-右）来序列化二叉搜索树，然后使用递归的方法来反序列化。

算法步骤:
1. 序列化：
   - 使用前序遍历将二叉搜索树转换为字符串。
   - 在每个节点值之间添加分隔符（如逗号），并在空节点处添加特殊字符（如'#'）。
2. 反序列化：
   - 将字符串分割成列表。
   - 使用递归方法构建二叉搜索树，根据前序遍历的特点，第一个元素是根节点，后续元素依次是左子树和右子树的节点。

关键点:
- 使用前序遍历可以唯一确定二叉搜索树。
- 优化时间和空间复杂度，尽量减少不必要的操作。
"""

