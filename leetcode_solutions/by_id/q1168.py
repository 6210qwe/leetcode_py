"""
1089. 复写零 - 给你一个长度固定的整数数组 arr ，请你将该数组中出现的每个零都复写一遍，并将其余的元素向右平移。 注意：请不要在超过该数组长度的位置写入元素。请对输入的数组 就地 进行上述修改，不要从函数返回任何东西。 示例 1： 输入：arr = [1,0,2,3,0,4,5,0] 输出：[1,0,0,2,3,0,0,4] 解释：调用函数后，输入的数组将被修改为：[1,0,0,2,3,0,0,4] 示例 2： 输入：arr = [1,2,3] 输出：[1,2,3] 解释：调用函数后，输入的数组将被修改为：[1,2,3] 提示： * 1 <= arr.length <= 104 * 0 <= arr[i] <= 9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法，先找到需要复写的零的位置，然后从后向前进行复制和复写。

算法步骤:
1. 计算出需要复写的零的数量。
2. 使用两个指针，一个指向原数组的末尾，另一个指向新数组的末尾。
3. 从后向前遍历数组，根据需要复写的零的数量进行复制和复写。

关键点:
- 通过两次遍历数组来实现就地修改，避免使用额外的空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组两次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def duplicateZeros(arr: List[int]) -> None:
    """
    函数式接口 - 复写零
    """
    n = len(arr)
    count_zeros = arr.count(0)  # 计算数组中零的数量
    new_length = n + count_zeros  # 新数组的长度

    # 从后向前遍历数组
    i = n - 1
    j = new_length - 1
    while j >= 0:
        if j < n:
            arr[j] = arr[i]
        j -= 1
        if arr[i] == 0:
            if j < n:
                arr[j] = arr[i]
            j -= 1
        i -= 1


Solution = create_solution(duplicateZeros)