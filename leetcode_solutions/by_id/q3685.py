"""
3392. 统计符合条件长度为 3 的子数组数目 - 给你一个整数数组 nums ，请你返回长度为 3 的 子数组 的数量，满足第一个数和第三个数的和恰好为第二个数的一半。 子数组 指的是一个数组中连续 非空 的元素序列。 示例 1： 输入：nums = [1,2,1,4,1] 输出：1 解释： 只有子数组 [1,4,1] 包含 3 个元素且第一个和第三个数字之和是中间数字的一半。number. 示例 2： 输入：nums = [1,1,1] 输出：0 解释： [1,1,1] 是唯一长度为 3 的子数组，但第一个数和第三个数的和不是第二个数的一半。 提示： * 3 <= nums.length <= 100 * -100 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组来检查每个长度为 3 的子数组是否满足条件。

算法步骤:
1. 初始化计数器 count 为 0。
2. 遍历数组，对于每个可能的子数组 [i, i+1, i+2]，检查 nums[i] + nums[i+2] 是否等于 nums[i+1] 的一半。
3. 如果满足条件，则将计数器 count 增加 1。
4. 返回计数器 count 的值。

关键点:
- 通过一次遍历来检查所有可能的子数组，时间复杂度为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 统计符合条件长度为 3 的子数组数目
    """
    count = 0
    n = len(nums)
    for i in range(n - 2):
        if (nums[i] + nums[i + 2]) == 2 * nums[i + 1]:
            count += 1
    return count


Solution = create_solution(solution_function_name)