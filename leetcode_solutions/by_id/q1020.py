"""
978. 最长湍流子数组 - 给定一个整数数组 arr ，返回 arr 的 最大湍流子数组的长度 。 如果比较符号在子数组中的每个相邻元素对之间翻转，则该子数组是 湍流子数组 。 更正式地来说，当 arr 的子数组 A[i], A[i+1], ..., A[j] 满足仅满足下列条件时，我们称其为湍流子数组： * 若 i <= k < j ： * 当 k 为奇数时， A[k] > A[k+1]，且 * 当 k 为偶数时，A[k] < A[k+1]； * 或 若 i <= k < j ： * 当 k 为偶数时，A[k] > A[k+1] ，且 * 当 k 为奇数时， A[k] < A[k+1]。 示例 1： 输入：arr = [9,4,2,10,7,8,8,1,9] 输出：5 解释：arr[1] > arr[2] < arr[3] > arr[4] < arr[5] 示例 2： 输入：arr = [4,8,12,16] 输出：2 示例 3： 输入：arr = [100] 输出：1 提示： * 1 <= arr.length <= 4 * 104 * 0 <= arr[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到最长的湍流子数组。

算法步骤:
1. 初始化两个变量 `prev` 和 `curr` 来记录前一个和当前的比较结果。
2. 使用一个变量 `max_len` 来记录最长的湍流子数组长度。
3. 遍历数组，根据当前和前一个元素的比较结果更新 `prev` 和 `curr`。
4. 如果当前比较结果与前一个比较结果相反或相等，则更新 `max_len`。
5. 返回 `max_len`。

关键点:
- 使用滑动窗口来维护当前的湍流子数组。
- 通过比较相邻元素来判断是否形成湍流。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_turbulent_subarray(arr: List[int]) -> int:
    """
    函数式接口 - 找到最长的湍流子数组
    """
    n = len(arr)
    if n < 2:
        return n

    max_len = 1
    prev, curr = 0, 0

    for i in range(1, n):
        if arr[i] > arr[i - 1]:
            curr = 1
        elif arr[i] < arr[i - 1]:
            curr = -1
        else:
            curr = 0

        if curr != 0 and curr != prev:
            max_len += 1
        else:
            max_len = 2 if curr != 0 else 1

        prev = curr

    return max_len


Solution = create_solution(longest_turbulent_subarray)