"""
561. 数组拆分 - 给定长度为 2n 的整数数组 nums ，你的任务是将这些数分成 n 对, 例如 (a1, b1), (a2, b2), ..., (an, bn) ，使得从 1 到 n 的 min(ai, bi) 总和最大。 返回该 最大总和 。 示例 1： 输入：nums = [1,4,3,2] 输出：4 解释：所有可能的分法（忽略元素顺序）为： 1. (1, 4), (2, 3) -> min(1, 4) + min(2, 3) = 1 + 2 = 3 2. (1, 3), (2, 4) -> min(1, 3) + min(2, 4) = 1 + 2 = 3 3. (1, 2), (3, 4) -> min(1, 2) + min(3, 4) = 1 + 3 = 4 所以最大总和为 4 示例 2： 输入：nums = [6,2,6,5,1,2] 输出：9 解释：最优的分法为 (2, 1), (2, 5), (6, 6). min(2, 1) + min(2, 5) + min(6, 6) = 1 + 2 + 6 = 9 提示： * 1 <= n <= 104 * nums.length == 2 * n * -104 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将数组排序后，每两个相邻的数配对，取每对中的较小值相加。

算法步骤:
1. 对数组进行排序。
2. 遍历排序后的数组，每隔一个元素取一次，累加这些元素的值。

关键点:
- 通过排序确保每对中的较小值尽可能大。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def array_pair_sum(nums: List[int]) -> int:
    """
    函数式接口 - 实现数组拆分的最大总和
    """
    # 对数组进行排序
    nums.sort()
    
    # 累加每对中的较小值
    return sum(nums[i] for i in range(0, len(nums), 2))


Solution = create_solution(array_pair_sum)