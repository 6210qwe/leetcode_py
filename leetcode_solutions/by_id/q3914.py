from typing import List
from collections import Counter

def is_prime(n: int) -> bool:
    """检查一个数是否为质数"""
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True

def solution_function_name(nums: List[int]) -> bool:
    """
    函数式接口 - 检查元素频次是否为质数
    """
    # 统计每个元素的频次
    freq = Counter(nums)
    
    # 检查每个频次是否为质数
    for count in freq.values():
        if is_prime(count):
            return True
    return False

