"""
核心思想: 使用哈希表统计每个元素的频次，然后检查这些频次是否为质数。

算法步骤:
1. 使用 Counter 统计数组中每个元素的频次。
2. 定义一个函数来检查一个数是否为质数。
3. 遍历频次字典，检查每个频次是否为质数，如果是则返回 True。
4. 如果所有频次都不是质数，则返回 False。

关键点:
- 使用 Counter 进行频次统计。
- 定义一个高效的质数检查函数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * sqrt(m))
  - n 是数组的长度。
  - m 是频次的最大值，质数检查的时间复杂度为 O(sqrt(m))。
空间复杂度: O(n)
  - 哈希表存储频次需要 O(n) 的空间。
"""

