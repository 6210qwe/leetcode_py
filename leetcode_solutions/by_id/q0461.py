def hamming_distance(x: int, y: int) -> int:
    """
    函数式接口 - 汉明距离
    
    实现思路:
    计算x和y的异或结果，统计其中1的个数。
    
    Args:
        x: 第一个整数
        y: 第二个整数
        
    Returns:
        汉明距离
        
    Example:
        >>> hamming_distance(1, 4)
        2
    """
    xor = x ^ y
    count = 0
    while xor:
        count += xor & 1
        xor >>= 1
    return count


