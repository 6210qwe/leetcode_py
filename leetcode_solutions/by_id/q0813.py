"""
797. 所有可能的路径 - 给你一个有 n 个节点的 有向无环图（DAG），请你找出从节点 0 到节点 n-1 的所有路径并输出（不要求按特定顺序） graph[i] 是一个从节点 i 可以访问的所有节点的列表（即从节点 i 到节点 graph[i][j]存在一条有向边）。 示例 1： [https://assets.leetcode.com/uploads/2020/09/28/all_1.jpg] 输入：graph = [[1,2],[3],[3],[]] 输出：[[0,1,3],[0,2,3]] 解释：有两条路径 0 -> 1 -> 3 和 0 -> 2 -> 3 示例 2： [https://assets.leetcode.com/uploads/2020/09/28/all_2.jpg] 输入：graph = [[4,3,1],[3,2,4],[3],[4],[]] 输出：[[0,4],[0,3,4],[0,1,3,4],[0,1,2,3,4],[0,1,4]] 提示： * n == graph.length * 2 <= n <= 15 * 0 <= graph[i][j] < n * graph[i][j] != i（即不存在自环） * graph[i] 中的所有元素 互不相同 * 保证输入为 有向无环图（DAG）
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）来遍历图，并记录从起点到终点的所有路径。

算法步骤:
1. 定义一个递归函数 dfs，用于从当前节点开始进行深度优先搜索。
2. 在递归函数中，如果当前节点是目标节点，则将当前路径加入结果列表。
3. 否则，遍历当前节点的所有邻居节点，继续递归搜索。
4. 初始化结果列表和当前路径，从起点开始调用递归函数。

关键点:
- 使用递归进行深度优先搜索。
- 通过传递路径列表来记录每条路径。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n)，其中 n 是节点数。在最坏情况下，每个节点都可能连接到其他所有节点，导致路径数量呈指数增长。
空间复杂度: O(n)，递归调用栈的深度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def all_paths_source_target(graph: List[List[int]]) -> List[List[int]]:
    """
    函数式接口 - 返回从节点 0 到节点 n-1 的所有路径
    """
    def dfs(node: int, path: List[int]):
        if node == len(graph) - 1:
            result.append(path)
            return
        for neighbor in graph[node]:
            dfs(neighbor, path + [neighbor])

    result = []
    dfs(0, [0])
    return result


Solution = create_solution(all_paths_source_target)