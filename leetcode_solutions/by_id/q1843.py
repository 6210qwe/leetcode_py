"""
1725. 可以形成最大正方形的矩形数目 - 给你一个数组 rectangles ，其中 rectangles[i] = [li, wi] 表示第 i 个矩形的长度为 li 、宽度为 wi 。 如果存在 k 同时满足 k <= li 和 k <= wi ，就可以将第 i 个矩形切成边长为 k 的正方形。例如，矩形 [4,6] 可以切成边长最大为 4 的正方形。 设 maxLen 为可以从矩形数组 rectangles 切分得到的 最大正方形 的边长。 请你统计有多少个矩形能够切出边长为 maxLen 的正方形，并返回矩形 数目 。 示例 1： 输入：rectangles = [[5,8],[3,9],[5,12],[16,5]] 输出：3 解释：能从每个矩形中切出的最大正方形边长分别是 [5,3,5,5] 。 最大正方形的边长为 5 ，可以由 3 个矩形切分得到。 示例 2： 输入：rectangles = [[2,3],[3,7],[4,3],[3,7]] 输出：3 提示： * 1 <= rectangles.length <= 1000 * rectangles[i].length == 2 * 1 <= li, wi <= 109 * li != wi
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历矩形数组，找到每个矩形可以切出的最大正方形边长，然后统计这些边长中的最大值出现的次数。

算法步骤:
1. 初始化两个变量：max_len 用于记录当前找到的最大正方形边长，count 用于记录最大正方形边长的出现次数。
2. 遍历矩形数组，对于每个矩形，计算其可以切出的最大正方形边长（即 min(li, wi)）。
3. 更新 max_len 和 count：
   - 如果当前边长大于 max_len，则更新 max_len 并重置 count 为 1。
   - 如果当前边长等于 max_len，则增加 count。
4. 返回 count。

关键点:
- 使用一次遍历来同时更新最大正方形边长和其出现次数，避免了多次遍历。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是矩形数组的长度。我们只需要遍历一次矩形数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


def solution_function_name(rectangles: List[List[int]]) -> int:
    """
    函数式接口 - 计算可以形成最大正方形的矩形数目
    """
    max_len = 0
    count = 0
    
    for l, w in rectangles:
        side = min(l, w)
        if side > max_len:
            max_len = side
            count = 1
        elif side == max_len:
            count += 1
    
    return count


