"""
1814. 统计一个数组中好对子的数目 - 给你一个数组 nums ，数组中只包含非负整数。定义 rev(x) 的值为将整数 x 各个数字位反转得到的结果。比方说 rev(123) = 321 ， rev(120) = 21 。我们称满足下面条件的下标对 (i, j) 是 好的 ： * 0 <= i < j < nums.length * nums[i] + rev(nums[j]) == nums[j] + rev(nums[i]) 请你返回好下标对的数目。由于结果可能会很大，请将结果对 109 + 7 取余 后返回。 示例 1： 输入：nums = [42,11,1,97] 输出：2 解释：两个坐标对为： - (0,3)：42 + rev(97) = 42 + 79 = 121, 97 + rev(42) = 97 + 24 = 121 。 - (1,2)：11 + rev(1) = 11 + 1 = 12, 1 + rev(11) = 1 + 11 = 12 。 示例 2： 输入：nums = [13,10,35,24,76] 输出：4 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个差值出现的次数，从而快速统计符合条件的对数。

算法步骤:
1. 定义一个辅助函数 `rev` 来计算一个整数的反转。
2. 初始化一个哈希表 `count` 来记录每个差值 `nums[i] - rev(nums[i])` 出现的次数。
3. 遍历数组 `nums`，对于每个元素 `nums[i]`，计算 `diff = nums[i] - rev(nums[i])`。
4. 将 `diff` 在哈希表中的计数加到结果中，并更新哈希表中 `diff` 的计数。
5. 返回结果对 `10^9 + 7` 取余后的值。

关键点:
- 使用哈希表来记录差值出现的次数，避免了双重循环的时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。每个元素只需要常数时间处理。
空间复杂度: O(n)，哈希表在最坏情况下需要存储 n 个不同的差值。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def count_nice_pairs(nums: List[int]) -> int:
    """
    统计数组中好对子的数目
    """
    MOD = 10**9 + 7
    
    def rev(x: int) -> int:
        """计算整数 x 的反转值"""
        return int(str(x)[::-1])
    
    count = {}
    result = 0
    
    for num in nums:
        diff = num - rev(num)
        if diff in count:
            result += count[diff]
            result %= MOD
        count[diff] = count.get(diff, 0) + 1
    
    return result

