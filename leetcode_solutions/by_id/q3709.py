"""
3456. 找出长度为 K 的特殊子字符串 - 给你一个字符串 s 和一个整数 k。 判断是否存在一个长度 恰好 为 k 的子字符串，该子字符串需要满足以下条件： 1. 该子字符串 只包含一个唯一字符（例如，"aaa" 或 "bbb"）。 2. 如果该子字符串的 前面 有字符，则该字符必须与子字符串中的字符不同。 3. 如果该子字符串的 后面 有字符，则该字符也必须与子字符串中的字符不同。 如果存在这样的子串，返回 true；否则，返回 false。 子字符串 是字符串中的连续、非空字符序列。 示例 1： 输入： s = "aaabaaa", k = 3 输出： true 解释： 子字符串 s[4..6] == "aaa" 满足条件： * 长度为 3。 * 所有字符相同。 * 子串 "aaa" 前的字符是 'b'，与 'a' 不同。 * 子串 "aaa" 后没有字符。 示例 2： 输入： s = "abc", k = 2 输出： false 解释： 不存在长度为 2 、仅由一个唯一字符组成且满足所有条件的子字符串。 提示： * 1 <= k <= s.length <= 100 * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来检查每个长度为 k 的子字符串是否满足条件。

算法步骤:
1. 初始化两个指针 i 和 j，分别表示当前窗口的起始和结束位置。
2. 遍历字符串 s，检查每个长度为 k 的子字符串：
   - 如果子字符串的所有字符都相同，并且前面和后面的字符（如果存在）都不同，则返回 True。
3. 如果遍历完字符串仍未找到满足条件的子字符串，则返回 False。

关键点:
- 使用滑动窗口来高效地检查每个长度为 k 的子字符串。
- 通过比较前后字符来确保子字符串满足条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历一次字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str, k: int) -> bool:
    """
    函数式接口 - 检查是否存在长度为 k 的特殊子字符串
    """
    n = len(s)
    if n < k:
        return False

    for i in range(n - k + 1):
        # 检查子字符串是否所有字符都相同
        if all(s[i] == s[i + j] for j in range(k)):
            # 检查前面的字符（如果存在）
            if i > 0 and s[i - 1] == s[i]:
                continue
            # 检查后面的字符（如果存在）
            if i + k < n and s[i + k] == s[i]:
                continue
            return True

    return False


Solution = create_solution(solution_function_name)