"""
1451. 重新排列句子中的单词 - 「句子」是一个用空格分隔单词的字符串。给你一个满足下述格式的句子 text : * 句子的首字母大写 * text 中的每个单词都用单个空格分隔。 请你重新排列 text 中的单词，使所有单词按其长度的升序排列。如果两个单词的长度相同，则保留其在原句子中的相对顺序。 请同样按上述格式返回新的句子。 示例 1： 输入：text = "Leetcode is cool" 输出："Is cool leetcode" 解释：句子中共有 3 个单词，长度为 8 的 "Leetcode" ，长度为 2 的 "is" 以及长度为 4 的 "cool" 。 输出需要按单词的长度升序排列，新句子中的第一个单词首字母需要大写。 示例 2： 输入：text = "Keep calm and code on" 输出："On and keep calm code" 解释：输出的排序情况如下： "On" 2 个字母。 "and" 3 个字母。 "keep" 4 个字母，因为存在长度相同的其他单词，所以它们之间需要保留在原句子中的相对顺序。 "calm" 4 个字母。 "code" 4 个字母。 示例 3： 输入：text = "To be or not to be" 输出："To be or to be not" 提示： * text 以大写字母开头，然后包含若干小写字母以及单词间的单个空格。 * 1 <= text.length <= 10^5
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用稳定排序算法（如归并排序）对单词按长度进行排序，同时保持原有顺序。

算法步骤:
1. 将句子按空格分割成单词列表。
2. 使用稳定排序算法对单词列表按长度进行排序。
3. 将排序后的单词列表重新组合成句子，并确保首字母大写。

关键点:
- 使用稳定排序算法保证相同长度的单词保持原有顺序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是单词的数量。稳定排序的时间复杂度为 O(n log n)。
空间复杂度: O(n)，存储单词列表和排序过程中使用的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def rearrange_words(text: str) -> str:
    """
    函数式接口 - 重新排列句子中的单词
    """
    # 将句子按空格分割成单词列表
    words = text.split()
    
    # 使用稳定排序算法对单词列表按长度进行排序
    words.sort(key=len)
    
    # 将排序后的单词列表重新组合成句子，并确保首字母大写
    new_text = ' '.join(words).capitalize()
    
    return new_text


Solution = create_solution(rearrange_words)