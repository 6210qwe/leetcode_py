"""
3165. 不包含相邻元素的子序列的最大和 - 给你一个整数数组 nums 和一个二维数组 queries，其中 queries[i] = [posi, xi]。 对于每个查询 i，首先将 nums[posi] 设置为 xi，然后计算查询 i 的答案，该答案为 nums 中 不包含相邻元素 的 子序列 的 最大 和。 返回所有查询的答案之和。 由于最终答案可能非常大，返回其对 109 + 7 取余 的结果。 子序列 是指从另一个数组中删除一些或不删除元素而不改变剩余元素顺序得到的数组。 示例 1： 输入：nums = [3,5,9], queries = [[1,-2],[0,-3]] 输出：21 解释： 执行第 1 个查询后，nums = [3,-2,9]，不包含相邻元素的子序列的最大和为 3 + 9 = 12。 执行第 2 个查询后，nums = [-3,-2,9]，不包含相邻元素的子序列的最大和为 9 。 示例 2： 输入：nums = [0,-1], queries = [[0,-5]] 输出：0 解释： 执行第 1 个查询后，nums = [-5,-1]，不包含相邻元素的子序列的最大和为 0（选择空子序列）。 提示： * 1 <= nums.length <= 5 * 104 * -105 <= nums[i] <= 105 * 1 <= queries.length <= 5 * 104 * queries[i] == [posi, xi] * 0 <= posi <= nums.length - 1 * -105 <= xi <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们需要维护一个动态规划数组 dp，其中 dp[i] 表示以 nums[i] 结尾的不包含相邻元素的子序列的最大和。对于每个查询，我们需要更新 nums 数组，并重新计算 dp 数组。

算法步骤:
1. 初始化 dp 数组，dp[0] = max(0, nums[0]), dp[1] = max(dp[0], nums[1])。
2. 计算初始的 dp 数组。
3. 对于每个查询，更新 nums 数组，并重新计算受影响的 dp 值。
4. 计算每个查询后的最大和，并累加到结果中。

关键点:
- 使用动态规划来维护不包含相邻元素的子序列的最大和。
- 对于每个查询，只需要更新受影响的 dp 值，而不需要重新计算整个 dp 数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 nums 的长度，m 是 queries 的长度。初始化 dp 数组的时间复杂度是 O(n)，每个查询的时间复杂度是 O(1)。
空间复杂度: O(n)，需要额外的空间来存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums: List[int], queries: List[List[int]]) -> int:
    MOD = 10**9 + 7
    n = len(nums)
    
    # 初始化 dp 数组
    dp = [0] * n
    dp[0] = max(0, nums[0])
    if n > 1:
        dp[1] = max(dp[0], nums[1])
    
    # 计算初始的 dp 数组
    for i in range(2, n):
        dp[i] = max(dp[i-1], dp[i-2] + nums[i])
    
    result = 0
    
    for pos, x in queries:
        # 更新 nums 数组
        nums[pos] = x
        
        # 重新计算受影响的 dp 值
        if pos == 0:
            dp[0] = max(0, nums[0])
            if n > 1:
                dp[1] = max(dp[0], nums[1])
        elif pos == 1:
            dp[1] = max(dp[0], nums[1])
        
        for i in range(max(2, pos), n):
            dp[i] = max(dp[i-1], dp[i-2] + nums[i])
        
        # 计算当前查询的结果
        result += max(dp[-1], 0)
        result %= MOD
    
    return result

