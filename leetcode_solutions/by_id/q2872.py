"""
2789. 合并后数组中的最大元素 - 给你一个下标从 0 开始、由正整数组成的数组 nums 。 你可以在数组上执行下述操作 任意 次： * 选中一个同时满足 0 <= i < nums.length - 1 和 nums[i] <= nums[i + 1] 的下标 i 。将元素 nums[i + 1] 替换为 nums[i] + nums[i + 1] ，并从数组中删除元素 nums[i] 。 返回你可以从最终数组中获得的 最大 元素的值。 示例 1： 输入：nums = [2,3,7,9,3] 输出：21 解释：我们可以在数组上执行下述操作： - 选中 i = 0 ，得到数组 nums = [5,7,9,3] 。 - 选中 i = 1 ，得到数组 nums = [5,16,3] 。 - 选中 i = 0 ，得到数组 nums = [21,3] 。 最终数组中的最大元素是 21 。可以证明我们无法获得更大的元素。 示例 2： 输入：nums = [5,3,3] 输出：11 解释：我们可以在数组上执行下述操作： - 选中 i = 1 ，得到数组 nums = [5,6] 。 - 选中 i = 0 ，得到数组 nums = [11] 。 最终数组中只有一个元素，即 11 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从右向左遍历数组，尽可能地合并相邻的元素。

算法步骤:
1. 从右向左遍历数组。
2. 如果当前元素小于等于下一个元素，则将它们合并，并更新下一个元素的值。
3. 继续遍历直到结束。
4. 返回最终数组中的最大元素。

关键点:
- 从右向左遍历可以确保每次合并时都选择最优的合并方式。
- 通过不断更新下一个元素的值，可以避免多次遍历。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要一次遍历数组。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 返回合并后的数组中的最大元素
    """
    if not nums:
        return 0

    # 从右向左遍历数组
    for i in range(len(nums) - 2, -1, -1):
        if nums[i] <= nums[i + 1]:
            nums[i + 1] += nums[i]

    # 返回最终数组中的最大元素
    return max(nums)


Solution = create_solution(solution_function_name)