"""
2657. 找到两个数组的前缀公共数组 - 给你两个下标从 0 开始长度为 n 的整数排列 A 和 B 。 A 和 B 的 前缀公共数组 定义为数组 C ，其中 C[i] 是数组 A 和 B 到下标为 i 之前公共元素的数目。 请你返回 A 和 B 的 前缀公共数组 。 如果一个长度为 n 的数组包含 1 到 n 的元素恰好一次，我们称这个数组是一个长度为 n 的 排列 。 示例 1： 输入：A = [1,3,2,4], B = [3,1,2,4] 输出：[0,2,3,4] 解释：i = 0：没有公共元素，所以 C[0] = 0 。 i = 1：1 和 3 是两个数组的前缀公共元素，所以 C[1] = 2 。 i = 2：1，2 和 3 是两个数组的前缀公共元素，所以 C[2] = 3 。 i = 3：1，2，3 和 4 是两个数组的前缀公共元素，所以 C[3] = 4 。 示例 2： 输入：A = [2,3,1], B = [3,1,2] 输出：[0,1,3] 解释：i = 0：没有公共元素，所以 C[0] = 0 。 i = 1：只有 3 是公共元素，所以 C[1] = 1 。 i = 2：1，2 和 3 是两个数组的前缀公共元素，所以 C[2] = 3 。 提示： * 1 <= A.length == B.length == n <= 50 * 1 <= A[i], B[i] <= n * 题目保证 A 和 B 两个数组都是 n 个元素的排列。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来记录已经出现的元素，并在遍历过程中计算前缀公共元素的数量。

算法步骤:
1. 初始化两个集合，分别用于记录 A 和 B 中已经出现的元素。
2. 初始化结果数组 C，长度与 A 和 B 相同。
3. 遍历 A 和 B 的每个元素：
   - 将当前元素添加到对应的集合中。
   - 计算两个集合的交集大小，即为当前前缀的公共元素数量。
   - 将该数量存储到结果数组 C 中。

关键点:
- 使用集合来高效地记录和查找已经出现的元素。
- 在每次迭代中更新集合并计算交集大小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 A 和 B 的长度。我们需要遍历每个元素一次。
空间复杂度: O(n)，使用了两个集合来记录已经出现的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def find_prefix_common_array(A: List[int], B: List[int]) -> List[int]:
    """
    函数式接口 - 找到两个数组的前缀公共数组
    """
    set_a = set()
    set_b = set()
    result = []
    
    for i in range(len(A)):
        set_a.add(A[i])
        set_b.add(B[i])
        common_count = len(set_a & set_b)
        result.append(common_count)
    
    return result

