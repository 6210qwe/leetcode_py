"""
2011. 执行操作后的变量值 - 存在一种仅支持 4 种操作和 1 个变量 X 的编程语言： * ++X 和 X++ 使变量 X 的值 加 1 * --X 和 X-- 使变量 X 的值 减 1 最初，X 的值是 0 给你一个字符串数组 operations ，这是由操作组成的一个列表，返回执行所有操作后， X 的 最终值 。 示例 1： 输入：operations = ["--X","X++","X++"] 输出：1 解释：操作按下述步骤执行： 最初，X = 0 --X：X 减 1 ，X = 0 - 1 = -1 X++：X 加 1 ，X = -1 + 1 = 0 X++：X 加 1 ，X = 0 + 1 = 1 示例 2： 输入：operations = ["++X","++X","X++"] 输出：3 解释：操作按下述步骤执行： 最初，X = 0 ++X：X 加 1 ，X = 0 + 1 = 1 ++X：X 加 1 ，X = 1 + 1 = 2 X++：X 加 1 ，X = 2 + 1 = 3 示例 3： 输入：operations = ["X++","++X","--X","X--"] 输出：0 解释：操作按下述步骤执行： 最初，X = 0 X++：X 加 1 ，X = 0 + 1 = 1 ++X：X 加 1 ，X = 1 + 1 = 2 --X：X 减 1 ，X = 2 - 1 = 1 X--：X 减 1 ，X = 1 - 1 = 0 提示： * 1 <= operations.length <= 100 * operations[i] 将会是 "++X"、"X++"、"--X" 或 "X--"
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历操作列表，根据每个操作的类型更新变量 X 的值。

算法步骤:
1. 初始化变量 X 为 0。
2. 遍历操作列表，对于每个操作：
   - 如果操作是 "++X" 或 "X++"，则将 X 增加 1。
   - 如果操作是 "--X" 或 "X--"，则将 X 减少 1。
3. 返回最终的 X 值。

关键点:
- 使用简单的条件判断来处理每种操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是操作列表的长度。我们需要遍历整个操作列表。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def final_value_after_operations(operations: List[str]) -> int:
    """
    函数式接口 - 计算执行所有操作后的变量 X 的最终值
    """
    x = 0
    for op in operations:
        if op in {"++X", "X++"}:
            x += 1
        elif op in {"--X", "X--"}:
            x -= 1
    return x


Solution = create_solution(final_value_after_operations)