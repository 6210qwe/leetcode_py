# ============================================================================
# 问题描述
# ============================================================================
"""
988. 从叶结点开始的最小字符串 - 给定一颗根结点为 root 的二叉树，树中的每一个结点都有一个 [0, 25] 范围内的值，分别代表字母 'a' 到 'z'。 返回 按字典序最小 的字符串，该字符串从这棵树的一个叶结点开始，到根结点结束。 > 注：字符串中任何较短的前缀在 字典序上 都是 较小 的： > > * 例如，在字典序上 "ab" 比 "aba" 要小。叶结点是指没有子结点的结点。 节点的叶节点是没有子节点的节点。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2019/02/02/tree1.png] 输入：root = [0,1,2,3,4,3,4] 输出："dba" 示例 2： [https://assets.leetcode.com/uploads/2019/01/30/tree2.png] 输入：root = [25,1,3,1,3,0,2] 输出："adz" 示例 3： [https://assets.leetcode.com/uploads/2019/02/01/tree3.png] 输入：root = [2,2,1,null,1,0,null,0] 输出："abc" 提示： * 给定树的结点数在 [1, 8500] 范围内 * 0 <= Node.val <= 25
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 从叶子节点到根节点构建字符串，并维护当前最小的字符串。

算法步骤:
1. 定义一个递归函数 `dfs` 来进行深度优先搜索。
2. 在每次递归调用中，将当前节点的字符添加到路径字符串中。
3. 如果当前节点是叶子节点，将路径字符串反转并与当前最小字符串比较，更新最小字符串。
4. 递归调用左子节点和右子节点。
5. 返回最终的最小字符串。

关键点:
- 使用递归进行深度优先搜索。
- 反转路径字符串以从叶子节点到根节点。
- 维护一个全局变量来存储当前最小的字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点都被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

class Solution:
    def smallestFromLeaf(self, root: Optional[TreeNode]) -> str:
        self.min_str = "~"  # 初始化最小字符串为最大可能值

        def dfs(node: Optional[TreeNode], path: str):
            if not node:
                return
            # 将当前节点的字符添加到路径字符串
            current_char = chr(ord('a') + node.val)
            new_path = current_char + path
            # 如果是叶子节点，更新最小字符串
            if not node.left and not node.right:
                self.min_str = min(self.min_str, new_path)
            # 递归调用左子节点和右子节点
            dfs(node.left, new_path)
            dfs(node.right, new_path)

        dfs(root, "")
        return self.min_str

Solution = create_solution(Solution)