"""
LCR 164. 破解闯关密码 - 闯关游戏需要破解一组密码，闯关组给出的有关密码的线索是： * 一个拥有密码所有元素的非负整数数组 password * 密码是 password 中所有元素拼接后得到的最小的一个数 请编写一个程序返回这个密码。 示例 1： 输入：password = [15, 8, 7] 输出："1578" 示例 2： 输入：password = [0, 3, 30, 34, 5, 9] 输出："03033459" 提示： * 0 < password.length <= 100 说明: * 输出结果可能非常大，所以你需要返回一个字符串而不是整数 * 拼接起来的数字可能会有前导 0，最后结果不需要去掉前导 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用自定义排序规则来对数组进行排序，使得拼接后的数字最小。

算法步骤:
1. 将数组中的每个整数转换为字符串。
2. 使用自定义排序规则对字符串数组进行排序：对于两个字符串 a 和 b，如果 a+b < b+a，则 a 应该排在 b 前面。
3. 将排序后的字符串数组拼接成一个字符串并返回。

关键点:
- 自定义排序规则：通过比较 a+b 和 b+a 来决定 a 和 b 的相对顺序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(n)，需要额外的空间来存储字符串数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(password: List[int]) -> str:
    """
    函数式接口 - 实现最优解法
    """
    # 将数组中的每个整数转换为字符串
    str_password = list(map(str, password))
    
    # 自定义排序规则
    def compare(a, b):
        return int(a + b) - int(b + a)
    
    # 使用自定义排序规则对字符串数组进行排序
    str_password.sort(key=lambda x: x*10, cmp=compare)
    
    # 将排序后的字符串数组拼接成一个字符串
    result = ''.join(str_password)
    
    # 返回结果
    return result

