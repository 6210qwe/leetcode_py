"""
1299. 将每个元素替换为右侧最大元素 - 给你一个数组 arr ，请你将每个元素用它右边最大的元素替换，如果是最后一个元素，用 -1 替换。 完成所有替换操作后，请你返回这个数组。 示例 1： 输入：arr = [17,18,5,4,6,1] 输出：[18,6,6,6,1,-1] 解释： - 下标 0 的元素 --> 右侧最大元素是下标 1 的元素 (18) - 下标 1 的元素 --> 右侧最大元素是下标 4 的元素 (6) - 下标 2 的元素 --> 右侧最大元素是下标 4 的元素 (6) - 下标 3 的元素 --> 右侧最大元素是下标 4 的元素 (6) - 下标 4 的元素 --> 右侧最大元素是下标 5 的元素 (1) - 下标 5 的元素 --> 右侧没有其他元素，替换为 -1 示例 2： 输入：arr = [400] 输出：[-1] 解释：下标 0 的元素右侧没有其他元素。 提示： * 1 <= arr.length <= 104 * 1 <= arr[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从右向左遍历数组，同时维护一个变量来记录当前的最大值。

算法步骤:
1. 初始化一个变量 `max_right` 为 -1，表示最右侧元素的替换值。
2. 从右向左遍历数组：
   - 保存当前元素的值到临时变量 `current`。
   - 将当前元素替换为 `max_right`。
   - 更新 `max_right` 为 `current` 和 `max_right` 中的较大值。

关键点:
- 通过从右向左遍历，可以在 O(1) 空间复杂度内完成替换。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要一次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def replace_elements(arr: List[int]) -> List[int]:
    """
    函数式接口 - 将每个元素替换为右侧最大元素
    """
    max_right = -1
    for i in range(len(arr) - 1, -1, -1):
        current = arr[i]
        arr[i] = max_right
        max_right = max(max_right, current)
    return arr


Solution = create_solution(replace_elements)