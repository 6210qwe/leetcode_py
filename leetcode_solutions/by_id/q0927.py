"""
891. 子序列宽度之和 - 一个序列的 宽度 定义为该序列中最大元素和最小元素的差值。 给你一个整数数组 nums ，返回 nums 的所有非空 子序列 的 宽度之和 。由于答案可能非常大，请返回对 109 + 7 取余 后的结果。 子序列 定义为从一个数组里删除一些（或者不删除）元素，但不改变剩下元素的顺序得到的数组。例如，[3,6,2,7] 就是数组 [0,3,1,6,2,2,7] 的一个子序列。 示例 1： 输入：nums = [2,1,3] 输出：6 解释：子序列为 [1], [2], [3], [2,1], [2,3], [1,3], [2,1,3] 。 相应的宽度是 0, 0, 0, 1, 1, 2, 2 。 宽度之和是 6 。 示例 2： 输入：nums = [2] 输出：0 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和组合数学来计算所有子序列的宽度之和。

算法步骤:
1. 对数组进行排序。
2. 计算每个元素作为最大值和最小值的贡献。
3. 使用组合数学公式计算每个元素的贡献，并累加结果。
4. 返回结果对 10^9 + 7 取余后的值。

关键点:
- 排序后，每个元素作为最大值和最小值的贡献可以通过组合数学公式快速计算。
- 使用幂运算和模运算来处理大数取余。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_subseq_widths(nums: List[int]) -> int:
    """
    函数式接口 - 计算所有非空子序列的宽度之和
    """
    MOD = 10**9 + 7
    n = len(nums)
    nums.sort()
    
    # 预计算 2 的幂次
    pow2 = [1] * n
    for i in range(1, n):
        pow2[i] = (pow2[i - 1] * 2) % MOD
    
    result = 0
    for i in range(n):
        # 计算当前元素作为最大值和最小值的贡献
        max_contribution = (nums[i] * (pow2[i] - 1)) % MOD
        min_contribution = (nums[i] * (pow2[n - i - 1] - 1)) % MOD
        result = (result + max_contribution - min_contribution) % MOD
    
    return result


Solution = create_solution(sum_subseq_widths)