"""
1328. 破坏回文串 - 给你一个由小写英文字母组成的回文字符串 palindrome ，请你将其中 一个 字符用任意小写英文字母替换，使得结果字符串的 字典序最小 ，且 不是 回文串。 请你返回结果字符串。如果无法做到，则返回一个 空串 。 如果两个字符串长度相同，那么字符串 a 字典序比字符串 b 小可以这样定义：在 a 和 b 出现不同的第一个位置上，字符串 a 中的字符严格小于 b 中的对应字符。例如，"abcc” 字典序比 "abcd" 小，因为不同的第一个位置是在第四个字符，显然 'c' 比 'd' 小。 示例 1： 输入：palindrome = "abccba" 输出："aaccba" 解释：存在多种方法可以使 "abccba" 不是回文，例如 "zbccba", "aaccba", 和 "abacba" 。 在所有方法中，"aaccba" 的字典序最小。 示例 2： 输入：palindrome = "a" 输出："" 解释：不存在替换一个字符使 "a" 变成非回文的方法，所以返回空字符串。 提示： * 1 <= palindrome.length <= 1000 * palindrome 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从左到右遍历字符串，找到第一个不是 'a' 的字符并将其替换为 'a'，以确保结果字符串的字典序最小。如果整个字符串都是 'a'，则将最后一个字符替换为 'b'。

算法步骤:
1. 遍历字符串的前半部分，找到第一个不是 'a' 的字符。
2. 如果找到了这样的字符，将其替换为 'a' 并返回结果字符串。
3. 如果没有找到这样的字符（即整个字符串都是 'a'），将最后一个字符替换为 'b' 并返回结果字符串。
4. 如果字符串长度为 1，返回空字符串，因为无法通过替换一个字符使其变成非回文。

关键点:
- 优先替换第一个不是 'a' 的字符，以确保字典序最小。
- 如果整个字符串都是 'a'，则只能替换最后一个字符为 'b'。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。最坏情况下需要遍历整个字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def break_palindrome(palindrome: str) -> str:
    """
    函数式接口 - 破坏回文串
    """
    n = len(palindrome)
    if n == 1:
        return ""

    # 将字符串转换为列表以便修改
    s = list(palindrome)

    # 遍历字符串的前半部分
    for i in range(n // 2):
        if s[i] != 'a':
            s[i] = 'a'
            return ''.join(s)

    # 如果整个字符串都是 'a'，将最后一个字符替换为 'b'
    s[-1] = 'b'
    return ''.join(s)


Solution = create_solution(break_palindrome)