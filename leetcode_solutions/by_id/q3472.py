# ============================================================================
# 问题描述
# ============================================================================
"""
给定一个整数数组 nums，返回一个新的数组 result，其中 result[i] 是 nums[i] 和 nums[i+1] 的按位或运算结果。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组，计算相邻元素的按位或运算。

算法步骤:
1. 初始化一个空的结果数组 result。
2. 遍历输入数组 nums，从第一个元素到倒数第二个元素。
3. 对于每个元素 nums[i]，计算 nums[i] | nums[i+1] 并将结果添加到 result 中。
4. 返回结果数组 result。

关键点:
- 使用列表推导式可以简化代码并提高可读性。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是输入数组的长度。我们需要遍历整个数组一次。
空间复杂度: O(n)，结果数组的大小与输入数组的长度成正比。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> List[int]:
    """
    函数式接口 - 计算相邻元素的按位或运算
    """
    # 使用列表推导式计算相邻元素的按位或运算
    return [nums[i] | nums[i + 1] for i in range(len(nums) - 1)]


Solution = create_solution(solution_function_name)