from typing import List

def solution_function_name(prices: List[int]) -> int:
    """
    函数式接口 - 计算最大线性股票得分
    """
    # 使用哈希表记录每个股票的出现次数
    count = {}
    max_score = 0
    
    for price in prices:
        if price in count:
            count[price] += 1
        else:
            count[price] = 1
        
        # 计算当前价格的得分
        score = price * count[price]
        max_score = max(max_score, score)
    
    return max_score

