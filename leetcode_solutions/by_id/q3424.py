def solution_function_name():
    """
    函数式接口 - 查询发生在周五的交易记录
    """
    # 实现最优解法
    query = """
    SELECT *
    FROM purchases
    WHERE DAYOFWEEK(purchase_date) = 6;
    """
    return query


