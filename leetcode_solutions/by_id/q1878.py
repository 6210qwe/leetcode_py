"""
1752. 检查数组是否经排序和轮转得到 - 给你一个数组 nums 。nums 的源数组中，所有元素与 nums 相同，但按非递减顺序排列。 如果 nums 能够由源数组轮转若干位置（包括 0 个位置）得到，则返回 true ；否则，返回 false 。 源数组中可能存在 重复项 。 注意：数组 A 在轮转 x 个位置后得到长度相同的数组 B ，使得对于每一个有效的下标 i，满足 B[i] == A[(i+x) % A.length]。 示例 1： 输入：nums = [3,4,5,1,2] 输出：true 解释：[1,2,3,4,5] 为有序的源数组。 可以轮转 x = 2 个位置，使新数组从值为 3 的元素开始：[3,4,5,1,2] 。 示例 2： 输入：nums = [2,1,3,4] 输出：false 解释：源数组无法经轮转得到 nums 。 示例 3： 输入：nums = [1,2,3] 输出：true 解释：[1,2,3] 为有序的源数组。 可以轮转 x = 0 个位置（即不轮转）得到 nums 。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 检查数组是否可以通过一次旋转得到非递减排列。

算法步骤:
1. 找到数组中第一个降序的位置。
2. 如果没有找到降序位置，说明数组已经是非递减排列，返回 True。
3. 如果找到降序位置，将数组分成两部分并拼接，检查拼接后的数组是否是非递减排列。
4. 如果拼接后的数组是非递减排列，返回 True；否则返回 False。

关键点:
- 通过找到第一个降序位置来确定旋转点。
- 拼接数组后检查是否是非递减排列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组一次来找到降序位置，并再次遍历拼接后的数组来检查是否是非递减排列。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_if_array_is_sorted_and_rotated(nums: List[int]) -> bool:
    """
    函数式接口 - 检查数组是否可以通过一次旋转得到非递减排列
    """
    n = len(nums)
    if n == 1:
        return True
    
    # 找到第一个降序位置
    rotation_index = -1
    for i in range(1, n):
        if nums[i] < nums[i - 1]:
            rotation_index = i
            break
    
    # 如果没有找到降序位置，说明数组已经是非递减排列
    if rotation_index == -1:
        return True
    
    # 将数组分成两部分并拼接
    rotated_nums = nums[rotation_index:] + nums[:rotation_index]
    
    # 检查拼接后的数组是否是非递减排列
    for i in range(1, n):
        if rotated_nums[i] < rotated_nums[i - 1]:
            return False
    
    return True


Solution = create_solution(check_if_array_is_sorted_and_rotated)