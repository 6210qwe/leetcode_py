# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算每个字符之间的最短移动距离，并累加这些距离和键入字符的时间。

算法步骤:
1. 初始化当前指针位置为 'a'，总时间为 0。
2. 遍历字符串中的每个字符，计算从当前指针位置到目标字符的最短移动距离。
3. 更新总时间，包括移动时间和键入时间。
4. 更新当前指针位置为目标字符。

关键点:
- 计算两个字符之间的最短移动距离时，考虑顺时针和逆时针两种情况。
- 总时间包括移动时间和键入时间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 word 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

