from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.solution import create_solution


def delete_node_in_a_linked_list(node: ListNode) -> None:
    """
    函数式接口 - 删除链表中的节点
    
    实现思路:
    由于无法访问前一个节点，将下一个节点的值复制到当前节点，然后删除下一个节点。
    
    Args:
        node: 要删除的节点（不是最后一个节点）
        
    Returns:
        None（直接修改链表）
        
    Example:
        >>> head = ListNode.from_list([4, 5, 1, 9])
        >>> node = head.next  # 节点5
        >>> delete_node_in_a_linked_list(node)
        >>> head.to_list()
        [4, 1, 9]
    """
    node.val = node.next.val
    node.next = node.next.next


