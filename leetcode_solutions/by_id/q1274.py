"""
核心思想: 使用 Python 的 datetime 模块来处理日期计算。

算法步骤:
1. 将输入的日期字符串转换为 datetime 对象。
2. 计算两个日期之间的差值。
3. 返回差值的绝对值。

关键点:
- 使用 datetime.strptime 将字符串转换为日期对象。
- 使用 (date1 - date2).days 来计算日期差值。
"""

