# ============================================================================
# 问题描述
# ============================================================================
"""
2150. 找出数组中的所有孤独数字 - 给你一个整数数组 nums 。如果数字 x 在数组中仅出现 一次 ，且没有 相邻 数字（即，x + 1 和 x - 1）出现在数组中，则认为数字 x 是 孤独数字 。 返回 nums 中的 所有 孤独数字。你可以按 任何顺序 返回答案。 示例 1： 输入：nums = [10,6,5,8] 输出：[10,8] 解释： - 10 是一个孤独数字，因为它只出现一次，并且 9 和 11 没有在 nums 中出现。 - 8 是一个孤独数字，因为它只出现一次，并且 7 和 9 没有在 nums 中出现。 - 5 不是一个孤独数字，因为 6 出现在 nums 中，反之亦然。 因此，nums 中的孤独数字是 [10, 8] 。 注意，也可以返回 [8, 10] 。 示例 2： 输入：nums = [1,3,5,3] 输出：[1,5] 解释： - 1 是一个孤独数字，因为它只出现一次，并且 0 和 2 没有在 nums 中出现。 - 5 是一个孤独数字，因为它只出现一次，并且 4 和 6 没有在 nums 中出现。 - 3 不是一个孤独数字，因为它出现两次。 因此，nums 中的孤独数字是 [1, 5] 。 注意，也可以返回 [5, 1] 。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来记录每个数字的出现次数，并检查每个数字及其相邻数字是否满足条件。

算法步骤:
1. 使用一个哈希表 `count` 来记录每个数字的出现次数。
2. 遍历 `nums` 数组，填充 `count` 哈希表。
3. 再次遍历 `nums` 数组，对于每个数字 `num`，检查其出现次数是否为 1 且其相邻数字 `num-1` 和 `num+1` 是否不在 `count` 哈希表中。
4. 如果满足条件，将该数字加入结果列表 `result` 中。
5. 返回结果列表 `result`。

关键点:
- 使用哈希表来高效地记录和查询每个数字的出现次数。
- 通过两次遍历来分别处理计数和筛选。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。我们需要两次遍历数组，每次遍历的时间复杂度都是 O(n)。
空间复杂度: O(n)，哈希表 `count` 在最坏情况下需要存储所有不同的数字。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_lonely_numbers(nums: List[int]) -> List[int]:
    """
    函数式接口 - 找出数组中的所有孤独数字
    """
    # 使用哈希表记录每个数字的出现次数
    count = {}
    for num in nums:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    # 结果列表
    result = []
    
    # 遍历数组，检查每个数字是否为孤独数字
    for num in nums:
        if count[num] == 1 and (num - 1 not in count) and (num + 1 not in count):
            result.append(num)
    
    return result


Solution = create_solution(find_lonely_numbers)