"""
873. 最长的斐波那契子序列的长度 - 如果序列 x1, x2, ..., xn 满足下列条件，就说它是 斐波那契式 的： * n >= 3 * 对于所有 i + 2 <= n，都有 xi + xi+1 == xi+2 给定一个 严格递增 的正整数数组形成序列 arr ，找到 arr 中最长的斐波那契式的子序列的长度。如果不存在，返回 0 。 子序列 是通过从另一个序列 arr 中删除任意数量的元素（包括删除 0 个元素）得到的，同时不改变剩余元素顺序。例如，[3, 5, 8] 是 [3, 4, 5, 6, 7, 8] 的子序列。 示例 1： 输入: arr = [1,2,3,4,5,6,7,8] 输出: 5 解释: 最长的斐波那契式子序列为 [1,2,3,5,8] 。 示例 2： 输入: arr = [1,3,7,11,12,14,18] 输出: 3 解释: 最长的斐波那契式子序列有 [1,11,12]、[3,11,14] 以及 [7,11,18] 。 提示： * 3 <= arr.length <= 1000 * 1 <= arr[i] < arr[i + 1] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和哈希表来记录每个可能的斐波那契子序列的长度。

算法步骤:
1. 初始化一个哈希表 `dp`，其中 `dp[(a, b)]` 表示以 `a` 和 `b` 结尾的斐波那契子序列的长度。
2. 遍历数组中的每个元素 `c`，对于每个 `c`，再遍历其之前的所有元素 `b`，并检查 `c - b` 是否在数组中。
3. 如果 `c - b` 在数组中，更新 `dp[(b, c)]` 为 `dp[(c - b, b)] + 1`。
4. 记录最长的斐波那契子序列的长度。

关键点:
- 使用哈希表来快速查找 `c - b` 是否在数组中。
- 动态规划的状态转移方程为 `dp[(b, c)] = dp[(c - b, b)] + 1`。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是数组的长度。我们需要遍历数组中的每对元素。
空间复杂度: O(n^2)，哈希表 `dp` 的大小最多为 n^2。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def lenLongestFibSubseq(arr: List[int]) -> int:
    n = len(arr)
    if n < 3:
        return 0
    
    # 哈希表用于快速查找
    index_map = {arr[i]: i for i in range(n)}
    
    # 动态规划表
    dp = {}
    max_length = 0
    
    for k in range(2, n):
        for j in range(1, k):
            i = index_map.get(arr[k] - arr[j], -1)
            if 0 <= i < j:
                dp[(j, k)] = dp.get((i, j), 2) + 1
                max_length = max(max_length, dp[(j, k)])
    
    return max_length if max_length >= 3 else 0

