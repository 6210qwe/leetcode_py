"""
3492. 船上可以装载的最大集装箱数量 - 给你一个正整数 n，表示船上的一个 n x n 的货物甲板。甲板上的每个单元格可以装载一个重量 恰好 为 w 的集装箱。 然而，如果将所有集装箱装载到甲板上，其总重量不能超过船的最大承载重量 maxWeight。 请返回可以装载到船上的 最大 集装箱数量。 示例 1： 输入： n = 2, w = 3, maxWeight = 15 输出： 4 解释： 甲板有 4 个单元格，每个集装箱的重量为 3。将所有集装箱装载后，总重量为 12，未超过 maxWeight。 示例 2： 输入： n = 3, w = 5, maxWeight = 20 输出： 4 解释： 甲板有 9 个单元格，每个集装箱的重量为 5。可以装载的最大集装箱数量为 4，此时总重量不超过 maxWeight。 提示： * 1 <= n <= 1000 * 1 <= w <= 1000 * 1 <= maxWeight <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算可以装载的最大集装箱数量，确保总重量不超过最大承载重量。

算法步骤:
1. 计算甲板上的总单元格数。
2. 计算在不超重的情况下可以装载的最大集装箱数量。
3. 返回结果。

关键点:
- 使用整数除法和取整操作来确保结果是整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 只涉及基本的算术运算。
空间复杂度: O(1) - 不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, w: int, maxWeight: int) -> int:
    """
    函数式接口 - 计算船上可以装载的最大集装箱数量
    """
    # 计算甲板上的总单元格数
    total_cells = n * n
    
    # 计算在不超重的情况下可以装载的最大集装箱数量
    max_containers = min(total_cells, maxWeight // w)
    
    return max_containers


Solution = create_solution(solution_function_name)