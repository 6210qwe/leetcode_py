import pandas as pd

def solution_function_name(students: pd.DataFrame) -> pd.DataFrame:
    """
    函数式接口 - 重命名 DataFrame 的列
    """
    # 定义重命名映射字典
    rename_map = {
        'id': 'student_id',
        'first': 'first_name',
        'last': 'last_name',
        'age': 'age_in_years'
    }
    
    # 使用 Pandas 的 rename 方法重命名列
    students_renamed = students.rename(columns=rename_map)
    
