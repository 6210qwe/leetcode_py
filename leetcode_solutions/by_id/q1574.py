"""
1464. 数组中两元素的最大乘积 - 给你一个整数数组 nums，请你选择数组的两个不同下标 i 和 j，使 (nums[i]-1)*(nums[j]-1) 取得最大值。 请你计算并返回该式的最大值。 示例 1： 输入：nums = [3,4,5,2] 输出：12 解释：如果选择下标 i=1 和 j=2（下标从 0 开始），则可以获得最大值，(nums[1]-1)*(nums[2]-1) = (4-1)*(5-1) = 3*4 = 12 。 示例 2： 输入：nums = [1,5,4,5] 输出：16 解释：选择下标 i=1 和 j=3（下标从 0 开始），则可以获得最大值 (5-1)*(5-1) = 16 。 示例 3： 输入：nums = [3,7] 输出：12 提示： * 2 <= nums.length <= 500 * 1 <= nums[i] <= 10^3
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到数组中的最大值和次大值，然后计算 (max1 - 1) * (max2 - 1)。

算法步骤:
1. 初始化两个变量 max1 和 max2，分别表示最大值和次大值。
2. 遍历数组，更新 max1 和 max2。
3. 计算并返回 (max1 - 1) * (max2 - 1)。

关键点:
- 通过一次遍历找到最大值和次大值，确保时间复杂度为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度，因为只需要遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_product(nums: List[int]) -> int:
    """
    函数式接口 - 计算数组中两元素的最大乘积
    """
    if not nums or len(nums) < 2:
        raise ValueError("输入数组长度必须大于等于2")

    # 初始化最大值和次大值
    max1, max2 = float('-inf'), float('-inf')

    # 遍历数组，更新最大值和次大值
    for num in nums:
        if num > max1:
            max2 = max1
            max1 = num
        elif num > max2:
            max2 = num

    # 计算并返回结果
    return (max1 - 1) * (max2 - 1)


Solution = create_solution(max_product)