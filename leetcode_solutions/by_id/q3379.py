"""
3110. 字符串的分数 - 给你一个字符串 s 。一个字符串的 分数 定义为相邻字符 ASCII 码差值绝对值的和。 请你返回 s 的 分数 。 示例 1： 输入：s = "hello" 输出：13 解释： s 中字符的 ASCII 码分别为：'h' = 104 ，'e' = 101 ，'l' = 108 ，'o' = 111 。所以 s 的分数为 |104 - 101| + |101 - 108| + |108 - 108| + |108 - 111| = 3 + 7 + 0 + 3 = 13 。 示例 2： 输入：s = "zaz" 输出：50 解释： s 中字符的 ASCII 码分别为：'z' = 122 ，'a' = 97 。所以 s 的分数为 |122 - 97| + |97 - 122| = 25 + 25 = 50 。 提示： * 2 <= s.length <= 100 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算相邻字符的 ASCII 码差值绝对值的和。

算法步骤:
1. 初始化一个变量 `score` 用于存储总分数。
2. 遍历字符串，从第二个字符开始，计算当前字符与前一个字符的 ASCII 码差值绝对值，并累加到 `score`。
3. 返回 `score`。

关键点:
- 使用 Python 内置函数 `ord` 获取字符的 ASCII 码。
- 使用 `abs` 函数计算绝对值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历一次字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算字符串的分数
    """
    score = 0
    for i in range(1, len(s)):
        score += abs(ord(s[i]) - ord(s[i - 1]))
    return score


Solution = create_solution(solution_function_name)