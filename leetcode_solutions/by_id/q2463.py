"""
2379. 得到 K 个黑块的最少涂色次数 - 给你一个长度为 n 下标从 0 开始的字符串 blocks ，blocks[i] 要么是 'W' 要么是 'B' ，表示第 i 块的颜色。字符 'W' 和 'B' 分别表示白色和黑色。 给你一个整数 k ，表示想要 连续 黑色块的数目。 每一次操作中，你可以选择一个白色块将它 涂成 黑色块。 请你返回至少出现 一次 连续 k 个黑色块的 最少 操作次数。 示例 1： 输入：blocks = "WBBWWBBWBW", k = 7 输出：3 解释： 一种得到 7 个连续黑色块的方法是把第 0 ，3 和 4 个块涂成黑色。 得到 blocks = "BBBBBBBWBW" 。 可以证明无法用少于 3 次操作得到 7 个连续的黑块。 所以我们返回 3 。 示例 2： 输入：blocks = "WBWBBBW", k = 2 输出：0 解释： 不需要任何操作，因为已经有 2 个连续的黑块。 所以我们返回 0 。 提示： * n == blocks.length * 1 <= n <= 100 * blocks[i] 要么是 'W' ，要么是 'B' 。 * 1 <= k <= n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到包含 k 个连续黑色块所需的最少涂色次数。

算法步骤:
1. 初始化一个滑动窗口，窗口大小为 k。
2. 计算初始窗口内的白色块数量。
3. 滑动窗口从左到右遍历整个字符串，每次移动一格，更新窗口内的白色块数量。
4. 记录最小的白色块数量，即为最少的涂色次数。

关键点:
- 使用滑动窗口技术，可以高效地找到每个窗口内的白色块数量。
- 通过维护一个计数器来记录当前窗口内的白色块数量，可以在 O(1) 时间内更新。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_recolors(blocks: str, k: int) -> int:
    """
    函数式接口 - 返回得到 k 个连续黑色块的最少涂色次数
    """
    # 初始化窗口内的白色块数量
    white_count = blocks[:k].count('W')
    min_operations = white_count

    # 滑动窗口从左到右遍历
    for i in range(k, len(blocks)):
        if blocks[i - k] == 'W':
            white_count -= 1
        if blocks[i] == 'W':
            white_count += 1
        min_operations = min(min_operations, white_count)

    return min_operations


Solution = create_solution(min_recolors)