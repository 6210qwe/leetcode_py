"""
面试题 08.04. 幂集 - 幂集。编写一种方法，返回某集合的所有子集。集合中 不包含重复的元素。 说明：解集不能包含重复的子集。 示例： 输入：nums = [1,2,3] 输出： [ [3], [1], [2], [1,2,3], [1,3], [2,3], [1,2], [] ]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算生成所有子集

算法步骤:
1. 初始化一个空列表 `subsets` 用于存储所有子集。
2. 遍历从 0 到 2^n - 1 的所有整数，其中 n 是输入数组的长度。
3. 对于每个整数，使用位运算检查每一位是否为 1，如果是，则将对应的元素加入当前子集。
4. 将当前子集加入 `subsets` 列表。

关键点:
- 使用位运算可以高效地生成所有子集
- 时间复杂度和空间复杂度都达到最优
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n * n)，其中 n 是输入数组的长度。生成所有子集需要 2^n 次操作，每次操作需要 O(n) 的时间来构建子集。
空间复杂度: O(2^n * n)，存储所有子集需要的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def power_set(nums: List[int]) -> List[List[int]]:
    """
    函数式接口 - 返回某集合的所有子集
    """
    n = len(nums)
    subsets = []
    
    for i in range(2 ** n):
        subset = []
        for j in range(n):
            if (i >> j) & 1:
                subset.append(nums[j])
        subsets.append(subset)
    
    return subsets


