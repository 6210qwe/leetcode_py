"""
2169. 得到 0 的操作数 - 给你两个 非负 整数 num1 和 num2 。 每一步 操作 中，如果 num1 >= num2 ，你必须用 num1 减 num2 ；否则，你必须用 num2 减 num1 。 * 例如，num1 = 5 且 num2 = 4 ，应该用 num1 减 num2 ，因此，得到 num1 = 1 和 num2 = 4 。然而，如果 num1 = 4且 num2 = 5 ，一步操作后，得到 num1 = 4 和 num2 = 1 。 返回使 num1 = 0 或 num2 = 0 的 操作数 。 示例 1： 输入：num1 = 2, num2 = 3 输出：3 解释： - 操作 1 ：num1 = 2 ，num2 = 3 。由于 num1 < num2 ，num2 减 num1 得到 num1 = 2 ，num2 = 3 - 2 = 1 。 - 操作 2 ：num1 = 2 ，num2 = 1 。由于 num1 > num2 ，num1 减 num2 。 - 操作 3 ：num1 = 1 ，num2 = 1 。由于 num1 == num2 ，num1 减 num2 。 此时 num1 = 0 ，num2 = 1 。由于 num1 == 0 ，不需要再执行任何操作。 所以总操作数是 3 。 示例 2： 输入：num1 = 10, num2 = 10 输出：1 解释： - 操作 1 ：num1 = 10 ，num2 = 10 。由于 num1 == num2 ，num1 减 num2 得到 num1 = 10 - 10 = 0 。 此时 num1 = 0 ，num2 = 10 。由于 num1 == 0 ，不需要再执行任何操作。 所以总操作数是 1 。 提示： * 0 <= num1, num2 <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用欧几里得算法（辗转相除法）来计算最大公约数，并利用最大公约数来减少操作次数。

算法步骤:
1. 初始化操作计数器为 0。
2. 使用辗转相除法计算 num1 和 num2 的最大公约数。
3. 计算操作次数为 (num1 / gcd) + (num2 / gcd) - 2，其中 gcd 是最大公约数。

关键点:
- 使用辗转相除法可以高效地计算最大公约数。
- 通过最大公约数可以减少不必要的减法操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log(min(num1, num2))) - 辗转相除法的时间复杂度是对数级别的。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_operations(num1: int, num2: int) -> int:
    """
    函数式接口 - 计算使 num1 或 num2 为 0 的操作数
    """
    # 辗转相除法计算最大公约数
    def gcd(a: int, b: int) -> int:
        while b:
            a, b = b, a % b
        return a

    # 计算操作次数
    if num1 == 0 or num2 == 0:
        return 0
    gcd_value = gcd(num1, num2)
    return (num1 // gcd_value) + (num2 // gcd_value) - 2


Solution = create_solution(count_operations)