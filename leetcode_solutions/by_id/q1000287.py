"""
LCR 101. 分割等和子集 - 给定一个非空的正整数数组 nums ，请判断能否将这些数字分成元素和相等的两部分。 示例 1： 输入：nums = [1,5,11,5] 输出：true 解释：nums 可以分割成 [1, 5, 5] 和 [11] 。 示例 2： 输入：nums = [1,2,3,5] 输出：false 解释：nums 不可以分为和相等的两部分 提示： * 1 <= nums.length <= 200 * 1 <= nums[i] <= 100 注意：本题与主站 416 题相同： https://leetcode.cn/problems/partition-equal-subset-sum/ [https://leetcode.cn/problems/partition-equal-subset-sum/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们需要找到一个子集，其和等于整个数组和的一半。如果总和是奇数，则不可能分割成两个和相等的子集。

算法步骤:
1. 计算数组的总和，如果总和是奇数，直接返回 False。
2. 初始化一个布尔型的 DP 数组，表示是否可以达到某个和。
3. 遍历数组中的每个元素，更新 DP 数组。
4. 最后检查 DP 数组中是否存在一个值为 True 的位置，该位置的索引等于总和的一半。

关键点:
- 动态规划的状态转移方程：dp[j] = dp[j] or dp[j - num]，其中 j 是当前的目标和，num 是当前处理的元素。
- 为了确保状态转移正确，需要从后向前更新 DP 数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * target)，其中 n 是数组的长度，target 是数组总和的一半。
空间复杂度: O(target)，DP 数组的大小为 target + 1。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_partition(nums: List[int]) -> bool:
    """
    函数式接口 - 判断是否可以将数组分割成两个和相等的子集
    """
    total_sum = sum(nums)
    if total_sum % 2 != 0:
        return False
    
    target = total_sum // 2
    dp = [False] * (target + 1)
    dp[0] = True
    
    for num in nums:
        for j in range(target, num - 1, -1):
            dp[j] = dp[j] or dp[j - num]
    
    return dp[target]


Solution = create_solution(can_partition)