"""
LCS 02. 完成一半题目 - 有 `N` 位扣友参加了微软与力扣举办了「以扣会友」线下活动。主办方提供了 `2*N` 道题目，整型数组 `questions` 中每个数字对应了每道题目所涉及的知识点类型。 若每位扣友选择不同的一题，请返回被选的 `N` 道题目至少包含多少种知识点类型。 **示例 1：** >输入：`questions = [2,1,6,2]` > >输出：`1` > >解释：有 2 位扣友在 4 道题目中选择 2 题。 > 可选择完成知识点类型为 2 的题目时，此时仅一种知识点类型 > 因此至少包含 1 种知识点类型。 **示例 2：** >输入：`questions = [1,5,1,3,4,5,2,5,3,3,8,6]` > >输出：`2` > >解释：有 6 位扣友在 12 道题目中选择题目，需要选择 6 题。 > 选择完成知识点类型为 3、5 的题目，因此至少包含 2 种知识点类型。 **提示：** - `questions.length == 2*n` - `2 <= questions.length <= 10^5` - `1 <= questions[i] <= 1000`
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每种知识点类型的出现次数，然后按出现次数从大到小排序，选择前 N 道题目。

算法步骤:
1. 使用哈希表统计每种知识点类型的出现次数。
2. 按出现次数从大到小排序。
3. 依次选择题目，直到选择的题目数量达到 N。

关键点:
- 使用哈希表统计频率。
- 按频率排序并选择题目。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + k log k)，其中 n 是 questions 的长度，k 是不同知识点类型的数量。
空间复杂度: O(k)，其中 k 是不同知识点类型的数量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(questions: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 统计每种知识点类型的出现次数
    count = {}
    for question in questions:
        if question in count:
            count[question] += 1
        else:
            count[question] = 1
    
    # 按出现次数从大到小排序
    sorted_count = sorted(count.items(), key=lambda x: -x[1])
    
    # 选择前 N 道题目
    n = len(questions) // 2
    selected_types = 0
    for _, freq in sorted_count:
        n -= freq
        selected_types += 1
        if n <= 0:
            break
    
    return selected_types


Solution = create_solution(solution_function_name)