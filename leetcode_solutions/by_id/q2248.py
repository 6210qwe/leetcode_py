"""
2144. 打折购买糖果的最小开销 - 一家商店正在打折销售糖果。每购买 两个 糖果，商店会 免费 送一个糖果。 免费送的糖果唯一的限制是：它的价格需要小于等于购买的两个糖果价格的 较小值 。 * 比方说，总共有 4 个糖果，价格分别为 1 ，2 ，3 和 4 ，一位顾客买了价格为 2 和 3 的糖果，那么他可以免费获得价格为 1 的糖果，但不能获得价格为 4 的糖果。 给你一个下标从 0 开始的整数数组 cost ，其中 cost[i] 表示第 i 个糖果的价格，请你返回获得 所有 糖果的 最小 总开销。 示例 1： 输入：cost = [1,2,3] 输出：5 解释：我们购买价格为 2 和 3 的糖果，然后免费获得价格为 1 的糖果。 总开销为 2 + 3 = 5 。这是开销最小的 唯一 方案。 注意，我们不能购买价格为 1 和 3 的糖果，并免费获得价格为 2 的糖果。 这是因为免费糖果的价格必须小于等于购买的 2 个糖果价格的较小值。 示例 2： 输入：cost = [6,5,7,9,2,2] 输出：23 解释：最小总开销购买糖果方案为： - 购买价格为 9 和 7 的糖果 - 免费获得价格为 6 的糖果 - 购买价格为 5 和 2 的糖果 - 免费获得价格为 2 的最后一个糖果 因此，最小总开销为 9 + 7 + 5 + 2 = 23 。 示例 3： 输入：cost = [5,5] 输出：10 解释：由于只有 2 个糖果，我们需要将它们都购买，而且没有免费糖果。 所以总最小开销为 5 + 5 = 10 。 提示： * 1 <= cost.length <= 100 * 1 <= cost[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，先对糖果价格进行降序排序，然后每次选择前两个价格最高的糖果购买，并免费获得下一个价格较低的糖果。

算法步骤:
1. 对糖果价格数组进行降序排序。
2. 初始化总开销为 0。
3. 遍历排序后的糖果价格数组，每次取三个糖果，前两个糖果计入总开销，第三个糖果免费。
4. 返回总开销。

关键点:
- 通过降序排序确保每次选择的两个糖果价格最高，从而使得免费获得的糖果价格最低。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是糖果数量。主要由排序操作决定。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_cost(cost: List[int]) -> int:
    """
    函数式接口 - 计算打折购买糖果的最小开销
    """
    # 对糖果价格进行降序排序
    cost.sort(reverse=True)
    
    total_cost = 0
    for i in range(len(cost)):
        if i % 3 < 2:  # 每三个糖果中，前两个计入总开销
            total_cost += cost[i]
    
    return total_cost


Solution = create_solution(minimum_cost)