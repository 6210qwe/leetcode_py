"""
1383. 最大的团队表现值 - 给定两个整数 n 和 k，以及两个长度为 n 的整数数组 speed 和 efficiency。现有 n 名工程师，编号从 1 到 n。其中 speed[i] 和 efficiency[i] 分别代表第 i 位工程师的速度和效率。 从这 n 名工程师中最多选择 k 名不同的工程师，使其组成的团队具有最大的团队表现值。 团队表现值 的定义为：一个团队中「所有工程师速度的和」乘以他们「效率值中的最小值」。 请你返回该团队的 最大团队表现值，由于答案可能很大，请你返回结果对 10^9 + 7 取余后的结果。 示例 1： 输入：n = 6, speed = [2,10,3,1,5,8], efficiency = [5,4,3,9,7,2], k = 2 输出：60 解释： 我们选择工程师 2（speed=10 且 efficiency=4）和工程师 5（speed=5 且 efficiency=7）。他们的团队表现值为 performance = (10 + 5) * min(4, 7) = 60 。 示例 2： 输入：n = 6, speed = [2,10,3,1,5,8], efficiency = [5,4,3,9,7,2], k = 3 输出：68 解释： 此示例与第一个示例相同，除了 k = 3 。我们可以选择工程师 1 ，工程师 2 和工程师 5 得到最大的团队表现值。表现值为 performance = (2 + 10 + 5) * min(5, 4, 7) = 68 。 示例 3： 输入：n = 6, speed = [2,10,3,1,5,8], efficiency = [5,4,3,9,7,2], k = 4 输出：72 提示： * 1 <= k <= n <= 10^5 * speed.length == n * efficiency.length == n * 1 <= speed[i] <= 10^5 * 1 <= efficiency[i] <= 10^8
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和优先队列来选择最优的工程师组合。

算法步骤:
1. 将工程师按照效率从高到低排序。
2. 使用一个最小堆来维护当前选择的工程师的速度和。
3. 遍历排序后的工程师列表，对于每个工程师：
   - 将其速度加入堆中。
   - 如果堆的大小超过 k，则移除堆中最小的速度。
   - 计算当前团队的表现值，并更新最大表现值。
4. 返回最大表现值对 10^9 + 7 取余的结果。

关键点:
- 按效率从高到低排序，确保每次选择的都是当前效率最高的工程师。
- 使用最小堆来维护当前选择的工程师的速度和，确保堆的大小不超过 k。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是工程师的数量。排序操作的时间复杂度是 O(n log n)，遍历和堆操作的时间复杂度是 O(n log k)。
空间复杂度: O(k)，用于存储最小堆。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def max_performance(n: int, speed: List[int], efficiency: List[int], k: int) -> int:
    MOD = 10**9 + 7
    engineers = sorted(zip(efficiency, speed), reverse=True)
    
    speed_heap = []
    speed_sum = 0
    max_performance = 0
    
    for e, s in engineers:
        if len(speed_heap) == k:
            speed_sum -= heapq.heappop(speed_heap)
        heapq.heappush(speed_heap, s)
        speed_sum += s
        max_performance = max(max_performance, speed_sum * e)
    
    return max_performance % MOD

