"""
2032. 至少在两个数组中出现的值 - 给你三个整数数组 nums1、nums2 和 nums3 ，请你构造并返回一个 元素各不相同的 数组，且由 至少 在 两个 数组中出现的所有值组成。数组中的元素可以按 任意 顺序排列。 示例 1： 输入：nums1 = [1,1,3,2], nums2 = [2,3], nums3 = [3] 输出：[3,2] 解释：至少在两个数组中出现的所有值为： - 3 ，在全部三个数组中都出现过。 - 2 ，在数组 nums1 和 nums2 中出现过。 示例 2： 输入：nums1 = [3,1], nums2 = [2,3], nums3 = [1,2] 输出：[2,3,1] 解释：至少在两个数组中出现的所有值为： - 2 ，在数组 nums2 和 nums3 中出现过。 - 3 ，在数组 nums1 和 nums2 中出现过。 - 1 ，在数组 nums1 和 nums3 中出现过。 示例 3： 输入：nums1 = [1,2,2], nums2 = [4,3,3], nums3 = [5] 输出：[] 解释：不存在至少在两个数组中出现的值。 提示： * 1 <= nums1.length, nums2.length, nums3.length <= 100 * 1 <= nums1[i], nums2[j], nums3[k] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来记录每个数组中出现的元素，并通过集合的交集操作找到至少在两个数组中出现的元素。

算法步骤:
1. 将每个数组转换为集合，以去除重复元素。
2. 计算这三个集合的交集，得到至少在两个数组中出现的元素。
3. 返回结果列表。

关键点:
- 使用集合操作来高效地找到至少在两个数组中出现的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n1 + n2 + n3)，其中 n1, n2, n3 分别是三个数组的长度。
空间复杂度: O(n1 + n2 + n3)，用于存储三个集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def two_out_of_three(nums1: List[int], nums2: List[int], nums3: List[int]) -> List[int]:
    """
    函数式接口 - 找到至少在两个数组中出现的元素
    """
    set1 = set(nums1)
    set2 = set(nums2)
    set3 = set(nums3)
    
    # 计算三个集合的并集，然后过滤出至少在两个集合中出现的元素
    result_set = (set1 & set2) | (set1 & set3) | (set2 & set3)
    
    return list(result_set)


Solution = create_solution(two_out_of_three)