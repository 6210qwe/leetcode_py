"""
898. 子数组按位或操作 - 给定一个整数数组 arr，返回所有 arr 的非空子数组的不同按位或的数量。 子数组的按位或是子数组中每个整数的按位或。含有一个整数的子数组的按位或就是该整数。 子数组 是数组内连续的非空元素序列。 示例 1： 输入：arr = [0] 输出：1 解释： 只有一个可能的结果 0 。 示例 2： 输入：arr = [1,1,2] 输出：3 解释： 可能的子数组为 [1]，[1]，[2]，[1, 1]，[1, 2]，[1, 1, 2]。 产生的结果为 1，1，2，1，3，3 。 有三个唯一值，所以答案是 3 。 示例 3： 输入：arr = [1,2,4] 输出：6 解释： 可能的结果是 1，2，3，4，6，以及 7 。 提示： * 1 <= nums.length <= 5 * 104 * 0 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来存储所有子数组的按位或结果，并利用前缀和的思想来减少重复计算。

算法步骤:
1. 初始化一个集合 `results` 来存储所有的按位或结果。
2. 遍历数组中的每个元素，对于每个元素，维护一个当前的按位或结果 `current_or`。
3. 对于每个元素，更新 `current_or` 并将其添加到 `results` 集合中。
4. 返回 `results` 集合的大小。

关键点:
- 使用集合来存储结果，确保结果的唯一性。
- 利用前缀和的思想，减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是数组的长度。我们需要遍历每个元素，并对每个元素进行一次完整的子数组按位或计算。
空间复杂度: O(n^2)，在最坏情况下，集合 `results` 中会存储 O(n^2) 个不同的按位或结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int]) -> int:
    """
    函数式接口 - 返回所有 arr 的非空子数组的不同按位或的数量
    """
    results = set()
    
    for i in range(len(arr)):
        current_or = 0
        for j in range(i, len(arr)):
            current_or |= arr[j]
            results.add(current_or)
    
    return len(results)


Solution = create_solution(solution_function_name)