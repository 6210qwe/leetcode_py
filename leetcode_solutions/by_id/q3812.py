"""
3517. 最小回文排列 I - 给你一个 回文 字符串 s。 返回 s 的按字典序排列的 最小 回文排列。 如果一个字符串从前往后和从后往前读都相同，那么这个字符串是一个 回文 字符串。 排列 是字符串中所有字符的重排。 如果字符串 a 按字典序小于字符串 b，则表示在第一个不同的位置，a 中的字符比 b 中的对应字符在字母表中更靠前。 如果在前 min(a.length, b.length) 个字符中没有区别，则较短的字符串按字典序更小。 示例 1： 输入： s = "z" 输出： "z" 解释： 仅由一个字符组成的字符串已经是按字典序最小的回文。 示例 2： 输入： s = "babab" 输出： "abbba" 解释： 通过重排 "babab" → "abbba"，可以得到按字典序最小的回文。 示例 3： 输入： s = "daccad" 输出： "acddca" 解释： 通过重排 "daccad" → "acddca"，可以得到按字典序最小的回文。 提示： * 1 <= s.length <= 105 * s 由小写英文字母组成。 * 保证 s 是回文字符串。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数排序来构建按字典序最小的回文字符串。

算法步骤:
1. 统计每个字符的出现次数。
2. 构建前半部分字符串，按字典序从小到大排列。
3. 构建中间部分字符串，如果字符总数为奇数，则中间部分为出现次数为奇数的字符。
4. 构建后半部分字符串，按字典序从大到小排列。
5. 将前半部分、中间部分和后半部分拼接成最终结果。

关键点:
- 使用计数数组来统计字符出现次数。
- 按字典序构建前半部分和后半部分字符串。
- 处理字符总数为奇数的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历字符串一次进行字符计数，然后再次遍历计数数组构建结果字符串。
空间复杂度: O(1)，因为计数数组的大小是固定的（26 个小写字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_palindromic_rearrangement(s: str) -> str:
    """
    函数式接口 - 返回 s 的按字典序排列的最小回文排列
    """
    # 统计每个字符的出现次数
    count = [0] * 26
    for char in s:
        count[ord(char) - ord('a')] += 1
    
    # 构建前半部分字符串
    first_half = []
    for i in range(26):
        if count[i] > 0:
            first_half.append(chr(i + ord('a')) * (count[i] // 2))
            count[i] %= 2
    
    # 构建中间部分字符串
    middle_part = []
    for i in range(26):
        if count[i] == 1:
            middle_part.append(chr(i + ord('a')))
            break
    
    # 构建后半部分字符串
    second_half = first_half[::-1]
    
    # 拼接成最终结果
    result = ''.join(first_half) + ''.join(middle_part) + ''.join(second_half)
    return result


Solution = create_solution(smallest_palindromic_rearrangement)