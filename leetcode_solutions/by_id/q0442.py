def find_all_duplicates_in_an_array(nums: List[int]) -> List[int]:
    """
    函数式接口 - 找出数组中所有出现两次的整数
    
    实现思路:
    通过原地修改的方法，将每个数字放到它应该在的位置上，如果某个位置上的数字已经正确，则标记该位置对应的数字为重复。
    
    Args:
        nums: 整数数组，其中 nums 的所有整数都在范围 [1, n] 内，且每个整数出现 最多两次。
        
    Returns:
        包含所有出现两次的整数的列表。
        
    Example:
        >>> find_all_duplicates_in_an_array([4,3,2,7,8,2,3,1])
        [2, 3]
    """
    result = []
    for i in range(len(nums)):
        while nums[i] != nums[nums[i] - 1]:
            # 交换 nums[i] 和 nums[nums[i] - 1]
            nums[nums[i] - 1], nums[i] = nums[i], nums[nums[i] - 1]
        if nums[i] - 1 != i and nums[i] == nums[nums[i] - 1]:
            result.append(nums[i])
    return result


