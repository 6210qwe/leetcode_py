"""
2811. 判断是否能拆分数组 - 给你一个长度为 n 的数组 nums 和一个整数 m 。请你判断能否执行一系列操作，将数组拆分成 n 个 非空 数组。 一个数组被称为 好 的，如果： * 子数组的长度为 1 ，或者 * 子数组元素之和 大于或等于 m 。 在每一步操作中，你可以选择一个 长度至少为 2 的现有数组（之前步骤的结果） 并将其拆分成 2 个子数组，而得到的 每个 子数组都需要是好的。 如果你可以将给定数组拆分成 n 个满足要求的数组，返回 true ；否则，返回 false 。 示例 1： 输入：nums = [2, 2, 1], m = 4 输出：true 解释： * 将 [2, 2, 1] 切分为 [2, 2] 和 [1]。数组 [1] 的长度为 1，数组 [2, 2] 的元素之和等于 4 >= m，所以两者都是好的数组。 * 将 [2, 2] 切分为 [2] 和 [2]。两个数组的长度都是 1，所以都是好的数组。 示例 2： 输入：nums = [2, 1, 3], m = 5 输出：false 解释： 第一步必须是以下之一： * 将 [2, 1, 3] 切分为 [2, 1] 和 [3]。数组 [2, 1] 既不是长度为 1，也没有大于或等于 m 的元素和。 * 将 [2, 1, 3] 切分为 [2] 和 [1, 3]。数组 [1, 3] 既不是长度为 1，也没有大于或等于 m 的元素和。 因此，由于这两个操作都无效（它们没有将数组分成两个好的数组），因此我们无法将 nums 分成 n 个大小为 1 的数组。 示例 3： 输入：nums = [2, 3, 3, 2, 3], m = 6 输出：true 解释： * 将 [2, 3, 3, 2, 3] 切分为 [2] 和 [3, 3, 2, 3]。 * 将 [3, 3, 2, 3] 切分为 [3, 3, 2] 和 [3]。 * 将 [3, 3, 2] 切分为 [3, 3] 和 [2]。 * 将 [3, 3] 切分为 [3] 和 [3]。 提示： * 1 <= n == nums.length <= 100 * 1 <= nums[i] <= 100 * 1 <= m <= 200
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从右到左遍历数组，检查是否存在一个分割点使得左侧子数组和右侧子数组都满足条件。

算法步骤:
1. 从右到左遍历数组，计算当前子数组的和。
2. 如果当前子数组的和大于或等于 m，则继续向前遍历。
3. 如果找到一个分割点使得左侧子数组和右侧子数组都满足条件，则返回 True。
4. 如果遍历完数组仍未找到符合条件的分割点，则返回 False。

关键点:
- 从右到左遍历可以确保每次分割时右侧子数组总是满足条件。
- 只需要一次遍历即可完成判断。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
