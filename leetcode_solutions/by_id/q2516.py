"""
核心思想: 使用 SQL 查询对奥运表进行排序

算法步骤:
1. 从表中选择所有列
2. 按照金牌数降序排序
3. 如果金牌数相同，则按照银牌数降序排序
4. 如果银牌数也相同，则按照铜牌数降序排序
5. 如果奖牌数都相同，则按照国家名升序排序

关键点:
- 使用 SQL 的 ORDER BY 子句进行多级排序
"""

# ============================================================================
