def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询对奥运表进行排序
    """
    # 实现最优解法
    query = """
    SELECT *
    FROM OlympicTable
    ORDER BY Gold DESC, Silver DESC, Bronze DESC, Country ASC
    """
