"""
3774. 最大和最小 K 个元素的绝对差 - 给你一个整数数组 nums 和一个整数 k。 请计算以下两者的绝对差值： * 数组中 k 个 最大 元素的总和； * 数组中 k 个 最小 元素的总和。 返回表示此差值的整数。 示例 1： 输入： nums = [5,2,2,4], k = 2 输出： 5 解释： * k = 2 个最大的元素是 4 和 5。它们的总和是 4 + 5 = 9。 * k = 2 个最小的元素是 2 和 2。它们的总和是 2 + 2 = 4。 * 绝对差值是 abs(9 - 4) = 5。 示例 2： 输入： nums = [100], k = 1 输出： 0 解释： * 最大的元素是 100。 * 最小的元素是 100。 * 绝对差值是 abs(100 - 100) = 0。 提示： * 1 <= n == nums.length <= 100 * 1 <= nums[i] <= 100 * 1 <= k <= n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用排序来找到前 k 个最大和最小的元素，然后计算它们的和并求差值。

算法步骤:
1. 对数组进行排序。
2. 计算前 k 个最小元素的总和。
3. 计算后 k 个最大元素的总和。
4. 返回两者差值的绝对值。

关键点:
- 排序的时间复杂度为 O(n log n)，但这是必要的以确保我们能准确找到前 k 个最大和最小的元素。
- 空间复杂度为 O(1)，因为我们只使用了常数级的额外空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算数组中 k 个最大和最小元素的绝对差值
    """
    # 对数组进行排序
    nums.sort()
    
    # 计算前 k 个最小元素的总和
    min_sum = sum(nums[:k])
    
    # 计算后 k 个最大元素的总和
    max_sum = sum(nums[-k:])
    
    # 返回两者差值的绝对值
    return abs(max_sum - min_sum)


Solution = create_solution(solution_function_name)