"""
3091. 执行操作使数据元素之和大于等于 K - 给你一个正整数 k 。最初，你有一个数组 nums = [1] 。 你可以对数组执行以下 任意 操作 任意 次数（可能为零）： * 选择数组中的任何一个元素，然后将它的值 增加 1 。 * 复制数组中的任何一个元素，然后将它附加到数组的末尾。 返回使得最终数组元素之 和 大于或等于 k 所需的 最少 操作次数。 示例 1： 输入：k = 11 输出：5 解释： 可以对数组 nums = [1] 执行以下操作： * 将元素的值增加 1 三次。结果数组为 nums = [4] 。 * 复制元素两次。结果数组为 nums = [4,4,4] 。 最终数组的和为 4 + 4 + 4 = 12 ，大于等于 k = 11 。 执行的总操作次数为 3 + 2 = 5 。 示例 2： 输入：k = 1 输出：0 解释： 原始数组的和已经大于等于 1 ，因此不需要执行操作。 提示： * 1 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，尽可能通过增加元素值来减少复制操作的次数。

算法步骤:
1. 初始化当前数组和为 1，操作次数为 0。
2. 如果当前数组和已经大于等于 k，则返回操作次数。
3. 否则，计算当前数组和与 k 的差值，并找到最优的增加和复制策略。
4. 更新当前数组和和操作次数，直到满足条件。

关键点:
- 通过增加元素值来减少复制操作的次数。
- 通过二分查找来找到最优的增加和复制策略。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log k)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(k: int) -> int:
    """
    函数式接口 - 计算使得最终数组元素之和大于或等于 k 所需的最少操作次数
    """
    if k == 1:
        return 0

    current_sum = 1
    operations = 0

    while current_sum < k:
        # 通过二分查找找到最优的增加和复制策略
        left, right = 1, k
        while left < right:
            mid = (left + right) // 2
            if mid * (current_sum + mid) >= k:
                right = mid
            else:
                left = mid + 1

        # 更新当前数组和和操作次数
        current_sum += left
        operations += left

    return operations


Solution = create_solution(solution_function_name)