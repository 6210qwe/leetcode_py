"""
3222. 求出硬币游戏的赢家 - 给你两个 正 整数 x 和 y ，分别表示价值为 75 和 10 的硬币的数目。 Alice 和 Bob 正在玩一个游戏。每一轮中，Alice 先进行操作，Bob 后操作。每次操作中，玩家需要拿走价值 总和 为 115 的硬币。如果一名玩家无法执行此操作，那么这名玩家 输掉 游戏。 两名玩家都采取 最优 策略，请你返回游戏的赢家。 示例 1： 输入：x = 2, y = 7 输出："Alice" 解释： 游戏一次操作后结束： * Alice 拿走 1 枚价值为 75 的硬币和 4 枚价值为 10 的硬币。 示例 2： 输入：x = 4, y = 11 输出："Bob" 解释： 游戏 2 次操作后结束： * Alice 拿走 1 枚价值为 75 的硬币和 4 枚价值为 10 的硬币。 * Bob 拿走 1 枚价值为 75 的硬币和 4 枚价值为 10 的硬币。 提示： * 1 <= x, y <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算总价值是否能被 115 整除来判断胜负。

算法步骤:
1. 计算总价值 total_value = 75 * x + 10 * y。
2. 如果 total_value % 115 == 0，则 Alice 赢；否则 Bob 赢。

关键点:
- 通过数学计算直接判断胜负，避免了复杂的模拟过程。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_winner(x: int, y: int) -> str:
    """
    函数式接口 - 判断硬币游戏的赢家
    """
    # 计算总价值
    total_value = 75 * x + 10 * y
    
    # 判断胜负
    if total_value % 115 == 0:
        return "Alice"
    else:
        return "Bob"

Solution = create_solution(find_winner)