"""
1781. 所有子字符串美丽值之和 - 一个字符串的 美丽值 定义为：出现频率最高字符与出现频率最低字符的出现次数之差。 * 比方说，"abaacc" 的美丽值为 3 - 1 = 2 。 给你一个字符串 s ，请你返回它所有子字符串的 美丽值 之和。 示例 1： 输入：s = "aabcb" 输出：5 解释：美丽值不为零的字符串包括 ["aab","aabc","aabcb","abcb","bcb"] ，每一个字符串的美丽值都为 1 。 示例 2： 输入：s = "aabcbaa" 输出：17 提示： * 1 <= s.length <= 500 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和计数器来计算每个子字符串的美丽值。

算法步骤:
1. 初始化一个长度为 26 的数组 `freq` 来记录当前窗口内每个字符的频率。
2. 使用两个嵌套循环遍历所有可能的子字符串。
3. 对于每个子字符串，更新 `freq` 数组，并计算该子字符串的美丽值。
4. 将所有子字符串的美丽值累加并返回结果。

关键点:
- 使用固定大小的数组 `freq` 来记录字符频率，避免了频繁的哈希操作。
- 通过滑动窗口的方式减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串 s 的长度。需要遍历所有可能的子字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def beauty_sum(s: str) -> int:
    """
    计算字符串 s 所有子字符串的美丽值之和。
    """
    n = len(s)
    total_beauty = 0
    
    for i in range(n):
        freq = [0] * 26
        for j in range(i, n):
            freq[ord(s[j]) - ord('a')] += 1
            max_freq = max(freq)
            min_freq = min(f for f in freq if f > 0)
            total_beauty += max_freq - min_freq
    
    return total_beauty


Solution = create_solution(beauty_sum)