"""
2099. 找到和最大的长度为 K 的子序列 - 给你一个整数数组 nums 和一个整数 k 。你需要找到 nums 中长度为 k 的 子序列 ，且这个子序列的 和最大 。 请你返回 任意 一个长度为 k 的整数子序列。 子序列 定义为从一个数组里删除一些元素后，不改变剩下元素的顺序得到的数组。 示例 1： 输入：nums = [2,1,3,3], k = 2 输出：[3,3] 解释： 子序列有最大和：3 + 3 = 6 。 示例 2： 输入：nums = [-1,-2,3,4], k = 3 输出：[-1,3,4] 解释： 子序列有最大和：-1 + 3 + 4 = 6 。 示例 3： 输入：nums = [3,4,3,3], k = 2 输出：[3,4] 解释： 子序列有最大和：3 + 4 = 7 。 另一个可行的子序列为 [4, 3] 。 提示： * 1 <= nums.length <= 1000 * -105 <= nums[i] <= 105 * 1 <= k <= nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用堆（优先队列）来选择和最大的 k 个元素，并保持它们在原数组中的相对顺序。

算法步骤:
1. 将数组元素及其索引存入一个列表中。
2. 对该列表按元素值降序排序。
3. 从排序后的列表中选择前 k 个元素，并按其在原数组中的索引顺序返回。

关键点:
- 使用堆来高效地选择和最大的 k 个元素。
- 保持选出的 k 个元素在原数组中的相对顺序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，存储元素及其索引的列表需要 O(n) 的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def find_largest_subsequence(nums: List[int], k: int) -> List[int]:
    """
    函数式接口 - 找到和最大的长度为 k 的子序列
    """
    # 将数组元素及其索引存入一个列表中
    indexed_nums = [(num, i) for i, num in enumerate(nums)]
    
    # 按元素值降序排序
    indexed_nums.sort(key=lambda x: -x[0])
    
    # 从排序后的列表中选择前 k 个元素
    largest_k_elements = indexed_nums[:k]
    
    # 按其在原数组中的索引顺序返回
    largest_k_elements.sort(key=lambda x: x[1])
    
    return [num for num, _ in largest_k_elements]

