"""
1432. 改变一个整数能得到的最大差值 - 给你一个整数 num 。你可以对它进行以下步骤共计 两次： * 选择一个数字 x (0 <= x <= 9). * 选择另一个数字 y (0 <= y <= 9) 。数字 y 可以等于 x 。 * 将 num 中所有出现 x 的数位都用 y 替换。 令两次对 num 的操作得到的结果分别为 a 和 b 。 请你返回 a 和 b 的 最大差值 。 注意，a 和 b 必须不能 含有前导 0，并且 不为 0。 示例 1： 输入：num = 555 输出：888 解释：第一次选择 x = 5 且 y = 9 ，并把得到的新数字保存在 a 中。 第二次选择 x = 5 且 y = 1 ，并把得到的新数字保存在 b 中。 现在，我们有 a = 999 和 b = 111 ，最大差值为 888 示例 2： 输入：num = 9 输出：8 解释：第一次选择 x = 9 且 y = 9 ，并把得到的新数字保存在 a 中。 第二次选择 x = 9 且 y = 1 ，并把得到的新数字保存在 b 中。 现在，我们有 a = 9 和 b = 1 ，最大差值为 8 示例 3： 输入：num = 123456 输出：820000 示例 4： 输入：num = 10000 输出：80000 示例 5： 输入：num = 9288 输出：8700 提示： * 1 <= num <= 10^8
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法找到替换后的最大值和最小值，然后计算它们的差值。

算法步骤:
1. 将 num 转换为字符串形式，方便逐位处理。
2. 找到第一个不是 '9' 的高位数字，将其替换为 '9'，生成最大值 max_num。
3. 找到第一个不是 '1' 且不是最高位的数字，将其替换为 '1' 或 '0'（如果最高位是 '1'），生成最小值 min_num。
4. 计算并返回 max_num 和 min_num 的差值。

关键点:
- 通过替换高位数字来最大化或最小化数值。
- 确保生成的数字没有前导零。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 num 的位数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: int) -> int:
    """
    函数式接口 - 通过贪心算法找到替换后的最大值和最小值，然后计算它们的差值。
    """
    num_str = str(num)
    
    # 生成最大值
    for i, ch in enumerate(num_str):
        if ch != '9':
            max_num = int(num_str.replace(ch, '9'))
            break
    else:
        max_num = num
    
    # 生成最小值
    if num_str[0] != '1':
        min_num = int(num_str.replace(num_str[0], '1', 1))
    else:
        for i, ch in enumerate(num_str[1:], start=1):
            if ch not in ('0', '1'):
                min_num = int(num_str[:i] + num_str[i:].replace(ch, '0', 1))
                break
        else:
            min_num = num
    
    return max_num - min_num


Solution = create_solution(solution_function_name)