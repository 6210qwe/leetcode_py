"""
3226. 使两个整数相等的位更改次数 - 给你两个正整数 n 和 k。 你可以选择 n 的 二进制表示 中任意一个值为 1 的位，并将其改为 0。 返回使得 n 等于 k 所需要的更改次数。如果无法实现，返回 -1。 示例 1： 输入： n = 13, k = 4 输出： 2 解释： 最初，n 和 k 的二进制表示分别为 n = (1101)2 和 k = (0100)2， 我们可以改变 n 的第一位和第四位。结果整数为 n = (0100)2 = k。 示例 2： 输入： n = 21, k = 21 输出： 0 解释： n 和 k 已经相等，因此不需要更改。 示例 3： 输入： n = 14, k = 13 输出： -1 解释： 无法使 n 等于 k。 提示： * 1 <= n, k <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算 n 和 k 的异或结果来确定需要更改的位数。如果 n 和 k 在某个位上不同，则该位在 n 中必须是 1 才能通过修改 n 使其等于 k。

算法步骤:
1. 计算 n 和 k 的异或结果。
2. 检查 n 和 k 的异或结果中所有位是否都在 n 中为 1。如果不是，返回 -1。
3. 计算异或结果中 1 的个数，即为需要更改的位数。

关键点:
- 使用异或操作来找出不同的位。
- 检查 n 中是否有足够的 1 来进行更改。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, k: int) -> int:
    """
    函数式接口 - 计算使两个整数相等的位更改次数
    """
    # 计算 n 和 k 的异或结果
    xor_result = n ^ k
    
    # 检查 n 中是否有足够的 1 来进行更改
    if xor_result & ~n != 0:
        return -1
    
    # 计算异或结果中 1 的个数
    count = 0
    while xor_result:
        xor_result &= xor_result - 1
        count += 1
    
    return count


Solution = create_solution(solution_function_name)