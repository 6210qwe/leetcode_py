"""
核心思想: 位运算，4的幂是2的幂且1在奇数位

算法步骤:
1. 检查n > 0
2. 检查n是2的幂：n & (n-1) == 0
3. 检查1在奇数位：n & 0x55555555 == n

关键点:
- 位运算
- 时间复杂度O(1)
"""

