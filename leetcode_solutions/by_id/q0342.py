def is_power_of_four(n: int) -> bool:
    """
    函数式接口 - 4的幂
    
    实现思路:
    位运算：4的幂是2的幂且1在奇数位。
    
    Args:
        n: 整数
        
    Returns:
        是否为4的幂
        
    Example:
        >>> is_power_of_four(16)
        True
    """
    return n > 0 and (n & (n - 1)) == 0 and (n & 0x55555555) == n


