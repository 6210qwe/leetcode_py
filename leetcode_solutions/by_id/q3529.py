# ============================================================================
# 问题描述
# ============================================================================
"""
给定一个整数数组 nums，你可以从任意位置开始跳跃。每次跳跃可以跳到下标 i + 1 或者 i + 2 的位置。你的得分是所有跳跃过的元素之和。返回你能得到的最大得分。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i] 表示从位置 i 开始能够获得的最大得分。

算法步骤:
1. 初始化一个长度为 n+2 的 dp 数组，dp[n] 和 dp[n+1] 初始化为 0。
2. 从后向前遍历数组，对于每个位置 i，计算 dp[i] = max(dp[i+1], dp[i+2]) + nums[i]。
3. 最后返回 dp[0] 和 dp[1] 中的最大值。

关键点:
- 动态规划的状态转移方程是 dp[i] = max(dp[i+1], dp[i+2]) + nums[i]。
- 从后向前遍历数组可以确保在计算 dp[i] 时，dp[i+1] 和 dp[i+2] 已经被计算过。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(n)，我们需要一个长度为 n+2 的 dp 数组来存储中间结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算最大数组跳跃得分
    """
    n = len(nums)
    if n == 0:
        return 0

    # 初始化 dp 数组
    dp = [0] * (n + 2)

    # 从后向前遍历数组
    for i in range(n - 1, -1, -1):
        dp[i] = max(dp[i + 1], dp[i + 2]) + nums[i]

    # 返回 dp[0] 和 dp[1] 中的最大值
    return max(dp[0], dp[1])


Solution = create_solution(solution_function_name)