"""
825. 适龄的朋友 - 在社交媒体网站上有 n 个用户。给你一个整数数组 ages ，其中 ages[i] 是第 i 个用户的年龄。 如果下述任意一个条件为真，那么用户 x 将不会向用户 y（x != y）发送好友请求： * ages[y] <= 0.5 * ages[x] + 7 * ages[y] > ages[x] * ages[y] > 100 && ages[x] < 100 否则，x 将会向 y 发送一条好友请求。 注意，如果 x 向 y 发送一条好友请求，y 不必也向 x 发送一条好友请求。另外，用户不会向自己发送好友请求。 返回在该社交媒体网站上产生的好友请求总数。 示例 1： 输入：ages = [16,16] 输出：2 解释：2 人互发好友请求。 示例 2： 输入：ages = [16,17,18] 输出：2 解释：产生的好友请求为 17 -> 16 ，18 -> 17 。 示例 3： 输入：ages = [20,30,100,110,120] 输出：3 解释：产生的好友请求为 110 -> 100 ，120 -> 110 ，120 -> 100 。 提示： * n == ages.length * 1 <= n <= 2 * 104 * 1 <= ages[i] <= 120
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来计算满足条件的配对数量。

算法步骤:
1. 对年龄数组进行排序。
2. 使用两个指针 left 和 right 来遍历数组，left 指针从左到右遍历，right 指针找到第一个不满足条件的年龄。
3. 计算每个 age[left] 可以发送的好友请求数量，即 right - left - 1。

关键点:
- 排序后的数组可以使用双指针高效地找到满足条件的配对。
- 通过移动 right 指针来减少不必要的比较。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 ages 的长度。排序操作的时间复杂度是 O(n log n)，双指针遍历的时间复杂度是 O(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def num_friend_requests(ages: List[int]) -> int:
    """
    函数式接口 - 计算在社交媒体网站上产生的好友请求总数
    """
    # 对年龄数组进行排序
    ages.sort()
    n = len(ages)
    left, right = 0, 0
    count = 0
    
    for left in range(n):
        while right < n and (ages[right] <= 0.5 * ages[left] + 7 or ages[right] <= ages[left]):
            right += 1
        # 计算每个 age[left] 可以发送的好友请求数量
        count += right - left - 1
    
    return count


Solution = create_solution(num_friend_requests)