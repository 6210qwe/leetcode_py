# 问题描述
# ============================================================================
"""
1846. 减小和重新排列数组后的最大元素 - 给你一个正整数数组 arr 。请你对 arr 执行一些操作（也可以不进行任何操作），使得数组满足以下条件： * arr 中 第一个 元素必须为 1 。 * 任意相邻两个元素的差的绝对值 小于等于 1 ，也就是说，对于任意的 1 <= i < arr.length （数组下标从 0 开始），都满足 abs(arr[i] - arr[i - 1]) <= 1 。abs(x) 为 x 的绝对值。 你可以执行以下 2 种操作任意次： * 减小 arr 中任意元素的值，使其变为一个 更小的正整数 。 * 重新排列 arr 中的元素，你可以以任意顺序重新排列。 请你返回执行以上操作后，在满足前文所述的条件下，arr 中可能的 最大值 。 示例 1： 输入：arr = [2,2,1,2,1] 输出：2 解释： 我们可以重新排列 arr 得到 [1,2,2,2,1] ，该数组满足所有条件。 arr 中最大元素为 2 。 示例 2： 输入：arr = [100,1,1000] 输出：3 解释： 一个可行的方案如下： 1. 重新排列 arr 得到 [1,100,1000] 。 2. 将第二个元素减小为 2 。 3. 将第三个元素减小为 3 。 现在 arr = [1,2,3] ，满足所有条件。 arr 中最大元素为 3 。 示例 3： 输入：arr = [1,2,3,4,5] 输出：5 解释：数组已经满足所有条件，最大元素为 5 。 提示： * 1 <= arr.length <= 105 * 1 <= arr[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和贪心算法来构建满足条件的数组。

算法步骤:
1. 对数组进行排序。
2. 初始化结果数组的第一个元素为 1。
3. 遍历排序后的数组，逐个调整元素值，使其满足相邻元素差的绝对值小于等于 1 的条件。
4. 返回结果数组的最大值。

关键点:
- 通过排序和贪心策略，确保每个元素尽可能大且满足条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)，遍历操作为 O(n)。
空间复杂度: O(1)，除了输入数组外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int]) -> int:
    """
    函数式接口 - 返回执行操作后数组中的最大值
    """
    # 对数组进行排序
    arr.sort()
    
    # 初始化结果数组的第一个元素为 1
    arr[0] = 1
    
    # 遍历排序后的数组，逐个调整元素值
    for i in range(1, len(arr)):
        if arr[i] > arr[i - 1] + 1:
            arr[i] = arr[i - 1] + 1
    
    # 返回结果数组的最大值
    return max(arr)


Solution = create_solution(solution_function_name)