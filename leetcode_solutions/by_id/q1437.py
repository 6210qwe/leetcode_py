"""
1312. 让字符串成为回文串的最少插入次数 - 给你一个字符串 s ，每一次操作你都可以在字符串的任意位置插入任意字符。 请你返回让 s 成为回文串的 最少操作次数 。 「回文串」是正读和反读都相同的字符串。 示例 1： 输入：s = "zzazz" 输出：0 解释：字符串 "zzazz" 已经是回文串了，所以不需要做任何插入操作。 示例 2： 输入：s = "mbadm" 输出：2 解释：字符串可变为 "mbdadbm" 或者 "mdbabdm" 。 示例 3： 输入：s = "leetcode" 输出：5 解释：插入 5 个字符后字符串变为 "leetcodocteel" 。 提示： * 1 <= s.length <= 500 * s 中所有字符都是小写字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。定义 dp[i][j] 为将子串 s[i:j+1] 变成回文串所需的最小插入次数。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][j] 表示将子串 s[i:j+1] 变成回文串所需的最小插入次数。
2. 对于每个单个字符，dp[i][i] = 0，因为单个字符本身就是回文串。
3. 从后向前遍历字符串，对于每个子串 s[i:j+1]，如果 s[i] == s[j]，则 dp[i][j] = dp[i+1][j-1]；否则，dp[i][j] = min(dp[i+1][j], dp[i][j-1]) + 1。
4. 返回 dp[0][n-1]，即整个字符串变成回文串所需的最小插入次数。

关键点:
- 动态规划的状态转移方程
- 从后向前遍历字符串以确保子问题已经解决
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串的长度。需要填充一个 n x n 的二维数组。
空间复杂度: O(n^2)，用于存储动态规划表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 返回将字符串 s 变成回文串所需的最少插入次数
    """
    n = len(s)
    if n <= 1:
        return 0

    # 初始化 dp 数组
    dp = [[0] * n for _ in range(n)]

    # 从后向前遍历字符串
    for i in range(n - 1, -1, -1):
        for j in range(i + 1, n):
            if s[i] == s[j]:
                dp[i][j] = dp[i + 1][j - 1]
            else:
                dp[i][j] = min(dp[i + 1][j], dp[i][j - 1]) + 1

    return dp[0][n - 1]


Solution = create_solution(solution_function_name)