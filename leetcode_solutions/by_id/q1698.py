def solution_function_name(s: str) -> str:
    """
    函数式接口 - 替换所有的问号，避免连续重复字符
    """
    # 将字符串转换为列表以便修改
    s_list = list(s)
    
    # 遍历字符串，找到每个 '?' 字符
    for i in range(len(s_list)):
        if s_list[i] == '?':
            # 选择一个不同于其前一个和后一个字符的小写字母进行替换
            for c in 'abc':
                if (i == 0 or s_list[i - 1] != c) and (i == len(s_list) - 1 or s_list[i + 1] != c):
                    s_list[i] = c
                    break
    
    # 将列表转换回字符串
    return ''.join(s_list)


