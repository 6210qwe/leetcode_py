"""
1594. 矩阵的最大非负积 - 给你一个大小为 m x n 的矩阵 grid 。最初，你位于左上角 (0, 0) ，每一步，你可以在矩阵中 向右 或 向下 移动。 在从左上角 (0, 0) 开始到右下角 (m - 1, n - 1) 结束的所有路径中，找出具有 最大非负积 的路径。路径的积是沿路径访问的单元格中所有整数的乘积。 返回 最大非负积 对 109 + 7 取余 的结果。如果最大积为 负数 ，则返回 -1 。 注意，取余是在得到最大积之后执行的。 示例 1： [https://assets.leetcode.com/uploads/2021/12/23/product1.jpg] 输入：grid = [[-1,-2,-3],[-2,-3,-3],[-3,-3,-2]] 输出：-1 解释：从 (0, 0) 到 (2, 2) 的路径中无法得到非负积，所以返回 -1 。 示例 2： [https://assets.leetcode.com/uploads/2021/12/23/product2.jpg] 输入：grid = [[1,-2,1],[1,-2,1],[3,-4,1]] 输出：8 解释：最大非负积对应的路径如图所示 (1 * 1 * -2 * -4 * 1 = 8) 示例 3： [https://assets.leetcode.com/uploads/2021/12/23/product3.jpg] 输入：grid = [[1,3],[0,-4]] 输出：0 解释：最大非负积对应的路径如图所示 (1 * 0 * -4 = 0) 提示： * m == grid.length * n == grid[i].length * 1 <= m, n <= 15 * -4 <= grid[i][j] <= 4
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来记录每个位置的最大和最小乘积。因为负数乘以负数会变成正数，所以我们需要同时记录最大和最小乘积。

算法步骤:
1. 初始化一个三维数组 dp，其中 dp[i][j][0] 表示到达 (i, j) 位置的最大乘积，dp[i][j][1] 表示到达 (i, j) 位置的最小乘积。
2. 遍历矩阵，更新 dp 数组。
3. 如果最终的最大乘积为负数，返回 -1；否则返回最大乘积对 10^9 + 7 取余的结果。

关键点:
- 使用动态规划记录每个位置的最大和最小乘积。
- 处理边界情况，确保初始值正确。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是矩阵的行数和列数。
空间复杂度: O(m * n)，使用了一个三维数组来存储中间结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

MOD = 10**9 + 7

def solution_function_name(grid: List[List[int]]) -> int:
    """
    函数式接口 - 计算从左上角到右下角路径的最大非负积
    """
    if not grid or not grid[0]:
        return -1

    m, n = len(grid), len(grid[0])
    dp = [[[0, 0] for _ in range(n)] for _ in range(m)]

    # 初始化起点
    dp[0][0][0] = dp[0][0][1] = grid[0][0]

    # 初始化第一行
    for j in range(1, n):
        dp[0][j][0] = dp[0][j][1] = dp[0][j-1][0] * grid[0][j]

    # 初始化第一列
    for i in range(1, m):
        dp[i][0][0] = dp[i][0][1] = dp[i-1][0][0] * grid[i][0]

    # 填充 dp 数组
    for i in range(1, m):
        for j in range(1, n):
            candidates = [
                dp[i-1][j][0] * grid[i][j],
                dp[i-1][j][1] * grid[i][j],
                dp[i][j-1][0] * grid[i][j],
                dp[i][j-1][1] * grid[i][j]
            ]
            dp[i][j][0] = max(candidates)
            dp[i][j][1] = min(candidates)

    # 计算最终结果
    max_product = dp[-1][-1][0]
    if max_product < 0:
        return -1
    else:
        return max_product % MOD

Solution = create_solution(solution_function_name)