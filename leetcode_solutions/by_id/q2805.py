"""
2695. 包装数组 - 创建一个名为 ArrayWrapper 的类，它在其构造函数中接受一个整数数组作为参数。该类应具有以下两个特性： * 当使用 + 运算符将两个该类的实例相加时，结果值为两个数组中所有元素的总和。 * 当在实例上调用 String() 函数时，它将返回一个由逗号分隔的括在方括号中的字符串。例如，[1,2,3] 。 示例 1： 输入：nums = [[1,2],[3,4]], operation = "Add" 输出：10 解释： const obj1 = new ArrayWrapper([1,2]); const obj2 = new ArrayWrapper([3,4]); obj1 + obj2; // 10 示例 2： 输入：nums = [[23,98,42,70]], operation = "String" 输出："[23,98,42,70]" 解释： const obj = new ArrayWrapper([23,98,42,70]); String(obj); // "[23,98,42,70]" 示例 3： 输入：nums = [[],[]], operation = "Add" 输出：0 解释： const obj1 = new ArrayWrapper([]); const obj2 = new ArrayWrapper([]); obj1 + obj2; // 0 提示： * 0 <= nums.length <= 1000 * 0 <= nums[i] <= 1000 * 注意：nums 是传递给构造函数的数组。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用类来封装数组，并实现加法和字符串表示方法。

算法步骤:
1. 在构造函数中初始化数组并计算数组的总和。
2. 重载 `__add__` 方法，使两个实例相加时返回它们数组总和的和。
3. 重载 `__str__` 方法，返回数组的字符串表示形式。

关键点:
- 使用 `__init__` 初始化数组并计算总和。
- 使用 `__add__` 实现加法操作。
- 使用 `__str__` 返回数组的字符串表示。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 构造函数需要遍历整个数组来计算总和。
空间复杂度: O(1) - 除了存储数组和总和外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


class ArrayWrapper:
    def __init__(self, nums: List[int]):
        self.nums = nums
        self.sum = sum(nums)

    def __add__(self, other):
        return self.sum + other.sum

    def __str__(self):
        return "[" + ",".join(map(str, self.nums)) + "]"


def create_solution():
    """
    工厂函数 - 创建并返回 ArrayWrapper 类的实例
    """
    return ArrayWrapper


Solution = create_solution()