"""
967. 连续差相同的数字 - 返回所有长度为 n 且满足其每两个连续位上的数字之间的差的绝对值为 k 的 非负整数 。 请注意，除了 数字 0 本身之外，答案中的每个数字都 不能 有前导零。例如，01 有一个前导零，所以是无效的；但 0 是有效的。 你可以按 任何顺序 返回答案。 示例 1： 输入：n = 3, k = 7 输出：[181,292,707,818,929] 解释：注意，070 不是一个有效的数字，因为它有前导零。 示例 2： 输入：n = 2, k = 1 输出：[10,12,21,23,32,34,43,45,54,56,65,67,76,78,87,89,98] 示例 3： 输入：n = 2, k = 0 输出：[11,22,33,44,55,66,77,88,99] 示例 4： 输入：n = 2, k = 2 输出：[13,20,24,31,35,42,46,53,57,64,68,75,79,86,97] 提示： * 2 <= n <= 9 * 0 <= k <= 9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用广度优先搜索（BFS）来生成所有满足条件的数字。

算法步骤:
1. 初始化一个队列，将 1 到 9 的数字加入队列。
2. 对于每一层（即每一位），从队列中取出当前数字，并生成下一层的所有可能数字。
3. 检查新生成的数字是否满足条件（即与前一位的差的绝对值为 k），如果满足则加入队列。
4. 重复上述过程直到生成 n 位数。
5. 如果 n 为 1，则单独处理，返回 0 到 9 的数字。

关键点:
- 使用 BFS 逐层生成数字，确保每一步都满足条件。
- 处理 n 为 1 的特殊情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n) - 每个数字最多有两个选择（加 k 或减 k），总共 n 层。
空间复杂度: O(2^n) - 队列中最多存储 2^n 个数字。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def nums_same_consec_diff(n: int, k: int) -> List[int]:
    if n == 1:
        return [i for i in range(10)]
    
    queue = [i for i in range(1, 10)]
    
    for _ in range(n - 1):
        next_queue = []
        for num in queue:
            last_digit = num % 10
            if last_digit + k < 10:
                next_queue.append(num * 10 + (last_digit + k))
            if k != 0 and last_digit - k >= 0:
                next_queue.append(num * 10 + (last_digit - k))
        queue = next_queue
    
    return queue

