"""
1903. 字符串中的最大奇数 - 给你一个字符串 num ，表示一个大整数。请你在字符串 num 的所有 非空子字符串 中找出 值最大的奇数 ，并以字符串形式返回。如果不存在奇数，则返回一个空字符串 "" 。 子字符串 是字符串中的一个连续的字符序列。 示例 1： 输入：num = "52" 输出："5" 解释：非空子字符串仅有 "5"、"2" 和 "52" 。"5" 是其中唯一的奇数。 示例 2： 输入：num = "4206" 输出："" 解释：在 "4206" 中不存在奇数。 示例 3： 输入：num = "35427" 输出："35427" 解释："35427" 本身就是一个奇数。 提示： * 1 <= num.length <= 105 * num 仅由数字组成且不含前导零
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从右向左遍历字符串，找到第一个奇数，然后返回从开始到该奇数的子字符串。

算法步骤:
1. 从右向左遍历字符串。
2. 找到第一个奇数的位置。
3. 返回从开始到该奇数的子字符串。
4. 如果没有找到奇数，返回空字符串。

关键点:
- 从右向左遍历可以确保找到的最大奇数是值最大的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。最坏情况下需要遍历整个字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: str) -> str:
    """
    函数式接口 - 在字符串 num 的所有非空子字符串中找出值最大的奇数，并以字符串形式返回。
    如果不存在奇数，则返回一个空字符串。
    """
    # 从右向左遍历字符串
    for i in range(len(num) - 1, -1, -1):
        if int(num[i]) % 2 != 0:
            return num[:i + 1]
    return ""


Solution = create_solution(solution_function_name)