from leetcode_solutions.utils.solution import create_solution


def reverse_words_in_a_string(s: str) -> str:
    """
    函数式接口 - 反转字符串中的单词
    
    实现思路:
    分割字符串，反转单词顺序，然后重新组合。
    
    Args:
        s: 输入字符串
        
    Returns:
        反转单词顺序后的字符串
        
    Example:
        >>> reverse_words_in_a_string("the sky is blue")
        'blue is sky the'
    """
    words = s.split()
    words.reverse()
    return ' '.join(words)


