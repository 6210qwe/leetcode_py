"""
核心思想: 分割字符串，反转单词顺序，然后重新组合

算法步骤:
1. 使用split()分割字符串，去除多余空格
2. 反转单词列表
3. 使用join()重新组合，单词间用单个空格连接

关键点:
- 处理前导、尾随和多个空格
- 时间复杂度O(n)，空间复杂度O(n)
"""

