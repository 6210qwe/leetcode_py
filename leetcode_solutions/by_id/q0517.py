from typing import List

def find_min_moves(machines: List[int]) -> int:
    """
    函数式接口 - 计算最少的操作步数使所有洗衣机中的衣物数量相等
    """
    n = len(machines)
    total = sum(machines)
    
    if total % n != 0:
        return -1
    
    target = total // n
    max_steps = 0
    balance = 0
    
    for i in range(n):
        balance += machines[i] - target
        max_steps = max(max_steps, abs(balance), machines[i] - target)
    
    return max_steps

