"""
3483. 不同三位偶数的数目 - 给你一个数字数组 digits，你需要从中选择三个数字组成一个三位偶数，你的任务是求出 不同 三位偶数的数量。 注意：每个数字在三位偶数中都只能使用 一次 ，并且 不能 有前导零。 示例 1： 输入： digits = [1,2,3,4] 输出： 12 解释： 可以形成的 12 个不同的三位偶数是 124，132，134，142，214，234，312，314，324，342，412 和 432。注意，不能形成 222，因为数字 2 只有一个。 示例 2： 输入： digits = [0,2,2] 输出： 2 解释： 可以形成的三位偶数是 202 和 220。注意，数字 2 可以使用两次，因为数组中有两个 2 。 示例 3： 输入： digits = [6,6,6] 输出： 1 解释： 只能形成 666。 示例 4： 输入： digits = [1,3,5] 输出： 0 解释： 无法形成三位偶数。 提示： * 3 <= digits.length <= 10 * 0 <= digits[i] <= 9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用三重循环枚举所有可能的三位数，并检查其是否为偶数且没有前导零。

算法步骤:
1. 使用三重循环枚举所有可能的三位数。
2. 检查每个三位数是否为偶数且没有前导零。
3. 使用集合去重，确保每个三位数都是唯一的。

关键点:
- 三重循环枚举所有可能的三位数。
- 使用集合去重，确保每个三位数都是唯一的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3)，其中 n 是 digits 的长度。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_unique_even_numbers(digits: List[int]) -> int:
    """
    函数式接口 - 计算不同三位偶数的数量
    """
    unique_numbers = set()
    
    for i in range(len(digits)):
        for j in range(len(digits)):
            if i == j:
                continue
            for k in range(len(digits)):
                if i == k or j == k:
                    continue
                # 确保没有前导零
                if digits[i] != 0 and digits[k] % 2 == 0:
                    number = digits[i] * 100 + digits[j] * 10 + digits[k]
                    unique_numbers.add(number)
    
    return len(unique_numbers)


Solution = create_solution(count_unique_even_numbers)