"""
1370. 上升下降字符串 - 给你一个字符串 s ，请你根据下面的算法重新构造字符串： 1. 从 s 中选出 最小 的字符，将它 接在 结果字符串的后面。 2. 从 s 剩余字符中选出比上一个添加字符更大的 最小 字符，将它 接在 结果字符串后面。 3. 重复步骤 2 ，直到你没法从 s 中选择字符。 4. 从 s 中选出 最大 的字符，将它 接在 结果字符串的后面。 5. 从 s 剩余字符中选出比上一个添加字符更小的 最大 字符，将它 接在 结果字符串后面。 6. 重复步骤 5 ，直到你没法从 s 中选择字符。 7. 重复步骤 1 到 6 ，直到 s 中所有字符都已经被选过。 在任何一步中，如果最小或者最大字符不止一个 ，你可以选择其中任意一个，并将其添加到结果字符串。 请你返回将 s 中字符重新排序后的 结果字符串 。 示例 1： 输入：s = "aaaabbbbcccc" 输出："abccbaabccba" 解释：第一轮的步骤 1，2，3 后，结果字符串为 result = "abc" 第一轮的步骤 4，5，6 后，结果字符串为 result = "abccba" 第一轮结束，现在 s = "aabbcc" ，我们再次回到步骤 1 第二轮的步骤 1，2，3 后，结果字符串为 result = "abccbaabc" 第二轮的步骤 4，5，6 后，结果字符串为 result = "abccbaabccba" 示例 2： 输入：s = "rat" 输出："art" 解释：单词 "rat" 在上述算法重排序以后变成 "art" 提示： * 1 <= s.length <= 500 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数数组记录每个字符的出现次数，然后按照题目要求构建结果字符串。

算法步骤:
1. 初始化一个长度为26的计数数组，记录每个字符的出现次数。
2. 构建结果字符串：
   - 从 'a' 到 'z' 依次添加字符（上升部分）。
   - 从 'z' 到 'a' 依次添加字符（下降部分）。
3. 重复步骤2，直到所有字符都被添加到结果字符串中。

关键点:
- 使用计数数组来高效地记录和处理字符。
- 通过两次遍历（上升和下降）来构建结果字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历字符串一次来统计字符频率，然后通过两次遍历计数数组来构建结果字符串。
空间复杂度: O(1)，因为计数数组的大小是固定的（26个字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sort_string(s: str) -> str:
    """
    根据题目要求重新构造字符串。
    """
    # 计数数组
    count = [0] * 26
    for char in s:
        count[ord(char) - ord('a')] += 1
    
    result = []
    while len(result) < len(s):
        # 上升部分
        for i in range(26):
            if count[i] > 0:
                result.append(chr(i + ord('a')))
                count[i] -= 1
        
        # 下降部分
        for i in range(25, -1, -1):
            if count[i] > 0:
                result.append(chr(i + ord('a')))
                count[i] -= 1
    
    return ''.join(result)


Solution = create_solution(sort_string)