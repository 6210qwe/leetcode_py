"""
核心思想: 通过贪心算法选择最优的修复方案。首先计算每个路段的坑洼数量，然后按坑洼数量从大到小排序，优先修复坑洼最多的路段。

算法步骤:
1. 计算每个路段的坑洼数量。
2. 按坑洼数量从大到小排序。
3. 依次修复坑洼最多的路段，直到材料用完。

关键点:
- 优先修复坑洼最多的路段，以最大化修复数量。
- 使用贪心算法确保每次选择当前最优解。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是路段的数量。主要时间开销在于排序操作。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

