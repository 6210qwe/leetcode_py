"""
693. 交替位二进制数 - 给定一个正整数，检查它的二进制表示是否总是 0、1 交替出现：换句话说，就是二进制表示中相邻两位的数字永不相同。 示例 1： 输入：n = 5 输出：true 解释：5 的二进制表示是：101 示例 2： 输入：n = 7 输出：false 解释：7 的二进制表示是：111. 示例 3： 输入：n = 11 输出：false 解释：11 的二进制表示是：1011. 提示： * 1 <= n <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 检查相邻位是否交替出现

算法步骤:
1. 将 n 右移一位得到 n >> 1。
2. 使用异或操作 (n ^ (n >> 1)) 检查相邻位是否交替出现。
3. 如果结果的所有位都是 1，则说明相邻位交替出现。

关键点:
- 使用位运算来高效地检查相邻位是否交替出现。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
