def has_alternating_bits(n: int) -> bool:
    """
    函数式接口 - 检查给定正整数的二进制表示是否总是 0、1 交替出现
    """
    # 使用位运算检查相邻位是否交替出现
    x = n ^ (n >> 1)
    return (x & (x + 1)) == 0


