"""
10. 正则表达式匹配 - 给你一个字符串 s 和一个字符规律 p，请你来实现一个支持 '.' 和 '*' 的正则表达式匹配。 * '.' 匹配任意单个字符 * '*' 匹配零个或多个前面的那一个元素 所谓匹配，是要涵盖 整个 字符串 s 的，而不是部分字符串。 示例 1： 输入：s = "aa", p = "a" 输出：false 解释："a" 无法匹配 "aa" 整个字符串。 示例 2: 输入：s = "aa", p = "a*" 输出：true 解释：因为 '*' 代表可以匹配零个或多个前面的那一个元素, 在这里前面的元素就是 'a'。因此，字符串 "aa" 可被视为 'a' 重复了一次。 示例 3： 输入：s = "ab", p = ".*" 输出：true 解释：".*" 表示可匹配零个或多个（'*'）任意字符（'.'）。 提示： * 1 <= s.length <= 20 * 1 <= p.length <= 20 * s 只包含从 a-z 的小写字母。 * p 只包含从 a-z 的小写字母，以及字符 . 和 *。 * 保证每次出现字符 * 时，前面都匹配到有效的字符
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划，dp[i][j]表示s的前i个字符能否匹配p的前j个字符

算法步骤:
1. 创建dp数组，dp[i][j]表示s[0:i]能否匹配p[0:j]
2. 初始化：dp[0][0] = True（空字符串匹配空模式）
3. 处理p开头的'*'情况（可以匹配0个字符）
4. 状态转移：
   - 如果p[j-1] == '*'：
     * 匹配0次：dp[i][j] = dp[i][j-2]
     * 匹配1次或多次：如果s[i-1] == p[j-2]或p[j-2] == '.'，则dp[i][j] = dp[i-1][j]
   - 如果p[j-1] == '.'或s[i-1] == p[j-1]：
     * dp[i][j] = dp[i-1][j-1]
5. 返回dp[m][n]

关键点:
- '*'可以匹配0个或多个前面的字符，需要分别考虑这两种情况
- '.'可以匹配任意单个字符
- 使用动态规划避免重复计算，时间复杂度O(mn)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(mn) - m和n分别为s和p的长度，需要填充m×n的dp数组
空间复杂度: O(mn) - dp数组的空间，可以优化到O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def is_match(s: str, p: str) -> bool:
    """
    函数式接口 - 动态规划实现
    
    实现思路:
    使用动态规划判断字符串s是否能匹配正则表达式p，支持'.'和'*'。
    
    Args:
        s: 输入字符串
        p: 正则表达式模式
        
    Returns:
        如果匹配返回True，否则返回False
        
    Example:
        >>> is_match("aa", "a")
        False
        >>> is_match("aa", "a*")
        True
        >>> is_match("ab", ".*")
        True
    """
    m, n = len(s), len(p)
    # dp[i][j]表示s的前i个字符能否匹配p的前j个字符
    dp = [[False] * (n + 1) for _ in range(m + 1)]
    
    # 空字符串匹配空模式
    dp[0][0] = True
    
    # 处理p开头的'*'情况（可以匹配0个字符）
    for j in range(2, n + 1):
        if p[j - 1] == '*':
            dp[0][j] = dp[0][j - 2]
    
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if p[j - 1] == '*':
                # '*'可以匹配0次或多次
                # 匹配0次：忽略p[j-2]和'*'
                dp[i][j] = dp[i][j - 2]
                # 匹配1次或多次：如果s[i-1]匹配p[j-2]
                if p[j - 2] == '.' or p[j - 2] == s[i - 1]:
                    dp[i][j] = dp[i][j] or dp[i - 1][j]
            elif p[j - 1] == '.' or p[j - 1] == s[i - 1]:
                # 普通字符或'.'匹配
                dp[i][j] = dp[i - 1][j - 1]
    
    return dp[m][n]


