"""
1835. 所有数对按位与结果的异或和 - 列表的 异或和（XOR sum）指对所有元素进行按位 XOR 运算的结果。如果列表中仅有一个元素，那么其 异或和 就等于该元素。 * 例如，[1,2,3,4] 的 异或和 等于 1 XOR 2 XOR 3 XOR 4 = 4 ，而 [3] 的 异或和 等于 3 。 给你两个下标 从 0 开始 计数的数组 arr1 和 arr2 ，两数组均由非负整数组成。 根据每个 (i, j) 数对，构造一个由 arr1[i] AND arr2[j]（按位 AND 运算）结果组成的列表。其中 0 <= i < arr1.length 且 0 <= j < arr2.length 。 返回上述列表的 异或和 。 示例 1： 输入：arr1 = [1,2,3], arr2 = [6,5] 输出：0 解释：列表 = [1 AND 6, 1 AND 5, 2 AND 6, 2 AND 5, 3 AND 6, 3 AND 5] = [0,1,2,0,2,1] ， 异或和 = 0 XOR 1 XOR 2 XOR 0 XOR 2 XOR 1 = 0 。 示例 2： 输入：arr1 = [12], arr2 = [4] 输出：4 解释：列表 = [12 AND 4] = [4] ，异或和 = 4 。 提示： * 1 <= arr1.length, arr2.length <= 105 * 0 <= arr1[i], arr2[j] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 利用按位运算的性质，将问题转化为线性时间复杂度的解决方案。

算法步骤:
1. 计算 arr1 中所有元素的按位与结果的异或和。
2. 计算 arr2 中所有元素的按位与结果的异或和。
3. 返回两个异或和的按位与结果。

关键点:
- 利用按位运算的结合律和分配律，可以将问题简化为线性时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 arr1 的长度，m 是 arr2 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_xor_sum_of_all_pairs_bitwise_and(arr1: List[int], arr2: List[int]) -> int:
    """
    函数式接口 - 计算所有数对按位与结果的异或和
    """
    # 计算 arr1 中所有元素的按位与结果的异或和
    xor_sum_arr1 = 0
    for num in arr1:
        xor_sum_arr1 ^= num
    
    # 计算 arr2 中所有元素的按位与结果的异或和
    xor_sum_arr2 = 0
    for num in arr2:
        xor_sum_arr2 ^= num
    
    # 返回两个异或和的按位与结果
    return xor_sum_arr1 & xor_sum_arr2


Solution = create_solution(find_xor_sum_of_all_pairs_bitwise_and)