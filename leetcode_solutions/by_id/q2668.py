"""
2605. 从两个数字数组里生成最小数字 - 给你两个只包含 1 到 9 之间数字的数组 nums1 和 nums2 ，每个数组中的元素 互不相同 ，请你返回 最小 的数字，两个数组都 至少 包含这个数字的某个数位。 示例 1： 输入：nums1 = [4,1,3], nums2 = [5,7] 输出：15 解释：数字 15 的数位 1 在 nums1 中出现，数位 5 在 nums2 中出现。15 是我们能得到的最小数字。 示例 2： 输入：nums1 = [3,5,2,6], nums2 = [3,1,7] 输出：3 解释：数字 3 的数位 3 在两个数组中都出现了。 提示： * 1 <= nums1.length, nums2.length <= 9 * 1 <= nums1[i], nums2[i] <= 9 * 每个数组中，元素 互不相同 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来查找两个数组的交集，并找到最小的共同数字。如果没有共同数字，则选择两个数组中最小的数字组合成两位数。

算法步骤:
1. 将两个数组转换为集合，以便快速查找交集。
2. 找到两个集合的交集，如果交集不为空，则返回最小的共同数字。
3. 如果没有共同数字，则分别找到两个数组中的最小数字，并组合成两位数，返回较小的两位数。

关键点:
- 使用集合操作来高效查找交集。
- 通过比较两个数组中的最小数字来生成最小的两位数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 和 m 分别是 nums1 和 nums2 的长度。集合操作的时间复杂度为 O(1)。
空间复杂度: O(n + m)，用于存储两个集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums1: List[int], nums2: List[int]) -> int:
    """
    函数式接口 - 实现
    """
    set1 = set(nums1)
    set2 = set(nums2)
    
    # 找到两个集合的交集
    common_digits = set1 & set2
    if common_digits:
        return min(common_digits)
    
    # 如果没有共同数字，则分别找到两个数组中的最小数字
    min_num1 = min(nums1)
    min_num2 = min(nums2)
    
    # 返回较小的两位数
    return min(min_num1 * 10 + min_num2, min_num2 * 10 + min_num1)

