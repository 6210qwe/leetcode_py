"""
核心思想: 使用 SQL 查询来统计不同类型的贷款数量。

算法步骤:
1. 从 `Loans` 表中选择 `type` 列。
2. 使用 `GROUP BY` 子句按 `type` 分组。
3. 使用 `COUNT` 函数计算每种类型的贷款数量。
4. 返回结果集。

关键点:
- 使用 `GROUP BY` 和 `COUNT` 来统计每种类型的贷款数量。
"""

