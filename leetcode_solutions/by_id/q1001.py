"""
961. 在长度 2N 的数组中找出重复 N 次的元素 - 给你一个整数数组 nums ，该数组具有以下属性： * nums.length == 2 * n. * nums 包含 n + 1 个 不同的 元素，其中 n 个值在数组中出现 恰好一次。 * nums 中恰有一个元素重复 n 次 找出并返回重复了 n 次的那个元素。 示例 1： 输入：nums = [1,2,3,3] 输出：3 示例 2： 输入：nums = [2,1,2,5,3,2] 输出：2 示例 3： 输入：nums = [5,1,5,2,5,3,5,4] 输出：5 提示： * 2 <= n <= 5000 * nums.length == 2 * n * 0 <= nums[i] <= 104 * nums 由 n + 1 个 不同的 元素组成，且其中一个元素恰好重复 n 次
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个元素的出现次数，找到出现次数为 n 的元素。

算法步骤:
1. 初始化一个空的哈希表。
2. 遍历数组，统计每个元素的出现次数。
3. 遍历哈希表，找到出现次数为 n 的元素并返回。

关键点:
- 使用哈希表可以在 O(n) 时间内完成统计。
- 由于数组长度为 2n，且只有一个元素重复 n 次，因此只需遍历一次即可找到目标元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 在长度 2N 的数组中找出重复 N 次的元素
    """
    # 使用哈希表记录每个元素的出现次数
    count = {}
    for num in nums:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    # 找到出现次数为 n 的元素
    n = len(nums) // 2
    for num, freq in count.items():
        if freq == n:
            return num

Solution = create_solution(solution_function_name)