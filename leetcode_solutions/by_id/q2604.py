"""
2541. 使数组中所有元素相等的最小操作数 II - 给你两个整数数组 nums1 和 nums2 ，两个数组长度都是 n ，再给你一个整数 k 。你可以对数组 nums1 进行以下操作： * 选择两个下标 i 和 j ，将 nums1[i] 增加 k ，将 nums1[j] 减少 k 。换言之，nums1[i] = nums1[i] + k 且 nums1[j] = nums1[j] - k 。 如果对于所有满足 0 <= i < n 都有 num1[i] == nums2[i] ，那么我们称 nums1 等于 nums2 。 请你返回使 nums1 等于 nums2 的 最少 操作数。如果没办法让它们相等，请你返回 -1 。 示例 1： 输入：nums1 = [4,3,1,4], nums2 = [1,3,7,1], k = 3 输出：2 解释：我们可以通过 2 个操作将 nums1 变成 nums2 。 第 1 个操作：i = 2 ，j = 0 。操作后得到 nums1 = [1,3,4,4] 。 第 2 个操作：i = 2 ，j = 3 。操作后得到 nums1 = [1,3,7,1] 。 无法用更少操作使两个数组相等。 示例 2： 输入：nums1 = [3,8,5,2], nums2 = [2,4,1,6], k = 1 输出：-1 解释：无法使两个数组相等。 提示： * n == nums1.length == nums2.length * 2 <= n <= 105 * 0 <= nums1[i], nums2[j] <= 109 * 0 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算每个位置的差值，并检查是否可以通过 k 的倍数来调整。

算法步骤:
1. 初始化一个变量 `diff_sum` 来记录总的差值。
2. 遍历数组，计算每个位置的差值 `diff`，并累加到 `diff_sum` 中。
3. 如果 `diff` 不是 k 的倍数，直接返回 -1。
4. 最后，如果 `diff_sum` 不为 0，返回 -1；否则返回 `total_operations`。

关键点:
- 差值必须是 k 的倍数。
- 总的差值必须为 0。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def min_operations_to_make_equal(nums1: List[int], nums2: List[int], k: int) -> int:
    if k == 0:
        return 0 if nums1 == nums2 else -1
    
    diff_sum = 0
    total_operations = 0
    
    for num1, num2 in zip(nums1, nums2):
        diff = num1 - num2
        if diff % k != 0:
            return -1
        diff_sum += diff
        total_operations += abs(diff) // k
    
    if diff_sum != 0:
        return -1
    return total_operations

