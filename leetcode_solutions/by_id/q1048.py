"""
核心思想: 使用栈来处理乘法、除法、加法和减法操作，确保正确的运算顺序。

算法步骤:
1. 初始化一个栈，将第一个数字压入栈。
2. 遍历从 N 到 1 的每个数字，根据当前操作符进行相应的运算：
   - 如果是乘法或除法，直接与栈顶元素进行运算并将结果压入栈。
   - 如果是加法或减法，将当前数字压入栈。
3. 最后，将栈中的所有元素相加得到最终结果。

关键点:
- 使用栈来处理运算顺序，确保乘法和除法优先于加法和减法。
- 减法操作可以通过将负数压入栈来实现。
"""

