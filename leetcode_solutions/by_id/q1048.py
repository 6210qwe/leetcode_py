def clumsy(n: int) -> int:
    if n == 1:
        return 1
    if n == 2:
        return 2
    if n == 3:
        return 6
    if n == 4:
        return 7

    stack = [n]
    n -= 1
    op = 0  # 0: 乘法, 1: 除法, 2: 加法, 3: 减法

    while n > 0:
        if op == 0:
            stack.append(stack.pop() * n)
        elif op == 1:
            stack.append(int(stack.pop() / n))
        elif op == 2:
            stack.append(n)
        elif op == 3:
            stack.append(-n)
        n -= 1
        op = (op + 1) % 4

    return sum(stack)


