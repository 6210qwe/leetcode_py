"""
667. 优美的排列 II - 给你两个整数 n 和 k ，请你构造一个答案列表 answer ，该列表应当包含从 1 到 n 的 n 个不同正整数，并同时满足下述条件： * 假设该列表是 answer = [a1, a2, a3, ... , an] ，那么列表 [|a1 - a2|, |a2 - a3|, |a3 - a4|, ... , |an-1 - an|] 中应该有且仅有 k 个不同整数。 返回列表 answer 。如果存在多种答案，只需返回其中 任意一种 。 示例 1： 输入：n = 3, k = 1 输出：[1, 2, 3] 解释：[1, 2, 3] 包含 3 个范围在 1-3 的不同整数，并且 [1, 1] 中有且仅有 1 个不同整数：1 示例 2： 输入：n = 3, k = 2 输出：[1, 3, 2] 解释：[1, 3, 2] 包含 3 个范围在 1-3 的不同整数，并且 [2, 1] 中有且仅有 2 个不同整数：1 和 2 提示： * 1 <= k < n <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法构建答案列表，使得相邻元素的差值尽可能地覆盖从 1 到 k 的所有整数。

算法步骤:
1. 初始化一个空列表 `answer`。
2. 从 1 开始交替添加 `k+1` 和 `k+1-i` 到 `answer` 中，直到 `i` 达到 `k`。
3. 将剩余的数字从 `k+2` 到 `n` 依次添加到 `answer` 中。

关键点:
- 通过交替添加 `k+1` 和 `k+1-i`，可以确保相邻元素的差值覆盖从 1 到 k 的所有整数。
- 最后将剩余的数字依次添加到 `answer` 中，确保所有的数字都被使用。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, k: int) -> List[int]:
    """
    函数式接口 - 构造一个答案列表，包含从 1 到 n 的 n 个不同正整数，并满足相邻元素的差值有且仅有 k 个不同整数。
    """
    answer = []
    for i in range(k + 1):
        if i % 2 == 0:
            answer.append(i // 2 + 1)
        else:
            answer.append(k + 1 - (i // 2))
    
    for i in range(k + 2, n + 1):
        answer.append(i)
    
    return answer


Solution = create_solution(solution_function_name)