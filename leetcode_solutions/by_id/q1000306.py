"""
LCR 049. 求根节点到叶节点数字之和 - 给定一个二叉树的根节点 root ，树中每个节点都存放有一个 0 到 9 之间的数字。 每条从根节点到叶节点的路径都代表一个数字： * 例如，从根节点到叶节点的路径 1 -> 2 -> 3 表示数字 123 。 计算从根节点到叶节点生成的 所有数字之和 。 叶节点 是指没有子节点的节点。 示例 1： [https://assets.leetcode.com/uploads/2021/02/19/num1tree.jpg] 输入：root = [1,2,3] 输出：25 解释： 从根到叶子节点路径 1->2 代表数字 12 从根到叶子节点路径 1->3 代表数字 13 因此，数字总和 = 12 + 13 = 25 示例 2： [https://assets.leetcode.com/uploads/2021/02/19/num2tree.jpg] 输入：root = [4,9,0,5,1] 输出：1026 解释： 从根到叶子节点路径 4->9->5 代表数字 495 从根到叶子节点路径 4->9->1 代表数字 491 从根到叶子节点路径 4->0 代表数字 40 因此，数字总和 = 495 + 491 + 40 = 1026 提示： * 树中节点的数目在范围 [1, 1000] 内 * 0 <= Node.val <= 9 * 树的深度不超过 10 注意：本题与主站 129 题相同： https://leetcode.cn/problems/sum-root-to-leaf-numbers/ [https://leetcode.cn/problems/sum-root-to-leaf-numbers/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）遍历二叉树，递归地计算从根节点到叶节点的路径数字之和。

算法步骤:
1. 定义一个递归函数 `dfs`，该函数接收当前节点和当前路径的数字值。
2. 如果当前节点是叶节点，则将当前路径的数字值加到结果中。
3. 如果当前节点不是叶节点，则递归地调用 `dfs` 函数，分别处理左子节点和右子节点，并更新当前路径的数字值。
4. 初始化结果变量 `result` 为 0，并从根节点开始调用 `dfs` 函数。
5. 返回结果变量 `result`。

关键点:
- 递归地构建从根节点到叶节点的路径数字。
- 在叶节点处将路径数字加到结果中。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数。每个节点都被访问一次。
空间复杂度: O(h)，其中 h 是二叉树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_numbers(root: Optional[TreeNode]) -> int:
    """
    计算从根节点到叶节点生成的所有数字之和。
    """
    def dfs(node: Optional[TreeNode], current_sum: int) -> None:
        if not node:
            return
        current_sum = current_sum * 10 + node.val
        if not node.left and not node.right:
            result[0] += current_sum
            return
        dfs(node.left, current_sum)
        dfs(node.right, current_sum)

    result = [0]
    dfs(root, 0)
    return result[0]


Solution = create_solution(sum_numbers)