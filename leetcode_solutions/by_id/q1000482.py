"""
LCP 67. 装饰树 - 力扣嘉年华上的 DIY 手工展位准备了一棵缩小版的 **二叉** 装饰树 `root` 和灯饰，你需要将灯饰逐一插入装饰树中，要求如下： - 完成装饰的二叉树根结点与 `root` 的根结点值相同 - 若一个节点拥有父节点，则在该节点和他的父节点之间插入一个灯饰（即插入一个值为 `-1` 的节点）。具体地： - 在一个 父节点 x 与其左子节点 y 之间添加 -1 节点， 节点 -1、节点 y 为各自父节点的左子节点， - 在一个 父节点 x 与其右子节点 y 之间添加 -1 节点， 节点 -1、节点 y 为各自父节点的右子节点， 现给定二叉树的根节点 `root` ，请返回完成装饰后的树的根节点。 **示例 1：** >输入： >`root = [7,5,6]` > >输出：`[7,-1,-1,5,null,null,6]` > >解释：如下图所示， >![image.png](https://pic.leetcode.cn/1663575757-yRLGaq-image.png){:width=400px} **示例 2：** >输入： >`root = [3,1,7,3,8,null,4]` > >输出：`[3,-1,-1,1,null,null,7,-1,-1,null,-1,3,null,null,8,null,4]` > >解释：如下图所示 ![image.png](https://pic.leetcode.cn/1663577920-sjrAYH-image.png){:width=500px} **提示：** >`0 <= root.Val <= 1000` >`root` 节点数量范围为 `[1, 10^5]`
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 递归遍历树，并在每个节点和其子节点之间插入一个值为 -1 的节点。

算法步骤:
1. 如果当前节点有左子节点，则在当前节点和左子节点之间插入一个值为 -1 的节点。
2. 如果当前节点有右子节点，则在当前节点和右子节点之间插入一个值为 -1 的节点。
3. 递归处理左子节点和右子节点。

关键点:
- 递归处理每个节点的左右子节点，并在它们之间插入 -1 节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点都被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的空间复杂度取决于树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def decorate_tree(root: Optional[TreeNode]) -> Optional[TreeNode]:
    """
    函数式接口 - 递归地在每个节点和其子节点之间插入一个值为 -1 的节点。
    """
    if not root:
        return None
    
    # 插入左子节点
    if root.left:
        new_left = TreeNode(-1)
        new_left.left = root.left
        root.left = new_left
        decorate_tree(new_left.left)
    
    # 插入右子节点
    if root.right:
        new_right = TreeNode(-1)
        new_right.right = root.right
        root.right = new_right
        decorate_tree(new_right.right)
    
    return root

Solution = create_solution(decorate_tree)