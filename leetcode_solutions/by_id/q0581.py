"""
581. 最短无序连续子数组 - 给你一个整数数组 nums ，你需要找出一个 连续子数组 ，如果对这个子数组进行升序排序，那么整个数组都会变为升序排序。 请你找出符合题意的 最短 子数组，并输出它的长度。 示例 1： 输入：nums = [2,6,4,8,10,9,15] 输出：5 解释：你只需要对 [6, 4, 8, 10, 9] 进行升序排序，那么整个表都会变为升序排序。 示例 2： 输入：nums = [1,2,3,4] 输出：0 示例 3： 输入：nums = [1] 输出：0 提示： * 1 <= nums.length <= 104 * -105 <= nums[i] <= 105 进阶：你可以设计一个时间复杂度为 O(n) 的解决方案吗？
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调栈来找到需要排序的子数组的左右边界。

算法步骤:
1. 从左到右遍历数组，使用单调递增栈找到最右边的需要排序的元素。
2. 从右到左遍历数组，使用单调递减栈找到最左边的需要排序的元素。
3. 计算左右边界的差值加一，即为最短无序连续子数组的长度。

关键点:
- 使用两个单调栈分别从左到右和从右到左遍历数组。
- 找到需要排序的子数组的左右边界。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    if not nums:
        return 0

    n = len(nums)
    left, right = n, 0
    stack = []

    # 从左到右遍历，找到最右边的需要排序的元素
    for i in range(n):
        while stack and nums[stack[-1]] > nums[i]:
            left = min(left, stack.pop())
        stack.append(i)

    stack.clear()

    # 从右到左遍历，找到最左边的需要排序的元素
    for i in range(n - 1, -1, -1):
        while stack and nums[stack[-1]] < nums[i]:
            right = max(right, stack.pop())
        stack.append(i)

    if left >= right:
        return 0
    return right - left + 1


Solution = create_solution(solution_function_name)