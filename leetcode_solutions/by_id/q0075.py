def sort_colors(nums: List[int]) -> None:
    """
    函数式接口 - 三指针（荷兰国旗问题）
    
    实现思路:
    使用三指针将数组分为三部分：0、1、2，一次遍历完成排序。
    
    Args:
        nums: 包含0、1、2的数组，原地修改
        
    Returns:
        None（原地修改nums）
        
    Example:
        >>> nums = [2,0,2,1,1,0]
        >>> sort_colors(nums)
        >>> # nums变为[0,0,1,1,2,2]
    """
    left, right = 0, len(nums) - 1
    i = 0
    
    while i <= right:
        if nums[i] == 0:
            nums[left], nums[i] = nums[i], nums[left]
            left += 1
            i += 1
        elif nums[i] == 1:
            i += 1
        else:  # nums[i] == 2
            nums[right], nums[i] = nums[i], nums[right]
            right -= 1
            # i不变，因为交换来的元素需要重新检查


