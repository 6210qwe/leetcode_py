"""
3003. 执行操作后的最大分割数量 - 给定一个字符串 s 和一个整数 k。 首先，你最多可以更改 s 中的 一处 下标对应字符为另一个小写英文字母。 之后，执行以下分割操作，直到 s 变为 空串： * 选择 s 的最长 前缀，该前缀最多包含 k 个 不同 字符。 * 从 s 中 删除 这个前缀，并将分割数量加一。如果有剩余字符，它们在 s 中保持原来的顺序。 返回一个整数，表示在 最多 改变一处下标对应字符的情况下，经过操作后得到的最大分割数。 示例 1： 输入：s = "accca", k = 2 输出：3 解释： 最好的方式是把 s[2] 变为除了 a 和 c 之外的东西，比如 b。然后它变成了 "acbca"。 然后我们执行以下操作： 1. 最多包含 2 个不同字符的最长前缀是 "ac"，我们删除它然后 s 变为 "bca"。 2. 现在最多包含 2 个不同字符的最长前缀是 "bc"，所以我们删除它然后 s 变为 "a"。 3. 最后，我们删除 "a" 并且 s 变成空串，所以该过程结束。 进行操作时，字符串被分成 3 个部分，所以答案是 3。 示例 2： 输入：s = "aabaab", k = 3 输出：1 解释： 一开始 s 包含 2 个不同的字符，所以无论我们改变哪个， 它最多包含 3 个不同字符，因此最多包含 3 个不同字符的最长前缀始终是所有字符，因此答案是 1。 示例 3： 输入：s = "xxyz", k = 1 输出：4 解释： 最好的方式是将 s[0] 或 s[1] 变为 s 中字符以外的东西，例如将 s[0] 变为 w。 然后 s 变为 "wxyz"，包含 4 个不同的字符，所以当 k 为 1，它将分为 4 个部分。 提示： * 1 <= s.length <= 104 * s 只包含小写英文字母。 * 1 <= k <= 26
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和状态压缩来解决这个问题。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][mask] 表示在字符串 s 的前 i 个字符中，使用 mask 表示的不同字符集合的最大分割数。
2. 遍历字符串 s 的每个字符，更新 dp 数组。
3. 对于每个字符，尝试将其替换为其他字符，并更新 dp 数组。
4. 返回 dp 数组中的最大值。

关键点:
- 使用状态压缩来表示不同字符的集合。
- 动态规划来记录每个状态下的最大分割数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * 2^k)，其中 n 是字符串 s 的长度，k 是不同字符的数量。
空间复杂度: O(n * 2^k)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_partitions_after_operations(s: str, k: int) -> int:
    n = len(s)
    if k >= 26:
        return 1  # 如果 k 大于等于 26，那么整个字符串就是一个分割

    # 初始化 dp 数组
    dp = [[0] * (1 << k) for _ in range(n + 1)]
    char_to_bit = {chr(ord('a') + i): 1 << i for i in range(k)}

    for i in range(1, n + 1):
        for mask in range(1 << k):
            if dp[i - 1][mask] == 0:
                continue
            # 不替换当前字符
            bit = char_to_bit.get(s[i - 1], 0)
            if (mask & bit) != 0 or bin(mask).count('1') < k:
                new_mask = mask | bit
                dp[i][new_mask] = max(dp[i][new_mask], dp[i - 1][mask] + (bin(new_mask).count('1') == k))

            # 替换当前字符
            for j in range(k):
                if (mask & (1 << j)) == 0:
                    new_mask = mask | (1 << j)
                    dp[i][new_mask] = max(dp[i][new_mask], dp[i - 1][mask] + (bin(new_mask).count('1') == k))

    return max(max(row) for row in dp)

Solution = create_solution(max_partitions_after_operations)