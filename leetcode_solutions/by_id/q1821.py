def solution_function_name(params):
    """
    函数式接口 - 实现最优解法
    """
    # SQL 查询实现
    query = """
    SELECT product_id, SUM(quantity * price) AS total_amount
    FROM invoices
    GROUP BY product_id
    HAVING total_amount >= 100;
    """
    return query

