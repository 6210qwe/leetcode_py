"""
核心思想: 使用 SQL 查询来计算每个产品的总金额，并筛选出总金额大于或等于指定阈值的产品。

算法步骤:
1. 使用 `GROUP BY` 子句按产品分组。
2. 使用 `SUM` 函数计算每个产品的总金额。
3. 使用 `HAVING` 子句筛选出总金额大于或等于指定阈值的产品。

关键点:
- 使用 `GROUP BY` 和 `HAVING` 子句进行分组和筛选。
- 使用 `SUM` 函数计算总金额。
"""

