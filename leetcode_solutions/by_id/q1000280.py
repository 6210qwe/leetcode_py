# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示 t 的前 i 个字符和 s 的前 j 个字符之间的不同子序列的数量。

算法步骤:
1. 初始化一个 (m+1) x (n+1) 的二维数组 dp，其中 m 是 t 的长度，n 是 s 的长度。
2. 对于 dp[0][j]，表示 t 为空字符串时，s 的任何子序列都是 t 的子序列，因此 dp[0][j] = 1。
3. 对于 dp[i][0]，表示 s 为空字符串时，t 的任何非空子序列都不可能是 s 的子序列，因此 dp[i][0] = 0。
4. 对于 dp[i][j]，如果 t[i-1] == s[j-1]，则 dp[i][j] = dp[i-1][j-1] + dp[i][j-1]；否则，dp[i][j] = dp[i][j-1]。

关键点:
- 动态规划的状态转移方程
- 边界条件的处理
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是 t 的长度，n 是 s 的长度。
空间复杂度: O(m * n)，用于存储动态规划表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def num_distinct(s: str, t: str) -> int:
    """
    计算在 s 的子序列中 t 出现的个数。
    """
    m, n = len(t), len(s)
    if m > n:
        return 0

    # 初始化动态规划表
    dp = [[0] * (n + 1) for _ in range(m + 1)]

    # 空字符串 t 是 s 的任何子序列
    for j in range(n + 1):
        dp[0][j] = 1

    # 填充动态规划表
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if t[i - 1] == s[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + dp[i][j - 1]
            else:
                dp[i][j] = dp[i][j - 1]

    return dp[m][n]


Solution = create_solution(num_distinct)