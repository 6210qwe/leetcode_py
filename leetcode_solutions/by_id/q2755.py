"""
2707. 字符串中的额外字符 - 给你一个下标从 0 开始的字符串 s 和一个单词字典 dictionary 。你需要将 s 分割成若干个 互不重叠 的子字符串，每个子字符串都在 dictionary 中出现过。s 中可能会有一些 额外的字符 不在任何子字符串中。 请你采取最优策略分割 s ，使剩下的字符 最少 。 示例 1： 输入：s = "leetscode", dictionary = ["leet","code","leetcode"] 输出：1 解释：将 s 分成两个子字符串：下标从 0 到 3 的 "leet" 和下标从 5 到 8 的 "code" 。只有 1 个字符没有使用（下标为 4），所以我们返回 1 。 示例 2： 输入：s = "sayhelloworld", dictionary = ["hello","world"] 输出：3 解释：将 s 分成两个子字符串：下标从 3 到 7 的 "hello" 和下标从 8 到 12 的 "world" 。下标为 0 ，1 和 2 的字符没有使用，所以我们返回 3 。 提示： * 1 <= s.length <= 50 * 1 <= dictionary.length <= 50 * 1 <= dictionary[i].length <= 50 * dictionary[i] 和 s 只包含小写英文字母。 * dictionary 中的单词互不相同。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i] 为字符串 s 前 i 个字符的最小额外字符数。通过遍历字符串 s 的每一个位置，并检查从该位置开始的所有可能的子字符串是否在字典中，我们可以更新 dp 数组。

算法步骤:
1. 初始化 dp 数组，dp[0] = 0，表示空字符串的额外字符数为 0。
2. 对于每一个位置 i，初始化 dp[i] 为 dp[i-1] + 1，表示当前字符是一个额外字符。
3. 检查从位置 i 开始的所有可能的子字符串，如果某个子字符串在字典中，则更新 dp[i+len(substring)] 为 min(dp[i+len(substring)], dp[i-1])。
4. 返回 dp[len(s)]，即整个字符串的最小额外字符数。

关键点:
- 动态规划的状态转移方程：dp[i] = min(dp[i], dp[j-1])，其中 j 是从 i 开始的子字符串的结束位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2 * m)，其中 n 是字符串 s 的长度，m 是字典中单词的最大长度。
空间复杂度: O(n)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_extra_chars(s: str, dictionary: List[str]) -> int:
    """
    函数式接口 - 计算字符串 s 中的最小额外字符数
    """
    n = len(s)
    dp = [float('inf')] * (n + 1)
    dp[0] = 0

    for i in range(1, n + 1):
        dp[i] = dp[i - 1] + 1
        for word in dictionary:
            if i >= len(word) and s[i - len(word):i] == word:
                dp[i] = min(dp[i], dp[i - len(word)])

    return dp[n]


Solution = create_solution(min_extra_chars)