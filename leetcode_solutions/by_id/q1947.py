"""
1819. 序列中不同最大公约数的数目 - 给你一个由正整数组成的数组 nums 。 数字序列的 最大公约数 定义为序列中所有整数的共有约数中的最大整数。 * 例如，序列 [4,6,16] 的最大公约数是 2 。 数组的一个 子序列 本质是一个序列，可以通过删除数组中的某些元素（或者不删除）得到。 * 例如，[2,5,10] 是 [1,2,1,2,4,1,5,10] 的一个子序列。 计算并返回 nums 的所有 非空 子序列中 不同 最大公约数的 数目 。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2021/04/03/image-1.png] 输入：nums = [6,10,3] 输出：5 解释：上图显示了所有的非空子序列与各自的最大公约数。 不同的最大公约数为 6 、10 、3 、2 和 1 。 示例 2： 输入：nums = [5,15,40,5,6] 输出：7 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 2 * 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来存储所有可能的最大公约数，并通过遍历每个可能的公约数来计算其是否可以作为某个子序列的最大公约数。

算法步骤:
1. 初始化一个集合 `gcds` 来存储所有可能的最大公约数。
2. 遍历从 1 到 max(nums) 的每一个整数 `i`，检查 `i` 是否可以作为某个子序列的最大公约数。
3. 对于每个 `i`，遍历 `nums` 中的所有元素 `num`，如果 `num % i == 0`，则更新当前子序列的最大公约数。
4. 如果当前子序列的最大公约数等于 `i`，则将 `i` 添加到 `gcds` 集合中。
5. 返回 `gcds` 集合的大小。

关键点:
- 使用集合来存储不同的最大公约数，确保唯一性。
- 通过遍历每个可能的公约数来计算其是否可以作为某个子序列的最大公约数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * log(max(nums)))
空间复杂度: O(max(nums))
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算并返回 nums 的所有非空子序列中不同最大公约数的数目
    """
    def gcd(a: int, b: int) -> int:
        """计算两个数的最大公约数"""
        while b:
            a, b = b, a % b
        return a

    max_num = max(nums)
    gcds = set()

    for i in range(1, max_num + 1):
        current_gcd = 0
        for num in nums:
            if num % i == 0:
                current_gcd = gcd(current_gcd, num)
                if current_gcd == i:
                    gcds.add(i)
                    break

    return len(gcds)


Solution = create_solution(solution_function_name)