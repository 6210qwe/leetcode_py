"""
2870. 使数组为空的最少操作次数 - 给你一个下标从 0 开始的正整数数组 nums 。 你可以对数组执行以下两种操作 任意次 ： * 从数组中选择 两个 值 相等 的元素，并将它们从数组中 删除 。 * 从数组中选择 三个 值 相等 的元素，并将它们从数组中 删除 。 请你返回使数组为空的 最少 操作次数，如果无法达成，请返回 -1 。 示例 1： 输入：nums = [2,3,3,2,2,4,2,3,4] 输出：4 解释：我们可以执行以下操作使数组为空： - 对下标为 0 和 3 的元素执行第一种操作，得到 nums = [3,3,2,4,2,3,4] 。 - 对下标为 2 和 4 的元素执行第一种操作，得到 nums = [3,3,4,3,4] 。 - 对下标为 0 ，1 和 3 的元素执行第二种操作，得到 nums = [4,4] 。 - 对下标为 0 和 1 的元素执行第一种操作，得到 nums = [] 。 至少需要 4 步操作使数组为空。 示例 2： 输入：nums = [2,1,2,2,3,3] 输出：-1 解释：无法使数组为空。 提示： * 2 <= nums.length <= 105 * 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个数字的出现次数，然后根据每个数字的出现次数计算最少操作次数。

算法步骤:
1. 统计每个数字的出现次数。
2. 对于每个数字的出现次数，计算最少操作次数：
   - 如果出现次数是 1，则无法通过任何操作使其消失，返回 -1。
   - 否则，优先使用删除三个相同元素的操作，如果剩余 1 或 2 个元素，则使用删除两个相同元素的操作。

关键点:
- 优先使用删除三个相同元素的操作，因为这样可以减少操作次数。
- 如果某个数字的出现次数是 1，则无法通过任何操作使其消失。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组来统计每个数字的出现次数。
空间复杂度: O(m)，其中 m 是数组中不同数字的数量。我们需要一个哈希表来存储每个数字的出现次数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import Counter

def min_operations(nums: List[int]) -> int:
    """
    函数式接口 - 计算使数组为空的最少操作次数
    """
    # 统计每个数字的出现次数
    count = Counter(nums)
    
    # 计算最少操作次数
    operations = 0
    for freq in count.values():
        if freq == 1:
            return -1
        # 优先使用删除三个相同元素的操作
        operations += freq // 3
        # 如果剩余 1 或 2 个元素，则使用删除两个相同元素的操作
        if freq % 3 != 0:
            operations += 1
    
    return operations

