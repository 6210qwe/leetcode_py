# ============================================================================
# 问题描述
# ============================================================================
"""
2576. 求出最多标记下标 - 给你一个下标从 0 开始的整数数组 nums 。 一开始，所有下标都没有被标记。你可以执行以下操作任意次： * 选择两个 互不相同且未标记 的下标 i 和 j ，满足 2 * nums[i] <= nums[j] ，标记下标 i 和 j 。 请你执行上述操作任意次，返回 nums 中最多可以标记的下标数目。 示例 1： 输入：nums = [3,5,2,4] 输出：2 解释：第一次操作中，选择 i = 2 和 j = 1 ，操作可以执行的原因是 2 * nums[2] <= nums[1] ，标记下标 2 和 1 。 没有其他更多可执行的操作，所以答案为 2 。 示例 2： 输入：nums = [9,2,5,4] 输出：4 解释：第一次操作中，选择 i = 3 和 j = 0 ，操作可以执行的原因是 2 * nums[3] <= nums[0] ，标记下标 3 和 0 。 第二次操作中，选择 i = 1 和 j = 2 ，操作可以执行的原因是 2 * nums[1] <= nums[2] ，标记下标 1 和 2 。 没有其他更多可执行的操作，所以答案为 4 。 示例 3： 输入：nums = [7,6,8] 输出：0 解释：没有任何可以执行的操作，所以答案为 0 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和双指针来最大化标记的下标数量。

算法步骤:
1. 对数组进行排序。
2. 将数组分成两部分，前半部分和后半部分。
3. 使用双指针分别遍历前半部分和后半部分，找到满足 2 * nums[i] <= nums[j] 的最大匹配对数。

关键点:
- 排序后，前半部分的元素较小，后半部分的元素较大，这样可以最大化匹配对数。
- 使用双指针可以高效地找到满足条件的匹配对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度是 O(n log n)，双指针遍历的时间复杂度是 O(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现求出最多标记下标的算法
    """
    # 对数组进行排序
    nums.sort()
    
    # 初始化双指针
    left, right = 0, (len(nums) + 1) // 2
    count = 0
    
    # 使用双指针遍历数组
    while left < right and right < len(nums):
        if 2 * nums[left] <= nums[right]:
            count += 2
            left += 1
        right += 1
    
    return count


Solution = create_solution(solution_function_name)