"""
2289. 使数组按非递减顺序排列 - 给你一个下标从 0 开始的整数数组 nums 。在一步操作中，移除所有满足 nums[i - 1] > nums[i] 的 nums[i] ，其中 0 < i < nums.length 。 重复执行步骤，直到 nums 变为 非递减 数组，返回所需执行的操作数。 示例 1： 输入：nums = [5,3,4,4,7,3,6,11,8,5,11] 输出：3 解释：执行下述几个步骤： - 步骤 1 ：[5,3,4,4,7,3,6,11,8,5,11] 变为 [5,4,4,7,6,11,11] - 步骤 2 ：[5,4,4,7,6,11,11] 变为 [5,4,7,11,11] - 步骤 3 ：[5,4,7,11,11] 变为 [5,7,11,11] [5,7,11,11] 是一个非递减数组，因此，返回 3 。 示例 2： 输入：nums = [4,5,7,7,13] 输出：0 解释：nums 已经是一个非递减数组，因此，返回 0 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调栈来维护当前的非递减序列，并记录每个元素被移除所需的步数。

算法步骤:
1. 初始化一个单调栈和一个计数器数组，用于记录每个元素被移除所需的步数。
2. 遍历数组，对于每个元素，如果它小于栈顶元素，则更新计数器并移除栈顶元素，直到找到一个合适的插入位置。
3. 将当前元素及其需要的步数压入栈中。
4. 返回计数器中的最大值。

关键点:
- 使用单调栈来维护当前的非递减序列。
- 记录每个元素被移除所需的步数。
"""

