"""
核心思想: 使用双指针从两端向中间遍历数组，当遇到不相等的元素时，选择较小的元素进行合并操作，直到数组变为回文。

算法步骤:
1. 初始化两个指针，分别指向数组的首尾。
2. 比较两个指针所指向的元素：
   - 如果相等，移动两个指针。
   - 如果不相等，选择较小的元素进行合并操作，并移动对应的指针。
3. 重复步骤2，直到两个指针相遇。

关键点:
- 使用双指针可以有效地减少不必要的比较和合并操作。
- 选择较小的元素进行合并操作，确保最终数组能变为回文。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个元素最多被访问一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

