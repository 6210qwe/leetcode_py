from typing import List

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    left, right = 0, len(nums) - 1
    operations = 0
    
    while left < right:
        if nums[left] == nums[right]:
            left += 1
            right -= 1
        elif nums[left] < nums[right]:
            nums[left + 1] += nums[left]
            left += 1
            operations += 1
        else:
            nums[right - 1] += nums[right]
            right -= 1
            operations += 1
    
