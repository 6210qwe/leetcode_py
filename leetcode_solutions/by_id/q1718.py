def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询来计算每个仓库的库存总量。
    """
    # SQL 查询实现最优解法
    query = """
    SELECT 
        warehouse_id, 
        SUM(quantity) AS total_quantity
    FROM 
        Inventory
    GROUP BY 
        warehouse_id;
    """
    return query


