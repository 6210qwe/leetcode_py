"""
2425. 所有数对的异或和 - 给你两个下标从 0 开始的数组 nums1 和 nums2 ，两个数组都只包含非负整数。请你求出另外一个数组 nums3 ，包含 nums1 和 nums2 中 所有数对 的异或和（nums1 中每个整数都跟 nums2 中每个整数 恰好 匹配一次）。 请你返回 nums3 中所有整数的 异或和 。 示例 1： 输入：nums1 = [2,1,3], nums2 = [10,2,5,0] 输出：13 解释： 一个可能的 nums3 数组是 [8,0,7,2,11,3,4,1,9,1,6,3] 。 所有这些数字的异或和是 13 ，所以我们返回 13 。 示例 2： 输入：nums1 = [1,2], nums2 = [3,4] 输出：0 解释： 所有数对异或和的结果分别为 nums1[0] ^ nums2[0] ，nums1[0] ^ nums2[1] ，nums1[1] ^ nums2[0] 和 nums1[1] ^ nums2[1] 。 所以，一个可能的 nums3 数组是 [2,5,1,6] 。 2 ^ 5 ^ 1 ^ 6 = 0 ，所以我们返回 0 。 提示： * 1 <= nums1.length, nums2.length <= 105 * 0 <= nums1[i], nums2[j] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 利用异或运算的性质，将所有数对的异或和简化为两个数组的长度和元素的异或和。

算法步骤:
1. 如果 nums1 的长度为奇数，则将 nums2 中所有元素的异或和加入结果。
2. 如果 nums2 的长度为奇数，则将 nums1 中所有元素的异或和加入结果。
3. 返回最终结果。

关键点:
- 利用异或运算的性质，可以将复杂度降低到 O(n + m)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 nums1 的长度，m 是 nums2 的长度。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums1: List[int], nums2: List[int]) -> int:
    """
    函数式接口 - 计算所有数对的异或和
    """
    xor_sum = 0
    
    # 如果 nums1 的长度为奇数，则将 nums2 中所有元素的异或和加入结果
    if len(nums1) % 2 == 1:
        for num in nums2:
            xor_sum ^= num
    
    # 如果 nums2 的长度为奇数，则将 nums1 中所有元素的异或和加入结果
    if len(nums2) % 2 == 1:
        for num in nums1:
            xor_sum ^= num
    
    return xor_sum


Solution = create_solution(solution_function_name)