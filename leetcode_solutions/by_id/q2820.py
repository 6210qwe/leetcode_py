"""
核心思想: 使用 Python 的 *args 参数来接收所有传递的参数，并返回其长度。

算法步骤:
1. 定义一个函数 argumentsLength，使用 *args 来接收所有传递的参数。
2. 返回 len(args) 以获取传递参数的数量。

关键点:
- 使用 *args 来接收所有传递的参数。
- 使用内置函数 len() 来计算参数的数量。
"""

