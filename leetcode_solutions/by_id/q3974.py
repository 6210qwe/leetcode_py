"""
3653. 区间乘法查询后的异或 I - 给你一个长度为 n 的整数数组 nums 和一个大小为 q 的二维整数数组 queries，其中 queries[i] = [li, ri, ki, vi]。 对于每个查询，按以下步骤执行操作： * 设定 idx = li。 * 当 idx <= ri 时： * 更新：nums[idx] = (nums[idx] * vi) % (109 + 7) * 将 idx += ki。 在处理完所有查询后，返回数组 nums 中所有元素的 按位异或 结果。 示例 1： 输入： nums = [1,1,1], queries = [[0,2,1,4]] 输出： 4 解释： * 唯一的查询 [0, 2, 1, 4] 将下标 0 到下标 2 的每个元素乘以 4。 * 数组从 [1, 1, 1] 变为 [4, 4, 4]。 * 所有元素的异或为 4 ^ 4 ^ 4 = 4。 示例 2： 输入： nums = [2,3,1,5,4], queries = [[1,4,2,3],[0,2,1,2]] 输出： 31 解释： * 第一个查询 [1, 4, 2, 3] 将下标 1 和 3 的元素乘以 3，数组变为 [2, 9, 1, 15, 4]。 * 第二个查询 [0, 2, 1, 2] 将下标 0、1 和 2 的元素乘以 2，数组变为 [4, 18, 2, 15, 4]。 * 所有元素的异或为 4 ^ 18 ^ 2 ^ 15 ^ 4 = 31。 提示： * 1 <= n == nums.length <= 103 * 1 <= nums[i] <= 109 * 1 <= q == queries.length <= 103 * queries[i] = [li, ri, ki, vi] * 0 <= li <= ri < n * 1 <= ki <= n * 1 <= vi <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历每个查询并更新指定范围内的元素，最后计算整个数组的异或结果。

算法步骤:
1. 初始化 MOD 为 10^9 + 7。
2. 遍历每个查询，对于每个查询：
   - 获取起始索引 idx。
   - 在 idx <= ri 的范围内，更新 nums[idx] = (nums[idx] * vi) % MOD。
   - 将 idx 增加 ki。
3. 计算并返回数组 nums 的异或结果。

关键点:
- 使用模运算来防止溢出。
- 逐个更新指定范围内的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * q)，其中 n 是 nums 的长度，q 是 queries 的长度。每个查询最多需要更新 n 个元素。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def xor_after_range_multiplication_queries(nums: List[int], queries: List[List[int]]) -> int:
    MOD = 10**9 + 7
    for query in queries:
        li, ri, ki, vi = query
        idx = li
        while idx <= ri:
            nums[idx] = (nums[idx] * vi) % MOD
            idx += ki
    return reduce(lambda x, y: x ^ y, nums)

