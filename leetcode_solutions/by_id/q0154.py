from typing import List
from leetcode_solutions.utils.solution import create_solution


def find_minimum_in_rotated_sorted_array_ii(nums: List[int]) -> int:
    """
    函数式接口 - 寻找旋转排序数组中的最小值II（含重复元素）
    
    实现思路:
    使用二分查找，处理重复元素的情况。
    
    Args:
        nums: 旋转排序数组（可能包含重复元素）
        
    Returns:
        数组中的最小元素
        
    Example:
        >>> find_minimum_in_rotated_sorted_array_ii([2, 2, 2, 0, 1])
        0
    """
    left, right = 0, len(nums) - 1
    
    while left < right:
        mid = (left + right) // 2
        
        if nums[mid] > nums[right]:
            left = mid + 1
        elif nums[mid] < nums[right]:
            right = mid
        else:
            right -= 1
    
    return nums[left]


