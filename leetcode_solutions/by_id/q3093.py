"""
2859. 计算 K 置位下标对应元素的和 - 给你一个下标从 0 开始的整数数组 nums 和一个整数 k 。 请你用整数形式返回 nums 中的特定元素之 和 ，这些特定元素满足：其对应下标的二进制表示中恰存在 k 个置位。 整数的二进制表示中的 1 就是这个整数的 置位 。 例如，21 的二进制表示为 10101 ，其中有 3 个置位。 示例 1： 输入：nums = [5,10,1,5,2], k = 1 输出：13 解释：下标的二进制表示是： 0 = 0002 1 = 0012 2 = 0102 3 = 0112 4 = 1002 下标 1、2 和 4 在其二进制表示中都存在 k = 1 个置位。 因此，答案为 nums[1] + nums[2] + nums[4] = 13 。 示例 2： 输入：nums = [4,3,2,1], k = 2 输出：1 解释：下标的二进制表示是： 0 = 002 1 = 012 2 = 102 3 = 112 只有下标 3 的二进制表示中存在 k = 2 个置位。 因此，答案为 nums[3] = 1 。 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= 105 * 0 <= k <= 10
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算计算每个下标的二进制表示中 1 的个数，并累加符合条件的元素。

算法步骤:
1. 初始化结果变量 res 为 0。
2. 遍历数组 nums 的每个下标 i。
3. 计算下标 i 的二进制表示中 1 的个数。
4. 如果 1 的个数等于 k，则将 nums[i] 加到 res 上。
5. 返回 res。

关键点:
- 使用 Brian Kernighan 算法高效计算二进制表示中 1 的个数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * log m)，其中 n 是数组长度，m 是数组的最大下标值。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_values_at_indices_with_k_set_bits(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算 K 置位下标对应元素的和
    """
    def count_set_bits(n: int) -> int:
        """使用 Brian Kernighan 算法计算二进制表示中 1 的个数"""
        count = 0
        while n:
            n &= (n - 1)
            count += 1
        return count

    res = 0
    for i in range(len(nums)):
        if count_set_bits(i) == k:
            res += nums[i]
    return res


