from typing import List
from leetcode_solutions.utils.solution import create_solution


def permutations_ii(nums: List[int]) -> List[List[int]]:
    """
    函数式接口 - 全排列II
    
    实现思路:
    回溯算法，使用交换+去重，跳过重复元素。
    
    Args:
        nums: 可包含重复数字的序列
        
    Returns:
        所有不重复的全排列
        
    Example:
        >>> permutations_ii([1,1,2])
        [[1, 1, 2], [1, 2, 1], [2, 1, 1]]
    """
    result = []
    nums.sort()
    
    def backtrack(start: int):
        """回溯函数"""
        if start == len(nums):
            result.append(nums[:])
            return
        
        used = set()
        for i in range(start, len(nums)):
            if nums[i] in used:
                continue
            used.add(nums[i])
            nums[start], nums[i] = nums[i], nums[start]
            backtrack(start + 1)
            nums[start], nums[i] = nums[i], nums[start]
    
    backtrack(0)
    return result


