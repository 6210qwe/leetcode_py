"""
322. 零钱兑换 - 给你一个整数数组 coins ，表示不同面额的硬币；以及一个整数 amount ，表示总金额。 计算并返回可以凑成总金额所需的 最少的硬币个数 。如果没有任何一种硬币组合能组成总金额，返回 -1 。 你可以认为每种硬币的数量是无限的。 示例 1： 输入：coins = [1, 2, 5], amount = 11 输出：3 解释：11 = 5 + 5 + 1 示例 2： 输入：coins = [2], amount = 3 输出：-1 示例 3： 输入：coins = [1], amount = 0 输出：0 提示： * 1 <= coins.length <= 12 * 1 <= coins[i] <= 231 - 1 * 0 <= amount <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划，dp[i]表示凑成金额i所需的最少硬币数

算法步骤:
1. dp[i] = min(dp[i], dp[i-coin] + 1) for coin in coins
2. 初始化dp[0] = 0，其他为无穷大
3. 遍历所有金额，更新dp数组

关键点:
- 完全背包问题的变种
- 时间复杂度O(amount * len(coins))，空间复杂度O(amount)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(amount * len(coins)) - 需要遍历所有金额和硬币
空间复杂度: O(amount) - dp数组空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def coin_change(coins: List[int], amount: int) -> int:
    """
    函数式接口 - 零钱兑换
    
    实现思路:
    动态规划，dp[i]表示凑成金额i所需的最少硬币数。
    
    Args:
        coins: 不同面额的硬币数组
        amount: 总金额
        
    Returns:
        可以凑成总金额所需的最少的硬币个数，如果无法凑成返回-1
        
    Example:
        >>> coin_change([1, 2, 5], 11)
        3
    """
    dp = [float('inf')] * (amount + 1)
    dp[0] = 0
    
    for i in range(1, amount + 1):
        for coin in coins:
            if i >= coin:
                dp[i] = min(dp[i], dp[i - coin] + 1)
    
    return dp[amount] if dp[amount] != float('inf') else -1


