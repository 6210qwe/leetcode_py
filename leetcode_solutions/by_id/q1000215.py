"""
LCP 28. 采购方案 - 小力将 N 个零件的报价存于数组 `nums`。小力预算为 `target`，假定小力仅购买两个零件，要求购买零件的花费不超过预算，请问他有多少种采购方案。 注意：答案需要以 `1e9 + 7 (1000000007)` 为底取模，如：计算初始结果为：`1000000008`，请返回 `1` **示例 1：** >输入：`nums = [2,5,3,5], target = 6` > >输出：`1` > >解释：预算内仅能购买 nums[0] 与 nums[2]。 **示例 2：** >输入：`nums = [2,2,1,9], target = 10` > >输出：`4` > >解释：符合预算的采购方案如下： >nums[0] + nums[1] = 4 >nums[0] + nums[2] = 3 >nums[1] + nums[2] = 3 >nums[2] + nums[3] = 10 **提示：** - `2 <= nums.length <= 10^5` - `1 <= nums[i], target <= 10^5`
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来找到所有符合条件的采购方案。

算法步骤:
1. 对数组进行排序。
2. 初始化两个指针，左指针从数组的起始位置开始，右指针从数组的末尾位置开始。
3. 当左指针小于右指针时，检查当前左右指针指向的元素之和是否小于等于目标值：
   - 如果是，则说明从左指针到右指针之间的所有元素都满足条件，累加这些组合的数量。
   - 如果不是，则移动右指针向左。
4. 移动左指针向右，重复上述过程。
5. 最后返回结果对 `1000000007` 取模。

关键点:
- 排序后的数组可以使用双指针方法高效地找到所有符合条件的采购方案。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序操作的时间复杂度为 O(n log n)，双指针遍历的时间复杂度为 O(n)。
空间复杂度: O(1) - 除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int], target: int) -> int:
    """
    函数式接口 - 使用双指针方法找到所有符合条件的采购方案。
    """
    MOD = 1000000007
    nums.sort()  # 对数组进行排序
    left, right = 0, len(nums) - 1
    count = 0

    while left < right:
        if nums[left] + nums[right] <= target:
            count += (right - left)
            left += 1
        else:
            right -= 1

    return count % MOD


Solution = create_solution(solution_function_name)