"""
给定一个递增排序的数组 nums，其中每个元素都是唯一的，并且在 [0, n] 范围内（n = nums.length - 1），请找出缺失的那个数字。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到缺失的数字。

算法步骤:
1. 初始化左右指针 left 和 right 分别为 0 和 len(nums) - 1。
2. 计算中间点 mid。
3. 检查 nums[mid] 是否等于其索引 mid。如果是，则缺失的数字在右半部分；否则，在左半部分。
4. 更新左右指针，继续二分查找直到找到缺失的数字。

关键点:
- 利用二分查找将时间复杂度降低到 O(log n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 使用二分查找找到缺失的数字
    """
    left, right = 0, len(nums) - 1
    
    while left <= right:
        mid = (left + right) // 2
        if nums[mid] == mid:
            left = mid + 1
        else:
            right = mid - 1
    
    return left


Solution = create_solution(solution_function_name)