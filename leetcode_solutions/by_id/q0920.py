"""
884. 两句话中的不常见单词 - 句子 是一串由空格分隔的单词。每个 单词 仅由小写字母组成。 如果某个单词在其中一个句子中恰好出现一次，在另一个句子中却 没有出现 ，那么这个单词就是 不常见的 。 给你两个 句子 s1 和 s2 ，返回所有 不常用单词 的列表。返回列表中单词可以按 任意顺序 组织。 示例 1： 输入：s1 = "this apple is sweet", s2 = "this apple is sour" 输出：["sweet","sour"] 示例 2： 输入：s1 = "apple apple", s2 = "banana" 输出：["banana"] 提示： * 1 <= s1.length, s2.length <= 200 * s1 和 s2 由小写英文字母和空格组成 * s1 和 s2 都不含前导或尾随空格 * s1 和 s2 中的所有单词间均由单个空格分隔
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个单词的出现次数，然后找出只出现一次的单词。

算法步骤:
1. 将两个句子合并成一个字符串。
2. 将字符串按空格分割成单词列表。
3. 使用哈希表统计每个单词的出现次数。
4. 遍历哈希表，找出只出现一次的单词。

关键点:
- 使用哈希表统计单词出现次数。
- 合并两个句子以简化处理。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 和 m 分别是 s1 和 s2 的长度。
空间复杂度: O(n + m)，用于存储单词及其出现次数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def uncommon_from_sentences(s1: str, s2: str) -> List[str]:
    """
    函数式接口 - 返回两个句子中的不常见单词
    """
    # 合并两个句子
    combined = s1 + " " + s2
    # 将字符串按空格分割成单词列表
    words = combined.split()
    # 使用哈希表统计每个单词的出现次数
    word_count = {}
    for word in words:
        if word in word_count:
            word_count[word] += 1
        else:
            word_count[word] = 1
    # 找出只出现一次的单词
    result = [word for word, count in word_count.items() if count == 1]
    return result


Solution = create_solution(uncommon_from_sentences)