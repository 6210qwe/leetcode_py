"""
1190. 反转每对括号间的子串 - 给出一个字符串 s（仅含有小写英文字母和括号）。 请你按照从括号内到外的顺序，逐层反转每对匹配括号中的字符串，并返回最终的结果。 注意，您的结果中 不应 包含任何括号。 示例 1： 输入：s = "(abcd)" 输出："dcba" 示例 2： 输入：s = "(u(love)i)" 输出："iloveu" 解释：先反转子字符串 "love" ，然后反转整个字符串。 示例 3： 输入：s = "(ed(et(oc))el)" 输出："leetcode" 解释：先反转子字符串 "oc" ，接着反转 "etco" ，然后反转整个字符串。 提示： * 1 <= s.length <= 2000 * s 中只有小写英文字母和括号 * 题目测试用例确保所有括号都是成对出现的
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来处理嵌套的括号，并在遇到右括号时反转子字符串。

算法步骤:
1. 初始化一个空栈。
2. 遍历字符串 s：
   - 如果遇到左括号 '(', 将当前构建的子字符串压入栈中，并重置子字符串。
   - 如果遇到右括号 ')', 弹出栈顶元素并将其与当前子字符串拼接后反转，然后继续处理。
   - 如果是字母字符，直接添加到当前子字符串中。
3. 最终栈底的元素即为结果字符串。

关键点:
- 使用栈来处理嵌套结构。
- 在遇到右括号时进行反转操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。每个字符最多只会被处理两次（一次入栈，一次出栈）。
空间复杂度: O(n)，最坏情况下栈的大小会达到 n/2。
"""

# ============================================================================
