"""
面试题 10.11. 峰与谷 - 在一个整数数组中，“峰”是大于或等于相邻整数的元素，相应地，“谷”是小于或等于相邻整数的元素。例如，在数组{5, 8, 4, 2, 3, 4, 6}中，{8, 6}是峰， {5, 2}是谷。现在给定一个整数数组，将该数组按峰与谷的交替顺序排序。 示例： 输入：[5, 3, 1, 2, 3] 输出：[5, 1, 3, 2, 3] 提示： * nums.length <= 10000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历调整数组，使其满足峰与谷的交替顺序。

算法步骤:
1. 遍历数组，从第二个元素开始，检查当前元素是否满足峰与谷的交替顺序。
2. 如果不满足，则交换当前元素和前一个元素，使其满足交替顺序。

关键点:
- 通过一次遍历即可完成调整，时间复杂度为 O(n)。
- 空间复杂度为 O(1)，因为只使用了常数级的额外空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
