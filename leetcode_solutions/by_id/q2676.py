"""
2640. 一个数组所有前缀的分数 - 定义一个数组 arr 的 转换数组 conver 为： * conver[i] = arr[i] + max(arr[0..i])，其中 max(arr[0..i]) 是满足 0 <= j <= i 的所有 arr[j] 中的最大值。 定义一个数组 arr 的 分数 为 arr 转换数组中所有元素的和。 给你一个下标从 0 开始长度为 n 的整数数组 nums ，请你返回一个长度为 n 的数组 ans ，其中 ans[i]是前缀 nums[0..i] 的分数。 示例 1： 输入：nums = [2,3,7,5,10] 输出：[4,10,24,36,56] 解释： 对于前缀 [2] ，转换数组为 [4] ，所以分数为 4 。 对于前缀 [2, 3] ，转换数组为 [4, 6] ，所以分数为 10 。 对于前缀 [2, 3, 7] ，转换数组为 [4, 6, 14] ，所以分数为 24 。 对于前缀 [2, 3, 7, 5] ，转换数组为 [4, 6, 14, 12] ，所以分数为 36 。 对于前缀 [2, 3, 7, 5, 10] ，转换数组为 [4, 6, 14, 12, 20] ，所以分数为 56 。 示例 2： 输入：nums = [1,1,2,4,8,16] 输出：[2,4,8,16,32,64] 解释： 对于前缀 [1] ，转换数组为 [2] ，所以分数为 2 。 对于前缀 [1, 1]，转换数组为 [2, 2] ，所以分数为 4 。 对于前缀 [1, 1, 2]，转换数组为 [2, 2, 4] ，所以分数为 8 。 对于前缀 [1, 1, 2, 4]，转换数组为 [2, 2, 4, 8] ，所以分数为 16 。 对于前缀 [1, 1, 2, 4, 8]，转换数组为 [2, 2, 4, 8, 16] ，所以分数为 32 。 对于前缀 [1, 1, 2, 4, 8, 16]，转换数组为 [2, 2, 4, 8, 16, 32] ，所以分数为 64 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一次遍历计算每个前缀的最大值，并在此过程中构建转换数组和分数数组。

算法步骤:
1. 初始化最大值变量 `max_val` 为 `nums[0]`。
2. 初始化转换数组 `conver` 和分数数组 `ans`，并将 `conver[0]` 设为 `nums[0] + max_val`，`ans[0]` 设为 `conver[0]`。
3. 遍历 `nums` 数组，从索引 1 开始：
   - 更新 `max_val` 为当前 `max_val` 和 `nums[i]` 的较大值。
   - 计算 `conver[i]` 为 `nums[i] + max_val`。
   - 计算 `ans[i]` 为 `ans[i-1] + conver[i]`。
4. 返回 `ans` 数组。

关键点:
- 在一次遍历中同时更新最大值、转换数组和分数数组，避免多次遍历。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 `nums` 的长度。我们只需要一次遍历。
空间复杂度: O(n)，我们需要额外的空间来存储转换数组和分数数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_prefix_scores(nums: List[int]) -> List[int]:
    """
    计算一个数组所有前缀的分数
    """
    n = len(nums)
    max_val = nums[0]
    conver = [0] * n
    ans = [0] * n
    
    # 初始化第一个前缀
    conver[0] = nums[0] + max_val
    ans[0] = conver[0]
    
    # 遍历数组，计算每个前缀的分数
    for i in range(1, n):
        max_val = max(max_val, nums[i])
        conver[i] = nums[i] + max_val
        ans[i] = ans[i - 1] + conver[i]
    
    return ans


Solution = create_solution(find_prefix_scores)