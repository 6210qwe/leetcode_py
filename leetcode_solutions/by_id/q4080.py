"""
3718. 缺失的最小倍数 - 给你一个整数数组 nums 和一个整数 k，请返回从 nums 中缺失的、最小的正整数 k 的倍数。 倍数 指能被 k 整除的任意正整数。 示例 1： 输入： nums = [8,2,3,4,6], k = 2 输出： 10 解释： 当 k = 2 时，其倍数为 2、4、6、8、10、12……，其中在 nums 中缺失的最小倍数是 10。 示例 2： 输入： nums = [1,4,7,10,15], k = 5 输出： 5 解释： 当 k = 5 时，其倍数为 5、10、15、20……，其中在 nums 中缺失的最小倍数是 5。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100 * 1 <= k <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来记录数组中的所有元素，然后从 k 开始逐个检查 k 的倍数是否在集合中。

算法步骤:
1. 将 nums 转换为集合以快速查找。
2. 从 k 开始逐个检查 k 的倍数是否在集合中，找到第一个不在集合中的 k 的倍数并返回。

关键点:
- 使用集合进行快速查找。
- 从 k 开始逐个检查 k 的倍数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 nums 的长度，m 是找到第一个缺失的 k 的倍数所需的步数。
空间复杂度: O(n)，用于存储 nums 的集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_missing_multiple(nums: List[int], k: int) -> int:
    """
    函数式接口 - 返回从 nums 中缺失的、最小的正整数 k 的倍数
    """
    # 将 nums 转换为集合
    num_set = set(nums)
    
    # 从 k 开始逐个检查 k 的倍数
    multiple = k
    while multiple in num_set:
        multiple += k
    
    return multiple


Solution = create_solution(smallest_missing_multiple)