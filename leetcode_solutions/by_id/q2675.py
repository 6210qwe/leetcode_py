"""
2639. 查询网格图中每一列的宽度 - 给你一个下标从 0 开始的 m x n 整数矩阵 grid 。矩阵中某一列的宽度是这一列数字的最大 字符串长度 。 * 比方说，如果 grid = [[-10], [3], [12]] ，那么唯一一列的宽度是 3 ，因为 -10 的字符串长度为 3 。 请你返回一个大小为 n 的整数数组 ans ，其中 ans[i] 是第 i 列的宽度。 一个有 len 个数位的整数 x ，如果是非负数，那么 字符串长度 为 len ，否则为 len + 1 。 示例 1： 输入：grid = [[1],[22],[333]] 输出：[3] 解释：第 0 列中，333 字符串长度为 3 。 示例 2： 输入：grid = [[-15,1,3],[15,7,12],[5,6,-2]] 输出：[3,1,2] 解释： 第 0 列中，只有 -15 字符串长度为 3 。 第 1 列中，所有整数的字符串长度都是 1 。 第 2 列中，12 和 -2 的字符串长度都为 2 。 提示： * m == grid.length * n == grid[i].length * 1 <= m, n <= 100 * -109 <= grid[r][c] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历每一列，计算每一列中的最大字符串长度。

算法步骤:
1. 初始化一个长度为 n 的结果数组 `ans`，用于存储每一列的最大字符串长度。
2. 遍历每一列，对于每一列中的每个元素，计算其字符串长度，并更新该列的最大字符串长度。
3. 返回结果数组 `ans`。

关键点:
- 使用 `str` 函数将整数转换为字符串，计算其长度。
- 对于负数，需要额外加 1 来考虑负号。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是矩阵的行数，n 是矩阵的列数。我们需要遍历整个矩阵来计算每一列的最大字符串长度。
空间复杂度: O(n)，结果数组 `ans` 的大小与列数 n 相同。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_column_width(grid: List[List[int]]) -> List[int]:
    """
    计算网格中每一列的宽度。
    """
    if not grid:
        return []

    m, n = len(grid), len(grid[0])
    ans = [0] * n

    for j in range(n):
        max_length = 0
        for i in range(m):
            length = len(str(grid[i][j]))
            if grid[i][j] < 0:
                length += 1
            max_length = max(max_length, length)
        ans[j] = max_length

    return ans


Solution = create_solution(find_column_width)