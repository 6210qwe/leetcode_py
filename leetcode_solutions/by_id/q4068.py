"""
3712. 出现次数能被 K 整除的元素总和 - 给你一个整数数组 nums 和一个整数 k。 请返回一个整数，表示 nums 中所有其 出现次数 能被 k 整除的元素的总和；如果没有这样的元素，则返回 0 。 注意： 若某个元素在数组中的总出现次数能被 k 整除，则它在求和中会被计算 恰好 与其出现次数相同的次数。 元素 x 的 出现次数 指它在数组中出现的次数。 示例 1： 输入： nums = [1,2,2,3,3,3,3,4], k = 2 输出： 16 解释： * 数字 1 出现 1 次（奇数次）。 * 数字 2 出现 2 次（偶数次）。 * 数字 3 出现 4 次（偶数次）。 * 数字 4 出现 1 次（奇数次）。 因此总和为 2 + 2 + 3 + 3 + 3 + 3 = 16。 示例 2： 输入： nums = [1,2,3,4,5], k = 2 输出： 0 解释： 没有元素出现偶数次，因此总和为 0。 示例 3： 输入： nums = [4,4,4,1,2,3], k = 3 输出： 12 解释： * 数字 1 出现 1 次。 * 数字 2 出现 1 次。 * 数字 3 出现 1 次。 * 数字 4 出现 3 次。 因此总和为 4 + 4 + 4 = 12。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100 * 1 <= k <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个元素的出现次数，然后遍历哈希表，累加出现次数能被 k 整除的元素。

算法步骤:
1. 使用哈希表统计每个元素的出现次数。
2. 遍历哈希表，对于每个元素，如果其出现次数能被 k 整除，则将其值乘以出现次数并累加到结果中。

关键点:
- 使用哈希表进行频率统计。
- 通过遍历哈希表来计算满足条件的元素总和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 nums 的长度。需要遍历数组一次来统计频率，再遍历哈希表一次来计算总和。
空间复杂度: O(n)，哈希表的空间开销。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_elements_with_frequency_divisible_by_k(nums: List[int], k: int) -> int:
    """
    计算出现次数能被 k 整除的元素总和
    """
    # 统计每个元素的出现次数
    frequency = {}
    for num in nums:
        if num in frequency:
            frequency[num] += 1
        else:
            frequency[num] = 1

    # 计算总和
    total_sum = 0
    for num, count in frequency.items():
        if count % k == 0:
            total_sum += num * count

    return total_sum


