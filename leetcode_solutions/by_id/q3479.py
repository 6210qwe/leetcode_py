"""
3234. 统计 1 显著的字符串的数量 - 给你一个二进制字符串 s。 请你统计并返回其中 1 显著 的 子字符串 的数量。 如果字符串中 1 的数量 大于或等于 0 的数量的 平方，则认为该字符串是一个 1 显著 的字符串 。 示例 1： 输入：s = "00011" 输出：5 解释： 1 显著的子字符串如下表所示。 i j s[i..j] 0 的数量 1 的数量 3 3 1 0 1 4 4 1 0 1 2 3 01 1 1 3 4 11 0 2 2 4 011 1 2 示例 2： 输入：s = "101101" 输出：16 解释： 1 不显著的子字符串如下表所示。 总共有 21 个子字符串，其中 5 个是 1 不显著字符串，因此有 16 个 1 显著子字符串。 i j s[i..j] 0 的数量 1 的数量 1 1 0 1 0 4 4 0 1 0 1 4 0110 2 2 0 4 10110 2 3 1 5 01101 2 3 提示： * 1 <= s.length <= 4 * 104 * s 仅包含字符 '0' 和 '1'。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和数组来快速计算任意子字符串中 1 和 0 的数量，并通过双重循环遍历所有可能的子字符串。

算法步骤:
1. 初始化前缀和数组 `prefix`，用于存储从字符串开头到当前位置的 1 和 0 的数量。
2. 遍历字符串，更新前缀和数组。
3. 使用双重循环遍历所有可能的子字符串，计算每个子字符串中 1 和 0 的数量。
4. 检查每个子字符串是否满足 1 的数量大于或等于 0 的数量的平方，如果是则计数。

关键点:
- 使用前缀和数组可以快速计算任意子字符串中 1 和 0 的数量。
- 双重循环遍历所有可能的子字符串，确保不遗漏任何子字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串的长度。双重循环遍历所有可能的子字符串。
空间复杂度: O(n)，使用前缀和数组存储 1 和 0 的数量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_dominant_substrings(s: str) -> int:
    """
    函数式接口 - 统计 1 显著的字符串的数量
    """
    n = len(s)
    prefix = [(0, 0)]  # (count_1, count_0)
    
    # 构建前缀和数组
    for i in range(n):
        count_1, count_0 = prefix[-1]
        if s[i] == '1':
            count_1 += 1
        else:
            count_0 += 1
        prefix.append((count_1, count_0))
    
    count = 0
    # 遍历所有可能的子字符串
    for i in range(n):
        for j in range(i, n):
            count_1 = prefix[j + 1][0] - prefix[i][0]
            count_0 = prefix[j + 1][1] - prefix[i][1]
            if count_1 >= count_0 ** 2:
                count += 1
    
    return count


Solution = create_solution(count_dominant_substrings)