"""
136. 只出现一次的数字 - 给你一个 非空 整数数组 nums ，除了某个元素只出现一次以外，其余每个元素均出现两次。找出那个只出现了一次的元素。 你必须设计并实现线性时间复杂度的算法来解决此问题，且该算法只使用常量额外空间。 示例 1 ： 输入：nums = [2,2,1] 输出：1 示例 2 ： 输入：nums = [4,1,2,1,2] 输出：4 示例 3 ： 输入：nums = [1] 输出：1 提示： * 1 <= nums.length <= 3 * 104 * -3 * 104 <= nums[i] <= 3 * 104 * 除了某个元素只出现一次以外，其余每个元素均出现两次。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用异或运算，相同数字异或为0，0与任何数异或为原数

算法步骤:
1. 初始化result为0
2. 遍历数组，对每个数字进行异或运算
3. 最终结果就是只出现一次的数字

关键点:
- 异或运算的性质
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组一次
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def single_number(nums: List[int]) -> int:
    """
    函数式接口 - 只出现一次的数字
    
    实现思路:
    使用异或运算，相同数字异或为0，0与任何数异或为原数。
    
    Args:
        nums: 非空整数数组
        
    Returns:
        只出现一次的元素
        
    Example:
        >>> single_number([2,2,1])
        1
    """
    result = 0
    for num in nums:
        result ^= num
    return result


