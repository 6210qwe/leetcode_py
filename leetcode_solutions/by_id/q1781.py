"""
1662. 检查两个字符串数组是否相等 - 给你两个字符串数组 word1 和 word2 。如果两个数组表示的字符串相同，返回 true ；否则，返回 false 。 数组表示的字符串 是由数组中的所有元素 按顺序 连接形成的字符串。 示例 1： 输入：word1 = ["ab", "c"], word2 = ["a", "bc"] 输出：true 解释： word1 表示的字符串为 "ab" + "c" -> "abc" word2 表示的字符串为 "a" + "bc" -> "abc" 两个字符串相同，返回 true 示例 2： 输入：word1 = ["a", "cb"], word2 = ["ab", "c"] 输出：false 示例 3： 输入：word1 = ["abc", "d", "defg"], word2 = ["abcddefg"] 输出：true 提示： * 1 <= word1.length, word2.length <= 103 * 1 <= word1[i].length, word2[i].length <= 103 * 1 <= sum(word1[i].length), sum(word2[i].length) <= 103 * word1[i] 和 word2[i] 由小写字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将两个字符串数组分别拼接成一个完整的字符串，然后比较这两个字符串是否相等。

算法步骤:
1. 将 word1 中的所有字符串拼接成一个完整的字符串。
2. 将 word2 中的所有字符串拼接成一个完整的字符串。
3. 比较这两个字符串是否相等。

关键点:
- 使用 join 方法将字符串数组拼接成一个完整的字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 word1 的总字符数，m 是 word2 的总字符数。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def array_strings_are_equal(word1: List[str], word2: List[str]) -> bool:
    """
    函数式接口 - 检查两个字符串数组是否相等
    """
    # 将 word1 中的所有字符串拼接成一个完整的字符串
    str1 = ''.join(word1)
    
    # 将 word2 中的所有字符串拼接成一个完整的字符串
    str2 = ''.join(word2)
    
    # 比较这两个字符串是否相等
    return str1 == str2


Solution = create_solution(array_strings_are_equal)