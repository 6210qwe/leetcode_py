def employees_earning_more_than_their_managers():
    """
    函数式接口 - 超过经理收入的员工（SQL查询）
    
    实现思路:
    使用自连接查询，比较员工和经理的薪水。
    
    Returns:
        SQL查询语句字符串
        
    Example:
        >>> sql = employees_earning_more_than_their_managers()
        >>> # 执行SQL查询
    """
    sql = """
    SELECT e1.name AS Employee
    FROM Employee e1
    JOIN Employee e2 ON e1.managerId = e2.id
    WHERE e1.salary > e2.salary
    """
    return sql.strip()


