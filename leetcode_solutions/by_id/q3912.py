"""
3602. 十六进制和三十六进制转化 - 给你一个整数 n。 返回 n2 的 十六进制表示 和 n3 的 三十六进制表示 拼接成的字符串。 十六进制 数定义为使用数字 0 – 9 和大写字母 A - F 表示 0 到 15 的值。 三十六进制 数定义为使用数字 0 – 9 和大写字母 A - Z 表示 0 到 35 的值。 示例 1： 输入：n = 13 输出： "A91P1" 解释： * n2 = 13 * 13 = 169。在十六进制中，它转换为 (10 * 16) + 9 = 169，对应于 "A9"。 * n3 = 13 * 13 * 13 = 2197。在三十六进制中，它转换为 (1 * 362) + (25 * 36) + 1 = 2197，对应于 "1P1"。 * 连接两个结果得到 "A9" + "1P1" = "A91P1"。 示例 2： 输入：n = 36 输出："5101000" 解释： * n2 = 36 * 36 = 1296。在十六进制中，它转换为 (5 * 162) + (1 * 16) + 0 = 1296，对应于 "510"。 * n3 = 36 * 36 * 36 = 46656。在三十六进制中，它转换为 (1 * 363) + (0 * 362) + (0 * 36) + 0 = 46656，对应于 "1000"。 * 连接两个结果得到 "510" + "1000" = "5101000"。 提示: * 1 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将给定的整数 n 的平方和立方分别转换为十六进制和三十六进制表示，并将结果拼接成一个字符串。

算法步骤:
1. 计算 n 的平方。
2. 将 n 的平方转换为十六进制字符串。
3. 计算 n 的立方。
4. 将 n 的立方转换为三十六进制字符串。
5. 将十六进制和三十六进制字符串拼接并返回。

关键点:
- 使用内置函数 `hex` 和自定义函数 `to_base_36` 来进行进制转换。
- 确保十六进制字符串去掉前缀 '0x'。
- 三十六进制转换时，使用字符 '0'-'9' 和 'A'-'Z' 来表示 0-35。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log N)，其中 N 是 n 的平方或立方，因为每次除以基数的操作都会减少一位。
空间复杂度: O(log N)，存储转换后的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
