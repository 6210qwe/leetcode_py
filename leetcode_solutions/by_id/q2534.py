"""
核心思想: 使用 SQL 查询来计算每个员工的扣除金额，并返回符合条件的员工记录。

算法步骤:
1. 计算每个员工的总扣除金额。
2. 过滤出总扣除金额大于等于某个阈值的员工记录。
3. 返回结果集。

关键点:
- 使用 SQL 的聚合函数 SUM 来计算总扣除金额。
- 使用 HAVING 子句来过滤出符合条件的员工记录。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是员工记录的数量。需要遍历所有记录来计算总扣除金额。
空间复杂度: O(1)，查询过程中使用的额外空间是常数级别的。
"""

