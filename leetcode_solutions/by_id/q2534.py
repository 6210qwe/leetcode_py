def solution_function_name(params):
    """
    函数式接口 - 实现 SQL 查询来计算员工扣除
    """
    # 实现最优解法
    query = """
    SELECT 
        employee_id, 
        SUM(deduction) AS total_deduction
    FROM 
        EmployeeDeductions
    GROUP BY 
        employee_id
    HAVING 
        SUM(deduction) >= 100;
    """
    return query

