def subtract_product_and_sum(n: int) -> int:
    """
    函数式接口 - 计算整数 n 的各位数字之积和各位数字之和的差。
    """
    prod = 1
    sum_ = 0
    
    while n > 0:
        digit = n % 10
        prod *= digit
        sum_ += digit
        n //= 10
    
    return prod - sum_


Solution = create_solution(subtract_product_and_sum)