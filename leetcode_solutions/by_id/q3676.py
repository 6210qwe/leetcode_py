"""
3370. 仅含置位位的最小整数 - 给你一个正整数 n。 返回 大于等于 n 且二进制表示仅包含 置位 位的 最小 整数 x 。 置位 位指的是二进制表示中值为 1 的位。 示例 1： 输入： n = 5 输出： 7 解释： 7 的二进制表示是 "111"。 示例 2： 输入： n = 10 输出： 15 解释： 15 的二进制表示是 "1111"。 示例 3： 输入： n = 3 输出： 3 解释： 3 的二进制表示是 "11"。 提示： * 1 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到大于等于 n 的最小的 2 的幂次减一。

算法步骤:
1. 计算 n 的二进制长度。
2. 构造一个所有位都为 1 的数，其长度与 n 的二进制长度相同。
3. 如果构造的数小于 n，则增加一位并重复步骤 2，直到找到满足条件的数。

关键点:
- 使用位运算来高效地构造和比较数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_number_with_all_set_bits(n: int) -> int:
    """
    函数式接口 - 找到大于等于 n 且二进制表示仅包含置位位的最小整数
    """
    # 计算 n 的二进制长度
    bit_length = n.bit_length()
    
    # 构造一个所有位都为 1 的数
    all_ones = (1 << bit_length) - 1
    
    # 如果构造的数小于 n，则增加一位
    while all_ones < n:
        bit_length += 1
        all_ones = (1 << bit_length) - 1
    
    return all_ones


