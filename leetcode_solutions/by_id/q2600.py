"""
2517. 礼盒的最大甜蜜度 - 给你一个正整数数组 price ，其中 price[i] 表示第 i 类糖果的价格，另给你一个正整数 k 。 商店组合 k 类 不同 糖果打包成礼盒出售。礼盒的 甜蜜度 是礼盒中任意两种糖果 价格 绝对差的最小值。 返回礼盒的 最大 甜蜜度。 示例 1： 输入：price = [13,5,1,8,21,2], k = 3 输出：8 解释：选出价格分别为 [13,5,21] 的三类糖果。 礼盒的甜蜜度为 min(|13 - 5|, |13 - 21|, |5 - 21|) = min(8, 8, 16) = 8 。 可以证明能够取得的最大甜蜜度就是 8 。 示例 2： 输入：price = [1,3,1], k = 2 输出：2 解释：选出价格分别为 [1,3] 的两类糖果。 礼盒的甜蜜度为 min(|1 - 3|) = min(2) = 2 。 可以证明能够取得的最大甜蜜度就是 2 。 示例 3： 输入：price = [7,7,7,7], k = 2 输出：0 解释：从现有的糖果中任选两类糖果，甜蜜度都会是 0 。 提示： * 2 <= k <= price.length <= 105 * 1 <= price[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来确定最大甜蜜度，并通过贪心算法验证是否可以选出 k 个糖果。

算法步骤:
1. 对价格数组进行排序。
2. 使用二分查找来确定最大甜蜜度的范围。
3. 在每次二分查找的过程中，使用贪心算法验证是否可以选出 k 个糖果，使得任意两个糖果的价格差至少为当前的甜蜜度。

关键点:
- 二分查找的范围是从 0 到 (max(price) - min(price)) // (k - 1)。
- 贪心算法用于验证当前甜蜜度是否可行。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log(max_price_diff))
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_tastiness(price: List[int], k: int) -> int:
    """
    函数式接口 - 计算礼盒的最大甜蜜度
    """
    def can_select(tastiness: int) -> bool:
        count = 1
        last_selected = price[0]
        for p in price[1:]:
            if p - last_selected >= tastiness:
                count += 1
                last_selected = p
                if count == k:
                    return True
        return False

    price.sort()
    left, right = 0, (price[-1] - price[0]) // (k - 1)

    while left < right:
        mid = (left + right + 1) // 2
        if can_select(mid):
            left = mid
        else:
            right = mid - 1

    return left


Solution = create_solution(max_tastiness)