"""
3046. 分割数组 - 给你一个长度为 偶数 的整数数组 nums 。你需要将这个数组分割成 nums1 和 nums2 两部分，要求： * nums1.length == nums2.length == nums.length / 2 。 * nums1 应包含 互不相同 的元素。 * nums2也应包含 互不相同 的元素。 如果能够分割数组就返回 true ，否则返回 false 。 示例 1： 输入：nums = [1,1,2,2,3,4] 输出：true 解释：分割 nums 的可行方案之一是 nums1 = [1,2,3] 和 nums2 = [1,2,4] 。 示例 2： 输入：nums = [1,1,1,1] 输出：false 解释：分割 nums 的唯一可行方案是 nums1 = [1,1] 和 nums2 = [1,1] 。但 nums1 和 nums2 都不是由互不相同的元素构成。因此，返回 false 。 提示： * 1 <= nums.length <= 100 * nums.length % 2 == 0 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个元素的出现次数，如果某个元素出现次数超过2次，则无法满足条件。

算法步骤:
1. 使用哈希表统计每个元素的出现次数。
2. 检查是否有任何元素出现次数超过2次，如果有则返回 False。
3. 否则返回 True。

关键点:
- 使用哈希表统计元素出现次数。
- 检查是否有元素出现次数超过2次。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历整个数组来统计元素出现次数。
空间复杂度: O(n) - 使用哈希表存储元素出现次数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> bool:
    """
    函数式接口 - 判断是否可以将数组分割成两个长度相等且各包含不同元素的部分
    """
    # 使用哈希表统计每个元素的出现次数
    count = {}
    for num in nums:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    # 检查是否有元素出现次数超过2次
    for value in count.values():
        if value > 2:
            return False
    
    return True


Solution = create_solution(solution_function_name)