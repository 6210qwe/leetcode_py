"""
2249. 统计圆内格点数目 - 给你一个二维整数数组 circles ，其中 circles[i] = [xi, yi, ri] 表示网格上圆心为 (xi, yi) 且半径为 ri 的第 i 个圆，返回出现在 至少一个 圆内的 格点数目 。 注意： * 格点 是指整数坐标对应的点。 * 圆周上的点 也被视为出现在圆内的点。 示例 1： [https://assets.leetcode.com/uploads/2022/03/02/exa-11.png] 输入：circles = [[2,2,1]] 输出：5 解释： 给定的圆如上图所示。 出现在圆内的格点为 (1, 2)、(2, 1)、(2, 2)、(2, 3) 和 (3, 2)，在图中用绿色标识。 像 (1, 1) 和 (1, 3) 这样用红色标识的点，并未出现在圆内。 因此，出现在至少一个圆内的格点数目是 5 。 示例 2： [https://assets.leetcode.com/uploads/2022/03/02/exa-22.png] 输入：circles = [[2,2,2],[3,4,1]] 输出：16 解释： 给定的圆如上图所示。 共有 16 个格点出现在至少一个圆内。 其中部分点的坐标是 (0, 2)、(2, 0)、(2, 4)、(3, 2) 和 (4, 4) 。 提示： * 1 <= circles.length <= 200 * circles[i].length == 3 * 1 <= xi, yi <= 100 * 1 <= ri <= min(xi, yi)
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来存储所有在圆内的格点，避免重复计算。

算法步骤:
1. 初始化一个集合 `points` 来存储所有的格点。
2. 遍历每个圆，对于每个圆，遍历其可能包含的所有格点。
3. 对于每个格点，检查它是否在圆内（即距离圆心的距离小于等于半径）。
4. 如果格点在圆内，将其加入集合 `points`。
5. 最后，返回集合 `points` 的大小。

关键点:
- 使用集合来存储格点，避免重复计算。
- 通过遍历圆的边界范围来减少不必要的计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * r^2)，其中 n 是圆的数量，r 是圆的最大半径。
空间复杂度: O(n * r^2)，最坏情况下所有格点都在集合中。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_lattice_points(circles: List[List[int]]) -> int:
    """
    函数式接口 - 统计圆内格点数目
    """
    points = set()
    
    for x, y, r in circles:
        for i in range(x - r, x + r + 1):
            for j in range(y - r, y + r + 1):
                if (i - x) ** 2 + (j - y) ** 2 <= r ** 2:
                    points.add((i, j))
    
    return len(points)


Solution = create_solution(count_lattice_points)