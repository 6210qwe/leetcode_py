"""
1043. 分隔数组以得到最大和 - 给你一个整数数组 arr，请你将该数组分隔为长度 最多 为 k 的一些（连续）子数组。分隔完成后，每个子数组的中的所有值都会变为该子数组中的最大值。 返回将数组分隔变换后能够得到的元素最大和。本题所用到的测试用例会确保答案是一个 32 位整数。 示例 1： 输入：arr = [1,15,7,9,2,5,10], k = 3 输出：84 解释：数组变为 [15,15,15,9,10,10,10] 示例 2： 输入：arr = [1,4,1,5,7,3,6,1,9,9,3], k = 4 输出：83 示例 3： 输入：arr = [1], k = 1 输出：1 提示： * 1 <= arr.length <= 500 * 0 <= arr[i] <= 109 * 1 <= k <= arr.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划

算法步骤:
1. 定义一个数组 dp，其中 dp[i] 表示前 i 个元素的最大和。
2. 初始化 dp[0] = 0。
3. 对于每个位置 i，从 i-1 到 max(0, i-k) 进行遍历，找到当前子数组的最大值，并更新 dp[i]。
4. 最终返回 dp[n]。

关键点:
- 使用动态规划来记录每个位置的最大和。
- 通过滑动窗口来找到当前子数组的最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)，其中 n 是数组的长度，k 是子数组的最大长度。
空间复杂度: O(n)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def maxSumAfterPartitioning(arr: List[int], k: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    n = len(arr)
    dp = [0] * (n + 1)

    for i in range(1, n + 1):
        max_val = 0
        for j in range(1, min(k, i) + 1):
            max_val = max(max_val, arr[i - j])
            dp[i] = max(dp[i], dp[i - j] + max_val * j)

    return dp[n]

