"""
3079. 求出加密整数的和 - 给你一个整数数组 nums ，数组中的元素都是 正 整数。定义一个加密函数 encrypt ，encrypt(x) 将一个整数 x 中 每一个 数位都用 x 中的 最大 数位替换。比方说 encrypt(523) = 555 且 encrypt(213) = 333 。 请你返回数组中所有元素加密后的 和 。 示例 1： 输入：nums = [1,2,3] 输出：6 解释：加密后的元素位 [1,2,3] 。加密元素的和为 1 + 2 + 3 == 6 。 示例 2： 输入：nums = [10,21,31] 输出：66 解释：加密后的元素为 [11,22,33] 。加密元素的和为 11 + 22 + 33 == 66 。 提示： * 1 <= nums.length <= 50 * 1 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对于每个整数，找到其最大数位，并将该整数的所有数位替换为这个最大数位，然后计算所有加密后整数的和。

算法步骤:
1. 遍历数组中的每个整数。
2. 对于每个整数，找到其最大数位。
3. 将该整数的所有数位替换为最大数位。
4. 计算所有加密后整数的和。

关键点:
- 使用字符串操作来找到最大数位并替换。
- 通过遍历数组来处理每个整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * d)，其中 n 是数组的长度，d 是整数的最大位数（在本题中 d 最大为 4）。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 返回数组中所有元素加密后的和
    """
    def encrypt(num: int) -> int:
        # 将整数转换为字符串，找到最大数位
        max_digit = max(str(num))
        # 将所有数位替换为最大数位
        encrypted_num = int(max_digit * len(str(num)))
        return encrypted_num

    # 计算所有加密后整数的和
    return sum(encrypt(num) for num in nums)

