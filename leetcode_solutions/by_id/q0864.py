# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个图像中 1 的坐标，并计算所有可能的平移向量及其重叠数。

算法步骤:
1. 记录 img1 和 img2 中所有 1 的坐标。
2. 计算所有可能的平移向量及其对应的重叠数。
3. 返回最大重叠数。

关键点:
- 使用哈希表记录坐标，提高查找效率。
- 通过平移向量计算重叠数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n^2)
"""

# ============================================================================
