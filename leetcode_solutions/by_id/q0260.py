from typing import List
from leetcode_solutions.utils.solution import create_solution


def single_number_iii(nums: List[int]) -> List[int]:
    """
    函数式接口 - 只出现一次的数字III
    
    实现思路:
    使用位运算，先找到两个只出现一次的数字的异或结果，然后根据最低位的1将数组分成两组。
    
    Args:
        nums: 整数数组
        
    Returns:
        只出现一次的两个元素
        
    Example:
        >>> single_number_iii([1,2,1,3,2,5])
        [3, 5]
    """
    # 所有数字的异或结果
    xor_all = 0
    for num in nums:
        xor_all ^= num
    
    # 找到最低位的1
    diff = xor_all & (-xor_all)
    
    # 根据这一位分组
    result = [0, 0]
    for num in nums:
        if num & diff:
            result[0] ^= num
        else:
            result[1] ^= num
    
    return result


