"""
1233. 删除子文件夹 - 你是一位系统管理员，手里有一份文件夹列表 folder，你的任务是要删除该列表中的所有 子文件夹，并以 任意顺序 返回剩下的文件夹。 如果文件夹 folder[i] 位于另一个文件夹 folder[j] 下，那么 folder[i] 就是 folder[j] 的 子文件夹 。folder[j] 的子文件夹必须以 folder[j] 开头，后跟一个 "/"。例如，"/a/b" 是 "/a" 的一个子文件夹，但 "/b" 不是 "/a/b/c" 的一个子文件夹。 文件夹的「路径」是由一个或多个按以下格式串联形成的字符串：'/' 后跟一个或者多个小写英文字母。 * 例如，"/leetcode" 和 "/leetcode/problems" 都是有效的路径，而空字符串和 "/" 不是。 示例 1： 输入：folder = ["/a","/a/b","/c/d","/c/d/e","/c/f"] 输出：["/a","/c/d","/c/f"] 解释："/a/b" 是 "/a" 的子文件夹，而 "/c/d/e" 是 "/c/d" 的子文件夹。 示例 2： 输入：folder = ["/a","/a/b/c","/a/b/d"] 输出：["/a"] 解释：文件夹 "/a/b/c" 和 "/a/b/d" 都会被删除，因为它们都是 "/a" 的子文件夹。 示例 3： 输入: folder = ["/a/b/c","/a/b/ca","/a/b/d"] 输出: ["/a/b/c","/a/b/ca","/a/b/d"] 提示： * 1 <= folder.length <= 4 * 104 * 2 <= folder[i].length <= 100 * folder[i] 只包含小写字母和 '/' * folder[i] 总是以字符 '/' 起始 * folder 每个元素都是 唯一 的
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用排序和前缀匹配来删除子文件夹

算法步骤:
1. 对文件夹列表进行排序，确保父文件夹在子文件夹之前。
2. 初始化一个结果列表，用于存储非子文件夹。
3. 遍历排序后的文件夹列表，对于每个文件夹：
   - 如果结果列表为空，直接添加当前文件夹到结果列表。
   - 如果当前文件夹不是结果列表中最后一个文件夹的子文件夹，添加当前文件夹到结果列表。

关键点:
- 通过排序和前缀匹配，可以高效地删除子文件夹。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是文件夹列表的长度，排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，除了结果列表外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_subfolders(folder: List[str]) -> List[str]:
    """
    函数式接口 - 删除子文件夹
    """
    # 对文件夹列表进行排序
    folder.sort()
    
    # 初始化结果列表
    result = []
    
    for f in folder:
        # 如果结果列表为空，或者当前文件夹不是结果列表中最后一个文件夹的子文件夹
        if not result or not f.startswith(result[-1] + '/'):
            result.append(f)
    
    return result


Solution = create_solution(remove_subfolders)