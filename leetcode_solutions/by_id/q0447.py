from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def number_of_boomerangs(points: List[List[int]]) -> int:
    """
    函数式接口 - 计算平面上所有回旋镖的数量
    
    实现思路:
    使用哈希表来记录每个点到其他点的距离，然后计算每个距离出现的次数，通过组合计数公式计算回旋镖的数量。
    
    Args:
        points: 平面上的点列表，每个点表示为 [x, y]
        
    Returns:
        返回平面上所有回旋镖的数量
        
    Example:
        >>> number_of_boomerangs([[0,0],[1,0],[2,0]])
        2
    """
    def distance(p1, p2):
        return (p1[0] - p2[0]) ** 2 + (p1[1] - p2[1]) ** 2

    res = 0
    for i in range(len(points)):
        dist_map = {}
        for j in range(len(points)):
            if i != j:
                d = distance(points[i], points[j])
                dist_map[d] = dist_map.get(d, 0) + 1
        for count in dist_map.values():
            res += count * (count - 1)
    return res


