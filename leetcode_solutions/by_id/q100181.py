"""
面试题 05.06. 整数转换 - 整数转换。编写一个函数，确定需要改变几个位才能将整数 A 转成整数 B。 示例 1： 输入：A = 29 （或者 0b11101）, B = 15（或者 0b01111） 输出：2 示例 2： 输入：A = 1，B = 2 输出：2 提示: 1. A，B范围在[-2147483648, 2147483647]之间
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用异或操作找到不同位的数量

算法步骤:
1. 计算 A 和 B 的异或结果
2. 统计异或结果中 1 的数量，即为需要改变的位数

关键点:
- 异或操作可以找出两个数不同的位
- 统计 1 的数量可以使用 Brian Kernighan 算法
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 因为整数的位数是固定的
空间复杂度: O(1) - 只使用了常数级的额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(A: int, B: int) -> int:
    """
    函数式接口 - 计算将整数 A 转换成整数 B 需要改变的位数
    """
    # 计算 A 和 B 的异或结果
    xor_result = A ^ B
    
    # 统计异或结果中 1 的数量
    count = 0
    while xor_result:
        xor_result &= (xor_result - 1)
        count += 1
    
    return count


Solution = create_solution(solution_function_name)