"""
881. 救生艇 - 给定数组 people 。people[i]表示第 i 个人的体重 ，船的数量不限，每艘船可以承载的最大重量为 limit。 每艘船最多可同时载两人，但条件是这些人的重量之和最多为 limit。 返回 承载所有人所需的最小船数 。 示例 1： 输入：people = [1,2], limit = 3 输出：1 解释：1 艘船载 (1, 2) 示例 2： 输入：people = [3,2,2,1], limit = 3 输出：3 解释：3 艘船分别载 (1, 2), (2) 和 (3) 示例 3： 输入：people = [3,5,3,4], limit = 5 输出：4 解释：4 艘船分别载 (3), (3), (4), (5) 提示： * 1 <= people.length <= 5 * 104 * 1 <= people[i] <= limit <= 3 * 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针法，将体重最轻和最重的人配对，尽量减少船的数量。

算法步骤:
1. 对数组 people 进行排序。
2. 初始化两个指针，left 指向数组的起始位置，right 指向数组的末尾。
3. 使用一个循环，直到 left 超过 right：
   - 如果 people[left] + people[right] <= limit，说明这两个人可以一起上船，left 向右移动一位，right 向左移动一位。
   - 否则，只有 right 指向的人单独上船，right 向左移动一位。
   - 每次循环结束时，船的数量加一。
4. 返回船的数量。

关键点:
- 通过排序和双指针法，确保每次尽可能多地利用船的载重能力。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 people 的长度。排序操作的时间复杂度为 O(n log n)，而后续的遍历操作为 O(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def num_rescue_boats(people: List[int], limit: int) -> int:
    """
    函数式接口 - 计算最少需要多少艘船来救所有的人
    """
    # 对数组进行排序
    people.sort()
    
    # 初始化双指针
    left, right = 0, len(people) - 1
    boats = 0
    
    # 使用双指针法
    while left <= right:
        if people[left] + people[right] <= limit:
            left += 1
        right -= 1
        boats += 1
    
    return boats


Solution = create_solution(num_rescue_boats)