"""
LCR 093. 最长的斐波那契子序列的长度 - 如果序列 X_1, X_2, ..., X_n 满足下列条件，就说它是 斐波那契式 的： * n >= 3 * 对于所有 i + 2 <= n，都有 X_i + X_{i+1} = X_{i+2} 给定一个严格递增的正整数数组形成序列 arr ，找到 arr 中最长的斐波那契式的子序列的长度。如果一个不存在，返回 0 。 （回想一下，子序列是从原序列 arr 中派生出来的，它从 arr 中删掉任意数量的元素（也可以不删），而不改变其余元素的顺序。例如， [3, 5, 8] 是 [3, 4, 5, 6, 7, 8] 的一个子序列） 示例 1： 输入: arr = [1,2,3,4,5,6,7,8] 输出: 5 解释: 最长的斐波那契式子序列为 [1,2,3,5,8] 。 示例 2： 输入: arr = [1,3,7,11,12,14,18] 输出: 3 解释: 最长的斐波那契式子序列有 [1,11,12]、[3,11,14] 以及 [7,11,18] 。 提示： * 3 <= arr.length <= 1000 * 1 <= arr[i] < arr[i + 1] <= 10^9 注意：本题与主站 873 题相同： https://leetcode.cn/problems/length-of-longest-fibonacci-subsequence/ [https://leetcode.cn/problems/length-of-longest-fibonacci-subsequence/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和哈希表来记录每个可能的斐波那契子序列的长度。

算法步骤:
1. 初始化一个字典 `dp`，其中 `dp[(a, b)]` 表示以 `a` 和 `b` 结尾的斐波那契子序列的长度。
2. 遍历数组中的每一对元素 `(a, b)`，并检查是否存在 `c` 使得 `a + b = c`。
3. 如果存在 `c`，则更新 `dp[(b, c)]` 为 `dp[(a, b)] + 1`。
4. 记录最长的斐波那契子序列的长度。

关键点:
- 使用哈希表来快速查找 `c`。
- 动态规划状态转移方程为 `dp[(b, c)] = dp[(a, b)] + 1`。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是数组的长度。需要遍历每一对元素。
空间复杂度: O(n^2)，用于存储动态规划的状态。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    if len(arr) < 3:
        return 0

    dp = {}
    max_length = 0

    for k in range(2, len(arr)):
        for j in range(k):
            a = arr[k] - arr[j]
            if a < arr[j] and a in arr[:j]:
                dp[(arr[j], arr[k])] = dp.get((a, arr[j]), 2) + 1
                max_length = max(max_length, dp[(arr[j], arr[k])])

    return max_length if max_length >= 3 else 0


Solution = create_solution(solution_function_name)