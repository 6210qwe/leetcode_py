"""
1617. 统计子树中城市之间最大距离 - 给你 n 个城市，编号为从 1 到 n 。同时给你一个大小为 n-1 的数组 edges ，其中 edges[i] = [ui, vi] 表示城市 ui 和 vi 之间有一条双向边。题目保证任意城市之间只有唯一的一条路径。换句话说，所有城市形成了一棵 树 。 一棵 子树 是城市的一个子集，且子集中任意城市之间可以通过子集中的其他城市和边到达。两个子树被认为不一样的条件是至少有一个城市在其中一棵子树中存在，但在另一棵子树中不存在。 对于 d 从 1 到 n-1 ，请你找到城市间 最大距离 恰好为 d 的所有子树数目。 请你返回一个大小为 n-1 的数组，其中第 d 个元素（下标从 1 开始）是城市间 最大距离 恰好等于 d 的子树数目。 请注意，两个城市间距离定义为它们之间需要经过的边的数目。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/10/11/p1.png] 输入：n = 4, edges = [[1,2],[2,3],[2,4]] 输出：[3,4,0] 解释： 子树 {1,2}, {2,3} 和 {2,4} 最大距离都是 1 。 子树 {1,2,3}, {1,2,4}, {2,3,4} 和 {1,2,3,4} 最大距离都为 2 。 不存在城市间最大距离为 3 的子树。 示例 2： 输入：n = 2, edges = [[1,2]] 输出：[1] 示例 3： 输入：n = 3, edges = [[1,2],[2,3]] 输出：[2,1] 提示： * 2 <= n <= 15 * edges.length == n-1 * edges[i].length == 2 * 1 <= ui, vi <= n * 题目保证 (ui, vi) 所表示的边互不相同。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用状态压缩动态规划来解决这个问题。我们使用一个整数来表示子树的状态，每一位表示一个城市是否在子树中。对于每个状态，计算其最大距离，并统计不同最大距离的子树数量。

算法步骤:
1. 构建邻接表表示图。
2. 使用深度优先搜索（DFS）来计算每个子树的最大距离。
3. 使用动态规划来遍历所有可能的子树状态，并计算其最大距离。
4. 统计不同最大距离的子树数量。

关键点:
- 使用状态压缩来表示子树。
- 使用DFS来计算子树的最大距离。
- 使用动态规划来遍历所有可能的子树状态。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n * n^2)，其中 n 是城市的数量。我们需要遍历所有可能的子树状态（2^n），并且对于每个状态，我们需要计算其最大距离（O(n^2)）。
空间复杂度: O(2^n * n)，用于存储每个子树状态的最大距离。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def count_subtrees_with_max_distance(n: int, edges: List[List[int]]) -> List[int]:
    # 构建邻接表
    graph = [[] for _ in range(n)]
    for u, v in edges:
        graph[u - 1].append(v - 1)
        graph[v - 1].append(u - 1)

    # 计算子树的最大距离
    def dfs(node: int, parent: int, state: int) -> int:
        max_dist = 0
        for neighbor in graph[node]:
            if neighbor != parent and (state & (1 << neighbor)):
                max_dist = max(max_dist, 1 + dfs(neighbor, node, state))
        return max_dist

    # 动态规划计算每个子树状态的最大距离
    dp = [-1] * (1 << n)
    for state in range(1, 1 << n):
        for i in range(n):
            if state & (1 << i):
                dp[state] = max(dp[state], dfs(i, -1, state))

    # 统计不同最大距离的子树数量
    result = [0] * (n - 1)
    for state in range(1, 1 << n):
        if dp[state] > 0:
            result[dp[state] - 1] += 1

    return result

