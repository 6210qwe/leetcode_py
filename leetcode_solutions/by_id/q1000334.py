"""
LCR 070. 有序数组中的单一元素 - 给定一个只包含整数的有序数组 nums ，每个元素都会出现两次，唯有一个数只会出现一次，请找出这个唯一的数字。 示例 1： 输入: nums = [1,1,2,3,3,4,4,8,8] 输出: 2 示例 2： 输入: nums = [3,3,7,7,10,11,11] 输出: 10 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 105 进阶：采用的方案可以在 O(log n) 时间复杂度和 O(1) 空间复杂度中运行吗？ 注意：本题与主站 540 题相同：https://leetcode.cn/problems/single-element-in-a-sorted-array/ [https://leetcode.cn/problems/single-element-in-a-sorted-array/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到唯一出现一次的元素。

算法步骤:
1. 初始化左右指针 left 和 right。
2. 在 while 循环中，计算中间位置 mid。
3. 检查 mid 是否是唯一元素：
   - 如果 mid 是偶数且 nums[mid] == nums[mid + 1]，则唯一元素在右半部分。
   - 如果 mid 是奇数且 nums[mid] == nums[mid - 1]，则唯一元素在右半部分。
   - 否则，唯一元素在左半部分。
4. 更新左右指针，继续二分查找，直到找到唯一元素。

关键点:
- 利用二分查找的对数时间复杂度来缩小搜索范围。
- 通过检查 mid 及其相邻元素来确定唯一元素的位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_single_element(nums: List[int]) -> int:
    """
    函数式接口 - 使用二分查找找到有序数组中的唯一元素
    """
    left, right = 0, len(nums) - 1
    
    while left < right:
        mid = (left + right) // 2
        
        # 确保 mid 是偶数
        if mid % 2 == 1:
            mid -= 1
        
        # 检查 mid 及其相邻元素
        if nums[mid] == nums[mid + 1]:
            left = mid + 2
        else:
            right = mid
    
    return nums[left]


Solution = create_solution(find_single_element)