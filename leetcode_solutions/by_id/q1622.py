"""
1499. 满足不等式的最大值 - 给你一个数组 points 和一个整数 k 。数组中每个元素都表示二维平面上的点的坐标，并按照横坐标 x 的值从小到大排序。也就是说 points[i] = [xi, yi] ，并且在 1 <= i < j <= points.length 的前提下， xi < xj 总成立。 请你找出 yi + yj + |xi - xj| 的 最大值，其中 |xi - xj| <= k 且 1 <= i < j <= points.length。 题目测试数据保证至少存在一对能够满足 |xi - xj| <= k 的点。 示例 1： 输入：points = [[1,3],[2,0],[5,10],[6,-10]], k = 1 输出：4 解释：前两个点满足 |xi - xj| <= 1 ，代入方程计算，则得到值 3 + 0 + |1 - 2| = 4 。第三个和第四个点也满足条件，得到值 10 + -10 + |5 - 6| = 1 。 没有其他满足条件的点，所以返回 4 和 1 中最大的那个。 示例 2： 输入：points = [[0,0],[3,0],[9,2]], k = 3 输出：3 解释：只有前两个点满足 |xi - xj| <= 3 ，代入方程后得到值 0 + 0 + |0 - 3| = 3 。 提示： * 2 <= points.length <= 105 * points[i].length == 2 * -108 <= xi, yi <= 108 * 0 <= k <= 2 * 108 * 对于所有的 1 <= i < j <= points.length，均有 xi < xj。 * xi 构成一个严格递增序列。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双端队列来维护一个滑动窗口，确保队列中的点满足 |xi - xj| <= k 的条件，并且队列中的点按 (yi - xi) 降序排列。

算法步骤:
1. 初始化一个双端队列 `deque` 和结果变量 `max_value`。
2. 遍历每个点 `(x, y)`：
   - 移除队列中所有不符合 |xi - xj| <= k 条件的点。
   - 计算当前点与队列头部点的值 `y + x + (deque[0][1] - deque[0][0])`，更新 `max_value`。
   - 将当前点 `(x, y - x)` 加入队列，同时移除队列尾部所有比当前点小的点，以保持队列按 (yi - xi) 降序排列。
3. 返回 `max_value`。

关键点:
- 使用双端队列来维护滑动窗口，确保队列中的点满足 |xi - xj| <= k 的条件。
- 通过移除队列尾部比当前点小的点，保持队列按 (yi - xi) 降序排列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 points 的长度。每个点最多被加入和移除队列一次。
空间复杂度: O(k)，队列中最多存储 k 个点。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import deque

def findMaxValueOfEquation(points: List[List[int]], k: int) -> int:
    """
    函数式接口 - 找出 yi + yj + |xi - xj| 的最大值，其中 |xi - xj| <= k 且 1 <= i < j <= points.length
    """
    deque = deque()
    max_value = float('-inf')
    
    for x, y in points:
        # 移除队列中所有不符合 |xi - xj| <= k 条件的点
        while deque and x - deque[0][0] > k:
            deque.popleft()
        
        # 计算当前点与队列头部点的值
        if deque:
            max_value = max(max_value, y + x + (deque[0][1] - deque[0][0]))
        
        # 将当前点 (x, y - x) 加入队列，同时移除队列尾部所有比当前点小的点
        while deque and y - x >= deque[-1][1]:
            deque.pop()
        deque.append((x, y - x))
    
    return max_value

