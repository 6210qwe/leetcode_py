"""
2597. 美丽子集的数目 - 给你一个由正整数组成的数组 nums 和一个 正 整数 k 。 如果 nums 的子集中，任意两个整数的绝对差均不等于 k ，则认为该子数组是一个 美丽 子集。 返回数组 nums 中 非空 且 美丽 的子集数目。 nums 的子集定义为：可以经由 nums 删除某些元素（也可能不删除）得到的一个数组。只有在删除元素时选择的索引不同的情况下，两个子集才会被视作是不同的子集。 示例 1： 输入：nums = [2,4,6], k = 2 输出：4 解释：数组 nums 中的美丽子集有：[2], [4], [6], [2, 6] 。 可以证明数组 [2,4,6] 中只存在 4 个美丽子集。 示例 2： 输入：nums = [1], k = 1 输出：1 解释：数组 nums 中的美丽数组有：[1] 。 可以证明数组 [1] 中只存在 1 个美丽子集。 提示： * 1 <= nums.length <= 18 * 1 <= nums[i], k <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用回溯法生成所有可能的子集，并检查每个子集是否满足条件。

算法步骤:
1. 对数组进行排序，以便于后续处理。
2. 使用回溯法生成所有可能的子集。
3. 在生成子集的过程中，检查当前子集是否满足美丽子集的条件。
4. 如果满足条件，则计数器加一。

关键点:
- 使用字典记录已经选择的元素，避免重复计算。
- 在回溯过程中，通过剪枝减少不必要的计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n)，其中 n 是数组的长度。最坏情况下需要生成所有子集。
空间复杂度: O(n)，递归调用栈的深度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_beautiful_subsets(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算美丽的子集数目
    """
    nums.sort()
    count = 0
    chosen = {}

    def backtrack(start: int):
        nonlocal count
        for i in range(start, len(nums)):
            if nums[i] - k in chosen:
                continue
            chosen[nums[i]] = 1
            count += 1
            backtrack(i + 1)
            del chosen[nums[i]]

    backtrack(0)
    return count


Solution = create_solution(count_beautiful_subsets)