"""
2740. 找出分区值 - 给你一个 正 整数数组 nums 。 将 nums 分成两个数组：nums1 和 nums2 ，并满足下述条件：
* 数组 nums 中的每个元素都属于数组 nums1 或数组 nums2 。
* 两个数组都 非空 。
* 分区值 最小 。
分区值的计算方法是 |max(nums1) - min(nums2)| 。
其中，max(nums1) 表示数组 nums1 中的最大元素，min(nums2) 表示数组 nums2 中的最小元素。
返回表示分区值的整数。

示例 1：
输入：nums = [1,3,2,4]
输出：1
解释：可以将数组 nums 分成 nums1 = [1,2] 和 nums2 = [3,4] 。
- 数组 nums1 的最大值等于 2 。
- 数组 nums2 的最小值等于 3 。
分区值等于 |2 - 3| = 1 。
可以证明 1 是所有分区方案的最小值。

示例 2：
输入：nums = [100,1,10]
输出：9
解释：可以将数组 nums 分成 nums1 = [10] 和 nums2 = [100,1] 。
- 数组 nums1 的最大值等于 10 。
- 数组 nums2 的最小值等于 1 。
分区值等于 |10 - 1| = 9 。
可以证明 9 是所有分区方案的最小值。

提示：
* 2 <= nums.length <= 105
* 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对数组进行排序，然后找到相邻元素之间的最小差值。这个最小差值即为最小的分区值。

算法步骤:
1. 对数组进行排序。
2. 遍历排序后的数组，找到相邻元素之间的最小差值。

关键点:
- 排序后，相邻元素之间的差值最小化了分区值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找到数组的最小分区值
    """
    # 对数组进行排序
    nums.sort()
    
    # 初始化最小分区值
    min_partition_value = float('inf')
    
    # 遍历排序后的数组，找到相邻元素之间的最小差值
    for i in range(1, len(nums)):
        min_partition_value = min(min_partition_value, nums[i] - nums[i - 1])
    
    return min_partition_value


Solution = create_solution(solution_function_name)