"""
1385. 两个数组间的距离值 - 给你两个整数数组 arr1 ， arr2 和一个整数 d ，请你返回两个数组之间的 距离值 。 「距离值」 定义为符合此距离要求的元素数目：对于元素 arr1[i] ，不存在任何元素 arr2[j] 满足 |arr1[i]-arr2[j]| <= d 。 示例 1： 输入：arr1 = [4,5,8], arr2 = [10,9,1,8], d = 2 输出：2 解释： 对于 arr1[0]=4 我们有： |4-10|=6 > d=2 |4-9|=5 > d=2 |4-1|=3 > d=2 |4-8|=4 > d=2 所以 arr1[0]=4 符合距离要求 对于 arr1[1]=5 我们有： |5-10|=5 > d=2 |5-9|=4 > d=2 |5-1|=4 > d=2 |5-8|=3 > d=2 所以 arr1[1]=5 也符合距离要求 对于 arr1[2]=8 我们有： |8-10|=2 <= d=2 |8-9|=1 <= d=2 |8-1|=7 > d=2 |8-8|=0 <= d=2 存在距离小于等于 2 的情况，不符合距离要求 故而只有 arr1[0]=4 和 arr1[1]=5 两个符合距离要求，距离值为 2 示例 2： 输入：arr1 = [1,4,2,3], arr2 = [-4,-3,6,10,20,30], d = 3 输出：2 示例 3： 输入：arr1 = [2,1,100,3], arr2 = [-5,-2,10,-3,7], d = 6 输出：1 提示： * 1 <= arr1.length, arr2.length <= 500 * -10^3 <= arr1[i], arr2[j] <= 10^3 * 0 <= d <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来优化查找过程，减少时间复杂度。

算法步骤:
1. 对 arr2 进行排序。
2. 遍历 arr1 中的每个元素，使用二分查找在 arr2 中查找是否存在满足 |arr1[i] - arr2[j]| <= d 的元素。
3. 如果没有找到这样的元素，则计数器加一。

关键点:
- 使用二分查找来优化查找过程，减少时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log m)，其中 n 是 arr1 的长度，m 是 arr2 的长度。排序操作的时间复杂度是 O(m log m)，二分查找的时间复杂度是 O(log m)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_distance_value(arr1: List[int], arr2: List[int], d: int) -> int:
    """
    函数式接口 - 计算两个数组之间的距离值
    """
    def is_valid(num: int) -> bool:
        left, right = 0, len(arr2) - 1
        while left <= right:
            mid = (left + right) // 2
            if abs(arr2[mid] - num) <= d:
                return False
            elif arr2[mid] < num:
                left = mid + 1
            else:
                right = mid - 1
        return True

    arr2.sort()
    count = sum(is_valid(num) for num in arr1)
    return count


Solution = create_solution(find_distance_value)