"""
542. 01 矩阵 - 给定一个由 0 和 1 组成的矩阵 mat ，请输出一个大小相同的矩阵，其中每一个格子是 mat 中对应位置元素到最近的 0 的距离。 两个相邻元素间的距离为 1 。 示例 1： [https://pic.leetcode.cn/1626667201-NCWmuP-image.png] 输入：mat = [[0,0,0],[0,1,0],[0,0,0]] 输出：[[0,0,0],[0,1,0],[0,0,0]] 示例 2： [https://pic.leetcode.cn/1626667205-xFxIeK-image.png] 输入：mat = [[0,0,0],[0,1,0],[1,1,1]] 输出：[[0,0,0],[0,1,0],[1,2,1]] 提示： * m == mat.length * n == mat[i].length * 1 <= m, n <= 104 * 1 <= m * n <= 104 * mat[i][j] is either 0 or 1. * mat 中至少有一个 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用广度优先搜索（BFS）从所有 0 出发，逐步扩展到 1，并记录每个 1 到最近 0 的距离。

算法步骤:
1. 初始化一个队列，将所有 0 的位置加入队列。
2. 初始化一个距离矩阵 `dist`，初始值为 -1，表示未访问过。
3. 对于队列中的每个 0，将其在 `dist` 中的距离设为 0。
4. 进行 BFS，每次从队列中取出一个位置，检查其四个方向的邻居：
   - 如果邻居未访问过且为 1，则将其距离设为当前距离 + 1，并将其加入队列。
5. 最终 `dist` 矩阵即为结果。

关键点:
- 使用 BFS 可以确保找到最短路径。
- 从所有 0 同时开始扩展，可以减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是矩阵的行数和列数。每个元素最多被访问一次。
空间复杂度: O(m * n)，用于存储队列和距离矩阵。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def update_matrix(mat: List[List[int]]) -> List[List[int]]:
    """
    函数式接口 - 更新矩阵，使得每个 1 到最近 0 的距离。
    """
    if not mat or not mat[0]:
        return mat

    m, n = len(mat), len(mat[0])
    dist = [[-1] * n for _ in range(m)]
    queue = []

    # 将所有 0 的位置加入队列，并初始化距离矩阵
    for i in range(m):
        for j in range(n):
            if mat[i][j] == 0:
                dist[i][j] = 0
                queue.append((i, j))

    # 定义四个方向
    directions = [(0, 1), (1, 0), (0, -1), (-1, 0)]

    # 进行 BFS
    while queue:
        x, y = queue.pop(0)
        for dx, dy in directions:
            nx, ny = x + dx, y + dy
            if 0 <= nx < m and 0 <= ny < n and dist[nx][ny] == -1:
                dist[nx][ny] = dist[x][y] + 1
                queue.append((nx, ny))

    return dist


Solution = create_solution(update_matrix)