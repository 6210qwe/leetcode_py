"""
2928. 给小朋友们分糖果 I - 给你两个正整数 n 和 limit 。 请你将 n 颗糖果分给 3 位小朋友，确保没有任何小朋友得到超过 limit 颗糖果，请你返回满足此条件下的 总方案数 。 示例 1： 输入：n = 5, limit = 2 输出：3 解释：总共有 3 种方法分配 5 颗糖果，且每位小朋友的糖果数不超过 2 ：(1, 2, 2) ，(2, 1, 2) 和 (2, 2, 1) 。 示例 2： 输入：n = 3, limit = 3 输出：10 解释：总共有 10 种方法分配 3 颗糖果，且每位小朋友的糖果数不超过 3 ：(0, 0, 3) ，(0, 1, 2) ，(0, 2, 1) ，(0, 3, 0) ，(1, 0, 2) ，(1, 1, 1) ，(1, 2, 0) ，(2, 0, 1) ，(2, 1, 0) 和 (3, 0, 0) 。 提示： * 1 <= n <= 50 * 1 <= limit <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用三重循环枚举所有可能的分配方案，并检查每个方案是否满足条件。

算法步骤:
1. 初始化计数器 count 为 0。
2. 使用三重循环枚举三个小朋友的糖果数 i, j, k。
3. 检查 i, j, k 是否满足条件：i + j + k == n 且 i, j, k 均不超过 limit。
4. 如果满足条件，计数器 count 加 1。
5. 返回计数器 count 的值。

关键点:
- 通过三重循环枚举所有可能的分配方案。
- 每个小朋友的糖果数不能超过 limit。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(limit^3)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def distribute_candies(n: int, limit: int) -> int:
    """
    函数式接口 - 分配糖果
    """
    count = 0
    for i in range(min(n, limit) + 1):
        for j in range(min(n - i, limit) + 1):
            k = n - i - j
            if 0 <= k <= limit:
                count += 1
    return count


Solution = create_solution(distribute_candies)