"""
2294. 划分数组使最大差为 K - 给你一个整数数组 nums 和一个整数 k 。你可以将 nums 划分成一个或多个 子序列 ，使 nums 中的每个元素都 恰好 出现在一个子序列中。 在满足每个子序列中最大值和最小值之间的差值最多为 k 的前提下，返回需要划分的 最少 子序列数目。 子序列 本质是一个序列，可以通过删除另一个序列中的某些元素（或者不删除）但不改变剩下元素的顺序得到。 示例 1： 输入：nums = [3,6,1,2,5], k = 2 输出：2 解释： 可以将 nums 划分为两个子序列 [3,1,2] 和 [6,5] 。 第一个子序列中最大值和最小值的差值是 3 - 1 = 2 。 第二个子序列中最大值和最小值的差值是 6 - 5 = 1 。 由于创建了两个子序列，返回 2 。可以证明需要划分的最少子序列数目就是 2 。 示例 2： 输入：nums = [1,2,3], k = 1 输出：2 解释： 可以将 nums 划分为两个子序列 [1,2] 和 [3] 。 第一个子序列中最大值和最小值的差值是 2 - 1 = 1 。 第二个子序列中最大值和最小值的差值是 3 - 3 = 0 。 由于创建了两个子序列，返回 2 。注意，另一种最优解法是将 nums 划分成子序列 [1] 和 [2,3] 。 示例 3： 输入：nums = [2,2,4,5], k = 0 输出：3 解释： 可以将 nums 划分为三个子序列 [2,2]、[4] 和 [5] 。 第一个子序列中最大值和最小值的差值是 2 - 2 = 0 。 第二个子序列中最大值和最小值的差值是 4 - 4 = 0 。 第三个子序列中最大值和最小值的差值是 5 - 5 = 0 。 由于创建了三个子序列，返回 3 。可以证明需要划分的最少子序列数目就是 3 。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 105 * 0 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 先对数组进行排序，然后遍历数组，使用贪心算法尽可能地将连续的元素放入同一个子序列中，直到无法再添加新的元素为止。

算法步骤:
1. 对数组进行排序。
2. 初始化一个计数器 `count` 用于记录子序列的数量，并初始化一个变量 `current_min` 用于记录当前子序列的最小值。
3. 遍历排序后的数组：
   - 如果当前元素与 `current_min` 的差值大于 `k`，则开始一个新的子序列，并更新 `current_min` 为当前元素。
   - 否则，继续将当前元素加入当前子序列。
4. 返回 `count` 作为结果。

关键点:
- 通过排序确保每次选择的元素都是当前子序列中最小的。
- 使用贪心算法尽可能地将连续的元素放入同一个子序列中。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度，因为排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def partition_array(nums: List[int], k: int) -> int:
    """
    函数式接口 - 划分数组使最大差为 K
    """
    # 对数组进行排序
    nums.sort()
    
    count = 1  # 记录子序列的数量
    current_min = nums[0]  # 当前子序列的最小值
    
    for num in nums:
        # 如果当前元素与 current_min 的差值大于 k，则开始一个新的子序列
        if num - current_min > k:
            count += 1
            current_min = num  # 更新 current_min 为当前元素
    
    return count


Solution = create_solution(partition_array)