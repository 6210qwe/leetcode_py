def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 实现带限制的子序列和
    """
    n = len(nums)
    dp = nums[:]
    deque = []

    for i in range(n):
        # 移除超出窗口范围的元素
        while deque and deque[0] < i - k:
            deque.pop(0)

        # 计算当前元素的最大值
        if deque:
            dp[i] += dp[deque[0]]

        # 更新结果
        result = max(result, dp[i])

        # 维护单调队列
        while deque and dp[deque[-1]] < dp[i]:
            deque.pop()
        deque.append(i)

    return result


Solution = create_solution(solution_function_name)