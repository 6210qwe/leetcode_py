"""
1425. 带限制的子序列和 - 给你一个整数数组 nums 和一个整数 k ，请你返回 非空 子序列元素和的最大值，子序列需要满足：子序列中每两个 相邻 的整数 nums[i] 和 nums[j] ，它们在原数组中的下标 i 和 j 满足 i < j 且 j - i <= k 。 数组的子序列定义为：将数组中的若干个数字删除（可以删除 0 个数字），剩下的数字按照原本的顺序排布。 示例 1： 输入：nums = [10,2,-10,5,20], k = 2 输出：37 解释：子序列为 [10, 2, 5, 20] 。 示例 2： 输入：nums = [-1,-2,-3], k = 1 输出：-1 解释：子序列必须是非空的，所以我们选择最大的数字。 示例 3： 输入：nums = [10,-2,-10,-5,20], k = 2 输出：23 解释：子序列为 [10, -2, -5, 20] 。 提示： * 1 <= k <= nums.length <= 10^5 * -10^4 <= nums[i] <= 10^4
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调队列来维护一个长度为 k 的滑动窗口，以实现 O(n) 时间复杂度。

算法步骤:
1. 初始化一个单调队列，用于存储当前窗口内的最大值。
2. 遍历数组，对于每个元素，计算其与前 k 个元素中的最大值之和，并更新结果。
3. 更新单调队列，确保队列中的元素是递减的，并且移除超出窗口范围的元素。

关键点:
- 使用单调队列来维护滑动窗口内的最大值。
- 动态规划的思想，利用前 k 个元素的最大值来更新当前元素的值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(k)
"""

# ============================================================================
