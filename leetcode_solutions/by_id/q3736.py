# ============================================================================
# 问题描述
# ============================================================================
"""
3438. 找到字符串中合法的相邻数字 - 给你一个只包含数字的字符串 s 。如果 s 中两个 相邻 的数字满足以下条件，我们称它们是 合法的 ： * 前面的数字 不等于 第二个数字。 * 两个数字在 s 中出现的次数 恰好 分别等于这个数字本身。 请你从左到右遍历字符串 s ，并返回最先找到的 合法 相邻数字。如果这样的相邻数字不存在，请你返回一个空字符串。 示例 1： 输入：s = "2523533" 输出："23" 解释： 数字 '2' 出现 2 次，数字 '3' 出现 3 次。"23" 中每个数字在 s 中出现的次数都恰好分别等于数字本身。所以输出 "23" 。 示例 2： 输入：s = "221" 输出："21" 解释： 数字 '2' 出现 2 次，数字 '1' 出现 1 次。所以输出 "21" 。 示例 3： 输入：s = "22" 输出："" 解释： 没有合法的相邻数字。 提示： * 2 <= s.length <= 100 * s 只包含 '1' 到 '9' 的数字。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个数字的出现次数，然后遍历字符串找到第一个合法的相邻数字对。

算法步骤:
1. 统计字符串中每个数字的出现次数。
2. 遍历字符串，检查每对相邻的数字是否满足条件：
   - 前面的数字不等于后面的数字。
   - 两个数字的出现次数分别等于它们本身。
3. 如果找到符合条件的相邻数字对，返回该对；否则返回空字符串。

关键点:
- 使用哈希表进行频率统计，时间复杂度为 O(n)。
- 一次遍历字符串即可找到合法的相邻数字对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历字符串两次，一次用于统计频率，一次用于查找合法的相邻数字对。
空间复杂度: O(1)，因为哈希表的大小最多为 9（'1' 到 '9'）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> str:
    """
    函数式接口 - 找到字符串中合法的相邻数字
    """
    # 统计每个数字的出现次数
    count = [0] * 10
    for char in s:
        count[int(char)] += 1
    
    # 遍历字符串，查找合法的相邻数字对
    for i in range(len(s) - 1):
        a, b = int(s[i]), int(s[i + 1])
        if a != b and count[a] == a and count[b] == b:
            return f"{a}{b}"
    
    return ""


Solution = create_solution(solution_function_name)