"""
LCP 78. 城墙防线 - 在探险营地间，小扣意外发现了一片城墙遗迹，在探索期间，却不巧遇到迁徙中的兽群向他迎面冲来。情急之下小扣吹响了他的苍蓝笛，随着笛声响起，遗迹中的城墙逐渐发生了横向膨胀。 已知 `rampart[i] = [x,y]` 表示第 `i` 段城墙的初始所在区间。当城墙发生膨胀时，将遵循以下规则： - 所有的城墙会同时膨胀相等的长度； - 每个城墙可以向左、向右或向两个方向膨胀。 小扣为了确保自身的安全，需要在所有城墙均无重叠的情况下，让城墙尽可能的膨胀。请返回城墙可以膨胀的 **最大值** 。 **注意：** - 初始情况下，所有城墙均不重叠，且 `rampart` 中的元素升序排列； - 两侧的城墙可以向外无限膨胀。 **示例 1：** >输入：`rampart = [[0,3],[4,5],[7,9]]` > >输出：`3` > >解释：如下图所示： >`rampart[0]` 向左侧膨胀 3 个单位； >`rampart[2]` 向右侧膨胀 3 个单位； >`rampart[1]` 向左侧膨胀 1 个单位，向右膨胀 2 个单位。 >不存在膨胀更多的方案，返回 3。 ![image.png](https://pic.leetcode.cn/1681717918-tWywrp-image.png){:width=750px} **示例 2：** >输入：`rampart = [[1,2],[5,8],[11,15],[18,25]]` > >输出：`4` **提示：** - `3 <= rampart.length <= 10^4` - `rampart[i].length == 2` - `0 <= rampart[i][0] < rampart[i][1] <= rampart[i+1][0] <= 10^8`
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来确定城墙的最大膨胀值。

算法步骤:
1. 初始化二分查找的左右边界。
2. 在每次迭代中，计算中间值 `mid`，并检查是否可以以 `mid` 为膨胀值使所有城墙无重叠。
3. 如果可以，则更新左边界，否则更新右边界。
4. 最终返回左边界作为最大膨胀值。

关键点:
- 使用二分查找来优化查找过程。
- 检查是否可以以某个膨胀值使所有城墙无重叠。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log(max_diff))
空间复杂度: O(1)
其中 n 是城墙的数量，max_diff 是城墙之间的最大间隔。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def can_expand(rampart: List[List[int]], expansion: int) -> bool:
    """
    检查是否可以以某个膨胀值使所有城墙无重叠。
    """
    n = len(rampart)
    for i in range(1, n - 1):
        left_expansion = min(expansion, rampart[i][0] - rampart[i - 1][1])
        right_expansion = min(expansion - left_expansion, rampart[i + 1][0] - rampart[i][1])
        if left_expansion + right_expansion < expansion:
            return False
    return True

def solution_function_name(rampart: List[List[int]]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    n = len(rampart)
    max_diff = max(rampart[i + 1][0] - rampart[i][1] for i in range(n - 1))
    left, right = 0, max_diff
    
    while left < right:
        mid = (left + right + 1) // 2
        if can_expand(rampart, mid):
            left = mid
        else:
            right = mid - 1
    
    return left

