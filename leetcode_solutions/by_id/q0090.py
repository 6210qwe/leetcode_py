"""
90. 子集 II - 给你一个整数数组 nums ，其中可能包含重复元素，请你返回该数组所有可能的 子集（幂集）。 解集 不能 包含重复的子集。返回的解集中，子集可以按 任意顺序 排列。 示例 1： 输入：nums = [1,2,2] 输出：[[],[1],[1,2],[1,2,2],[2],[2,2]] 示例 2： 输入：nums = [0] 输出：[[],[0]] 提示： * 1 <= nums.length <= 10 * -10 <= nums[i] <= 10
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 回溯算法 + 去重优化，排序后通过跳过相同元素避免重复子集

算法步骤:
1. 对数组排序，便于去重
2. 回溯函数backtrack(start, path)：
   - 将当前path加入结果
   - 对于i从start到n-1：
     * 去重：如果i > start且nums[i] == nums[i-1]，跳过
     * 选择nums[i]，加入path
     * 递归backtrack(i+1, path)
     * 回溯，移除nums[i]
3. 调用backtrack(0, [])开始回溯

关键点:
- 去重关键：在同一层递归中，如果当前元素与前一个相同，跳过
- 排序后相同元素相邻，便于判断
- 时间复杂度O(2^n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n) - 需要生成所有子集
空间复杂度: O(n) - 递归栈深度和路径长度
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def subsets_with_dup(nums: List[int]) -> List[List[int]]:
    """
    函数式接口 - 回溯算法 + 去重优化
    
    实现思路:
    使用回溯算法生成所有子集，通过排序和去重技巧避免重复子集。
    
    Args:
        nums: 可能包含重复元素的整数数组
        
    Returns:
        所有可能的子集列表（不包含重复子集）
        
    Example:
        >>> subsets_with_dup([1, 2, 2])
        [[], [1], [2], [1, 2], [1, 2, 2], [2, 2]]
    """
    result = []
    nums.sort()  # 排序便于去重
    
    def backtrack(start: int, path: List[int]):
        """回溯函数"""
        result.append(path[:])  # 所有路径都是子集
        
        for i in range(start, len(nums)):
            # 去重：在同一层递归中，如果当前元素与前一个相同，跳过
            if i > start and nums[i] == nums[i - 1]:
                continue
            
            path.append(nums[i])
            backtrack(i + 1, path)
            path.pop()
    
    backtrack(0, [])
    return result


