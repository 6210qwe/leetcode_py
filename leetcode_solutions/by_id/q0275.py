"""
275. H 指数 II - 给你一个整数数组 citations ，其中 citations[i] 表示研究者的第 i 篇论文被引用的次数，citations 已经按照 非降序排列 。计算并返回该研究者的 h 指数。 h 指数的定义 [https://baike.baidu.com/item/h-index/3991452?fr=aladdin]：h 代表“高引用次数”（high citations），一名科研人员的 h 指数是指他（她）的 （n 篇论文中）至少 有 h 篇论文分别被引用了至少 h 次。 请你设计并实现对数时间复杂度的算法解决此问题。 示例 1： 输入：citations = [0,1,3,5,6] 输出：3 解释：给定数组表示研究者总共有 5 篇论文，每篇论文相应的被引用了 0, 1, 3, 5, 6 次。 由于研究者有3篇论文每篇 至少 被引用了 3 次，其余两篇论文每篇被引用 不多于 3 次，所以她的 h 指数是 3 。 示例 2： 输入：citations = [1,2,100] 输出：2 提示： * n == citations.length * 1 <= n <= 105 * 0 <= citations[i] <= 1000 * citations 按 升序排列
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 二分查找，找到最大的h使得至少有h篇论文引用>=h

算法步骤:
1. 使用二分查找
2. 对于mid，检查citations[n-mid] >= mid
3. 如果满足，尝试更大的h

关键点:
- 二分查找
- 时间复杂度O(log n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 二分查找
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
