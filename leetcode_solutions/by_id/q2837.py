"""
2749. 得到整数零需要执行的最少操作数 - 给你两个整数：num1 和 num2 。 在一步操作中，你需要从范围 [0, 60] 中选出一个整数 i ，并从 num1 减去 2i + num2 。 请你计算，要想使 num1 等于 0 需要执行的最少操作数，并以整数形式返回。 如果无法使 num1 等于 0 ，返回 -1 。 示例 1： 输入：num1 = 3, num2 = -2 输出：3 解释：可以执行下述步骤使 3 等于 0 ： - 选择 i = 2 ，并从 3 减去 22 + (-2) ，num1 = 3 - (4 + (-2)) = 1 。 - 选择 i = 2 ，并从 1 减去 22 + (-2) ，num1 = 1 - (4 + (-2)) = -1 。 - 选择 i = 0 ，并从 -1 减去 20 + (-2) ，num1 = (-1) - (1 + (-2)) = 0 。 可以证明 3 是需要执行的最少操作数。 示例 2： 输入：num1 = 5, num2 = 7 输出：-1 解释：可以证明，执行操作无法使 5 等于 0 。 提示： * 1 <= num1 <= 109 * -109 <= num2 <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过枚举操作次数 k 来检查是否可以通过 k 次操作使 num1 等于 0。

算法步骤:
1. 枚举操作次数 k 从 0 到 60。
2. 对于每个 k，计算 num1 - k * num2 是否可以表示为 k 个 2 的幂次之和。
3. 如果可以，则返回 k。
4. 如果遍历完所有 k 仍无法找到合适的解，则返回 -1。

关键点:
- 使用位运算来检查 num1 - k * num2 是否可以表示为 k 个 2 的幂次之和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 枚举的次数是固定的 61 次。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_zero(num1: int, num2: int) -> int:
    """
    函数式接口 - 计算使 num1 等于 0 需要的最少操作数
    """
    for k in range(61):
        target = num1 - k * num2
        if target < 0:
            break
        # 检查 target 是否可以表示为 k 个 2 的幂次之和
        if bin(target).count('1') <= k:
            return k
    return -1


Solution = create_solution(min_operations_to_zero)