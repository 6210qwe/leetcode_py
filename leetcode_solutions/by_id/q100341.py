"""
LCR 186. 文物朝代判断 - 展览馆展出来自 13 个朝代的文物，每排展柜展出 5 个文物。某排文物的摆放情况记录于数组 places，其中 places[i] 表示处于第 i 位文物的所属朝代编号。其中，编号为 0 的朝代表示未知朝代。请判断并返回这排文物的所属朝代编号是否能够视为连续的五个朝代（如遇未知朝代可算作连续情况）。 示例 1： 输入：places = [0, 6, 9, 0, 7] 输出：True 示例 2： 输入：places = [7, 8, 9, 10, 11] 输出：True 提示： * places.length = 5 * 0 <= places[i] <= 13
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断数组中的非零元素是否能构成连续的五个朝代编号。

算法步骤:
1. 去除数组中的 0，并对剩余元素进行排序。
2. 检查排序后的数组是否连续，即相邻元素之间的差值是否为 1 或 0。

关键点:
- 排序后的数组中，相邻元素之间的差值应为 1 或 0。
- 0 可以被视为任意朝代编号。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度（固定为 5），排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(places: List[int]) -> bool:
    """
    函数式接口 - 判断数组中的非零元素是否能构成连续的五个朝代编号。
    """
    # 去除数组中的 0，并对剩余元素进行排序
    non_zero_places = sorted([place for place in places if place != 0])
    
    # 检查排序后的数组是否连续
    for i in range(1, len(non_zero_places)):
        if non_zero_places[i] - non_zero_places[i - 1] > 1:
            return False
    
    return True


Solution = create_solution(solution_function_name)