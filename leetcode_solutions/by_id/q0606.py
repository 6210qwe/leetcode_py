"""
606. 根据二叉树创建字符串 - 给你二叉树的根节点 root ，请你采用前序遍历的方式，将二叉树转化为一个由括号和整数组成的字符串，返回构造出的字符串。 空节点使用一对空括号对 "()" 表示，转化后需要省略所有不影响字符串与原始二叉树之间的一对一映射关系的空括号对。 示例 1： [https://assets.leetcode.com/uploads/2021/05/03/cons1-tree.jpg] 输入：root = [1,2,3,4] 输出："1(2(4))(3)" 解释：初步转化后得到 "1(2(4)())(3()())" ，但省略所有不必要的空括号对后，字符串应该是"1(2(4))(3)" 。 示例 2： [https://assets.leetcode.com/uploads/2021/05/03/cons2-tree.jpg] 输入：root = [1,2,3,null,4] 输出："1(2()(4))(3)" 解释：和第一个示例类似，但是无法省略第一个空括号对，否则会破坏输入与输出一一映射的关系。 提示： * 树中节点的数目范围是 [1, 104] * -1000 <= Node.val <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前序遍历（DFS）来构建字符串，并在适当的位置添加括号。

算法步骤:
1. 如果当前节点为空，返回空字符串。
2. 初始化结果字符串为当前节点的值。
3. 如果左子节点存在，递归处理左子节点，并在其结果前后加上括号。
4. 如果右子节点存在且左子节点不存在，递归处理右子节点并在其结果前后加上括号。
5. 如果右子节点存在且左子节点也存在，递归处理右子节点并在其结果前后加上括号。
6. 返回最终构建的字符串。

关键点:
- 只有当左子节点为空且右子节点不为空时，才需要在左子节点位置添加空括号。
- 递归处理左右子节点，并在结果前后加上括号。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数。每个节点只会被访问一次。
空间复杂度: O(h)，其中 h 是二叉树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def tree2str(root: Optional[TreeNode]) -> str:
    """
    函数式接口 - 将二叉树转化为字符串
    """
    if not root:
        return ""

    result = str(root.val)

    if root.left or root.right:
        result += f"({tree2str(root.left)})"
    
    if root.right:
        result += f"({tree2str(root.right)})"

    return result


Solution = create_solution(tree2str)