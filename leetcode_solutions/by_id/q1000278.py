"""
LCR 035. 最小时间差 - 给定一个 24 小时制（小时:分钟 "HH:MM"）的时间列表，找出列表中任意两个时间的最小时间差并以分钟数表示。 示例 1： 输入：timePoints = ["23:59","00:00"] 输出：1 示例 2： 输入：timePoints = ["00:00","23:59","00:00"] 输出：0 提示： * 2 <= timePoints <= 2 * 104 * timePoints[i] 格式为 "HH:MM" 注意：本题与主站 539 题相同： https://leetcode.cn/problems/minimum-time-difference/ [https://leetcode.cn/problems/minimum-time-difference/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将所有时间转换为分钟数，并排序，然后计算相邻时间的差值，同时考虑首尾时间的差值。

算法步骤:
1. 将所有时间转换为从午夜开始的分钟数。
2. 对这些分钟数进行排序。
3. 计算相邻时间的差值，找到最小差值。
4. 考虑首尾时间的差值（即最后一个时间与第一个时间的差值），因为一天是循环的。

关键点:
- 将时间转换为分钟数后排序，可以简化计算。
- 需要考虑首尾时间的差值，因为一天是循环的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 timePoints 的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，需要存储转换后的分钟数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_min_difference(time_points: List[str]) -> int:
    """
    函数式接口 - 找出列表中任意两个时间的最小时间差并以分钟数表示
    """
    # 将所有时间转换为从午夜开始的分钟数
    minutes = [int(t[:2]) * 60 + int(t[3:]) for t in time_points]

    # 对这些分钟数进行排序
    minutes.sort()

    # 初始化最小时间差为一整天的分钟数
    min_diff = 1440

    # 计算相邻时间的差值
    for i in range(1, len(minutes)):
        min_diff = min(min_diff, minutes[i] - minutes[i - 1])

    # 考虑首尾时间的差值
    min_diff = min(min_diff, 1440 - (minutes[-1] - minutes[0]))

    return min_diff


Solution = create_solution(find_min_difference)