"""
2429. 最小异或 - 给你两个正整数 num1 和 num2 ，找出满足下述条件的正整数 x ： * x 的置位数和 num2 相同，且 * x XOR num1 的值 最小 注意 XOR 是按位异或运算。 返回整数 x 。题目保证，对于生成的测试用例， x 是 唯一确定 的。 整数的 置位数 是其二进制表示中 1 的数目。 示例 1： 输入：num1 = 3, num2 = 5 输出：3 解释： num1 和 num2 的二进制表示分别是 0011 和 0101 。 整数 3 的置位数与 num2 相同，且 3 XOR 3 = 0 是最小的。 示例 2： 输入：num1 = 1, num2 = 12 输出：3 解释： num1 和 num2 的二进制表示分别是 0001 和 1100 。 整数 3 的置位数与 num2 相同，且 3 XOR 1 = 2 是最小的。 提示： * 1 <= num1, num2 <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，从高位到低位依次设置 x 的每一位，使得 x 的置位数与 num2 相同，并且 x XOR num1 的值最小。

算法步骤:
1. 计算 num2 的置位数 count。
2. 初始化 x 为 0。
3. 从高位到低位遍历 num1 的每一位：
   - 如果当前位是 1 且 count > 0，则将 x 的对应位置 1，并减少 count。
   - 如果当前位是 0 且 count > 0，则将 x 的对应位置 1，并减少 count。
4. 返回 x。

关键点:
- 从高位到低位设置 x 的每一位，确保 x XOR num1 的值最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log(num1))，因为我们需要遍历 num1 的每一位。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimize_xor(num1: int, num2: int) -> int:
    """
    函数式接口 - 找出满足条件的正整数 x
    """
    # 计算 num2 的置位数
    count = bin(num2).count('1')
    
    # 初始化 x 为 0
    x = 0
    bit = 1 << 30  # 从最高位开始
    
    while bit > 0:
        if count == 0:
            break
        if num1 & bit:
            x |= bit
            count -= 1
        elif count > 0:
            x |= bit
            count -= 1
        bit >>= 1
    
    return x


Solution = create_solution(minimize_xor)