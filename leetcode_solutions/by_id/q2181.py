"""
2057. 值相等的最小索引 - 给你一个下标从 0 开始的整数数组 nums ，返回 nums 中满足 i mod 10 == nums[i] 的最小下标 i ；如果不存在这样的下标，返回 -1 。 x mod y 表示 x 除以 y 的 余数 。 示例 1： 输入：nums = [0,1,2] 输出：0 解释： i=0: 0 mod 10 = 0 == nums[0]. i=1: 1 mod 10 = 1 == nums[1]. i=2: 2 mod 10 = 2 == nums[2]. 所有下标都满足 i mod 10 == nums[i] ，所以返回最小下标 0 示例 2： 输入：nums = [4,3,2,1] 输出：2 解释： i=0: 0 mod 10 = 0 != nums[0]. i=1: 1 mod 10 = 1 != nums[1]. i=2: 2 mod 10 = 2 == nums[2]. i=3: 3 mod 10 = 3 != nums[3]. 2 唯一一个满足 i mod 10 == nums[i] 的下标 示例 3： 输入：nums = [1,2,3,4,5,6,7,8,9,0] 输出：-1 解释：不存在满足 i mod 10 == nums[i] 的下标 示例 4： 输入：nums = [2,1,3,5,2] 输出：1 解释：1 是唯一一个满足 i mod 10 == nums[i] 的下标 提示： * 1 <= nums.length <= 100 * 0 <= nums[i] <= 9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，找到第一个满足 i % 10 == nums[i] 的下标。

算法步骤:
1. 遍历数组，对于每个下标 i，检查 i % 10 是否等于 nums[i]。
2. 如果找到满足条件的下标，立即返回该下标。
3. 如果遍历完数组没有找到满足条件的下标，返回 -1。

关键点:
- 通过一次遍历即可找到满足条件的最小下标。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历整个数组一次。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_equal(nums: List[int]) -> int:
    """
    函数式接口 - 返回 nums 中满足 i % 10 == nums[i] 的最小下标 i ；如果不存在这样的下标，返回 -1 。
    """
    for i in range(len(nums)):
        if i % 10 == nums[i]:
            return i
    return -1


Solution = create_solution(smallest_equal)