"""
219. 存在重复元素 II - 给你一个整数数组 nums 和一个整数 k ，判断数组中是否存在两个 不同的索引 i 和 j ，满足 nums[i] == nums[j] 且 abs(i - j) <= k 。如果存在，返回 true ；否则，返回 false 。 示例 1： 输入：nums = [1,2,3,1], k = 3 输出：true 示例 2： 输入：nums = [1,0,1,1], k = 1 输出：true 示例 3： 输入：nums = [1,2,3,1,2,3], k = 2 输出：false 提示： * 1 <= nums.length <= 105 * -109 <= nums[i] <= 109 * 0 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和哈希表，维护窗口内最近k个元素

算法步骤:
1. 使用哈希表记录元素及其最近出现的索引
2. 遍历数组，如果元素已存在且索引差<=k，返回True
3. 更新元素的最新索引

关键点:
- 使用哈希表记录元素索引
- 时间复杂度O(n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组一次
空间复杂度: O(n) - 哈希表存储元素索引
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def contains_duplicate_ii(nums: List[int], k: int) -> bool:
    """
    函数式接口 - 存在重复元素II
    
    实现思路:
    使用滑动窗口和哈希表，维护窗口内最近k个元素。
    
    Args:
        nums: 整数数组
        k: 索引差的最大值
        
    Returns:
        如果存在满足条件的重复元素返回True，否则返回False
        
    Example:
        >>> contains_duplicate_ii([1, 2, 3, 1], 3)
        True
    """
    seen = {}
    for i, num in enumerate(nums):
        if num in seen and i - seen[num] <= k:
            return True
        seen[num] = i
    return False


