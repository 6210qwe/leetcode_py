# ============================================================================
# 问题描述
# ============================================================================
"""
LCR 051. 二叉树中的最大路径和 - 路径 被定义为一条从树中任意节点出发，沿父节点-子节点连接，达到任意节点的序列。同一个节点在一条路径序列中 至多出现一次 。该路径 至少包含一个 节点，且不一定经过根节点。 路径和 是路径中各节点值的总和。 给定一个二叉树的根节点 root ，返回其 最大路径和，即所有路径上节点值之和的最大值。 示例 1： [https://assets.leetcode.com/uploads/2020/10/13/exx1.jpg] 输入：root = [1,2,3] 输出：6 解释：最优路径是 2 -> 1 -> 3 ，路径和为 2 + 1 + 3 = 6 示例 2： [https://assets.leetcode.com/uploads/2020/10/13/exx2.jpg] 输入：root = [-10,9,20,null,null,15,7] 输出：42 解释：最优路径是 15 -> 20 -> 7 ，路径和为 15 + 20 + 7 = 42 提示： * 树中节点数目范围是 [1, 3 * 104] * -1000 <= Node.val <= 1000 注意：本题与主站 124 题相同： https://leetcode.cn/problems/binary-tree-maximum-path-sum/ [https://leetcode.cn/problems/binary-tree-maximum-path-sum/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）来遍历每个节点，并计算以该节点为根的最大路径和。

算法步骤:
1. 定义一个递归函数 `max_gain`，用于计算从当前节点出发的最大路径和。
2. 在 `max_gain` 函数中，递归计算左子树和右子树的最大路径和。
3. 计算当前节点的最大路径和，并更新全局最大路径和。
4. 返回当前节点的最大增益（只能选择一边子树）。

关键点:
- 通过递归计算每个节点的最大路径和，并更新全局最大路径和。
- 递归函数返回的是从当前节点出发的最大增益，而不是整个路径和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点只被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

class Solution:
    def maxPathSum(self, root: Optional[TreeNode]) -> int:
        self.max_sum = float('-inf')
        
        def max_gain(node: Optional[TreeNode]) -> int:
            if not node:
                return 0
            
            # 只有在最大贡献值大于 0 时，才会选取对应子节点
            left_gain = max(max_gain(node.left), 0)
            right_gain = max(max_gain(node.right), 0)
            
            # 节点的最大路径和取决于该节点的值与该节点的左右子节点的最大贡献值
            price_newpath = node.val + left_gain + right_gain
            
            # 更新答案
            self.max_sum = max(self.max_sum, price_newpath)
            
            # 返回节点的最大贡献值
            return node.val + max(left_gain, right_gain)
        
        max_gain(root)
        return self.max_sum

Solution = create_solution(Solution)