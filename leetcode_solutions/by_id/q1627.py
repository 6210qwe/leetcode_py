"""
1503. 所有蚂蚁掉下来前的最后一刻 - 有一块木板，长度为 n 个 单位 。一些蚂蚁在木板上移动，每只蚂蚁都以 每秒一个单位 的速度移动。其中，一部分蚂蚁向 左 移动，其他蚂蚁向 右 移动。 当两只向 不同 方向移动的蚂蚁在某个点相遇时，它们会同时改变移动方向并继续移动。假设更改方向不会花费任何额外时间。 而当蚂蚁在某一时刻 t 到达木板的一端时，它立即从木板上掉下来。 给你一个整数 n 和两个整数数组 left 以及 right 。两个数组分别标识向左或者向右移动的蚂蚁在 t = 0 时的位置。请你返回最后一只蚂蚁从木板上掉下来的时刻。 示例 1： [https://assets.leetcode.com/uploads/2020/06/17/ants.jpg] 输入：n = 4, left = [4,3], right = [0,1] 输出：4 解释：如上图所示： -下标 0 处的蚂蚁命名为 A 并向右移动。 -下标 1 处的蚂蚁命名为 B 并向右移动。 -下标 3 处的蚂蚁命名为 C 并向左移动。 -下标 4 处的蚂蚁命名为 D 并向左移动。 请注意，蚂蚁在木板上的最后时刻是 t = 4 秒，之后蚂蚁立即从木板上掉下来。（也就是说在 t = 4.0000000001 时，木板上没有蚂蚁）。 示例 2： [https://assets.leetcode.com/uploads/2020/06/17/ants2.jpg] 输入：n = 7, left = [], right = [0,1,2,3,4,5,6,7] 输出：7 解释：所有蚂蚁都向右移动，下标为 0 的蚂蚁需要 7 秒才能从木板上掉落。 示例 3： [https://assets.leetcode.com/uploads/2020/06/17/ants3.jpg] 输入：n = 7, left = [0,1,2,3,4,5,6,7], right = [] 输出：7 解释：所有蚂蚁都向左移动，下标为 7 的蚂蚁需要 7 秒才能从木板上掉落。 提示： * 1 <= n <= 10^4 * 0 <= left.length <= n + 1 * 0 <= left[i] <= n * 0 <= right.length <= n + 1 * 0 <= right[i] <= n * 1 <= left.length + right.length <= n + 1 * left 和 right 中的所有值都是唯一的，并且每个值 只能出现在二者之一 中。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 
- 由于蚂蚁相遇后会交换方向，但实际上这对结果没有影响，因为它们仍然会在相同的时间内到达木板的两端。因此，我们只需要考虑每只蚂蚁到达木板边缘的时间。

算法步骤:
1. 计算所有向左移动的蚂蚁到达左端的时间，即 `max(left)`。
2. 计算所有向右移动的蚂蚁到达右端的时间，即 `max(n - x for x in right)`。
3. 返回这两个时间的最大值。

关键点:
- 蚂蚁相遇后交换方向对最终结果没有影响。
- 只需计算每只蚂蚁到达木板边缘的时间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def get_last_moment(n: int, left: List[int], right: List[int]) -> int:
    """
    函数式接口 - 计算最后一只蚂蚁从木板上掉下来的时刻
    """
    # 计算所有向左移动的蚂蚁到达左端的时间
    max_left_time = max(left) if left else 0
    
    # 计算所有向右移动的蚂蚁到达右端的时间
    max_right_time = max(n - x for x in right) if right else 0
    
    # 返回这两个时间的最大值
    return max(max_left_time, max_right_time)


Solution = create_solution(get_last_moment)