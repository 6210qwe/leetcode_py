"""
2376. 统计特殊整数 - 如果一个正整数每一个数位都是 互不相同 的，我们称它是 特殊整数 。 给你一个 正 整数 n ，请你返回区间 [1, n] 之间特殊整数的数目。 示例 1： 输入：n = 20 输出：19 解释：1 到 20 之间所有整数除了 11 以外都是特殊整数。所以总共有 19 个特殊整数。 示例 2： 输入：n = 5 输出：5 解释：1 到 5 所有整数都是特殊整数。 示例 3： 输入：n = 135 输出：110 解释：从 1 到 135 总共有 110 个整数是特殊整数。 不特殊的部分数字为：22 ，114 和 131 。 提示： * 1 <= n <= 2 * 10^9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和组合数学来计算特殊整数的数量。

算法步骤:
1. 将 n 转换为字符串形式，逐位处理。
2. 使用动态规划预计算长度为 i 的特殊整数数量。
3. 对于每一位，计算当前位之前的所有可能组合，并根据当前位的值进行调整。
4. 最后加上小于 n 的部分。

关键点:
- 动态规划表 dp[i][j] 表示长度为 i 的特殊整数中使用 j 个不同数字的数量。
- 使用组合数学计算每种情况下的特殊整数数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d * 2^d)，其中 d 是 n 的位数。因为我们需要考虑每一位的组合情况。
空间复杂度: O(d * 2^d)，用于存储动态规划表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def count_special_integers(n: int) -> int:
    s = str(n)
    d = len(s)
    dp = [[0] * (1 << 10) for _ in range(d + 1)]
    dp[0][0] = 1

    for i in range(1, d + 1):
        for mask in range(1 << 10):
            if dp[i - 1][mask] == 0:
                continue
            for digit in range(10):
                if mask & (1 << digit) > 0:
                    continue
                new_mask = mask | (1 << digit)
                dp[i][new_mask] += dp[i - 1][mask]

    def count_less_than(s: str) -> int:
        res = 0
        seen = 0
        for i, c in enumerate(s):
            for digit in range(int(c)):
                if seen & (1 << digit) == 0:
                    res += dp[d - i - 1][(seen | (1 << digit))]
            if seen & (1 << int(c)) > 0:
                break
            seen |= (1 << int(c))
        return res + (seen == (1 << 10) - 1)

    return sum(dp[d][mask] for mask in range(1, 1 << 10)) - count_less_than(s)

Solution = create_solution(count_special_integers)