"""
LCR 127. 跳跃训练 - 今天的有氧运动训练内容是在一个长条形的平台上跳跃。平台有 num 个小格子，每次可以选择跳 一个格子 或者 两个格子。请返回在训练过程中，学员们共有多少种不同的跳跃方式。 结果可能过大，因此结果需要取模 1e9+7（1000000007），如计算初始结果为：1000000008，请返回 1。 示例 1： 输入：n = 2 输出：2 示例 2： 输入：n = 5 输出：8 提示： * 0 <= n <= 100 注意：本题与主站 70 题相同：https://leetcode.cn/problems/climbing-stairs/ [https://leetcode.cn/problems/climbing-stairs/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。定义 dp[i] 为跳到第 i 个格子的不同方法数。状态转移方程为 dp[i] = dp[i-1] + dp[i-2]，因为可以从第 i-1 个格子跳一步到达第 i 个格子，也可以从第 i-2 个格子跳两步到达第 i 个格子。

算法步骤:
1. 初始化 dp 数组，dp[0] = 1, dp[1] = 1。
2. 从 2 到 n 进行迭代，更新 dp[i]。
3. 返回 dp[n] % (10**9 + 7)。

关键点:
- 使用动态规划来避免重复计算。
- 由于只需要前两个状态，可以使用常数空间优化。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 计算跳到第 n 个格子的不同方法数
    """
    if n == 0 or n == 1:
        return 1
    
    a, b = 1, 1
    for i in range(2, n + 1):
        a, b = b, (a + b) % (10**9 + 7)
    
    return b


Solution = create_solution(solution_function_name)