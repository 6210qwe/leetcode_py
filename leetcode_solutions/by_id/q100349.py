def max_value(a: int, b: int) -> int:
    """
    函数式接口 - 找出两个数字a和b中最大的那一个
    """
    # 计算 diff
    diff = a - b
    # 计算 k
    k = (diff >> 31) & 1
    # 返回结果
    return a * k + b * (1 - k)


