"""
1475. 商品折扣后的最终价格 - 给你一个数组 prices ，其中 prices[i] 是商店里第 i 件商品的价格。 商店里正在进行促销活动，如果你要买第 i 件商品，那么你可以得到与 prices[j] 相等的折扣，其中 j 是满足 j > i 且 prices[j] <= prices[i] 的 最小下标 ，如果没有满足条件的 j ，你将没有任何折扣。 请你返回一个数组，数组中第 i 个元素是折扣后你购买商品 i 最终需要支付的价格。 示例 1： 输入：prices = [8,4,6,2,3] 输出：[4,2,4,2,3] 解释： 商品 0 的价格为 price[0]=8 ，你将得到 prices[1]=4 的折扣，所以最终价格为 8 - 4 = 4 。 商品 1 的价格为 price[1]=4 ，你将得到 prices[3]=2 的折扣，所以最终价格为 4 - 2 = 2 。 商品 2 的价格为 price[2]=6 ，你将得到 prices[3]=2 的折扣，所以最终价格为 6 - 2 = 4 。 商品 3 和 4 都没有折扣。 示例 2： 输入：prices = [1,2,3,4,5] 输出：[1,2,3,4,5] 解释：在这个例子中，所有商品都没有折扣。 示例 3： 输入：prices = [10,1,1,6] 输出：[9,0,1,6] 提示： * 1 <= prices.length <= 500 * 1 <= prices[i] <= 10^3
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调栈来找到每个商品的第一个小于等于它的价格。

算法步骤:
1. 初始化一个空栈和结果数组。
2. 遍历价格数组，对于每个价格：
   - 如果栈不为空且当前价格小于等于栈顶价格，则弹出栈顶并计算折扣后的价格。
   - 将当前价格及其索引压入栈中。
3. 返回结果数组。

关键点:
- 使用单调栈可以高效地找到每个价格的第一个小于等于它的价格。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是价格数组的长度。每个元素最多只会被压入和弹出栈一次。
空间复杂度: O(n)，最坏情况下栈中会存储所有的价格。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def final_prices(prices: List[int]) -> List[int]:
    """
    函数式接口 - 计算商品折扣后的最终价格
    """
    stack = []
    result = prices[:]
    
    for i, price in enumerate(prices):
        while stack and prices[stack[-1]] >= price:
            index = stack.pop()
            result[index] -= price
        stack.append(i)
    
    return result


Solution = create_solution(final_prices)