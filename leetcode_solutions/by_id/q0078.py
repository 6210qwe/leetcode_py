"""
78. 子集 - 给你一个整数数组 nums ，数组中的元素 互不相同 。返回该数组所有可能的子集（幂集）。 解集 不能 包含重复的子集。你可以按 任意顺序 返回解集。 示例 1： 输入：nums = [1,2,3] 输出：[[],[1],[2],[1,2],[3],[1,3],[2,3],[1,2,3]] 示例 2： 输入：nums = [0] 输出：[[],[0]] 提示： * 1 <= nums.length <= 10 * -10 <= nums[i] <= 10 * nums 中的所有元素 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 回溯算法，每个元素可以选择或不选择

算法步骤:
1. 回溯函数backtrack(start, path)：
   - 将当前path加入结果（所有路径都是子集）
   - 对于i从start到n-1：
     * 选择nums[i]，加入path
     * 递归backtrack(i+1, path)
     * 回溯，移除nums[i]
2. 调用backtrack(0, [])开始回溯

关键点:
- 每个元素可以选择或不选择
- 所有路径都是有效的子集
- 时间复杂度O(2^n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n) - 需要生成所有子集
空间复杂度: O(n) - 递归栈深度和路径长度
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def subsets(nums: List[int]) -> List[List[int]]:
    """
    函数式接口 - 回溯算法
    
    实现思路:
    使用回溯算法生成所有子集，每个元素可以选择或不选择。
    
    Args:
        nums: 整数数组，元素互不相同
        
    Returns:
        所有可能的子集列表
        
    Example:
        >>> subsets([1, 2, 3])
        [[], [1], [2], [1, 2], [3], [1, 3], [2, 3], [1, 2, 3]]
    """
    result = []
    
    def backtrack(start: int, path: List[int]):
        """回溯函数"""
        result.append(path[:])  # 所有路径都是子集
        
        for i in range(start, len(nums)):
            path.append(nums[i])
            backtrack(i + 1, path)
            path.pop()
    
    backtrack(0, [])
    return result


