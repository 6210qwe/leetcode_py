"""
LCR 095. 最长公共子序列 - 给定两个字符串 text1 和 text2，返回这两个字符串的最长 公共子序列 的长度。如果不存在 公共子序列 ，返回 0 。 一个字符串的 子序列 是指这样一个新的字符串：它是由原字符串在不改变字符的相对顺序的情况下删除某些字符（也可以不删除任何字符）后组成的新字符串。 * 例如，"ace" 是 "abcde" 的子序列，但 "aec" 不是 "abcde" 的子序列。 两个字符串的 公共子序列 是这两个字符串所共同拥有的子序列。 示例 1： 输入：text1 = "abcde", text2 = "ace" 输出：3 解释：最长公共子序列是 "ace" ，它的长度为 3 。 示例 2： 输入：text1 = "abc", text2 = "abc" 输出：3 解释：最长公共子序列是 "abc" ，它的长度为 3 。 示例 3： 输入：text1 = "abc", text2 = "def" 输出：0 解释：两个字符串没有公共子序列，返回 0 。 提示： * 1 <= text1.length, text2.length <= 1000 * text1 和 text2 仅由小写英文字符组成。 注意：本题与主站 1143 题相同： https://leetcode.cn/problems/longest-common-subsequence/ [https://leetcode.cn/problems/longest-common-subsequence/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。定义一个二维数组 dp，其中 dp[i][j] 表示 text1[0:i] 和 text2[0:j] 的最长公共子序列的长度。

算法步骤:
1. 初始化一个 (m+1) x (n+1) 的二维数组 dp，所有元素初始化为 0。
2. 遍历 text1 和 text2，更新 dp 数组：
   - 如果 text1[i-1] == text2[j-1]，则 dp[i][j] = dp[i-1][j-1] + 1。
   - 否则，dp[i][j] = max(dp[i-1][j], dp[i][j-1])。
3. 返回 dp[m][n]，即为最长公共子序列的长度。

关键点:
- 动态规划的状态转移方程和边界条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是 text1 和 text2 的长度。
空间复杂度: O(m * n)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_common_subsequence(text1: str, text2: str) -> int:
    """
    函数式接口 - 计算两个字符串的最长公共子序列的长度
    """
    m, n = len(text1), len(text2)
    # 初始化 dp 数组
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    
    # 填充 dp 数组
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if text1[i - 1] == text2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
    
    return dp[m][n]


Solution = create_solution(longest_common_subsequence)