"""
1347. 制造字母异位词的最小步骤数 - 给你两个长度相等的字符串 s 和 t。每一个步骤中，你可以选择将 t 中的 任一字符 替换为 另一个字符。 返回使 t 成为 s 的字母异位词的最小步骤数。 字母异位词 指字母相同，但排列不同（也可能相同）的字符串。 示例 1： 输出：s = "bab", t = "aba" 输出：1 提示：用 'b' 替换 t 中的第一个 'a'，t = "bba" 是 s 的一个字母异位词。 示例 2： 输出：s = "leetcode", t = "practice" 输出：5 提示：用合适的字符替换 t 中的 'p', 'r', 'a', 'i' 和 'c'，使 t 变成 s 的字母异位词。 示例 3： 输出：s = "anagram", t = "mangaar" 输出：0 提示："anagram" 和 "mangaar" 本身就是一组字母异位词。 示例 4： 输出：s = "xxyyzz", t = "xxyyzz" 输出：0 示例 5： 输出：s = "friend", t = "family" 输出：4 提示： * 1 <= s.length <= 50000 * s.length == t.length * s 和 t 只包含小写英文字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来记录每个字符在 s 和 t 中的出现次数，然后计算需要替换的字符数量。

算法步骤:
1. 初始化两个哈希表分别记录 s 和 t 中每个字符的出现次数。
2. 遍历 s 和 t，更新哈希表。
3. 计算需要替换的字符数量，即 s 中比 t 多的字符数量。

关键点:
- 使用哈希表来记录字符出现次数，可以高效地进行比较和计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们需要遍历字符串 s 和 t 各一次。
空间复杂度: O(1)，因为哈希表的大小是固定的（最多 26 个小写字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_steps_to_make_anagram(s: str, t: str) -> int:
    """
    函数式接口 - 计算使 t 成为 s 的字母异位词的最小步骤数
    """
    # 初始化两个哈希表
    count_s = [0] * 26
    count_t = [0] * 26
    
    # 更新哈希表
    for char in s:
        count_s[ord(char) - ord('a')] += 1
    for char in t:
        count_t[ord(char) - ord('a')] += 1
    
    # 计算需要替换的字符数量
    steps = 0
    for i in range(26):
        if count_s[i] > count_t[i]:
            steps += count_s[i] - count_t[i]
    
    return steps


Solution = create_solution(min_steps_to_make_anagram)