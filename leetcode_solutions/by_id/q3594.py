"""
3371. 识别数组中的最大异常值 - 给你一个整数数组 nums。该数组包含 n 个元素，其中 恰好 有 n - 2 个元素是 特殊数字 。剩下的 两个 元素中，一个是所有 特殊数字 的 和 ，另一个是 异常值 。 异常值 的定义是：既不是原始特殊数字之一，也不是表示元素和的那个数。 注意，特殊数字、和 以及 异常值 的下标必须 不同 ，但可以共享 相同 的值。 返回 nums 中可能的 最大异常值。 示例 1： 输入： nums = [2,3,5,10] 输出： 10 解释： 特殊数字可以是 2 和 3，因此和为 5，异常值为 10。 示例 2： 输入： nums = [-2,-1,-3,-6,4] 输出： 4 解释： 特殊数字可以是 -2、-1 和 -3，因此和为 -6，异常值为 4。 示例 3： 输入： nums = [1,1,1,1,1,5,5] 输出： 5 解释： 特殊数字可以是 1、1、1、1 和 1，因此和为 5，另一个 5 为异常值。 提示： * 3 <= nums.length <= 105 * -1000 <= nums[i] <= 1000 * 输入保证 nums 中至少存在 一个 可能的异常值。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和求和来找到最大异常值。

算法步骤:
1. 对数组进行排序。
2. 计算前 n-2 个元素的和。
3. 检查最后一个元素是否是异常值，如果是则返回它。
4. 否则，检查倒数第二个元素是否是异常值，如果是则返回它。

关键点:
- 通过排序可以快速找到可能的异常值。
- 通过计算前 n-2 个元素的和来确定异常值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序操作的时间复杂度。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 识别数组中的最大异常值
    """
    # 对数组进行排序
    nums.sort()
    
    # 计算前 n-2 个元素的和
    total_sum = sum(nums[:-2])
    
    # 检查最后一个元素是否是异常值
    if total_sum == nums[-2]:
        return nums[-1]
    
    # 检查倒数第二个元素是否是异常值
    return nums[-2]


Solution = create_solution(solution_function_name)