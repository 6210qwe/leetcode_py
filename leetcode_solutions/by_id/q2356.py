"""
2275. 按位与结果大于零的最长组合 - 对数组 nums 执行 按位与 相当于对数组 nums 中的所有整数执行 按位与 。 * 例如，对 nums = [1, 5, 3] 来说，按位与等于 1 & 5 & 3 = 1 。 * 同样，对 nums = [7] 而言，按位与等于 7 。 给你一个正整数数组 candidates 。计算 candidates 中的数字每种组合下 按位与 的结果。 返回按位与结果大于 0 的 最长 组合的长度。 示例 1： 输入：candidates = [16,17,71,62,12,24,14] 输出：4 解释：组合 [16,17,62,24] 的按位与结果是 16 & 17 & 62 & 24 = 16 > 0 。 组合长度是 4 。 可以证明不存在按位与结果大于 0 且长度大于 4 的组合。 注意，符合长度最大的组合可能不止一种。 例如，组合 [62,12,24,14] 的按位与结果是 62 & 12 & 24 & 14 = 8 > 0 。 示例 2： 输入：candidates = [8,8] 输出：2 解释：最长组合是 [8,8] ，按位与结果 8 & 8 = 8 > 0 。 组合长度是 2 ，所以返回 2 。 提示： * 1 <= candidates.length <= 105 * 1 <= candidates[i] <= 107
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过统计每个二进制位上为1的数字个数，找到最大值。

算法步骤:
1. 初始化一个长度为32的数组，用于记录每个二进制位上为1的数字个数。
2. 遍历每个数字，更新二进制位计数数组。
3. 找到计数数组中的最大值，即为按位与结果大于0的最长组合的长度。

关键点:
- 使用一个长度为32的数组来记录每个二进制位上为1的数字个数。
- 通过遍历每个数字的二进制表示，更新计数数组。
- 最后找到计数数组中的最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def largest_combination(candidates: List[int]) -> int:
    """
    函数式接口 - 计算按位与结果大于0的最长组合的长度
    """
    # 初始化一个长度为32的数组，用于记录每个二进制位上为1的数字个数
    bit_count = [0] * 32
    
    # 遍历每个数字，更新二进制位计数数组
    for num in candidates:
        for i in range(32):
            if num & (1 << i):
                bit_count[i] += 1
    
    # 找到计数数组中的最大值
    return max(bit_count)


Solution = create_solution(largest_combination)