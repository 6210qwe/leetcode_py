"""
396. 旋转函数 - 给定一个长度为 n 的整数数组 nums 。 假设 arrk 是数组 nums 顺时针旋转 k 个位置后的数组，我们定义 nums 的 旋转函数 F 为： * F(k) = 0 * arrk[0] + 1 * arrk[1] + ... + (n - 1) * arrk[n - 1] 返回 F(0), F(1), ..., F(n-1)中的最大值 。 生成的测试用例让答案符合 32 位 整数。 示例 1: 输入: nums = [4,3,2,6] 输出: 26 解释: F(0) = (0 * 4) + (1 * 3) + (2 * 2) + (3 * 6) = 0 + 3 + 4 + 18 = 25 F(1) = (0 * 6) + (1 * 4) + (2 * 3) + (3 * 2) = 0 + 4 + 6 + 6 = 16 F(2) = (0 * 2) + (1 * 6) + (2 * 4) + (3 * 3) = 0 + 6 + 8 + 9 = 23 F(3) = (0 * 3) + (1 * 2) + (2 * 6) + (3 * 4) = 0 + 2 + 12 + 12 = 26 所以 F(0), F(1), F(2), F(3) 中的最大值是 F(3) = 26 。 示例 2: 输入: nums = [100] 输出: 0 提示: * n == nums.length * 1 <= n <= 105 * -100 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 推导相邻两个旋转函数 F(k) 和 F(k+1) 的递推关系。

算法步骤:
1. 设数组为 nums，长度为 n，计算数组和 sum(nums) 以及 F(0) = sum(i * nums[i])。
2. 根据旋转性质推导得到：F(k+1) = F(k) + sum(nums) - n * nums[n-1-k]。
3. 从 k=0 开始迭代计算 F(k)，每次根据上式得到下一个值，并更新最大值。

关键点:
- 不需要显式构造每次旋转后的数组，只用原数组和一个递推式即可一遍求完所有 F(k)。
- 小心下标对应关系：每往右旋转一次，原数组的最后一个元素移动到下标 0。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，n 为数组长度，一次遍历求 F(0)，一次遍历递推 F(k)。
空间复杂度: O(1)，除若干变量外不需要额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def rotate_function(nums: List[int]) -> int:
    """
    计算旋转函数 F(k) 的最大值。

    使用递推关系：F(k+1) = F(k) + sum(nums) - n * nums[n-1-k]，一次遍历即可。
    """
    n = len(nums)
    if n == 0:
        return 0
    s = sum(nums)
    f0 = sum(i * v for i, v in enumerate(nums))
    best = f0
    fk = f0
    for k in range(1, n):
        fk = fk + s - n * nums[-k]
        best = max(best, fk)
    return best


