def generate_the_string(n: int) -> str:
    """
    生成一个含 n 个字符的字符串，其中每种字符在该字符串中都恰好出现奇数次。
    """
    if n % 2 == 1:
        # 如果 n 是奇数，直接返回 n 个 'a'
        return 'a' * n
    else:
        # 如果 n 是偶数，返回 (n-1) 个 'a' 和 1 个 'b'
        return 'a' * (n - 1) + 'b'


