"""
2527. 查询数组异或美丽值 - 给你一个下标从 0 开始的整数数组 nums 。 三个下标 i ，j 和 k 的 有效值 定义为 ((nums[i] | nums[j]) & nums[k]) 。 一个数组的 异或美丽值 是数组中所有满足 0 <= i, j, k < n 的三元组 (i, j, k) 的 有效值 的异或结果。 请你返回 nums 的异或美丽值。 注意： * val1 | val2 是 val1 和 val2 的按位或。 * val1 & val2 是 val1 和 val2 的按位与。 示例 1： 输入：nums = [1,4] 输出：5 解释： 三元组和它们对应的有效值如下： - (0,0,0) 有效值为 ((1 | 1) & 1) = 1 - (0,0,1) 有效值为 ((1 | 1) & 4) = 0 - (0,1,0) 有效值为 ((1 | 4) & 1) = 1 - (0,1,1) 有效值为 ((1 | 4) & 4) = 4 - (1,0,0) 有效值为 ((4 | 1) & 1) = 1 - (1,0,1) 有效值为 ((4 | 1) & 4) = 4 - (1,1,0) 有效值为 ((4 | 4) & 1) = 0 - (1,1,1) 有效值为 ((4 | 4) & 4) = 4 数组的异或美丽值为所有有效值的按位异或 1 ^ 0 ^ 1 ^ 4 ^ 1 ^ 4 ^ 0 ^ 4 = 5 。 示例 2： 输入：nums = [15,45,20,2,34,35,5,44,32,30] 输出：34 解释：数组的异或美丽值为 34 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过位运算的性质，可以将问题简化为对数组中所有元素的异或结果进行计算。

算法步骤:
1. 计算数组中所有元素的异或结果。
2. 返回该异或结果。

关键点:
- 通过位运算的性质，可以证明最终结果就是所有元素的异或结果。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_xor_beauty(nums: List[int]) -> int:
    """
    函数式接口 - 计算数组的异或美丽值
    """
    xor_result = 0
    for num in nums:
        xor_result ^= num
    return xor_result


Solution = create_solution(find_xor_beauty)