"""
1718. 构建字典序最大的可行序列 - 给你一个整数 n ，请你找到满足下面条件的一个序列： * 整数 1 在序列中只出现一次。 * 2 到 n 之间每个整数都恰好出现两次。 * 对于每个 2 到 n 之间的整数 i ，两个 i 之间出现的距离恰好为 i 。 序列里面两个数 a[i] 和 a[j] 之间的 距离 ，我们定义为它们下标绝对值之差 |j - i| 。 请你返回满足上述条件中 字典序最大 的序列。题目保证在给定限制条件下，一定存在解。 一个序列 a 被认为比序列 b （两者长度相同）字典序更大的条件是： a 和 b 中第一个不一样的数字处，a 序列的数字比 b 序列的数字大。比方说，[0,1,9,0] 比 [0,1,5,6] 字典序更大，因为第一个不同的位置是第三个数字，且 9 比 5 大。 示例 1： 输入：n = 3 输出：[3,1,2,3,2] 解释：[2,3,2,1,3] 也是一个可行的序列，但是 [3,1,2,3,2] 是字典序最大的序列。 示例 2： 输入：n = 5 输出：[5,3,1,4,3,5,2,4,2] 提示： * 1 <= n <= 20
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用回溯法从大到小尝试放置每个数字，确保每次放置时满足距离要求，并且尽量保持字典序最大。

算法步骤:
1. 初始化一个结果数组 res，长度为 2 * n - 1。
2. 定义一个递归函数 backtrack(i)，表示从第 i 个位置开始尝试放置数字。
3. 在递归函数中，如果 i 达到数组长度，说明已经成功构建了序列，返回 True。
4. 如果 res[i] 已经被占用，跳过当前位置。
5. 从 n 到 1 逆序尝试放置每个数字 k：
   - 如果 k 为 1，直接放置在当前位置。
   - 否则，检查 i + k 是否在有效范围内且未被占用，如果可以放置，则放置 k 并继续递归。
6. 如果递归成功返回 True，否则撤销当前放置，继续尝试下一个数字。
7. 最终返回结果数组 res。

关键点:
- 从大到小尝试放置数字，确保字典序最大。
- 使用递归和回溯确保每一步都满足距离要求。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n) - 回溯法的时间复杂度在最坏情况下接近指数级。
空间复杂度: O(n) - 递归调用栈的深度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> List[int]:
    """
    函数式接口 - 返回满足条件的字典序最大的序列
    """
    res = [0] * (2 * n - 1)

    def backtrack(i: int) -> bool:
        if i == len(res):
            return True
        if res[i] != 0:
            return backtrack(i + 1)
        
        for k in range(n, 0, -1):
            if k == 1 and res[i] == 0:
                res[i] = k
                if backtrack(i + 1):
                    return True
                res[i] = 0
            elif i + k < len(res) and res[i + k] == 0:
                res[i] = res[i + k] = k
                if backtrack(i + 1):
                    return True
                res[i] = res[i + k] = 0
        
        return False

    backtrack(0)
    return res


Solution = create_solution(solution_function_name)