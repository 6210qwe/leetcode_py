"""
LCP 64. 二叉树灯饰 - 「力扣嘉年华」的中心广场放置了一个巨型的二叉树形状的装饰树。每个节点上均有一盏灯和三个开关。节点值为 `0` 表示灯处于「关闭」状态，节点值为 `1` 表示灯处于「开启」状态。每个节点上的三个开关各自功能如下： - 开关 `1`：切换当前节点的灯的状态； - 开关 `2`：切换 **以当前节点为根** 的子树中，所有节点上的灯的状态，； - 开关 `3`：切换 **当前节点及其左右子节点**（若存在的话） 上的灯的状态； 给定该装饰的初始状态 `root`，请返回最少需要操作多少次开关，可以关闭所有节点的灯。 **示例 1：** >输入：`root = [1,1,0,null,null,null,1]` > >输出：`2` > >解释：以下是最佳的方案之一，如图所示 ![b71b95bf405e3b223e00b2820a062ba4.gif](https://pic.leetcode.cn/1629357030-GSbzpY-b71b95bf405e3b223e00b2820a062ba4.gif){:width="300px"} **示例 2：** >输入：`root = [1,1,1,1,null,null,1]` > >输出：`1` > >解释：以下是最佳的方案，如图所示 ![a4091b6448a0089b4d9e8f0390ff9ac6.gif](https://pic.leetcode.cn/1629356950-HZsKZC-a4091b6448a0089b4d9e8f0390ff9ac6.gif){:width="300px"} **示例 3：** >输入：`root = [0,null,0]` > >输出：`0` > >解释：无需操作开关，当前所有节点上的灯均已关闭 **提示：** - `1 <= 节点个数 <= 10^5` - `0 <= Node.val <= 1`
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和深度优先搜索 (DFS) 来解决这个问题。我们定义一个递归函数来处理每个节点，并记录四种状态下的最小操作次数：
- 当前节点及其子树全部关闭
- 当前节点关闭，子树全部打开
- 当前节点打开，子树全部关闭
- 当前节点及其子树全部打开

算法步骤:
1. 定义一个递归函数 `dfs`，它接受一个节点并返回一个四元组，表示上述四种状态下的最小操作次数。
2. 在递归函数中，根据当前节点的值和其子节点的状态，计算出四种状态下的最小操作次数。
3. 通过递归调用 `dfs` 处理左子树和右子树，将结果合并到当前节点的结果中。
4. 最终返回根节点在所有灯都关闭状态下的最小操作次数。

关键点:
- 使用动态规划的思想，通过递归和状态转移来减少重复计算。
- 通过递归函数处理每个节点，并记录四种状态下的最小操作次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点只被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_turn_off_lights(root: Optional[TreeNode]) -> int:
    def dfs(node: Optional[TreeNode]) -> List[int]:
        if not node:
            return [0, 0, 0, 0]
        
        left = dfs(node.left)
        right = dfs(node.right)
        
        res = [float('inf')] * 4
        
        # 当前节点及其子树全部关闭
        res[0] = min(left[0], left[1] + 1, left[2] + 1, left[3] + 2) + min(right[0], right[1] + 1, right[2] + 1, right[3] + 2)
        if node.val == 1:
            res[0] += 1
        
        # 当前节点关闭，子树全部打开
        res[1] = min(left[0] + 1, left[1], left[2] + 2, left[3] + 1) + min(right[0] + 1, right[1], right[2] + 2, right[3] + 1)
        if node.val == 0:
            res[1] += 1
        
        # 当前节点打开，子树全部关闭
        res[2] = min(left[0] + 1, left[1] + 2, left[2], left[3] + 1) + min(right[0] + 1, right[1] + 2, right[2], right[3] + 1)
        if node.val == 1:
            res[2] += 1
        
        # 当前节点及其子树全部打开
        res[3] = min(left[0] + 2, left[1] + 1, left[2] + 1, left[3]) + min(right[0] + 2, right[1] + 1, right[2] + 1, right[3])
        if node.val == 0:
            res[3] += 1
        
        return res
    
    return dfs(root)[0]


Solution = create_solution(min_operations_to_turn_off_lights)