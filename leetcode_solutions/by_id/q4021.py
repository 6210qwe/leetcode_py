"""
3694. 删除子字符串后不同的终点 - 给你一个由字符 'U'、'D'、'L' 和 'R' 组成的字符串 s，表示在无限的二维笛卡尔网格上的移动。 * 'U': 从 (x, y) 移动到 (x, y + 1)。 * 'D': 从 (x, y) 移动到 (x, y - 1)。 * 'L': 从 (x, y) 移动到 (x - 1, y)。 * 'R': 从 (x, y) 移动到 (x + 1, y)。 你还得到了一个正整数 k。 你 必须 选择并移除 恰好一个 长度为 k 的连续子字符串 s。然后，从坐标 (0, 0) 开始，按顺序执行剩余的移动。 返回可到达的 不同 最终坐标的数量。 示例 1: 输入：s = "LUL", k = 1 输出：2 解释： 移除长度为 1 的子字符串后，s 可以是 "UL"、"LL" 或 "LU"。执行这些移动后，最终坐标将分别是 (-1, 1)、(-2, 0) 和 (-1, 1)。有两个不同的点 (-1, 1) 和 (-2, 0)，因此答案是 2。 示例 2: 输入：s = "UDLR", k = 4 输出：1 解释： 移除长度为 4 的子字符串后，s 只能是空字符串。最终坐标将是 (0, 0)。只有一个不同的点 (0, 0)，因此答案是 1。 示例 3: 输入：s = "UU", k = 1 输出：1 解释： 移除长度为 1 的子字符串后，s 变为 "U"，它总是以 (0, 1) 结束，因此只有一个不同的最终坐标。 提示: * 1 <= s.length <= 105 * s 只包含 'U'、'D'、'L' 和 'R'。 * 1 <= k <= s.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来记录每个位置的累计移动结果，并使用哈希集合来存储所有可能的最终坐标。

算法步骤:
1. 计算每个位置的前缀和（即从起点 (0, 0) 到当前位置的累计移动结果）。
2. 遍历字符串 s，对于每个可能的删除子字符串的位置，计算删除后的最终坐标，并将其加入哈希集合中。
3. 返回哈希集合的大小，即不同最终坐标的数量。

关键点:
- 使用前缀和可以快速计算任意子串的累计移动结果。
- 使用哈希集合来存储不同的最终坐标，确保不会重复计数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def distinct_points_reachable(s: str, k: int) -> int:
    """
    函数式接口 - 计算删除长度为 k 的子字符串后可到达的不同最终坐标的数量
    """
    n = len(s)
    # 计算前缀和
    prefix_sum = [(0, 0)]
    for char in s:
        x, y = prefix_sum[-1]
        if char == 'U':
            y += 1
        elif char == 'D':
            y -= 1
        elif char == 'L':
            x -= 1
        elif char == 'R':
            x += 1
        prefix_sum.append((x, y))
    
    # 存储不同的最终坐标
    unique_points = set()
    
    # 遍历字符串 s，计算删除每个长度为 k 的子字符串后的最终坐标
    for i in range(n - k + 1):
        # 计算删除子字符串后的最终坐标
        final_x, final_y = prefix_sum[i][0] + (prefix_sum[n][0] - prefix_sum[i + k][0]), prefix_sum[i][1] + (prefix_sum[n][1] - prefix_sum[i + k][1])
        unique_points.add((final_x, final_y))
    
    return len(unique_points)


Solution = create_solution(distinct_points_reachable)