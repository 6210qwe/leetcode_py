"""
629. K 个逆序对数组 - 对于一个整数数组 nums，逆序对是一对满足 0 <= i < j < nums.length 且 nums[i] > nums[j]的整数对 [i, j] 。 给你两个整数 n 和 k，找出所有包含从 1 到 n 的数字，且恰好拥有 k 个 逆序对 的不同的数组的个数。由于答案可能很大，只需要返回对 109 + 7 取余的结果。 示例 1： 输入：n = 3, k = 0 输出：1 解释： 只有数组 [1,2,3] 包含了从1到3的整数并且正好拥有 0 个逆序对。 示例 2： 输入：n = 3, k = 1 输出：2 解释： 数组 [1,3,2] 和 [2,1,3] 都有 1 个逆序对。 提示： * 1 <= n <= 1000 * 0 <= k <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示长度为 i 的数组中恰好有 j 个逆序对的数量。状态转移方程为 dp[i][j] = dp[i-1][j] + dp[i-1][j-1] + ... + dp[i-1][j-(i-1)]，其中 j-(i-1) >= 0。

算法步骤:
1. 初始化 dp 数组，dp[0][0] = 1。
2. 通过双重循环遍历 n 和 k，更新 dp 数组。
3. 返回 dp[n][k] % (10^9 + 7)。

关键点:
- 使用前缀和优化状态转移方程，减少计算量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)
空间复杂度: O(n * k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def k_inverse_pairs(n: int, k: int) -> int:
    """
    函数式接口 - 计算长度为 n 的数组中恰好有 k 个逆序对的数量
    """
    MOD = 10**9 + 7
    dp = [[0] * (k + 1) for _ in range(n + 1)]
    dp[0][0] = 1

    for i in range(1, n + 1):
        prefix_sum = 0
        for j in range(k + 1):
            prefix_sum += dp[i - 1][j]
            if j >= i:
                prefix_sum -= dp[i - 1][j - i]
            dp[i][j] = prefix_sum % MOD

    return dp[n][k]


Solution = create_solution(k_inverse_pairs)