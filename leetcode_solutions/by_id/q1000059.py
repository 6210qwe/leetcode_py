"""
LCP 14. 切分数组 - 给定一个整数数组 nums ，小李想将 nums 切割成若干个非空子数组，使得每个子数组最左边的数和最右边的数的最大公约数大于 1 。为了减少他的工作量，请求出最少可以切成多少个子数组。 示例 1： > 输入：nums = [2,3,3,2,3,3] > > 输出：2 > > 解释：最优切割为 [2,3,3,2] 和 [3,3] 。第一个子数组头尾数字的最大公约数为 2 ，第二个子数组头尾数字的最大公约数为 3 。 示例 2： > 输入：nums = [2,3,5,7] > > 输出：4 > > 解释：只有一种可行的切割：[2], [3], [5], [7] 限制： * 1 <= nums.length <= 10^5 * 2 <= nums[i] <= 10^6
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划结合质因数分解来解决问题。

算法步骤:
1. 对于每个数进行质因数分解，并记录每个质因数最后出现的位置。
2. 使用动态规划数组 dp 来记录到当前位置为止的最小切割次数。
3. 对于每个位置 i，更新 dp[i] 为从上一个相同质因数的位置 j 到 i 的最小切割次数加一。
4. 最终结果为 dp[n-1]。

关键点:
- 通过质因数分解和记录每个质因数最后出现的位置，可以快速找到可以合并的子数组。
- 动态规划数组 dp 用于记录到当前位置为止的最小切割次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * sqrt(m))，其中 n 是数组长度，m 是数组中最大值。
空间复杂度: O(n + m)，其中 n 是数组长度，m 是数组中最大值。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    n = len(nums)
    if n == 1:
        return 1

    # 质因数分解
    def prime_factors(x):
        factors = set()
        d = 2
        while d * d <= x:
            while (x % d) == 0:
                factors.add(d)
                x //= d
            d += 1
        if x > 1:
            factors.add(x)
        return factors

    # 记录每个质因数最后出现的位置
    last_pos = {}
    for i, num in enumerate(nums):
        for factor in prime_factors(num):
            last_pos[factor] = i

    # 动态规划数组
    dp = [float('inf')] * n
    dp[0] = 1

    for i in range(1, n):
        dp[i] = dp[i - 1] + 1
        for factor in prime_factors(nums[i]):
            if last_pos[factor] < i:
                dp[i] = min(dp[i], dp[last_pos[factor]] + 1)

    return dp[-1]


Solution = create_solution(solution_function_name)