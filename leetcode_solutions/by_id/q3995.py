"""
3658. 奇数和与偶数和的最大公约数 - 给你一个整数 n。请你计算以下两个值的 最大公约数（GCD）： * sumOdd：最小的 n 个正奇数的总和。 * sumEven：最小的 n 个正偶数的总和。 返回 sumOdd 和 sumEven 的 GCD。 示例 1： 输入： n = 4 输出： 4 解释： * 前 4 个奇数的总和 sumOdd = 1 + 3 + 5 + 7 = 16 * 前 4 个偶数的总和 sumEven = 2 + 4 + 6 + 8 = 20 因此，GCD(sumOdd, sumEven) = GCD(16, 20) = 4。 示例 2： 输入： n = 5 输出： 5 解释： * 前 5 个奇数的总和 sumOdd = 1 + 3 + 5 + 7 + 9 = 25 * 前 5 个偶数的总和 sumEven = 2 + 4 + 6 + 8 + 10 = 30 因此，GCD(sumOdd, sumEven) = GCD(25, 30) = 5。 提示： * 1 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 利用数学公式直接计算前 n 个奇数和偶数的总和，并使用欧几里得算法计算最大公约数。

算法步骤:
1. 计算前 n 个奇数的总和 sumOdd。
2. 计算前 n 个偶数的总和 sumEven。
3. 使用欧几里得算法计算 sumOdd 和 sumEven 的最大公约数。

关键点:
- 前 n 个奇数的总和为 n^2。
- 前 n 个偶数的总和为 n * (n + 1)。
- 欧几里得算法用于计算两个数的最大公约数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log(min(a, b)))，其中 a 和 b 是 sumOdd 和 sumEven。欧几里得算法的时间复杂度是 O(log(min(a, b)))。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def gcd_of_odd_and_even_sums(n: int) -> int:
    """
    计算前 n 个奇数和偶数的总和的最大公约数。
    """
    # 计算前 n 个奇数的总和
    sum_odd = n * n
    # 计算前 n 个偶数的总和
    sum_even = n * (n + 1)
    
    # 使用欧几里得算法计算最大公约数
    def gcd(a: int, b: int) -> int:
        while b:
            a, b = b, a % b
        return a
    
    return gcd(sum_odd, sum_even)


