# 问题描述
# ============================================================================
"""
888. 公平的糖果交换 - 爱丽丝和鲍勃拥有不同总数量的糖果。给你两个数组 aliceSizes 和 bobSizes ，aliceSizes[i] 是爱丽丝拥有的第 i 盒糖果中的糖果数量，bobSizes[j] 是鲍勃拥有的第 j 盒糖果中的糖果数量。 两人想要互相交换一盒糖果，这样在交换之后，他们就可以拥有相同总数量的糖果。一个人拥有的糖果总数量是他们每盒糖果数量的总和。 返回一个整数数组 answer，其中 answer[0] 是爱丽丝必须交换的糖果盒中的糖果的数目，answer[1] 是鲍勃必须交换的糖果盒中的糖果的数目。如果存在多个答案，你可以返回其中 任何一个 。题目测试用例保证存在与输入对应的答案。 示例 1： 输入：aliceSizes = [1,1], bobSizes = [2,2] 输出：[1,2] 示例 2： 输入：aliceSizes = [1,2], bobSizes = [2,3] 输出：[1,2] 示例 3： 输入：aliceSizes = [2], bobSizes = [1,3] 输出：[2,3] 示例 4： 输入：aliceSizes = [1,2,5], bobSizes = [2,4] 输出：[5,4] 提示： * 1 <= aliceSizes.length, bobSizes.length <= 104 * 1 <= aliceSizes[i], bobSizes[j] <= 105 * 爱丽丝和鲍勃的糖果总数量不同。 * 题目数据保证对于给定的输入至少存在一个有效答案。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来记录 Bob 的糖果数量，并通过数学公式找到需要交换的糖果。

算法步骤:
1. 计算 Alice 和 Bob 的糖果总数。
2. 计算两者的糖果总数之差的一半。
3. 将 Bob 的糖果数量存入集合中。
4. 遍历 Alice 的糖果，检查是否存在一个 Bob 的糖果使得两者交换后糖果总数相等。

关键点:
- 使用集合来存储 Bob 的糖果数量，以便快速查找。
- 通过数学公式 (Alice 总数 + Bob 总数) / 2 来计算目标糖果总数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 aliceSizes 的长度，m 是 bobSizes 的长度。
空间复杂度: O(m)，用于存储 Bob 的糖果数量集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def fair_candy_swap(aliceSizes: List[int], bobSizes: List[int]) -> List[int]:
    """
    函数式接口 - 实现公平的糖果交换
    """
    # 计算 Alice 和 Bob 的糖果总数
    sum_alice = sum(aliceSizes)
    sum_bob = sum(bobSizes)
    
    # 计算两者的糖果总数之差的一半
    diff = (sum_alice - sum_bob) // 2
    
    # 将 Bob 的糖果数量存入集合中
    bob_set = set(bobSizes)
    
    # 遍历 Alice 的糖果，检查是否存在一个 Bob 的糖果使得两者交换后糖果总数相等
    for a in aliceSizes:
        if a - diff in bob_set:
            return [a, a - diff]

Solution = create_solution(fair_candy_swap)