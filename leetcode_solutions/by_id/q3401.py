"""
核心思想: 通过询问每一位的值来确定目标数字。

算法步骤:
1. 从最高位到最低位依次询问每一位的值。
2. 根据询问结果逐步构建目标数字。

关键点:
- 通过二进制表示法逐位确定目标数字。
- 每次询问可以确定一位的值。
"""

