def solution_function_name(params):
    """
    函数式接口 - 通过询问每一位的值来确定目标数字。
    """
    # 定义一个辅助函数来询问第 i 位的值
    def ask(i: int) -> bool:
        return params[i]

    # 确定目标数字的范围
    max_num = (1 << 30) - 1  # 最大可能的数字是 2^30 - 1

    # 从最高位到最低位依次询问每一位的值
    result = 0
    for i in range(30, -1, -1):
        if ask(i):
            result |= (1 << i)

    return result


