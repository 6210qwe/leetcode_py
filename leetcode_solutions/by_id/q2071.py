"""
核心思想: 使用双指针法来找到两个排序数组的最长公共子序列。

算法步骤:
1. 初始化两个指针 i 和 j 分别指向两个数组的起始位置。
2. 遍历两个数组，比较当前指针所指向的元素：
   - 如果相等，则将该元素加入结果列表，并移动两个指针。
   - 如果不相等，则移动指向较小元素的指针。
3. 最终结果列表即为最长公共子序列。

关键点:
- 由于数组是排序的，可以利用这一点通过双指针法高效地找到最长公共子序列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 和 m 分别是两个数组的长度。
空间复杂度: O(1)，除了结果列表外，只使用了常数级的额外空间。
"""

# ============================================================================
