"""
核心思想: 使用滑动窗口和动态规划来解决这个问题。我们首先计算每个可能的线段的最大奖品数，然后使用动态规划找到两个不重叠线段的最大奖品数。

算法步骤:
1. 计算每个可能的线段的最大奖品数。
2. 使用动态规划找到两个不重叠线段的最大奖品数。

关键点:
- 使用滑动窗口来计算每个可能的线段的最大奖品数。
- 使用动态规划来找到两个不重叠线段的最大奖品数。
"""

# ============================================================================
# 复杂度分析
