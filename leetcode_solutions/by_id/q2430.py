"""
2354. 优质数对的数目 - 给你一个下标从 0 开始的正整数数组 nums 和一个正整数 k 。 如果满足下述条件，则数对 (num1, num2) 是 优质数对 ： * num1 和 num2 都 在数组 nums 中存在。 * num1 OR num2 和 num1 AND num2 的二进制表示中值为 1 的位数之和大于等于 k ，其中 OR 是按位 或 操作，而 AND 是按位 与 操作。 返回 不同 优质数对的数目。 如果 a != c 或者 b != d ，则认为 (a, b) 和 (c, d) 是不同的两个数对。例如，(1, 2) 和 (2, 1) 不同。 注意：如果 num1 在数组中至少出现 一次 ，则满足 num1 == num2 的数对 (num1, num2) 也可以是优质数对。 示例 1： 输入：nums = [1,2,3,1], k = 3 输出：5 解释：有如下几个优质数对： - (3, 3)：(3 AND 3) 和 (3 OR 3) 的二进制表示都等于 (11) 。值为 1 的位数和等于 2 + 2 = 4 ，大于等于 k = 3 。 - (2, 3) 和 (3, 2)： (2 AND 3) 的二进制表示等于 (10) ，(2 OR 3) 的二进制表示等于 (11) 。值为 1 的位数和等于 1 + 2 = 3 。 - (1, 3) 和 (3, 1)： (1 AND 3) 的二进制表示等于 (01) ，(1 OR 3) 的二进制表示等于 (11) 。值为 1 的位数和等于 1 + 2 = 3 。 所以优质数对的数目是 5 。 示例 2： 输入：nums = [5,1,1], k = 10 输出：0 解释：该数组中不存在优质数对。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109 * 1 <= k <= 60
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想:
- 使用哈希表记录每个数字的二进制表示中 1 的个数。
- 利用排序和双指针方法来计算符合条件的数对。

算法步骤:
1. 计算每个数字的二进制表示中 1 的个数，并去重。
2. 对这些 1 的个数进行排序。
3. 使用双指针方法遍历排序后的列表，计算符合条件的数对数量。

关键点:
- 去重后计算每个数字的二进制表示中 1 的个数。
- 使用双指针方法高效地计算符合条件的数对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度。主要的时间开销在于排序操作。
空间复杂度: O(n)，用于存储每个数字的二进制表示中 1 的个数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_excellent_pairs(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算优质数对的数量
    """
    # 计算每个数字的二进制表示中 1 的个数，并去重
    bit_counts = [bin(num).count('1') for num in set(nums)]
    
    # 对 1 的个数进行排序
    bit_counts.sort()
    
    # 使用双指针方法计算符合条件的数对数量
    left, right = 0, len(bit_counts) - 1
    result = 0
    
    while left <= right:
        if bit_counts[left] + bit_counts[right] >= k:
            result += (right - left) * 2 + 1
            right -= 1
        else:
            left += 1
    
    return result


Solution = create_solution(count_excellent_pairs)