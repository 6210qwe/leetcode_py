"""
2843. 统计对称整数的数目 - 给你两个正整数 low 和 high 。 对于一个由 2 * n 位数字组成的整数 x ，如果其前 n 位数字之和与后 n 位数字之和相等，则认为这个数字是一个对称整数。 返回在 [low, high] 范围内的 对称整数的数目 。 示例 1： 输入：low = 1, high = 100 输出：9 解释：在 1 到 100 范围内共有 9 个对称整数：11、22、33、44、55、66、77、88 和 99 。 示例 2： 输入：low = 1200, high = 1230 输出：4 解释：在 1200 到 1230 范围内共有 4 个对称整数：1203、1212、1221 和 1230 。 提示： * 1 <= low <= high <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 枚举所有可能的对称整数，并检查它们是否在给定范围内。

算法步骤:
1. 生成所有可能的对称整数。
2. 检查每个对称整数是否在 [low, high] 范围内。
3. 统计符合条件的对称整数的数量。

关键点:
- 生成对称整数时，可以利用数学方法直接构造。
- 只考虑 2 位和 4 位的对称整数，因为 10^4 以内的对称整数最多只有 4 位。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
