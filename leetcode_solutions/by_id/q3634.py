"""
3412. 计算字符串的镜像分数 - 给你一个字符串 s。 英文字母中每个字母的 镜像 定义为反转字母表之后对应位置上的字母。例如，'a' 的镜像是 'z'，'y' 的镜像是 'b'。 最初，字符串 s 中的所有字符都 未标记 。 字符串 s 的初始分数为 0 ，你需要对其执行以下过程： * 从左到右遍历字符串。 * 对于每个下标 i ，找到距离最近的 未标记 下标 j，下标 j 需要满足 j < i 且 s[j] 是 s[i] 的镜像。然后 标记 下标 i 和 j，总分加上 i - j 的值。 * 如果对于下标 i，不存在满足条件的下标 j，则跳过该下标，继续处理下一个下标，不需要进行标记。 返回最终的总分。 示例 1： 输入： s = "aczzx" 输出： 5 解释： * i = 0。没有符合条件的下标 j，跳过。 * i = 1。没有符合条件的下标 j，跳过。 * i = 2。距离最近的符合条件的下标是 j = 0，因此标记下标 0 和 2，然后将总分加上 2 - 0 = 2 。 * i = 3。没有符合条件的下标 j，跳过。 * i = 4。距离最近的符合条件的下标是 j = 1，因此标记下标 1 和 4，然后将总分加上 4 - 1 = 3 。 示例 2： 输入： s = "abcdef" 输出： 0 解释： 对于每个下标 i，都不存在满足条件的下标 j。 提示： * 1 <= s.length <= 105 * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个字符的镜像字符的位置，从左到右遍历字符串，查找每个字符的镜像字符的最近位置。

算法步骤:
1. 初始化一个哈希表，用于记录每个字符的镜像字符的位置。
2. 从左到右遍历字符串，对于每个字符，查找其镜像字符的最近位置。
3. 如果找到镜像字符的位置，更新总分并标记该位置。
4. 更新哈希表中当前字符的位置。

关键点:
- 使用哈希表记录每个字符的镜像字符的位置，以便快速查找。
- 从左到右遍历字符串，确保找到的是最近的镜像字符位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，因为哈希表的大小是固定的（26 个字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_mirror_score(s: str) -> int:
    """
    计算字符串的镜像分数
    """
    # 镜像字符映射
    mirror_map = {chr(i): chr(219 - i) for i in range(97, 123)}
    
    # 哈希表记录每个字符的镜像字符的位置
    char_positions = {}
    score = 0
    
    for i, char in enumerate(s):
        mirror_char = mirror_map[char]
        if mirror_char in char_positions:
            score += i - char_positions[mirror_char]
            del char_positions[mirror_char]
        else:
            char_positions[char] = i
    
    return score


Solution = create_solution(find_mirror_score)