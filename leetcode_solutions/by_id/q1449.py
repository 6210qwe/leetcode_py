"""
1324. 竖直打印单词 - 给你一个字符串 s。请你按照单词在 s 中的出现顺序将它们全部竖直返回。 单词应该以字符串列表的形式返回，必要时用空格补位，但输出尾部的空格需要删除（不允许尾随空格）。 每个单词只能放在一列上，每一列中也只能有一个单词。 示例 1： 输入：s = "HOW ARE YOU" 输出：["HAY","ORO","WEU"] 解释：每个单词都应该竖直打印。 "HAY" "ORO" "WEU" 示例 2： 输入：s = "TO BE OR NOT TO BE" 输出：["TBONTB","OEROOE"," T"] 解释：题目允许使用空格补位，但不允许输出末尾出现空格。 "TBONTB" "OEROOE" " T" 示例 3： 输入：s = "CONTEST IS COMING" 输出：["CIC","OSO","N M","T I","E N","S G","T"] 提示： * 1 <= s.length <= 200 * s 仅含大写英文字母。 * 题目数据保证两个单词之间只有一个空格。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将字符串按单词分割，然后逐字符构建结果。

算法步骤:
1. 将输入字符串按空格分割成单词列表。
2. 找到最长单词的长度，确定结果列表的行数。
3. 逐字符构建结果列表，不足的部分用空格补位。
4. 去除每行末尾的多余空格。

关键点:
- 使用 zip_longest 来处理不同长度的单词，并用空格补位。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是单词数量，m 是最长单词的长度。
空间复杂度: O(n * m)，存储结果所需的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from itertools import zip_longest

def print_words_vertically(s: str) -> List[str]:
    """
    函数式接口 - 实现竖直打印单词
    """
    # 将字符串按空格分割成单词列表
    words = s.split()
    
    # 使用 zip_longest 处理不同长度的单词，并用空格补位
    result = [''.join(char).rstrip() for char in zip_longest(*words, fillvalue=' ')]
    
    return result

