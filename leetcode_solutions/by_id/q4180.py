"""
核心思想: 使用广度优先搜索 (BFS) 来计算每个节点到目标节点 x, y, z 的距离。然后检查这些距离是否构成勾股数元组。

算法步骤:
1. 构建图的邻接表表示。
2. 使用 BFS 计算每个节点到目标节点 x, y, z 的距离。
3. 对于每个节点，检查其到 x, y, z 的距离是否构成勾股数元组。
4. 统计并返回特殊节点的数量。

关键点:
- 使用 BFS 确保每个节点到目标节点的距离是最短路径。
- 通过排序和勾股定理检查来确定特殊节点。
"""

