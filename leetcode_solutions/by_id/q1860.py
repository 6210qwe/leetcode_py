"""
1738. 找出第 K 大的异或坐标值 - 给你一个二维矩阵 matrix 和一个整数 k ，矩阵大小为 m x n 由非负整数组成。 矩阵中坐标 (a, b) 的 目标值 可以通过对所有元素 matrix[i][j] 执行异或运算得到，其中 i 和 j 满足 0 <= i <= a < m 且 0 <= j <= b < n（下标从 0 开始计数）。 请你找出 matrix 的所有坐标中第 k 大的目标值（k 的值从 1 开始计数）。 示例 1： 输入：matrix = [[5,2],[1,6]], k = 1 输出：7 解释：坐标 (0,1) 的目标值是 5 XOR 2 = 7 ，为最大的目标值。 示例 2： 输入：matrix = [[5,2],[1,6]], k = 2 输出：5 解释：坐标 (0,0) 的目标值是 5 = 5 ，为第 2 大的目标值。 示例 3： 输入：matrix = [[5,2],[1,6]], k = 3 输出：4 解释：坐标 (1,0) 的目标值是 5 XOR 1 = 4 ，为第 3 大的目标值。 示例 4： 输入：matrix = [[5,2],[1,6]], k = 4 输出：0 解释：坐标 (1,1) 的目标值是 5 XOR 2 XOR 1 XOR 6 = 0 ，为第 4 大的目标值。 提示： * m == matrix.length * n == matrix[i].length * 1 <= m, n <= 1000 * 0 <= matrix[i][j] <= 106 * 1 <= k <= m * n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀异或数组来计算每个坐标的异或值，并使用最小堆来找到第 k 大的异或值。

算法步骤:
1. 计算前缀异或数组。
2. 遍历所有坐标，计算每个坐标的异或值，并将其加入最小堆。
3. 保持堆的大小为 k，最后堆顶元素即为第 k 大的异或值。

关键点:
- 前缀异或数组可以高效地计算任意子矩阵的异或值。
- 使用最小堆来维护前 k 大的异或值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n * log k)，其中 m 和 n 分别是矩阵的行数和列数，k 是需要找到的第 k 大的值。
空间复杂度: O(m * n + k)，前缀异或数组的空间复杂度为 O(m * n)，堆的空间复杂度为 O(k)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def kth_largest_xor_coordinate_value(matrix: List[List[int]], k: int) -> int:
    m, n = len(matrix), len(matrix[0])
    
    # 计算前缀异或数组
    prefix_xor = [[0] * (n + 1) for _ in range(m + 1)]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            prefix_xor[i][j] = prefix_xor[i-1][j] ^ prefix_xor[i][j-1] ^ prefix_xor[i-1][j-1] ^ matrix[i-1][j-1]
    
    # 使用最小堆来找到第 k 大的异或值
    min_heap = []
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            value = prefix_xor[i][j]
            if len(min_heap) < k:
                heapq.heappush(min_heap, value)
            elif value > min_heap[0]:
                heapq.heappushpop(min_heap, value)
    
    return min_heap[0]

