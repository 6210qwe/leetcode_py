"""
2391. 收集垃圾的最少总时间 - 给你一个下标从 0 开始的字符串数组 garbage ，其中 garbage[i] 表示第 i 个房子的垃圾集合。garbage[i] 只包含字符 'M' ，'P' 和 'G' ，但可能包含多个相同字符，每个字符分别表示一单位的金属、纸和玻璃。垃圾车收拾 一 单位的任何一种垃圾都需要花费 1 分钟。 同时给你一个下标从 0 开始的整数数组 travel ，其中 travel[i] 是垃圾车从房子 i 行驶到房子 i + 1 需要的分钟数。 城市里总共有三辆垃圾车，分别收拾三种垃圾。每辆垃圾车都从房子 0 出发，按顺序 到达每一栋房子。但它们 不是必须 到达所有的房子。 任何时刻只有 一辆 垃圾车处在使用状态。当一辆垃圾车在行驶或者收拾垃圾的时候，另外两辆车 不能 做任何事情。 请你返回收拾完所有垃圾需要花费的 最少 总分钟数。 示例 1： 输入：garbage = ["G","P","GP","GG"], travel = [2,4,3] 输出：21 解释： 收拾纸的垃圾车： 1. 从房子 0 行驶到房子 1 2. 收拾房子 1 的纸垃圾 3. 从房子 1 行驶到房子 2 4. 收拾房子 2 的纸垃圾 收拾纸的垃圾车总共花费 8 分钟收拾完所有的纸垃圾。 收拾玻璃的垃圾车： 1. 收拾房子 0 的玻璃垃圾 2. 从房子 0 行驶到房子 1 3. 从房子 1 行驶到房子 2 4. 收拾房子 2 的玻璃垃圾 5. 从房子 2 行驶到房子 3 6. 收拾房子 3 的玻璃垃圾 收拾玻璃的垃圾车总共花费 13 分钟收拾完所有的玻璃垃圾。 由于没有金属垃圾，收拾金属的垃圾车不需要花费任何时间。 所以总共花费 8 + 13 = 21 分钟收拾完所有垃圾。 示例 2： 输入：garbage = ["MMM","PGM","GP"], travel = [3,10] 输出：37 解释： 收拾金属的垃圾车花费 7 分钟收拾完所有的金属垃圾。 收拾纸的垃圾车花费 15 分钟收拾完所有的纸垃圾。 收拾玻璃的垃圾车花费 15 分钟收拾完所有的玻璃垃圾。 总共花费 7 + 15 + 15 = 37 分钟收拾完所有的垃圾。 提示： * 2 <= garbage.length <= 105 * garbage[i] 只包含字母 'M' ，'P' 和 'G' 。 * 1 <= garbage[i].length <= 10 * travel.length == garbage.length - 1 * 1 <= travel[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来计算每种垃圾车的行驶时间和收集时间。

算法步骤:
1. 计算每种垃圾最后出现的房子索引。
2. 计算前缀和数组，用于快速计算任意两个房子之间的行驶时间。
3. 遍历每种垃圾，计算其收集时间和行驶时间。

关键点:
- 使用前缀和来快速计算行驶时间。
- 只计算每种垃圾最后出现的房子之前的行驶时间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 garbage 的长度。我们需要遍历 garbage 和 travel 数组。
空间复杂度: O(1)，除了输入和输出外，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def minimum_amount_of_time_to_collect_garbage(garbage: List[str], travel: List[int]) -> int:
    # 计算每种垃圾最后出现的房子索引
    last_M, last_P, last_G = -1, -1, -1
    for i, g in enumerate(garbage):
        if 'M' in g:
            last_M = i
        if 'P' in g:
            last_P = i
        if 'G' in g:
            last_G = i
    
    # 计算前缀和数组
    prefix_sum = [0]
    for t in travel:
        prefix_sum.append(prefix_sum[-1] + t)
    
    # 计算总时间
    total_time = 0
    for i, g in enumerate(garbage):
        total_time += len(g)  # 收集时间
        if i <= last_M:
            total_time += prefix_sum[i] - prefix_sum[last_M]
        if i <= last_P:
            total_time += prefix_sum[i] - prefix_sum[last_P]
        if i <= last_G:
            total_time += prefix_sum[i] - prefix_sum[last_G]
    
    return total_time

