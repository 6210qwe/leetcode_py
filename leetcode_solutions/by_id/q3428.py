from typing import List

def find_xor_of_numbers_which_appear_twice(nums: List[int]) -> int:
    """
    函数式接口 - 计算数组中所有出现两次数字的按位 XOR 值
    """
    # 使用哈希表记录每个数字的出现次数
    count = {}
    for num in nums:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    # 计算出现两次的数字的 XOR 值
    result = 0
    for num, freq in count.items():
        if freq == 2:
            result ^= num
    
    return result

