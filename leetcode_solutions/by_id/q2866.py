"""
2760. 最长奇偶子数组 - 给你一个下标从 0 开始的整数数组 nums 和一个整数 threshold 。 请你从 nums 的子数组中找出以下标 l 开头、下标 r 结尾 (0 <= l <= r < nums.length) 且满足以下条件的 最长子数组 ： * nums[l] % 2 == 0 * 对于范围 [l, r - 1] 内的所有下标 i ，nums[i] % 2 != nums[i + 1] % 2 * 对于范围 [l, r] 内的所有下标 i ，nums[i] <= threshold 以整数形式返回满足题目要求的最长子数组的长度。 注意：子数组 是数组中的一个连续非空元素序列。 示例 1： 输入：nums = [3,2,5,4], threshold = 5 输出：3 解释：在这个示例中，我们选择从 l = 1 开始、到 r = 3 结束的子数组 => [2,5,4] ，满足上述条件。 因此，答案就是这个子数组的长度 3 。可以证明 3 是满足题目要求的最大长度。 示例 2： 输入：nums = [1,2], threshold = 2 输出：1 解释： 在这个示例中，我们选择从 l = 1 开始、到 r = 1 结束的子数组 => [2] 。 该子数组满足上述全部条件。可以证明 1 是满足题目要求的最大长度。 示例 3： 输入：nums = [2,3,4,5], threshold = 4 输出：3 解释： 在这个示例中，我们选择从 l = 0 开始、到 r = 2 结束的子数组 => [2,3,4] 。 该子数组满足上述全部条件。 因此，答案就是这个子数组的长度 3 。可以证明 3 是满足题目要求的最大长度。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100 * 1 <= threshold <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到满足条件的最长子数组。

算法步骤:
1. 初始化两个指针 `left` 和 `right`，分别表示当前窗口的左边界和右边界。
2. 遍历数组，使用 `right` 指针扩展窗口。
3. 检查当前窗口是否满足条件：
   - 窗口的第一个元素是偶数。
   - 窗口内的元素交替出现奇数和偶数。
   - 窗口内的所有元素都小于等于 `threshold`。
4. 如果不满足条件，移动 `left` 指针缩小窗口，直到满足条件。
5. 更新最长子数组的长度。

关键点:
- 使用滑动窗口来高效地找到满足条件的最长子数组。
- 通过调整 `left` 和 `right` 指针来维护窗口的条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个元素最多被访问两次（一次由 `right` 指针，一次由 `left` 指针）。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_even_odd_subarray_with_threshold(nums: List[int], threshold: int) -> int:
    """
    函数式接口 - 找到满足条件的最长子数组的长度
    """
    n = len(nums)
    max_length = 0
    left = 0

    for right in range(n):
        # 检查当前窗口是否满足条件
        if (nums[left] % 2 == 0 and
                all(nums[i] % 2 != nums[i + 1] % 2 for i in range(left, right)) and
                all(nums[i] <= threshold for i in range(left, right + 1))):
            max_length = max(max_length, right - left + 1)
        else:
            left = right

    return max_length


