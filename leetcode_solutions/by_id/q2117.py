"""
2007. 从双倍数组中还原原数组 - 一个整数数组 original 可以转变成一个 双倍 数组 changed ，转变方式为将 original 中每个元素 值乘以 2 加入数组中，然后将所有元素 随机打乱 。 给你一个数组 changed ，如果 change 是 双倍 数组，那么请你返回 original数组，否则请返回空数组。original 的元素可以以 任意 顺序返回。 示例 1： 输入：changed = [1,3,4,2,6,8] 输出：[1,3,4] 解释：一个可能的 original 数组为 [1,3,4] : - 将 1 乘以 2 ，得到 1 * 2 = 2 。 - 将 3 乘以 2 ，得到 3 * 2 = 6 。 - 将 4 乘以 2 ，得到 4 * 2 = 8 。 其他可能的原数组方案为 [4,3,1] 或者 [3,1,4] 。 示例 2： 输入：changed = [6,3,0,1] 输出：[] 解释：changed 不是一个双倍数组。 示例 3： 输入：changed = [1] 输出：[] 解释：changed 不是一个双倍数组。 提示： * 1 <= changed.length <= 105 * 0 <= changed[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个元素的频率，然后从小到大处理每个元素，确保每个元素和其两倍都存在且数量匹配。

算法步骤:
1. 如果数组长度为奇数，直接返回空数组。
2. 使用哈希表记录每个元素的频率。
3. 对数组进行排序。
4. 遍历排序后的数组，对于每个元素：
   - 如果该元素在哈希表中的频率为0，跳过。
   - 检查该元素的两倍是否在哈希表中且频率大于0，如果不是，返回空数组。
   - 否则，将该元素加入结果数组，并更新哈希表中该元素及其两倍的频率。
5. 返回结果数组。

关键点:
- 通过排序确保先处理较小的元素。
- 使用哈希表记录频率，确保每个元素及其两倍的数量匹配。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 changed 的长度。主要由排序操作决定。
空间复杂度: O(n)，用于存储哈希表和结果数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_original_array(changed: List[int]) -> List[int]:
    """
    函数式接口 - 从双倍数组中还原原数组
    """
    if len(changed) % 2 != 0:
        return []

    from collections import Counter
    count = Counter(changed)
    changed.sort()

    original = []
    for num in changed:
        if count[num] == 0:
            continue
        if count[2 * num] == 0:
            return []
        original.append(num)
        count[num] -= 1
        count[2 * num] -= 1

    return original


Solution = create_solution(find_original_array)