"""
3537. 填充特殊网格 - 给你一个非负整数 N，表示一个 2N x 2N 的网格。你需要用从 0 到 2^(2N) - 1 的整数填充网格，使其成为一个 特殊 网格。一个网格当且仅当满足以下 所有 条件时，才能称之为 特殊 网格： * 右上角象限中的所有数字都小于右下角象限中的所有数字。 * 右下角象限中的所有数字都小于左下角象限中的所有数字。 * 左下角象限中的所有数字都小于左上角象限中的所有数字。 * 每个象限也都是一个特殊网格。 返回一个 2N x 2N 的特殊网格。 注意：任何 1x1 的网格都是特殊网格。 示例 1： 输入： N = 0 输出： [[0]] 解释： 唯一可以放置的数字是 0，并且网格中只有一个位置。 示例 2： 输入： N = 1 输出： [[3,0],[2,1]] 解释： 每个象限的数字如下： * 右上角：0 * 右下角：1 * 左下角：2 * 左上角：3 由于 0 < 1 < 2 < 3，该网格满足给定的约束条件。 示例 3： 输入： N = 2 输出： [[15,12,3,0],[14,13,2,1],[11,8,7,4],[10,9,6,5]] 解释： [https://pic.leetcode.cn/1746289512-jpANZH-4123example3p1drawio.png] 每个象限的数字如下： * 右上角：3, 0, 2, 1 * 右下角：7, 4, 6, 5 * 左下角：11, 8, 10, 9 * 左上角：15, 12, 14, 13 * max(3, 0, 2, 1) < min(7, 4, 6, 5) * max(7, 4, 6, 5) < min(11, 8, 10, 9) * max(11, 8, 10, 9) < min(15, 12, 14, 13) 这满足前三个要求。此外，每个象限也是一个特殊网格。因此，这是一个特殊网格。 提示： * 0 <= N <= 10
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用递归和分治法来填充网格。

算法步骤:
1. 定义一个递归函数 `fill_grid`，用于填充子网格。
2. 在 `fill_grid` 函数中，根据当前子网格的位置（右上、右下、左下、左上）来确定填充的起始值。
3. 递归调用 `fill_grid` 函数来填充四个子网格。
4. 最终返回完整的网格。

关键点:
- 递归地将大网格分解为四个子网格。
- 根据子网格的位置确定填充的起始值。
- 递归终止条件是子网格大小为 1x1。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(4^N)，因为每次递归调用都会将网格分为四个子网格。
空间复杂度: O(4^N)，递归调用栈的深度为 2N。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def fill_special_grid(N: int) -> List[List[int]]:
    def fill_grid(grid, start, end, top_left, top_right, bottom_left, bottom_right):
        if start == end:
            return
        
        mid = (start + end) // 2
        # 填充右上角
        fill_grid(grid, start, mid, top_left, (top_left + top_right) // 2, (bottom_left + bottom_right) // 2, bottom_right)
        # 填充右下角
        fill_grid(grid, mid + 1, end, (top_left + top_right) // 2 + 1, top_right, bottom_left, (bottom_left + bottom_right) // 2)
        # 填充左下角
        fill_grid(grid, mid + 1, end, (bottom_left + bottom_right) // 2 + 1, bottom_right, (top_left + top_right) // 2 + 1, (bottom_left + bottom_right) // 2)
        # 填充左上角
        fill_grid(grid, start, mid, (top_left + top_right) // 2 + 1, top_right, (bottom_left + bottom_right) // 2 + 1, bottom_right)

    size = 2 ** N
    grid = [[0] * size for _ in range(size)]
    fill_grid(grid, 0, size * size - 1, 0, size * size - 1, 0, size * size - 1)
    return grid

