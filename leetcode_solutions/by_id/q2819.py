"""
2710. 移除字符串中的尾随零 - 给你一个用字符串表示的正整数 num ，请你以字符串形式返回不含尾随零的整数 num 。 示例 1： 输入：num = "51230100" 输出："512301" 解释：整数 "51230100" 有 2 个尾随零，移除并返回整数 "512301" 。 示例 2： 输入：num = "123" 输出："123" 解释：整数 "123" 不含尾随零，返回整数 "123" 。 提示： * 1 <= num.length <= 1000 * num 仅由数字 0 到 9 组成 * num 不含前导零
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从字符串末尾开始遍历，找到第一个非零字符的位置，然后截取该位置之前的子字符串。

算法步骤:
1. 从字符串末尾开始向前遍历。
2. 找到第一个非零字符的位置。
3. 截取从开头到该位置的子字符串并返回。

关键点:
- 从后向前遍历可以快速找到第一个非零字符的位置。
- 使用切片操作截取子字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。最坏情况下需要遍历整个字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_trailing_zeros(num: str) -> str:
    """
    函数式接口 - 移除字符串中的尾随零
    """
    # 从字符串末尾开始向前遍历
    i = len(num) - 1
    while i >= 0 and num[i] == '0':
        i -= 1
    # 截取从开头到第一个非零字符位置的子字符串
    return num[:i + 1]


Solution = create_solution(remove_trailing_zeros)