"""
1984. 学生分数的最小差值 - 给你一个 下标从 0 开始 的整数数组 nums ，其中 nums[i] 表示第 i 名学生的分数。另给你一个整数 k 。 从数组中选出任意 k 名学生的分数，使这 k 个分数间 最高分 和 最低分 的 差值 达到 最小化 。 返回可能的 最小差值 。 示例 1： 输入：nums = [90], k = 1 输出：0 解释：选出 1 名学生的分数，仅有 1 种方法： - [90] 最高分和最低分之间的差值是 90 - 90 = 0 可能的最小差值是 0 示例 2： 输入：nums = [9,4,1,7], k = 2 输出：2 解释：选出 2 名学生的分数，有 6 种方法： - [9,4,1,7] 最高分和最低分之间的差值是 9 - 4 = 5 - [9,4,1,7] 最高分和最低分之间的差值是 9 - 1 = 8 - [9,4,1,7] 最高分和最低分之间的差值是 9 - 7 = 2 - [9,4,1,7] 最高分和最低分之间的差值是 4 - 1 = 3 - [9,4,1,7] 最高分和最低分之间的差值是 7 - 4 = 3 - [9,4,1,7] 最高分和最低分之间的差值是 7 - 1 = 6 可能的最小差值是 2 提示： * 1 <= k <= nums.length <= 1000 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 先对数组进行排序，然后使用滑动窗口找到最小的 k 个连续元素的最大值和最小值的差值。

算法步骤:
1. 对数组进行排序。
2. 使用滑动窗口遍历排序后的数组，找到 k 个连续元素的最大值和最小值的差值。
3. 记录并返回最小的差值。

关键点:
- 排序后，滑动窗口可以有效地找到 k 个连续元素的最大值和最小值的差值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度，排序的时间复杂度为 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_difference(nums: List[int], k: int) -> int:
    """
    函数式接口 - 找出 k 个连续元素的最大值和最小值的最小差值
    """
    if k == 1 or not nums:
        return 0

    # 对数组进行排序
    nums.sort()

    # 初始化最小差值
    min_diff = float('inf')

    # 使用滑动窗口找到最小的 k 个连续元素的最大值和最小值的差值
    for i in range(len(nums) - k + 1):
        current_diff = nums[i + k - 1] - nums[i]
        min_diff = min(min_diff, current_diff)

    return min_diff


Solution = create_solution(minimum_difference)