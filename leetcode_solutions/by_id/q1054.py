"""
1009. 十进制整数的反码 - 每个非负整数 N 都有其二进制表示。例如， 5 可以被表示为二进制 "101"，11 可以用二进制 "1011" 表示，依此类推。注意，除 N = 0 外，任何二进制表示中都不含前导零。 二进制的反码表示是将每个 1 改为 0 且每个 0 变为 1。例如，二进制数 "101" 的二进制反码为 "010"。 给你一个十进制数 N，请你返回其二进制表示的反码所对应的十进制整数。 示例 1： 输入：5 输出：2 解释：5 的二进制表示为 "101"，其二进制反码为 "010"，也就是十进制中的 2 。 示例 2： 输入：7 输出：0 解释：7 的二进制表示为 "111"，其二进制反码为 "000"，也就是十进制中的 0 。 示例 3： 输入：10 输出：5 解释：10 的二进制表示为 "1010"，其二进制反码为 "0101"，也就是十进制中的 5 。 提示： 1. 0 <= N < 10^9 2. 本题与 476：https://leetcode.cn/problems/number-complement/ [https://leetcode.cn/problems/number-complement/] 相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算给定整数 N 的二进制表示的反码。可以通过以下步骤实现：
1. 找到 N 的最高有效位（即最左边的 1）。
2. 构造一个与 N 位数相同的全 1 的掩码。
3. 使用按位异或操作计算 N 和掩码的反码。

算法步骤:
1. 找到 N 的最高有效位的位置。
2. 构造一个与 N 位数相同的全 1 的掩码。
3. 使用按位异或操作计算 N 和掩码的反码。

关键点:
- 使用位运算高效地找到最高有效位和构造掩码。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log N) - 因为需要找到 N 的最高有效位。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def bitwise_complement(n: int) -> int:
    """
    函数式接口 - 计算给定整数 N 的二进制表示的反码。
    """
    if n == 0:
        return 1  # 特殊情况处理，0 的反码是 1
    
    # 找到 N 的最高有效位的位置
    highest_bit = 1
    while highest_bit <= n:
        highest_bit <<= 1
    
    # 构造一个与 N 位数相同的全 1 的掩码
    mask = highest_bit - 1
    
    # 使用按位异或操作计算 N 和掩码的反码
    return n ^ mask


Solution = create_solution(bitwise_complement)