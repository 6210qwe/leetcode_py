"""
743. 网络延迟时间 - 有 n 个网络节点，标记为 1 到 n。 给你一个列表 times，表示信号经过 有向 边的传递时间。 times[i] = (ui, vi, wi)，其中 ui 是源节点，vi 是目标节点， wi 是一个信号从源节点传递到目标节点的时间。 现在，从某个节点 K 发出一个信号。需要多久才能使所有节点都收到信号？如果不能使所有节点收到信号，返回 -1 。 示例 1： [https://assets.leetcode.com/uploads/2019/05/23/931_example_1.png] 输入：times = [[2,1,1],[2,3,1],[3,4,1]], n = 4, k = 2 输出：2 示例 2： 输入：times = [[1,2,1]], n = 2, k = 1 输出：1 示例 3： 输入：times = [[1,2,1]], n = 2, k = 2 输出：-1 提示： * 1 <= k <= n <= 100 * 1 <= times.length <= 6000 * times[i].length == 3 * 1 <= ui, vi <= n * ui != vi * 0 <= wi <= 100 * 所有 (ui, vi) 对都 互不相同（即，不含重复边）
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 Dijkstra 算法找到从起点 K 到所有其他节点的最短路径。

算法步骤:
1. 构建图的邻接表表示。
2. 使用优先队列（最小堆）进行 Dijkstra 算法。
3. 初始化距离数组，将起点的距离设为 0，其他节点的距离设为无穷大。
4. 从起点开始，不断更新相邻节点的最短距离。
5. 最后检查所有节点是否都能到达，如果能则返回最大距离，否则返回 -1。

关键点:
- 使用优先队列优化 Dijkstra 算法的时间复杂度。
- 通过邻接表存储图结构，方便快速访问相邻节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(E log V)，其中 E 是边的数量，V 是节点的数量。
空间复杂度: O(V + E)，用于存储图的邻接表和距离数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
import heapq

def network_delay_time(times: List[List[int]], n: int, k: int) -> int:
    # 构建图的邻接表
    graph = {i: [] for i in range(1, n + 1)}
    for u, v, w in times:
        graph[u].append((v, w))
    
    # 初始化距离数组
    dist = {i: float('inf') for i in range(1, n + 1)}
    dist[k] = 0
    
    # 优先队列（最小堆）
    min_heap = [(0, k)]
    
    while min_heap:
        current_dist, u = heapq.heappop(min_heap)
        
        if current_dist > dist[u]:
            continue
        
        for v, w in graph[u]:
            new_dist = current_dist + w
            if new_dist < dist[v]:
                dist[v] = new_dist
                heapq.heappush(min_heap, (new_dist, v))
    
    # 检查所有节点是否都能到达
    max_dist = max(dist.values())
    return max_dist if max_dist < float('inf') else -1

