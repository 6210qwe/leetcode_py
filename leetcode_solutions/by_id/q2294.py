"""
2187. 完成旅途的最少时间 - 给你一个数组 time ，其中 time[i] 表示第 i 辆公交车完成 一趟旅途 所需要花费的时间。 每辆公交车可以 连续 完成多趟旅途，也就是说，一辆公交车当前旅途完成后，可以 立马开始 下一趟旅途。每辆公交车 独立 运行，也就是说可以同时有多辆公交车在运行且互不影响。 给你一个整数 totalTrips ，表示所有公交车 总共 需要完成的旅途数目。请你返回完成 至少 totalTrips 趟旅途需要花费的 最少 时间。 示例 1： 输入：time = [1,2,3], totalTrips = 5 输出：3 解释： - 时刻 t = 1 ，每辆公交车完成的旅途数分别为 [1,0,0] 。 已完成的总旅途数为 1 + 0 + 0 = 1 。 - 时刻 t = 2 ，每辆公交车完成的旅途数分别为 [2,1,0] 。 已完成的总旅途数为 2 + 1 + 0 = 3 。 - 时刻 t = 3 ，每辆公交车完成的旅途数分别为 [3,1,1] 。 已完成的总旅途数为 3 + 1 + 1 = 5 。 所以总共完成至少 5 趟旅途的最少时间为 3 。 示例 2： 输入：time = [2], totalTrips = 1 输出：2 解释： 只有一辆公交车，它将在时刻 t = 2 完成第一趟旅途。 所以完成 1 趟旅途的最少时间为 2 。 提示： * 1 <= time.length <= 105 * 1 <= time[i], totalTrips <= 107
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到最小的时间，使得在这个时间内所有公交车能完成至少 totalTrips 趟旅途。

算法步骤:
1. 初始化二分查找的左右边界，左边界为 1，右边界为 min(time) * totalTrips。
2. 在每次迭代中，计算中间值 mid，并检查在 mid 时间内所有公交车能完成的旅途数是否大于等于 totalTrips。
3. 如果满足条件，则更新右边界为 mid；否则，更新左边界为 mid + 1。
4. 当左右边界相遇时，返回左边界作为结果。

关键点:
- 使用二分查找来减少搜索空间，从而提高效率。
- 计算在给定时间内的总旅途数时，使用整除操作来避免浮点数误差。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log(max_time))，其中 n 是 time 数组的长度，max_time 是时间的最大值。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def minimumTime(time: List[int], totalTrips: int) -> int:
    """
    返回完成至少 totalTrips 趟旅途所需的最少时间。
    """
    def can_complete_trips(t: int) -> bool:
        return sum(t // bus_time for bus_time in time) >= totalTrips

    left, right = 1, min(time) * totalTrips
    while left < right:
        mid = (left + right) // 2
        if can_complete_trips(mid):
            right = mid
        else:
            left = mid + 1
    return left

