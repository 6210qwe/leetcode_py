"""
1595. 连通两组点的最小成本 - 给你两组点，其中第一组中有 size1 个点，第二组中有 size2 个点，且 size1 >= size2 。 任意两点间的连接成本 cost 由大小为 size1 x size2 矩阵给出，其中 cost[i][j] 是第一组中的点 i 和第二组中的点 j 的连接成本。如果两个组中的每个点都与另一组中的一个或多个点连接，则称这两组点是连通的。换言之，第一组中的每个点必须至少与第二组中的一个点连接，且第二组中的每个点必须至少与第一组中的一个点连接。 返回连通两组点所需的最小成本。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/09/20/ex1.jpg] 输入：cost = [[15, 96], [36, 2]] 输出：17 解释：连通两组点的最佳方法是： 1--A 2--B 总成本为 17 。 示例 2： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/09/20/ex2.jpg] 输入：cost = [[1, 3, 5], [4, 1, 1], [1, 5, 3]] 输出：4 解释：连通两组点的最佳方法是： 1--A 2--B 2--C 3--A 最小成本为 4 。 请注意，虽然有多个点连接到第一组中的点 2 和第二组中的点 A ，但由于题目并不限制连接点的数目，所以只需要关心最低总成本。 示例 3： 输入：cost = [[2, 5, 1], [3, 4, 7], [8, 1, 2], [6, 2, 4], [3, 8, 8]] 输出：10 提示： * size1 == cost.length * size2 == cost[i].length * 1 <= size1, size2 <= 12 * size1 >= size2 * 0 <= cost[i][j] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和状态压缩来解决这个问题。

算法步骤:
1. 初始化一个二维 DP 数组 dp，其中 dp[mask][i] 表示第一组中前 i 个点已经连接了第二组中某些点（用 mask 表示）的最小成本。
2. 遍历所有可能的状态 mask，并更新 DP 数组。
3. 对于每个状态 mask，尝试将第一组中的点 i 连接到第二组中的点 j，并更新 DP 数组。
4. 最终结果在 dp[(1 << size2) - 1][size1] 中。

关键点:
- 使用位掩码表示第二组中哪些点已经被连接。
- 动态规划转移方程考虑了当前点连接到第二组中的点的成本。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(size1 * 2^size2 * size2)
空间复杂度: O(2^size2 * size1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def min_cost_connect_points(cost: List[List[int]]) -> int:
    size1, size2 = len(cost), len(cost[0])
    
    # 初始化 DP 数组
    dp = [[float('inf')] * (size1 + 1) for _ in range(1 << size2)]
    dp[0][0] = 0
    
    # 遍历所有可能的状态 mask
    for mask in range(1 << size2):
        for i in range(size1):
            for j in range(size2):
                new_mask = mask | (1 << j)
                if i > 0:
                    dp[new_mask][i + 1] = min(dp[new_mask][i + 1], dp[mask][i] + cost[i][j])
                else:
                    dp[new_mask][i + 1] = min(dp[new_mask][i + 1], dp[mask][i] + cost[i][j])
    
    # 计算最终结果
    result = float('inf')
    for mask in range(1 << size2):
        if bin(mask).count('1') == size2:
            result = min(result, dp[mask][size1])
    
    return result

