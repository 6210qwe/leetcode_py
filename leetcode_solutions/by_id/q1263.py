"""
1155. 掷骰子等于目标和的方法数 - 这里有 n 个一样的骰子，每个骰子上都有 k 个面，分别标号为 1 到 k 。 给定三个整数 n、k 和 target，请返回投掷骰子的所有可能得到的结果（共有 kn 种方式），使得骰子面朝上的数字总和等于 target。 由于答案可能很大，你需要对 109 + 7 取模。 示例 1： 输入：n = 1, k = 6, target = 3 输出：1 解释：你掷了一个有 6 个面的骰子。 得到总和为 3 的结果的方式只有一种。 示例 2： 输入：n = 2, k = 6, target = 7 输出：6 解释：你掷了两个骰子，每个骰子有 6 个面。 有 6 种方式得到总和为 7 的结果: 1+6, 2+5, 3+4, 4+3, 5+2, 6+1。 示例 3： 输入：n = 30, k = 30, target = 500 输出：222616187 解释：返回的结果必须对 109 + 7 取模。 提示： * 1 <= n, k <= 30 * 1 <= target <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示使用 i 个骰子得到和为 j 的方法数。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][j] 表示使用 i 个骰子得到和为 j 的方法数。
2. 对于每一个骰子数量 i，从 1 到 n，对于每一个可能的和 j，从 i 到 min(i * f, target)，更新 dp[i][j]。
3. 最终返回 dp[n][target] 即可。

关键点:
- 使用滚动数组优化空间复杂度。
- 对每一步的结果取模以防止溢出。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * target * f)
空间复杂度: O(target)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def numRollsToTarget(n: int, k: int, target: int) -> int:
    """
    函数式接口 - 计算使用 n 个骰子得到和为 target 的方法数
    """
    MOD = 10**9 + 7
    if n * k < target or n > target:
        return 0

    # 使用滚动数组优化空间复杂度
    dp = [0] * (target + 1)
    dp[0] = 1

    for i in range(1, n + 1):
        new_dp = [0] * (target + 1)
        for j in range(i, min(i * k, target) + 1):
            for face in range(1, min(k, j) + 1):
                new_dp[j] = (new_dp[j] + dp[j - face]) % MOD
        dp = new_dp

    return dp[target]


Solution = create_solution(numRollsToTarget)