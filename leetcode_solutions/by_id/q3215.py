"""
2946. 循环移位后的矩阵相似检查 - 给你一个下标从 0 开始且大小为 m x n 的整数矩阵 mat 和一个整数 k 。请你将矩阵中的 奇数 行循环 右 移 k 次，偶数 行循环 左 移 k 次。 如果初始矩阵和最终矩阵完全相同，则返回 true ，否则返回 false 。 示例 1： 输入：mat = [[1,2,1,2],[5,5,5,5],[6,3,6,3]], k = 2 输出：true 解释： [https://assets.leetcode.com/uploads/2023/10/29/similarmatrix.png] 初始矩阵如图一所示。 图二表示对奇数行右移一次且对偶数行左移一次后的矩阵状态。 图三是经过两次循环移位后的最终矩阵状态，与初始矩阵相同。 因此，返回 true 。 示例 2： 输入：mat = [[2,2],[2,2]], k = 3 输出：true 解释：由于矩阵中的所有值都相等，即使进行循环移位，矩阵仍然保持不变。因此，返回 true 。 示例 3： 输入：mat = [[1,2]], k = 1 输出：false 解释：循环移位一次后，mat = [[2,1]]，与初始矩阵不相等。因此，返回 false 。 提示： * 1 <= mat.length <= 25 * 1 <= mat[i].length <= 25 * 1 <= mat[i][j] <= 25 * 1 <= k <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过循环移位操作，检查矩阵是否在移位后与原矩阵相同。

算法步骤:
1. 对于每一行，根据其索引是奇数还是偶数，分别进行右移或左移操作。
2. 将移位后的矩阵与原矩阵进行比较，如果相同则返回 True，否则返回 False。

关键点:
- 使用切片操作来实现循环移位。
- 通过取模操作来处理移位次数超过行长度的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是矩阵的行数，n 是矩阵的列数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(mat: List[List[int]], k: int) -> bool:
    """
    函数式接口 - 检查矩阵在循环移位后是否与原矩阵相同
    """
    m, n = len(mat), len(mat[0])
    
    for i in range(m):
        if i % 2 == 0:
            # 偶数行左移 k 次
            k_mod = k % n
            mat[i] = mat[i][k_mod:] + mat[i][:k_mod]
        else:
            # 奇数行右移 k 次
            k_mod = k % n
            mat[i] = mat[i][-k_mod:] + mat[i][:-k_mod]
    
    # 检查移位后的矩阵是否与原矩阵相同
    return all(mat[i] == mat[i][:] for i in range(m))


Solution = create_solution(solution_function_name)