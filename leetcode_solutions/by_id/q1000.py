"""
960. 删列造序 III - 给定由 n 个小写字母字符串组成的数组 strs ，其中每个字符串长度相等。 选取一个删除索引序列，对于 strs 中的每个字符串，删除对应每个索引处的字符。 比如，有 strs = ["abcdef","uvwxyz"] ，删除索引序列 {0, 2, 3} ，删除后为 ["bef", "vyz"] 。 假设，我们选择了一组删除索引 answer ，那么在执行删除操作之后，最终得到的数组的行中的 每个元素 都是按字典序排列的（即 (strs[0][0] <= strs[0][1] <= ... <= strs[0][strs[0].length - 1]) 和 (strs[1][0] <= strs[1][1] <= ... <= strs[1][strs[1].length - 1]) ，依此类推）。 请返回 answer.length 的最小可能值 。 示例 1： 输入：strs = ["babca","bbazb"] 输出：3 解释： 删除 0、1 和 4 这三列后，最终得到的数组是 strs = ["bc", "az"]。 这两行是分别按字典序排列的（即，strs[0][0] <= strs[0][1] 且 strs[1][0] <= strs[1][1]）。 注意，strs[0] > strs[1] —— 数组 strs 不一定是按字典序排列的。 示例 2： 输入：strs = ["edcba"] 输出：4 解释：如果删除的列少于 4 列，则剩下的行都不会按字典序排列。 示例 3： 输入：strs = ["ghi","def","abc"] 输出：0 解释：所有行都已按字典序排列。 提示： * n == strs.length * 1 <= n <= 100 * 1 <= strs[i].length <= 100 * strs[i] 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。定义 dp[i] 表示以第 i 列结尾的最大非递减子序列的长度。我们需要找到最长的非递减子序列，然后用总列数减去这个长度即可。

算法步骤:
1. 初始化 dp 数组，dp[i] 表示以第 i 列结尾的最大非递减子序列的长度。
2. 遍历每一列，检查当前列是否可以接在之前的某列后面形成非递减子序列。
3. 更新 dp 数组。
4. 最后，用总列数减去 dp 数组中的最大值，即为需要删除的最少列数。

关键点:
- 动态规划的状态转移方程：dp[j] = max(dp[j], dp[i] + 1) 如果 strs[k][j] >= strs[k][i] 对于所有 k 成立。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m^2 * n)，其中 m 是字符串的长度，n 是字符串的数量。
空间复杂度: O(m)，因为 dp 数组的长度为 m。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_deletion_size(strs: List[str]) -> int:
    """
    函数式接口 - 返回需要删除的最少列数
    """
    if not strs:
        return 0

    m, n = len(strs), len(strs[0])
    dp = [1] * n

    for j in range(1, n):
        for i in range(j):
            if all(strs[k][j] >= strs[k][i] for k in range(m)):
                dp[j] = max(dp[j], dp[i] + 1)

    return n - max(dp)


Solution = create_solution(min_deletion_size)