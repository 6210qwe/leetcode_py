"""
263. 丑数 - 丑数 就是只包含质因数 2、3 和 5 的 正 整数。 给你一个整数 n ，请你判断 n 是否为 丑数 。如果是，返回 true ；否则，返回 false 。 示例 1： 输入：n = 6 输出：true 解释：6 = 2 × 3 示例 2： 输入：n = 1 输出：true 解释：1 没有质因数。 示例 3： 输入：n = 14 输出：false 解释：14 不是丑数，因为它包含了另外一个质因数 7 。 提示： * -231 <= n <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 不断除以2、3、5，检查是否能除尽

算法步骤:
1. 如果n<=0，返回False
2. 不断除以2、3、5
3. 如果最后结果为1，则是丑数

关键点:
- 数学方法
- 时间复杂度O(log n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 不断除以因子
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_ugly(n: int) -> bool:
    """
    函数式接口 - 丑数
    
    实现思路:
    不断除以2、3、5，检查是否能除尽。
    
    Args:
        n: 整数
        
    Returns:
        是否为丑数
        
    Example:
        >>> is_ugly(6)
        True
    """
    if n <= 0:
        return False
    
    factors = [2, 3, 5]
    for factor in factors:
        while n % factor == 0:
            n //= factor
    
    return n == 1


