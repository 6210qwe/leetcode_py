from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def lowest_common_ancestor(p: 'Node', q: 'Node') -> 'Node':
    """
    函数式接口 - 二叉树的最近公共祖先 III
    
    实现思路:
    使用哈希表记录从节点到根节点的路径。
    
    Args:
        p: 节点p
        q: 节点q
        
    Returns:
        最近公共祖先节点
        
    Example:
        >>> # 示例用法
    """
    ancestors = set()
    
    # 记录p的所有祖先
    while p:
        ancestors.add(p)
        p = p.parent
    
    # 找q的祖先中第一个在p的祖先集合中的节点
    while q:
        if q in ancestors:
            return q
        q = q.parent
    
    return None


Solution = create_solution(lowest_common_ancestor)
