"""
1013. 将数组分成和相等的三个部分 - 给你一个整数数组 arr，只有可以将其划分为三个和相等的 非空 部分时才返回 true，否则返回 false。 形式上，如果可以找出索引 i + 1 < j 且满足 (arr[0] + arr[1] + ... + arr[i] == arr[i + 1] + arr[i + 2] + ... + arr[j - 1] == arr[j] + arr[j + 1] + ... + arr[arr.length - 1]) 就可以将数组三等分。 示例 1： 输入：arr = [0,2,1,-6,6,-7,9,1,2,0,1] 输出：true 解释：0 + 2 + 1 = -6 + 6 - 7 + 9 + 1 = 2 + 0 + 1 示例 2： 输入：arr = [0,2,1,-6,6,7,9,-1,2,0,1] 输出：false 示例 3： 输入：arr = [3,3,6,5,-2,2,5,1,-9,4] 输出：true 解释：3 + 3 = 6 = 5 - 2 + 2 + 5 + 1 - 9 + 4 提示： * 3 <= arr.length <= 5 * 104 * -104 <= arr[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算数组总和，判断是否能被3整除，然后使用双指针找到两个分割点。

算法步骤:
1. 计算数组的总和，如果总和不能被3整除，则直接返回False。
2. 使用双指针从左到右找到第一个分割点，使得其和为总和的三分之一。
3. 使用双指针从右到左找到第二个分割点，使得其和为总和的三分之一。
4. 如果能找到两个分割点，则返回True，否则返回False。

关键点:
- 数组总和必须能被3整除。
- 使用双指针高效地找到分割点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中n是数组的长度。我们需要遍历数组两次来找到分割点。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def can_three_parts_equal_sum(arr: List[int]) -> bool:
    """
    函数式接口 - 判断数组是否可以分成三个和相等的部分
    """
    total_sum = sum(arr)
    if total_sum % 3 != 0:
        return False
    
    target_sum = total_sum // 3
    current_sum = 0
    count = 0
    
    for num in arr:
        current_sum += num
        if current_sum == target_sum:
            count += 1
            current_sum = 0
            if count == 2:
                break
    
    return count == 2 and current_sum == 0

Solution = can_three_parts_equal_sum