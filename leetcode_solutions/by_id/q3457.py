"""
核心思想: 使用 SQL 查询来筛选出内容长度超过 15 个字符的推文。

算法步骤:
1. 从 `Tweets` 表中选择所有列。
2. 使用 WHERE 子句过滤出 `content` 列长度大于 15 的记录。
3. 按照 `tweet_id` 升序排序结果。

关键点:
- 使用 SQL 内置函数 `LENGTH` 来计算字符串长度。
- 使用 ORDER BY 子句对结果进行排序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 `Tweets` 表中的行数。因为我们需要对结果进行排序。
空间复杂度: O(1)，查询本身不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(params):
    """
    函数式接口 - 返回无效推文的查询结果
    """
    # 实现最优解法
    query = """
    SELECT *
    FROM Tweets
    WHERE LENGTH(content) > 15
    ORDER BY tweet_id ASC;
    """
    return query


