"""
2423. 删除字符使频率相同 - 给你一个下标从 0 开始的字符串 word ，字符串只包含小写英文字母。你需要选择 一个 下标并 删除 下标处的字符，使得 word 中剩余每个字母出现 频率 相同。 如果删除一个字母后，word 中剩余所有字母的出现频率都相同，那么返回 true ，否则返回 false 。 注意： * 字母 x 的 频率 是这个字母在字符串中出现的次数。 * 你 必须 恰好删除一个字母，不能一个字母都不删除。 示例 1： 输入：word = "abcc" 输出：true 解释：选择下标 3 并删除该字母：word 变成 "abc" 且每个字母出现频率都为 1 。 示例 2： 输入：word = "aazz" 输出：false 解释：我们必须删除一个字母，所以要么 "a" 的频率变为 1 且 "z" 的频率为 2 ，要么两个字母频率反过来。所以不可能让剩余所有字母出现频率相同。 提示： * 2 <= word.length <= 100 * word 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算每个字符的频率，尝试删除每个字符后检查剩余字符的频率是否相同。

算法步骤:
1. 计算每个字符的频率。
2. 尝试删除每个字符，并检查剩余字符的频率是否相同。
3. 如果存在一种删除方式使得剩余字符的频率相同，则返回 True；否则返回 False。

关键点:
- 使用 Counter 来高效计算字符频率。
- 通过集合来检查频率是否相同。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是字符串的长度。最坏情况下，我们需要遍历每个字符并检查剩余字符的频率。
空间复杂度: O(1)，因为字符集是固定的（26 个小写字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
from collections import Counter


def equalFrequency(word: str) -> bool:
    """
    函数式接口 - 判断是否可以通过删除一个字符使剩余字符的频率相同
    """
    # 计算每个字符的频率
    freq = Counter(word)
    
    # 尝试删除每个字符，并检查剩余字符的频率是否相同
    for char in word:
        new_freq = freq.copy()
        new_freq[char] -= 1
        if new_freq[char] == 0:
            del new_freq[char]
        if len(set(new_freq.values())) == 1:
            return True
    
    return False


Solution = create_solution(equalFrequency)