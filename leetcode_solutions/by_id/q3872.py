"""
3541. 找到频率最高的元音和辅音 - 给你一个由小写英文字母（'a' 到 'z'）组成的字符串 s。你的任务是找出出现频率 最高 的元音（'a'、'e'、'i'、'o'、'u' 中的一个）和出现频率最高的辅音（除元音以外的所有字母），并返回这两个频数之和。 注意：如果有多个元音或辅音具有相同的最高频率，可以任选其中一个。如果字符串中没有元音或没有辅音，则其频率视为 0。 一个字母 x 的 频率 是它在字符串中出现的次数。 示例 1： 输入: s = "successes" 输出: 6 解释: * 元音有：'u' 出现 1 次，'e' 出现 2 次。最大元音频率 = 2。 * 辅音有：'s' 出现 4 次，'c' 出现 2 次。最大辅音频率 = 4。 * 输出为 2 + 4 = 6。 示例 2： 输入: s = "aeiaeia" 输出: 3 解释: * 元音有：'a' 出现 3 次，'e' 出现 2 次，'i' 出现 2 次。最大元音频率 = 3。 * s 中没有辅音。因此，最大辅音频率 = 0。 * 输出为 3 + 0 = 3。 提示: * 1 <= s.length <= 100 * s 只包含小写英文字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个字符的频率，然后分别找到元音和辅音的最大频率。

算法步骤:
1. 初始化两个哈希表，一个用于记录元音频率，一个用于记录辅音频率。
2. 遍历字符串，更新哈希表中的频率。
3. 分别找到元音和辅音的最大频率。
4. 返回元音和辅音的最大频率之和。

关键点:
- 使用哈希表高效统计字符频率。
- 通过遍历字符串一次完成频率统计。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历字符串一次来统计频率。
空间复杂度: O(1)，因为哈希表的大小是固定的（最多 26 个字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_most_frequent_vowel_and_consonant(s: str) -> int:
    """
    函数式接口 - 找到频率最高的元音和辅音，并返回它们的频率之和。
    """
    # 定义元音集合
    vowels = set('aeiou')
    
    # 初始化元音和辅音的频率字典
    vowel_count = {}
    consonant_count = {}
    
    # 遍历字符串，更新频率字典
    for char in s:
        if char in vowels:
            vowel_count[char] = vowel_count.get(char, 0) + 1
        else:
            consonant_count[char] = consonant_count.get(char, 0) + 1
    
    # 找到元音和辅音的最大频率
    max_vowel_freq = max(vowel_count.values(), default=0)
    max_consonant_freq = max(consonant_count.values(), default=0)
    
    # 返回最大频率之和
    return max_vowel_freq + max_consonant_freq


Solution = create_solution(find_most_frequent_vowel_and_consonant)