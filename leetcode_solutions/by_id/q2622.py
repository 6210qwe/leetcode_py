def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询来找到可以形成化学键的原子对
    """
    # 实现最优解法
    query = """
    SELECT t1.atom1, t1.atom2
    FROM (
        SELECT a1.id AS atom1, a2.id AS atom2
        FROM Atoms a1
        JOIN Atoms a2 ON a1.id < a2.id
    ) t1
    WHERE t1.atom1 + t1.atom2 = 0;
    """
    return query


