"""
1075. 项目员工 I - 项目表 Project： +-------------+---------+ | Column Name | Type | +-------------+---------+ | project_id | int | | employee_id | int | +-------------+---------+ 主键为 (project_id, employee_id)。 employee_id 是员工表 Employee 表的外键。 这张表的每一行表示 employee_id 的员工正在 project_id 的项目上工作。 员工表 Employee： +------------------+---------+ | Column Name | Type | +------------------+---------+ | employee_id | int | | name | varchar | | experience_years | int | +------------------+---------+ 主键是 employee_id。数据保证 experience_years 非空。 这张表的每一行包含一个员工的信息。 请写一个 SQL 语句，查询每一个项目中员工的 平均 工作年限，精确到小数点后两位。 以 任意 顺序返回结果表。 查询结果的格式如下。 示例 1: 输入： Project 表： +-------------+-------------+ | project_id | employee_id | +-------------+-------------+ | 1 | 1 | | 1 | 2 | | 1 | 3 | | 2 | 1 | | 2 | 4 | +-------------+-------------+ Employee 表： +-------------+--------+------------------+ | employee_id | name | experience_years | +-------------+--------+------------------+ | 1 | Khaled | 3 | | 2 | Ali | 2 | | 3 | John | 1 | | 4 | Doe | 2 | +-------------+--------+------------------+ 输出： +-------------+---------------+ | project_id | average_years | +-------------+---------------+ | 1 | 2.00 | | 2 | 2.50 | +-------------+---------------+ 解释：第一个项目中，员工的平均工作年限是 (3 + 2 + 1) / 3 = 2.00；第二个项目中，员工的平均工作年限是 (3 + 2) / 2 = 2.50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 查询来计算每个项目的员工平均工作年限。

算法步骤:
1. 将 Project 表和 Employee 表通过 employee_id 进行连接。
2. 按照 project_id 分组，计算每个项目的员工平均工作年限。
3. 使用 ROUND 函数将结果保留两位小数。

关键点:
- 使用 INNER JOIN 进行表连接。
- 使用 GROUP BY 对项目进行分组。
- 使用 AVG 函数计算平均值。
- 使用 ROUND 函数保留两位小数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * log n)，其中 n 是表中的记录数。JOIN 操作的时间复杂度通常是 O(n * log n)。
空间复杂度: O(1)，查询操作的空间复杂度通常与输入大小无关。
"""

# ============================================================================
# 代码实现
# ============================================================================

def solution_function_name():
    """
    函数式接口 - 返回 SQL 查询语句
    """
    sql_query = """
    SELECT p.project_id, ROUND(AVG(e.experience_years), 2) AS average_years
    FROM Project p
    INNER JOIN Employee e ON p.employee_id = e.employee_id
    GROUP BY p.project_id;
    """
    return sql_query


