"""
核心思想: 通过贪心算法选择尽可能多的元素，并确保总和不超过阈值。

算法步骤:
1. 将数组按元素值从大到小排序。
2. 依次累加元素，直到总和超过阈值或所有元素都已处理完毕。
3. 返回累加的总和。

关键点:
- 通过排序和贪心选择，确保在不超过阈值的情况下，总和最大。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。主要由排序操作决定。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

