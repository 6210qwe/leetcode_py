"""
3201. 找出有效子序列的最大长度 I - 给你一个整数数组 nums。 nums 的子序列 sub 的长度为 x ，如果其满足以下条件，则称其为 有效子序列： * (sub[0] + sub[1]) % 2 == (sub[1] + sub[2]) % 2 == ... == (sub[x - 2] + sub[x - 1]) % 2 返回 nums 的 最长的有效子序列 的长度。 一个 子序列 指的是从原数组中删除一些元素（也可以不删除任何元素），剩余元素保持原来顺序组成的新数组。 示例 1： 输入： nums = [1,2,3,4] 输出： 4 解释： 最长的有效子序列是 [1, 2, 3, 4]。 示例 2： 输入： nums = [1,2,1,1,2,1,2] 输出： 6 解释： 最长的有效子序列是 [1, 2, 1, 2, 1, 2]。 示例 3： 输入： nums = [1,3] 输出： 2 解释： 最长的有效子序列是 [1, 3]。 提示： * 2 <= nums.length <= 2 * 105 * 1 <= nums[i] <= 107
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们维护两个数组，分别记录以偶数和奇数结尾的最长有效子序列的长度。

算法步骤:
1. 初始化两个数组 `dp_even` 和 `dp_odd`，分别记录以偶数和奇数结尾的最长有效子序列的长度。
2. 遍历数组 `nums`，根据当前元素的奇偶性更新 `dp_even` 和 `dp_odd`。
3. 最后返回 `dp_even` 和 `dp_odd` 中的最大值。

关键点:
- 通过动态规划避免重复计算，确保时间复杂度最优。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找出有效子序列的最大长度 I
    """
    if not nums:
        return 0

    n = len(nums)
    dp_even = 0
    dp_odd = 0

    for num in nums:
        if num % 2 == 0:
            dp_even = max(dp_even + 1, 1)
            dp_odd = 0
        else:
            dp_odd = max(dp_odd + 1, 1)
            dp_even = 0

    return max(dp_even, dp_odd)


Solution = create_solution(solution_function_name)