"""
2659. 将数组清空 - 给你一个包含若干 互不相同 整数的数组 nums ，你需要执行以下操作 直到数组为空 ： * 如果数组中第一个元素是当前数组中的 最小值 ，则删除它。 * 否则，将第一个元素移动到数组的 末尾 。 请你返回需要多少个操作使 nums 为空。 示例 1： 输入：nums = [3,4,-1] 输出：5 Operation Array 1 [4, -1, 3] 2 [-1, 3, 4] 3 [3, 4] 4 [4] 5 [] 示例 2： 输入：nums = [1,2,4,3] 输出：5 Operation Array 1 [2, 4, 3] 2 [4, 3] 3 [3, 4] 4 [4] 5 [] 示例 3： 输入：nums = [1,2,3] 输出：3 Operation Array 1 [2, 3] 2 [3] 3 [] 提示： * 1 <= nums.length <= 105 * -109 <= nums[i] <= 109 * nums 中的元素 互不相同 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和有序集合来高效地找到和删除最小值。

算法步骤:
1. 使用有序集合（如SortedList）存储数组元素及其索引。
2. 初始化操作计数器为0。
3. 当有序集合不为空时，执行以下操作：
   - 找到当前最小值及其索引。
   - 如果最小值在数组的第一个位置，则直接删除。
   - 否则，将第一个元素移动到数组末尾，并更新有序集合。
4. 返回操作计数器的值。

关键点:
- 使用有序集合可以高效地找到和删除最小值。
- 通过维护元素及其索引，可以在O(log n)时间内完成插入和删除操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中n是数组长度。每次插入和删除操作的时间复杂度为O(log n)。
空间复杂度: O(n)，使用有序集合存储数组元素及其索引。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from sortedcontainers import SortedList

def make_array_empty(nums: List[int]) -> int:
    """
    函数式接口 - 返回使数组为空所需的操作次数
    """
    # 使用有序集合存储数组元素及其索引
    sorted_list = SortedList((num, i) for i, num in enumerate(nums))
    operations = 0
    n = len(nums)
    
    while sorted_list:
        min_val, min_index = sorted_list[0]
        
        if min_index == 0:
            # 如果最小值在数组的第一个位置，直接删除
            sorted_list.pop(0)
            nums.pop(0)
        else:
            # 否则，将第一个元素移动到数组末尾
            first_val = nums.pop(0)
            nums.append(first_val)
            sorted_list.remove((min_val, min_index))
            sorted_list.add((first_val, n - 1))
        
        operations += 1
    
    return operations

