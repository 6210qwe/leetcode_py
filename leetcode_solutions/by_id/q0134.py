from typing import List
from leetcode_solutions.utils.solution import create_solution


def gas_station(gas: List[int], cost: List[int]) -> int:
    """
    函数式接口 - 加油站
    
    实现思路:
    贪心算法，如果总油量小于总消耗，无解；否则从某个起点开始，如果当前油量不足，更新起点。
    
    Args:
        gas: 每个加油站的汽油量
        cost: 从第i个加油站到第i+1个加油站需要消耗的汽油量
        
    Returns:
        出发时加油站的编号，如果无解返回-1
        
    Example:
        >>> gas_station([1,2,3,4,5], [3,4,5,1,2])
        3
    """
    total_gas = sum(gas)
    total_cost = sum(cost)
    
    if total_gas < total_cost:
        return -1
    
    start = 0
    current_gas = 0
    
    for i in range(len(gas)):
        current_gas += gas[i] - cost[i]
        if current_gas < 0:
            start = i + 1
            current_gas = 0
    
    return start


