from typing import List
import heapq

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 使前缀和数组非负
    """
    # 初始化最小堆和前缀和
    min_heap = []
    prefix_sum = 0
    operations = 0
    
    for num in nums:
        prefix_sum += num
        if prefix_sum < 0:
            # 从堆中取出最小值并调整前缀和
            min_val = heapq.heappop(min_heap)
            prefix_sum -= min_val
            operations += 1
        heapq.heappush(min_heap, num)
    
    return operations

