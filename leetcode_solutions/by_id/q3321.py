"""
3024. 三角形类型 - 给你一个下标从 0 开始长度为 3 的整数数组 nums ，需要用它们来构造三角形。 * 如果一个三角形的所有边长度相等，那么这个三角形称为 equilateral 。 * 如果一个三角形恰好有两条边长度相等，那么这个三角形称为 isosceles 。 * 如果一个三角形三条边的长度互不相同，那么这个三角形称为 scalene 。 如果这个数组无法构成一个三角形，请你返回字符串 "none" ，否则返回一个字符串表示这个三角形的类型。 示例 1： 输入：nums = [3,3,3] 输出："equilateral" 解释：由于三条边长度相等，所以可以构成一个等边三角形，返回 "equilateral" 。 示例 2： 输入：nums = [3,4,5] 输出："scalene" 解释： nums[0] + nums[1] = 3 + 4 = 7 ，大于 nums[2] = 5 。 nums[0] + nums[2] = 3 + 5 = 8 ，大于 nums[1] = 4 。 nums[1] + nums[2] = 4 + 5 = 9 ，大于 nums[0] = 3 。 由于任意两边之和都大于第三边，所以可以构成一个三角形，因为三条边的长度互不相等，所以返回 "scalene"。 提示： * nums.length == 3 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断给定的三边是否能构成三角形，并根据边长关系确定三角形的类型。

算法步骤:
1. 检查给定的三边是否满足三角形不等式。
2. 根据边长关系判断三角形的类型：
   - 如果三条边都相等，则是等边三角形。
   - 如果两条边相等，则是等腰三角形。
   - 如果三条边都不相等，则是不等边三角形。

关键点:
- 使用三角形不等式来判断是否能构成三角形。
- 通过比较边长来确定三角形的类型。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> str:
    """
    函数式接口 - 判断给定的三边是否能构成三角形，并返回三角形的类型。
    """
    # 检查三角形不等式
    if (nums[0] + nums[1] <= nums[2]) or (nums[0] + nums[2] <= nums[1]) or (nums[1] + nums[2] <= nums[0]):
        return "none"

    # 判断三角形类型
    if nums[0] == nums[1] == nums[2]:
        return "equilateral"
    elif nums[0] == nums[1] or nums[0] == nums[2] or nums[1] == nums[2]:
        return "isosceles"
    else:
        return "scalene"


Solution = create_solution(solution_function_name)