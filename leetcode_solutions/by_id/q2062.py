# 实现思路
# ============================================================================
"""
核心思想: 使用异或运算来判断当前状态是否为必胜态。如果所有堆的石子数的异或和为0，则先手必败；否则先手必胜。

算法步骤:
1. 计算所有堆的石子数的异或和。
2. 判断异或和是否为0，如果是0则返回False（先手必败），否则返回True（先手必胜）。

关键点:
- 异或运算的性质：a ^ a = 0，a ^ 0 = a。
- 如果所有堆的石子数的异或和为0，则无论先手如何操作，后手总能通过相应的操作使异或和保持为0，从而保证先手最终失败。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中n是堆的数量。我们需要遍历所有堆来计算异或和。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(piles: List[int]) -> bool:
    """
    函数式接口 - 判断先手是否必胜
    """
    # 计算所有堆的石子数的异或和
    xor_sum = 0
    for pile in piles:
        xor_sum ^= pile
    
    # 判断异或和是否为0
    return xor_sum != 0


Solution = create_solution(solution_function_name)