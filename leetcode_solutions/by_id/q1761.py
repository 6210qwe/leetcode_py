"""
1641. 统计字典序元音字符串的数目 - 给你一个整数 n，请返回长度为 n 、仅由元音 (a, e, i, o, u) 组成且按 字典序排列 的字符串数量。 字符串 s 按 字典序排列 需要满足：对于所有有效的 i，s[i] 在字母表中的位置总是与 s[i+1] 相同或在 s[i+1] 之前。 示例 1： 输入：n = 1 输出：5 解释：仅由元音组成的 5 个字典序字符串为 ["a","e","i","o","u"] 示例 2： 输入：n = 2 输出：15 解释：仅由元音组成的 15 个字典序字符串为 ["aa","ae","ai","ao","au","ee","ei","eo","eu","ii","io","iu","oo","ou","uu"] 注意，"ea" 不是符合题意的字符串，因为 'e' 在字母表中的位置比 'a' 靠后 示例 3： 输入：n = 33 输出：66045 提示： * 1 <= n <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们可以定义一个二维数组 dp，其中 dp[i][j] 表示长度为 i 且以第 j 个元音结尾的字符串的数量。通过状态转移方程可以计算出最终结果。

算法步骤:
1. 初始化一个二维数组 dp，dp[0][j] 表示长度为 1 且以第 j 个元音结尾的字符串数量。
2. 通过状态转移方程 dp[i][j] = sum(dp[i-1][k] for k in range(j+1)) 来更新 dp 数组。
3. 最终结果是 dp[n-1] 的总和。

关键点:
- 使用动态规划减少重复计算。
- 状态转移方程确保了字符串按字典序排列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是字符串的长度，m 是元音的数量（固定为 5）。
空间复杂度: O(n * m)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_vowel_strings(n: int) -> int:
    """
    函数式接口 - 计算长度为 n 且由元音组成的字典序字符串的数量
    """
    # 初始化 dp 数组
    dp = [[0] * 5 for _ in range(n)]
    
    # 初始化第一行
    for j in range(5):
        dp[0][j] = 1
    
    # 填充 dp 数组
    for i in range(1, n):
        for j in range(5):
            dp[i][j] = sum(dp[i-1][k] for k in range(j+1))
    
    # 返回最终结果
    return sum(dp[n-1])

Solution = create_solution(count_vowel_strings)