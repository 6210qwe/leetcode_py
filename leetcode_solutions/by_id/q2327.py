# 问题描述
# ============================================================================
"""
2231. 按奇偶性交换后的最大数字 - 给你一个正整数 num 。你可以交换 num 中 奇偶性 相同的任意两位数字（即，都是奇数或者偶数）。 返回交换 任意 次之后 num 的 最大 可能值。 示例 1： 输入：num = 1234 输出：3412 解释：交换数字 3 和数字 1 ，结果得到 3214 。 交换数字 2 和数字 4 ，结果得到 3412 。 注意，可能存在其他交换序列，但是可以证明 3412 是最大可能值。 注意，不能交换数字 4 和数字 1 ，因为它们奇偶性不同。 示例 2： 输入：num = 65875 输出：87655 解释：交换数字 8 和数字 6 ，结果得到 85675 。 交换数字 5 和数字 7 ，结果得到 87655 。 注意，可能存在其他交换序列，但是可以证明 87655 是最大可能值。 提示： * 1 <= num <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将 num 转换为字符串，然后分别对奇数和偶数进行排序，最后重新组合成最大的数字。

算法步骤:
1. 将 num 转换为字符串列表。
2. 分别提取出所有的奇数和偶数，并按降序排序。
3. 遍历原字符串列表，将奇数和偶数替换为排序后的最大值。
4. 将处理后的字符串列表重新组合成一个整数并返回。

关键点:
- 使用字符串操作来处理数字，便于逐位处理。
- 对奇数和偶数分别排序，确保在相同奇偶性的位置上选择最大的数字。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 num 的位数。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，需要存储 num 的每一位以及排序后的奇数和偶数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: int) -> int:
    """
    函数式接口 - 实现
    """
    # 将 num 转换为字符串列表
    digits = list(str(num))
    
    # 提取出所有的奇数和偶数，并按降序排序
    odd_digits = sorted([d for d in digits if int(d) % 2 == 1], reverse=True)
    even_digits = sorted([d for d in digits if int(d) % 2 == 0], reverse=True)
    
    # 重新组合成最大的数字
    result = []
    for d in digits:
        if int(d) % 2 == 1:
            result.append(odd_digits.pop(0))
        else:
            result.append(even_digits.pop(0))
    
    return int(''.join(result))


Solution = create_solution(solution_function_name)