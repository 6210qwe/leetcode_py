def magical_string(n: int) -> int:
    """
    函数式接口 - 返回神奇字符串 s 的前 n 个数字中 1 的数目
    
    实现思路:
    使用双指针方法生成神奇字符串，并统计前 n 个字符中 1 的数量。
    
    Args:
        n: 整数，表示需要生成的神奇字符串的前 n 个字符
        
    Returns:
        返回神奇字符串 s 的前 n 个数字中 1 的数目
        
    Example:
        >>> magical_string(6)
        3
    """
    if n == 0:
        return 0
    if n <= 3:
        return 1

    s = [1, 2, 2]
    group_count = [1, 2, 2]
    count_ones = 1
    i, j = 2, 2

    while len(s) < n:
        next_num = 3 - s[-1]  # 交替使用 1 和 2
        for _ in range(group_count[i]):
            s.append(next_num)
            if next_num == 1 and len(s) <= n:
                count_ones += 1
        group_count.append(next_num)
        i += 1

    return count_ones


