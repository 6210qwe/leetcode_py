"""
1138. 字母板上的路径 - 我们从一块字母板上的位置 (0, 0) 出发，该坐标对应的字符为 board[0][0]。 在本题里，字母板为board = ["abcde", "fghij", "klmno", "pqrst", "uvwxy", "z"]，如下所示。 [https://assets.leetcode.com/uploads/2019/07/28/azboard.png] 我们可以按下面的指令规则行动： * 如果方格存在，'U' 意味着将我们的位置上移一行； * 如果方格存在，'D' 意味着将我们的位置下移一行； * 如果方格存在，'L' 意味着将我们的位置左移一列； * 如果方格存在，'R' 意味着将我们的位置右移一列； * '!' 会把在我们当前位置 (r, c) 的字符 board[r][c] 添加到答案中。 （注意，字母板上只存在有字母的位置。） 返回指令序列，用最小的行动次数让答案和目标 target 相同。你可以返回任何达成目标的路径。 示例 1： 输入：target = "leet" 输出："DDR!UURRR!!DDD!" 示例 2： 输入：target = "code" 输出："RR!DDRR!UUL!R!" 提示： * 1 <= target.length <= 100 * target 仅含有小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一个字典来存储每个字母在字母板上的坐标，然后根据当前坐标和目标坐标的差异生成移动指令。

算法步骤:
1. 初始化字母板和字母到坐标的映射。
2. 从起点 (0, 0) 开始，遍历目标字符串中的每个字符。
3. 对于每个字符，计算从当前坐标到目标坐标的移动指令，并更新当前坐标。
4. 特别处理 'z' 的情况，确保先左右移动再上下移动。
5. 将移动指令和 '!' 添加到结果字符串中。

关键点:
- 使用字典存储字母到坐标的映射，快速查找坐标。
- 特殊处理 'z' 的情况，避免无效移动。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是目标字符串的长度。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

def alphabet_board_path(target: str) -> str:
    # 字母板
    board = ["abcde", "fghij", "klmno", "pqrst", "uvwxy", "z"]
    
    # 字母到坐标的映射
    char_to_pos = {char: (i, j) for i, row in enumerate(board) for j, char in enumerate(row)}
    
    # 起点坐标
    x, y = 0, 0
    result = []
    
    for char in target:
        # 目标坐标
        tx, ty = char_to_pos[char]
        
        # 先左右移动
        if y > ty:
            result.append('L' * (y - ty))
        elif y < ty:
            result.append('R' * (ty - y))
        
        # 再上下移动
        if x > tx:
            result.append('U' * (x - tx))
        elif x < tx:
            result.append('D' * (tx - x))
        
        # 添加 '!'
        result.append('!')
        
        # 更新当前坐标
        x, y = tx, ty
    
    return ''.join(result)

