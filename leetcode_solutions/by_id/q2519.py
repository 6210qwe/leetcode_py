"""
2433. 找出前缀异或的原始数组 - 给你一个长度为 n 的 整数 数组 pref 。找出并返回满足下述条件且长度为 n 的数组 arr ： * pref[i] = arr[0] ^ arr[1] ^ ... ^ arr[i]. 注意 ^ 表示 按位异或（bitwise-xor）运算。 可以证明答案是 唯一 的。 示例 1： 输入：pref = [5,2,0,3,1] 输出：[5,7,2,3,2] 解释：从数组 [5,7,2,3,2] 可以得到如下结果： - pref[0] = 5 - pref[1] = 5 ^ 7 = 2 - pref[2] = 5 ^ 7 ^ 2 = 0 - pref[3] = 5 ^ 7 ^ 2 ^ 3 = 3 - pref[4] = 5 ^ 7 ^ 2 ^ 3 ^ 2 = 1 示例 2： 输入：pref = [13] 输出：[13] 解释：pref[0] = arr[0] = 13 提示： * 1 <= pref.length <= 105 * 0 <= pref[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 利用异或的性质，通过前缀异或数组反推出原始数组。

算法步骤:
1. 初始化结果数组 `arr`，并将 `arr[0]` 设为 `pref[0]`。
2. 从索引 1 开始遍历 `pref` 数组，对于每个 `i`，计算 `arr[i] = pref[i] ^ pref[i-1]`。
3. 返回结果数组 `arr`。

关键点:
- 异或运算的性质：`a ^ a = 0` 和 `a ^ 0 = a`。
- 通过 `pref[i] = arr[0] ^ ... ^ arr[i]` 和 `pref[i-1] = arr[0] ^ ... ^ arr[i-1]` 可以推导出 `arr[i] = pref[i] ^ pref[i-1]`。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 `pref` 的长度。我们需要遍历整个 `pref` 数组一次。
空间复杂度: O(n)，结果数组 `arr` 的空间复杂度为 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_original_array(pref: List[int]) -> List[int]:
    """
    通过前缀异或数组反推出原始数组。

    :param pref: 前缀异或数组
    :return: 原始数组
    """
    n = len(pref)
    arr = [0] * n
    arr[0] = pref[0]
    for i in range(1, n):
        arr[i] = pref[i] ^ pref[i - 1]
    return arr


Solution = create_solution(find_original_array)