"""
657. 机器人能否返回原点 - 在二维平面上，有一个机器人从原点 (0, 0) 开始。给出它的移动顺序，判断这个机器人在完成移动后是否在 (0, 0) 处结束。 移动顺序由字符串 moves 表示。字符 move[i] 表示其第 i 次移动。机器人的有效动作有 R（右），L（左），U（上）和 D（下）。 如果机器人在完成所有动作后返回原点，则返回 true。否则，返回 false。 注意：机器人“面朝”的方向无关紧要。 “R” 将始终使机器人向右移动一次，“L” 将始终向左移动等。此外，假设每次移动机器人的移动幅度相同。 示例 1: 输入: moves = "UD" 输出: true 解释：机器人向上移动一次，然后向下移动一次。所有动作都具有相同的幅度，因此它最终回到它开始的原点。因此，我们返回 true。 示例 2: 输入: moves = "LL" 输出: false 解释：机器人向左移动两次。它最终位于原点的左侧，距原点有两次 “移动” 的距离。我们返回 false，因为它在移动结束时没有返回原点。 提示: * 1 <= moves.length <= 2 * 104 * moves 只包含字符 'U', 'D', 'L' 和 'R'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 统计每个方向的移动次数，如果上下移动次数相等且左右移动次数相等，则机器人返回原点。

算法步骤:
1. 初始化两个变量 x 和 y，分别表示水平和垂直方向的位置。
2. 遍历 moves 字符串，根据字符更新 x 和 y 的值。
3. 最后检查 x 和 y 是否都为 0，如果是则返回 True，否则返回 False。

关键点:
- 使用两个变量来记录位置变化。
- 通过遍历字符串来更新位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 moves 的长度，因为我们需要遍历整个字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def judgeCircle(moves: str) -> bool:
    """
    函数式接口 - 判断机器人是否能返回原点
    """
    x, y = 0, 0
    for move in moves:
        if move == 'U':
            y += 1
        elif move == 'D':
            y -= 1
        elif move == 'L':
            x -= 1
        elif move == 'R':
            x += 1
    return x == 0 and y == 0


Solution = create_solution(judgeCircle)