# ============================================================================
# 问题描述
# ============================================================================
"""
给定两个列表 A 和 B，其中 A 是 B 的排列。找到一个索引映射 P，使得 A[i] = B[P[i]]。

输入:
A = [12,28,46,32,50]
B = [50,12,32,46,28]

输出:
[1,4,3,2,0]

解释:
A[0] = 12, 在 B 中是第 1 个元素 (从 0 开始计数)
A[1] = 28, 在 B 中是第 4 个元素
A[2] = 46, 在 B 中是第 3 个元素
A[3] = 32, 在 B 中是第 2 个元素
A[4] = 50, 在 B 中是第 0 个元素
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录 B 中每个元素的索引，然后通过查找 A 中元素在 B 中的位置来构建映射。

算法步骤:
1. 创建一个哈希表，记录 B 中每个元素的索引。
2. 遍历 A，通过哈希表查找 A 中每个元素在 B 中的索引，并构建结果列表。

关键点:
- 使用哈希表可以在 O(1) 时间内查找 B 中元素的索引。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 A 和 B 的长度。我们需要遍历 B 来构建哈希表，再遍历 A 来构建结果列表。
空间复杂度: O(n)，哈希表需要存储 B 中所有元素的索引。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_anagram_mappings(A: List[int], B: List[int]) -> List[int]:
    """
    找出变位映射
    """
    # 创建一个哈希表，记录 B 中每个元素的索引
    index_map = {num: i for i, num in enumerate(B)}
    
    # 构建结果列表
    result = [index_map[num] for num in A]
    
    return result


Solution = create_solution(find_anagram_mappings)