"""
1672. 最富有客户的资产总量 - 给你一个 m x n 的整数网格 accounts ，其中 accounts[i][j] 是第 i 位客户在第 j 家银行托管的资产数量。返回最富有客户所拥有的 资产总量 。 客户的 资产总量 就是他们在各家银行托管的资产数量之和。最富有客户就是 资产总量 最大的客户。 示例 1： 输入：accounts = [[1,2,3],[3,2,1]] 输出：6 解释： 第 1 位客户的资产总量 = 1 + 2 + 3 = 6 第 2 位客户的资产总量 = 3 + 2 + 1 = 6 两位客户都是最富有的，资产总量都是 6 ，所以返回 6 。 示例 2： 输入：accounts = [[1,5],[7,3],[3,5]] 输出：10 解释： 第 1 位客户的资产总量 = 6 第 2 位客户的资产总量 = 10 第 3 位客户的资产总量 = 8 第 2 位客户是最富有的，资产总量是 10 示例 3： 输入：accounts = [[2,8,7],[7,1,3],[1,9,5]] 输出：17 提示： * m == accounts.length * n == accounts[i].length * 1 <= m, n <= 50 * 1 <= accounts[i][j] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历每个客户，计算资产总和，返回最大值

算法步骤:
1. 遍历每个客户
2. 计算每个客户的资产总和
3. 返回最大值

关键点:
- 简单遍历
- 时间复杂度O(m*n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m*n) - m为客户数，n为银行数
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def maximum_wealth(accounts: List[List[int]]) -> int:
    """
    函数式接口 - 最富有客户的资产总量
    
    实现思路:
    遍历每个客户，计算资产总和，返回最大值。
    
    Args:
        accounts: 客户资产矩阵
        
    Returns:
        最大资产总量
        
    Example:
        >>> maximum_wealth([[1,2,3],[3,2,1]])
        6
    """
    return max(sum(account) for account in accounts)


Solution = create_solution(maximum_wealth)
