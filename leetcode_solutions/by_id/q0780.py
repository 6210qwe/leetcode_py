"""
769. 最多能完成排序的块 - 给定一个长度为 n 的整数数组 arr ，它表示在 [0, n - 1] 范围内的整数的排列。 我们将 arr 分割成若干 块 (即分区)，并对每个块单独排序。将它们连接起来后，使得连接的结果和按升序排序后的原数组相同。 返回数组能分成的最多块数量。 示例 1: 输入: arr = [4,3,2,1,0] 输出: 1 解释: 将数组分成2块或者更多块，都无法得到所需的结果。 例如，分成 [4, 3], [2, 1, 0] 的结果是 [3, 4, 0, 1, 2]，这不是有序的数组。 示例 2: 输入: arr = [1,0,2,3,4] 输出: 4 解释: 我们可以把它分成两块，例如 [1, 0], [2, 3, 4]。 然而，分成 [1, 0], [2], [3], [4] 可以得到最多的块数。 对每个块单独排序后，结果为 [0, 1], [2], [3], [4] 提示: * n == arr.length * 1 <= n <= 10 * 0 <= arr[i] < n * arr 中每个元素都 不同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，遍历数组并记录当前最大值，当当前最大值等于当前索引时，说明可以分割成一个块。

算法步骤:
1. 初始化一个变量 `max_val` 用于记录当前遍历到的最大值。
2. 初始化一个变量 `chunks` 用于记录块的数量。
3. 遍历数组：
   - 更新 `max_val` 为当前元素和 `max_val` 中的较大值。
   - 如果 `max_val` 等于当前索引，则增加 `chunks` 计数。
4. 返回 `chunks`。

关键点:
- 通过比较当前最大值和当前索引，确定是否可以分割成一个块。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历整个数组一次。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_chunks_to_make_sorted(arr: List[int]) -> int:
    """
    函数式接口 - 返回数组能分成的最多块数量
    """
    max_val = 0
    chunks = 0
    for i, num in enumerate(arr):
        max_val = max(max_val, num)
        if max_val == i:
            chunks += 1
    return chunks


Solution = create_solution(max_chunks_to_make_sorted)