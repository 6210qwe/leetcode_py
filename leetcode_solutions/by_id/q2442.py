"""
2367. 等差三元组的数目 - 给你一个下标从 0 开始、严格递增 的整数数组 nums 和一个正整数 diff 。如果满足下述全部条件，则三元组 (i, j, k) 就是一个 等差三元组 ： * i < j < k ， * nums[j] - nums[i] == diff 且 * nums[k] - nums[j] == diff 返回不同 等差三元组 的数目。 示例 1： 输入：nums = [0,1,4,6,7,10], diff = 3 输出：2 解释： (1, 2, 4) 是等差三元组：7 - 4 == 3 且 4 - 1 == 3 。 (2, 4, 5) 是等差三元组：10 - 7 == 3 且 7 - 4 == 3 。 示例 2： 输入：nums = [4,5,6,7,8,9], diff = 2 输出：2 解释： (0, 2, 4) 是等差三元组：8 - 6 == 2 且 6 - 4 == 2 。 (1, 3, 5) 是等差三元组：9 - 7 == 2 且 7 - 5 == 2 。 提示： * 3 <= nums.length <= 200 * 0 <= nums[i] <= 200 * 1 <= diff <= 50 * nums 严格 递增
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合来存储数组中的元素，然后遍历数组，检查是否存在满足等差条件的三元组。

算法步骤:
1. 创建一个哈希集合，将数组中的所有元素加入集合。
2. 遍历数组中的每个元素，检查是否存在满足等差条件的三元组。
3. 如果存在，计数器加一。

关键点:
- 使用哈希集合可以在 O(1) 时间内检查元素是否存在。
- 只需要遍历一次数组即可找到所有满足条件的三元组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(n)，使用了一个哈希集合来存储数组中的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_arithmetic_triplets(nums: List[int], diff: int) -> int:
    """
    函数式接口 - 计算等差三元组的数量
    """
    # 创建一个哈希集合来存储数组中的元素
    num_set = set(nums)
    
    # 初始化计数器
    count = 0
    
    # 遍历数组中的每个元素
    for num in nums:
        # 检查是否存在满足等差条件的三元组
        if num + diff in num_set and num + 2 * diff in num_set:
            count += 1
    
    return count


Solution = create_solution(count_arithmetic_triplets)