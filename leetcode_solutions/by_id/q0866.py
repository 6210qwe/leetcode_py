"""
836. 矩形重叠 - 矩形以列表 [x1, y1, x2, y2] 的形式表示，其中 (x1, y1) 为左下角的坐标，(x2, y2) 是右上角的坐标。矩形的上下边平行于 x 轴，左右边平行于 y 轴。 如果相交的面积为 正 ，则称两矩形重叠。需要明确的是，只在角或边接触的两个矩形不构成重叠。 给出两个矩形 rec1 和 rec2 。如果它们重叠，返回 true；否则，返回 false 。 示例 1： 输入：rec1 = [0,0,2,2], rec2 = [1,1,3,3] 输出：true 示例 2： 输入：rec1 = [0,0,1,1], rec2 = [1,0,2,1] 输出：false 示例 3： 输入：rec1 = [0,0,1,1], rec2 = [2,2,3,3] 输出：false 提示： * rect1.length == 4 * rect2.length == 4 * -109 <= rec1[i], rec2[i] <= 109 * rec1 和 rec2 表示一个面积不为零的有效矩形
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断两个矩形是否重叠可以通过检查它们在 x 轴和 y 轴上的投影是否有重叠来实现。

算法步骤:
1. 检查 rec1 和 rec2 在 x 轴上的投影是否有重叠。
2. 检查 rec1 和 rec2 在 y 轴上的投影是否有重叠。
3. 如果在 x 轴和 y 轴上都有重叠，则两个矩形重叠，返回 True；否则返回 False。

关键点:
- 两个矩形在 x 轴上的投影有重叠的条件是：rec1 的最右端大于 rec2 的最左端，并且 rec1 的最左端小于 rec2 的最右端。
- 两个矩形在 y 轴上的投影有重叠的条件是：rec1 的最上端大于 rec2 的最下端，并且 rec1 的最下端小于 rec2 的最上端。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_rectangle_overlap(rec1: List[int], rec2: List[int]) -> bool:
    """
    判断两个矩形是否重叠
    """
    # 检查 x 轴上的投影是否有重叠
    if rec1[2] <= rec2[0] or rec2[2] <= rec1[0]:
        return False
    
    # 检查 y 轴上的投影是否有重叠
    if rec1[3] <= rec2[1] or rec2[3] <= rec1[1]:
        return False
    
    return True


Solution = create_solution(is_rectangle_overlap)