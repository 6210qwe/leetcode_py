"""
3096. 得到更多分数的最少关卡数目 - 给你一个长度为 n 的二进制数组 possible 。 Alice 和 Bob 正在玩一个有 n 个关卡的游戏，游戏中有一些关卡是 困难 模式，其他的关卡是 简单 模式。如果 possible[i] == 0 ，那么第 i 个关卡是 困难 模式，两个玩家 都不可能 通过。一个玩家通过一个简单模式的关卡可以获得 1 分，遇到困难模式的关卡将失去 1 分。 游戏的一开始，Alice 将从第 0 级开始 按顺序 完成一些关卡，然后 Bob 会完成剩下的所有关卡。 假设两名玩家都采取最优策略，目的是 最大化 自己的得分，Alice 想知道自己 最少 需要完成多少个关卡，才能获得比 Bob 更多的分数。 请你返回 Alice 获得比 Bob 更多的分数所需要完成的 最少 关卡数目，如果 无法 达成，那么返回 -1 。 注意，每个玩家都至少需要完成 1 个关卡。 示例 1： 输入：possible = [1,0,1,0] 输出：1 解释： 我们来看一下 Alice 可以完成的关卡数目： * 如果 Alice 只完成关卡 0 ，Bob 完成剩下的所有关卡，那么 Alice 获得 1 分，Bob 获得 -1 + 1 - 1 = -1 分。 * 如果 Alice 完成到关卡 1 ，Bob 完成剩下的所有关卡，那么 Alice 获得 1 - 1 = 0 分，Bob 获得 1 - 1 = 0 分。 * 如果 Alice 完成到关卡 2 ，Bob 完成剩下的所有关卡，那么 Alice 获得 1 - 1 + 1 = 1 分，Bob 获得 -1 分。 Alice 需要完成至少一个关卡获得更多的分数。 示例 2： 输入：possible = [1,1,1,1,1] 输出：3 解释： 我们来看一下 Alice 可以完成的关卡数目： * 如果 Alice 只完成关卡 0 ，Bob 完成剩下的所有关卡，那么 Alice 获得 1 分，Bob 获得 4 分。 * 如果 Alice 完成到关卡 1 ，Bob 完成剩下的所有关卡，那么 Alice 获得 2 分，Bob 获得 3 分。 * 如果 Alice 完成到关卡 2 ，Bob 完成剩下的所有关卡，那么 Alice 获得 3 分，Bob 获得 2 分。 * 如果 Alice 完成到关卡 3 ，Bob 完成剩下的所有关卡，那么 Alice 获得 4 分，Bob 获得 1 分。 Alice 需要完成至少三个关卡获得更多的分数。 示例 3： 输入：possible = [0,0] 输出：-1 解释： 两名玩家只能各完成 1 个关卡，Alice 完成关卡 0 得到 -1 分，Bob 完成关卡 1 得到 -1 分。两名玩家得分相同，所以 Alice 无法得到更多分数。 提示： * 2 <= n == possible.length <= 105 * possible[i] 要么是 0 要么是 1 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来计算 Alice 和 Bob 的得分，并找到 Alice 至少需要完成的关卡数目，使得她的得分高于 Bob。

算法步骤:
1. 计算前缀和数组 `prefix_sum`，其中 `prefix_sum[i]` 表示从第 0 个关卡到第 i 个关卡的总得分。
2. 遍历前缀和数组，找到第一个满足条件的索引 `i`，使得 `prefix_sum[i] > prefix_sum[-1] - prefix_sum[i]`。
3. 返回该索引 `i + 1`，即 Alice 需要完成的最少关卡数目。如果没有找到满足条件的索引，则返回 -1。

关键点:
- 使用前缀和数组可以高效地计算任意子数组的和。
- 通过遍历前缀和数组，可以在 O(n) 时间内找到满足条件的索引。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_levels_to_gain_more_points(possible: List[int]) -> int:
    """
    函数式接口 - 计算 Alice 获得比 Bob 更多的分数所需要完成的最少关卡数目
    """
    n = len(possible)
    if n < 2:
        return -1

    # 计算前缀和数组
    prefix_sum = [0] * (n + 1)
    for i in range(n):
        prefix_sum[i + 1] = prefix_sum[i] + (1 if possible[i] == 1 else -1)

    # 遍历前缀和数组，找到第一个满足条件的索引
    for i in range(1, n):
        if prefix_sum[i] > prefix_sum[-1] - prefix_sum[i]:
            return i

    return -1


Solution = create_solution(min_levels_to_gain_more_points)