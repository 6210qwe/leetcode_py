"""
2860. 让所有学生保持开心的分组方法数 - 给你一个下标从 0 开始、长度为 n 的整数数组 nums ，其中 n 是班级中学生的总数。班主任希望能够在让所有学生保持开心的情况下选出一组学生： 如果能够满足下述两个条件之一，则认为第 i 位学生将会保持开心： * 这位学生被选中，并且被选中的学生人数 严格大于 nums[i] 。 * 这位学生没有被选中，并且被选中的学生人数 严格小于 nums[i] 。 返回能够满足让所有学生保持开心的分组方法的数目。 示例 1： 输入：nums = [1,1] 输出：2 解释： 有两种可行的方法： 班主任没有选中学生。 班主任选中所有学生形成一组。 如果班主任仅选中一个学生来完成分组，那么两个学生都无法保持开心。因此，仅存在两种可行的方法。 示例 2： 输入：nums = [6,0,3,3,6,7,2,7] 输出：3 解释： 存在三种可行的方法： 班主任选中下标为 1 的学生形成一组。 班主任选中下标为 1、2、3、6 的学生形成一组。 班主任选中所有学生形成一组。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] < nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和枚举来找到所有可能的分组方法。

算法步骤:
1. 对数组进行排序。
2. 枚举每一种可能的分组方法，检查是否满足所有学生保持开心的条件。
3. 计算并返回满足条件的分组方法数。

关键点:
- 排序后的数组可以方便地枚举每一种可能的分组方法。
- 通过比较当前分组的人数和 `nums` 中的值来判断是否满足条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)，后续的遍历操作为 O(n)。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_happy_students(nums: List[int]) -> int:
    """
    函数式接口 - 计算让所有学生保持开心的分组方法数
    """
    # 对数组进行排序
    nums.sort()
    
    n = len(nums)
    count = 0
    
    # 检查不选择任何学生的情况
    if all(num > 0 for num in nums):
        count += 1
    
    # 检查选择所有学生的情况
    if all(num < n for num in nums):
        count += 1
    
    # 枚举每一种可能的分组方法
    for k in range(1, n):
        if nums[k - 1] < k and nums[k] > k:
            count += 1
    
    return count


Solution = create_solution(count_happy_students)