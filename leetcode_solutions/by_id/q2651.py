# ============================================================================
# 问题描述
# ============================================================================
"""
2580. 统计将重叠区间合并成组的方案数 - 给你一个二维整数数组 ranges ，其中 ranges[i] = [starti, endi] 表示 starti 到 endi 之间（包括二者）的所有整数都包含在第 i 个区间中。 你需要将 ranges 分成 两个 组（可以为空），满足： * 每个区间只属于一个组。 * 两个有 交集 的区间必须在 同一个 组内。 如果两个区间有至少 一个 公共整数，那么这两个区间是 有交集 的。 * 比方说，区间 [1, 3] 和 [2, 5] 有交集，因为 2 和 3 在两个区间中都被包含。 请你返回将 ranges 划分成两个组的 总方案数 。由于答案可能很大，将它对 109 + 7 取余 后返回。 示例 1： 输入：ranges = [[6,10],[5,15]] 输出：2 解释： 两个区间有交集，所以它们必须在同一个组内。 所以有两种方案： - 将两个区间都放在第 1 个组中。 - 将两个区间都放在第 2 个组中。 示例 2： 输入：ranges = [[1,3],[10,20],[2,5],[4,8]] 输出：4 解释： 区间 [1,3] 和 [2,5] 有交集，所以它们必须在同一个组中。 同理，区间 [2,5] 和 [4,8] 也有交集，所以它们也必须在同一个组中。 所以总共有 4 种分组方案： - 所有区间都在第 1 组。 - 所有区间都在第 2 组。 - 区间 [1,3] ，[2,5] 和 [4,8] 在第 1 个组中，[10,20] 在第 2 个组中。 - 区间 [1,3] ，[2,5] 和 [4,8] 在第 2 个组中，[10,20] 在第 1 个组中。 提示： * 1 <= ranges.length <= 105 * ranges[i].length == 2 * 0 <= starti <= endi <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和合并区间来统计不相交的区间组数，然后计算分组方案数。

算法步骤:
1. 对区间按起点进行排序。
2. 遍历排序后的区间，合并有交集的区间。
3. 计算不相交的区间组数。
4. 根据不相交的区间组数计算分组方案数。

关键点:
- 排序后合并区间可以有效地找出所有不相交的区间组。
- 每个不相交的区间组可以独立地分配到两个组中的任意一个。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 ranges 的长度，主要由排序操作决定。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def count_ways_to_group_overlapping_ranges(ranges: List[List[int]]) -> int:
    """
    返回将 ranges 划分成两个组的总方案数，并对 10^9 + 7 取余。
    """
    MOD = 10**9 + 7
    ranges.sort()  # 按起点排序
    merged_count = 1  # 初始化不相交的区间组数为 1
    current_end = ranges[0][1]  # 当前区间的结束位置

    for start, end in ranges[1:]:
        if start > current_end:
            # 如果当前区间的起点大于上一个区间的结束位置，则它们不相交
            merged_count += 1
        current_end = max(current_end, end)  # 更新当前区间的结束位置

    # 计算分组方案数
    return pow(2, merged_count, MOD)


Solution = create_solution(count_ways_to_group_overlapping_ranges)