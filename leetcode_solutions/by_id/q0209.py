"""
209. 长度最小的子数组 - 给定一个含有 n 个正整数的数组和一个正整数 target 。 找出该数组中满足其总和大于等于 target 的长度最小的 子数组 [numsl, numsl+1, ..., numsr-1, numsr] ，并返回其长度。如果不存在符合条件的子数组，返回 0 。 示例 1： 输入：target = 7, nums = [2,3,1,2,4,3] 输出：2 解释：子数组 [4,3] 是该条件下的长度最小的子数组。 示例 2： 输入：target = 4, nums = [1,4,4] 输出：1 示例 3： 输入：target = 11, nums = [1,1,1,1,1,1,1,1] 输出：0 提示： * 1 <= target <= 109 * 1 <= nums.length <= 105 * 1 <= nums[i] <= 104 进阶： * 如果你已经实现 O(n) 时间复杂度的解法, 请尝试设计一个 O(n log(n)) 时间复杂度的解法。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口，维护窗口内元素和大于等于target

算法步骤:
1. 使用左右指针维护滑动窗口
2. 右指针扩展窗口，累加元素和
3. 当和大于等于target时，收缩左指针
4. 更新最小长度

关键点:
- 使用滑动窗口维护子数组和
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组一次
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def minimum_size_subarray_sum(target: int, nums: List[int]) -> int:
    """
    函数式接口 - 长度最小的子数组
    
    实现思路:
    使用滑动窗口，维护窗口内元素和大于等于target。
    
    Args:
        target: 目标和
        nums: 正整数数组
        
    Returns:
        满足条件的长度最小的子数组长度
        
    Example:
        >>> minimum_size_subarray_sum(7, [2, 3, 1, 2, 4, 3])
        2
    """
    left = 0
    current_sum = 0
    min_len = float('inf')
    
    for right in range(len(nums)):
        current_sum += nums[right]
        
        while current_sum >= target:
            min_len = min(min_len, right - left + 1)
            current_sum -= nums[left]
            left += 1
    
    return min_len if min_len != float('inf') else 0


