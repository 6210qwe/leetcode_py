"""
1491. 去掉最低工资和最高工资后的工资平均值 - 给你一个整数数组 salary ，数组里每个数都是 唯一 的，其中 salary[i] 是第 i 个员工的工资。 请你返回去掉最低工资和最高工资以后，剩下员工工资的平均值。 示例 1： 输入：salary = [4000,3000,1000,2000] 输出：2500.00000 解释：最低工资和最高工资分别是 1000 和 4000 。 去掉最低工资和最高工资以后的平均工资是 (2000+3000)/2= 2500 示例 2： 输入：salary = [1000,2000,3000] 输出：2000.00000 解释：最低工资和最高工资分别是 1000 和 3000 。 去掉最低工资和最高工资以后的平均工资是 (2000)/1= 2000 示例 3： 输入：salary = [6000,5000,4000,3000,2000,1000] 输出：3500.00000 示例 4： 输入：salary = [8000,9000,2000,3000,6000,1000] 输出：4750.00000 提示： * 3 <= salary.length <= 100 * 10^3 <= salary[i] <= 10^6 * salary[i] 是唯一的。 * 与真实值误差在 10^-5 以内的结果都将视为正确答案。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历找到最小值和最大值，并计算总和，最后减去最小值和最大值后求平均值。

算法步骤:
1. 初始化最小值 `min_salary` 为正无穷大，最大值 `max_salary` 为负无穷大，总和 `total` 为 0。
2. 遍历 `salary` 数组，更新 `min_salary` 和 `max_salary`，并累加 `total`。
3. 计算去掉最小值和最大值后的平均值 `(total - min_salary - max_salary) / (len(salary) - 2)`。

关键点:
- 一次遍历即可完成最小值、最大值和总和的计算，时间复杂度最优。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 salary 数组的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def average_salary_excluding_min_max(salary: List[int]) -> float:
    """
    函数式接口 - 返回去掉最低工资和最高工资后的工资平均值
    """
    min_salary = float('inf')
    max_salary = float('-inf')
    total = 0
    
    for s in salary:
        if s < min_salary:
            min_salary = s
        if s > max_salary:
            max_salary = s
        total += s
    
    return (total - min_salary - max_salary) / (len(salary) - 2)


Solution = create_solution(average_salary_excluding_min_max)