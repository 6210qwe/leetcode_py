import pandas as pd

def npv_queries(values: pd.DataFrame) -> pd.DataFrame:
    """
    函数式接口 - 计算每个项目的净现值 (NPV)
    """
    # 按项目 ID 分组，并计算每个项目的 NPV
    result = values.groupby('project_id').agg(
        npv=('value', 'sum')
    ).reset_index()
    
    return result

