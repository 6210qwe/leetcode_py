"""
404. 左叶子之和 - 给定二叉树的根节点 root ，返回所有左叶子之和。 示例 1： [https://assets.leetcode.com/uploads/2021/04/08/leftsum-tree.jpg] 输入: root = [3,9,20,null,null,15,7] 输出: 24 解释: 在这个二叉树中，有两个左叶子，分别是 9 和 15，所以返回 24 示例 2: 输入: root = [1] 输出: 0 提示: * 节点数在 [1, 1000] 范围内 * -1000 <= Node.val <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 递归遍历二叉树，判断左子节点是否为叶子节点

算法步骤:
1. 递归遍历二叉树
2. 如果左子节点存在且为叶子节点，累加其值
3. 递归处理左右子树

关键点:
- 判断左子节点是否为叶子节点
- 时间复杂度O(n)，空间复杂度O(h)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要访问每个节点一次
空间复杂度: O(h) - 递归栈深度，h为树的高度
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_left_leaves(root: Optional[TreeNode]) -> int:
    """
    函数式接口 - 左叶子之和
    
    实现思路:
    递归遍历二叉树，判断左子节点是否为叶子节点。
    
    Args:
        root: 二叉树的根节点
        
    Returns:
        所有左叶子之和
        
    Example:
        >>> root = TreeNode.from_list([3, 9, 20, None, None, 15, 7])
        >>> sum_of_left_leaves(root)
        24
    """
    if not root:
        return 0
    
    total = 0
    
    # 检查左子节点是否为叶子节点
    if root.left and not root.left.left and not root.left.right:
        total += root.left.val
    
    # 递归处理左右子树
    total += sum_of_left_leaves(root.left)
    total += sum_of_left_leaves(root.right)
    
    return total


