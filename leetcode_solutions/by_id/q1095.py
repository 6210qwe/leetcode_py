"""
1029. 两地调度 - 公司计划面试 2n 人。给你一个数组 costs ，其中 costs[i] = [aCosti, bCosti] 。第 i 人飞往 a 市的费用为 aCosti ，飞往 b 市的费用为 bCosti 。 返回将每个人都飞到 a 、b 中某座城市的最低费用，要求每个城市都有 n 人抵达。 示例 1： 输入：costs = [[10,20],[30,200],[400,50],[30,20]] 输出：110 解释： 第一个人去 a 市，费用为 10。 第二个人去 a 市，费用为 30。 第三个人去 b 市，费用为 50。 第四个人去 b 市，费用为 20。 最低总费用为 10 + 30 + 50 + 20 = 110，每个城市都有一半的人在面试。 示例 2： 输入：costs = [[259,770],[448,54],[926,667],[184,139],[840,118],[577,469]] 输出：1859 示例 3： 输入：costs = [[515,563],[451,713],[537,709],[343,819],[855,779],[457,60],[650,359],[631,42]] 输出：3086 提示： * 2 * n == costs.length * 2 <= costs.length <= 100 * costs.length 为偶数 * 1 <= aCosti, bCosti <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算每个人去两个城市的差价，选择差价最小的人去更便宜的城市，从而达到总费用最小。

算法步骤:
1. 计算每个人去两个城市的差价，并按差价从小到大排序。
2. 将前一半的人分配到 A 城市，后一半的人分配到 B 城市。
3. 计算总费用。

关键点:
- 通过差价排序来决定每个人的最优选择。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 costs 的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def two_city_scheduling(costs: List[List[int]]) -> int:
    """
    函数式接口 - 两地调度
    """
    # 按照去 A 城市和 B 城市的差价从小到大排序
    costs.sort(key=lambda x: x[0] - x[1])
    
    total_cost = 0
    n = len(costs) // 2
    
    # 前一半的人去 A 城市
    for i in range(n):
        total_cost += costs[i][0]
    
    # 后一半的人去 B 城市
    for i in range(n, 2 * n):
        total_cost += costs[i][1]
    
    return total_cost

