"""
3386. 按下时间最长的按钮 - 给你一个二维数组 events，表示孩子在键盘上按下一系列按钮触发的按钮事件。 每个 events[i] = [indexi, timei] 表示在时间 timei 时，按下了下标为 indexi 的按钮。 * 数组按照 time 的递增顺序排序。 * 按下一个按钮所需的时间是连续两次按钮按下的时间差。按下第一个按钮所需的时间就是其时间戳。 返回按下时间 最长 的按钮的 index。如果有多个按钮的按下时间相同，则返回 index 最小的按钮。 示例 1： 输入： events = [[1,2],[2,5],[3,9],[1,15]] 输出： 1 解释： * 下标为 1 的按钮在时间 2 被按下。 * 下标为 2 的按钮在时间 5 被按下，因此按下时间为 5 - 2 = 3。 * 下标为 3 的按钮在时间 9 被按下，因此按下时间为 9 - 5 = 4。 * 下标为 1 的按钮再次在时间 15 被按下，因此按下时间为 15 - 9 = 6。 最终，下标为 1 的按钮按下时间最长，为 6。 示例 2： 输入： events = [[10,5],[1,7]] 输出： 10 解释： * 下标为 10 的按钮在时间 5 被按下。 * 下标为 1 的按钮在时间 7 被按下，因此按下时间为 7 - 5 = 2。 最终，下标为 10 的按钮按下时间最长，为 5。 提示： * 1 <= events.length <= 1000 * events[i] == [indexi, timei] * 1 <= indexi, timei <= 105 * 输入保证数组 events 按照 timei 的递增顺序排序。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历 events 数组，计算每个按钮的按下时间，并记录按下时间最长的按钮。

算法步骤:
1. 初始化变量 `max_time` 用于记录最长按下时间，`max_index` 用于记录按下时间最长的按钮索引。
2. 初始化 `prev_time` 为第一个事件的时间。
3. 遍历 events 数组，对于每个事件：
   - 计算当前事件与前一个事件的时间差 `time_diff`。
   - 更新 `max_time` 和 `max_index`，如果 `time_diff` 大于 `max_time` 或者 `time_diff` 等于 `max_time` 但当前按钮索引小于 `max_index`。
   - 更新 `prev_time` 为当前事件的时间。
4. 返回 `max_index`。

关键点:
- 通过一次遍历即可找到按下时间最长的按钮。
- 使用 `max_time` 和 `max_index` 来记录当前最长按下时间和对应的按钮索引。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 events 的长度。我们只需要遍历一次 events 数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


def solution_function_name(events: List[List[int]]) -> int:
    """
    函数式接口 - 找到按下时间最长的按钮
    """
    if not events:
        return -1

    max_time = 0
    max_index = events[0][0]
    prev_time = events[0][1]

    for i in range(1, len(events)):
        index, time = events[i]
        time_diff = time - prev_time
        if time_diff > max_time or (time_diff == max_time and index < max_index):
            max_time = time_diff
            max_index = index
        prev_time = time

    return max_index


