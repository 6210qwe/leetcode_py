"""
838. 推多米诺 - n 张多米诺骨牌排成一行，将每张多米诺骨牌垂直竖立。在开始时，同时把一些多米诺骨牌向左或向右推。 每过一秒，倒向左边的多米诺骨牌会推动其左侧相邻的多米诺骨牌。同样地，倒向右边的多米诺骨牌也会推动竖立在其右侧的相邻多米诺骨牌。 如果一张垂直竖立的多米诺骨牌的两侧同时有多米诺骨牌倒下时，由于受力平衡， 该骨牌仍然保持不变。 就这个问题而言，我们会认为一张正在倒下的多米诺骨牌不会对其它正在倒下或已经倒下的多米诺骨牌施加额外的力。 给你一个字符串 dominoes 表示这一行多米诺骨牌的初始状态，其中： * dominoes[i] = 'L'，表示第 i 张多米诺骨牌被推向左侧， * dominoes[i] = 'R'，表示第 i 张多米诺骨牌被推向右侧， * dominoes[i] = '.'，表示没有推动第 i 张多米诺骨牌。 返回表示最终状态的字符串。 示例 1： 输入：dominoes = "RR.L" 输出："RR.L" 解释：第一张多米诺骨牌没有给第二张施加额外的力。 示例 2： [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/05/18/domino.png] 输入：dominoes = ".L.R...LR..L.." 输出："LL.RR.LLRRLL.." 提示： * n == dominoes.length * 1 <= n <= 105 * dominoes[i] 为 'L'、'R' 或 '.'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来模拟多米诺骨牌的推倒过程。

算法步骤:
1. 在字符串两端添加虚拟的 'L' 和 'R'，以处理边界情况。
2. 使用双指针遍历字符串，找到连续的 'L' 和 'R' 之间的区间。
3. 根据区间的两端字符，决定中间的 '.' 的方向。
4. 更新字符串并返回最终结果。

关键点:
- 使用双指针可以有效地处理区间内的多米诺骨牌。
- 虚拟的 'L' 和 'R' 可以简化边界处理。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def push_dominoes(dominoes: str) -> str:
    """
    函数式接口 - 推多米诺骨牌
    """
    # 在字符串两端添加虚拟的 'L' 和 'R'
    dominoes = 'L' + dominoes + 'R'
    n = len(dominoes)
    result = list(dominoes)
    
    i = 0
    for j in range(1, n):
        if dominoes[j] == '.':
            continue
        if i > 0:  # 跳过开头的 'L'
            left, right = dominoes[i], dominoes[j]
            if left == right:
                result[i+1:j] = [left] * (j - i - 1)
            elif left == 'R' and right == 'L':
                k = (j - i - 1) // 2
                result[i+1:i+1+k] = ['R'] * k
                result[j-k:j] = ['L'] * k
                if (j - i - 1) % 2 == 1:
                    result[i+1+k] = '.'
        i = j
    
    return ''.join(result[1:-1])


Solution = create_solution(push_dominoes)