"""
核心思想: 使用滑动窗口来找到连续 k 个袋子的最大硬币数量。

算法步骤:
1. 将每个区间展开成单个袋子，并计算每个袋子的硬币数量。
2. 使用滑动窗口来找到连续 k 个袋子的最大硬币数量。

关键点:
- 展开区间时，使用前缀和来快速计算每个袋子的硬币数量。
- 滑动窗口用于高效地找到最大硬币数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
