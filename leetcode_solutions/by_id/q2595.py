"""
2507. 使用质因数之和替换后可以取到的最小值 - 给你一个正整数 n 。 请你将 n 的值替换为 n 的 质因数 之和，重复这一过程。 * 注意，如果 n 能够被某个质因数多次整除，则在求和时，应当包含这个质因数同样次数。 返回 n 可以取到的最小值。 示例 1： 输入：n = 15 输出：5 解释：最开始，n = 15 。 15 = 3 * 5 ，所以 n 替换为 3 + 5 = 8 。 8 = 2 * 2 * 2 ，所以 n 替换为 2 + 2 + 2 = 6 。 6 = 2 * 3 ，所以 n 替换为 2 + 3 = 5 。 5 是 n 可以取到的最小值。 示例 2： 输入：n = 3 输出：3 解释：最开始，n = 3 。 3 是 n 可以取到的最小值。 提示： * 2 <= n <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过不断将 n 替换为其质因数之和，直到 n 不再变化为止。

算法步骤:
1. 定义一个函数 `prime_factors_sum` 来计算 n 的所有质因数之和。
2. 在主函数中，不断调用 `prime_factors_sum` 并更新 n，直到 n 不再变化。
3. 返回最终的 n 值。

关键点:
- 使用试除法找到所有质因数并求和。
- 通过循环不断更新 n 直到其不再变化。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(sqrt(n) * log n)
- 每次找质因数的时间复杂度是 O(sqrt(n))，最坏情况下需要多次迭代。
空间复杂度: O(1)
- 除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def prime_factors_sum(n: int) -> int:
    """计算 n 的所有质因数之和"""
    factor_sum = 0
    # 试除法找质因数
    for i in range(2, int(n**0.5) + 1):
        while n % i == 0:
            factor_sum += i
            n //= i
    if n > 1:
        factor_sum += n
    return factor_sum


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 使用质因数之和替换后可以取到的最小值
    """
    while True:
        new_n = prime_factors_sum(n)
        if new_n == n:
            break
        n = new_n
    return n


Solution = create_solution(solution_function_name)