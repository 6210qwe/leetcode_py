# 问题描述
# ============================================================================
"""
3019. 按键变更的次数 - 给你一个下标从 0 开始的字符串 s ，该字符串由用户输入。按键变更的定义是：使用与上次使用的按键不同的键。例如 s = "ab" 表示按键变更一次，而 s = "bBBb" 不存在按键变更。 返回用户输入过程中按键变更的次数。 注意：shift 或 caps lock 等修饰键不计入按键变更，也就是说，如果用户先输入字母 'a' 然后输入字母 'A' ，不算作按键变更。 示例 1： 输入：s = "aAbBcC" 输出：2 解释： 从 s[0] = 'a' 到 s[1] = 'A'，不存在按键变更，因为不计入 caps lock 或 shift 。 从 s[1] = 'A' 到 s[2] = 'b'，按键变更。 从 s[2] = 'b' 到 s[3] = 'B'，不存在按键变更，因为不计入 caps lock 或 shift 。 从 s[3] = 'B' 到 s[4] = 'c'，按键变更。 从 s[4] = 'c' 到 s[5] = 'C'，不存在按键变更，因为不计入 caps lock 或 shift 。 示例 2： 输入：s = "AaAaAaaA" 输出：0 解释： 不存在按键变更，因为这个过程中只按下字母 'a' 和 'A' ，不需要进行按键变更。 提示： * 1 <= s.length <= 100 * s 仅由英文大写字母和小写字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串并比较相邻字符（忽略大小写）来计算按键变更的次数。

算法步骤:
1. 初始化一个计数器 `count` 为 0。
2. 将字符串转换为小写，以便忽略大小写的差异。
3. 遍历字符串，从第二个字符开始，比较当前字符和前一个字符。
4. 如果当前字符和前一个字符不同，则增加计数器 `count`。
5. 返回计数器 `count` 的值。

关键点:
- 忽略大小写的差异，将字符串转换为小写。
- 通过比较相邻字符来判断按键是否变更。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历整个字符串一次。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算按键变更的次数
    """
    # 将字符串转换为小写
    s = s.lower()
    count = 0
    
    # 遍历字符串，从第二个字符开始
    for i in range(1, len(s)):
        if s[i] != s[i - 1]:
            count += 1
    
    return count


Solution = create_solution(solution_function_name)