"""
2255. 统计是给定字符串前缀的字符串数目 - 给你一个字符串数组 words 和一个字符串 s ，其中 words[i] 和 s 只包含 小写英文字母 。 请你返回 words 中是字符串 s 前缀 的 字符串数目 。 一个字符串的 前缀 是出现在字符串开头的子字符串。子字符串 是一个字符串中的连续一段字符序列。 示例 1： 输入：words = ["a","b","c","ab","bc","abc"], s = "abc" 输出：3 解释： words 中是 s = "abc" 前缀的字符串为： "a" ，"ab" 和 "abc" 。 所以 words 中是字符串 s 前缀的字符串数目为 3 。 示例 2： 输入：words = ["a","a"], s = "aa" 输出：2 解释： 两个字符串都是 s 的前缀。 注意，相同的字符串可能在 words 中出现多次，它们应该被计数多次。 提示： * 1 <= words.length <= 1000 * 1 <= words[i].length, s.length <= 10 * words[i] 和 s 只 包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用字符串的 startswith 方法来检查每个 word 是否是 s 的前缀。

算法步骤:
1. 初始化一个计数器 count 为 0。
2. 遍历 words 数组，对于每个 word，使用 s.startswith(word) 检查是否是 s 的前缀。
3. 如果是前缀，则将计数器 count 加 1。
4. 返回计数器 count 的值。

关键点:
- 使用内置的 startswith 方法可以高效地检查前缀。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是 words 的长度，m 是 s 的长度。每个 word 的检查操作是 O(m)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_prefixes(words: List[str], s: str) -> int:
    """
    函数式接口 - 统计是给定字符串前缀的字符串数目
    """
    count = 0
    for word in words:
        if s.startswith(word):
            count += 1
    return count


Solution = create_solution(count_prefixes)