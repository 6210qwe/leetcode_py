"""
3282. 到达数组末尾的最大得分 - 给你一个长度为 n 的整数数组 nums 。 你的目标是从下标 0 出发，到达下标 n - 1 处。每次你只能移动到 更大 的下标处。 从下标 i 跳到下标 j 的得分为 (j - i) * nums[i] 。 请你返回你到达最后一个下标处能得到的 最大总得分 。 示例 1： 输入：nums = [1,3,1,5] 输出：7 解释： 一开始跳到下标 1 处，然后跳到最后一个下标处。总得分为 1 * 1 + 2 * 3 = 7 。 示例 2： 输入：nums = [4,3,1,3,2] 输出：16 解释： 直接跳到最后一个下标处。总得分为 4 * 4 = 16 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i] 表示到达下标 i 的最大得分。对于每个下标 i，我们可以从任意一个小于 i 的下标 j 跳过来，计算 (i - j) * nums[j] 并更新 dp[i]。

算法步骤:
1. 初始化 dp 数组，dp[0] = 0。
2. 遍历数组，对于每个下标 i，计算从所有小于 i 的下标 j 跳过来的最大得分，并更新 dp[i]。
3. 返回 dp[n-1] 作为结果。

关键点:
- 动态规划的状态转移方程是 dp[i] = max(dp[i], dp[j] + (i - j) * nums[j])。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算到达数组末尾的最大得分
    """
    n = len(nums)
    if n == 1:
        return 0

    dp = [0] * n

    for i in range(1, n):
        for j in range(i):
            dp[i] = max(dp[i], dp[j] + (i - j) * nums[j])

    return dp[n - 1]


Solution = create_solution(solution_function_name)