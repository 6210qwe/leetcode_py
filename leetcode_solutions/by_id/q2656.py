"""
2588. 统计美丽子数组数目 - 给你一个下标从 0 开始的整数数组nums 。每次操作中，你可以： * 选择两个满足 0 <= i, j < nums.length 的不同下标 i 和 j 。 * 选择一个非负整数 k ，满足 nums[i] 和 nums[j] 在二进制下的第 k 位（下标编号从 0 开始）是 1 。 * 将 nums[i] 和 nums[j] 都减去 2k 。 如果一个子数组内执行上述操作若干次（包括 0 次）后，该子数组可以变成一个全为 0 的数组，那么我们称它是一个 美丽 的子数组。 请你返回数组 nums 中 美丽子数组 的数目。 子数组是一个数组中一段连续 非空 的元素序列。 注意：所有元素最初都是 0 的子数组被认为是美丽的，因为不需要进行任何操作。 示例 1： 输入：nums = [4,3,1,2,4] 输出：2 解释：nums 中有 2 个美丽子数组：[4,3,1,2,4] 和 [4,3,1,2,4] 。 - 按照下述步骤，我们可以将子数组 [3,1,2] 中所有元素变成 0 ： - 选择 [3, 1, 2] 和 k = 1 。将 2 个数字都减去 21 ，子数组变成 [1, 1, 0] 。 - 选择 [1, 1, 0] 和 k = 0 。将 2 个数字都减去 20 ，子数组变成 [0, 0, 0] 。 - 按照下述步骤，我们可以将子数组 [4,3,1,2,4] 中所有元素变成 0 ： - 选择 [4, 3, 1, 2, 4] 和 k = 2 。将 2 个数字都减去 22 ，子数组变成 [0, 3, 1, 2, 0] 。 - 选择 [0, 3, 1, 2, 0] 和 k = 0 。将 2 个数字都减去 20 ，子数组变成 [0, 2, 0, 2, 0] 。 - 选择 [0, 2, 0, 2, 0] 和 k = 1 。将 2 个数字都减去 21 ，子数组变成 [0, 0, 0, 0, 0] 。 示例 2： 输入：nums = [1,10,4] 输出：0 解释：nums 中没有任何美丽子数组。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀异或和和哈希表来统计美丽子数组的数量。

算法步骤:
1. 初始化一个哈希表 `prefix_count`，用于记录每个前缀异或和出现的次数。
2. 初始化变量 `xor_sum` 为 0，用于存储当前的前缀异或和。
3. 初始化变量 `beautiful_count` 为 0，用于存储美丽子数组的数量。
4. 遍历数组 `nums`：
   - 更新 `xor_sum` 为 `xor_sum ^ num`。
   - 如果 `xor_sum` 已经在 `prefix_count` 中，则增加 `beautiful_count` 为 `prefix_count[xor_sum]`。
   - 更新 `prefix_count[xor_sum]` 的值加 1。
5. 返回 `beautiful_count`。

关键点:
- 使用前缀异或和来判断子数组是否美丽。
- 使用哈希表来记录前缀异或和的出现次数，以快速计算美丽子数组的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。我们只需要遍历数组一次。
空间复杂度: O(n)，哈希表 `prefix_count` 最多需要存储 n 个不同的前缀异或和。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_beautiful_subarrays(nums: List[int]) -> int:
    """
    函数式接口 - 统计美丽子数组的数量
    """
    prefix_count = {0: 1}  # 哈希表，初始值为 0: 1
    xor_sum = 0  # 当前前缀异或和
    beautiful_count = 0  # 美丽子数组的数量

    for num in nums:
        xor_sum ^= num  # 更新前缀异或和
        if xor_sum in prefix_count:
            beautiful_count += prefix_count[xor_sum]  # 更新美丽子数组的数量
        prefix_count[xor_sum] = prefix_count.get(xor_sum, 0) + 1  # 更新哈希表

    return beautiful_count


Solution = create_solution(count_beautiful_subarrays)