from leetcode_solutions.utils.solution import create_solution


def power_of_three(n: int) -> bool:
    """
    函数式接口 - 3的幂
    
    实现思路:
    使用数学方法，检查n是否能被3的幂整除。
    
    Args:
        n: 整数
        
    Returns:
        是否是3的幂次方
        
    Example:
        >>> power_of_three(27)
        True
    """
    if n <= 0:
        return False
    
    while n % 3 == 0:
        n //= 3
    
    return n == 1


