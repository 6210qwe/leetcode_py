"""
核心思想: 使用数学方法，检查n是否能被3的幂整除

算法步骤:
1. 如果n <= 0，返回False
2. 不断除以3，直到n不能被3整除
3. 如果最终n == 1，说明是3的幂

关键点:
- 使用循环或递归
- 时间复杂度O(log3(n))，空间复杂度O(1)
"""

