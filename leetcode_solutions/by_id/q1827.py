"""
1683. 无效的推文 - 表：Tweets +----------------+---------+ | Column Name | Type | +----------------+---------+ | tweet_id | int | | content | varchar | +----------------+---------+ 在 SQL 中，tweet_id 是这个表的主键。 content 只包含字母数字字符，'!'，' '，不包含其它特殊字符。 这个表包含某社交媒体 App 中所有的推文。 查询所有无效推文的编号（ID）。当推文内容中的字符数严格大于 15 时，该推文是无效的。 以任意顺序返回结果表。 查询结果格式如下所示： 示例 1： 输入： Tweets 表： +----------+----------------------------------+ | tweet_id | content | +----------+----------------------------------+ | 1 | Vote for Biden | | 2 | Let us make America great again! | +----------+----------------------------------+ 输出： +----------+ | tweet_id | +----------+ | 2 | +----------+ 解释： 推文 1 的长度 length = 14。该推文是有效的。 推文 2 的长度 length = 32。该推文是无效的。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 查询来筛选出内容长度大于 15 的推文。

算法步骤:
1. 从 Tweets 表中选择 tweet_id 和 content 列。
2. 使用 WHERE 子句过滤出 content 长度大于 15 的记录。
3. 返回结果表，只包含 tweet_id 列。

关键点:
- 使用 CHAR_LENGTH 函数计算 content 的长度。
- 确保查询结果只包含 tweet_id 列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 Tweets 表中的行数。需要遍历整个表来计算每个 content 的长度。
空间复杂度: O(1)，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(tweets: List[dict]) -> List[int]:
    """
    函数式接口 - 查询所有无效推文的编号（ID）
    """
    # 实现最优解法
    invalid_tweets = [tweet['tweet_id'] for tweet in tweets if len(tweet['content']) > 15]
    return invalid_tweets


Solution = create_solution(solution_function_name)