def decrypt_string(s: str) -> str:
    """
    函数式接口 - 解码字符串从字母到整数映射
    """
    result = []
    i = 0
    while i < len(s):
        if i + 2 < len(s) and s[i + 2] == '#':
            num = int(s[i:i+2])
            result.append(chr(num + ord('a') - 1))
            i += 3
        else:
            num = int(s[i])
            result.append(chr(num + ord('a') - 1))
            i += 1
    return ''.join(result)


Solution = create_solution(decrypt_string)