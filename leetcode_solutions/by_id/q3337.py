"""
3084. 统计以给定字符开头和结尾的子字符串总数 - 给你一个字符串 s 和一个字符 c 。返回在字符串 s 中并且以 c 字符开头和结尾的非空子字符串的总数。 示例 1： 输入：s = "abada", c = "a" 输出：6 解释：以 "a" 开头和结尾的子字符串有： "abada"、"abada"、"abada"、"abada"、"abada"、"abada"。 示例 2： 输入：s = "zzz", c = "z" 输出：6 解释：字符串 s 中总共有 6 个子字符串，并且它们都以 "z" 开头和结尾。 提示： * 1 <= s.length <= 105 * s 和 c 均由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算以给定字符开头和结尾的子字符串数量，可以通过统计字符 c 在字符串 s 中出现的位置来实现。具体来说，如果字符 c 在字符串 s 中出现了 n 次，那么以 c 开头和结尾的子字符串的数量就是 n * (n + 1) / 2。

算法步骤:
1. 遍历字符串 s，记录字符 c 出现的所有位置。
2. 计算这些位置的数量 n。
3. 返回 n * (n + 1) / 2 作为结果。

关键点:
- 通过统计字符 c 的出现位置，可以有效地计算出以 c 开头和结尾的子字符串数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历整个字符串一次。
空间复杂度: O(1)，我们只需要常数级的额外空间来存储计数器和结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_substrings(s: str, c: str) -> int:
    """
    计算以给定字符开头和结尾的子字符串数量。
    """
    # 统计字符 c 在字符串 s 中出现的次数
    count = s.count(c)
    
    # 计算以 c 开头和结尾的子字符串数量
    return count * (count + 1) // 2


Solution = create_solution(count_substrings)