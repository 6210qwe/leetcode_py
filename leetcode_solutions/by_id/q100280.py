"""
LCR 122. 路径加密 - 假定一段路径记作字符串 path，其中以 "." 作为分隔符。现需将路径加密，加密方法为将 path 中的分隔符替换为空格 " "，请返回加密后的字符串。 示例 1： 输入：path = "a.aef.qerf.bb" 输出："a aef qerf bb" 限制： 0 <= path.length <= 10000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 Python 的字符串替换方法 replace 来实现路径加密。

算法步骤:
1. 使用 replace 方法将所有的 "." 替换为空格 " "。
2. 返回替换后的字符串。

关键点:
- 使用内置的字符串方法可以高效地完成替换操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。replace 方法的时间复杂度是线性的。
空间复杂度: O(n)，因为 replace 方法会创建一个新的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(path: str) -> str:
    """
    函数式接口 - 将路径中的分隔符 "." 替换为空格 " "。
    """
    # 使用 replace 方法将所有的 "." 替换为空格 " "
    return path.replace(".", " ")


Solution = create_solution(solution_function_name)