"""
3028. 边界上的蚂蚁 - 边界上有一只蚂蚁，它有时向 左 走，有时向 右 走。 给你一个 非零 整数数组 nums 。蚂蚁会按顺序读取 nums 中的元素，从第一个元素开始直到结束。每一步，蚂蚁会根据当前元素的值移动： * 如果 nums[i] < 0 ，向 左 移动 -nums[i]单位。 * 如果 nums[i] > 0 ，向 右 移动 nums[i]单位。 返回蚂蚁 返回 到边界上的次数。 注意： * 边界两侧有无限的空间。 * 只有在蚂蚁移动了 |nums[i]| 单位后才检查它是否位于边界上。换句话说，如果蚂蚁只是在移动过程中穿过了边界，则不会计算在内。 示例 1： 输入：nums = [2,3,-5] 输出：1 解释：第 1 步后，蚂蚁距边界右侧 2 单位远。 第 2 步后，蚂蚁距边界右侧 5 单位远。 第 3 步后，蚂蚁位于边界上。 所以答案是 1 。 示例 2： 输入：nums = [3,2,-3,-4] 输出：0 解释：第 1 步后，蚂蚁距边界右侧 3 单位远。 第 2 步后，蚂蚁距边界右侧 5 单位远。 第 3 步后，蚂蚁距边界右侧 2 单位远。 第 4 步后，蚂蚁距边界左侧 2 单位远。 蚂蚁从未返回到边界上，所以答案是 0 。 提示： * 1 <= nums.length <= 100 * -10 <= nums[i] <= 10 * nums[i] != 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过累加数组中的元素来模拟蚂蚁的移动，并在每次移动后检查蚂蚁是否回到了边界。

算法步骤:
1. 初始化位置为 0。
2. 遍历数组，累加每个元素的值。
3. 每次累加后，检查当前位置是否为 0，如果是则计数器加 1。
4. 返回计数器的值。

关键点:
- 通过累加来模拟蚂蚁的移动。
- 只有在移动结束后检查蚂蚁是否回到边界。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算蚂蚁返回边界的次数
    """
    position = 0
    count = 0
    for num in nums:
        position += num
        if position == 0:
            count += 1
    return count


Solution = create_solution(solution_function_name)