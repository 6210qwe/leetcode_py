"""
3427. 变长子数组求和 - 给你一个长度为 n 的整数数组 nums 。对于 每个 下标 i（0 <= i < n），定义对应的子数组 nums[start ... i]（start = max(0, i - nums[i])）。 返回为数组中每个下标定义的子数组中所有元素的总和。 子数组 是数组中的一个连续、非空 的元素序列。 示例 1： 输入：nums = [2,3,1] 输出：11 解释： 下标 i 子数组 和 0 nums[0] = [2] 2 1 nums[0 ... 1] = [2, 3] 5 2 nums[1 ... 2] = [3, 1] 4 总和 11 总和为 11 。因此，输出 11 。 示例 2： 输入：nums = [3,1,1,2] 输出：13 解释： 下标 i 子数组 和 0 nums[0] = [3] 3 1 nums[0 ... 1] = [3, 1] 4 2 nums[1 ... 2] = [1, 1] 2 3 nums[1 ... 3] = [1, 1, 2] 4 总和 13 总和为 13 。因此，输出为 13 。 提示： * 1 <= n == nums.length <= 100 * 1 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来优化子数组求和的过程。

算法步骤:
1. 计算前缀和数组。
2. 遍历每个下标 i，计算对应的子数组和，并累加到结果中。

关键点:
- 使用前缀和可以快速计算任意子数组的和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_variable_length_subarrays(nums: List[int]) -> int:
    """
    函数式接口 - 计算变长子数组的和
    """
    n = len(nums)
    prefix_sum = [0] * (n + 1)

    # 计算前缀和数组
    for i in range(n):
        prefix_sum[i + 1] = prefix_sum[i] + nums[i]

    total_sum = 0

    # 遍历每个下标 i，计算对应的子数组和
    for i in range(n):
        start = max(0, i - nums[i])
        subarray_sum = prefix_sum[i + 1] - prefix_sum[start]
        total_sum += subarray_sum

    return total_sum


Solution = create_solution(sum_of_variable_length_subarrays)