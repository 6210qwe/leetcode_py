"""
1763. 最长的美好子字符串 - 当一个字符串 s 包含的每一种字母的大写和小写形式 同时 出现在 s 中，就称这个字符串 s 是 美好 字符串。比方说，"abABB" 是美好字符串，因为 'A' 和 'a' 同时出现了，且 'B' 和 'b' 也同时出现了。然而，"abA" 不是美好字符串因为 'b' 出现了，而 'B' 没有出现。 给你一个字符串 s ，请你返回 s 最长的 美好子字符串 。如果有多个答案，请你返回 最早 出现的一个。如果不存在美好子字符串，请你返回一个空字符串。 示例 1： 输入：s = "YazaAay" 输出："aAa" 解释："aAa" 是一个美好字符串，因为这个子串中仅含一种字母，其小写形式 'a' 和大写形式 'A' 也同时出现了。 "aAa" 是最长的美好子字符串。 示例 2： 输入：s = "Bb" 输出："Bb" 解释："Bb" 是美好字符串，因为 'B' 和 'b' 都出现了。整个字符串也是原字符串的子字符串。 示例 3： 输入：s = "c" 输出："" 解释：没有美好子字符串。 示例 4： 输入：s = "dDzeE" 输出："dD" 解释："dD" 和 "eE" 都是最长美好子字符串。 由于有多个美好子字符串，返回 "dD" ，因为它出现得最早。 提示： * 1 <= s.length <= 100 * s 只包含大写和小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用分治法来找到最长的美好子字符串。

算法步骤:
1. 如果字符串长度小于等于1，直接返回空字符串。
2. 使用一个集合记录所有字符。
3. 遍历字符串，检查每个字符及其对应的大写或小写形式是否都在集合中。
4. 如果某个字符及其对应的大写或小写形式不在集合中，则以该字符为分隔点，将字符串分成两部分，递归处理这两部分。
5. 返回两部分中最长的美好子字符串。

关键点:
- 使用分治法可以有效地减少重复计算。
- 通过集合快速判断字符及其对应的大写或小写形式是否存在。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2) - 在最坏情况下，每次分割都会产生两个子问题，总共需要 O(n^2) 的时间。
空间复杂度: O(n) - 递归调用栈的深度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_nice_substring(s: str) -> str:
    """
    函数式接口 - 实现最优解法
    """
    if len(s) <= 1:
        return ""

    char_set = set(s)
    
    for i in range(len(s)):
        if s[i].swapcase() not in char_set:
            left = longest_nice_substring(s[:i])
            right = longest_nice_substring(s[i+1:])
            return max(left, right, key=len)

    return s


Solution = create_solution(longest_nice_substring)