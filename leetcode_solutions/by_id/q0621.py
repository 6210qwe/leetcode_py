"""
621. 任务调度器 - 给你一个用字符数组 tasks 表示的 CPU 需要执行的任务列表，用字母 A 到 Z 表示，以及一个冷却时间 n。每个周期或时间间隔允许完成一项任务。任务可以按任何顺序完成，但有一个限制：两个 相同种类 的任务之间必须有长度为 n 的冷却时间。 返回完成所有任务所需要的 最短时间间隔 。 示例 1： 输入：tasks = ["A","A","A","B","B","B"], n = 2 输出：8 解释： 在完成任务 A 之后，你必须等待两个间隔。对任务 B 来说也是一样。在第 3 个间隔，A 和 B 都不能完成，所以你需要待命。在第 4 个间隔，由于已经经过了 2 个间隔，你可以再次执行 A 任务。 示例 2： 输入：tasks = ["A","C","A","B","D","B"], n = 1 输出：6 解释：一种可能的序列是：A -> B -> C -> D -> A -> B。 由于冷却间隔为 1，你可以在完成另一个任务后重复执行这个任务。 示例 3： 输入：tasks = ["A","A","A","B","B","B"], n = 3 输出：10 解释：一种可能的序列为：A -> B -> idle -> idle -> A -> B -> idle -> idle -> A -> B。 只有两种任务类型，A 和 B，需要被 3 个间隔分割。这导致重复执行这些任务的间隔当中有两次待命状态。 提示： * 1 <= tasks.length <= 104 * tasks[i] 是大写英文字母 * 0 <= n <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和最大堆来安排任务，以确保最短的时间间隔。

算法步骤:
1. 统计每个任务的频率。
2. 将任务频率存入最大堆中。
3. 每次从堆中取出最多 n+1 个任务执行，并将剩余的任务频率重新放回堆中。
4. 计算总的时间间隔。

关键点:
- 使用最大堆来动态获取当前频率最高的任务。
- 每次从堆中取出 n+1 个任务，确保满足冷却时间要求。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log k)，其中 n 是任务的数量，k 是不同任务的种类数。每次插入和删除堆的操作时间复杂度为 O(log k)。
空间复杂度: O(k)，用于存储任务频率的计数和最大堆。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
import heapq

def solution_function_name(tasks: List[str], n: int) -> int:
    """
    函数式接口 - 实现任务调度器
    """
    # 统计每个任务的频率
    task_counts = [0] * 26
    for task in tasks:
        task_counts[ord(task) - ord('A')] += 1
    
    # 将任务频率存入最大堆
    max_heap = [-count for count in task_counts if count > 0]
    heapq.heapify(max_heap)
    
    time = 0
    while max_heap:
        temp = []
        for _ in range(n + 1):
            if max_heap:
                temp.append(heapq.heappop(max_heap))
            time += 1
            if not max_heap and all(x == 0 for x in temp):
                break
        for count in temp:
            if count < -1:
                heapq.heappush(max_heap, count + 1)
    
    return time

Solution = create_solution(solution_function_name)