# 问题描述
# ============================================================================
"""
面试题 01.03. URL化 - URL化。编写一种方法，将字符串中的空格全部替换为%20。假定该字符串尾部有足够的空间存放新增字符，并且知道字符串的“真实”长度。（注：用Java实现的话，请使用字符数组实现，以便直接在数组上操作。） 示例 1： 输入："Mr John Smith ", 13 输出："Mr%20John%20Smith" 示例 2： 输入：" ", 5 输出："%20%20%20%20%20" 提示： * 字符串长度在 [0, 500000] 范围内。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针从后向前遍历字符串，将空格替换为%20。

算法步骤:
1. 计算字符串中空格的数量。
2. 根据空格数量计算新字符串的长度。
3. 从后向前遍历字符串，将非空格字符复制到新位置，将空格替换为%20。

关键点:
- 从后向前遍历可以避免覆盖未处理的字符。
- 直接在原字符串上进行修改，减少额外空间的使用。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(text: str, length: int) -> str:
    """
    函数式接口 - 将字符串中的空格全部替换为%20
    """
    # 计算空格数量
    space_count = text[:length].count(' ')
    
    # 计算新字符串的长度
    new_length = length + space_count * 2
    
    # 创建一个足够大的字符列表来存储结果
    result = list(text)
    result.extend([' '] * (new_length - length))
    
    # 从后向前遍历字符串
    index = new_length - 1
    for i in range(length - 1, -1, -1):
        if result[i] == ' ':
            result[index] = '0'
            result[index - 1] = '2'
            result[index - 2] = '%'
            index -= 3
        else:
            result[index] = result[i]
            index -= 1
    
    return ''.join(result)


Solution = create_solution(solution_function_name)