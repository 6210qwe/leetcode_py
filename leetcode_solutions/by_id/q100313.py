"""
核心思想: 通过计算不同位数的数字范围，找到第 k 位数字所在的数字和具体位置。

算法步骤:
1. 计算 k 位数字所在的数字的位数。
2. 确定 k 位数字所在的数字的具体值。
3. 找到 k 位数字在该数字中的具体位置并返回。

关键点:
- 通过逐步缩小范围，确定 k 位数字所在的数字。
- 使用数学公式计算不同位数的数字范围。
"""

