from leetcode_solutions.utils.solution import create_solution


def reverse_bits(n: int) -> int:
    """
    函数式接口 - 颠倒二进制位
    
    实现思路:
    逐位提取并反转，从右到左提取每一位，从左到右放置。
    
    Args:
        n: 32位无符号整数
        
    Returns:
        颠倒二进制位后的整数
        
    Example:
        >>> reverse_bits(43261596)
        964176192
    """
    result = 0
    
    for i in range(32):
        result <<= 1
        result |= n & 1
        n >>= 1
    
    return result


