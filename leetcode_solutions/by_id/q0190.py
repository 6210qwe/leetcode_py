"""
核心思想: 逐位提取并反转，从右到左提取每一位，从左到右放置

算法步骤:
1. 初始化结果为0
2. 遍历32位，每次提取n的最低位
3. 将提取的位放到结果的对应位置
4. n右移，result左移

关键点:
- 逐位提取和放置
- 时间复杂度O(1)，空间复杂度O(1)
"""

