def is_subsequence(s: str, t: str) -> bool:
    """
    函数式接口 - 判断子序列
    
    实现思路:
    使用双指针遍历两个字符串，匹配s中的字符。
    
    Args:
        s: 子序列字符串
        t: 目标字符串
        
    Returns:
        s是否为t的子序列
        
    Example:
        >>> is_subsequence("abc", "ahbgdc")
        True
    """
    if not s:
        return True
    
    i = 0
    for char in t:
        if i < len(s) and char == s[i]:
            i += 1
        if i == len(s):
            return True
    
    return False


