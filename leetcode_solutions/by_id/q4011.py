"""
3678. 大于平均值的最小未出现正整数 - 给你一个整数数组 nums。 返回 nums 中 严格大于 nums 中所有元素 平均值 的 最小未出现正整数。 数组的 平均值 定义为数组中所有元素的总和除以元素的数量。 示例 1: 输入: nums = [3,5] 输出: 6 解释: * nums 的平均值是 (3 + 5) / 2 = 8 / 2 = 4 。 * 大于 4 的最小未出现正整数是 6。 示例 2: 输入: nums = [-1,1,2] 输出: 3 解释: * nums 的平均值是 (-1 + 1 + 2) / 3 = 2 / 3 = 0.667 。 * 大于 0.667 的最小未出现正整数是 3 。 示例 3: 输入: nums = [4,-1] 输出: 2 解释: * nums 的平均值是 (4 + (-1)) / 2 = 3 / 2 = 1.50。 * 大于 1.50 的最小未出现正整数是 2。 提示: * 1 <= nums.length <= 100 * -100 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算数组的平均值，然后从 1 开始逐个检查正整数是否在数组中出现过，直到找到第一个大于平均值且未出现的正整数。

算法步骤:
1. 计算数组的平均值。
2. 从 1 开始逐个检查正整数是否在数组中出现过，直到找到第一个大于平均值且未出现的正整数。

关键点:
- 使用集合来存储数组中的元素，以便快速查找。
- 从 1 开始逐个检查正整数，确保找到的是最小的未出现正整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 返回大于平均值的最小未出现正整数
    """
    # 计算数组的平均值
    average = sum(nums) / len(nums)
    
    # 使用集合存储数组中的元素
    num_set = set(nums)
    
    # 从 1 开始逐个检查正整数
    i = 1
    while True:
        if i > average and i not in num_set:
            return i
        i += 1


Solution = create_solution(solution_function_name)