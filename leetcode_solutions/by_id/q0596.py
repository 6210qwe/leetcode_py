"""
596. 超过 5 名学生的课 - 表: Courses +-------------+---------+ | Column Name | Type | +-------------+---------+ | student | varchar | | class | varchar | +-------------+---------+ (student, class)是该表的主键（不同值的列的组合）。 该表的每一行表示学生的名字和他们注册的课程。 查询 至少有 5 个学生 的所有课程。 以 任意顺序 返回结果表。 结果格式如下所示。 示例 1: 输入: Courses table: +---------+----------+ | student | class | +---------+----------+ | A | Math | | B | English | | C | Math | | D | Biology | | E | Math | | F | Computer | | G | Math | | H | Math | | I | Math | +---------+----------+ 输出: +---------+ | class | +---------+ | Math | +---------+ 解释: -数学课有 6 个学生，所以我们包括它。 -英语课有 1 名学生，所以我们不包括它。 -生物课有 1 名学生，所以我们不包括它。 -计算机课有 1 个学生，所以我们不包括它。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 查询来统计每门课程的学生数量，并筛选出学生数量大于等于 5 的课程。

算法步骤:
1. 使用 GROUP BY 对课程进行分组。
2. 使用 COUNT 统计每门课程的学生数量。
3. 使用 HAVING 筛选出学生数量大于等于 5 的课程。

关键点:
- 使用 GROUP BY 和 HAVING 来筛选符合条件的课程。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 Courses 表中的行数。
空间复杂度: O(1)，因为只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

def solution_function_name(courses):
    """
    函数式接口 - 实现查询至少有 5 个学生的所有课程
    """
    # 使用 SQL 查询来实现
    query = """
    SELECT class
    FROM courses
    GROUP BY class
    HAVING COUNT(student) >= 5
    """
