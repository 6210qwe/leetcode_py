# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用生成器来生成从 1 到 10^cnt - 1 的所有整数。

算法步骤:
1. 计算最大值 10^cnt - 1。
2. 使用生成器生成从 1 到最大值的所有整数。

关键点:
- 使用生成器可以节省内存，避免一次性生成大量数据。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(10^n)，其中 n 是输入的位数 cnt。
空间复杂度: O(1)，除了输出结果外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(cnt: int) -> List[int]:
    """
    函数式接口 - 返回从 1 到 10^cnt - 1 的所有整数。
    """
    max_value = 10 ** cnt - 1
    return [i for i in range(1, max_value + 1)]


Solution = create_solution(solution_function_name)