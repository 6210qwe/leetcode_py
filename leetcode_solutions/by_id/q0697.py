"""
697. 数组的度 - 给定一个非空且只包含非负数的整数数组 nums，数组的 度 的定义是指数组里任一元素出现频数的最大值。 你的任务是在 nums 中找到与 nums 拥有相同大小的度的最短连续子数组，返回其长度。 示例 1： 输入：nums = [1,2,2,3,1] 输出：2 解释： 输入数组的度是 2 ，因为元素 1 和 2 的出现频数最大，均为 2 。 连续子数组里面拥有相同度的有如下所示： [1, 2, 2, 3, 1], [1, 2, 2, 3], [2, 2, 3, 1], [1, 2, 2], [2, 2, 3], [2, 2] 最短连续子数组 [2, 2] 的长度为 2 ，所以返回 2 。 示例 2： 输入：nums = [1,2,2,3,1,4,2] 输出：6 解释： 数组的度是 3 ，因为元素 2 重复出现 3 次。 所以 [2,2,3,1,4,2] 是最短子数组，因此返回 6 。 提示： * nums.length 在 1 到 50,000 范围内。 * nums[i] 是一个在 0 到 49,999 范围内的整数。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个元素的频率、第一次出现的位置和最后一次出现的位置，然后遍历哈希表找到度最大的元素，并计算其最短子数组的长度。

算法步骤:
1. 初始化三个哈希表分别记录每个元素的频率、第一次出现的位置和最后一次出现的位置。
2. 遍历数组，更新这三个哈希表。
3. 遍历频率哈希表，找到度最大的元素，并计算其最短子数组的长度。
4. 返回最短子数组的长度。

关键点:
- 使用哈希表记录每个元素的频率、第一次出现的位置和最后一次出现的位置。
- 通过遍历频率哈希表找到度最大的元素，并计算其最短子数组的长度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组两次，一次用于填充哈希表，一次用于找到度最大的元素。
空间复杂度: O(n)，我们使用了三个哈希表来存储每个元素的频率、第一次出现的位置和最后一次出现的位置。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_shortest_subarray(nums: List[int]) -> int:
    """
    函数式接口 - 找到与 nums 拥有相同大小的度的最短连续子数组的长度
    """
    # 哈希表记录每个元素的频率
    count = {}
    # 哈希表记录每个元素第一次出现的位置
    first_occurrence = {}
    # 哈希表记录每个元素最后一次出现的位置
    last_occurrence = {}
    
    # 遍历数组，更新哈希表
    for i, num in enumerate(nums):
        if num not in first_occurrence:
            first_occurrence[num] = i
        last_occurrence[num] = i
        count[num] = count.get(num, 0) + 1
    
    # 找到度最大的元素，并计算其最短子数组的长度
    max_degree = max(count.values())
    min_length = float('inf')
    for num in count:
        if count[num] == max_degree:
            min_length = min(min_length, last_occurrence[num] - first_occurrence[num] + 1)
    
    return min_length


Solution = create_solution(find_shortest_subarray)