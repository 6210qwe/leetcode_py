"""
2537. 统计好子数组的数目 - 给你一个整数数组 nums 和一个整数 k ，请你返回 nums 中 好 子数组的数目。 一个子数组 arr 如果有 至少 k 对下标 (i, j) 满足 i < j 且 arr[i] == arr[j] ，那么称它是一个 好 子数组。 子数组 是原数组中一段连续 非空 的元素序列。 示例 1： 输入：nums = [1,1,1,1,1], k = 10 输出：1 解释：唯一的好子数组是这个数组本身。 示例 2： 输入：nums = [3,1,4,3,2,2,4], k = 2 输出：4 解释：总共有 4 个不同的好子数组： - [3,1,4,3,2,2] 有 2 对。 - [3,1,4,3,2,2,4] 有 3 对。 - [1,4,3,2,2,4] 有 2 对。 - [4,3,2,2,4] 有 2 对。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i], k <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和哈希表来统计每个子数组中相同元素的对数。

算法步骤:
1. 初始化两个指针 left 和 right，分别表示滑动窗口的左右边界。
2. 使用一个哈希表 count 来记录当前窗口内每个元素的出现次数。
3. 移动右指针扩展窗口，更新哈希表，并计算当前窗口内的相同元素对数。
4. 当相同元素对数达到或超过 k 时，移动左指针收缩窗口，并更新结果。
5. 重复步骤 3 和 4，直到右指针遍历完整个数组。

关键点:
- 使用滑动窗口技术来高效地找到所有满足条件的子数组。
- 通过哈希表记录元素出现次数，快速计算相同元素对数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个元素最多被访问两次（一次通过右指针，一次通过左指针）。
空间复杂度: O(n)，哈希表在最坏情况下需要存储 n 个不同的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_good_subarrays(nums: List[int], k: int) -> int:
    """
    函数式接口 - 统计好子数组的数目
    """
    left = 0
    count = {}
    pairs = 0
    result = 0
    
    for right in range(len(nums)):
        if nums[right] in count:
            count[nums[right]] += 1
            pairs += count[nums[right]] - 1
        else:
            count[nums[right]] = 1
        
        while pairs >= k:
            result += len(nums) - right
            count[nums[left]] -= 1
            if count[nums[left]] > 0:
                pairs -= count[nums[left]]
            left += 1
    
    return result


Solution = create_solution(count_good_subarrays)