def solution_function_name():
    """
    函数式接口 - 返回一个 SQL 查询语句，用于过滤出评分小于 3 的问题。
    """
    # 实现最优解法
    query = """
    SELECT *
    FROM problems
    WHERE score < 3;
    """
    return query


