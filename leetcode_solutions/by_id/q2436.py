"""
2357. 使数组中所有元素都等于零 - 给你一个非负整数数组 nums 。在一步操作中，你必须： * 选出一个正整数 x ，x 需要小于或等于 nums 中 最小 的 非零 元素。 * nums 中的每个正整数都减去 x。 返回使 nums 中所有元素都等于 0 需要的 最少 操作数。 示例 1： 输入：nums = [1,5,0,3,5] 输出：3 解释： 第一步操作：选出 x = 1 ，之后 nums = [0,4,0,2,4] 。 第二步操作：选出 x = 2 ，之后 nums = [0,2,0,0,2] 。 第三步操作：选出 x = 2 ，之后 nums = [0,0,0,0,0] 。 示例 2： 输入：nums = [0] 输出：0 解释：nums 中的每个元素都已经是 0 ，所以不需要执行任何操作。 提示： * 1 <= nums.length <= 100 * 0 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来记录数组中的不同非零元素，每次选择最小的非零元素进行操作。

算法步骤:
1. 将数组中的非零元素放入集合中。
2. 计算集合的大小，即为需要的操作次数。

关键点:
- 使用集合可以自动去重，并且可以快速找到最小的非零元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 遍历数组一次，将非零元素放入集合。
空间复杂度: O(n) - 使用集合存储非零元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def make_array_zero(nums: List[int]) -> int:
    """
    函数式接口 - 使数组中所有元素都等于零
    """
    # 使用集合记录非零元素
    non_zero_elements = set()
    for num in nums:
        if num > 0:
            non_zero_elements.add(num)
    
    # 集合的大小即为所需的操作次数
    return len(non_zero_elements)


Solution = create_solution(make_array_zero)