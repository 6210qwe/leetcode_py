"""
2215. 找出两数组的不同 - 给你两个下标从 0 开始的整数数组 nums1 和 nums2 ，请你返回一个长度为 2 的列表 answer ，其中： * answer[0] 是 nums1 中所有 不 存在于 nums2 中的 不同 整数组成的列表。 * answer[1] 是 nums2 中所有 不 存在于 nums1 中的 不同 整数组成的列表。 注意：列表中的整数可以按 任意 顺序返回。 示例 1： 输入：nums1 = [1,2,3], nums2 = [2,4,6] 输出：[[1,3],[4,6]] 解释： 对于 nums1 ，nums1[1] = 2 出现在 nums2 中下标 0 处，然而 nums1[0] = 1 和 nums1[2] = 3 没有出现在 nums2 中。因此，answer[0] = [1,3]。 对于 nums2 ，nums2[0] = 2 出现在 nums1 中下标 1 处，然而 nums2[1] = 4 和 nums2[2] = 6 没有出现在 nums1 中。因此，answer[1] = [4,6]。 示例 2： 输入：nums1 = [1,2,3,3], nums2 = [1,1,2,2] 输出：[[3],[]] 解释： 对于 nums1 ，nums1[2] 和 nums1[3] 没有出现在 nums2 中。由于 nums1[2] == nums1[3] ，二者的值只需要在 answer[0] 中出现一次，故 answer[0] = [3]。 nums2 中的每个整数都在 nums1 中出现，因此，answer[1] = [] 。 提示： * 1 <= nums1.length, nums2.length <= 1000 * -1000 <= nums1[i], nums2[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来存储两个数组中的元素，然后通过集合操作找出不同元素。

算法步骤:
1. 将 nums1 和 nums2 转换为集合 set1 和 set2。
2. 使用集合差集操作找到 set1 中不在 set2 中的元素，即 set1 - set2。
3. 使用集合差集操作找到 set2 中不在 set1 中的元素，即 set2 - set1。
4. 返回结果列表 [list(set1 - set2), list(set2 - set1)]。

关键点:
- 使用集合操作可以高效地找到不同元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 和 m 分别是 nums1 和 nums2 的长度。集合操作的时间复杂度接近 O(1)。
空间复杂度: O(n + m)，需要额外的空间来存储两个集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def find_difference(nums1: List[int], nums2: List[int]) -> List[List[int]]:
    """
    返回两个数组的不同元素。
    """
    set1 = set(nums1)
    set2 = set(nums2)
    
    diff1 = list(set1 - set2)
    diff2 = list(set2 - set1)
    
    return [diff1, diff2]

