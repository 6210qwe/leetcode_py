from typing import List

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找出强数对的最大异或值
    """
    max_xor = 0
    for i in range(len(nums)):
        for j in range(i, len(nums)):
            if abs(nums[i] - nums[j]) <= min(nums[i], nums[j]):
                max_xor = max(max_xor, nums[i] ^ nums[j])
    return max_xor

