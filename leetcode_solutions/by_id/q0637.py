"""
637. 二叉树的层平均值 - 给定一个非空二叉树的根节点 root , 以数组的形式返回每一层节点的平均值。与实际答案相差 10-5 以内的答案可以被接受。 示例 1： [https://assets.leetcode.com/uploads/2021/03/09/avg1-tree.jpg] 输入：root = [3,9,20,null,null,15,7] 输出：[3.00000,14.50000,11.00000] 解释：第 0 层的平均值为 3,第 1 层的平均值为 14.5,第 2 层的平均值为 11 。 因此返回 [3, 14.5, 11] 。 示例 2: [https://assets.leetcode.com/uploads/2021/03/09/avg2-tree.jpg] 输入：root = [3,9,20,15,7] 输出：[3.00000,14.50000,11.00000] 提示： * 树中节点数量在 [1, 104] 范围内 * -231 <= Node.val <= 231 - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用广度优先搜索（BFS）遍历二叉树，逐层计算每层节点的平均值。

算法步骤:
1. 初始化一个队列，将根节点加入队列。
2. 进行层次遍历：
   - 对于当前层的所有节点，计算它们的总和和节点数。
   - 将当前层的平均值加入结果列表。
   - 将当前层所有节点的子节点加入队列，准备处理下一层。
3. 返回结果列表。

关键点:
- 使用队列进行层次遍历。
- 每层计算节点的总和和节点数，然后计算平均值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数。每个节点只访问一次。
空间复杂度: O(n)，队列的最大空间消耗为树的一层节点数，最坏情况下为 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def averageOfLevels(root: Optional[TreeNode]) -> List[float]:
    """
    函数式接口 - 计算二叉树每层的平均值
    """
    if not root:
        return []

    result = []
    queue = [root]

    while queue:
        level_sum = 0
        level_count = len(queue)
        for _ in range(level_count):
            node = queue.pop(0)
            level_sum += node.val
            if node.left:
                queue.append(node.left)
            if node.right:
                queue.append(node.right)
        result.append(level_sum / level_count)

    return result


Solution = create_solution(averageOfLevels)