from typing import List

def canVisitAllRooms(rooms: List[List[int]]) -> bool:
    def dfs(room: int):
        if visited[room]:
            return
        visited[room] = True
        for key in rooms[room]:
            dfs(key)
    
    n = len(rooms)
    visited = [False] * n
    dfs(0)
    return all(visited)

