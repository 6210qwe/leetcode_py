"""
3146. 两个字符串的排列差 - 给你两个字符串 s 和 t，每个字符串中的字符都不重复，且 t 是 s 的一个排列。 排列差 定义为 s 和 t 中每个字符在两个字符串中位置的绝对差值之和。 返回 s 和 t 之间的 排列差 。 示例 1： 输入：s = "abc", t = "bac" 输出：2 解释： 对于 s = "abc" 和 t = "bac"，排列差是： * "a" 在 s 中的位置与在 t 中的位置之差的绝对值。 * "b" 在 s 中的位置与在 t 中的位置之差的绝对值。 * "c" 在 s 中的位置与在 t 中的位置之差的绝对值。 即，s 和 t 的排列差等于 |0 - 1| + |1 - 0| + |2 - 2| = 2。 示例 2： 输入：s = "abcde", t = "edbac" 输出：12 解释： s 和 t 的排列差等于 |0 - 3| + |1 - 2| + |2 - 4| + |3 - 1| + |4 - 0| = 12。 提示： * 1 <= s.length <= 26 * 每个字符在 s 中最多出现一次。 * t 是 s 的一个排列。 * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个字符在 s 和 t 中的位置，然后计算每个字符的位置差的绝对值之和。

算法步骤:
1. 创建两个字典分别记录 s 和 t 中每个字符的位置。
2. 遍历 s 中的每个字符，计算其在 s 和 t 中的位置差的绝对值，并累加到结果中。

关键点:
- 使用字典来存储字符的位置，可以快速查找。
- 由于 s 和 t 的长度相同且 t 是 s 的一个排列，因此可以直接遍历 s 来计算位置差。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历字符串 s 和 t 各一次。
空间复杂度: O(n)，我们使用了两个字典来存储字符的位置。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str, t: str) -> int:
    """
    函数式接口 - 计算两个字符串的排列差
    """
    # 创建两个字典来记录 s 和 t 中每个字符的位置
    pos_s = {char: idx for idx, char in enumerate(s)}
    pos_t = {char: idx for idx, char in enumerate(t)}

    # 计算每个字符的位置差的绝对值之和
    diff_sum = sum(abs(pos_s[char] - pos_t[char]) for char in s)

    return diff_sum


Solution = create_solution(solution_function_name)