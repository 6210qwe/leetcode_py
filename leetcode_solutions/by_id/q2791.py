from typing import List

def find_losers(n: int, k: int) -> List[int]:
    """
    找出转圈游戏的输家
    :param n: 参与游戏的朋友数量
    :param k: 每次传球的距离
    :return: 输家的编号列表
    """
    # 初始化已经接过球的朋友集合
    visited = set()
    # 初始化当前持球的朋友
    current = 1
    # 初始化传球轮次
    round = 1
    
    while True:
        # 如果当前持球的朋友已经接过球，游戏结束
        if current in visited:
            break
        # 记录当前持球的朋友
        visited.add(current)
        # 更新当前持球的朋友
        current = (current + round * k - 1) % n + 1
        # 更新传球轮次
        round += 1
    
    # 返回所有没有接到球的朋友
    return [i for i in range(1, n + 1) if i not in visited]

