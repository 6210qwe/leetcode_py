"""
核心思想: 使用二分查找来确定可以形成的完整阶梯行数

算法步骤:
1. 初始化左右边界 left 和 right。
2. 使用二分查找来确定可以形成的完整阶梯行数。
3. 计算中间值 mid，并检查是否满足条件。
4. 根据条件调整左右边界。

关键点:
- 注意边界条件，确保不会越界。
- 优化时间和空间复杂度，使用二分查找达到 O(log n) 的时间复杂度。
"""

