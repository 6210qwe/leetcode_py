def arranging_coins(n: int) -> int:
    """
    函数式接口 - 计算可以形成的完整阶梯行数
    
    实现思路:
    使用二分查找来确定可以形成的完整阶梯行数。
    
    Args:
        n: 总共的硬币数量
        
    Returns:
        可以形成的完整阶梯行数
        
    Example:
        >>> arranging_coins(5)
        2
        >>> arranging_coins(8)
        3
    """
    left, right = 1, n
    while left <= right:
        mid = (left + right) // 2
        # 计算前 mid 行所需的硬币总数
        total_coins = mid * (mid + 1) // 2
        if total_coins == n:
            return mid
        elif total_coins < n:
            left = mid + 1
        else:
            right = mid - 1
    return right


