"""
2053. 数组中第 K 个独一无二的字符串 - 独一无二的字符串 指的是在一个数组中只出现过 一次 的字符串。 给你一个字符串数组 arr 和一个整数 k ，请你返回 arr 中第 k 个 独一无二的字符串 。如果 少于 k 个独一无二的字符串，那么返回 空字符串 "" 。 注意，按照字符串在原数组中的 顺序 找到第 k 个独一无二字符串。 示例 1: 输入：arr = ["d","b","c","b","c","a"], k = 2 输出："a" 解释： arr 中独一无二字符串包括 "d" 和 "a" 。 "d" 首先出现，所以它是第 1 个独一无二字符串。 "a" 第二个出现，所以它是 2 个独一无二字符串。 由于 k == 2 ，返回 "a" 。 示例 2: 输入：arr = ["aaa","aa","a"], k = 1 输出："aaa" 解释： arr 中所有字符串都是独一无二的，所以返回第 1 个字符串 "aaa" 。 示例 3： 输入：arr = ["a","b","a"], k = 3 输出："" 解释： 唯一一个独一无二字符串是 "b" 。由于少于 3 个独一无二字符串，我们返回空字符串 "" 。 提示： * 1 <= k <= arr.length <= 1000 * 1 <= arr[i].length <= 5 * arr[i] 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个字符串的出现次数，然后遍历数组找到第 k 个独一无二的字符串。

算法步骤:
1. 使用一个哈希表来统计每个字符串的出现次数。
2. 遍历数组，找到第 k 个出现次数为 1 的字符串。

关键点:
- 使用哈希表进行频率统计。
- 按照原数组顺序查找第 k 个独一无二的字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组两次，一次用于统计频率，一次用于查找第 k 个独一无二的字符串。
空间复杂度: O(n)，哈希表的空间复杂度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def kth_distinct_string(arr: List[str], k: int) -> str:
    """
    返回数组中第 k 个独一无二的字符串。
    
    :param arr: 字符串数组
    :param k: 第 k 个独一无二的字符串
    :return: 第 k 个独一无二的字符串，如果少于 k 个独一无二的字符串则返回空字符串
    """
    # 统计每个字符串的出现次数
    count = {}
    for s in arr:
        if s in count:
            count[s] += 1
        else:
            count[s] = 1
    
    # 找到第 k 个独一无二的字符串
    unique_count = 0
    for s in arr:
        if count[s] == 1:
            unique_count += 1
            if unique_count == k:
                return s
    
    return ""

Solution = create_solution(kth_distinct_string)