"""给定一个整数数组 nums 和一个整数目标值 target，请你在该数组中找出 和为目标值 target 的那 两个 整数，并返回它们的数组下标。 你可以假设每种输入只会对应一个答案，并且你不能使用两次相同的元素。 你可以按任意顺序返回答案。 示例 1： 输入：nums = [2,7,11,15], target = 9 输出：[0,1] 解释：因为 nums[0] + nums[1] == 9 ，返回 [0, 1] 。 示例 2： 输入：nums = [3,2,4], target = 6 输出：[1,2] 示例 3： 输入：nums = [3,3], target = 6 输出：[0,1] 提示： 2 <= nums.length <= 104 -109 <= nums[i] <= 109 -109 <= target <= 109 只会存在一个有效答案 进阶：你可以想出一个时间复杂度小于 O(n2) 的算法吗？"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表存储已遍历的元素及其下标，在遍历过程中查找目标值

算法步骤:
1. 创建一个哈希表，用于存储元素值到索引的映射
2. 遍历数组，对于每个元素 num:
   - 计算目标值 target - num（即需要的另一个数）
   - 如果这个数在哈希表中，说明找到了答案，返回两个下标
   - 否则，将当前元素和下标存入哈希表
3. 如果遍历完都没找到，返回空列表（题目保证有解，此情况不会发生）

关键点:
- 一次遍历即可完成，不需要嵌套循环
- 哈希表查找和插入都是O(1)操作
- 空间换时间的经典案例
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 只需遍历一次数组，n为数组长度
空间复杂度: O(n) - 最坏情况下需要存储n-1个元素到哈希表
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def two_sum(nums: List[int], target: int) -> List[int]:
    """
    函数式接口 - 哈希表法实现
    
    实现思路:
    使用哈希表存储已遍历的元素，在遍历过程中查找目标值，一次遍历即可完成。
    
    Args:
        nums: 整数数组
        target: 目标值
        
    Returns:
        两个数的下标列表
        
    Example:
        >>> two_sum([2, 7, 11, 15], 9)
        [0, 1]
        >>> two_sum([3, 2, 4], 6)
        [1, 2]
    """
    hash_map = {}  # key: 数值, value: 下标
    for idx, num in enumerate(nums):
        complement = target - num
        if complement in hash_map:
            return [hash_map[complement], idx]
        hash_map[num] = idx
    return []  # 题目保证有解，此处仅为语法完整性


