"""
3225. 网格图操作后的最大分数 - 给你一个大小为 n x n 的二维矩阵 grid ，一开始所有格子都是白色的。一次操作中，你可以选择任意下标为 (i, j) 的格子，并将第 j 列中从最上面到第 i 行所有格子改成黑色。 如果格子 (i, j) 为白色，且左边或者右边的格子至少一个格子为黑色，那么我们将 grid[i][j] 加到最后网格图的总分中去。 请你返回执行任意次操作以后，最终网格图的 最大 总分数。 示例 1： 输入：grid = [[0,0,0,0,0],[0,0,3,0,0],[0,1,0,0,0],[5,0,0,3,0],[0,0,0,0,2]] 输出：11 解释： [https://assets.leetcode.com/uploads/2024/05/11/one.png] 第一次操作中，我们将第 1 列中，最上面的格子到第 3 行的格子染成黑色。第二次操作中，我们将第 4 列中，最上面的格子到最后一行的格子染成黑色。最后网格图总分为 grid[3][0] + grid[1][2] + grid[3][3] 等于 11 。 示例 2： 输入：grid = [[10,9,0,0,15],[7,1,0,8,0],[5,20,0,11,0],[0,0,0,1,2],[8,12,1,10,3]] 输出：94 解释： [https://assets.leetcode.com/uploads/2024/05/11/two-1.png] 我们对第 1 ，2 ，3 列分别从上往下染黑色到第 1 ，4， 0 行。最后网格图总分为 grid[0][0] + grid[1][0] + grid[2][1] + grid[4][1] + grid[1][3] + grid[2][3] + grid[3][3] + grid[4][3] + grid[0][4] 等于 94 。 提示： * 1 <= n == grid.length <= 100 * n == grid[i].length * 0 <= grid[i][j] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们可以通过预处理每一列的最大值来简化问题。

算法步骤:
1. 计算每一列的最大值。
2. 使用动态规划来计算最大得分。定义 dp[i][j] 为考虑前 i 列，且第 i 列的最高黑色格子在第 j 行时的最大得分。
3. 初始化 dp 数组，遍历每一列，更新 dp 数组。
4. 返回 dp 数组中的最大值。

关键点:
- 通过预处理每一列的最大值来简化问题。
- 使用动态规划来计算最大得分。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n^2)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(grid: List[List[int]]) -> int:
    """
    函数式接口 - 计算网格图操作后的最大分数
    """
    n = len(grid)
    if n == 0:
        return 0

    # 计算每一列的最大值
    max_cols = [max(col) for col in zip(*grid)]

    # 初始化 dp 数组
    dp = [[0] * n for _ in range(n)]

    # 动态规划计算最大得分
    for i in range(n):
        for j in range(n):
            if i == 0:
                dp[i][j] = sum(max_cols[:j + 1])
            else:
                for k in range(j + 1):
                    dp[i][j] = max(dp[i][j], dp[i - 1][k] + sum(max_cols[k + 1:j + 1]))

    return max(max(row) for row in dp)


Solution = create_solution(solution_function_name)