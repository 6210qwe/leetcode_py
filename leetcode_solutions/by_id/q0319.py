"""
319. 灯泡开关 - 初始时有 n 个灯泡处于关闭状态。第一轮，你将会打开所有灯泡。接下来的第二轮，你将会每两个灯泡关闭第二个。 第三轮，你每三个灯泡就切换第三个灯泡的开关（即，打开变关闭，关闭变打开）。第 i 轮，你每 i 个灯泡就切换第 i 个灯泡的开关。直到第 n 轮，你只需要切换最后一个灯泡的开关。 找出并返回 n 轮后有多少个亮着的灯泡。 示例 1： [https://assets.leetcode.com/uploads/2020/11/05/bulb.jpg] 输入：n = 3 输出：1 解释： 初始时, 灯泡状态 [关闭, 关闭, 关闭]. 第一轮后, 灯泡状态 [开启, 开启, 开启]. 第二轮后, 灯泡状态 [开启, 关闭, 开启]. 第三轮后, 灯泡状态 [开启, 关闭, 关闭]. 你应该返回 1，因为只有一个灯泡还亮着。 示例 2： 输入：n = 0 输出：0 示例 3： 输入：n = 1 输出：1 提示： * 0 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 数学规律，只有完全平方数的因子个数为奇数，最终会亮着

算法步骤:
1. 第i个灯泡会被切换的次数等于它的因子个数
2. 只有因子个数为奇数的灯泡最终会亮着
3. 完全平方数的因子个数为奇数
4. 答案就是小于等于n的完全平方数的个数，即sqrt(n)

关键点:
- 数学规律：完全平方数的因子个数为奇数
- 时间复杂度O(1)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 直接计算
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

import math
from leetcode_solutions.utils.solution import create_solution


def bulb_switcher(n: int) -> int:
    """
    函数式接口 - 灯泡开关
    
    实现思路:
    数学规律，只有完全平方数的因子个数为奇数，最终会亮着。
    
    Args:
        n: 灯泡数量
        
    Returns:
        n轮后亮着的灯泡数量
        
    Example:
        >>> bulb_switcher(3)
        1
    """
    return int(math.sqrt(n))


