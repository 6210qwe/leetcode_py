"""
3364. 最小正和子数组 - 给你一个整数数组 nums 和 两个 整数 l 和 r。你的任务是找到一个长度在 l 和 r 之间（包含）且和大于 0 的 子数组 的 最小 和。 返回满足条件的子数组的 最小 和。如果不存在这样的子数组，则返回 -1。 子数组 是数组中的一个连续 非空 元素序列。 示例 1： 输入： nums = [3, -2, 1, 4], l = 2, r = 3 输出： 1 解释： 长度在 l = 2 和 r = 3 之间且和大于 0 的子数组有： * [3, -2] 和为 1 * [1, 4] 和为 5 * [3, -2, 1] 和为 2 * [-2, 1, 4] 和为 3 其中，子数组 [3, -2] 的和为 1，是所有正和中最小的。因此，答案为 1。 示例 2： 输入： nums = [-2, 2, -3, 1], l = 2, r = 3 输出： -1 解释： 不存在长度在 l 和 r 之间且和大于 0 的子数组。因此，答案为 -1。 示例 3： 输入： nums = [1, 2, 3, 4], l = 2, r = 4 输出： 3 解释： 子数组 [1, 2] 的长度为 2，和为 3，是所有正和中最小的。因此，答案为 3。 提示： * 1 <= nums.length <= 100 * 1 <= l <= r <= nums.length * -1000 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和 + 滑动窗口来找到满足条件的子数组。

算法步骤:
1. 计算前缀和数组。
2. 使用滑动窗口遍历前缀和数组，找到长度在 l 和 r 之间的子数组，并记录其和。
3. 找到和大于 0 的子数组中最小的和。

关键点:
- 使用前缀和可以快速计算任意子数组的和。
- 滑动窗口可以高效地遍历所有可能的子数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_positive_sum_subarray(nums: List[int], l: int, r: int) -> int:
    """
    函数式接口 - 找到长度在 l 和 r 之间且和大于 0 的子数组的最小和
    """
    n = len(nums)
    prefix_sum = [0] * (n + 1)
    
    # 计算前缀和数组
    for i in range(n):
        prefix_sum[i + 1] = prefix_sum[i] + nums[i]
    
    min_sum = float('inf')
    
    # 使用滑动窗口遍历前缀和数组
    for i in range(1, n + 1):
        for j in range(i - r, i - l + 1):
            if j < 0:
                continue
            subarray_sum = prefix_sum[i] - prefix_sum[j]
            if subarray_sum > 0:
                min_sum = min(min_sum, subarray_sum)
    
    return min_sum if min_sum != float('inf') else -1


Solution = create_solution(minimum_positive_sum_subarray)