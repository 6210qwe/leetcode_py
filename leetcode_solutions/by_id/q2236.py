"""
2130. 链表最大孪生和 - 在一个大小为 n 且 n 为 偶数 的链表中，对于 0 <= i <= (n / 2) - 1 的 i ，第 i 个节点（下标从 0 开始）的孪生节点为第 (n-1-i) 个节点 。 * 比方说，n = 4 那么节点 0 是节点 3 的孪生节点，节点 1 是节点 2 的孪生节点。这是长度为 n = 4 的链表中所有的孪生节点。 孪生和 定义为一个节点和它孪生节点两者值之和。 给你一个长度为偶数的链表的头节点 head ，请你返回链表的 最大孪生和 。 示例 1： [https://assets.leetcode.com/uploads/2021/12/03/eg1drawio.png] 输入：head = [5,4,2,1] 输出：6 解释： 节点 0 和节点 1 分别是节点 3 和 2 的孪生节点。孪生和都为 6 。 链表中没有其他孪生节点。 所以，链表的最大孪生和是 6 。 示例 2： [https://assets.leetcode.com/uploads/2021/12/03/eg2drawio.png] 输入：head = [4,2,2,3] 输出：7 解释： 链表中的孪生节点为： - 节点 0 是节点 3 的孪生节点，孪生和为 4 + 3 = 7 。 - 节点 1 是节点 2 的孪生节点，孪生和为 2 + 2 = 4 。 所以，最大孪生和为 max(7, 4) = 7 。 示例 3： [https://assets.leetcode.com/uploads/2021/12/03/eg3drawio.png] 输入：head = [1,100000] 输出：100001 解释： 链表中只有一对孪生节点，孪生和为 1 + 100000 = 100001 。 提示： * 链表的节点数目是 [2, 105] 中的 偶数 。 * 1 <= Node.val <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用快慢指针找到链表的中间节点，然后反转后半部分链表，最后遍历前半部分和反转后的后半部分链表，计算每对孪生节点的和，并记录最大值。

算法步骤:
1. 使用快慢指针找到链表的中间节点。
2. 反转后半部分链表。
3. 遍历前半部分和反转后的后半部分链表，计算每对孪生节点的和，并记录最大值。

关键点:
- 使用快慢指针找到链表的中间节点。
- 反转后半部分链表。
- 计算每对孪生节点的和，并记录最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.solution import create_solution


def pair_sum(head: Optional[ListNode]) -> int:
    """
    函数式接口 - 返回链表的最大孪生和
    """
    # 使用快慢指针找到链表的中间节点
    slow, fast = head, head
    while fast and fast.next:
        slow = slow.next
        fast = fast.next.next
    
    # 反转后半部分链表
    prev, curr = None, slow
    while curr:
        next_node = curr.next
        curr.next = prev
        prev = curr
        curr = next_node
    
    # 计算每对孪生节点的和，并记录最大值
    max_twin_sum = 0
    first, second = head, prev
    while second:
        twin_sum = first.val + second.val
        max_twin_sum = max(max_twin_sum, twin_sum)
        first = first.next
        second = second.next
    
    return max_twin_sum


Solution = create_solution(pair_sum)