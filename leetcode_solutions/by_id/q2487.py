"""
2405. 子字符串的最优划分 - 给你一个字符串 s ，请你将该字符串划分成一个或多个 子字符串 ，并满足每个子字符串中的字符都是 唯一 的。也就是说，在单个子字符串中，字母的出现次数都不超过 一次 。 满足题目要求的情况下，返回 最少 需要划分多少个子字符串。 注意，划分后，原字符串中的每个字符都应该恰好属于一个子字符串。 示例 1： 输入：s = "abacaba" 输出：4 解释： 两种可行的划分方法分别是 ("a","ba","cab","a") 和 ("ab","a","ca","ba") 。 可以证明最少需要划分 4 个子字符串。 示例 2： 输入：s = "ssssss" 输出：6 解释： 只存在一种可行的划分方法 ("s","s","s","s","s","s") 。 提示： * 1 <= s.length <= 105 * s 仅由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和哈希集合来记录当前子字符串中的字符，当遇到重复字符时，开始一个新的子字符串。

算法步骤:
1. 初始化一个哈希集合来记录当前子字符串中的字符。
2. 遍历字符串 s，对于每个字符：
   - 如果字符已经在哈希集合中，说明当前子字符串已经包含该字符，需要开始一个新的子字符串，并清空哈希集合。
   - 将当前字符加入哈希集合。
3. 每次开始新的子字符串时，计数器加一。
4. 返回计数器的值作为结果。

关键点:
- 使用哈希集合来快速判断字符是否已经存在于当前子字符串中。
- 每次遇到重复字符时，重新开始一个新的子字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，哈希集合的大小最多为 26（小写字母的数量），因此是常数级别的空间复杂度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def optimal_partition_string(s: str) -> int:
    """
    函数式接口 - 实现最优解法
    """
    seen = set()
    count = 1  # 至少有一个子字符串

    for char in s:
        if char in seen:
            count += 1
            seen = {char}  # 开始一个新的子字符串
        else:
            seen.add(char)

    return count


Solution = create_solution(optimal_partition_string)