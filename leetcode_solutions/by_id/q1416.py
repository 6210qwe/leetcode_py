"""
核心思想: 使用哈希表来检查数组是否是连续的

算法步骤:
1. 计算数组的最大值和最小值
2. 检查数组的长度是否等于最大值减最小值加一
3. 使用哈希表检查是否有重复元素

关键点:
- 使用哈希表可以快速检查重复元素
- 通过最大值和最小值可以确定数组的范围
"""

