"""
1481. 不同整数的最少数目 - 给你一个整数数组 arr 和一个整数 k 。现需要从数组中恰好移除 k 个元素，请找出移除后数组中不同整数的最少数目。 示例 1： 输入：arr = [5,5,4], k = 1 输出：1 解释：移除 1 个 4 ，数组中只剩下 5 一种整数。 示例 2： 输入：arr = [4,3,1,1,3,3,2], k = 3 输出：2 解释：先移除 4、2 ，然后再移除两个 1 中的任意 1 个或者三个 3 中的任意 1 个，最后剩下 1 和 3 两种整数。 提示： * 1 <= arr.length <= 10^5 * 1 <= arr[i] <= 10^9 * 0 <= k <= arr.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，优先移除出现次数最少的元素。

算法步骤:
1. 统计每个元素的出现次数。
2. 按照出现次数从小到大排序。
3. 依次移除出现次数最少的元素，直到移除 k 个元素。
4. 计算剩余的不同整数的数量。

关键点:
- 使用哈希表统计元素出现次数。
- 按照出现次数排序，优先移除出现次数少的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。主要的时间开销在于排序操作。
空间复杂度: O(n)，用于存储元素出现次数的哈希表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import Counter

def solution_function_name(arr: List[int], k: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 统计每个元素的出现次数
    count = Counter(arr)
    
    # 按照出现次数从小到大排序
    freq = sorted(count.values())
    
    # 依次移除出现次数最少的元素，直到移除 k 个元素
    for i in range(len(freq)):
        if k >= freq[i]:
            k -= freq[i]
        else:
            return len(freq) - i
    
    return 0

