"""
2582. 递枕头 - n 个人站成一排，按从 1 到 n 编号。最初，排在队首的第一个人拿着一个枕头。每秒钟，拿着枕头的人会将枕头传递给队伍中的下一个人。一旦枕头到达队首或队尾，传递方向就会改变，队伍会继续沿相反方向传递枕头。 * 例如，当枕头到达第 n 个人时，TA 会将枕头传递给第 n - 1 个人，然后传递给第 n - 2 个人，依此类推。 给你两个正整数 n 和 time ，返回 time 秒后拿着枕头的人的编号。 示例 1： 输入：n = 4, time = 5 输出：2 解释：队伍中枕头的传递情况为：1 -> 2 -> 3 -> 4 -> 3 -> 2 。 5 秒后，枕头传递到第 2 个人手中。 示例 2： 输入：n = 3, time = 2 输出：3 解释：队伍中枕头的传递情况为：1 -> 2 -> 3 。 2 秒后，枕头传递到第 3 个人手中。 提示： * 2 <= n <= 1000 * 1 <= time <= 1000 注意：本题与 3178.找出 K 秒后拿着球的孩子 [https://leetcode.cn/problems/find-the-child-who-has-the-ball-after-k-seconds/] 一致。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算时间 `time` 对 `2 * (n - 1)` 的余数来确定当前的方向和位置。

算法步骤:
1. 计算 `time` 对 `2 * (n - 1)` 的余数 `remainder`。
2. 如果 `remainder < n`，则枕头在前向传递，直接返回 `remainder + 1`。
3. 否则，枕头在反向传递，返回 `2 * n - remainder - 1`。

关键点:
- 通过取余运算减少不必要的循环，直接定位到最终位置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def pass_the_pillow(n: int, time: int) -> int:
    """
    函数式接口 - 通过计算时间 `time` 对 `2 * (n - 1)` 的余数来确定当前的方向和位置。
    """
    # 计算时间对 2 * (n - 1) 的余数
    remainder = time % (2 * (n - 1))
    
    # 如果余数小于 n，枕头在前向传递
    if remainder < n:
        return remainder + 1
    # 否则，枕头在反向传递
    else:
        return 2 * n - remainder - 1


Solution = create_solution(pass_the_pillow)