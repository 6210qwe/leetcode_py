"""
1108. IP 地址无效化 - 给你一个有效的 IPv4 [https://baike.baidu.com/item/IPv4] 地址 address，返回这个 IP 地址的无效化版本。 所谓无效化 IP 地址，其实就是用 "[.]" 代替了每个 "."。 示例 1： 输入：address = "1.1.1.1" 输出："1[.]1[.]1[.]1" 示例 2： 输入：address = "255.100.50.0" 输出："255[.]100[.]50[.]0" 提示： * 给出的 address 是一个有效的 IPv4 地址
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用字符串替换方法将所有的 "." 替换为 "[.]"。

算法步骤:
1. 使用 Python 的字符串 replace 方法进行替换。
2. 返回替换后的字符串。

关键点:
- 使用内置的字符串替换方法可以简洁高效地完成任务。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是输入字符串的长度。replace 方法的时间复杂度是线性的。
空间复杂度: O(n)，因为需要创建一个新的字符串来存储结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def defang_ip_address(address: str) -> str:
    """
    函数式接口 - 将给定的有效 IPv4 地址中的每个 "." 替换为 "[.]"。
    """
    # 使用字符串替换方法进行替换
    return address.replace('.', '[.]')


Solution = create_solution(defang_ip_address)