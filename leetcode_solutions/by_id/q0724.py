# ============================================================================
# 问题描述
# ============================================================================
"""
724. 寻找数组的中心下标 - 给你一个整数数组 nums ，请计算数组的 中心下标 。 数组 中心下标 是数组的一个下标，其左侧所有元素相加的和等于右侧所有元素相加的和。 如果中心下标位于数组最左端，那么左侧数之和视为 0 ，因为在下标的左侧不存在元素。这一点对于中心下标位于数组最右端同样适用。 如果数组有多个中心下标，应该返回 最靠近左边 的那一个。如果数组不存在中心下标，返回 -1 。 示例 1： 输入：nums = [1, 7, 3, 6, 5, 6] 输出：3 解释： 中心下标是 3 。 左侧数之和 sum = nums[0] + nums[1] + nums[2] = 1 + 7 + 3 = 11 ， 右侧数之和 sum = nums[4] + nums[5] = 5 + 6 = 11 ，二者相等。 示例 2： 输入：nums = [1, 2, 3] 输出：-1 解释： 数组中不存在满足此条件的中心下标。 示例 3： 输入：nums = [2, 1, -1] 输出：0 解释： 中心下标是 0 。 左侧数之和 sum = 0 ，（下标 0 左侧不存在元素）， 右侧数之和 sum = nums[1] + nums[2] = 1 + -1 = 0 。 提示： * 1 <= nums.length <= 104 * -1000 <= nums[i] <= 1000 注意：本题与主站 1991 题相同：https://leetcode.cn/problems/find-the-middle-index-in-array/ [https://leetcode.cn/problems/find-the-middle-index-in-array/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来快速计算每个位置的左右和。

算法步骤:
1. 计算整个数组的总和。
2. 遍历数组，同时维护一个左侧和的变量。
3. 对于每个位置，检查左侧和是否等于总和减去当前元素再减去左侧和。
4. 如果相等，则该位置为中心下标，返回该位置。
5. 如果遍历完数组没有找到中心下标，返回 -1。

关键点:
- 使用前缀和可以避免重复计算，提高效率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。需要遍历数组两次，一次计算总和，一次查找中心下标。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_pivot_index(nums: List[int]) -> int:
    """
    函数式接口 - 寻找数组的中心下标
    """
    total_sum = sum(nums)
    left_sum = 0
    for i, num in enumerate(nums):
        if left_sum == (total_sum - left_sum - num):
            return i
        left_sum += num
    return -1


Solution = create_solution(find_pivot_index)