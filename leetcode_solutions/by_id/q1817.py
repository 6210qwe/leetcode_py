# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算每周的存款总额和剩余天数的存款总额来求解。

算法步骤:
1. 计算完整的周数和剩余的天数。
2. 计算完整周数的总存款。
3. 计算剩余天数的总存款。
4. 返回总存款。

关键点:
- 使用等差数列求和公式来计算每周的存款总额。
- 递增每周的起始存款金额。
"""

