"""
2273. 移除字母异位词后的结果数组 - 给你一个下标从 0 开始的字符串数组 words ，其中 words[i] 由小写英文字符组成。 在一步操作中，需要选出任一下标 i ，从 words 中 删除 words[i] 。其中下标 i 需要同时满足下述两个条件： 1. 0 < i < words.length 2. words[i - 1] 和 words[i] 是 字母异位词 。 只要可以选出满足条件的下标，就一直执行这个操作。 在执行所有操作后，返回 words 。可以证明，按任意顺序为每步操作选择下标都会得到相同的结果。 字母异位词 是由重新排列源单词的字母得到的一个新单词，所有源单词中的字母通常恰好只用一次。例如，"dacb" 是 "abdc" 的一个字母异位词。 示例 1： 输入：words = ["abba","baba","bbaa","cd","cd"] 输出：["abba","cd"] 解释： 获取结果数组的方法之一是执行下述步骤： - 由于 words[2] = "bbaa" 和 words[1] = "baba" 是字母异位词，选择下标 2 并删除 words[2] 。 现在 words = ["abba","baba","cd","cd"] 。 - 由于 words[1] = "baba" 和 words[0] = "abba" 是字母异位词，选择下标 1 并删除 words[1] 。 现在 words = ["abba","cd","cd"] 。 - 由于 words[2] = "cd" 和 words[1] = "cd" 是字母异位词，选择下标 2 并删除 words[2] 。 现在 words = ["abba","cd"] 。 无法再执行任何操作，所以 ["abba","cd"] 是最终答案。 示例 2： 输入：words = ["a","b","c","d","e"] 输出：["a","b","c","d","e"] 解释： words 中不存在互为字母异位词的两个相邻字符串，所以无需执行任何操作。 提示： * 1 <= words.length <= 100 * 1 <= words[i].length <= 10 * words[i] 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一个列表来存储结果，并通过比较相邻字符串的排序形式来判断它们是否是字母异位词。

算法步骤:
1. 初始化一个空列表 `result` 用于存储最终结果。
2. 遍历输入的字符串数组 `words`，对于每个字符串：
   - 如果 `result` 为空或者当前字符串与 `result` 中最后一个字符串不是字母异位词，则将当前字符串添加到 `result` 中。
   - 否则，跳过当前字符串。
3. 返回 `result` 作为最终结果。

关键点:
- 使用 `sorted` 函数对字符串进行排序，以判断两个字符串是否是字母异位词。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m log m)，其中 n 是 words 的长度，m 是每个字符串的最大长度。因为我们需要对每个字符串进行排序。
空间复杂度: O(n * m)，最坏情况下，结果列表 `result` 会包含所有的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_anagrams(words: List[str]) -> List[str]:
    """
    函数式接口 - 移除字母异位词后的结果数组
    """
    result = []
    for word in words:
        if not result or sorted(word) != sorted(result[-1]):
            result.append(word)
    return result


Solution = create_solution(remove_anagrams)