"""
2670. 找出不同元素数目差数组 - 给你一个下标从 0 开始的数组 nums ，数组长度为 n 。 nums 的 不同元素数目差 数组可以用一个长度为 n 的数组 diff 表示，其中 diff[i] 等于前缀 nums[0, ..., i] 中不同元素的数目 减去 后缀 nums[i + 1, ..., n - 1] 中不同元素的数目。 返回 nums 的 不同元素数目差 数组。 注意 nums[i, ..., j] 表示 nums 的一个从下标 i 开始到下标 j 结束的子数组（包含下标 i 和 j 对应元素）。特别需要说明的是，如果 i > j ，则 nums[i, ..., j] 表示一个空子数组。 示例 1： 输入：nums = [1,2,3,4,5] 输出：[-3,-1,1,3,5] 解释： 对于 i = 0，前缀中有 1 个不同的元素，而在后缀中有 4 个不同的元素。因此，diff[0] = 1 - 4 = -3 。 对于 i = 1，前缀中有 2 个不同的元素，而在后缀中有 3 个不同的元素。因此，diff[1] = 2 - 3 = -1 。 对于 i = 2，前缀中有 3 个不同的元素，而在后缀中有 2 个不同的元素。因此，diff[2] = 3 - 2 = 1 。 对于 i = 3，前缀中有 4 个不同的元素，而在后缀中有 1 个不同的元素。因此，diff[3] = 4 - 1 = 3 。 对于 i = 4，前缀中有 5 个不同的元素，而在后缀中有 0 个不同的元素。因此，diff[4] = 5 - 0 = 5 。 示例 2： 输入：nums = [3,2,3,4,2] 输出：[-2,-1,0,2,3] 解释： 对于 i = 0，前缀中有 1 个不同的元素，而在后缀中有 3 个不同的元素。因此，diff[0] = 1 - 3 = -2 。 对于 i = 1，前缀中有 2 个不同的元素，而在后缀中有 3 个不同的元素。因此，diff[1] = 2 - 3 = -1 。 对于 i = 2，前缀中有 2 个不同的元素，而在后缀中有 2 个不同的元素。因此，diff[2] = 2 - 2 = 0 。 对于 i = 3，前缀中有 3 个不同的元素，而在后缀中有 1 个不同的元素。因此，diff[3] = 3 - 1 = 2 。 对于 i = 4，前缀中有 3 个不同的元素，而在后缀中有 0 个不同的元素。因此，diff[4] = 3 - 0 = 3 。 提示： * 1 <= n == nums.length <= 50 * 1 <= nums[i] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个哈希集合分别记录前缀和后缀的不同元素数量。

算法步骤:
1. 初始化两个哈希集合 `prefix` 和 `suffix`，分别用于存储前缀和后缀的不同元素。
2. 计算初始的 `suffix` 哈希集合，包含所有元素。
3. 遍历数组，逐步更新 `prefix` 和 `suffix`，并计算每个位置的 `diff` 值。

关键点:
- 使用哈希集合来高效地记录不同元素的数量。
- 在遍历过程中，逐步更新前缀和后缀的不同元素集合。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组两次，一次是初始化后缀集合，一次是计算结果。
空间复杂度: O(n)，使用了两个哈希集合来存储前缀和后缀的不同元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def find_distinct_difference_array(nums: List[int]) -> List[int]:
    n = len(nums)
    prefix = set()
    suffix = set(nums)
    diff = []

    for i in range(n):
        # 更新前缀集合
        prefix.add(nums[i])
        # 计算当前 diff 值
        diff.append(len(prefix) - (len(suffix) - 1 if i < n - 1 else 0))
        # 更新后缀集合
        if i < n - 1:
            suffix.remove(nums[i])

    return diff

