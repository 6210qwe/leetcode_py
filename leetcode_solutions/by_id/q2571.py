"""
2485. 找出中枢整数 - 给你一个正整数 n ，找出满足下述条件的 中枢整数 x ： * 1 和 x 之间的所有元素之和等于 x 和 n 之间所有元素之和。 返回中枢整数 x 。如果不存在中枢整数，则返回 -1 。题目保证对于给定的输入，至多存在一个中枢整数。 示例 1： 输入：n = 8 输出：6 解释：6 是中枢整数，因为 1 + 2 + 3 + 4 + 5 + 6 = 6 + 7 + 8 = 21 。 示例 2： 输入：n = 1 输出：1 解释：1 是中枢整数，因为 1 = 1 。 示例 3： 输入：n = 4 输出：-1 解释：可以证明不存在满足题目要求的整数。 提示： * 1 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过数学公式求解中枢整数。根据题目条件，我们需要找到一个整数 x 使得 1 到 x 的和等于 x 到 n 的和。

算法步骤:
1. 计算从 1 到 n 的总和 total_sum。
2. 遍历 1 到 n，计算当前前缀和 prefix_sum。
3. 如果 2 * prefix_sum == total_sum + x，则 x 是中枢整数。
4. 如果遍历完没有找到符合条件的 x，则返回 -1。

关键点:
- 使用数学公式简化计算。
- 通过一次遍历找到中枢整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 找出中枢整数
    """
    total_sum = n * (n + 1) // 2  # 计算从 1 到 n 的总和
    prefix_sum = 0  # 初始化前缀和

    for x in range(1, n + 1):
        prefix_sum += x  # 更新前缀和
        if 2 * prefix_sum == total_sum + x:
            return x  # 找到中枢整数

    return -1  # 未找到中枢整数


Solution = create_solution(solution_function_name)