"""
核心思想: 使用双指针比较两个字符串，允许一次编辑操作

算法步骤:
1. 如果长度差大于1，返回False
2. 使用双指针比较字符
3. 如果字符不同：
   - 长度相同：替换操作，两个指针都前进
   - 长度不同：删除/插入操作，较长字符串的指针前进
4. 最多允许一次编辑操作

关键点:
- 根据长度差判断是替换还是删除/插入
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - n为较短字符串的长度
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def one_edit_distance(s: str, t: str) -> bool:
    """
    函数式接口 - 判断两个字符串是否相隔为1的编辑距离
    
    实现思路:
    使用双指针比较两个字符串，允许一次编辑操作。
    
    Args:
        s: 第一个字符串
        t: 第二个字符串
        
    Returns:
        如果编辑距离为1返回True，否则返回False
        
    Example:
        >>> one_edit_distance("ab", "acb")
        True
    """
    m, n = len(s), len(t)
    
    if abs(m - n) > 1:
        return False
    
    i, j = 0, 0
    edits = 0
    
    while i < m and j < n:
        if s[i] == t[j]:
            i += 1
            j += 1
        else:
            if edits >= 1:
                return False
            
            if m == n:
                # 替换操作
                i += 1
                j += 1
            elif m < n:
                # 插入操作（在s中插入）
                j += 1
            else:
                # 删除操作（从s中删除）
                i += 1
            
            edits += 1
    
    # 处理剩余字符
    if i < m or j < n:
        edits += 1
    
    return edits == 1


