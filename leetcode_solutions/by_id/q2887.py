"""
2785. 将字符串中的元音字母排序 - 给你一个下标从 0 开始的字符串 s ，将 s 中的元素重新 排列 得到新的字符串 t ，它满足： * 所有辅音字母都在原来的位置上。更正式的，如果满足 0 <= i < s.length 的下标 i 处的 s[i] 是个辅音字母，那么 t[i] = s[i] 。 * 元音字母都必须以他们的 ASCII 值按 非递减 顺序排列。更正式的，对于满足 0 <= i < j < s.length 的下标 i 和 j ，如果 s[i] 和 s[j] 都是元音字母，那么 t[i] 的 ASCII 值不能大于 t[j] 的 ASCII 值。 请你返回结果字母串。 元音字母为 'a' ，'e' ，'i' ，'o' 和 'u' ，它们可能是小写字母也可能是大写字母，辅音字母是除了这 5 个字母以外的所有字母。 示例 1： 输入：s = "lEetcOde" 输出："lEOtcede" 解释：'E' ，'O' 和 'e' 是 s 中的元音字母，'l' ，'t' ，'c' 和 'd' 是所有的辅音。将元音字母按照 ASCII 值排序，辅音字母留在原地。 示例 2： 输入：s = "lYmpH" 输出："lYmpH" 解释：s 中没有元音字母（s 中都为辅音字母），所以我们返回 "lYmpH" 。 提示： * 1 <= s.length <= 105 * s 只包含英语字母表中的 大写 和 小写 字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数排序对元音字母进行排序，并保持辅音字母在原位置。

算法步骤:
1. 创建一个元音字母的集合。
2. 遍历字符串，记录每个元音字母的出现次数。
3. 再次遍历字符串，将元音字母按顺序替换为排序后的元音字母。

关键点:
- 使用计数排序来高效地对元音字母进行排序。
- 保持辅音字母在原位置不变。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要两次遍历字符串。
空间复杂度: O(1)，因为元音字母的种类是固定的，所以额外空间是常数级别的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sort_vowels(s: str) -> str:
    """
    函数式接口 - 将字符串中的元音字母排序
    """
    vowels = "AEIOUaeiou"
    vowel_count = [0] * 10  # 用于记录元音字母的出现次数
    result = []

    # 记录每个元音字母的出现次数
    for char in s:
        if char in vowels:
            vowel_count[ord(char) - ord('A')] += 1

    # 生成排序后的字符串
    vowel_index = 0
    for char in s:
        if char in vowels:
            while vowel_count[vowel_index] == 0:
                vowel_index += 1
            result.append(chr(vowel_index + ord('A')))
            vowel_count[vowel_index] -= 1
        else:
            result.append(char)

    return ''.join(result)


Solution = create_solution(sort_vowels)