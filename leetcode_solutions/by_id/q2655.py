"""
2587. 重排数组以得到最大前缀分数 - 给你一个下标从 0 开始的整数数组 nums 。你可以将 nums 中的元素按 任意顺序 重排（包括给定顺序）。 令 prefix 为一个数组，它包含了 nums 重新排列后的前缀和。换句话说，prefix[i] 是 nums 重新排列后下标从 0 到 i 的元素之和。nums 的 分数 是 prefix 数组中正整数的个数。 返回可以得到的最大分数。 示例 1： 输入：nums = [2,-1,0,1,-3,3,-3] 输出：6 解释：数组重排为 nums = [2,3,1,-1,-3,0,-3] 。 prefix = [2,5,6,5,2,2,-1] ，分数为 6 。 可以证明 6 是能够得到的最大分数。 示例 2： 输入：nums = [-2,-3,0] 输出：0 解释：不管怎么重排数组得到的分数都是 0 。 提示： * 1 <= nums.length <= 105 * -106 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将数组按降序排序，然后计算前缀和，直到前缀和变为非正数。

算法步骤:
1. 对数组进行降序排序。
2. 计算前缀和，直到前缀和变为非正数，记录正前缀和的数量。

关键点:
- 通过降序排序，确保前缀和尽可能长时间保持为正数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序操作的时间复杂度。
空间复杂度: O(1) - 仅使用常数级额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 重排数组以得到最大前缀分数
    """
    # 降序排序
    nums.sort(reverse=True)
    
    prefix_sum = 0
    score = 0
    
    for num in nums:
        prefix_sum += num
        if prefix_sum > 0:
            score += 1
        else:
            break
    
    return score


Solution = create_solution(solution_function_name)