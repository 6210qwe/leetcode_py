# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算，2的幂的二进制表示只有一个1

算法步骤:
1. 如果n <= 0，返回False
2. 使用n & (n-1) == 0判断，因为2的幂减1后所有位都是1，按位与结果为0

关键点:
- 使用位运算优化
- 时间复杂度O(1)，空间复杂度O(1)
"""

