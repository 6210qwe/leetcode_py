from leetcode_solutions.utils.solution import create_solution


def power_of_two(n: int) -> bool:
    """
    函数式接口 - 2的幂
    
    实现思路:
    使用位运算，2的幂的二进制表示只有一个1。
    
    Args:
        n: 整数
        
    Returns:
        是否是2的幂次方
        
    Example:
        >>> power_of_two(16)
        True
    """
    return n > 0 and (n & (n - 1)) == 0


