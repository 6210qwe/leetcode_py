"""
3092. 最高频率的 ID - 你需要在一个集合里动态记录 ID 的出现频率。给你两个长度都为 n 的整数数组 nums 和 freq ，nums 中每一个元素表示一个 ID ，对应的 freq 中的元素表示这个 ID 在集合中此次操作后需要增加或者减少的数目。 * 增加 ID 的数目：如果 freq[i] 是正数，那么 freq[i] 个 ID 为 nums[i] 的元素在第 i 步操作后会添加到集合中。 * 减少 ID 的数目：如果 freq[i] 是负数，那么 -freq[i] 个 ID 为 nums[i] 的元素在第 i 步操作后会从集合中删除。 请你返回一个长度为 n 的数组 ans ，其中 ans[i] 表示第 i 步操作后出现频率最高的 ID 数目 ，如果在某次操作后集合为空，那么 ans[i] 为 0 。 示例 1： 输入：nums = [2,3,2,1], freq = [3,2,-3,1] 输出：[3,3,2,2] 解释： 第 0 步操作后，有 3 个 ID 为 2 的元素，所以 ans[0] = 3 。 第 1 步操作后，有 3 个 ID 为 2 的元素和 2 个 ID 为 3 的元素，所以 ans[1] = 3 。 第 2 步操作后，有 2 个 ID 为 3 的元素，所以 ans[2] = 2 。 第 3 步操作后，有 2 个 ID 为 3 的元素和 1 个 ID 为 1 的元素，所以 ans[3] = 2 。 示例 2： 输入：nums = [5,5,3], freq = [2,-2,1] 输出：[2,0,1] 解释： 第 0 步操作后，有 2 个 ID 为 5 的元素，所以 ans[0] = 2 。 第 1 步操作后，集合中没有任何元素，所以 ans[1] = 0 。 第 2 步操作后，有 1 个 ID 为 3 的元素，所以 ans[2] = 1 。 提示： * 1 <= nums.length == freq.length <= 105 * 1 <= nums[i] <= 105 * -105 <= freq[i] <= 105 * freq[i] != 0 * 输入保证任何操作后，集合中的元素出现次数不会为负数。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一个哈希表来记录每个 ID 的频率，并使用一个有序集合来维护频率的最大值。

算法步骤:
1. 初始化一个哈希表 `id_freq` 来记录每个 ID 的频率。
2. 初始化一个有序集合 `freq_set` 来维护频率的最大值。
3. 遍历 `nums` 和 `freq`，更新 `id_freq` 和 `freq_set`。
4. 每次更新后，从 `freq_set` 中获取当前最大频率。

关键点:
- 使用有序集合 `SortedSet` 来维护频率的最大值。
- 使用哈希表 `id_freq` 来记录每个 ID 的频率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 `nums` 的长度。每次插入和删除有序集合的时间复杂度是 O(log n)。
空间复杂度: O(n)，哈希表和有序集合的空间复杂度都是 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from sortedcontainers import SortedSet

def most_frequent_ids(nums: List[int], freq: List[int]) -> List[int]:
    id_freq = {}
    freq_set = SortedSet()
    result = []

    for i in range(len(nums)):
        num = nums[i]
        f = freq[i]

        if num in id_freq:
            old_freq = id_freq[num]
            freq_set.discard(old_freq)
            new_freq = old_freq + f
            id_freq[num] = new_freq
            freq_set.add(new_freq)
        else:
            id_freq[num] = f
            freq_set.add(f)

        if freq_set:
            result.append(freq_set[-1])
        else:
            result.append(0)

    return result

