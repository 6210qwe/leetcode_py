"""
740. 删除并获得点数 - 给你一个整数数组 nums ，你可以对它进行一些操作。 每次操作中，选择任意一个 nums[i] ，删除它并获得 nums[i] 的点数。之后，你必须删除 所有 等于 nums[i] - 1 和 nums[i] + 1 的元素。 开始你拥有 0 个点数。返回你能通过这些操作获得的最大点数。 示例 1： 输入：nums = [3,4,2] 输出：6 解释： 你可以执行下列步骤： - 删除 4 获得 4 个点数，因此 3 也被删除。nums = [2]。 - 之后，删除 2 获得 2 个点数。nums = []。 总共获得 6 个点数。 示例 2： 输入：nums = [2,2,3,3,3,4] 输出：9 解释： 你可以执行下列步骤： - 删除 3 获得 3 个点数。所有的 2 和 4 也被删除。nums = [3,3]。 - 之后，再次删除 3 获得 3 个点数。nums = [3]。 - 再次删除 3 获得 3 个点数。nums = []。 总共获得 9 个点数。 提示： * 1 <= nums.length <= 2 * 104 * 1 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们可以将问题转化为一个类似于“打家劫舍”的问题。

算法步骤:
1. 计算每个数字的总点数。
2. 将问题转化为在一个数组上进行选择的问题，其中相邻的元素不能同时选择。
3. 使用动态规划来解决这个转化后的问题。

关键点:
- 使用一个数组来存储每个数字的总点数。
- 动态规划的状态转移方程为 `dp[i] = max(dp[i-1], dp[i-2] + total_points[i])`。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 nums 的长度，m 是 nums 中的最大值。
空间复杂度: O(m)，其中 m 是 nums 中的最大值。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def delete_and_earn(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    if not nums:
        return 0

    # 计算每个数字的总点数
    max_num = max(nums)
    total_points = [0] * (max_num + 1)
    for num in nums:
        total_points[num] += num

    # 动态规划数组
    dp = [0] * (max_num + 1)
    dp[1] = total_points[1]

    for i in range(2, max_num + 1):
        dp[i] = max(dp[i - 1], dp[i - 2] + total_points[i])

    return dp[max_num]


Solution = create_solution(delete_and_earn)