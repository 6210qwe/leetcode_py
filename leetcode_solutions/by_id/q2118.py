"""
2008. 出租车的最大盈利 - 你驾驶出租车行驶在一条有 n 个地点的路上。这 n 个地点从近到远编号为 1 到 n ，你想要从 1 开到 n ，通过接乘客订单盈利。你只能沿着编号递增的方向前进，不能改变方向。 乘客信息用一个下标从 0 开始的二维数组 rides 表示，其中 rides[i] = [starti, endi, tipi] 表示第 i 位乘客需要从地点 starti 前往 endi ，愿意支付 tipi 元的小费。 每一位 你选择接单的乘客 i ，你可以 盈利 endi - starti + tipi 元。你同时 最多 只能接一个订单。 给你 n 和 rides ，请你返回在最优接单方案下，你能盈利 最多 多少元。 注意：你可以在一个地点放下一位乘客，并在同一个地点接上另一位乘客。 示例 1： 输入：n = 5, rides = [[2,5,4],[1,5,1]] 输出：7 解释：我们可以接乘客 0 的订单，获得 5 - 2 + 4 = 7 元。 示例 2： 输入：n = 20, rides = [[1,6,1],[3,10,2],[10,12,3],[11,12,2],[12,15,2],[13,18,1]] 输出：20 解释：我们可以接以下乘客的订单： - 将乘客 1 从地点 3 送往地点 10 ，获得 10 - 3 + 2 = 9 元。 - 将乘客 2 从地点 10 送往地点 12 ，获得 12 - 10 + 3 = 5 元。 - 将乘客 5 从地点 13 送往地点 18 ，获得 18 - 13 + 1 = 6 元。 我们总共获得 9 + 5 + 6 = 20 元。 提示： * 1 <= n <= 105 * 1 <= rides.length <= 3 * 104 * rides[i].length == 3 * 1 <= starti < endi <= n * 1 <= tipi <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和二分查找来解决这个问题。

算法步骤:
1. 对 rides 按照结束时间进行排序。
2. 初始化一个 dp 数组，dp[i] 表示前 i 个订单的最大收益。
3. 遍历每个订单，对于每个订单，使用二分查找找到最后一个不与当前订单冲突的订单。
4. 更新 dp 数组，dp[i] = max(dp[i-1], dp[j] + earnings)，其中 j 是最后一个不冲突的订单索引。
5. 返回 dp 数组的最后一个元素。

关键点:
- 使用二分查找来优化查找不冲突订单的时间复杂度。
- 动态规划的状态转移方程确保了最优解。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m log m + m log m) = O(m log m)，其中 m 是 rides 的长度。排序的时间复杂度是 O(m log m)，每次二分查找的时间复杂度是 O(log m)，总的时间复杂度是 O(m log m)。
空间复杂度: O(m)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from bisect import bisect_right

def max_taxi_earnings(n: int, rides: List[List[int]]) -> int:
    # 按结束时间排序
    rides.sort(key=lambda x: x[1])
    
    # 初始化 dp 数组
    dp = [0] * (len(rides) + 1)
    
    for i in range(1, len(rides) + 1):
        start, end, tip = rides[i - 1]
        earnings = end - start + tip
        
        # 二分查找最后一个不冲突的订单
        j = bisect_right(rides, start, key=lambda x: x[1]) - 1
        
        # 更新 dp 数组
        dp[i] = max(dp[i - 1], dp[j + 1] + earnings)
    
    return dp[-1]

