# ============================================================================
# 问题描述
# ============================================================================
"""
面试题 10.01. 合并排序的数组 - 给定两个排序后的数组 A 和 B，其中 A 的末端有足够的缓冲空间容纳 B。 编写一个方法，将 B 合并入 A 并排序。 初始化 A 和 B 的元素数量分别为 m 和 n。 示例： 输入： A = [1,2,3,0,0,0], m = 3 B = [2,5,6], n = 3 输出： [1,2,2,3,5,6] 说明： * A.length == n + m
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针从后向前合并两个数组，避免覆盖未处理的元素。

算法步骤:
1. 初始化三个指针 i, j, k 分别指向 A 和 B 的有效元素末尾以及 A 的总长度末尾。
2. 从后向前遍历 A 和 B，比较 A[i] 和 B[j] 的大小，将较大的元素放到 A[k] 位置，并移动相应的指针。
3. 如果 B 还有剩余元素，继续将其放入 A 中。

关键点:
- 从后向前遍历可以避免覆盖 A 中未处理的元素。
- 使用双指针可以高效地合并两个有序数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m + n)，其中 m 和 n 分别是 A 和 B 的有效元素数量。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(A: List[int], m: int, B: List[int], n: int) -> None:
    """
    函数式接口 - 合并两个有序数组
    """
    # 从后向前遍历
    i, j, k = m - 1, n - 1, m + n - 1
    
    while i >= 0 and j >= 0:
        if A[i] > B[j]:
            A[k] = A[i]
            i -= 1
        else:
            A[k] = B[j]
            j -= 1
        k -= 1
    
    # 如果 B 还有剩余元素
    while j >= 0:
        A[k] = B[j]
        j -= 1
        k -= 1

Solution = create_solution(solution_function_name)