"""
3179. K 秒后第 N 个元素的值 - 给你两个整数 n 和 k。 最初，你有一个长度为 n 的整数数组 a，对所有 0 <= i <= n - 1，都有 a[i] = 1 。每过一秒，你会同时更新每个元素为其前面所有元素的和加上该元素本身。例如，一秒后，a[0] 保持不变，a[1] 变为 a[0] + a[1]，a[2] 变为 a[0] + a[1] + a[2]，以此类推。 返回 k 秒后 a[n - 1] 的值。 由于答案可能非常大，返回其对 109 + 7 取余 后的结果。 示例 1： 输入：n = 4, k = 5 输出：56 解释： 时间（秒） 数组状态 0 [1,1,1,1] 1 [1,2,3,4] 2 [1,3,6,10] 3 [1,4,10,20] 4 [1,5,15,35] 5 [1,6,21,56] 示例 2： 输入：n = 5, k = 3 输出：35 解释： 时间（秒） 数组状态 0 [1,1,1,1,1] 1 [1,2,3,4,5] 2 [1,3,6,10,15] 3 [1,4,10,20,35] 提示： * 1 <= n, k <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用组合数学中的二项式系数来计算 k 秒后的 a[n-1] 值。

算法步骤:
1. 计算组合数 C(n + k - 1, k)。
2. 返回结果对 10^9 + 7 取余。

关键点:
- 使用组合数公式 C(n, k) = n! / (k! * (n - k)!)。
- 为了避免大数运算，使用模意义下的乘法逆元来计算组合数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + k)
空间复杂度: O(1)
"""

# ============================================================================
