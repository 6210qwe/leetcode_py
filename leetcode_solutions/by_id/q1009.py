"""
969. 煎饼排序 - 给你一个整数数组 arr ，请使用 煎饼翻转 完成对数组的排序。 一次煎饼翻转的执行过程如下： * 选择一个整数 k ，1 <= k <= arr.length * 反转子数组 arr[0...k-1]（下标从 0 开始） 例如，arr = [3,2,1,4] ，选择 k = 3 进行一次煎饼翻转，反转子数组 [3,2,1] ，得到 arr = [1,2,3,4] 。 以数组形式返回能使 arr 有序的煎饼翻转操作所对应的 k 值序列。任何将数组排序且翻转次数在 10 * arr.length 范围内的有效答案都将被判断为正确。 示例 1： 输入：[3,2,4,1] 输出：[4,2,4,3] 解释： 我们执行 4 次煎饼翻转，k 值分别为 4，2，4，和 3。 初始状态 arr = [3, 2, 4, 1] 第一次翻转后（k = 4）：arr = [1, 4, 2, 3] 第二次翻转后（k = 2）：arr = [4, 1, 2, 3] 第三次翻转后（k = 4）：arr = [3, 2, 1, 4] 第四次翻转后（k = 3）：arr = [1, 2, 3, 4]，此时已完成排序。 示例 2： 输入：[1,2,3] 输出：[] 解释： 输入已经排序，因此不需要翻转任何内容。 请注意，其他可能的答案，如 [3，3] ，也将被判断为正确。 提示： * 1 <= arr.length <= 100 * 1 <= arr[i] <= arr.length * arr 中的所有整数互不相同（即，arr 是从 1 到 arr.length 整数的一个排列）
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，每次找到当前未排序部分的最大值，将其通过两次翻转移动到未排序部分的末尾。

算法步骤:
1. 找到当前未排序部分的最大值及其索引。
2. 将最大值翻转到数组开头。
3. 将最大值翻转到未排序部分的末尾。
4. 重复上述步骤，直到整个数组排序完成。

关键点:
- 通过两次翻转将最大值移动到未排序部分的末尾。
- 记录每次翻转的 k 值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是数组长度。每次找到最大值需要 O(n) 时间，总共需要进行 O(n) 次翻转。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def flip(arr: List[int], k: int) -> None:
    """
    反转数组 arr 的前 k 个元素。
    """
    arr[:k] = arr[:k][::-1]

def pancake_sort(arr: List[int]) -> List[int]:
    """
    使用煎饼排序对数组进行排序，并返回翻转操作的 k 值序列。
    """
    def find_max_index(sub_arr: List[int]) -> int:
        return sub_arr.index(max(sub_arr))

    flips = []
    for size in range(len(arr), 1, -1):
        max_index = find_max_index(arr[:size])
        if max_index + 1 != size:
            # 将最大值翻转到数组开头
            if max_index != 0:
                flip(arr, max_index + 1)
                flips.append(max_index + 1)
            # 将最大值翻转到未排序部分的末尾
            flip(arr, size)
            flips.append(size)
    return flips

