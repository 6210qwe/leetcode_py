"""
3011. 判断一个数组是否可以变为有序 - 给你一个下标从 0 开始且全是 正 整数的数组 nums 。 一次 操作 中，如果两个 相邻 元素在二进制下 设置位 的数目 相同 ，那么你可以将这两个元素交换。你可以执行这个操作 任意次 （也可以 0 次）。 如果你可以使数组变为非降序，请你返回 true ，否则返回 false 。 示例 1： 输入：nums = [8,4,2,30,15] 输出：true 解释：我们先观察每个元素的二进制表示。 2 ，4 和 8 分别都只有一个数位为 1 ，分别为 "10" ，"100" 和 "1000" 。15 和 30 分别有 4 个数位为 1 ："1111" 和 "11110" 。 我们可以通过 4 个操作使数组非降序： - 交换 nums[0] 和 nums[1] 。8 和 4 分别只有 1 个数位为 1 。数组变为 [4,8,2,30,15] 。 - 交换 nums[1] 和 nums[2] 。8 和 2 分别只有 1 个数位为 1 。数组变为 [4,2,8,30,15] 。 - 交换 nums[0] 和 nums[1] 。4 和 2 分别只有 1 个数位为 1 。数组变为 [2,4,8,30,15] 。 - 交换 nums[3] 和 nums[4] 。30 和 15 分别有 4 个数位为 1 ，数组变为 [2,4,8,15,30] 。 数组变成有序的，所以我们返回 true 。 注意我们还可以通过其他的操作序列使数组变得有序。 示例 2： 输入：nums = [1,2,3,4,5] 输出：true 解释：数组已经是非降序的，所以我们返回 true 。 示例 3： 输入：nums = [3,16,8,4,2] 输出：false 解释：无法通过操作使数组变为非降序。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 28
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将数组按位数相同的元素分组，检查每组内是否可以排序成非降序。

算法步骤:
1. 计算每个元素的二进制中1的个数。
2. 使用字典将相同位数的元素分组。
3. 对每个组内的元素进行排序。
4. 检查排序后的数组是否是非降序。

关键点:
- 使用位运算计算每个元素的二进制中1的个数。
- 使用字典将相同位数的元素分组。
- 对每个组内的元素进行排序并合并。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中n是数组长度。排序操作的时间复杂度为O(n log n)。
空间复杂度: O(n)，需要额外的空间存储分组后的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_be_sorted(nums: List[int]) -> bool:
    """
    函数式接口 - 判断一个数组是否可以变为有序
    """
    # 计算每个元素的二进制中1的个数
    bit_counts = [bin(num).count('1') for num in nums]
    
    # 使用字典将相同位数的元素分组
    groups = {}
    for i, count in enumerate(bit_counts):
        if count not in groups:
            groups[count] = []
        groups[count].append(nums[i])
    
    # 对每个组内的元素进行排序
    for count in groups:
        groups[count].sort()
    
    # 合并排序后的组
    sorted_nums = []
    for count in sorted(groups.keys()):
        sorted_nums.extend(groups[count])
    
    # 检查排序后的数组是否是非降序
    return all(sorted_nums[i] <= sorted_nums[i + 1] for i in range(len(sorted_nums) - 1))


Solution = create_solution(can_be_sorted)