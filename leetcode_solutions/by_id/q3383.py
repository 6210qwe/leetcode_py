"""
3147. 从魔法师身上吸取的最大能量 - 在神秘的地牢中，n 个魔法师站成一排。每个魔法师都拥有一个属性，这个属性可以给你提供能量。有些魔法师可能会给你负能量，即从你身上吸取能量。 你被施加了一种诅咒，当你从魔法师 i 处吸收能量后，你将被立即传送到魔法师 (i + k) 处。这一过程将重复进行，直到你到达一个不存在 (i + k) 的魔法师为止。 换句话说，你将选择一个起点，然后以 k 为间隔跳跃，直到到达魔法师序列的末端，在过程中吸收所有的能量。 给定一个数组 energy 和一个整数k，返回你能获得的 最大 能量。 示例 1： 输入： energy = [5,2,-10,-5,1], k = 3 输出： 3 解释：可以从魔法师 1 开始，吸收能量 2 + 1 = 3。 示例 2： 输入： energy = [-2,-3,-1], k = 2 输出： -1 解释：可以从魔法师 2 开始，吸收能量 -1。 提示： * 1 <= energy.length <= 105 * -1000 <= energy[i] <= 1000 * 1 <= k <= energy.length - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i] 为从第 i 个魔法师开始能获得的最大能量。通过递推公式 dp[i] = max(dp[i], dp[i + k] + energy[i]) 来更新 dp 数组。

算法步骤:
1. 初始化一个长度为 n 的 dp 数组，初始值为负无穷。
2. 从后向前遍历数组，更新 dp 数组。
3. 返回 dp 数组中的最大值。

关键点:
- 使用动态规划来避免重复计算。
- 从后向前遍历数组，确保在更新 dp[i] 时，dp[i + k] 已经被计算过。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(energy: List[int], k: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    n = len(energy)
    if n == 0:
        return 0

    # 初始化 dp 数组
    dp = [float('-inf')] * n

    # 从后向前遍历数组
    for i in range(n - 1, -1, -1):
        if i + k < n:
            dp[i] = dp[i + k] + energy[i]
        else:
            dp[i] = energy[i]

    # 更新 dp 数组
    for i in range(n - k - 1, -1, -1):
        dp[i] = max(dp[i], dp[i + k] + energy[i])

    # 返回 dp 数组中的最大值
    return max(dp)


Solution = create_solution(solution_function_name)