def my_pow(x: float, n: int) -> float:
    """
    计算 x 的 n 次幂。
    :param x: 底数
    :param n: 指数
    :return: x 的 n 次幂
    """
    if n == 0:
        return 1
    if n < 0:
        x = 1 / x
        n = -n
    
    result = 1
    while n > 0:
        if n % 2 == 1:
            result *= x
        x *= x
        n //= 2
    return result


