"""
核心思想: 使用快速幂算法来计算 x 的 n 次幂。

算法步骤:
1. 如果 n 为 0，直接返回 1。
2. 如果 n 为负数，将 x 取倒数并将 n 取反。
3. 使用递归或迭代的方法，通过将 n 不断减半来减少乘法次数。
4. 递归地计算 x 的 n/2 次幂，并根据 n 的奇偶性决定是否需要再乘以一个 x。

关键点:
- 通过将 n 不断减半，可以将时间复杂度从 O(n) 降低到 O(log n)。
- 递归方法易于理解和实现，但迭代方法在空间复杂度上更优。
"""

