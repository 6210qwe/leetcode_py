"""
3005. 最大频率元素计数 - 给你一个由 正整数 组成的数组 nums 。 返回数组 nums 中所有具有 最大 频率的元素的 总频率 。 元素的 频率 是指该元素在数组中出现的次数。 示例 1： 输入：nums = [1,2,2,3,1,4] 输出：4 解释：元素 1 和 2 的频率为 2 ，是数组中的最大频率。 因此具有最大频率的元素在数组中的数量是 4 。 示例 2： 输入：nums = [1,2,3,4,5] 输出：5 解释：数组中的所有元素的频率都为 1 ，是最大频率。 因此具有最大频率的元素在数组中的数量是 5 。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个元素的频率，然后找到最大频率，并计算所有具有最大频率的元素的总频率。

算法步骤:
1. 使用哈希表统计每个元素的频率。
2. 找到最大频率。
3. 计算所有具有最大频率的元素的总频率。

关键点:
- 使用哈希表进行频率统计。
- 一次遍历找到最大频率。
- 再次遍历哈希表计算总频率。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。需要两次遍历数组。
空间复杂度: O(n)，哈希表的空间复杂度为 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现
    """
    # 使用哈希表统计每个元素的频率
    frequency = {}
    for num in nums:
        if num in frequency:
            frequency[num] += 1
        else:
            frequency[num] = 1
    
    # 找到最大频率
    max_freq = max(frequency.values())
    
    # 计算所有具有最大频率的元素的总频率
    total_max_freq = sum(freq for freq in frequency.values() if freq == max_freq)
    
    return total_max_freq


Solution = create_solution(solution_function_name)