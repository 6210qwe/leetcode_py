"""
2413. 最小偶倍数 - 给你一个正整数 n ，返回 2 和 n 的最小公倍数（正整数）。 示例 1： 输入：n = 5 输出：10 解释：5 和 2 的最小公倍数是 10 。 示例 2： 输入：n = 6 输出：6 解释：6 和 2 的最小公倍数是 6 。注意数字会是它自身的倍数。 提示： * 1 <= n <= 150
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 如果 n 是偶数，那么 2 和 n 的最小公倍数就是 n 本身；如果 n 是奇数，那么 2 和 n 的最小公倍数就是 2 * n。

算法步骤:
1. 检查 n 是否为偶数。
2. 如果 n 是偶数，返回 n。
3. 如果 n 是奇数，返回 2 * n。

关键点:
- 判断 n 的奇偶性。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_even_multiple(n: int) -> int:
    """
    函数式接口 - 返回 2 和 n 的最小公倍数
    """
    if n % 2 == 0:
        return n
    else:
        return 2 * n


