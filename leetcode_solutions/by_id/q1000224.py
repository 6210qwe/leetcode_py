"""
LCP 30. 魔塔游戏 - 小扣当前位于魔塔游戏第一层，共有 `N` 个房间，编号为 `0 ~ N-1`。每个房间的补血道具/怪物对于血量影响记于数组 `nums`，其中正数表示道具补血数值，即血量增加对应数值；负数表示怪物造成伤害值，即血量减少对应数值；`0` 表示房间对血量无影响。 **小扣初始血量为 1，且无上限**。假定小扣原计划按房间编号升序访问所有房间补血/打怪，**为保证血量始终为正值**，小扣需对房间访问顺序进行调整，**每次仅能将一个怪物房间（负数的房间）调整至访问顺序末尾**。请返回小扣最少需要调整几次，才能顺利访问所有房间。若调整顺序也无法访问完全部房间，请返回 -1。 **示例 1：** >输入：`nums = [100,100,100,-250,-60,-140,-50,-50,100,150]` > >输出：`1` > >解释：初始血量为 1。至少需要将 nums[3] 调整至访问顺序末尾以满足要求。 **示例 2：** >输入：`nums = [-200,-300,400,0]` > >输出：`-1` > >解释：调整访问顺序也无法完成全部房间的访问。 **提示：** - `1 <= nums.length <= 10^5` - `-10^5 <= nums[i] <= 10^5`
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和最小堆来解决这个问题。我们使用一个最小堆来存储遇到的所有负数房间，以便在需要时可以快速找到并调整到末尾。

算法步骤:
1. 初始化血量为 1，调整次数为 0。
2. 遍历所有房间：
   - 如果当前房间是补血房间（正数或零），直接增加血量。
   - 如果当前房间是怪物房间（负数），将该房间加入最小堆，并减少血量。
   - 如果血量小于等于 0，则从最小堆中取出一个最小的负数房间，将其调整到末尾，并增加血量和调整次数。
3. 最后检查是否所有房间都可以访问，如果不能则返回 -1。

关键点:
- 使用最小堆来快速找到并调整最小的负数房间。
- 通过贪心算法确保每次调整都是最优的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度。遍历数组的时间复杂度是 O(n)，而每次插入和删除堆的操作时间复杂度是 O(log n)。
空间复杂度: O(n)，最坏情况下堆中可能包含所有的负数房间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现
    """
    if sum(nums) < 0:
        return -1
    
    hp = []
    blood = 1
    adjust_count = 0
    
    for num in nums:
        if num >= 0:
            blood += num
        else:
            heapq.heappush(hp, num)
            blood += num
            while blood <= 0 and hp:
                blood -= heapq.heappop(hp)
                adjust_count += 1
    
    if blood + sum(hp) <= 0:
        return -1
    return adjust_count

