"""
1750. 删除字符串两端相同字符后的最短长度 - 给你一个只包含字符 'a'，'b' 和 'c' 的字符串 s ，你可以执行下面这个操作（5 个步骤）任意次： 1. 选择字符串 s 一个 非空 的前缀，这个前缀的所有字符都相同。 2. 选择字符串 s 一个 非空 的后缀，这个后缀的所有字符都相同。 3. 前缀和后缀在字符串中任意位置都不能有交集。 4. 前缀和后缀包含的所有字符都要相同。 5. 同时删除前缀和后缀。 请你返回对字符串 s 执行上面操作任意次以后（可能 0 次），能得到的 最短长度 。 示例 1： 输入：s = "ca" 输出：2 解释：你没法删除任何一个字符，所以字符串长度仍然保持不变。 示例 2： 输入：s = "cabaabac" 输出：0 解释：最优操作序列为： - 选择前缀 "c" 和后缀 "c" 并删除它们，得到 s = "abaaba" 。 - 选择前缀 "a" 和后缀 "a" 并删除它们，得到 s = "baab" 。 - 选择前缀 "b" 和后缀 "b" 并删除它们，得到 s = "aa" 。 - 选择前缀 "a" 和后缀 "a" 并删除它们，得到 s = "" 。 示例 3： 输入：s = "aabccabba" 输出：3 解释：最优操作序列为： - 选择前缀 "aa" 和后缀 "a" 并删除它们，得到 s = "bccabb" 。 - 选择前缀 "b" 和后缀 "bb" 并删除它们，得到 s = "cca" 。 提示： * 1 <= s.length <= 105 * s 只包含字符 'a'，'b' 和 'c' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针从两端向中间遍历，同时删除相同的前缀和后缀。

算法步骤:
1. 初始化两个指针 left 和 right 分别指向字符串的开头和结尾。
2. 当 left < right 且 s[left] == s[right] 时，继续删除相同的前缀和后缀。
3. 如果 left 和 right 相遇或交错，则返回剩余字符串的长度。

关键点:
- 使用双指针从两端向中间遍历，确保删除相同的前缀和后缀。
- 注意处理边界情况，如字符串为空或所有字符都被删除的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。每个字符最多被访问两次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_length(s: str) -> int:
    """
    函数式接口 - 返回删除相同前缀和后缀后的最短字符串长度
    """
    if not s:
        return 0

    left, right = 0, len(s) - 1

    while left < right and s[left] == s[right]:
        current_char = s[left]
        while left <= right and s[left] == current_char:
            left += 1
        while left <= right and s[right] == current_char:
            right -= 1

    return right - left + 1


Solution = create_solution(minimum_length)