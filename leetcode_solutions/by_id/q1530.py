"""
1433. 检查一个字符串是否可以打破另一个字符串 - 给你两个字符串 s1 和 s2 ，它们长度相等，请你检查是否存在一个 s1 的排列可以打破 s2 的一个排列，或者是否存在一个 s2 的排列可以打破 s1 的一个排列。 字符串 x 可以打破字符串 y （两者长度都为 n ）需满足对于所有 i（在 0 到 n - 1 之间）都有 x[i] >= y[i]（字典序意义下的顺序）。 示例 1： 输入：s1 = "abc", s2 = "xya" 输出：true 解释："ayx" 是 s2="xya" 的一个排列，"abc" 是字符串 s1="abc" 的一个排列，且 "ayx" 可以打破 "abc" 。 示例 2： 输入：s1 = "abe", s2 = "acd" 输出：false 解释：s1="abe" 的所有排列包括："abe"，"aeb"，"bae"，"bea"，"eab" 和 "eba" ，s2="acd" 的所有排列包括："acd"，"adc"，"cad"，"cda"，"dac" 和 "dca"。然而没有任何 s1 的排列可以打破 s2 的排列。也没有 s2 的排列能打破 s1 的排列。 示例 3： 输入：s1 = "leetcodee", s2 = "interview" 输出：true 提示： * s1.length == n * s2.length == n * 1 <= n <= 10^5 * 所有字符串都只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和贪心算法来判断一个字符串的排列是否可以打破另一个字符串的排列。

算法步骤:
1. 将 s1 和 s2 分别排序。
2. 检查 s1 是否可以打破 s2，即对于所有 i，s1[i] >= s2[i]。
3. 如果 s1 不能打破 s2，则检查 s2 是否可以打破 s1，即对于所有 i，s2[i] >= s1[i]。
4. 如果上述任一条件成立，则返回 True，否则返回 False。

关键点:
- 排序后，可以通过逐个比较字符来判断是否可以打破。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是字符串的长度。主要由排序操作决定。
空间复杂度: O(1)，除了输入和输出外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_if_can_break(s1: str, s2: str) -> bool:
    """
    检查一个字符串是否可以打破另一个字符串
    """
    # 将 s1 和 s2 分别排序
    sorted_s1 = sorted(s1)
    sorted_s2 = sorted(s2)

    # 检查 s1 是否可以打破 s2
    can_break_1 = all(c1 >= c2 for c1, c2 in zip(sorted_s1, sorted_s2))

    # 如果 s1 不能打破 s2，则检查 s2 是否可以打破 s1
    if not can_break_1:
        can_break_2 = all(c2 >= c1 for c1, c2 in zip(sorted_s1, sorted_s2))
        return can_break_2

    return can_break_1


Solution = create_solution(check_if_can_break)