"""
2348. 全 0 子数组的数目 - 给你一个整数数组 nums ，返回全部为 0 的 子数组 数目。 子数组 是一个数组中一段连续非空元素组成的序列。 示例 1： 输入：nums = [1,3,0,0,2,0,0,4] 输出：6 解释： 子数组 [0] 出现了 4 次。 子数组 [0,0] 出现了 2 次。 不存在长度大于 2 的全 0 子数组，所以我们返回 6 。 示例 2： 输入：nums = [0,0,0,2,0,0] 输出：9 解释： 子数组 [0] 出现了 5 次。 子数组 [0,0] 出现了 3 次。 子数组 [0,0,0] 出现了 1 次。 不存在长度大于 3 的全 0 子数组，所以我们返回 9 。 示例 3： 输入：nums = [2,10,2019] 输出：0 解释：没有全 0 子数组，所以我们返回 0 。 提示： * 1 <= nums.length <= 105 * -109 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组，计算每个连续 0 子数组的长度，并利用组合数学公式计算子数组的数量。

算法步骤:
1. 初始化计数器和当前连续 0 子数组的长度。
2. 遍历数组，如果遇到 0，则增加当前连续 0 子数组的长度。
3. 如果遇到非 0 元素或到达数组末尾，则计算当前连续 0 子数组的所有子数组数量，并重置计数器。
4. 返回总的数量。

关键点:
- 利用组合数学公式 (n * (n + 1) // 2) 计算长度为 n 的连续 0 子数组的所有子数组数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度，因为我们需要遍历整个数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算全 0 子数组的数目
    """
    count = 0
    current_length = 0
    
    for num in nums:
        if num == 0:
            current_length += 1
        else:
            count += current_length * (current_length + 1) // 2
            current_length = 0
    
    # 处理数组末尾的连续 0 子数组
    if current_length > 0:
        count += current_length * (current_length + 1) // 2
    
    return count

