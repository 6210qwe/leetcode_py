# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划，dp[i][j]表示第i个房子涂第j种颜色的最小成本

算法步骤:
1. dp[i][0] = costs[i][0] + min(dp[i-1][1], dp[i-1][2])
2. dp[i][1] = costs[i][1] + min(dp[i-1][0], dp[i-1][2])
3. dp[i][2] = costs[i][2] + min(dp[i-1][0], dp[i-1][1])
4. 返回min(dp[n-1])

关键点:
- 相邻房子不能同色
- 空间优化到O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - n为房子数量
空间复杂度: O(1) - 优化后空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_cost(costs: List[List[int]]) -> int:
    """
    函数式接口 - 粉刷房子
    
    实现思路:
    动态规划：相邻房子不能同色。
    
    Args:
        costs: 每个房子涂三种颜色的成本
        
    Returns:
        最小成本
        
    Example:
        >>> min_cost([[17,2,17],[16,16,5],[14,3,19]])
        10
    """
    if not costs:
        return 0
    
    # 空间优化：只使用三个变量
    prev = costs[0][:]
    
    for i in range(1, len(costs)):
        curr = [
            costs[i][0] + min(prev[1], prev[2]),
            costs[i][1] + min(prev[0], prev[2]),
            costs[i][2] + min(prev[0], prev[1])
        ]
        prev = curr
    
    return min(prev)


