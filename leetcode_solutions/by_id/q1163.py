import pandas as pd

def solution_function_name(project_employee: pd.DataFrame, threshold: int) -> pd.DataFrame:
    """
    函数式接口 - 实现最优解法
    """
    # 计算每个项目的员工数量
    project_count = project_employee.groupby('project_id').size().reset_index(name='employee_count')
    
    # 过滤出员工数量大于或等于阈值的项目
