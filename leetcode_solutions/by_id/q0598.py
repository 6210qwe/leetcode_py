"""
598. 区间加法 II - 给你一个 m x n 的矩阵 M 和一个操作数组 op 。矩阵初始化时所有的单元格都为 0 。ops[i] = [ai, bi] 意味着当所有的 0 <= x < ai 和 0 <= y < bi 时， M[x][y] 应该加 1。 在 执行完所有操作后 ，计算并返回 矩阵中最大整数的个数 。 示例 1: [https://assets.leetcode.com/uploads/2020/10/02/ex1.jpg] 输入: m = 3, n = 3，ops = [[2,2],[3,3]] 输出: 4 解释: M 中最大的整数是 2, 而且 M 中有4个值为2的元素。因此返回 4。 示例 2: 输入: m = 3, n = 3, ops = [[2,2],[3,3],[3,3],[3,3],[2,2],[3,3],[3,3],[3,3],[2,2],[3,3],[3,3],[3,3]] 输出: 4 示例 3: 输入: m = 3, n = 3, ops = [] 输出: 9 提示: * 1 <= m, n <= 4 * 104 * 0 <= ops.length <= 104 * ops[i].length == 2 * 1 <= ai <= m * 1 <= bi <= n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过找到所有操作的最小范围来确定矩阵中最大整数的个数。

算法步骤:
1. 初始化两个变量 min_a 和 min_b 为 m 和 n。
2. 遍历操作数组 ops，更新 min_a 和 min_b 为所有操作中的最小值。
3. 返回 min_a * min_b，即为矩阵中最大整数的个数。

关键点:
- 通过找到所有操作的最小范围，可以确定矩阵中最大整数的个数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(k)，其中 k 是 ops 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_count(m: int, n: int, ops: List[List[int]]) -> int:
    """
    函数式接口 - 计算矩阵中最大整数的个数
    """
    # 初始化最小范围
    min_a = m
    min_b = n

    # 遍历操作数组，更新最小范围
    for op in ops:
        min_a = min(min_a, op[0])
        min_b = min(min_b, op[1])

    # 返回最大整数的个数
    return min_a * min_b


Solution = create_solution(max_count)