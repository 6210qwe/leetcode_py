"""
1039. 多边形三角剖分的最低得分 - 你有一个凸的 n 边形，其每个顶点都有一个整数值。给定一个整数数组 values ，其中 values[i] 是按 顺时针顺序 第 i 个顶点的值。 假设将多边形 剖分 为 n - 2 个三角形。对于每个三角形，该三角形的值是顶点标记的乘积，三角剖分的分数是进行三角剖分后所有 n - 2 个三角形的值之和。 返回 多边形进行三角剖分后可以得到的最低分 。 示例 1： [https://assets.leetcode.com/uploads/2025/10/23/ex0-2.png] 输入：values = [1,2,3] 输出：6 解释：多边形已经三角化，唯一三角形的分数为 6。 示例 2： [https://assets.leetcode.com/uploads/2025/10/23/ex1-2.png] 输入：values = [3,7,4,5] 输出：144 解释：有两种三角剖分，可能得分分别为：3*7*5 + 4*5*7 = 245，或 3*4*5 + 3*4*7 = 144。最低分数为 144。 示例 3： [https://assets.leetcode.com/uploads/2025/10/23/ex2.png] 输入：values = [1,3,1,4,1,5] 输出：13 解释：最低分数三角剖分的得分情况为 1*1*3 + 1*1*4 + 1*1*5 + 1*1*1 = 13。 提示： * n == values.length * 3 <= n <= 50 * 1 <= values[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 为从顶点 i 到顶点 j 的子多边形的最小三角剖分分数。通过递归地计算每个子多边形的最小分数，并利用之前计算的结果来避免重复计算。

算法步骤:
1. 初始化一个二维数组 dp，其中 dp[i][j] 表示从顶点 i 到顶点 j 的子多边形的最小三角剖分分数。
2. 对于每个子多边形，枚举中间顶点 k，计算 dp[i][j] = min(dp[i][j], values[i] * values[j] * values[k] + dp[i][k] + dp[k][j])。
3. 最终结果保存在 dp[0][n-1] 中。

关键点:
- 动态规划的状态转移方程。
- 递归地计算每个子多边形的最小分数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3)，其中 n 是多边形的顶点数。三重循环的时间复杂度为 O(n^3)。
空间复杂度: O(n^2)，需要一个二维数组 dp 来存储子多边形的最小三角剖分分数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def min_score_triangulation(values: List[int]) -> int:
    """
    计算多边形三角剖分的最低得分
    """
    n = len(values)
    dp = [[0] * n for _ in range(n)]

    # 枚举区间长度
    for length in range(2, n):
        for i in range(n - length):
            j = i + length
            dp[i][j] = float('inf')
            for k in range(i + 1, j):
                dp[i][j] = min(dp[i][j], values[i] * values[j] * values[k] + dp[i][k] + dp[k][j])

    return dp[0][n - 1]

