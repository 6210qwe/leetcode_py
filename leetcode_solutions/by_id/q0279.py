from typing import List
import math
from leetcode_solutions.utils.solution import create_solution


def perfect_squares(n: int) -> int:
    """
    函数式接口 - 完全平方数
    
    实现思路:
    动态规划，dp[i]表示和为i的完全平方数的最少数量。
    
    Args:
        n: 整数
        
    Returns:
        和为n的完全平方数的最少数量
        
    Example:
        >>> perfect_squares(12)
        3
    """
    dp = [float('inf')] * (n + 1)
    dp[0] = 0
    
    for i in range(1, n + 1):
        j = 1
        while j * j <= i:
            dp[i] = min(dp[i], dp[i - j * j] + 1)
            j += 1
    
    return dp[n]


