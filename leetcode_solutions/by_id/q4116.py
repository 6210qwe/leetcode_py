"""
3736. 最小操作次数使数组元素相等 III - 给你一个整数数组 nums。 在一步操作中，你可以将任意单个元素 nums[i] 的值 增加 1。 返回使数组中的所有元素都 相等 所需的 最小总操作次数 。 示例 1: 输入: nums = [2,1,3] 输出: 3 解释: 使所有元素相等的操作如下: * 将 nums[0] = 2 增加 1, 变为 3。 * 将 nums[1] = 1 增加 1, 变为 2。 * 将 nums[1] = 2 增加 1, 变为 3。 现在，nums 中的所有元素都等于 3。最小总操作次数为 3。 示例 2: 输入: nums = [4,4,5] 输出: 2 解释: 使所有元素相等的操作如下: * 将 nums[0] = 4 增加 1, 变为 5。 * 将 nums[1] = 4 增加 1, 变为 5。 现在，nums 中的所有元素都等于 5。最小总操作次数为 2。 提示: * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过将所有元素增加到数组的中位数来最小化操作次数。

算法步骤:
1. 对数组进行排序。
2. 找到数组的中位数。
3. 计算每个元素与中位数的差值之和，即为所需的操作次数。

关键点:
- 使用中位数可以确保总操作次数最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度，因为排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def min_moves_to_equal_array_elements(nums: List[int]) -> int:
    """
    函数式接口 - 返回使数组中的所有元素都相等所需的最小总操作次数。
    """
    # 对数组进行排序
    nums.sort()
    
    # 找到数组的中位数
    median = nums[len(nums) // 2]
    
    # 计算每个元素与中位数的差值之和
    total_moves = sum(abs(num - median) for num in nums)
    
    return total_moves

