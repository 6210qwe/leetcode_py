"""
1323. 6 和 9 组成的最大数字 - 给你一个仅由数字 6 和 9 组成的正整数 num。 你最多只能翻转一位数字，将 6 变成 9，或者把 9 变成 6 。 请返回你可以得到的最大数字。 示例 1： 输入：num = 9669 输出：9969 解释： 改变第一位数字可以得到 6669 。 改变第二位数字可以得到 9969 。 改变第三位数字可以得到 9699 。 改变第四位数字可以得到 9666 。 其中最大的数字是 9969 。 示例 2： 输入：num = 9996 输出：9999 解释：将最后一位从 6 变到 9，其结果 9999 是最大的数。 示例 3： 输入：num = 9999 输出：9999 解释：无需改变就已经是最大的数字了。 提示： * 1 <= num <= 10^4 * num 每一位上的数字都是 6 或者 9 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 尽可能将第一个出现的 6 翻转为 9，以获得最大值。

算法步骤:
1. 将数字转换为字符串形式。
2. 查找第一个出现的 6 并将其替换为 9。
3. 将修改后的字符串转换回整数并返回。

关键点:
- 通过字符串操作来实现高效的查找和替换。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数字的位数。最坏情况下需要遍历整个数字。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: int) -> int:
    """
    函数式接口 - 返回将一个 6 翻转为 9 后的最大数字
    """
    # 将数字转换为字符串
    num_str = str(num)
    
    # 查找第一个出现的 6 并将其替换为 9
    for i in range(len(num_str)):
        if num_str[i] == '6':
            num_str = num_str[:i] + '9' + num_str[i+1:]
            break
    
    # 将修改后的字符串转换回整数并返回
    return int(num_str)


Solution = create_solution(solution_function_name)