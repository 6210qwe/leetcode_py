def base_neg2(n: int) -> str:
    """
    函数式接口 - 将十进制数 n 转换为负二进制字符串
    """
    if n == 0:
        return "0"
    
    result = []
    while n != 0:
        remainder = n % -2
        n = n // -2
        if remainder < 0:
            remainder += 2
            n += 1
        result.append(str(remainder))
    
    return ''.join(result[::-1])


