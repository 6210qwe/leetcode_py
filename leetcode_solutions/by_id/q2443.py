"""
2369. 检查数组是否存在有效划分 - 给你一个下标从 0 开始的整数数组 nums ，你必须将数组划分为一个或多个 连续 子数组。 如果获得的这些子数组中每个都能满足下述条件 之一 ，则可以称其为数组的一种 有效 划分： 1. 子数组 恰 由 2 个相等元素组成，例如，子数组 [2,2] 。 2. 子数组 恰 由 3 个相等元素组成，例如，子数组 [4,4,4] 。 3. 子数组 恰 由 3 个连续递增元素组成，并且相邻元素之间的差值为 1 。例如，子数组 [3,4,5] ，但是子数组 [1,3,5] 不符合要求。 如果数组 至少 存在一种有效划分，返回 true ，否则，返回 false 。 示例 1： 输入：nums = [4,4,4,5,6] 输出：true 解释：数组可以划分成子数组 [4,4] 和 [4,5,6] 。 这是一种有效划分，所以返回 true 。 示例 2： 输入：nums = [1,1,1,2] 输出：false 解释：该数组不存在有效划分。 提示： * 2 <= nums.length <= 105 * 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来判断数组是否存在有效划分。

算法步骤:
1. 初始化一个长度为 n+1 的 dp 数组，dp[i] 表示前 i 个元素是否可以有效划分。
2. 设置 dp[0] = True，因为空数组是有效的。
3. 遍历数组，对于每个位置 i，检查以下三种情况：
   - 如果 nums[i-1] == nums[i-2]，则 dp[i] |= dp[i-2]。
   - 如果 nums[i-1] == nums[i-2] == nums[i-3]，则 dp[i] |= dp[i-3]。
   - 如果 nums[i-1] == nums[i-2] + 1 == nums[i-3] + 2，则 dp[i] |= dp[i-3]。
4. 返回 dp[n]。

关键点:
- 动态规划的状态转移方程。
- 边界条件的处理。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。
空间复杂度: O(n)，使用了一个长度为 n+1 的 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


def can_partition_array(nums: List[int]) -> bool:
    n = len(nums)
    dp = [False] * (n + 1)
    dp[0] = True

    for i in range(2, n + 1):
        if nums[i - 1] == nums[i - 2]:
            dp[i] |= dp[i - 2]
        if i >= 3 and nums[i - 1] == nums[i - 2] == nums[i - 3]:
            dp[i] |= dp[i - 3]
        if i >= 3 and nums[i - 1] == nums[i - 2] + 1 == nums[i - 3] + 2:
            dp[i] |= dp[i - 3]

    return dp[n]


Solution = can_partition_array