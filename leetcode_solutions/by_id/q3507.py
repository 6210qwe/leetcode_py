"""
3233. 统计不是特殊数字的数字数量 - 给你两个 正整数 l 和 r。对于任何数字 x，x 的所有正因数（除了 x 本身）被称为 x 的 真因数。 如果一个数字恰好仅有两个 真因数，则称该数字为 特殊数字。例如： * 数字 4 是 特殊数字，因为它的真因数为 1 和 2。 * 数字 6 不是 特殊数字，因为它的真因数为 1、2 和 3。 返回区间 [l, r] 内 不是 特殊数字 的数字数量。 示例 1： 输入： l = 5, r = 7 输出： 3 解释： 区间 [5, 7] 内不存在特殊数字。 示例 2： 输入： l = 4, r = 16 输出： 11 解释： 区间 [4, 16] 内的特殊数字为 4 和 9。 提示： * 1 <= l <= r <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过筛选出平方数来确定特殊数字，然后计算非特殊数字的数量。

算法步骤:
1. 计算区间 [l, r] 内的平方数。
2. 计算区间 [l, r] 内的所有数字数量。
3. 用总数量减去平方数的数量，得到非特殊数字的数量。

关键点:
- 只有平方数才是特殊数字，因为它们只有两个真因数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(√r - √l)，因为我们需要计算区间内的平方数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_non_special_numbers(l: int, r: int) -> int:
    """
    计算区间 [l, r] 内不是特殊数字的数字数量。
    """
    # 计算区间 [l, r] 内的平方数数量
    def count_square_numbers(start: int, end: int) -> int:
        return int(end**0.5) - int((start - 1)**0.5)
    
    # 计算区间 [l, r] 内的所有数字数量
    total_numbers = r - l + 1
    
    # 计算区间 [l, r] 内的平方数数量
    square_numbers = count_square_numbers(l, r)
    
    # 计算非特殊数字的数量
    non_special_numbers = total_numbers - square_numbers
    
    return non_special_numbers


Solution = create_solution(count_non_special_numbers)