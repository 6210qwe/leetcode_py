def non_overlapping_intervals(intervals: List[List[int]]) -> int:
    """
    函数式接口 - 计算需要移除的最小区间数量以使剩余区间互不重叠
    
    实现思路:
    1. 按照区间的结束时间对区间进行排序。
    2. 初始化一个变量 `end` 为第一个区间的结束时间，并初始化计数器 `count` 为 0。
    3. 遍历排序后的区间列表，对于每个区间：
       - 如果当前区间的开始时间大于等于 `end`，则更新 `end` 为当前区间的结束时间。
       - 否则，增加 `count` 计数器。
    4. 返回 `count` 作为需要移除的区间数量。
    
    Args:
        intervals: 区间列表，每个区间表示为 [start, end]
        
    Returns:
        需要移除的最小区间数量
        
    Example:
        >>> non_overlapping_intervals([[1,2],[2,3],[3,4],[1,3]])
        1
    """
    if not intervals:
        return 0

    # 按照区间的结束时间进行排序
    intervals.sort(key=lambda x: x[1])

    # 初始化变量
    end = intervals[0][1]
    count = 0

    # 遍历排序后的区间列表
    for interval in intervals[1:]:
        if interval[0] >= end:
            # 更新结束时间
            end = interval[1]
        else:
            # 增加计数器
            count += 1

    return count


