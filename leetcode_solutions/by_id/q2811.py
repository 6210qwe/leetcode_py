"""
2829. k-avoiding 数组的最小总和 - 给你两个整数 n 和 k 。 对于一个由 不同 正整数组成的数组，如果其中不存在任何求和等于 k 的不同元素对，则称其为 k-avoiding 数组。 返回长度为 n 的 k-avoiding 数组的可能的最小总和。 示例 1： 输入：n = 5, k = 4 输出：18 解释：设若 k-avoiding 数组为 [1,2,4,5,6] ，其元素总和为 18 。 可以证明不存在总和小于 18 的 k-avoiding 数组。 示例 2： 输入：n = 2, k = 6 输出：3 解释：可以构造数组 [1,2] ，其元素总和为 3 。 可以证明不存在总和小于 3 的 k-avoiding 数组。 提示： * 1 <= n, k <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，从 1 开始选择尽可能小的数，并跳过与已选数之和为 k 的数。

算法步骤:
1. 初始化一个空集合 `used` 来记录已使用的数。
2. 初始化一个变量 `sum` 来记录当前数组的总和。
3. 从 1 开始遍历，直到找到 n 个数：
   - 如果当前数 i 和已使用的数中的某个数 j 之和等于 k，则跳过当前数 i。
   - 否则，将当前数 i 加入 `used` 集合，并将其加入 `sum`。
4. 返回 `sum` 作为结果。

关键点:
- 使用集合来记录已使用的数，以便快速检查是否存在与当前数之和为 k 的数。
- 从 1 开始选择尽可能小的数，确保总和最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, k: int) -> int:
    """
    函数式接口 - 计算长度为 n 的 k-avoiding 数组的最小总和
    """
    used = set()
    total_sum = 0
    current_num = 1
    
    while len(used) < n:
        if k - current_num not in used:
            used.add(current_num)
            total_sum += current_num
        current_num += 1
    
    return total_sum


Solution = create_solution(solution_function_name)