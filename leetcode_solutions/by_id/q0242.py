# ============================================================================

from leetcode_solutions.utils.solution import create_solution


def valid_anagram(s: str, t: str) -> bool:
    """
    函数式接口 - 有效的字母异位词
    
    实现思路:
    统计两个字符串中每个字符的出现次数，如果完全相同则是字母异位词。
    
    Args:
        s: 字符串s
        t: 字符串t
        
    Returns:
        t是否是s的字母异位词
        
    Example:
        >>> valid_anagram("anagram", "nagaram")
        True
    """
    if len(s) != len(t):
        return False
    
    count = [0] * 26
    
    for char in s:
        count[ord(char) - ord('a')] += 1
    
    for char in t:
        count[ord(char) - ord('a')] -= 1
        if count[ord(char) - ord('a')] < 0:
            return False
    
    return True


