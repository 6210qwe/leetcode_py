# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 统计两个字符串中每个字符的出现次数，如果完全相同则是字母异位词

算法步骤:
1. 如果两个字符串长度不同，返回False
2. 使用数组或哈希表统计每个字符的出现次数
3. 比较两个字符串的字符计数是否相同

关键点:
- 使用数组统计字符出现次数（小写字母）
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
