"""
2195. 向数组中追加 K 个整数 - 给你一个整数数组 nums 和一个整数 k 。请你向 nums 中追加 k 个 未 出现在 nums 中的、互不相同 的 正 整数，并使结果数组的元素和 最小 。 返回追加到 nums 中的 k 个整数之和。 示例 1： 输入：nums = [1,4,25,10,25], k = 2 输出：5 解释：在该解法中，向数组中追加的两个互不相同且未出现的正整数是 2 和 3 。 nums 最终元素和为 1 + 4 + 25 + 10 + 25 + 2 + 3 = 70 ，这是所有情况中的最小值。 所以追加到数组中的两个整数之和是 2 + 3 = 5 ，所以返回 5 。 示例 2： 输入：nums = [5,6], k = 6 输出：25 解释：在该解法中，向数组中追加的两个互不相同且未出现的正整数是 1 、2 、3 、4 、7 和 8 。 nums 最终元素和为 5 + 6 + 1 + 2 + 3 + 4 + 7 + 8 = 36 ，这是所有情况中的最小值。 所以追加到数组中的两个整数之和是 1 + 2 + 3 + 4 + 7 + 8 = 25 ，所以返回 25 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i], k <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法选择最小的未出现在 nums 中的正整数，确保追加的 k 个整数之和最小。

算法步骤:
1. 将 nums 排序并去重。
2. 初始化一个变量 `current` 为 1，表示当前要检查的最小正整数。
3. 使用一个循环来找到 k 个未出现在 nums 中的正整数：
   - 如果 `current` 不在 nums 中，则将其加入结果，并增加 `current`。
   - 如果 `current` 在 nums 中，则跳过。
4. 计算并返回这 k 个整数的和。

关键点:
- 通过排序和去重，确保我们只检查未出现的最小正整数。
- 通过贪心算法，确保每次选择的都是当前最小的未出现正整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + k)，其中 n 是 nums 的长度，k 是需要追加的整数个数。排序操作的时间复杂度为 O(n log n)，查找 k 个未出现的正整数的时间复杂度为 O(k)。
空间复杂度: O(n)，用于存储去重后的 nums。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def append_k_integers_with_minimal_sum(nums: List[int], k: int) -> int:
    """
    向数组中追加 K 个整数 - 使结果数组的元素和最小。
    """
    # 去重并排序
    nums = sorted(set(nums))
    
    current = 1
    result = 0
    added_count = 0
    
    for num in nums:
        while current < num:
            result += current
            added_count += 1
            if added_count == k:
                return result
            current += 1
        current = num + 1
    
    while added_count < k:
        result += current
        added_count += 1
        current += 1
    
    return result


Solution = create_solution(append_k_integers_with_minimal_sum)