"""
1507. 转变日期格式 - 给你一个字符串 date ，它的格式为 Day Month Year ，其中： * Day 是集合 {"1st", "2nd", "3rd", "4th", ..., "30th", "31st"} 中的一个元素。 * Month 是集合 {"Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"} 中的一个元素。 * Year 的范围在 [1900, 2100] 之间。 请你将字符串转变为 YYYY-MM-DD 的格式，其中： * YYYY 表示 4 位的年份。 * MM 表示 2 位的月份。 * DD 表示 2 位的天数。 示例 1： 输入：date = "20th Oct 2052" 输出："2052-10-20" 示例 2： 输入：date = "6th Jun 1933" 输出："1933-06-06" 示例 3： 输入：date = "26th May 1960" 输出："1960-05-26" 提示： * 给定日期保证是合法的，所以不需要处理异常输入。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用字符串操作和字典来解析和重新格式化日期。

算法步骤:
1. 将输入字符串按空格分割成三个部分：day, month, year。
2. 去除 day 中的后缀（如 "th", "st", "nd", "rd"）并转换为整数。
3. 使用字典将 month 映射到对应的数字。
4. 格式化 day 和 month 为两位数。
5. 拼接 year, month, day 为 YYYY-MM-DD 格式。

关键点:
- 使用字典快速映射月份。
- 确保 day 和 month 为两位数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 字符串操作和字典查找都是常数时间。
空间复杂度: O(1) - 使用固定大小的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def reformat_date(date: str) -> str:
    """
    将给定的日期字符串从 Day Month Year 格式转换为 YYYY-MM-DD 格式。
    """
    # 月份映射
    month_map = {
        "Jan": "01", "Feb": "02", "Mar": "03", "Apr": "04",
        "May": "05", "Jun": "06", "Jul": "07", "Aug": "08",
        "Sep": "09", "Oct": "10", "Nov": "11", "Dec": "12"
    }
    
    # 分割日期字符串
    day, month, year = date.split()
    
    # 去除 day 中的后缀并转换为整数
    day = int(day[:-2])
    
    # 格式化 day 和 month
    day_str = f"{day:02d}"
    month_str = month_map[month]
    
    # 拼接结果
    return f"{year}-{month_str}-{day_str}"

Solution = create_solution(reformat_date)