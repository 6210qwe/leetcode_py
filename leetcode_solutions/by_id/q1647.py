"""
1540. K 次操作转变字符串 - 给你两个字符串 s 和 t ，你的目标是在 k 次操作以内把字符串 s 转变成 t 。 在第 i 次操作时（1 <= i <= k），你可以选择进行如下操作： * 选择字符串 s 中满足 1 <= j <= s.length 且之前未被选过的任意下标 j （下标从 1 开始），并将此位置的字符切换 i 次。 * 不进行任何操作。 切换 1 个字符的意思是用字母表中该字母的下一个字母替换它（字母表环状接起来，所以 'z' 切换后会变成 'a'）。第 i 次操作意味着该字符应切换 i 次 请记住任意一个下标 j 最多只能被操作 1 次。 如果在不超过 k 次操作内可以把字符串 s 转变成 t ，那么请你返回 true ，否则请你返回 false 。 示例 1： 输入：s = "input", t = "ouput", k = 9 输出：true 解释：第 6 次操作时，我们将 'i' 切换 6 次得到 'o' 。第 7 次操作时，我们将 'n' 切换 7 次得到 'u' 。 示例 2： 输入：s = "abc", t = "bcd", k = 10 输出：false 解释：我们需要将每个字符切换 1 次才能得到 t 。我们可以在第 1 次操作时将 'a' 切换成 'b' ，但另外 2 个字母在剩余操作中无法再转变为 t 中对应字母。 示例 3： 输入：s = "aab", t = "bbb", k = 27 输出：true 解释：第 1 次操作时，我们将第一个 'a' 切换 1 次得到 'b' 。在第 27 次操作时，我们将第二个字母 'a' 切换 27 次得到 'b' 。 提示： * 1 <= s.length, t.length <= 10^5 * 0 <= k <= 10^9 * s 和 t 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个字符需要切换的次数，并检查是否能在 k 次操作内完成。

算法步骤:
1. 检查 s 和 t 的长度是否相同，如果不同直接返回 False。
2. 初始化一个哈希表，记录每个字符需要切换的次数。
3. 遍历 s 和 t，计算每个字符需要切换的次数，并更新哈希表。
4. 检查哈希表中的每个切换次数是否能在 k 次操作内完成。

关键点:
- 使用哈希表记录每个字符需要切换的次数。
- 计算每个字符需要切换的次数时，使用模运算处理环状字母表。
- 检查每个切换次数是否能在 k 次操作内完成。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 和 t 的长度。
空间复杂度: O(1)，哈希表的大小最多为 26。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_convert_string(s: str, t: str, k: int) -> bool:
    """
    函数式接口 - 判断是否能在 k 次操作内将字符串 s 转变成 t
    """
    if len(s) != len(t):
        return False
    
    # 记录每个字符需要切换的次数
    needed_moves = [0] * 26
    
    for char_s, char_t in zip(s, t):
        diff = (ord(char_t) - ord(char_s)) % 26
        if diff > 0:
            needed_moves[diff] += 1
    
    # 检查每个切换次数是否能在 k 次操作内完成
    for i in range(1, 26):
        if needed_moves[i] > 0:
            max_move = i + 26 * (needed_moves[i] - 1)
            if max_move > k:
                return False
    
    return True


Solution = create_solution(can_convert_string)