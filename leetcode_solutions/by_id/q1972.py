"""
1861. 旋转盒子 - 给你一个 m x n 的字符矩阵 boxGrid ，它表示一个箱子的侧视图。箱子的每一个格子可能为： * '#' 表示石头 * '*' 表示固定的障碍物 * '.' 表示空位置 这个箱子被 顺时针旋转 90 度 ，由于重力原因，部分石头的位置会发生改变。每个石头会垂直掉落，直到它遇到障碍物，另一个石头或者箱子的底部。重力 不会 影响障碍物的位置，同时箱子旋转不会产生惯性 ，也就是说石头的水平位置不会发生改变。 题目保证初始时 boxGrid 中的石头要么在一个障碍物上，要么在另一个石头上，要么在箱子的底部。 请你返回一个 n x m 的矩阵，表示按照上述旋转后，箱子内的结果。 示例 1： [https://assets.leetcode.com/uploads/2021/04/08/rotatingtheboxleetcodewithstones.png] 输入：box = [["#",".","#"]] 输出：[["."], ["#"], ["#"]] 示例 2： [https://assets.leetcode.com/uploads/2021/04/08/rotatingtheboxleetcode2withstones.png] 输入：box = [["#",".","*","."], ["#","#","*","."]] 输出：[["#","."], ["#","#"], ["*","*"], [".","."]] 示例 3： [https://assets.leetcode.com/uploads/2021/04/08/rotatingtheboxleetcode3withstone.png] 输入：box = [["#","#","*",".","*","."], ["#","#","#","*",".","."], ["#","#","#",".","#","."]] 输出：[[".","#","#"], [".","#","#"], ["#","#","*"], ["#","*","."], ["#",".","*"], ["#",".","."]] 提示： * m == boxGrid.length * n == boxGrid[i].length * 1 <= m, n <= 500 * boxGrid[i][j] 只可能是 '#' ，'*' 或者 '.' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想:
1. 先处理每一行，让石头尽可能下落到最下方，直到遇到障碍物或箱子底部。
2. 然后将整个矩阵顺时针旋转90度。

算法步骤:
1. 对于每一行，使用双指针方法，将石头移动到最下方。
2. 旋转矩阵，生成新的矩阵。

关键点:
- 使用双指针方法可以高效地处理每一行中的石头下落。
- 旋转矩阵时，注意行列互换和顺序调整。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)
空间复杂度: O(m * n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def rotateTheBox(box: List[List[str]]) -> List[List[str]]:
    m, n = len(box), len(box[0])
    
    # 处理每一行，让石头尽可能下落
    for i in range(m):
        j, k = n - 1, n - 1
        while j >= 0:
            if box[i][j] == '*':
                k = j - 1
            elif box[i][j] == '#':
                box[i][j], box[i][k] = box[i][k], box[i][j]
                k -= 1
            j -= 1
    
    # 旋转矩阵
    rotated_box = [[box[m - 1 - r][c] for r in range(m)] for c in range(n)]
    
    return rotated_box

