"""
核心思想: 通过询问每个二进制位的状态来确定目标数字。

算法步骤:
1. 初始化一个变量 `ans` 为 0。
2. 对于从 0 到 30 的每一位 i（因为 10^9 < 2^30），询问该位是否为 1。
3. 如果该位是 1，则将 `ans` 加上 2^i。
4. 返回 `ans` 作为最终答案。

关键点:
- 通过询问每个二进制位的状态来确定目标数字。
- 由于数字范围在 [1, 10^9] 内，最多只需要询问 30 次。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 固定的 30 次询问。
空间复杂度: O(1) - 只使用常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(guessNumber: 'GuessGame') -> int:
    """
    函数式接口 - 通过询问每个二进制位的状态来确定目标数字。
    """
    ans = 0
    for i in range(31):
        if guessNumber.guess(i):
            ans |= (1 << i)
    return ans


Solution = create_solution(solution_function_name)