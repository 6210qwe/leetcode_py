# ============================================================================
# 问题描述
# ============================================================================
"""
100. 相同的树 - 给你两棵二叉树的根节点 p 和 q ，编写一个函数来检验这两棵树是否相同。 如果两个树在结构上相同，并且节点具有相同的值，则认为它们是相同的。 示例 1： [https://assets.leetcode.com/uploads/2020/12/20/ex1.jpg] 输入：p = [1,2,3], q = [1,2,3] 输出：true 示例 2： [https://assets.leetcode.com/uploads/2020/12/20/ex2.jpg] 输入：p = [1,2], q = [1,null,2] 输出：false 示例 3： [https://assets.leetcode.com/uploads/2020/12/20/ex3.jpg] 输入：p = [1,2,1], q = [1,1,2] 输出：false 提示： * 两棵树上的节点数目都在范围 [0, 100] 内 * -104 <= Node.val <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 递归比较两棵树的对应节点

算法步骤:
1. 如果两个节点都为空，返回True
2. 如果只有一个节点为空，返回False
3. 如果两个节点的值不同，返回False
4. 递归比较左子树和右子树

关键点:
- 同时遍历两棵树，比较对应节点
- 时间复杂度O(n)，空间复杂度O(h)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要访问每个节点一次
空间复杂度: O(h) - 递归栈深度，h为树的高度
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_same_tree(p: Optional[TreeNode], q: Optional[TreeNode]) -> bool:
    """
    函数式接口 - 递归比较
    
    实现思路:
    递归比较两棵树的对应节点，判断它们是否相同。
    
    Args:
        p: 第一棵二叉树的根节点
        q: 第二棵二叉树的根节点
        
    Returns:
        如果两棵树相同返回True，否则返回False
        
    Example:
        >>> p = TreeNode.from_list([1, 2, 3])
        >>> q = TreeNode.from_list([1, 2, 3])
        >>> is_same_tree(p, q)
        True
    """
    # 如果两个节点都为空，返回True
    if not p and not q:
        return True
    
    # 如果只有一个节点为空，返回False
    if not p or not q:
        return False
    
    # 如果两个节点的值不同，返回False
    if p.val != q.val:
        return False
    
    # 递归比较左子树和右子树
    return is_same_tree(p.left, q.left) and is_same_tree(p.right, q.right)


# 自动生成Solution类（无需手动编写）
Solution = create_solution(is_same_tree)
