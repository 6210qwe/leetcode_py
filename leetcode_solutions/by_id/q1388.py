"""
1262. 可被三整除的最大和 - 给你一个整数数组 nums，请你找出并返回能被三整除的元素 最大和。 示例 1： 输入：nums = [3,6,5,1,8] 输出：18 解释：选出数字 3, 6, 1 和 8，它们的和是 18（可被 3 整除的最大和）。 示例 2： 输入：nums = [4] 输出：0 解释：4 不能被 3 整除，所以无法选出数字，返回 0。 示例 3： 输入：nums = [1,2,3,4,4] 输出：12 解释：选出数字 1, 3, 4 以及 4，它们的和是 12（可被 3 整除的最大和）。 提示： * 1 <= nums.length <= 4 * 104 * 1 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划

算法步骤:
1. 初始化一个长度为 3 的数组 dp，其中 dp[i] 表示当前和对 3 取模为 i 的最大和。
2. 遍历数组 nums，对于每个数 num，更新 dp 数组。
3. 最后返回 dp[0]，即和对 3 取模为 0 的最大和。

关键点:
- 使用动态规划来维护当前和对 3 取模为 0, 1, 2 的最大和。
- 通过遍历数组不断更新 dp 数组，确保最终结果是最优解。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现
    """
    # 初始化 dp 数组
    dp = [0, 0, 0]
    
    for num in nums:
        # 保存当前 dp 数组的状态
        new_dp = list(dp)
        
        for i in range(3):
            # 更新 dp 数组
            new_dp[(i + num) % 3] = max(new_dp[(i + num) % 3], dp[i] + num)
        
        dp = new_dp
    
    return dp[0]


Solution = create_solution(solution_function_name)