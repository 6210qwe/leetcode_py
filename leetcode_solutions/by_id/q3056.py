"""
2849. 判断能否在给定时间到达单元格 - 给你四个整数 sx、sy、fx、fy 以及一个 非负整数 t 。 在一个无限的二维网格中，你从单元格 (sx, sy) 开始出发。每一秒，你 必须 移动到任一与之前所处单元格相邻的单元格中。 如果你能在 恰好 t 秒 后到达单元格 (fx, fy) ，返回 true ；否则，返回 false 。 单元格的 相邻单元格 是指该单元格周围与其至少共享一个角的 8 个单元格。你可以多次访问同一个单元格。 示例 1： [https://assets.leetcode.com/uploads/2023/08/05/example2.svg] 输入：sx = 2, sy = 4, fx = 7, fy = 7, t = 6 输出：true 解释：从单元格 (2, 4) 开始出发，穿过上图标注的单元格，可以在恰好 6 秒后到达单元格 (7, 7) 。 示例 2： [https://assets.leetcode.com/uploads/2023/08/05/example1.svg] 输入：sx = 3, sy = 1, fx = 7, fy = 3, t = 3 输出：false 解释：从单元格 (3, 1) 开始出发，穿过上图标注的单元格，至少需要 4 秒后到达单元格 (7, 3) 。 因此，无法在 3 秒后到达单元格 (7, 3) 。 提示： * 1 <= sx, sy, fx, fy <= 109 * 0 <= t <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算起始点和目标点之间的曼哈顿距离来判断是否可以在给定时间内到达。

算法步骤:
1. 计算起始点 (sx, sy) 和目标点 (fx, fy) 之间的曼哈顿距离。
2. 如果曼哈顿距离小于等于 t，则可以到达目标点。
3. 特殊情况处理：如果起始点和目标点相同且 t 为 1，则不能在 1 秒内到达。

关键点:
- 曼哈顿距离可以用来判断是否可以在 t 秒内到达目标点。
- 考虑特殊情况，当起始点和目标点相同时，t 不能为 1。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_reachable_at_time(sx: int, sy: int, fx: int, fy: int, t: int) -> bool:
    """
    函数式接口 - 判断是否可以在给定时间内到达目标点
    """
    # 计算曼哈顿距离
    distance = max(abs(fx - sx), abs(fy - sy))
    
    # 特殊情况处理
    if distance == 0 and t == 1:
        return False
    
    # 判断是否可以在 t 秒内到达
    return distance <= t


Solution = create_solution(is_reachable_at_time)