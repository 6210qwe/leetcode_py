"""
3081. 替换字符串中的问号使分数最小 - 给你一个字符串 s 。s[i] 要么是小写英文字母，要么是问号 '?' 。 对于长度为 m 且 只 含有小写英文字母的字符串 t ，我们定义函数 cost(i) 为下标 i 之前（也就是范围 [0, i - 1] 中）出现过与 t[i] 相同 字符出现的次数。 字符串 t 的 分数 为所有下标 i 的 cost(i) 之 和 。 比方说，字符串 t = "aab" ： * cost(0) = 0 * cost(1) = 1 * cost(2) = 0 * 所以，字符串 "aab" 的分数为 0 + 1 + 0 = 1 。 你的任务是用小写英文字母 替换 s 中 所有 问号，使 s 的 分数最小 。 请你返回替换所有问号 '?' 之后且分数最小的字符串。如果有多个字符串的 分数最小 ，那么返回字典序最小的一个。 示例 1： 输入：s = "???" 输出： "abc" 解释：这个例子中，我们将 s 中的问号 '?' 替换得到 "abc" 。 对于字符串 "abc" ，cost(0) = 0 ，cost(1) = 0 和 cost(2) = 0 。 "abc" 的分数为 0 。 其他修改 s 得到分数 0 的字符串为 "cba" ，"abz" 和 "hey" 。 这些字符串中，我们返回字典序最小的。 示例 2： 输入： s = "a?a?" 输出： "abac" 解释：这个例子中，我们将 s 中的问号 '?' 替换得到 "abac" 。 对于字符串 "abac" ，cost(0) = 0 ，cost(1) = 0 ，cost(2) = 1 和 cost(3) = 0 。 "abac" 的分数为 1 。 提示： * 1 <= s.length <= 105 * s[i] 要么是小写英文字母，要么是 '?' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和优先队列来替换问号，使得替换后的字符串分数最小且字典序最小。

算法步骤:
1. 统计每个字符的出现次数。
2. 使用优先队列（最小堆）来存储每个字符及其出现次数。
3. 遍历字符串，对于每个问号，从优先队列中取出当前出现次数最少的字符进行替换，并更新其出现次数。
4. 将替换后的字符放回优先队列中。
5. 返回最终的字符串。

关键点:
- 使用优先队列来动态维护字符及其出现次数，确保每次替换时选择出现次数最少的字符。
- 通过优先队列保证替换后的字符串在字典序上最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是字符串的长度。插入和删除操作的时间复杂度为 O(log n)。
空间复杂度: O(1)，因为优先队列中最多只会存储 26 个字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
import heapq

def solution_function_name(s: str) -> str:
    """
    函数式接口 - 替换字符串中的问号使分数最小
    """
    # 统计每个字符的出现次数
    char_count = [0] * 26
    for char in s:
        if char != '?':
            char_count[ord(char) - ord('a')] += 1
    
    # 初始化优先队列
    min_heap = []
    for i in range(26):
        heapq.heappush(min_heap, (char_count[i], chr(i + ord('a'))))
    
    # 构建结果字符串
    result = list(s)
    for i in range(len(result)):
        if result[i] == '?':
            count, char = heapq.heappop(min_heap)
            result[i] = char
            heapq.heappush(min_heap, (count + 1, char))
    
    return ''.join(result)

Solution = create_solution(solution_function_name)