"""
核心思想: 通过观察可以发现，经过一系列操作后，所有可能的二进制字符串都可以通过将原字符串中的某个子串翻转得到。因此，最终的不同二进制字符串数量取决于原字符串中 '0' 和 '1' 的数量。

算法步骤:
1. 统计原字符串中 '0' 和 '1' 的数量。
2. 计算不同二进制字符串的数量，即为 C(n, k) 其中 n 为字符串长度，k 为 '1' 的数量。

关键点:
- 二进制字符串的翻转操作不会改变 '0' 和 '1' 的总数。
- 使用组合数公式计算不同二进制字符串的数量。
"""

