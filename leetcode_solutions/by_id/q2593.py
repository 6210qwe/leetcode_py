def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算应用操作后不同二进制字符串的数量
    """
    # 统计 '0' 和 '1' 的数量
    count_0 = s.count('0')
    count_1 = s.count('1')
    
    # 计算不同二进制字符串的数量
    from math import comb
    return comb(len(s), count_1)


