"""
1844. 将所有数字用字符替换 - 给你一个下标从 0 开始的字符串 s ，它的 偶数 下标处为小写英文字母，奇数 下标处为数字。 定义一个函数 shift(c, x) ，其中 c 是一个字符且 x 是一个数字，函数返回字母表中 c 后面第 x 个字符。 * 比方说，shift('a', 5) = 'f' 和 shift('x', 0) = 'x' 。 对于每个 奇数 下标 i ，你需要将数字 s[i] 用 shift(s[i-1], s[i]) 替换。 请你替换所有数字以后，将字符串 s 返回。题目 保证 shift(s[i-1], s[i]) 不会超过 'z' 。 示例 1： 输入：s = "a1c1e1" 输出："abcdef" 解释：数字被替换结果如下： - s[1] -> shift('a',1) = 'b' - s[3] -> shift('c',1) = 'd' - s[5] -> shift('e',1) = 'f' 示例 2： 输入：s = "a1b2c3d4e" 输出："abbdcfdhe" 解释：数字被替换结果如下： - s[1] -> shift('a',1) = 'b' - s[3] -> shift('b',2) = 'd' - s[5] -> shift('c',3) = 'f' - s[7] -> shift('d',4) = 'h' 提示： * 1 <= s.length <= 100 * s 只包含小写英文字母和数字。 * 对所有 奇数 下标处的 i ，满足 shift(s[i-1], s[i]) <= 'z' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串，将奇数下标的数字替换为对应的字符。

算法步骤:
1. 遍历字符串，对于每个奇数下标 i，计算 shift(s[i-1], s[i]) 的值。
2. 将计算得到的字符替换到原字符串中。
3. 返回处理后的字符串。

关键点:
- 使用 ASCII 码进行字符偏移计算。
- 保持字符串的偶数下标字符不变。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历一次字符串。
空间复杂度: O(n)，因为我们需要构建一个新的字符串来存储结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def replace_digits(s: str) -> str:
    """
    函数式接口 - 将字符串 s 中的奇数下标处的数字替换为对应的字符。
    """
    result = []
    for i in range(len(s)):
        if i % 2 == 0:
            result.append(s[i])
        else:
            # 计算 shift(s[i-1], s[i])
            shifted_char = chr(ord(s[i-1]) + int(s[i]))
            result.append(shifted_char)
    
    return ''.join(result)


Solution = create_solution(replace_digits)