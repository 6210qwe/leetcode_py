# 问题描述
# ============================================================================
"""
796. 旋转字符串 - 给定两个字符串, s 和 goal。如果在若干次旋转操作之后，s 能变成 goal ，那么返回 true 。 s 的 旋转操作 就是将 s 最左边的字符移动到最右边。 * 例如, 若 s = 'abcde'，在旋转一次之后结果就是'bcdea' 。 示例 1: 输入: s = "abcde", goal = "cdeab" 输出: true 示例 2: 输入: s = "abcde", goal = "abced" 输出: false 提示: * 1 <= s.length, goal.length <= 100 * s 和 goal 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过检查 s+s 是否包含 goal 来判断 s 是否可以通过旋转得到 goal。

算法步骤:
1. 如果 s 和 goal 的长度不同，直接返回 False。
2. 检查 s+s 是否包含 goal，如果包含则返回 True，否则返回 False。

关键点:
- 利用字符串拼接和子串查找来简化旋转操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 其中 n 是 s 的长度，因为字符串拼接和子串查找的时间复杂度都是 O(n)。
空间复杂度: O(n) - 因为需要创建一个新的字符串 s+s。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str, goal: str) -> bool:
    """
    函数式接口 - 判断 s 是否可以通过旋转得到 goal。
    """
    if len(s) != len(goal):
        return False
    return goal in (s + s)


Solution = create_solution(solution_function_name)