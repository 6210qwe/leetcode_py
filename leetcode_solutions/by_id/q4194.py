"""
3827. 统计单比特整数 - 给你一个整数 n。 如果一个整数的二进制表示中所有位都相同，则称其为 单比特数（Monobit）。 返回范围[0, n]（包括两端）内 单比特数 的个数。 示例 1： 输入： n = 1 输出： 2 解释： * 范围[0, 1]内的整数对应的二进制表示为"0"和"1"。 * 每个表示都由相同的位组成，因此答案是2。 示例 2： 输入： n = 4 输出： 3 解释： * 范围[0, 4]内的整数对应的二进制表示为"0"、"1"、"10"、"11"和"100"。 * 只有0、1和3满足单比特条件。因此答案是3。 提示： * 0 <= n <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算范围内的所有2的幂次和它们的补码来确定单比特数。

算法步骤:
1. 初始化结果变量 res 为 0。
2. 计算 n 的二进制长度 bits。
3. 对于每个 i 从 0 到 bits-1，检查 2^i 是否小于等于 n，并且 2^(i+1) - 1 是否大于 n。如果是，则将 res 加 1。
4. 返回 res + 1（因为 0 也是一个单比特数）。

关键点:
- 2^i 表示所有位为 1 的数。
- 2^(i+1) - 1 表示所有位为 0 的数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_monobit_integers(n: int) -> int:
    """
    函数式接口 - 统计单比特整数
    """
    res = 0
    bits = n.bit_length()
    
    for i in range(bits):
        if (1 << i) <= n and (1 << (i + 1)) - 1 > n:
            res += 1
    
    return res + 1  # 包括 0


