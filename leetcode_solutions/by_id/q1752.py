"""
1630. 等差子数组 - 如果一个数列由至少两个元素组成，且每两个连续元素之间的差值都相同，那么这个序列就是 等差数列 。更正式地，数列 s 是等差数列，只需要满足：对于每个有效的 i ， s[i+1] - s[i] == s[1] - s[0] 都成立。 例如，下面这些都是 等差数列 ： 1, 3, 5, 7, 9 7, 7, 7, 7 3, -1, -5, -9 下面的数列 不是等差数列 ： 1, 1, 2, 5, 7 给你一个由 n 个整数组成的数组 nums，和两个由 m 个整数组成的数组 l 和 r，后两个数组表示 m 组范围查询，其中第 i 个查询对应范围 [l[i], r[i]] 。所有数组的下标都是 从 0 开始 的。 返回 boolean 元素构成的答案列表 answer 。如果子数组 nums[l[i]], nums[l[i]+1], ... , nums[r[i]] 可以 重新排列 形成 等差数列 ，answer[i] 的值就是 true；否则answer[i] 的值就是 false 。 示例 1： 输入：nums = [4,6,5,9,3,7], l = [0,0,2], r = [2,3,5] 输出：[true,false,true] 解释： 第 0 个查询，对应子数组 [4,6,5] 。可以重新排列为等差数列 [6,5,4] 。 第 1 个查询，对应子数组 [4,6,5,9] 。无法重新排列形成等差数列。 第 2 个查询，对应子数组 [5,9,3,7] 。可以重新排列为等差数列 [3,5,7,9] 。 示例 2： 输入：nums = [-12,-9,-3,-12,-6,15,20,-25,-20,-15,-10], l = [0,1,6,4,8,7], r = [4,4,9,7,9,10] 输出：[false,true,false,false,true,true] 提示： * n == nums.length * m == l.length * m == r.length * 2 <= n <= 500 * 1 <= m <= 500 * 0 <= l[i] < r[i] < n * -105 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对于每个查询，提取子数组并检查其是否可以重新排列为等差数列。

算法步骤:
1. 遍历每个查询范围 [l[i], r[i]]。
2. 提取子数组并对其进行排序。
3. 检查排序后的子数组是否为等差数列。
4. 将结果添加到答案列表中。

关键点:
- 排序后的子数组可以通过检查相邻元素之间的差值是否相等来判断是否为等差数列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * k log k)，其中 m 是查询的数量，k 是子数组的最大长度。
空间复杂度: O(k)，用于存储排序后的子数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_arithmetic_subarrays(nums: List[int], l: List[int], r: List[int]) -> List[bool]:
    def is_arithmetic(arr: List[int]) -> bool:
        if len(arr) < 2:
            return False
        diff = arr[1] - arr[0]
        for i in range(2, len(arr)):
            if arr[i] - arr[i - 1] != diff:
                return False
        return True

    result = []
    for i in range(len(l)):
        subarray = sorted(nums[l[i]:r[i] + 1])
        result.append(is_arithmetic(subarray))
    return result


Solution = create_solution(check_arithmetic_subarrays)