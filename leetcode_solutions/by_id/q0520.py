"""
520. 检测大写字母 - 我们定义，在以下情况时，单词的大写用法是正确的： * 全部字母都是大写，比如 "USA" 。 * 所有字母都不是大写，比如 "leetcode" 。 * 只有首字母大写， 比如 "Google" 。 给你一个字符串 word 。如果大写用法正确，返回 true ；否则，返回 false 。 示例 1： 输入：word = "USA" 输出：true 示例 2： 输入：word = "FlaG" 输出：false 提示： * 1 <= word.length <= 100 * word 由小写和大写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 检查字符串是否符合三种大写规则之一。

算法步骤:
1. 检查字符串是否全部为大写。
2. 检查字符串是否全部为小写。
3. 检查字符串是否只有首字母大写，其余字母小写。

关键点:
- 使用 Python 的内置方法 `isupper` 和 `islower` 来检查字符的大小写。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们需要遍历字符串一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def detectCapitalUse(word: str) -> bool:
    """
    函数式接口 - 检测字符串是否符合大写规则
    """
    # 检查字符串是否全部为大写
    if word.isupper():
        return True
    # 检查字符串是否全部为小写
    if word.islower():
        return True
    # 检查字符串是否只有首字母大写，其余字母小写
    if word[0].isupper() and word[1:].islower():
        return True
    return False


Solution = create_solution(detectCapitalUse)