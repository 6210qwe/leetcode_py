"""
1219. 黄金矿工 - 你要开发一座金矿，地质勘测学家已经探明了这座金矿中的资源分布，并用大小为 m * n 的网格 grid 进行了标注。每个单元格中的整数就表示这一单元格中的黄金数量；如果该单元格是空的，那么就是 0。 为了使收益最大化，矿工需要按以下规则来开采黄金： * 每当矿工进入一个单元，就会收集该单元格中的所有黄金。 * 矿工每次可以从当前位置向上下左右四个方向走。 * 每个单元格只能被开采（进入）一次。 * 不得开采（进入）黄金数目为 0 的单元格。 * 矿工可以从网格中 任意一个 有黄金的单元格出发或者是停止。 示例 1： 输入：grid = [[0,6,0],[5,8,7],[0,9,0]] 输出：24 解释： [[0,6,0], [5,8,7], [0,9,0]] 一种收集最多黄金的路线是：9 -> 8 -> 7。 示例 2： 输入：grid = [[1,0,7],[2,0,6],[3,4,5],[0,3,0],[9,0,20]] 输出：28 解释： [[1,0,7], [2,0,6], [3,4,5], [0,3,0], [9,0,20]] 一种收集最多黄金的路线是：1 -> 2 -> 3 -> 4 -> 5 -> 6 -> 7。 提示： * 1 <= grid.length, grid[i].length <= 15 * 0 <= grid[i][j] <= 100 * 最多 25 个单元格中有黄金。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用回溯法从每个有黄金的单元格开始搜索，记录路径上的最大黄金数量。

算法步骤:
1. 初始化最大黄金数量为 0。
2. 遍历网格中的每个单元格，如果该单元格有黄金，则从该单元格开始进行深度优先搜索。
3. 在深度优先搜索过程中，标记当前单元格为已访问，并递归地探索其四个方向的相邻单元格。
4. 在递归返回时，取消标记当前单元格为已访问，以便其他路径可以使用该单元格。
5. 更新最大黄金数量。

关键点:
- 使用标记数组来避免重复访问同一个单元格。
- 递归地探索所有可能的路径，并记录路径上的最大黄金数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(4^k)，其中 k 是网格中黄金单元格的数量。每个单元格最多有 4 个方向可以走，最坏情况下需要遍历所有可能的路径。
空间复杂度: O(k)，递归调用栈的深度最多为 k。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def getMaximumGold(grid: List[List[int]]) -> int:
    def dfs(x: int, y: int, gold: int) -> None:
        nonlocal max_gold
        max_gold = max(max_gold, gold)
        for dx, dy in directions:
            nx, ny = x + dx, y + dy
            if 0 <= nx < m and 0 <= ny < n and not visited[nx][ny] and grid[nx][ny] > 0:
                visited[nx][ny] = True
                dfs(nx, ny, gold + grid[nx][ny])
                visited[nx][ny] = False

    m, n = len(grid), len(grid[0])
    directions = [(0, 1), (1, 0), (0, -1), (-1, 0)]
    max_gold = 0
    visited = [[False] * n for _ in range(m)]

    for i in range(m):
        for j in range(n):
            if grid[i][j] > 0:
                visited[i][j] = True
                dfs(i, j, grid[i][j])
                visited[i][j] = False

    return max_gold

