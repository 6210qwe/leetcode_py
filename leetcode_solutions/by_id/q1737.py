"""
1614. 括号的最大嵌套深度 - 给定 有效括号字符串 s，返回 s 的 嵌套深度。嵌套深度是嵌套括号的 最大 数量。 示例 1： 输入：s = "(1+(2*3)+((8)/4))+1" 输出：3 解释：数字 8 在嵌套的 3 层括号中。 示例 2： 输入：s = "(1)+((2))+(((3)))" 输出：3 解释：数字 3 在嵌套的 3 层括号中。 示例 3： 输入：s = "()(())((()()))" 输出：3 提示： * 1 <= s.length <= 100 * s 由数字 0-9 和字符 '+'、'-'、'*'、'/'、'('、')' 组成 * 题目数据保证括号字符串 s 是 有效的括号字符串
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一个计数器来记录当前的嵌套深度，并在遍历字符串时更新最大嵌套深度。

算法步骤:
1. 初始化两个变量 `current_depth` 和 `max_depth` 为 0。
2. 遍历字符串 s：
   - 如果遇到 '('，则 `current_depth` 加 1，并更新 `max_depth`。
   - 如果遇到 ')'，则 `current_depth` 减 1。
3. 返回 `max_depth`。

关键点:
- 通过遍历字符串一次，使用计数器来记录当前的嵌套深度，并在遇到 '(' 时更新最大嵌套深度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_depth(s: str) -> int:
    """
    函数式接口 - 计算给定字符串 s 的最大嵌套深度
    """
    current_depth = 0
    max_depth = 0
    for char in s:
        if char == '(':
            current_depth += 1
            max_depth = max(max_depth, current_depth)
        elif char == ')':
            current_depth -= 1
    return max_depth


