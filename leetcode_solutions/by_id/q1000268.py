"""
LCR 091. 粉刷房子 - 假如有一排房子，共 n 个，每个房子可以被粉刷成红色、蓝色或者绿色这三种颜色中的一种，你需要粉刷所有的房子并且使其相邻的两个房子颜色不能相同。 当然，因为市场上不同颜色油漆的价格不同，所以房子粉刷成不同颜色的花费成本也是不同的。每个房子粉刷成不同颜色的花费是以一个 n x 3 的正整数矩阵 costs 来表示的。 例如，costs[0][0] 表示第 0 号房子粉刷成红色的成本花费；costs[1][2] 表示第 1 号房子粉刷成绿色的花费，以此类推。 请计算出粉刷完所有房子最少的花费成本。 示例 1： 输入: costs = [[17,2,17],[16,16,5],[14,3,19]] 输出: 10 解释: 将 0 号房子粉刷成蓝色，1 号房子粉刷成绿色，2 号房子粉刷成蓝色。 最少花费: 2 + 5 + 3 = 10。 示例 2： 输入: costs = [[7,6,2]] 输出: 2 提示: * costs.length == n * costs[i].length == 3 * 1 <= n <= 100 * 1 <= costs[i][j] <= 20 注意：本题与主站 256 题相同：https://leetcode.cn/problems/paint-house/ [https://leetcode.cn/problems/paint-house/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 为粉刷到第 i 个房子且颜色为 j 时的最小花费。对于每个房子，我们可以选择三种颜色之一，但不能与前一个房子的颜色相同。

算法步骤:
1. 初始化 dp 数组，dp[0][j] 为第一个房子粉刷成颜色 j 的花费。
2. 对于每个房子 i (从 1 到 n-1)，更新 dp[i][j] 为 min(dp[i-1][k]) + costs[i][j]，其中 k 不等于 j。
3. 最后返回 dp[n-1] 中的最小值。

关键点:
- 动态规划的状态转移方程
- 通过在原数组上进行更新来优化空间复杂度
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)（使用原数组进行更新）
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(costs: List[List[int]]) -> int:
    """
    函数式接口 - 计算粉刷所有房子的最小花费
    """
    if not costs:
        return 0

    n = len(costs)

    # 动态规划状态转移
    for i in range(1, n):
        costs[i][0] += min(costs[i-1][1], costs[i-1][2])
        costs[i][1] += min(costs[i-1][0], costs[i-1][2])
        costs[i][2] += min(costs[i-1][0], costs[i-1][1])

    # 返回最后一个房子的最小花费
    return min(costs[-1])


Solution = create_solution(solution_function_name)