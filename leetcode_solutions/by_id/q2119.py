"""
2009. 使数组连续的最少操作数 - 给你一个整数数组 nums 。每一次操作中，你可以将 nums 中 任意 一个元素替换成 任意 整数。 如果 nums 满足以下条件，那么它是 连续的 ： * nums 中所有元素都是 互不相同 的。 * nums 中 最大 元素与 最小 元素的差等于 nums.length - 1 。 比方说，nums = [4, 2, 5, 3] 是 连续的 ，但是 nums = [1, 2, 3, 5, 6] 不是连续的 。 请你返回使 nums 连续 的 最少 操作次数。 示例 1： 输入：nums = [4,2,5,3] 输出：0 解释：nums 已经是连续的了。 示例 2： 输入：nums = [1,2,3,5,6] 输出：1 解释：一个可能的解是将最后一个元素变为 4 。 结果数组为 [1,2,3,5,4] ，是连续数组。 示例 3： 输入：nums = [1,10,100,1000] 输出：3 解释：一个可能的解是： - 将第二个元素变为 2 。 - 将第三个元素变为 3 。 - 将第四个元素变为 4 。 结果数组为 [1,2,3,4] ，是连续数组。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找和滑动窗口来找到最长的连续子数组，然后计算需要的操作次数。

算法步骤:
1. 去重并排序数组。
2. 使用滑动窗口找到最长的连续子数组。
3. 计算需要的操作次数。

关键点:
- 去重后的数组长度决定了最长连续子数组的长度。
- 使用二分查找来确定当前窗口内的最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def min_operations(nums: List[int]) -> int:
    """
    函数式接口 - 返回使 nums 连续的最少操作次数
    """
    # 去重并排序
    unique_nums = sorted(set(nums))
    
    # 使用滑动窗口找到最长的连续子数组
    n = len(nums)
    max_length = 0
    left = 0
    for right in range(len(unique_nums)):
        while unique_nums[right] - unique_nums[left] >= n:
            left += 1
        max_length = max(max_length, right - left + 1)
    
    # 计算需要的操作次数
    return n - max_length

