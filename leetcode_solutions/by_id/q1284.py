"""
1390. 四因数 - 给你一个整数数组 nums，请你返回该数组中恰有四个因数的这些整数的各因数之和。如果数组中不存在满足题意的整数，则返回 0 。 示例 1： 输入：nums = [21,4,7] 输出：32 解释： 21 有 4 个因数：1, 3, 7, 21 4 有 3 个因数：1, 2, 4 7 有 2 个因数：1, 7 答案仅为 21 的所有因数的和。 示例 2: 输入: nums = [21,21] 输出: 64 示例 3: 输入: nums = [1,2,3,4,5] 输出: 0 提示： * 1 <= nums.length <= 104 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对每个数找到其所有因数，并检查是否恰好有四个因数。如果有，计算这些因数的和。

算法步骤:
1. 定义一个辅助函数 `get_divisors` 来获取一个数的所有因数。
2. 遍历数组中的每个数，使用 `get_divisors` 获取其因数。
3. 检查因数的数量是否为4，如果是，计算这些因数的和并累加到结果中。

关键点:
- 使用平方根优化因数查找过程，避免重复计算。
- 只考虑小于等于平方根的因数，以减少计算量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * sqrt(m))，其中 n 是数组长度，m 是数组中的最大值。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def get_divisors(num: int) -> List[int]:
    """获取一个数的所有因数"""
    divisors = set()
    for i in range(1, int(num**0.5) + 1):
        if num % i == 0:
            divisors.add(i)
            divisors.add(num // i)
    return list(divisors)


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 返回数组中恰有四个因数的这些整数的各因数之和
    """
    result = 0
    for num in nums:
        divisors = get_divisors(num)
        if len(divisors) == 4:
            result += sum(divisors)
    return result


Solution = create_solution(solution_function_name)