def solution_function_name():
    """
    函数式接口 - 实现 SQL 查询
    """
    # SQL 查询实现
    query = """
    SELECT country,
           COUNT(DISTINCT weather_type) AS num_distinct_weather_types
    FROM Weather
    GROUP BY country;
    """
    return query


