from typing import List

def is_valid(nums: List[int], k: int, mid: int) -> bool:
    count = 0
    current_sum = 0
    for num in nums:
        current_sum += num
        if current_sum >= mid:
            count += 1
            current_sum = 0
    return count > k

def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    n = len(nums)
    prefix_sum = [0] * (n + 1)
    for i in range(n):
        prefix_sum[i + 1] = prefix_sum[i] + nums[i]
    
    left, right = min(nums), sum(nums) // (k + 1)
    while left < right:
        mid = (left + right + 1) // 2
        if is_valid(prefix_sum, k, mid):
            left = mid
        else:
            right = mid - 1
    return left

