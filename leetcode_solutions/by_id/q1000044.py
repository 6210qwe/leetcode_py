"""
面试题 17.19. 消失的两个数字 - 给定一个数组，包含从 1 到 N 所有的整数，但其中缺了两个数字。你能在 O(N) 时间内只用 O(1) 的空间找到它们吗？ 以任意顺序返回这两个数字均可。 示例 1： 输入：[1] 输出：[2,3] 示例 2： 输入：[2,3] 输出：[1,4] 提示： * nums.length <= 30000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算来找到缺失的两个数字。

算法步骤:
1. 计算所有数字的异或结果。
2. 找到异或结果中的任意一个为1的位，将数组分为两组。
3. 分别计算两组的异或结果，得到缺失的两个数字。

关键点:
- 通过异或操作找到两个缺失数字的不同位。
- 通过分组异或操作找到具体的缺失数字。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(N)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> List[int]:
    """
    函数式接口 - 使用位运算找到缺失的两个数字
    """
    # 计算所有数字的异或结果
    xor = 0
    n = len(nums) + 2
    for num in nums:
        xor ^= num
    for i in range(1, n + 1):
        xor ^= i

    # 找到异或结果中的任意一个为1的位
    diff_bit = xor & -xor

    # 分别计算两组的异或结果
    num1, num2 = 0, 0
    for num in nums:
        if num & diff_bit:
            num1 ^= num
        else:
            num2 ^= num
    for i in range(1, n + 1):
        if i & diff_bit:
            num1 ^= i
        else:
            num2 ^= i

    return [num1, num2]


Solution = create_solution(solution_function_name)