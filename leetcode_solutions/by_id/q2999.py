"""
2839. 判断通过操作能否让字符串相等 I - 给你两个字符串 s1 和 s2 ，两个字符串的长度都为 4 ，且只包含 小写 英文字母。 你可以对两个字符串中的 任意一个 执行以下操作 任意 次： * 选择两个下标 i 和 j 且满足 j - i = 2 ，然后 交换 这个字符串中两个下标对应的字符。 如果你可以让字符串 s1 和 s2 相等，那么返回 true ，否则返回 false 。 示例 1： 输入：s1 = "abcd", s2 = "cdab" 输出：true 解释： 我们可以对 s1 执行以下操作： - 选择下标 i = 0 ，j = 2 ，得到字符串 s1 = "cbad" 。 - 选择下标 i = 1 ，j = 3 ，得到字符串 s1 = "cdab" = s2 。 示例 2： 输入：s1 = "abcd", s2 = "dacb" 输出：false 解释：无法让两个字符串相等。 提示： * s1.length == s2.length == 4 * s1 和 s2 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过比较特定位置的字符是否相同来判断两个字符串是否可以通过交换操作变得相等。

算法步骤:
1. 比较 s1 和 s2 在下标 0 和 2 的字符是否相同。
2. 比较 s1 和 s2 在下标 1 和 3 的字符是否相同。
3. 如果两组字符都相同，则返回 True，否则返回 False。

关键点:
- 由于只能交换距离为 2 的字符，因此只需要检查这两组字符是否相同。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 固定的比较次数
空间复杂度: O(1) - 不需要额外的空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_be_equal(s1: str, s2: str) -> bool:
    """
    函数式接口 - 判断通过操作能否让字符串相等 I
    """
    # 比较 s1 和 s2 在下标 0 和 2 的字符是否相同
    if s1[0] != s2[0] or s1[2] != s2[2]:
        return False
    # 比较 s1 和 s2 在下标 1 和 3 的字符是否相同
    if s1[1] != s2[1] or s1[3] != s2[3]:
        return False
    return True


Solution = create_solution(can_be_equal)