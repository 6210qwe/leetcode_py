"""
2629. 复合函数 - 请你编写一个函数，它接收一个函数数组 [f1, f2, f3，…， fn] ，并返回一个新的函数 fn ，它是函数数组的 复合函数 。 [f(x)， g(x)， h(x)] 的 复合函数 为 fn(x) = f(g(h(x))) 。 一个空函数列表的 复合函数 是 恒等函数 f(x) = x 。 你可以假设数组中的每个函数接受一个整型参数作为输入，并返回一个整型作为输出。 示例 1： 输入：functions = [x => x + 1, x => x * x, x => 2 * x], x = 4 输出：65 解释： 从右向左计算...... Starting with x = 4. 2 * (4) = 8 (8) * (8) = 64 (64) + 1 = 65 示例 2： 输入：functions = [x => 10 * x, x => 10 * x, x => 10 * x], x = 1 输出：1000 解释： 从右向左计算...... 10 * (1) = 10 10 * (10) = 100 10 * (100) = 1000 示例 3： 输入：functions = [], x = 42 输出：42 解释： 空函数列表的复合函数就是恒等函数 提示： * -1000 <= x <= 1000 * 0 <= functions.length <= 1000 * 所有函数都接受并返回一个整型
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从右向左依次应用函数数组中的每个函数。

算法步骤:
1. 从右向左遍历函数数组。
2. 对于每个函数，将其应用于当前的结果。
3. 返回最终结果。

关键点:
- 使用 `reduce` 函数从右向左应用函数数组中的每个函数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是函数数组的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from functools import reduce

def compose(functions: List[callable]) -> callable:
    """
    返回一个复合函数，该函数是从右向左应用函数数组中的每个函数。
    """
    def composed_function(x):
        return reduce(lambda acc, f: f(acc), reversed(functions), x)
    return composed_function

Solution = compose

# 示例测试
if __name__ == "__main__":
    # 示例 1
    functions1 = [lambda x: x + 1, lambda x: x * x, lambda x: 2 * x]
    composed_func1 = compose(functions1)
    print(composed_func1(4))  # 输出: 65

    # 示例 2
    functions2 = [lambda x: 10 * x, lambda x: 10 * x, lambda x: 10 * x]
    composed_func2 = compose(functions2)
    print(composed_func2(1))  # 输出: 1000

    # 示例 3
    functions3 = []
    composed_func3 = compose(functions3)
    print(composed_func3(42))  # 输出: 42