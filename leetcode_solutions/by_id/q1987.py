"""
1876. 长度为三且各字符不同的子字符串 - 如果一个字符串不含有任何重复字符，我们称这个字符串为 好 字符串。 给你一个字符串 s ，请你返回 s 中长度为 3 的 好子字符串 的数量。 注意，如果相同的好子字符串出现多次，每一次都应该被记入答案之中。 子字符串 是一个字符串中连续的字符序列。 示例 1： 输入：s = "xyzzaz" 输出：1 解释：总共有 4 个长度为 3 的子字符串："xyz"，"yzz"，"zza" 和 "zaz" 。 唯一的长度为 3 的好子字符串是 "xyz" 。 示例 2： 输入：s = "aababcabc" 输出：4 解释：总共有 7 个长度为 3 的子字符串："aab"，"aba"，"bab"，"abc"，"bca"，"cab" 和 "abc" 。 好子字符串包括 "abc"，"bca"，"cab" 和 "abc" 。 提示： * 1 <= s.length <= 100 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来检查每个长度为3的子字符串是否包含不同字符。

算法步骤:
1. 初始化计数器 `count` 为0。
2. 遍历字符串 `s`，对于每个长度为3的子字符串，检查其是否包含不同字符。
3. 如果包含不同字符，则计数器 `count` 加1。
4. 返回计数器 `count`。

关键点:
- 使用滑动窗口技术，每次只检查当前子字符串的三个字符。
- 通过集合来判断三个字符是否不同。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们需要遍历整个字符串一次。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_good_substrings(s: str) -> int:
    """
    函数式接口 - 计算字符串 s 中长度为 3 的好子字符串的数量
    """
    count = 0
    for i in range(len(s) - 2):
        if len(set(s[i:i+3])) == 3:
            count += 1
    return count


Solution = create_solution(count_good_substrings)