"""
2520. 统计能整除数字的位数 - 给你一个整数 num ，返回 num 中能整除 num 的数位的数目。 如果满足 nums % val == 0 ，则认为整数 val 可以整除 nums 。 示例 1： 输入：num = 7 输出：1 解释：7 被自己整除，因此答案是 1 。 示例 2： 输入：num = 121 输出：2 解释：121 可以被 1 整除，但无法被 2 整除。由于 1 出现两次，所以返回 2 。 示例 3： 输入：num = 1248 输出：4 解释：1248 可以被它每一位上的数字整除，因此答案是 4 。 提示： * 1 <= num <= 109 * num 的数位中不含 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历 num 的每一位，检查该位是否能整除 num。

算法步骤:
1. 初始化计数器 count 为 0。
2. 将 num 转换为字符串形式，以便逐位处理。
3. 遍历 num 的每一位，将其转换为整数并检查是否能整除 num。
4. 如果能整除，则增加计数器 count。
5. 返回计数器 count。

关键点:
- 使用字符串遍历来逐位处理 num。
- 检查每一位是否能整除 num。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d)，其中 d 是 num 的位数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_digits(num: int) -> int:
    """
    函数式接口 - 统计能整除数字的位数
    """
    count = 0
    for digit in str(num):
        if num % int(digit) == 0:
            count += 1
    return count


Solution = create_solution(count_digits)