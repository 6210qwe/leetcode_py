"""
2434. 使用机器人打印字典序最小的字符串 - 给你一个字符串 s 和一个机器人，机器人当前有一个空字符串 t 。执行以下操作之一，直到 s 和 t 都变成空字符串： * 删除字符串 s 的 第一个 字符，并将该字符给机器人。机器人把这个字符添加到 t 的尾部。 * 删除字符串 t 的 最后一个 字符，并将该字符给机器人。机器人将该字符写到纸上。 请你返回纸上能写出的字典序最小的字符串。 示例 1： 输入：s = "zza" 输出："azz" 解释：用 p 表示写出来的字符串。 一开始，p="" ，s="zza" ，t="" 。 执行第一个操作三次，得到 p="" ，s="" ，t="zza" 。 执行第二个操作三次，得到 p="azz" ，s="" ，t="" 。 示例 2： 输入：s = "bac" 输出："abc" 解释：用 p 表示写出来的字符串。 执行第一个操作两次，得到 p="" ，s="c" ，t="ba" 。 执行第二个操作两次，得到 p="ab" ，s="c" ，t="" 。 执行第一个操作，得到 p="ab" ，s="" ，t="c" 。 执行第二个操作，得到 p="abc" ，s="" ，t="" 。 示例 3： 输入：s = "bdda" 输出："addb" 解释：用 p 表示写出来的字符串。 一开始，p="" ，s="bdda" ，t="" 。 执行第一个操作四次，得到 p="" ，s="" ，t="bdda" 。 执行第二个操作四次，得到 p="addb" ，s="" ，t="" 。 提示： * 1 <= s.length <= 105 * s 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和栈来确保每次输出的字符都是当前可以输出的最小字符。

算法步骤:
1. 初始化一个栈 `t` 和结果字符串 `p`。
2. 遍历字符串 `s`，对于每个字符：
   - 如果当前字符比栈顶字符小，则将栈顶字符弹出并加入结果字符串 `p`，直到栈为空或栈顶字符不大于当前字符。
   - 将当前字符压入栈 `t`。
3. 最后，将栈 `t` 中剩余的字符依次弹出并加入结果字符串 `p`。

关键点:
- 使用栈来存储暂时不能输出的字符。
- 贪心地选择当前可以输出的最小字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。每个字符最多只会被压入和弹出栈一次。
空间复杂度: O(n)，最坏情况下栈中会存储所有字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def robot_print(s: str) -> str:
    """
    函数式接口 - 使用机器人打印字典序最小的字符串
    """
    t = []  # 用于存储暂时不能输出的字符
    p = []  # 用于存储最终输出的字符
    for char in s:
        while t and t[-1] > char:
            p.append(t.pop())
        t.append(char)
    
    # 将栈中剩余的字符依次弹出并加入结果字符串
    while t:
        p.append(t.pop())
    
    return ''.join(p)


Solution = create_solution(robot_print)