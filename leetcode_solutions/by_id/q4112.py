"""
3745. 三元素表达式的最大值 - 给你一个整数数组 nums。 从 nums 中选择三个元素 a、b 和 c，它们的下标需 互不相同 ，使表达式 a + b - c 的值最大化。 返回该表达式可能的 最大值 。 示例 1： 输入： nums = [1,4,2,5] 输出： 8 解释： 可以选择 a = 4，b = 5，c = 1。表达式的值为 4 + 5 - 1 = 8，这是可能的最大值。 示例 2： 输入： nums = [-2,0,5,-2,4] 输出： 11 解释： 可以选择 a = 5，b = 4，c = -2。表达式的值为 5 + 4 - (-2) = 11，这是可能的最大值。 提示： * 3 <= nums.length <= 100 * -100 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 为了最大化 a + b - c，我们需要找到最大的两个数作为 a 和 b，并且找到最小的一个数作为 c。

算法步骤:
1. 对数组进行排序。
2. 选择最大的两个数作为 a 和 b。
3. 选择最小的一个数作为 c。
4. 计算 a + b - c 并返回结果。

关键点:
- 通过排序，我们可以轻松找到最大的两个数和最小的一个数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现
    """
    # 对数组进行排序
    nums.sort()
    
    # 选择最大的两个数作为 a 和 b
    a = nums[-1]
    b = nums[-2]
    
    # 选择最小的一个数作为 c
    c = nums[0]
    
    # 计算 a + b - c 并返回结果
    return a + b - c


Solution = create_solution(solution_function_name)