"""
3029. 将单词恢复初始状态所需的最短时间 I - 给你一个下标从 0 开始的字符串 word 和一个整数 k 。 在每一秒，你必须执行以下操作： * 移除 word 的前 k 个字符。 * 在 word 的末尾添加 k 个任意字符。 注意 添加的字符不必和移除的字符相同。但是，必须在每一秒钟都执行 两种 操作。 返回将 word 恢复到其 初始 状态所需的 最短 时间（该时间必须大于零）。 示例 1： 输入：word = "abacaba", k = 3 输出：2 解释： 第 1 秒，移除 word 的前缀 "aba"，并在末尾添加 "bac" 。因此，word 变为 "cababac"。 第 2 秒，移除 word 的前缀 "cab"，并在末尾添加 "aba" 。因此，word 变为 "abacaba" 并恢复到始状态。 可以证明，2 秒是 word 恢复到其初始状态所需的最短时间。 示例 2： 输入：word = "abacaba", k = 4 输出：1 解释： 第 1 秒，移除 word 的前缀 "abac"，并在末尾添加 "caba" 。因此，word 变为 "abacaba" 并恢复到初始状态。 可以证明，1 秒是 word 恢复到其初始状态所需的最短时间。 示例 3： 输入：word = "abcbabcd", k = 2 输出：4 解释： 每一秒，我们都移除 word 的前 2 个字符，并在 word 末尾添加相同的字符。 4 秒后，word 变为 "abcbabcd" 并恢复到初始状态。 可以证明，4 秒是 word 恢复到其初始状态所需的最短时间。 提示： * 1 <= word.length <= 50 * 1 <= k <= word.length * word仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过检查每隔 k 个字符的子串是否与原字符串的前缀匹配来确定最小时间。

算法步骤:
1. 初始化时间 t 为 1。
2. 在每次迭代中，检查 word 的前缀是否与从当前索引开始的长度为 n - k * t 的子串匹配。
3. 如果匹配，则返回当前时间 t。
4. 否则，增加时间 t 并继续检查。

关键点:
- 使用切片操作来获取子串并进行比较。
- 循环直到找到匹配或遍历完所有可能的时间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n/k)，其中 n 是 word 的长度。每次迭代最多需要 O(n) 的时间来比较子串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_time_to_revert_word(word: str, k: int) -> int:
    """
    函数式接口 - 计算将 word 恢复到初始状态所需的最短时间
    """
    n = len(word)
    t = 1
    while t * k < n:
        if word[:n - t * k] == word[t * k:]:
            return t
        t += 1
    return (n + k - 1) // k  # 如果没有找到匹配，返回完整的循环次数


Solution = create_solution(minimum_time_to_revert_word)