"""
627. 变更性别 - Salary 表： +-------------+----------+ | Column Name | Type | +-------------+----------+ | id | int | | name | varchar | | sex | ENUM | | salary | int | +-------------+----------+ id 是这个表的主键（具有唯一值的列）。 sex 这一列的值是 ENUM 类型，只能从 ('m', 'f') 中取。 本表包含公司雇员的信息。 请你编写一个解决方案来交换所有的 'f' 和 'm' （即，将所有 'f' 变为 'm' ，反之亦然），仅使用 单个 update 语句 ，且不产生中间临时表。 注意，你必须仅使用一条 update 语句，且 不能 使用 select 语句。 结果如下例所示。 示例 1: 输入： Salary 表： +----+------+-----+--------+ | id | name | sex | salary | +----+------+-----+--------+ | 1 | A | m | 2500 | | 2 | B | f | 1500 | | 3 | C | m | 5500 | | 4 | D | f | 500 | +----+------+-----+--------+ 输出： +----+------+-----+--------+ | id | name | sex | salary | +----+------+-----+--------+ | 1 | A | f | 2500 | | 2 | B | m | 1500 | | 3 | C | f | 5500 | | 4 | D | m | 500 | +----+------+-----+--------+ 解释： (1, A) 和 (3, C) 从 'm' 变为 'f' 。 (2, B) 和 (4, D) 从 'f' 变为 'm' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 CASE WHEN 语句在单个 UPDATE 语句中实现性别交换。

算法步骤:
1. 使用 CASE WHEN 语句检查每一行的 sex 列值。
2. 如果 sex 为 'm'，则将其更新为 'f'。
3. 如果 sex 为 'f'，则将其更新为 'm'。
4. 执行 UPDATE 语句以应用更改。

关键点:
- 使用 CASE WHEN 语句可以在单个 SQL 语句中完成条件判断和更新操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 Salary 表中的行数。每个行都需要进行一次更新操作。
空间复杂度: O(1)，因为我们在原表上进行更新，没有使用额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(params):
    """
    函数式接口 - 实现 SQL 语句来交换性别
    """
    # 最优解法
    sql = """
    UPDATE Salary
    SET sex = CASE
        WHEN sex = 'm' THEN 'f'
        WHEN sex = 'f' THEN 'm'
    END;
    """
    return sql


Solution = create_solution(solution_function_name)