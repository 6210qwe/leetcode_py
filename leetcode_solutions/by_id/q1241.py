"""
1313. 解压缩编码列表 - 给你一个以行程长度编码压缩的整数列表 nums 。 考虑每对相邻的两个元素 [freq, val] = [nums[2*i], nums[2*i+1]] （其中 i >= 0 ），每一对都表示解压后子列表中有 freq 个值为 val 的元素，你需要从左到右连接所有子列表以生成解压后的列表。 请你返回解压后的列表。 示例 1： 输入：nums = [1,2,3,4] 输出：[2,4,4,4] 解释：第一对 [1,2] 代表着 2 的出现频次为 1，所以生成数组 [2]。 第二对 [3,4] 代表着 4 的出现频次为 3，所以生成数组 [4,4,4]。 最后将它们串联到一起 [2] + [4,4,4] = [2,4,4,4]。 示例 2： 输入：nums = [1,1,2,3] 输出：[1,3,3] 提示： * 2 <= nums.length <= 100 * nums.length % 2 == 0 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用列表推导式来生成解压后的列表。

算法步骤:
1. 遍历 `nums` 列表，每次取两个元素 `[freq, val]`。
2. 对于每一对 `[freq, val]`，生成 `freq` 个 `val` 的列表。
3. 将所有生成的子列表合并成一个最终的解压列表。

关键点:
- 使用列表推导式可以简洁地实现上述步骤。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是解压后的列表长度。
空间复杂度: O(n)，存储解压后的列表需要 O(n) 的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def decompressRLElist(nums: List[int]) -> List[int]:
    """
    函数式接口 - 解压缩行程长度编码列表
    """
    # 使用列表推导式生成解压后的列表
    return [val for freq, val in zip(nums[::2], nums[1::2]) for _ in range(freq)]


Solution = create_solution(decompressRLElist)