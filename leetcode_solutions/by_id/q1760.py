"""
1640. 能否连接形成数组 - 给你一个整数数组 arr ，数组中的每个整数 互不相同 。另有一个由整数数组构成的数组 pieces，其中的整数也 互不相同 。请你以 任意顺序 连接 pieces 中的数组以形成 arr 。但是，不允许 对每个数组 pieces[i] 中的整数重新排序。 如果可以连接 pieces 中的数组形成 arr ，返回 true ；否则，返回 false 。 示例 1： 输入：arr = [15,88], pieces = [[88],[15]] 输出：true 解释：依次连接 [15] 和 [88] 示例 2： 输入：arr = [49,18,16], pieces = [[16,18,49]] 输出：false 解释：即便数字相符，也不能重新排列 pieces[0] 示例 3： 输入：arr = [91,4,64,78], pieces = [[78],[4,64],[91]] 输出：true 解释：依次连接 [91]、[4,64] 和 [78] 提示： * 1 <= pieces.length <= arr.length <= 100 * sum(pieces[i].length) == arr.length * 1 <= pieces[i].length <= arr.length * 1 <= arr[i], pieces[i][j] <= 100 * arr 中的整数 互不相同 * pieces 中的整数 互不相同（也就是说，如果将 pieces 扁平化成一维数组，数组中的所有整数互不相同）
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个子数组的起始元素及其在 pieces 中的位置，然后遍历 arr 检查是否能按顺序匹配。

算法步骤:
1. 创建一个哈希表，键为 pieces 中每个子数组的第一个元素，值为该子数组。
2. 遍历 arr，对于每个元素，检查其是否在哈希表中：
   - 如果在哈希表中，获取对应的子数组并检查该子数组是否与 arr 中的相应部分匹配。
   - 如果匹配失败，返回 False。
3. 如果遍历完 arr 且所有子数组都匹配成功，返回 True。

关键点:
- 使用哈希表快速查找 pieces 中的子数组。
- 逐个检查子数组是否与 arr 中的相应部分匹配。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 arr 的长度。我们只需遍历 arr 一次，并在常数时间内查找和比较子数组。
空间复杂度: O(m)，其中 m 是 pieces 的长度。我们需要存储 pieces 中的每个子数组的起始元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def canFormArray(arr: List[int], pieces: List[List[int]]) -> bool:
    """
    判断是否可以通过连接 pieces 中的数组来形成 arr。
    
    :param arr: 目标数组
    :param pieces: 子数组列表
    :return: 是否可以连接形成 arr
    """
    # 创建哈希表，键为子数组的第一个元素，值为子数组
    piece_map = {piece[0]: piece for piece in pieces}
    
    i = 0
    while i < len(arr):
        if arr[i] not in piece_map:
            return False
        piece = piece_map[arr[i]]
        for num in piece:
            if i >= len(arr) or arr[i] != num:
                return False
            i += 1
    
    return True

