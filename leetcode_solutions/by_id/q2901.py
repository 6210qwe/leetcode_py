"""
核心思想: 使用动态规划来计算阶乘，避免重复计算。

算法步骤:
1. 初始化一个列表 `factorials`，用于存储从 0 到 n 的阶乘值。
2. 计算并存储每个数的阶乘值。
3. 返回 `factorials` 列表。

关键点:
- 使用动态规划来存储已经计算过的阶乘值，以减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

