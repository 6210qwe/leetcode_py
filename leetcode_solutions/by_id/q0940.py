"""
904. 水果成篮 - 你正在探访一家农场，农场从左到右种植了一排果树。这些树用一个整数数组 fruits 表示，其中 fruits[i] 是第 i 棵树上的水果 种类 。 你想要尽可能多地收集水果。然而，农场的主人设定了一些严格的规矩，你必须按照要求采摘水果： * 你只有 两个 篮子，并且每个篮子只能装 单一类型 的水果。每个篮子能够装的水果总量没有限制。 * 你可以选择任意一棵树开始采摘，你必须从 每棵 树（包括开始采摘的树）上 恰好摘一个水果 。采摘的水果应当符合篮子中的水果类型。每采摘一次，你将会向右移动到下一棵树，并继续采摘。 * 一旦你走到某棵树前，但水果不符合篮子的水果类型，那么就必须停止采摘。 给你一个整数数组 fruits ，返回你可以收集的水果的 最大 数目。 示例 1： 输入：fruits = [1,2,1] 输出：3 解释：可以采摘全部 3 棵树。 示例 2： 输入：fruits = [0,1,2,2] 输出：3 解释：可以采摘 [1,2,2] 这三棵树。 如果从第一棵树开始采摘，则只能采摘 [0,1] 这两棵树。 示例 3： 输入：fruits = [1,2,3,2,2] 输出：4 解释：可以采摘 [2,3,2,2] 这四棵树。 如果从第一棵树开始采摘，则只能采摘 [1,2] 这两棵树。 示例 4： 输入：fruits = [3,3,3,1,2,1,1,2,3,3,4] 输出：5 解释：可以采摘 [1,2,1,1,2] 这五棵树。 提示： * 1 <= fruits.length <= 105 * 0 <= fruits[i] < fruits.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来维护当前可以采摘的水果种类不超过两种。

算法步骤:
1. 初始化两个指针 left 和 right，分别表示滑动窗口的左右边界。
2. 使用一个字典 basket 来记录当前窗口内的水果种类及其数量。
3. 移动右指针 right，扩展窗口，并更新 basket。
4. 当 basket 中的水果种类超过两种时，移动左指针 left，缩小窗口，并更新 basket。
5. 记录窗口的最大长度。

关键点:
- 使用滑动窗口来维护当前窗口内的水果种类不超过两种。
- 通过字典来记录当前窗口内的水果种类及其数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 fruits 的长度。每个元素最多被访问两次（一次由右指针，一次由左指针）。
空间复杂度: O(1)，因为 basket 中最多只会存储三种水果类型。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(fruits: List[int]) -> int:
    """
    函数式接口 - 返回可以收集的水果的最大数目
    """
    if not fruits:
        return 0

    left, right = 0, 0
    max_fruits = 0
    basket = {}

    while right < len(fruits):
        # 将当前水果加入篮子
        basket[fruits[right]] = basket.get(fruits[right], 0) + 1

        # 如果篮子中的水果种类超过两种，移动左指针
        while len(basket) > 2:
            basket[fruits[left]] -= 1
            if basket[fruits[left]] == 0:
                del basket[fruits[left]]
            left += 1

        # 更新最大水果数目
        max_fruits = max(max_fruits, right - left + 1)
        right += 1

    return max_fruits


Solution = create_solution(solution_function_name)