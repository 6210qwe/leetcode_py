"""
2678. 老人的数目 - 给你一个下标从 0 开始的字符串 details 。details 中每个元素都是一位乘客的信息，信息用长度为 15 的字符串表示，表示方式如下： * 前十个字符是乘客的手机号码。 * 接下来的一个字符是乘客的性别。 * 接下来两个字符是乘客的年龄。 * 最后两个字符是乘客的座位号。 请你返回乘客中年龄 严格大于 60 岁 的人数。 示例 1： 输入：details = ["7868190130M7522","5303914400F9211","9273338290F4010"] 输出：2 解释：下标为 0 ，1 和 2 的乘客年龄分别为 75 ，92 和 40 。所以有 2 人年龄大于 60 岁。 示例 2： 输入：details = ["1313579440F2036","2921522980M5644"] 输出：0 解释：没有乘客的年龄大于 60 岁。 提示： * 1 <= details.length <= 100 * details[i].length == 15 * details[i] 中的数字只包含 '0' 到 '9' 。 * details[i][10] 是 'M' ，'F' 或者 'O' 之一。 * 所有乘客的手机号码和座位号互不相同。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历每个乘客的信息字符串，提取年龄并判断是否大于60岁。

算法步骤:
1. 初始化计数器 `count` 为 0。
2. 遍历 `details` 列表中的每个字符串。
3. 提取每个字符串的第11和第12个字符（即年龄）。
4. 将提取的年龄字符转换为整数，并判断是否大于60岁。
5. 如果年龄大于60岁，则将计数器 `count` 加1。
6. 返回计数器 `count` 的值。

关键点:
- 使用切片操作提取年龄部分。
- 使用 `int` 函数将字符串转换为整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 `details` 列表的长度。我们需要遍历整个列表一次。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_senior_citizens(details: List[str]) -> int:
    """
    计算年龄严格大于60岁的乘客数量。
    """
    count = 0
    for detail in details:
        age = int(detail[11:13])
        if age > 60:
            count += 1
    return count


Solution = create_solution(count_senior_citizens)