"""
2439. 最小化数组中的最大值 - 给你一个下标从 0 开始的数组 nums ，它含有 n 个非负整数。 每一步操作中，你需要： * 选择一个满足 1 <= i < n 的整数 i ，且 nums[i] > 0 。 * 将 nums[i] 减 1 。 * 将 nums[i - 1] 加 1 。 你可以对数组执行 任意 次上述操作，请你返回可以得到的 nums 数组中 最大值 最小 为多少。 示例 1： 输入：nums = [3,7,1,6] 输出：5 解释： 一串最优操作是： 1. 选择 i = 1 ，nums 变为 [4,6,1,6] 。 2. 选择 i = 3 ，nums 变为 [4,6,2,5] 。 3. 选择 i = 1 ，nums 变为 [5,5,2,5] 。 nums 中最大值为 5 。无法得到比 5 更小的最大值。 所以我们返回 5 。 示例 2： 输入：nums = [10,1] 输出：10 解释： 最优解是不改动 nums ，10 是最大值，所以返回 10 。 提示： * n == nums.length * 2 <= n <= 105 * 0 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找来找到最小的最大值。通过二分查找确定一个目标值，然后检查是否可以通过操作使得数组中的最大值不超过这个目标值。

算法步骤:
1. 初始化二分查找的左右边界 left 和 right。
2. 在每次迭代中，计算中间值 mid。
3. 检查是否可以通过操作使得数组中的最大值不超过 mid。
4. 如果可以，则更新右边界 right；否则，更新左边界 left。
5. 最终返回 left 作为结果。

关键点:
- 使用二分查找来缩小可能的最大值范围。
- 检查是否可以通过操作使得数组中的最大值不超过当前中间值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log(max(nums)))，其中 n 是数组的长度，max(nums) 是数组中的最大值。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimize_array_value(nums: List[int]) -> int:
    """
    函数式接口 - 使用二分查找来找到最小的最大值
    """
    def can_make_max_less_equal(mid: int) -> bool:
        carry = 0
        for num in reversed(nums):
            if num + carry > mid:
                carry = (num + carry) - mid
            else:
                carry = 0
        return carry == 0

    left, right = 0, max(nums)
    while left < right:
        mid = (left + right) // 2
        if can_make_max_less_equal(mid):
            right = mid
        else:
            left = mid + 1
    return left


Solution = create_solution(minimize_array_value)