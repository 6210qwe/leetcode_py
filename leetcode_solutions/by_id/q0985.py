"""
948. 令牌放置 - 你的初始 能量 为 power，初始 分数 为 0，只有一包令牌以整数数组 tokens 给出。其中 tokens[i] 是第 i 个令牌的值（下标从 0 开始）。 你的目标是通过有策略地使用这些令牌以 最大化 总 分数。在一次行动中，你可以用两种方式中的一种来使用一个 未被使用的 令牌（但不是对同一个令牌使用两种方式）： * 朝上：如果你当前 至少 有 tokens[i] 点 能量 ，可以使用令牌 i ，失去 tokens[i] 点 能量 ，并得到 1 分 。 * 朝下：如果你当前至少有 1 分 ，可以使用令牌 i ，获得 tokens[i] 点 能量 ，并失去 1 分 。 在使用 任意 数量的令牌后，返回我们可以得到的最大 分数 。 示例 1： 输入：tokens = [100], power = 50 输出：0 解释：因为你的初始分数为 0，无法使令牌朝下。你也不能使令牌朝上因为你的能量（50）比 tokens[0] 少（100）。 示例 2： 输入：tokens = [200,100], power = 150 输出：1 解释：使令牌 1 正面朝上，能量变为 50，分数变为 1 。 不必使用令牌 0，因为你无法使用它来提高分数。可得到的最大分数是 1。 示例 3： 输入：tokens = [100,200,300,400], power = 200 输出：2 解释：按下面顺序使用令牌可以得到 2 分： 1. 令牌 0 (100)正面朝上，能量变为 100 ，分数变为 1 2. 令牌 3 (400)正面朝下，能量变为 500 ，分数变为 0 3. 令牌 1 (200)正面朝上，能量变为 300 ，分数变为 1 4. 令牌 2 (300)正面朝上，能量变为 0 ，分数变为 2 可得的最大分数是 2。 提示： * 0 <= tokens.length <= 1000 * 0 <= tokens[i], power < 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针和贪心算法来最大化分数。首先对令牌进行排序，然后使用两个指针分别指向令牌数组的两端。尽量使用能量最小的令牌来增加分数，当能量不足时，使用能量最大的令牌来增加能量。

算法步骤:
1. 对令牌数组进行排序。
2. 初始化两个指针 left 和 right，分别指向令牌数组的起始和末尾。
3. 初始化分数 score 为 0。
4. 当 left 指针小于等于 right 指针时，执行以下操作：
   - 如果当前能量足够使用 left 指针指向的令牌，则使用该令牌，增加分数，移动 left 指针。
   - 否则，如果分数大于 0 且 left 指针小于 right 指针，则使用 right 指针指向的令牌来增加能量，减少分数，移动 right 指针。
   - 如果以上两种情况都不满足，则退出循环。
5. 返回最大分数。

关键点:
- 排序后的令牌数组使得我们可以优先使用能量最小的令牌来增加分数。
- 使用双指针来灵活地选择使用哪个令牌来增加分数或能量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 tokens 的长度。主要的时间开销在于排序操作。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def bag_of_tokens_score(tokens: List[int], power: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 对令牌数组进行排序
    tokens.sort()
    
    # 初始化双指针
    left, right = 0, len(tokens) - 1
    score = 0
    max_score = 0
    
    while left <= right:
        if power >= tokens[left]:
            # 使用能量最小的令牌增加分数
            power -= tokens[left]
            score += 1
            left += 1
            max_score = max(max_score, score)
        elif score > 0 and left < right:
            # 使用能量最大的令牌增加能量
            power += tokens[right]
            score -= 1
            right -= 1
        else:
            # 无法继续操作，退出循环
            break
    
    return max_score

