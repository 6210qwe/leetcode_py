"""
532. 数组中的 k-diff 数对 - 给你一个整数数组 nums 和一个整数 k，请你在数组中找出 不同的 k-diff 数对，并返回不同的 k-diff 数对 的数目。 k-diff 数对定义为一个整数对 (nums[i], nums[j]) ，并满足下述全部条件： * 0 <= i, j < nums.length * i != j * |nums[i] - nums[j]| == k 注意，|val| 表示 val 的绝对值。 示例 1： 输入：nums = [3, 1, 4, 1, 5], k = 2 输出：2 解释：数组中有两个 2-diff 数对, (1, 3) 和 (3, 5)。 尽管数组中有两个 1 ，但我们只应返回不同的数对的数量。 示例 2： 输入：nums = [1, 2, 3, 4, 5], k = 1 输出：4 解释：数组中有四个 1-diff 数对, (1, 2), (2, 3), (3, 4) 和 (4, 5) 。 示例 3： 输入：nums = [1, 3, 1, 5, 4], k = 0 输出：1 解释：数组中只有一个 0-diff 数对，(1, 1) 。 提示： * 1 <= nums.length <= 104 * -107 <= nums[i] <= 107 * 0 <= k <= 107
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个数字出现的次数，然后遍历哈希表来找到符合条件的数对。

算法步骤:
1. 使用一个哈希表记录每个数字出现的次数。
2. 遍历哈希表，对于每个数字 num：
   - 如果 k > 0，则检查 num + k 是否在哈希表中，如果存在则计数加一。
   - 如果 k == 0，则检查 num 出现的次数是否大于 1，如果存在则计数加一。

关键点:
- 使用哈希表可以在 O(1) 时间内检查某个数字是否存在。
- 对于 k == 0 的情况，需要额外处理。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。遍历数组和哈希表的时间复杂度都是 O(n)。
空间复杂度: O(n)，哈希表的空间复杂度是 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_pairs(nums: List[int], k: int) -> int:
    """
    函数式接口 - 找到数组中不同的 k-diff 数对的数量
    """
    if k < 0:
        return 0
    
    # 使用哈希表记录每个数字出现的次数
    num_count = {}
    for num in nums:
        if num in num_count:
            num_count[num] += 1
        else:
            num_count[num] = 1
    
    count = 0
    for num in num_count:
        if k > 0 and num + k in num_count:
            count += 1
        elif k == 0 and num_count[num] > 1:
            count += 1
    
    return count


Solution = create_solution(find_pairs)