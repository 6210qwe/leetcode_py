"""
1694. 重新格式化电话号码 - 给你一个字符串形式的电话号码 number 。number 由数字、空格 ' '、和破折号 '-' 组成。 请你按下述方式重新格式化电话号码。 * 首先，删除 所有的空格和破折号。 * 其次，将数组从左到右 每 3 个一组 分块，直到 剩下 4 个或更少数字。剩下的数字将按下述规定再分块： * 2 个数字：单个含 2 个数字的块。 * 3 个数字：单个含 3 个数字的块。 * 4 个数字：两个分别含 2 个数字的块。 最后用破折号将这些块连接起来。注意，重新格式化过程中 不应该 生成仅含 1 个数字的块，并且 最多 生成两个含 2 个数字的块。 返回格式化后的电话号码。 示例 1： 输入：number = "1-23-45 6" 输出："123-456" 解释：数字是 "123456" 步骤 1：共有超过 4 个数字，所以先取 3 个数字分为一组。第 1 个块是 "123" 。 步骤 2：剩下 3 个数字，将它们放入单个含 3 个数字的块。第 2 个块是 "456" 。 连接这些块后得到 "123-456" 。 示例 2： 输入：number = "123 4-567" 输出："123-45-67" 解释：数字是 "1234567". 步骤 1：共有超过 4 个数字，所以先取 3 个数字分为一组。第 1 个块是 "123" 。 步骤 2：剩下 4 个数字，所以将它们分成两个含 2 个数字的块。这 2 块分别是 "45" 和 "67" 。 连接这些块后得到 "123-45-67" 。 示例 3： 输入：number = "123 4-5678" 输出："123-456-78" 解释：数字是 "12345678" 。 步骤 1：第 1 个块 "123" 。 步骤 2：第 2 个块 "456" 。 步骤 3：剩下 2 个数字，将它们放入单个含 2 个数字的块。第 3 个块是 "78" 。 连接这些块后得到 "123-456-78" 。 示例 4： 输入：number = "12" 输出："12" 示例 5： 输入：number = "--17-5 229 35-39475 " 输出："175-229-353-94-75" 提示： * 2 <= number.length <= 100 * number 由数字和字符 '-' 及 ' ' 组成。 * number 中至少含 2 个数字。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 删除所有非数字字符，然后按规则分块。

算法步骤:
1. 删除所有空格和破折号，只保留数字。
2. 根据剩余数字的数量进行分块：
   - 如果剩余数字数量大于 4，则每 3 个数字为一块。
   - 如果剩余数字数量为 4，则分成两个 2 个数字的块。
   - 如果剩余数字数量为 3 或 2，则直接作为一个块。
3. 将分好的块用破折号连接起来。

关键点:
- 使用正则表达式删除非数字字符。
- 根据剩余数字数量的不同情况进行分块。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是输入字符串的长度。我们需要遍历整个字符串来删除非数字字符，并进行分块。
空间复杂度: O(n)，需要存储处理后的数字字符串和结果字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
import re


def solution_function_name(number: str) -> str:
    """
    函数式接口 - 重新格式化电话号码
    """
    # 删除所有空格和破折号
    cleaned_number = re.sub(r'[\s-]', '', number)
    
    # 根据剩余数字数量进行分块
    if len(cleaned_number) % 3 == 1:
        # 如果剩余数字数量为 4 的倍数加 1，则分成 (n-4) 个 3 个数字的块，最后两个 2 个数字的块
        parts = [cleaned_number[i:i+3] for i in range(0, len(cleaned_number)-4, 3)]
        parts.append(cleaned_number[-4:-2])
        parts.append(cleaned_number[-2:])
    else:
        # 否则，每 3 个数字为一块
        parts = [cleaned_number[i:i+3] for i in range(0, len(cleaned_number), 3)]
    
    # 用破折号将这些块连接起来
    return '-'.join(parts)


Solution = create_solution(solution_function_name)