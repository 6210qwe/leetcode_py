"""
970. 强整数 - 给定三个整数 x 、 y 和 bound ，返回 值小于或等于 bound 的所有 强整数 组成的列表 。 如果某一整数可以表示为 xi + yj ，其中整数 i >= 0 且 j >= 0，那么我们认为该整数是一个 强整数 。 你可以按 任何顺序 返回答案。在你的回答中，每个值 最多 出现一次。 示例 1： 输入：x = 2, y = 3, bound = 10 输出：[2,3,4,5,7,9,10] 解释： 2 = 20 + 30 3 = 21 + 30 4 = 20 + 31 5 = 21 + 31 7 = 22 + 31 9 = 23 + 30 10 = 20 + 32 示例 2： 输入：x = 3, y = 5, bound = 15 输出：[2,4,6,8,10,14] 提示： * 1 <= x, y <= 100 * 0 <= bound <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个嵌套循环来枚举所有可能的 xi + yj 组合，并使用集合来去重。

算法步骤:
1. 初始化一个空集合来存储结果。
2. 使用两个嵌套循环分别枚举 i 和 j，计算 xi + yj。
3. 如果 xi + yj 小于等于 bound，则将其加入集合。
4. 将集合转换为列表并返回。

关键点:
- 使用集合来去重。
- 通过提前终止循环来减少不必要的计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log_x(bound) * log_y(bound))
空间复杂度: O(log_x(bound) * log_y(bound))
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def powerful_integers(x: int, y: int, bound: int) -> List[int]:
    """
    返回值小于或等于 bound 的所有强整数组成的列表。
    """
    result = set()
    # 枚举 i
    for i in range(20):  # log_2(10^6) < 20
        if x ** i > bound:
            break
        # 枚举 j
        for j in range(20):
            if y ** j > bound:
                break
            value = x ** i + y ** j
            if value <= bound:
                result.add(value)
    return list(result)

