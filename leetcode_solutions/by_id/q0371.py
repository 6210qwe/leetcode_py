def get_sum(a: int, b: int) -> int:
    """
    函数式接口 - 两整数之和
    
    实现思路:
    使用位运算模拟加法，异或得到无进位和，与运算左移得到进位。
    
    Args:
        a: 第一个整数
        b: 第二个整数
        
    Returns:
        两数之和
        
    Example:
        >>> get_sum(1, 2)
        3
    """
    # 32位整数掩码
    mask = 0xFFFFFFFF
    
    while b != 0:
        # 无进位和
        sum_without_carry = (a ^ b) & mask
        # 进位
        carry = ((a & b) << 1) & mask
        a = sum_without_carry
        b = carry
    
    # 处理负数情况
    if a > 0x7FFFFFFF:
        return ~(a ^ mask)
    return a


