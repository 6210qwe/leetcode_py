"""
371. 两整数之和 - 给你两个整数 a 和 b ，不使用 运算符 + 和 - ，计算并返回两整数之和。 示例 1： 输入：a = 1, b = 2 输出：3 示例 2： 输入：a = 2, b = 3 输出：5 提示： * -1000 <= a, b <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 位运算模拟加法

算法步骤:
1. 使用异或运算得到无进位和
2. 使用与运算左移1位得到进位
3. 重复直到进位为0

关键点:
- 异或运算得到无进位和
- 与运算左移得到进位
- 循环直到没有进位
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 最多32次循环（32位整数）
空间复杂度: O(1) - 只使用常数空间
"""

