"""
2496. 数组中字符串的最大值 - 一个由字母和数字组成的字符串的 值 定义如下： * 如果字符串 只 包含数字，那么值为该字符串在 10 进制下的所表示的数字。 * 否则，值为字符串的 长度 。 给你一个字符串数组 strs ，每个字符串都只由字母和数字组成，请你返回 strs 中字符串的 最大值 。 示例 1： 输入：strs = ["alic3","bob","3","4","00000"] 输出：5 解释： - "alic3" 包含字母和数字，所以值为长度 5 。 - "bob" 只包含字母，所以值为长度 3 。 - "3" 只包含数字，所以值为 3 。 - "4" 只包含数字，所以值为 4 。 - "00000" 只包含数字，所以值为 0 。 所以最大的值为 5 ，是字符串 "alic3" 的值。 示例 2： 输入：strs = ["1","01","001","0001"] 输出：1 解释： 数组中所有字符串的值都是 1 ，所以我们返回 1 。 提示： * 1 <= strs.length <= 100 * 1 <= strs[i].length <= 9 * strs[i] 只包含小写英文字母和数字。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对于每个字符串，判断其是否只包含数字。如果是，则将其转换为整数；否则，取其长度。最后返回这些值中的最大值。

算法步骤:
1. 初始化一个变量 `max_value` 为 0，用于存储当前找到的最大值。
2. 遍历字符串数组 `strs`，对于每个字符串：
   - 如果字符串只包含数字，则将其转换为整数并更新 `max_value`。
   - 否则，取字符串的长度并更新 `max_value`。
3. 返回 `max_value`。

关键点:
- 使用 `str.isdigit()` 方法来判断字符串是否只包含数字。
- 使用 `int(str)` 将字符串转换为整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是字符串数组的长度，m 是字符串的平均长度。我们需要遍历每个字符串并进行判断和转换。
空间复杂度: O(1)，只需要常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_string_value(strs: List[str]) -> int:
    """
    函数式接口 - 返回字符串数组中字符串的最大值
    """
    max_value = 0
    for s in strs:
        if s.isdigit():
            value = int(s)
        else:
            value = len(s)
        max_value = max(max_value, value)
    return max_value


Solution = create_solution(max_string_value)