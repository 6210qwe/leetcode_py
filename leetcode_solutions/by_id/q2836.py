# 问题描述
# ============================================================================
"""
2733. 既不是最小值也不是最大值 - 给你一个整数数组 nums ，数组由 不同正整数 组成，请你找出并返回数组中 任一 既不是 最小值 也不是 最大值 的数字，如果不存在这样的数字，返回 -1 。 返回所选整数。 示例 1： 输入：nums = [3,2,1,4] 输出：2 解释：在这个示例中，最小值是 1 ，最大值是 4 。因此，2 或 3 都是有效答案。 示例 2： 输入：nums = [1,2] 输出：-1 解释：由于不存在既不是最大值也不是最小值的数字，我们无法选出满足题目给定条件的数字。因此，不存在答案，返回 -1 。 示例 3： 输入：nums = [2,1,3] 输出：2 解释：2 既不是最小值，也不是最大值，这个示例只有这一个有效答案。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100 * nums 中的所有数字互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到数组中的任意一个既不是最小值也不是最大值的元素。

算法步骤:
1. 如果数组长度小于等于2，直接返回-1，因为没有既不是最小值也不是最大值的元素。
2. 找到数组中的最小值和最大值。
3. 遍历数组，找到第一个既不是最小值也不是最大值的元素并返回。

关键点:
- 通过一次遍历找到最小值和最大值。
- 再次遍历数组找到符合条件的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要两次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找到数组中既不是最小值也不是最大值的任意一个元素。
    """
    if len(nums) <= 2:
        return -1

    min_val = min(nums)
    max_val = max(nums)

    for num in nums:
        if num != min_val and num != max_val:
            return num

    return -1


Solution = create_solution(solution_function_name)