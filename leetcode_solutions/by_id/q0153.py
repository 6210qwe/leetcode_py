from typing import List
from leetcode_solutions.utils.solution import create_solution


def find_minimum_in_rotated_sorted_array(nums: List[int]) -> int:
    """
    函数式接口 - 寻找旋转排序数组中的最小值
    
    实现思路:
    使用二分查找，比较中间元素和右边界元素判断旋转点位置。
    
    Args:
        nums: 旋转排序数组
        
    Returns:
        数组中的最小元素
        
    Example:
        >>> find_minimum_in_rotated_sorted_array([3, 4, 5, 1, 2])
        1
    """
    left, right = 0, len(nums) - 1
    
    while left < right:
        mid = (left + right) // 2
        
        if nums[mid] > nums[right]:
            left = mid + 1
        else:
            right = mid
    
    return nums[left]


