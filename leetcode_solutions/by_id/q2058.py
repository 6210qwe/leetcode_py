"""
1929. 数组串联 - 给你一个长度为 n 的整数数组 nums 。请你构建一个长度为 2n 的答案数组 ans ，数组下标 从 0 开始计数 ，对于所有 0 <= i < n 的 i ，满足下述所有要求： * ans[i] == nums[i] * ans[i + n] == nums[i] 具体而言，ans 由两个 nums 数组 串联 形成。 返回数组 ans 。 示例 1： 输入：nums = [1,2,1] 输出：[1,2,1,1,2,1] 解释：数组 ans 按下述方式形成： - ans = [nums[0],nums[1],nums[2],nums[0],nums[1],nums[2]] - ans = [1,2,1,1,2,1] 示例 2： 输入：nums = [1,3,2,1] 输出：[1,3,2,1,1,3,2,1] 解释：数组 ans 按下述方式形成： - ans = [nums[0],nums[1],nums[2],nums[3],nums[0],nums[1],nums[2],nums[3]] - ans = [1,3,2,1,1,3,2,1] 提示： * n == nums.length * 1 <= n <= 1000 * 1 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 直接将原数组拼接到自身后面，形成新的数组。

算法步骤:
1. 创建一个新的数组，将原数组 nums 拼接到自身后面。
2. 返回新数组。

关键点:
- 使用列表的加法操作来拼接数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 遍历和拼接数组的时间复杂度是线性的。
空间复杂度: O(n) - 新数组的空间复杂度是线性的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def get_concatenation(nums: List[int]) -> List[int]:
    """
    函数式接口 - 将原数组拼接到自身后面，形成新的数组。
    """
    # 直接将原数组拼接到自身后面
    return nums + nums


Solution = create_solution(get_concatenation)