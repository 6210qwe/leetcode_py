# 问题描述
# ============================================================================
"""
3587. 最小相邻交换至奇偶交替 - 给你一个由互不相同的整数组成的数组 nums 。 在一次操作中，你可以交换任意两个 相邻 元素。 在一个排列中，当所有相邻元素的奇偶性交替出现，我们认为该排列是 有效排列。这意味着每对相邻元素中一个是偶数，一个是奇数。 请返回将 nums 变成任意一种 有效排列 所需的最小相邻交换次数。 如果无法重排 nums 来获得有效排列，则返回 -1。 示例 1： 输入： nums = [2,4,6,5,7] 输出：3 解释： 将 5 和 6 交换，数组变成 [2,4,5,6,7] 将 5 和 4 交换，数组变成 [2,5,4,6,7] 将 6 和 7 交换，数组变成 [2,5,4,7,6]。此时是一个有效排列。因此答案是 3。 示例 2： 输入： nums = [2,4,5,7] 输出： 1 解释： 将 4 和 5 交换，数组变成 [2,5,4,7]。此时是一个有效排列。因此答案是 1。 示例 3： 输入： nums = [1,2,3] 输出： 0 解释： 数组已经是有效排列，因此不需要任何操作。 示例 4： 输入： nums = [4,5,6,8] 输出：-1 解释： 没有任何一种排列可以满足奇偶交替的要求，因此返回 -1。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109 * nums 中的所有元素都是 唯一 的
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，通过维护两个指针分别指向当前需要放置奇数和偶数的位置，然后计算每个位置上应该放置的奇数或偶数所需的最小交换次数。

算法步骤:
1. 初始化两个指针 `odd_ptr` 和 `even_ptr`，分别指向第一个奇数和偶数的位置。
2. 遍历数组，根据当前指针的位置判断当前位置应该放置奇数还是偶数。
3. 如果当前位置的元素不符合要求，则找到下一个符合要求的元素，并计算交换次数。
4. 更新指针位置，继续遍历直到数组结束。
5. 如果在遍历过程中发现无法满足条件，则返回 -1。

关键点:
- 使用两个指针分别指向当前需要放置奇数和偶数的位置。
- 计算每次交换的最小次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_swaps_to_alternate_parity(nums: List[int]) -> int:
    """
    函数式接口 - 返回将 nums 变成任意一种有效排列所需的最小相邻交换次数。
    """
    n = len(nums)
    if n == 1:
        return 0

    # 初始化指针
    odd_ptr, even_ptr = 0, 0
    swaps = 0

    for i in range(n):
        if i % 2 == 0:
            # 当前位置应该是偶数
            while even_ptr < n and nums[even_ptr] % 2 != 0:
                even_ptr += 1
            if even_ptr == n:
                return -1
            swaps += abs(even_ptr - i)
            even_ptr += 1
        else:
            # 当前位置应该是奇数
            while odd_ptr < n and nums[odd_ptr] % 2 == 0:
                odd_ptr += 1
            if odd_ptr == n:
                return -1
            swaps += abs(odd_ptr - i)
            odd_ptr += 1

    return swaps


Solution = create_solution(min_swaps_to_alternate_parity)