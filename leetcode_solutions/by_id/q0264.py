from leetcode_solutions.utils.solution import create_solution


def ugly_number_ii(n: int) -> int:
    """
    函数式接口 - 丑数II
    
    实现思路:
    动态规划，使用三个指针分别指向乘以2、3、5的下一个位置。
    
    Args:
        n: 要查找的位置
        
    Returns:
        第n个丑数
        
    Example:
        >>> ugly_number_ii(10)
        12
    """
    dp = [1] * n
    i2 = i3 = i5 = 0
    
    for i in range(1, n):
        next2 = dp[i2] * 2
        next3 = dp[i3] * 3
        next5 = dp[i5] * 5
        
        dp[i] = min(next2, next3, next5)
        
        if dp[i] == next2:
            i2 += 1
        if dp[i] == next3:
            i3 += 1
        if dp[i] == next5:
            i5 += 1
    
    return dp[n - 1]


