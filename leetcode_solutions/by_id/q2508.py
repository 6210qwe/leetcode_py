"""
2428. 沙漏的最大总和 - 给你一个大小为 m x n 的整数矩阵 grid 。 按以下形式将矩阵的一部分定义为一个 沙漏 ： [https://assets.leetcode.com/uploads/2022/08/21/img.jpg] 返回沙漏中元素的 最大 总和。 注意：沙漏无法旋转且必须整个包含在矩阵中。 示例 1： [https://assets.leetcode.com/uploads/2022/08/21/1.jpg] 输入：grid = [[6,2,1,3],[4,2,1,5],[9,2,8,7],[4,1,2,9]] 输出：30 解释：上图中的单元格表示元素总和最大的沙漏：6 + 2 + 1 + 2 + 9 + 2 + 8 = 30 。 示例 2： [https://assets.leetcode.com/uploads/2022/08/21/2.jpg] 输入：grid = [[1,2,3],[4,5,6],[7,8,9]] 输出：35 解释：上图中的单元格表示元素总和最大的沙漏：1 + 2 + 3 + 5 + 7 + 8 + 9 = 35 。 提示： * m == grid.length * n == grid[i].length * 3 <= m, n <= 150 * 0 <= grid[i][j] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历矩阵中的每个可能的沙漏形状，计算其总和并记录最大值。

算法步骤:
1. 初始化最大沙漏和为负无穷。
2. 遍历矩阵中的每个可能的沙漏中心点 (i, j)，其中 i 和 j 的范围分别为 [1, m-2] 和 [1, n-2]。
3. 对于每个中心点，计算沙漏的总和。
4. 更新最大沙漏和。
5. 返回最大沙漏和。

关键点:
- 确保沙漏的边界条件正确，即中心点 (i, j) 的范围是 [1, m-2] 和 [1, n-2]。
- 计算沙漏总和时，确保只计算沙漏形状内的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_hourglass_sum(grid: List[List[int]]) -> int:
    """
    函数式接口 - 计算矩阵中沙漏的最大总和
    """
    if not grid or not grid[0]:
        return 0
    
    m, n = len(grid), len(grid[0])
    max_sum = float('-inf')
    
    for i in range(1, m - 1):
        for j in range(1, n - 1):
            # 计算当前沙漏的总和
            hourglass_sum = (
                grid[i-1][j-1] + grid[i-1][j] + grid[i-1][j+1] +
                grid[i][j] +
                grid[i+1][j-1] + grid[i+1][j] + grid[i+1][j+1]
            )
            # 更新最大沙漏和
            max_sum = max(max_sum, hourglass_sum)
    
    return max_sum


Solution = create_solution(max_hourglass_sum)