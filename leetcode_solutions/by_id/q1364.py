"""
1726. 同积元组 - 给你一个由 不同 正整数组成的数组 nums ，请你返回满足 a * b = c * d 的元组 (a, b, c, d) 的数量。其中 a、b、c 和 d 都是 nums 中的元素，且 a != b != c != d 。 示例 1： 输入：nums = [2,3,4,6] 输出：8 解释：存在 8 个满足题意的元组： (2,6,3,4) , (2,6,4,3) , (6,2,3,4) , (6,2,4,3) (3,4,2,6) , (4,3,2,6) , (3,4,6,2) , (4,3,6,2) 示例 2： 输入：nums = [1,2,4,5,10] 输出：16 解释：存在 16 个满足题意的元组： (1,10,2,5) , (1,10,5,2) , (10,1,2,5) , (10,1,5,2) (2,5,1,10) , (2,5,10,1) , (5,2,1,10) , (5,2,10,1) (2,10,4,5) , (2,10,5,4) , (10,2,4,5) , (10,2,5,4) (4,5,2,10) , (4,5,10,2) , (5,4,2,10) , (5,4,10,2) 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= 104 * nums 中的所有元素 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每对乘积出现的次数，然后计算满足条件的四元组数量。

算法步骤:
1. 初始化一个哈希表 `product_count` 用于记录每对乘积出现的次数。
2. 遍历数组中的每一对元素 `(i, j)`，计算它们的乘积 `prod = i * j`，并更新 `product_count[prod]`。
3. 遍历哈希表 `product_count`，对于每个乘积 `prod`，如果 `product_count[prod] > 1`，则可以形成 `product_count[prod] * (product_count[prod] - 1) * 4` 个满足条件的四元组。
4. 返回所有满足条件的四元组数量之和。

关键点:
- 使用哈希表记录每对乘积出现的次数。
- 计算满足条件的四元组数量时，考虑排列组合。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n^2)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def tuple_same_product(nums: List[int]) -> int:
    """
    函数式接口 - 计算满足 a * b = c * d 的元组 (a, b, c, d) 的数量
    """
    product_count = {}
    
    # 计算每对乘积出现的次数
    for i in range(len(nums)):
        for j in range(i + 1, len(nums)):
            prod = nums[i] * nums[j]
            if prod in product_count:
                product_count[prod] += 1
            else:
                product_count[prod] = 1
    
    # 计算满足条件的四元组数量
    count = 0
    for prod, freq in product_count.items():
        if freq > 1:
            count += freq * (freq - 1) * 4
    
    return count


Solution = create_solution(tuple_same_product)