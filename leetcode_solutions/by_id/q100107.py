"""
LCP 01. 猜数字 - 小A 和 小B 在玩猜数字。小B 每次从 1, 2, 3 中随机选择一个，小A 每次也从 1, 2, 3 中选择一个猜。他们一共进行三次这个游戏，请返回 小A 猜对了几次？ 输入的guess数组为 小A 每次的猜测，answer数组为 小B 每次的选择。guess和answer的长度都等于3。 示例 1： 输入：guess = [1,2,3], answer = [1,2,3] 输出：3 解释：小A 每次都猜对了。 示例 2： 输入：guess = [2,2,3], answer = [3,2,1] 输出：1 解释：小A 只猜对了第二次。 限制： 1. guess的长度 = 3 2. answer的长度 = 3 3. guess的元素取值为 {1, 2, 3} 之一。 4. answer的元素取值为 {1, 2, 3} 之一。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历两个数组，逐个比较对应位置的元素是否相等，统计相等的次数。

算法步骤:
1. 初始化一个计数器 `count` 为 0。
2. 遍历 `guess` 和 `answer` 数组，比较每个位置的元素。
3. 如果 `guess[i]` 等于 `answer[i]`，则 `count` 加 1。
4. 返回 `count`。

关键点:
- 使用简单的遍历来比较两个数组的对应元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 因为数组长度固定为 3，所以时间复杂度是常数级别的。
空间复杂度: O(1) - 只使用了常数级别的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(guess: List[int], answer: List[int]) -> int:
    """
    函数式接口 - 计算小A猜对的次数
    """
    count = 0
    for i in range(len(guess)):
        if guess[i] == answer[i]:
            count += 1
    return count


Solution = create_solution(solution_function_name)