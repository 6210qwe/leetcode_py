# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算第 n 个泰波那契数。

算法步骤:
1. 初始化前三个泰波那契数 T0, T1, T2。
2. 从第 3 个数开始，依次计算每个泰波那契数，直到第 n 个数。
3. 返回第 n 个泰波那契数。

关键点:
- 使用常数空间存储当前和前两个泰波那契数，以节省空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

