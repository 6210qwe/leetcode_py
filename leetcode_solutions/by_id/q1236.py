def tribonacci(n: int) -> int:
    """
    函数式接口 - 计算第 n 个泰波那契数
    """
    if n == 0:
        return 0
    elif n == 1 or n == 2:
        return 1
    
    t0, t1, t2 = 0, 1, 1
    for i in range(3, n + 1):
        t0, t1, t2 = t1, t2, t0 + t1 + t2
    
    return t2


Solution = create_solution(tribonacci)