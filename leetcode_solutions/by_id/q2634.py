"""
2540. 最小公共值 - 给你两个整数数组 nums1 和 nums2 ，它们已经按非降序排序，请你返回两个数组的 最小公共整数 。如果两个数组 nums1 和 nums2 没有公共整数，请你返回 -1 。 如果一个整数在两个数组中都 至少出现一次 ，那么这个整数是数组 nums1 和 nums2 公共 的。 示例 1： 输入：nums1 = [1,2,3], nums2 = [2,4] 输出：2 解释：两个数组的最小公共元素是 2 ，所以我们返回 2 。 示例 2： 输入：nums1 = [1,2,3,6], nums2 = [2,3,4,5] 输出：2 解释：两个数组中的公共元素是 2 和 3 ，2 是较小值，所以返回 2 。 提示： * 1 <= nums1.length, nums2.length <= 105 * 1 <= nums1[i], nums2[j] <= 109 * nums1 和 nums2 都是 非降序 的。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来找到两个有序数组的最小公共值。

算法步骤:
1. 初始化两个指针 i 和 j 分别指向 nums1 和 nums2 的起始位置。
2. 比较 nums1[i] 和 nums2[j]：
   - 如果 nums1[i] < nums2[j]，则将 i 向右移动一位。
   - 如果 nums1[i] > nums2[j]，则将 j 向右移动一位。
   - 如果 nums1[i] == nums2[j]，则找到了最小公共值，返回该值。
3. 如果遍历完其中一个数组仍未找到公共值，则返回 -1。

关键点:
- 双指针方法可以有效地找到两个有序数组的最小公共值，时间复杂度为 O(n + m)，其中 n 和 m 分别是 nums1 和 nums2 的长度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums1: List[int], nums2: List[int]) -> int:
    """
    函数式接口 - 使用双指针方法找到两个有序数组的最小公共值
    """
    i, j = 0, 0
    while i < len(nums1) and j < len(nums2):
        if nums1[i] < nums2[j]:
            i += 1
        elif nums1[i] > nums2[j]:
            j += 1
        else:
            return nums1[i]
    return -1


Solution = create_solution(solution_function_name)