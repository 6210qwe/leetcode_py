"""
3170. 删除星号以后字典序最小的字符串 - 给你一个字符串 s 。它可能包含任意数量的 '*' 字符。你的任务是删除所有的 '*' 字符。 当字符串还存在至少一个 '*' 字符时，你可以执行以下操作： * 删除最左边的 '*' 字符，同时删除该星号字符左边一个字典序 最小 的字符。如果有多个字典序最小的字符，你可以删除它们中的任意一个。 请你返回删除所有 '*' 字符以后，剩余字符连接而成的 字典序最小 的字符串。 示例 1： 输入：s = "aaba*" 输出："aab" 解释： 删除 '*' 号和它左边的其中一个 'a' 字符。如果我们选择删除 s[3] ，s 字典序最小。 示例 2： 输入：s = "abc" 输出："abc" 解释： 字符串中没有 '*' 字符。 提示： * 1 <= s.length <= 105 * s 只含有小写英文字母和 '*' 字符。 * 输入保证操作可以删除所有的 '*' 字符。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来处理字符串，遇到星号时弹出栈顶元素，最后将栈中的元素拼接成结果字符串。

算法步骤:
1. 初始化一个空栈。
2. 遍历字符串 s 中的每个字符：
   - 如果字符是星号 '*'，则弹出栈顶元素。
   - 否则，将字符压入栈中。
3. 将栈中的元素拼接成结果字符串并返回。

关键点:
- 使用栈来维护当前的字符序列，确保在遇到星号时能够正确地删除前一个字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历一次字符串。
空间复杂度: O(n)，在最坏情况下，栈中可能会存储所有的字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> str:
    """
    函数式接口 - 返回删除所有 '*' 字符以后，剩余字符连接而成的字典序最小的字符串
    """
    stack = []
    for char in s:
        if char == '*':
            if stack:
                stack.pop()
        else:
            stack.append(char)
    return ''.join(stack)


Solution = create_solution(solution_function_name)