"""
核心思想: 使用深度优先搜索 (DFS) 来遍历树，并使用动态规划 (DP) 来记录每个节点的状态。通过位运算来检查和更新每个节点的数字频率。

算法步骤:
1. 构建树结构。
2. 使用 DFS 遍历树，对于每个节点，计算其子树中的最大好子集分数。
3. 使用 DP 数组来记录每个节点的状态，状态包括当前节点的数字频率和最大分数。
4. 通过位运算来检查和更新数字频率，确保每个数字在子集中最多出现一次。
5. 返回所有节点的最大好子集分数的总和。

关键点:
- 使用位运算来高效地检查和更新数字频率。
- 使用 DFS 和 DP 来处理树的遍历和状态传递。
"""

# ============================================================================
# 复杂度分析
