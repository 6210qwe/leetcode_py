"""
944. 删列造序 - 给你由 n 个小写字母字符串组成的数组 strs，其中每个字符串长度相等。 这些字符串可以每个一行，排成一个网格。例如，strs = ["abc", "bce", "cae"] 可以排列为： abc bce cae 你需要找出并删除 不是按字典序非严格递增排列的 列。在上面的例子（下标从 0 开始）中，列 0（'a', 'b', 'c'）和列 2（'c', 'e', 'e'）都是按字典序非严格递增排列的，而列 1（'b', 'c', 'a'）不是，所以要删除列 1 。 返回你需要删除的列数。 示例 1： 输入：strs = ["cba","daf","ghi"] 输出：1 解释：网格示意如下： cba daf ghi 列 0 和列 2 按升序排列，但列 1 不是，所以只需要删除列 1 。 示例 2： 输入：strs = ["a","b"] 输出：0 解释：网格示意如下： a b 只有列 0 这一列，且已经按升序排列，所以不用删除任何列。 示例 3： 输入：strs = ["zyx","wvu","tsr"] 输出：3 解释：网格示意如下： zyx wvu tsr 所有 3 列都是非升序排列的，所以都要删除。 提示： * n == strs.length * 1 <= n <= 100 * 1 <= strs[i].length <= 1000 * strs[i] 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 对每一列进行检查，如果某一列不是按字典序非严格递增排列，则需要删除该列。

算法步骤:
1. 初始化一个计数器 `delete_count` 用于记录需要删除的列数。
2. 遍历每一列，对于每一列，检查是否所有字符都按字典序非严格递增排列。
3. 如果某一列不满足条件，则将 `delete_count` 增加 1。
4. 返回 `delete_count`。

关键点:
- 通过遍历每一列来检查是否按字典序非严格递增排列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是字符串的数量，m 是每个字符串的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def delete_columns_to_make_sorted(strs: List[str]) -> int:
    """
    函数式接口 - 删除不按字典序非严格递增排列的列，并返回需要删除的列数。
    """
    if not strs:
        return 0

    num_rows = len(strs)
    num_cols = len(strs[0])
    delete_count = 0

    for col in range(num_cols):
        for row in range(1, num_rows):
            if strs[row][col] < strs[row - 1][col]:
                delete_count += 1
                break

    return delete_count


Solution = create_solution(delete_columns_to_make_sorted)