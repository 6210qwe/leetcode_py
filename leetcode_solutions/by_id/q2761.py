"""
2626. 数组归约运算 - 给定一个整数数组 nums、一个 reducer 函数 fn 和一个初始值 init，返回通过依次对数组的每个元素执行 fn 函数得到的最终结果。 通过以下操作实现这个结果：val = fn(init, nums[0])，val = fn(val, nums[1])，val = fn(val, nums[2])，... 直到处理数组中的每个元素。然后返回 val 的最终值。 如果数组的长度为 0，则函数应返回 init。 请你在不使用内置数组方法的 Array.reduce 前提下解决这个问题。 示例 1： 输入： nums = [1,2,3,4] fn = function sum(accum, curr) { return accum + curr; } init = 0 输出：10 解释： 初始值为 init=0 。 (0) + nums[0] = 1 (1) + nums[1] = 3 (3) + nums[2] = 6 (6) + nums[3] = 10 Val 最终值为 10。 示例 2： 输入： nums = [1,2,3,4] fn = function sum(accum, curr) { return accum + curr * curr; } init = 100 输出：130 解释： 初始值为 init=100 。 (100) + nums[0]^2 = 101 (101) + nums[1]^2 = 105 (105) + nums[2]^2 = 114 (114) + nums[3]^2 = 130 Val 最终值为 130。 示例3: 输入： nums = [] fn = function sum(accum, curr) { return 0; } init = 25 输出：25 解释：这是一个空数组，所以返回 init 。 提示： * 0 <= nums.length <= 1000 * 0 <= nums[i] <= 1000 * 0 <= init <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组并依次应用 reducer 函数来计算最终结果。

算法步骤:
1. 初始化结果变量 `result` 为 `init`。
2. 遍历数组 `nums`，对于每个元素 `num`，更新 `result` 为 `fn(result, num)`。
3. 返回最终的 `result`。

关键点:
- 使用一个循环遍历数组，并在每次迭代中应用 reducer 函数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。我们需要遍历整个数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Callable

def array_reduce(nums: List[int], fn: Callable[[int, int], int], init: int) -> int:
    """
    通过依次对数组的每个元素执行 fn 函数得到的最终结果。
    
    :param nums: 整数数组
    :param fn: reducer 函数
    :param init: 初始值
    :return: 最终结果
    """
    result = init
    for num in nums:
        result = fn(result, num)
    return result

