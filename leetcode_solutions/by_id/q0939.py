"""
903. DI 序列的有效排列 - 给定一个长度为 n 的字符串 s ，其中 s[i] 是: * “D” 意味着减少，或者 * “I” 意味着增加 有效排列 是对有 n + 1 个在 [0, n] 范围内的整数的一个排列 perm ，使得对所有的 i： * 如果 s[i] == 'D'，那么 perm[i] > perm[i+1]，以及； * 如果 s[i] == 'I'，那么 perm[i] < perm[i+1]。 返回 有效排列 perm的数量 。因为答案可能很大，所以请返回你的答案对 109 + 7 取余。 示例 1： 输入：s = "DID" 输出：5 解释： (0, 1, 2, 3) 的五个有效排列是： (1, 0, 3, 2) (2, 0, 3, 1) (2, 1, 3, 0) (3, 0, 2, 1) (3, 1, 2, 0) 示例 2: 输入: s = "D" 输出: 1 提示: * n == s.length * 1 <= n <= 200 * s[i] 不是 'I' 就是 'D'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示前 i 个字符且最后一个数字为 j 的有效排列数。

算法步骤:
1. 初始化 dp 数组，dp[0][0] = 1。
2. 遍历字符串 s，对于每个字符：
   - 如果是 'D'，则 dp[i][j] = sum(dp[i-1][j+1:])。
   - 如果是 'I'，则 dp[i][j] = sum(dp[i-1][:j])。
3. 最后结果是 dp[n][0] 到 dp[n][n] 的和。

关键点:
- 使用前缀和优化求和操作，避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n^2)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算有效的排列数量
    """
    n = len(s)
    mod = 10**9 + 7
    dp = [[0] * (n + 1) for _ in range(n + 1)]
    dp[0][0] = 1

    for i in range(1, n + 1):
        prefix_sum = 0
        for j in range(i + 1):
            if s[i - 1] == 'I':
                dp[i][j] = prefix_sum
            else:
                dp[i][j] = (dp[i - 1][i] - dp[i - 1][j] + mod) % mod
            prefix_sum = (prefix_sum + dp[i][j]) % mod

    return sum(dp[n]) % mod


Solution = create_solution(solution_function_name)