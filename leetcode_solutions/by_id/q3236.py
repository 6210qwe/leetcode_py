"""
2996. 大于等于顺序前缀和的最小缺失整数 - 给你一个下标从 0 开始的整数数组 nums 。 如果一个前缀 nums[0..i] 满足对于 1 <= j <= i 的所有元素都有 nums[j] = nums[j - 1] + 1 ，那么我们称这个前缀是一个 顺序前缀 。特殊情况是，只包含 nums[0] 的前缀也是一个 顺序前缀 。 请你返回 nums 中没有出现过的 最小 整数 x ，满足 x 大于等于 最长 顺序前缀的和。 示例 1： 输入：nums = [1,2,3,2,5] 输出：6 解释：nums 的最长顺序前缀是 [1,2,3] ，和为 6 ，6 不在数组中，所以 6 是大于等于最长顺序前缀和的最小整数。 示例 2： 输入：nums = [3,4,5,1,12,14,13] 输出：15 解释：nums 的最长顺序前缀是 [3,4,5] ，和为 12 ，12、13 和 14 都在数组中，但 15 不在，所以 15 是大于等于最长顺序前缀和的最小整数。 提示： * 1 <= nums.length <= 50 * 1 <= nums[i] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到最长的顺序前缀，并计算其和，然后找到第一个不在数组中的大于等于该和的整数。

算法步骤:
1. 遍历数组，找到最长的顺序前缀。
2. 计算最长顺序前缀的和。
3. 从该和开始，逐个检查是否存在数组中，直到找到第一个不存在的整数。

关键点:
- 使用集合来快速查找数组中的元素。
- 通过遍历数组找到最长的顺序前缀。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def smallest_missing_integer(nums: List[int]) -> int:
    """
    函数式接口 - 找到大于等于顺序前缀和的最小缺失整数
    """
    # 将数组转换为集合以进行快速查找
    num_set = set(nums)
    
    # 找到最长的顺序前缀
    prefix_sum = 0
    for i in range(len(nums)):
        if i == 0 or nums[i] == nums[i - 1] + 1:
            prefix_sum += nums[i]
        else:
            break
    
    # 从最长顺序前缀的和开始，找到第一个不在数组中的整数
    while prefix_sum in num_set:
        prefix_sum += 1
    
    return prefix_sum


Solution = create_solution(smallest_missing_integer)