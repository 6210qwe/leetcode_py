"""
2478. 完美分割的方案数 - 给你一个字符串 s ，每个字符是数字 '1' 到 '9' ，再给你两个整数 k 和 minLength 。 如果对 s 的分割满足以下条件，那么我们认为它是一个 完美 分割： * s 被分成 k 段互不相交的子字符串。 * 每个子字符串长度都 至少 为 minLength 。 * 每个子字符串的第一个字符都是一个 质数 数字，最后一个字符都是一个 非质数 数字。质数数字为 '2' ，'3' ，'5' 和 '7' ，剩下的都是非质数数字。 请你返回 s 的 完美 分割数目。由于答案可能很大，请返回答案对 109 + 7 取余 后的结果。 一个 子字符串 是字符串中一段连续字符串序列。 示例 1： 输入：s = "23542185131", k = 3, minLength = 2 输出：3 解释：存在 3 种完美分割方案： "2354 | 218 | 5131" "2354 | 21851 | 31" "2354218 | 51 | 31" 示例 2： 输入：s = "23542185131", k = 3, minLength = 3 输出：1 解释：存在一种完美分割方案："2354 | 218 | 5131" 。 示例 3： 输入：s = "3312958", k = 3, minLength = 1 输出：1 解释：存在一种完美分割方案："331 | 29 | 58" 。 提示： * 1 <= k, minLength <= s.length <= 1000 * s 每个字符都为数字 '1' 到 '9' 之一。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示将前 i 个字符分成 j 段的完美分割方案数。

算法步骤:
1. 初始化 dp 数组，dp[0][0] = 1。
2. 遍历字符串 s，对于每个位置 i，检查是否可以作为某个子字符串的结尾。
3. 如果 s[i] 是非质数且 s[i - minLength + 1] 是质数，则更新 dp 数组。
4. 最终结果保存在 dp[n][k] 中，其中 n 是字符串 s 的长度。

关键点:
- 使用动态规划来记录每一步的状态。
- 通过前缀和优化状态转移。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)，其中 n 是字符串 s 的长度，k 是分割段数。
空间复杂度: O(n * k)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def is_prime(ch: str) -> bool:
    return ch in {'2', '3', '5', '7'}

def number_of_beautiful_partitions(s: str, k: int, min_length: int) -> int:
    MOD = 10**9 + 7
    n = len(s)
    
    if not is_prime(s[0]) or is_prime(s[-1]):
        return 0
    
    dp = [[0] * (k + 1) for _ in range(n + 1)]
    dp[0][0] = 1
    
    for i in range(1, n + 1):
        for j in range(1, k + 1):
            if i >= min_length and is_prime(s[i - min_length]) and not is_prime(s[i - 1]):
                dp[i][j] = (dp[i][j] + dp[i - min_length][j - 1]) % MOD
    
    return dp[n][k]

Solution = create_solution(number_of_beautiful_partitions)