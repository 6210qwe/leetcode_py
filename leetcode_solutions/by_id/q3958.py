"""
3634. 使数组平衡的最少移除数目 - 给你一个整数数组 nums 和一个整数 k。 如果一个数组的 最大 元素的值 至多 是其 最小 元素的 k 倍，则该数组被称为是 平衡 的。 你可以从 nums 中移除 任意 数量的元素，但不能使其变为 空 数组。 返回为了使剩余数组平衡，需要移除的元素的 最小 数量。 注意：大小为 1 的数组被认为是平衡的，因为其最大值和最小值相等，且条件总是成立。 示例 1: 输入：nums = [2,1,5], k = 2 输出：1 解释： * 移除 nums[2] = 5 得到 nums = [2, 1]。 * 现在 max = 2, min = 1，且 max <= min * k，因为 2 <= 1 * 2。因此，答案是 1。 示例 2: 输入：nums = [1,6,2,9], k = 3 输出：2 解释： * 移除 nums[0] = 1 和 nums[3] = 9 得到 nums = [6, 2]。 * 现在 max = 6, min = 2，且 max <= min * k，因为 6 <= 2 * 3。因此，答案是 2。 示例 3: 输入：nums = [4,6], k = 2 输出：0 解释： * 由于 nums 已经平衡，因为 6 <= 4 * 2，所以不需要移除任何元素。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109 * 1 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和双指针方法找到最长的平衡子数组。

算法步骤:
1. 对数组进行排序。
2. 使用双指针方法，左指针从头开始，右指针从尾部开始。
3. 每次检查当前子数组是否平衡，如果不平衡则移动左指针，否则移动右指针。
4. 记录最长的平衡子数组长度。
5. 返回需要移除的元素数量，即总长度减去最长平衡子数组长度。

关键点:
- 排序后的数组可以简化问题，只需检查相邻元素。
- 双指针方法可以高效地找到最长的平衡子数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序的时间复杂度。
空间复杂度: O(1) - 仅使用常数级额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_removals_to_balance_array(nums: List[int], k: int) -> int:
    """
    函数式接口 - 返回为了使剩余数组平衡，需要移除的元素的最小数量。
    """
    nums.sort()  # 对数组进行排序
    n = len(nums)
    left = 0
    right = 0
    max_length = 0
    
    while right < n:
        while right < n and nums[right] <= k * nums[left]:
            right += 1
        max_length = max(max_length, right - left)
        left += 1
    
    return n - max_length


Solution = create_solution(minimum_removals_to_balance_array)