"""
1832. 判断句子是否为全字母句 - 全字母句 指包含英语字母表中每个字母至少一次的句子。 给你一个仅由小写英文字母组成的字符串 sentence ，请你判断 sentence 是否为 全字母句 。 如果是，返回 true ；否则，返回 false 。 示例 1： 输入：sentence = "thequickbrownfoxjumpsoverthelazydog" 输出：true 解释：sentence 包含英语字母表中每个字母至少一次。 示例 2： 输入：sentence = "leetcode" 输出：false 提示： * 1 <= sentence.length <= 1000 * sentence 由小写英语字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来检查句子中是否包含所有26个英文字母。

算法步骤:
1. 将句子转换为集合，去除重复字符。
2. 检查集合的大小是否为26，如果是则说明句子是全字母句。

关键点:
- 使用集合可以高效地去重并检查字母的存在性。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是句子的长度。因为我们需要遍历整个句子来构建集合。
空间复杂度: O(1)，因为集合的最大大小为26（即字母表的大小），与输入大小无关。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_if_pangram(sentence: str) -> bool:
    """
    函数式接口 - 检查句子是否为全字母句
    """
    # 将句子转换为集合，去除重复字符
    unique_chars = set(sentence)
    
    # 检查集合的大小是否为26
    return len(unique_chars) == 26


Solution = create_solution(check_if_pangram)