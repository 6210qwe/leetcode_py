"""
2095. 删除链表的中间节点 - 给你一个链表的头节点 head 。删除 链表的 中间节点 ，并返回修改后的链表的头节点 head 。 长度为 n 链表的中间节点是从头数起第 ⌊n / 2⌋ 个节点（下标从 0 开始），其中 ⌊x⌋ 表示小于或等于 x 的最大整数。 * 对于 n = 1、2、3、4 和 5 的情况，中间节点的下标分别是 0、1、1、2 和 2 。 示例 1： [https://assets.leetcode.com/uploads/2021/11/16/eg1drawio.png] 输入：head = [1,3,4,7,1,2,6] 输出：[1,3,4,1,2,6] 解释： 上图表示给出的链表。节点的下标分别标注在每个节点的下方。 由于 n = 7 ，值为 7 的节点 3 是中间节点，用红色标注。 返回结果为移除节点后的新链表。 示例 2： [https://assets.leetcode.com/uploads/2021/11/16/eg2drawio.png] 输入：head = [1,2,3,4] 输出：[1,2,4] 解释： 上图表示给出的链表。 对于 n = 4 ，值为 3 的节点 2 是中间节点，用红色标注。 示例 3： [https://assets.leetcode.com/uploads/2021/11/16/eg3drawio.png] 输入：head = [2,1] 输出：[2] 解释： 上图表示给出的链表。 对于 n = 2 ，值为 1 的节点 1 是中间节点，用红色标注。 值为 2 的节点 0 是移除节点 1 后剩下的唯一一个节点。 提示： * 链表中节点的数目在范围 [1, 105] 内 * 1 <= Node.val <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用快慢指针来找到链表的中间节点，并删除该节点。

算法步骤:
1. 初始化两个指针 slow 和 fast，都指向头节点。
2. 快指针每次移动两步，慢指针每次移动一步。
3. 当快指针到达链表末尾时，慢指针正好位于中间节点。
4. 跳过慢指针指向的节点，完成删除操作。

关键点:
- 使用快慢指针可以有效地找到链表的中间节点。
- 通过跳过中间节点来实现删除操作。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是链表的长度。快慢指针遍历链表一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.solution import create_solution

def delete_middle(head: Optional[ListNode]) -> Optional[ListNode]:
    """
    删除链表的中间节点，并返回修改后的链表的头节点。
    """
    if not head or not head.next:
        return None

    # 初始化快慢指针
    slow = head
    fast = head
    prev = None

    # 快指针每次移动两步，慢指针每次移动一步
    while fast and fast.next:
        fast = fast.next.next
        prev = slow
        slow = slow.next

    # 跳过中间节点
    if prev:
        prev.next = slow.next

    return head

Solution = create_solution(delete_middle)