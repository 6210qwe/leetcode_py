# 问题描述
# ============================================================================
"""
1509. 三次操作后最大值与最小值的最小差 - 给你一个数组 nums 。 每次操作你可以选择 nums 中的任意一个元素并将它改成 任意值 。 在 执行最多三次移动后 ，返回 nums 中最大值与最小值的最小差值。 示例 1： 输入：nums = [5,3,2,4] 输出：0 解释：我们最多可以走 3 步。 第一步，将 2 变为 3 。 nums 变成 [5,3,3,4] 。 第二步，将 4 改为 3 。 nums 变成 [5,3,3,3] 。 第三步，将 5 改为 3 。 nums 变成 [3,3,3,3] 。 执行 3 次移动后，最小值和最大值之间的差值为 3 - 3 = 0 。 示例 2： 输入：nums = [1,5,0,10,14] 输出：1 解释：我们最多可以走 3 步。 第一步，将 5 改为 0 。 nums变成 [1,0,0,10,14] 。 第二步，将 10 改为 0 。 nums变成 [1,0,0,0,14] 。 第三步，将 14 改为 1 。 nums变成 [1,0,0,0,1] 。 执行 3 步后，最小值和最大值之间的差值为 1 - 0 = 1 。 可以看出，没有办法可以在 3 步内使差值变为0。 示例 3： 输入：nums = [3,100,20] 输出：0 解释：我们最多可以走 3 步。 第一步，将 100 改为 7 。 nums 变成 [3,7,20] 。 第二步，将 20 改为 7 。 nums 变成 [3,7,7] 。 第三步，将 3 改为 7 。 nums 变成 [7,7,7] 。 执行 3 步后，最小值和最大值之间的差值是 7 - 7 = 0。 提示： * 1 <= nums.length <= 105 * -109 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序找到最小的三个数和最大的三个数，然后计算在去掉这三个数的情况下，剩余数组的最大值和最小值的差值。

算法步骤:
1. 对数组进行排序。
2. 计算去掉前三个数后的最大值和最小值的差值。
3. 计算去掉后三个数后的最大值和最小值的差值。
4. 计算去掉前两个数和最后一个数后的最大值和最小值的差值。
5. 计算去掉第一个数和最后两个数后的最大值和最小值的差值。
6. 返回上述四种情况中的最小值。

关键点:
- 通过排序简化了问题，使得我们可以直接比较去掉特定位置的数后的结果。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，除了输入数组外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_difference(nums: List[int]) -> int:
    """
    函数式接口 - 返回三次操作后最大值与最小值的最小差值
    """
    if len(nums) <= 4:
        return 0
    
    nums.sort()
    n = len(nums)
    
    # 去掉前三个数
    diff1 = nums[-1] - nums[3]
    
    # 去掉后三个数
    diff2 = nums[n-4] - nums[0]
    
    # 去掉前两个数和最后一个数
    diff3 = nums[-2] - nums[2]
    
    # 去掉第一个数和最后两个数
    diff4 = nums[n-3] - nums[1]
    
    return min(diff1, diff2, diff3, diff4)


Solution = create_solution(min_difference)