"""
1963. 使字符串平衡的最小交换次数 - 给你一个字符串 s ，下标从 0 开始 ，且长度为偶数 n 。字符串 恰好 由 n / 2 个开括号 '[' 和 n / 2 个闭括号 ']' 组成。 只有能满足下述所有条件的字符串才能称为 平衡字符串 ： * 字符串是一个空字符串，或者 * 字符串可以记作 AB ，其中 A 和 B 都是 平衡字符串 ，或者 * 字符串可以写成 [C] ，其中 C 是一个 平衡字符串 。 你可以交换 任意 两个下标所对应的括号 任意 次数。 返回使 s 变成 平衡字符串 所需要的 最小 交换次数。 示例 1： 输入：s = "][][" 输出：1 解释：交换下标 0 和下标 3 对应的括号，可以使字符串变成平衡字符串。 最终字符串变成 "[[]]" 。 示例 2： 输入：s = "]]][[[" 输出：2 解释：执行下述操作可以使字符串变成平衡字符串： - 交换下标 0 和下标 4 对应的括号，s = "[]][][" 。 - 交换下标 1 和下标 5 对应的括号，s = "[[][]]" 。 最终字符串变成 "[[][]]" 。 示例 3： 输入：s = "[]" 输出：0 解释：这个字符串已经是平衡字符串。 提示： * n == s.length * 2 <= n <= 106 * n 为偶数 * s[i] 为'[' 或 ']' * 开括号 '[' 的数目为 n / 2 ，闭括号 ']' 的数目也是 n / 2
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，我们只需要关注未匹配的右括号数量。每次遇到一个左括号时，如果之前有未匹配的右括号，则进行一次交换。

算法步骤:
1. 初始化一个计数器 `balance` 来记录未匹配的右括号数量。
2. 初始化一个计数器 `swaps` 来记录交换次数。
3. 遍历字符串 `s`：
   - 如果当前字符是 `[`，则增加 `balance`。
   - 如果当前字符是 `]`，则减少 `balance`。
   - 如果 `balance` 小于 0，说明当前右括号没有匹配的左括号，需要进行一次交换，并将 `balance` 增加 2（因为交换后会多一个左括号）。
4. 返回 `swaps`。

关键点:
- 通过维护 `balance` 来判断是否需要交换。
- 交换次数可以通过 `balance` 的变化来计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 使字符串平衡的最小交换次数
    """
    balance = 0
    swaps = 0
    
    for char in s:
        if char == '[':
            balance += 1
        else:
            balance -= 1
        if balance < 0:
            swaps += 1
            balance += 2
    
    return swaps


Solution = create_solution(solution_function_name)