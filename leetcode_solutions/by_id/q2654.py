"""
2586. 统计范围内的元音字符串数 - 给你一个下标从 0 开始的字符串数组 words 和两个整数：left 和 right 。 如果字符串以元音字母开头并以元音字母结尾，那么该字符串就是一个 元音字符串 ，其中元音字母是 'a'、'e'、'i'、'o'、'u' 。 返回 words[i] 是元音字符串的数目，其中 i 在闭区间 [left, right] 内。 示例 1： 输入：words = ["are","amy","u"], left = 0, right = 2 输出：2 解释： - "are" 是一个元音字符串，因为它以 'a' 开头并以 'e' 结尾。 - "amy" 不是元音字符串，因为它没有以元音字母结尾。 - "u" 是一个元音字符串，因为它以 'u' 开头并以 'u' 结尾。 在上述范围中的元音字符串数目为 2 。 示例 2： 输入：words = ["hey","aeo","mu","ooo","artro"], left = 1, right = 4 输出：3 解释： - "aeo" 是一个元音字符串，因为它以 'a' 开头并以 'o' 结尾。 - "mu" 不是元音字符串，因为它没有以元音字母开头。 - "ooo" 是一个元音字符串，因为它以 'o' 开头并以 'o' 结尾。 - "artro" 是一个元音字符串，因为它以 'a' 开头并以 'o' 结尾。 在上述范围中的元音字符串数目为 3 。 提示： * 1 <= words.length <= 1000 * 1 <= words[i].length <= 10 * words[i] 仅由小写英文字母组成 * 0 <= left <= right < words.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历指定范围内的字符串，检查每个字符串是否以元音字母开头和结尾。

算法步骤:
1. 定义一个包含所有元音字母的集合。
2. 初始化一个计数器，用于记录元音字符串的数量。
3. 遍历指定范围内的每个字符串。
4. 检查当前字符串是否以元音字母开头和结尾。
5. 如果是，则增加计数器。
6. 返回计数器的值。

关键点:
- 使用集合来快速检查字符是否为元音字母。
- 只遍历指定范围内的字符串，避免不必要的计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - n 为 right - left + 1，即指定范围内的字符串数量。
空间复杂度: O(1) - 仅使用常数级额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def count_vowel_strings(words: List[str], left: int, right: int) -> int:
    """
    函数式接口 - 统计指定范围内的元音字符串数量
    """
    vowels = set('aeiou')
    count = 0
    for i in range(left, right + 1):
        word = words[i]
        if word[0] in vowels and word[-1] in vowels:
            count += 1
    return count

