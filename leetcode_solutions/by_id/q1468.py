"""
1346. 检查整数及其两倍数是否存在 - 给你一个整数数组 arr，请你检查是否存在两个整数 N 和 M，满足 N 是 M 的两倍（即，N = 2 * M）。 更正式地，检查是否存在两个下标 i 和 j 满足： * i != j * 0 <= i, j < arr.length * arr[i] == 2 * arr[j] 示例 1： 输入：arr = [10,2,5,3] 输出：true 解释：N = 10 是 M = 5 的两倍，即 10 = 2 * 5 。 示例 2： 输入：arr = [7,1,14,11] 输出：true 解释：N = 14 是 M = 7 的两倍，即 14 = 2 * 7 。 示例 3： 输入：arr = [3,1,7,11] 输出：false 解释：在该情况下不存在 N 和 M 满足 N = 2 * M 。 提示： * 2 <= arr.length <= 500 * -10^3 <= arr[i] <= 10^3
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表来存储数组中的元素及其索引，然后遍历数组检查是否存在某个元素的两倍也在数组中。

算法步骤:
1. 初始化一个空的哈希表。
2. 遍历数组，将每个元素及其索引存入哈希表。
3. 再次遍历数组，对于每个元素，检查其两倍是否存在于哈希表中，并且确保不是同一个元素。

关键点:
- 使用哈希表可以在 O(1) 时间内检查元素是否存在。
- 需要确保找到的两倍元素不是同一个元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组两次，每次遍历的时间复杂度是 O(n)。
空间复杂度: O(n)，哈希表的空间复杂度是 O(n)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_if_exist(arr: List[int]) -> bool:
    """
    函数式接口 - 检查整数及其两倍数是否存在
    """
    # 使用哈希表存储数组中的元素及其索引
    num_map = {}
    for i, num in enumerate(arr):
        num_map[num] = i
    
    # 遍历数组，检查是否存在某个元素的两倍
    for i, num in enumerate(arr):
        if 2 * num in num_map and num_map[2 * num] != i:
            return True
    return False


Solution = create_solution(check_if_exist)