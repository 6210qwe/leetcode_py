"""
2895. 最小处理时间 - 你有 n 颗处理器，每颗处理器都有 4 个核心。现有 n * 4 个待执行任务，每个核心只执行 一次 任务。 给你一个下标从 0 开始的整数数组 processorTime ，表示每颗处理器最早空闲时间。另给你一个下标从 0 开始的整数数组 tasks ，表示执行每个任务所需的时间。返回所有任务都执行完毕需要的 最小时间 。 注意：每个核心独立执行任务。 示例 1： 输入：processorTime = [8,10], tasks = [2,2,3,1,8,7,4,5] 输出：16 解释： 最优的方案是将下标为 4, 5, 6, 7 的任务分配给第一颗处理器（最早空闲时间 time = 8），下标为 0, 1, 2, 3 的任务分配给第二颗处理器（最早空闲时间 time = 10）。 第一颗处理器执行完所有任务需要花费的时间 = max(8 + 8, 8 + 7, 8 + 4, 8 + 5) = 16 。 第二颗处理器执行完所有任务需要花费的时间 = max(10 + 2, 10 + 2, 10 + 3, 10 + 1) = 13 。 因此，可以证明执行完所有任务需要花费的最小时间是 16 。 示例 2： 输入：processorTime = [10,20], tasks = [2,3,1,2,5,8,4,3] 输出：23 解释： 最优的方案是将下标为 1, 4, 5, 6 的任务分配给第一颗处理器（最早空闲时间 time = 10），下标为 0, 2, 3, 7 的任务分配给第二颗处理器（最早空闲时间 time = 20）。 第一颗处理器执行完所有任务需要花费的时间 = max(10 + 3, 10 + 5, 10 + 8, 10 + 4) = 18 。 第二颗处理器执行完所有任务需要花费的时间 = max(20 + 2, 20 + 1, 20 + 2, 20 + 3) = 23 。 因此，可以证明执行完所有任务需要花费的最小时间是 23 。 提示： * 1 <= n == processorTime.length <= 25000 * 1 <= tasks.length <= 105 * 0 <= processorTime[i] <= 109 * 1 <= tasks[i] <= 109 * tasks.length == 4 * n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，将任务分配给处理器，使得总处理时间最小。

算法步骤:
1. 将处理器按最早空闲时间升序排序。
2. 将任务按所需时间降序排序。
3. 按顺序将任务分配给处理器的核心，确保每个处理器的核心都尽可能处理较长时间的任务。
4. 计算每个处理器完成所有任务的最大时间，并取这些时间的最大值作为最终结果。

关键点:
- 通过排序和贪心策略，确保每个处理器的核心都能处理较长时间的任务，从而最小化总处理时间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + m log m)，其中 n 是处理器的数量，m 是任务的数量。排序操作的时间复杂度为 O(n log n) 和 O(m log m)。
空间复杂度: O(1)，除了输入和输出外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_processing_time(processor_time: List[int], tasks: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 将处理器按最早空闲时间升序排序
    processor_time.sort()
    
    # 将任务按所需时间降序排序
    tasks.sort(reverse=True)
    
    # 计算每个处理器完成所有任务的最大时间
    max_time = 0
    for i in range(len(processor_time)):
        # 每个处理器处理 4 个任务
        start_index = i * 4
        end_index = start_index + 4
        current_tasks = tasks[start_index:end_index]
        max_time = max(max_time, processor_time[i] + max(current_tasks))
    
    return max_time


Solution = create_solution(minimum_processing_time)