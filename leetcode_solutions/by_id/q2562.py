"""
2466. 统计构造好字符串的方案数 - 给你整数 zero ，one ，low 和 high ，我们从空字符串开始构造一个字符串，每一步执行下面操作中的一种：
* 将 '0' 在字符串末尾添加 zero 次。
* 将 '1' 在字符串末尾添加 one 次。
以上操作可以执行任意次。如果通过以上过程得到一个 长度 在 low 和 high 之间（包含上下边界）的字符串，那么这个字符串我们称为 好 字符串。
请你返回满足以上要求的 不同 好字符串数目。由于答案可能很大，请将结果对 109 + 7 取余 后返回。

示例 1：
输入：low = 3, high = 3, zero = 1, one = 1
输出：8
解释：
一个可能的好字符串是 "011" 。
可以这样构造得到："\" -> "0" -> "01" -> "011" 。
从 "000" 到 "111" 之间所有的二进制字符串都是好字符串。

示例 2：
输入：low = 2, high = 3, zero = 1, one = 2
输出：5
解释：
好字符串为 "00" ，"11" ，"000" ，"110" 和 "011" 。

提示：
* 1 <= low <= high <= 105
* 1 <= zero, one <= low
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算长度在 [low, high] 范围内的好字符串数量。

算法步骤:
1. 初始化一个长度为 high+1 的 dp 数组，dp[i] 表示长度为 i 的好字符串的数量。
2. 设置 dp[0] = 1，表示空字符串的数量为 1。
3. 从 1 到 high 遍历，对于每个 i，如果 i >= zero，则 dp[i] += dp[i-zero]；如果 i >= one，则 dp[i] += dp[i-one]。
4. 最后，累加 dp[low] 到 dp[high] 的值，并对 10^9 + 7 取余。

关键点:
- 使用动态规划避免重复计算。
- 对结果取模以防止溢出。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(high)
空间复杂度: O(high)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_good_strings(low: int, high: int, zero: int, one: int) -> int:
    """
    函数式接口 - 计算长度在 [low, high] 范围内的好字符串数量
    """
    MOD = 10**9 + 7
    dp = [0] * (high + 1)
    dp[0] = 1  # 空字符串的数量为 1

    for i in range(1, high + 1):
        if i >= zero:
            dp[i] += dp[i - zero]
        if i >= one:
            dp[i] += dp[i - one]
        dp[i] %= MOD

    return sum(dp[low:high + 1]) % MOD


Solution = create_solution(count_good_strings)