"""
2656. K 个元素的最大和 - 给你一个下标从 0 开始的整数数组 nums 和一个整数 k 。你需要执行以下操作 恰好 k 次，最大化你的得分： 1. 从 nums 中选择一个元素 m 。 2. 将选中的元素 m 从数组中删除。 3. 将新元素 m + 1 添加到数组中。 4. 你的得分增加 m 。 请你返回执行以上操作恰好 k 次后的最大得分。 示例 1： 输入：nums = [1,2,3,4,5], k = 3 输出：18 解释：我们需要从 nums 中恰好选择 3 个元素并最大化得分。 第一次选择 5 。和为 5 ，nums = [1,2,3,4,6] 。 第二次选择 6 。和为 6 ，nums = [1,2,3,4,7] 。 第三次选择 7 。和为 5 + 6 + 7 = 18 ，nums = [1,2,3,4,8] 。 所以我们返回 18 。 18 是可以得到的最大答案。 示例 2： 输入：nums = [5,5,5], k = 2 输出：11 解释：我们需要从 nums 中恰好选择 2 个元素并最大化得分。 第一次选择 5 。和为 5 ，nums = [5,5,6] 。 第二次选择 6 。和为 6 ，nums = [5,5,7] 。 所以我们返回 11 。 11 是可以得到的最大答案。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100 * 1 <= k <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 为了最大化得分，每次选择当前数组中的最大值，并将其加一后放回数组。由于每次选择的都是最大值，因此可以直接计算出总得分。

算法步骤:
1. 找到数组中的最大值。
2. 计算初始得分。
3. 通过公式计算后续的得分增量。
4. 返回总得分。

关键点:
- 选择最大值并更新数组。
- 使用数学公式简化计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组找到最大值。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_sum_with_k_elements(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算执行 k 次操作后的最大得分
    """
    # 找到数组中的最大值
    max_val = max(nums)
    
    # 初始得分
    initial_score = max_val
    
    # 计算后续的得分增量
    for i in range(1, k):
        max_val += 1
        initial_score += max_val
    
    return initial_score


Solution = create_solution(max_sum_with_k_elements)