"""
1534. 统计好三元组 - 给你一个整数数组 arr ，以及 a、b 、c 三个整数。请你统计其中好三元组的数量。 如果三元组 (arr[i], arr[j], arr[k]) 满足下列全部条件，则认为它是一个 好三元组 。 * 0 <= i < j < k < arr.length * |arr[i] - arr[j]| <= a * |arr[j] - arr[k]| <= b * |arr[i] - arr[k]| <= c 其中 |x| 表示 x 的绝对值。 返回 好三元组的数量 。 示例 1： 输入：arr = [3,0,1,1,9,7], a = 7, b = 2, c = 3 输出：4 解释：一共有 4 个好三元组：[(3,0,1), (3,0,1), (3,1,1), (0,1,1)] 。 示例 2： 输入：arr = [1,1,2,2,3], a = 0, b = 0, c = 1 输出：0 解释：不存在满足所有条件的三元组。 提示： * 3 <= arr.length <= 100 * 0 <= arr[i] <= 1000 * 0 <= a, b, c <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过三重循环枚举所有可能的三元组，并检查它们是否满足给定的条件。

算法步骤:
1. 初始化计数器 count 为 0。
2. 使用三重循环枚举所有可能的三元组 (i, j, k)。
3. 对于每个三元组，检查是否满足 |arr[i] - arr[j]| <= a, |arr[j] - arr[k]| <= b, |arr[i] - arr[k]| <= c。
4. 如果满足条件，将计数器 count 加 1。
5. 返回计数器 count 的值。

关键点:
- 三重循环枚举所有可能的三元组。
- 条件判断确保三元组满足给定的约束。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3)，其中 n 是数组 arr 的长度。需要三重循环枚举所有可能的三元组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int], a: int, b: int, c: int) -> int:
    """
    函数式接口 - 统计好三元组的数量
    """
    count = 0
    n = len(arr)
    for i in range(n):
        for j in range(i + 1, n):
            for k in range(j + 1, n):
                if abs(arr[i] - arr[j]) <= a and abs(arr[j] - arr[k]) <= b and abs(arr[i] - arr[k]) <= c:
                    count += 1
    return count


Solution = create_solution(solution_function_name)