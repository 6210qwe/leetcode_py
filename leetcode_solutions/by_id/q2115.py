"""
1987. 不同的好子序列数目 - 给你一个二进制字符串 binary 。 binary 的一个 子序列 如果是 非空 的且没有 前导 0 （除非数字是 "0" 本身），那么它就是一个 好 的子序列。 请你找到 binary 不同好子序列 的数目。 * 比方说，如果 binary = "001" ，那么所有 好 子序列为 ["0", "0", "1"] ，所以 不同 的好子序列为 "0" 和 "1" 。 注意，子序列 "00" ，"01" 和 "001" 不是好的，因为它们有前导 0 。 请你返回 binary 中 不同好子序列 的数目。由于答案可能很大，请将它对 109 + 7 取余 后返回。 一个 子序列 指的是从原数组中删除若干个（可以一个也不删除）元素后，不改变剩余元素顺序得到的序列。 示例 1： 输入：binary = "001" 输出：2 解释：好的二进制子序列为 ["0", "0", "1"] 。 不同的好子序列为 "0" 和 "1" 。 示例 2： 输入：binary = "11" 输出：2 解释：好的二进制子序列为 ["1", "1", "11"] 。 不同的好子序列为 "1" 和 "11" 。 示例 3： 输入：binary = "101" 输出：5 解释：好的二进制子序列为 ["1", "0", "1", "10", "11", "101"] 。 不同的好子序列为 "0" ，"1" ，"10" ，"11" 和 "101" 。 提示： * 1 <= binary.length <= 105 * binary 只含有 '0' 和 '1' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算不同的好子序列的数目。

算法步骤:
1. 初始化两个变量 `end_with_0` 和 `end_with_1`，分别表示以 '0' 结尾和以 '1' 结尾的不同好子序列的数目。
2. 遍历字符串 `binary`，根据当前字符更新 `end_with_0` 和 `end_with_1`。
3. 最终结果是 `end_with_0` 和 `end_with_1` 的总和，加上单独的 '0' 子序列（如果存在）。

关键点:
- 动态规划的状态转移方程：
  - 如果当前字符是 '1'，则 `end_with_1` 更新为 `(end_with_0 + end_with_1 + 1) % MOD`。
  - 如果当前字符是 '0'，则 `end_with_0` 更新为 `(end_with_0 + end_with_1) % MOD`。
- 最终结果需要考虑单独的 '0' 子序列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 `binary` 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

MOD = 10**9 + 7

def solution_function_name(binary: str) -> int:
    """
    函数式接口 - 计算不同的好子序列的数目
    """
    end_with_0 = 0
    end_with_1 = 0
    has_zero = False
    
    for char in binary:
        if char == '1':
            end_with_1 = (end_with_0 + end_with_1 + 1) % MOD
        else:
            end_with_0 = (end_with_0 + end_with_1) % MOD
            has_zero = True
    
    return (end_with_0 + end_with_1 + (1 if has_zero else 0)) % MOD

Solution = create_solution(solution_function_name)