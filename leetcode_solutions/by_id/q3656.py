"""
3396. 使数组元素互不相同所需的最少操作次数 - 给你一个整数数组 nums，你需要确保数组中的元素 互不相同 。为此，你可以执行以下操作任意次： * 从数组的开头移除 3 个元素。如果数组中元素少于 3 个，则移除所有剩余元素。 注意：空数组也视作为数组元素互不相同。返回使数组元素互不相同所需的 最少操作次数 。 示例 1： 输入： nums = [1,2,3,4,2,3,3,5,7] 输出： 2 解释： * 第一次操作：移除前 3 个元素，数组变为 [4, 2, 3, 3, 5, 7]。 * 第二次操作：再次移除前 3 个元素，数组变为 [3, 5, 7]，此时数组中的元素互不相同。 因此，答案是 2。 示例 2： 输入： nums = [4,5,6,4,4] 输出： 2 解释： * 第一次操作：移除前 3 个元素，数组变为 [4, 4]。 * 第二次操作：移除所有剩余元素，数组变为空。 因此，答案是 2。 示例 3： 输入： nums = [6,7,8,9] 输出： 0 解释： 数组中的元素已经互不相同，因此不需要进行任何操作，答案是 0。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个元素的出现次数，并计算需要移除的元素数量。

算法步骤:
1. 使用哈希表统计每个元素的出现次数。
2. 计算需要移除的元素数量，每次移除 3 个元素，直到所有重复元素都被移除。
3. 返回需要的操作次数。

关键点:
- 使用哈希表统计元素频率。
- 每次移除 3 个元素，直到所有重复元素都被移除。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们需要遍历数组来统计元素频率。
空间复杂度: O(n)，哈希表在最坏情况下需要存储所有元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_make_distinct(nums: List[int]) -> int:
    """
    函数式接口 - 使数组元素互不相同所需的最少操作次数
    """
    # 统计每个元素的出现次数
    count = {}
    for num in nums:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1

    # 计算需要移除的元素数量
    operations = 0
    for freq in count.values():
        if freq > 1:
            # 每次移除 3 个元素
            operations += (freq - 1 + 2) // 3

    return operations


Solution = create_solution(min_operations_to_make_distinct)