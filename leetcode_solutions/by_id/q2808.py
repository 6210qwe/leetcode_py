# ============================================================================
# 问题描述
# ============================================================================
"""
2742. 给墙壁刷油漆 - 给你两个长度为 n 下标从 0 开始的整数数组 cost 和 time ，分别表示给 n 堵不同的墙刷油漆需要的开销和时间。你有两名油漆匠： * 一位需要 付费 的油漆匠，刷第 i 堵墙需要花费 time[i] 单位的时间，开销为 cost[i] 单位的钱。 * 一位 免费 的油漆匠，刷 任意 一堵墙的时间为 1 单位，开销为 0 。但是必须在付费油漆匠 工作 时，免费油漆匠才会工作。 请你返回刷完 n 堵墙最少开销为多少。 示例 1： 输入：cost = [1,2,3,2], time = [1,2,3,2] 输出：3 解释：下标为 0 和 1 的墙由付费油漆匠来刷，需要 3 单位时间。同时，免费油漆匠刷下标为 2 和 3 的墙，需要 2 单位时间，开销为 0 。总开销为 1 + 2 = 3 。 示例 2： 输入：cost = [2,3,4,2], time = [1,1,1,1] 输出：4 解释：下标为 0 和 3 的墙由付费油漆匠来刷，需要 2 单位时间。同时，免费油漆匠刷下标为 1 和 2 的墙，需要 2 单位时间，开销为 0 。总开销为 2 + 2 = 4 。 提示： * 1 <= cost.length <= 500 * cost.length == time.length * 1 <= cost[i] <= 106 * 1 <= time[i] <= 500
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划解决这个问题。定义 dp(i, j) 表示从第 i 堵墙开始，剩余 j 单位时间的最小开销。状态转移方程为：
- 如果选择付费油漆匠刷第 i 堵墙，则 dp(i, j) = cost[i] + dp(i+1, j + time[i])
- 如果选择免费油漆匠刷第 i 堵墙，则 dp(i, j) = dp(i+1, j - 1)

算法步骤:
1. 初始化 dp 数组，dp[i][j] 表示从第 i 堵墙开始，剩余 j 单位时间的最小开销。
2. 从后往前遍历每一堵墙，更新 dp 数组。
3. 返回 dp[0][0]，即从第 0 堵墙开始，剩余 0 单位时间的最小开销。

关键点:
- 使用二维 DP 数组来存储中间结果，避免重复计算。
- 注意边界条件，当 j < 0 时，表示没有足够的时间让免费油漆匠工作，此时 dp[i][j] 应该是无穷大。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * (n + max(time)))，其中 n 是墙的数量，max(time) 是时间的最大值。
空间复杂度: O(n * (n + max(time)))，用于存储 DP 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(cost: List[int], time: List[int]) -> int:
    """
    函数式接口 - 使用动态规划求解刷墙的最小开销
    """
    n = len(cost)
    max_time = sum(time)
    dp = [[float('inf')] * (max_time + 1) for _ in range(n + 1)]

    # 初始化 dp 数组
    for j in range(max_time + 1):
        dp[n][j] = 0

    # 动态规划
    for i in range(n - 1, -1, -1):
        for j in range(max_time + 1):
            if j - 1 >= 0:
                dp[i][j] = min(dp[i][j], dp[i + 1][j - 1])
            dp[i][j] = min(dp[i][j], cost[i] + dp[i + 1][j + time[i]])

    return dp[0][0]


Solution = create_solution(solution_function_name)