"""
2798. 满足目标工作时长的员工数目 - 公司里共有 n 名员工，按从 0 到 n - 1 编号。每个员工 i 已经在公司工作了 hours[i] 小时。 公司要求每位员工工作 至少 target 小时。 给你一个下标从 0 开始、长度为 n 的非负整数数组 hours 和一个非负整数 target 。 请你用整数表示并返回工作至少 target 小时的员工数。 示例 1： 输入：hours = [0,1,2,3,4], target = 2 输出：3 解释：公司要求每位员工工作至少 2 小时。 - 员工 0 工作 0 小时，不满足要求。 - 员工 1 工作 1 小时，不满足要求。 - 员工 2 工作 2 小时，满足要求。 - 员工 3 工作 3 小时，满足要求。 - 员工 4 工作 4 小时，满足要求。 共有 3 位满足要求的员工。 示例 2： 输入：hours = [5,1,4,2,2], target = 6 输出：0 解释：公司要求每位员工工作至少 6 小时。 共有 0 位满足要求的员工。 提示： * 1 <= n == hours.length <= 50 * 0 <= hours[i], target <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，统计满足条件的员工数量。

算法步骤:
1. 初始化一个计数器 `count` 为 0。
2. 遍历 `hours` 数组，对于每个元素 `hour`，如果 `hour` 大于等于 `target`，则将 `count` 加 1。
3. 返回 `count`。

关键点:
- 通过一次遍历数组来统计满足条件的员工数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 `hours` 数组的长度。我们只需要遍历一次数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(hours: List[int], target: int) -> int:
    """
    函数式接口 - 统计满足目标工作时长的员工数量
    """
    count = 0
    for hour in hours:
        if hour >= target:
            count += 1
    return count


Solution = create_solution(solution_function_name)