"""
2910. 合法分组的最少组数 - 给你一组带编号的 balls 并要求将它们分类到盒子里，以便均衡地分配。你必须遵守两条规则： * 同一个盒子里的球必须具有相同的编号。但是，如果你有多个相同编号的球，你可以把它们放在不同的盒子里。 * 最大的盒子只能比最小的盒子多一个球。 返回遵循上述规则排列这些球所需要的盒子的最小数目。 示例 1： 输入：balls = [3,2,3,2,3] 输出：2 解释：一个得到 2 个分组的方案如下，中括号内的数字都是下标： 我们可以如下排列 balls 到盒子里： - [3,3,3] - [2,2] 两个盒子之间的大小差没有超过 1。 示例 2： 输入：balls = [10,10,10,3,1,1] 输出：4 解释：我们可以如下排列 balls 到盒子里： - [10] - [10,10] - [3] - [1,1] 无法得到一个遵循上述规则且小于 4 盒的答案。例如，把所有三个编号为 10 的球都放在一个盒子里，就会打破盒子之间最大尺寸差异的规则。 提示： * 1 <= balls.length <= 105 * 1 <= balls[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法来找到最小的盒子数量，使得每个盒子中的球数最多相差1。

算法步骤:
1. 统计每个编号的球的数量。
2. 找到最小的球的数量 `min_count`。
3. 尝试从 `min_count` 开始递减，直到找到一个合适的 `k`，使得所有球可以被分成大小为 `k` 或 `k+1` 的盒子。
4. 计算所需的盒子总数。

关键点:
- 通过贪心算法找到最小的 `k`，使得所有球可以被分成大小为 `k` 或 `k+1` 的盒子。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m^2)，其中 n 是 balls 的长度，m 是不同编号的数量。
空间复杂度: O(m)，其中 m 是不同编号的数量。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import Counter

def min_groups_to_create_valid_assignment(balls: List[int]) -> int:
    """
    函数式接口 - 计算合法分组的最少组数
    """
    # 统计每个编号的球的数量
    count = Counter(balls)
    # 获取所有编号的球的数量列表
    counts = list(count.values())
    # 找到最小的球的数量
    min_count = min(counts)
    
    # 尝试从 min_count 开始递减，直到找到一个合适的 k
    for k in range(min_count, 0, -1):
        total_groups = 0
        valid = True
        for c in counts:
            if (c - 1) // k > c // (k + 1):
                valid = False
                break
            total_groups += (c + k) // (k + 1)
        if valid:
            return total_groups

Solution = min_groups_to_create_valid_assignment