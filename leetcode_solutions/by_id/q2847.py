"""
2744. 最大字符串配对数目 - 给你一个下标从 0 开始的数组 words ，数组中包含 互不相同 的字符串。 如果字符串 words[i] 与字符串 words[j] 满足以下条件，我们称它们可以匹配： * 字符串 words[i] 等于 words[j] 的反转字符串。 * 0 <= i < j < words.length 请你返回数组 words 中的 最大 匹配数目。 注意，每个字符串最多匹配一次。 示例 1： 输入：words = ["cd","ac","dc","ca","zz"] 输出：2 解释：在此示例中，我们可以通过以下方式匹配 2 对字符串： - 我们将第 0 个字符串与第 2 个字符串匹配，因为 word[0] 的反转字符串是 "dc" 并且等于 words[2]。 - 我们将第 1 个字符串与第 3 个字符串匹配，因为 word[1] 的反转字符串是 "ca" 并且等于 words[3]。 可以证明最多匹配数目是 2 。 示例 2： 输入：words = ["ab","ba","cc"] 输出：1 解释：在此示例中，我们可以通过以下方式匹配 1 对字符串： - 我们将第 0 个字符串与第 1 个字符串匹配，因为 words[1] 的反转字符串 "ab" 与 words[0] 相等。 可以证明最多匹配数目是 1 。 示例 3： 输入：words = ["aa","ab"] 输出：0 解释：这个例子中，无法匹配任何字符串。 提示： * 1 <= words.length <= 50 * words[i].length == 2 * words 包含的字符串互不相同。 * words[i] 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个字符串及其反转字符串的出现情况，从而快速查找匹配对。

算法步骤:
1. 初始化一个哈希表 `seen` 和一个计数器 `count`。
2. 遍历 `words` 数组，对于每个字符串 `word`：
   - 计算其反转字符串 `reversed_word`。
   - 如果 `reversed_word` 在 `seen` 中，则增加计数器 `count`。
   - 将 `word` 添加到 `seen` 中。
3. 返回计数器 `count` 的值。

关键点:
- 使用哈希表来存储已遍历过的字符串，以便快速查找其反转字符串。
- 每个字符串最多匹配一次，因此只需在找到匹配时增加计数器。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 `words` 的长度。每个字符串的处理时间是常数级别的。
空间复杂度: O(n)，最坏情况下，哈希表 `seen` 需要存储所有的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(words: List[str]) -> int:
    """
    函数式接口 - 返回数组 words 中的最大匹配数目
    """
    seen = set()
    count = 0
    
    for word in words:
        reversed_word = word[::-1]
        if reversed_word in seen:
            count += 1
        seen.add(word)
    
    return count


Solution = create_solution(solution_function_name)