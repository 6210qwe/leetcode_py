def exchange_bits(num: int) -> int:
    """
    交换整数的奇数位和偶数位。
    
    :param num: 输入整数
    :return: 交换后的整数
    """
    # 掩码 0xAAAAAAAA 用于提取偶数位，0x55555555 用于提取奇数位
    even_mask = 0xAAAAAAAA
    odd_mask = 0x55555555
    
    # 提取偶数位并左移一位
    even_bits = (num & even_mask) >> 1
    # 提取奇数位并右移一位
    odd_bits = (num & odd_mask) << 1
    
    # 合并处理后的奇数位和偶数位
    result = even_bits | odd_bits
    return result


Solution = create_solution(exchange_bits)