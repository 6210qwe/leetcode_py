"""
核心思想: 动态规划

算法步骤:
1. 初始化 dp 数组，dp[i] 表示前 i 个字符的解码方法数。
2. 处理单个字符的情况：
   - 如果字符是 '*', 它可以表示 1-9 中的任意一个数字，因此有 9 种解码方法。
   - 如果字符是 '0', 无法解码，直接返回 0。
   - 如果字符是其他数字，只有 1 种解码方法。
3. 处理两个字符的情况：
   - 如果第一个字符是 '*', 第二个字符是 '0'，只能表示 10 或 20，有 2 种解码方法。
   - 如果第一个字符是 '*', 第二个字符是 1-6，可以表示 11-16 或 21-26，有 2 种解码方法。
   - 如果第一个字符是 '*', 第二个字符是 7-9，只能表示 17-19，有 1 种解码方法。
   - 如果第一个字符是 1，第二个字符是 '*', 可以表示 11-19，有 9 种解码方法。
   - 如果第一个字符是 2，第二个字符是 '*', 可以表示 21-26，有 6 种解码方法。
   - 如果第一个字符是 1 或 2，第二个字符是 0-9，根据是否在 10-26 范围内决定解码方法数。
4. 更新 dp 数组，最后返回 dp[n]。

关键点:
- 使用动态规划来处理字符串的解码方法数。
- 处理 '*' 字符的各种情况。
"""

