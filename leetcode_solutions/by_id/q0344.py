"""
核心思想: 双指针，交换首尾字符

算法步骤:
1. 使用双指针left和right
2. 交换s[left]和s[right]
3. left++，right--

关键点:
- 双指针
- 时间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
