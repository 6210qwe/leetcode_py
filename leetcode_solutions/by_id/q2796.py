from typing import Callable, Any

def once(fn: Callable) -> Callable:
    called = False
    result = None
    
    def once_fn(*args, **kwargs):
        nonlocal called, result
        if not called:
            result = fn(*args, **kwargs)
            called = True
        return result if called else None
    
    return once_fn

