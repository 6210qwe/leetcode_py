"""
面试题 08.12. 八皇后 - 设计一种算法，打印 N 皇后在 N × N 棋盘上的各种摆法，其中每个皇后都不同行、不同列，也不在对角线上。这里的“对角线”指的是所有的对角线，不只是平分整个棋盘的那两条对角线。 注意：本题相对原题做了扩展 示例： 输入：4 输出：[[".Q..","...Q","Q...","..Q."],["..Q.","Q...","...Q",".Q.."]] 解释：4 皇后问题存在如下两个不同的解法。 [ [".Q..", // 解法 1 "...Q", "Q...", "..Q."], ["..Q.", // 解法 2 "Q...", "...Q", ".Q.."] ]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用回溯法来解决八皇后问题。

算法步骤:
1. 初始化一个空的结果列表和一个空的棋盘。
2. 定义一个递归函数 `backtrack`，该函数接受当前行作为参数。
3. 在递归函数中，遍历当前行的所有列，尝试放置皇后。
4. 如果当前列可以放置皇后（即不在同一列、不在对角线上），则放置皇后并继续递归到下一行。
5. 如果所有行都成功放置了皇后，则将当前棋盘状态加入结果列表。
6. 回溯时移除当前行的皇后，继续尝试下一列。
7. 从第 0 行开始调用递归函数。
8. 返回结果列表。

关键点:
- 使用三个集合来记录已经放置的列、主对角线和副对角线。
- 通过递归和回溯来尝试所有可能的放置方式。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(N!)
空间复杂度: O(N)
"""

# ============================================================================
