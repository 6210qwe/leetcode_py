"""
核心思想: 使用正则表达式来匹配有效的电子邮件地址。

算法步骤:
1. 定义一个正则表达式来匹配有效的电子邮件地址。
2. 使用 pandas 库读取输入数据。
3. 使用正则表达式过滤出有效的电子邮件地址。
4. 返回过滤后的数据。

关键点:
- 正则表达式的定义要符合题目要求的有效电子邮件格式。
"""

# ============================================================================
# 复杂度分析
