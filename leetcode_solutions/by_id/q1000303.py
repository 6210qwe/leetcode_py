"""
LCR 110. 所有可能的路径 - 给定一个有 n 个节点的有向无环图，用二维数组 graph 表示，请找到所有从 0 到 n-1 的路径并输出（不要求按顺序）。 graph 的第 i 个数组中的单元都表示有向图中 i 号节点所能到达的下一些结点（译者注：有向图是有方向的，即规定了 a→b 你就不能从 b→a ），若为空，就是没有下一个节点了。 示例 1： [https://assets.leetcode.com/uploads/2020/09/28/all_1.jpg] 输入：graph = [[1,2],[3],[3],[]] 输出：[[0,1,3],[0,2,3]] 解释：有两条路径 0 -> 1 -> 3 和 0 -> 2 -> 3 示例 2： [https://assets.leetcode.com/uploads/2020/09/28/all_2.jpg] 输入：graph = [[4,3,1],[3,2,4],[3],[4],[]] 输出：[[0,4],[0,3,4],[0,1,3,4],[0,1,2,3,4],[0,1,4]] 示例 3： 输入：graph = [[1],[]] 输出：[[0,1]] 示例 4： 输入：graph = [[1,2,3],[2],[3],[]] 输出：[[0,1,2,3],[0,2,3],[0,3]] 示例 5： 输入：graph = [[1,3],[2],[3],[]] 输出：[[0,1,2,3],[0,3]] 提示： * n == graph.length * 2 <= n <= 15 * 0 <= graph[i][j] < n * graph[i][j] != i * 保证输入为有向无环图 (GAD) 注意：本题与主站 797 题相同：https://leetcode.cn/problems/all-paths-from-source-to-target/ [https://leetcode.cn/problems/all-paths-from-source-to-target/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 来遍历图，并记录从起点到终点的所有路径。

算法步骤:
1. 定义一个递归函数 `dfs`，用于进行深度优先搜索。
2. 从起点 0 开始，初始化路径列表 `path`，并调用 `dfs` 函数。
3. 在 `dfs` 函数中，如果当前节点是终点 n-1，则将当前路径加入结果列表 `paths`。
4. 否则，遍历当前节点的所有邻接节点，继续进行深度优先搜索。
5. 返回结果列表 `paths`。

关键点:
- 使用递归进行深度优先搜索。
- 通过路径列表 `path` 记录当前路径。
- 当到达终点时，将当前路径加入结果列表。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(2^n)，在最坏情况下，每个节点都有两个选择，因此可能的路径数最多为 2^n。
空间复杂度: O(n)，递归调用栈的深度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def all_paths_source_target(graph: List[List[int]]) -> List[List[int]]:
    """
    函数式接口 - 找到所有从 0 到 n-1 的路径并输出
    """
    def dfs(node: int, path: List[int]):
        if node == len(graph) - 1:
            paths.append(path[:])
            return
        for next_node in graph[node]:
            path.append(next_node)
            dfs(next_node, path)
            path.pop()

    paths = []
    dfs(0, [0])
    return paths


Solution = create_solution(all_paths_source_target)