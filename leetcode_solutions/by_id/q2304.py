"""
2194. Excel 表中某个范围内的单元格 - Excel 表中的一个单元格 (r, c) 会以字符串 "<col><row>" 的形式进行表示，其中： * <col> 即单元格的列号 c 。用英文字母表中的 字母 标识。 * 例如，第 1 列用 'A' 表示，第 2 列用 'B' 表示，第 3 列用 'C' 表示，以此类推。 * <row> 即单元格的行号 r 。第 r 行就用 整数 r 标识。 给你一个格式为 "<col1><row1>:<col2><row2>" 的字符串 s ，其中 <col1> 表示 c1 列，<row1> 表示 r1 行，<col2> 表示 c2 列，<row2> 表示 r2 行，并满足 r1 <= r2 且 c1 <= c2 。 找出所有满足 r1 <= x <= r2 且 c1 <= y <= c2 的单元格，并以列表形式返回。单元格应该按前面描述的格式用 字符串 表示，并以 非递减 顺序排列（先按列排，再按行排）。 示例 1： [https://assets.leetcode.com/uploads/2022/02/08/ex1drawio.png] 输入：s = "K1:L2" 输出：["K1","K2","L1","L2"] 解释： 上图显示了列表中应该出现的单元格。 红色箭头指示单元格的出现顺序。 示例 2： [https://assets.leetcode.com/uploads/2022/02/09/exam2drawio.png] 输入：s = "A1:F1" 输出：["A1","B1","C1","D1","E1","F1"] 解释： 上图显示了列表中应该出现的单元格。 红色箭头指示单元格的出现顺序。 提示： * s.length == 5 * 'A' <= s[0] <= s[3] <= 'Z' * '1' <= s[1] <= s[4] <= '9' * s 由大写英文字母、数字、和 ':' 组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将输入字符串解析为起始和结束的列和行，然后生成所有符合条件的单元格。

算法步骤:
1. 解析输入字符串，获取起始列 col1、起始行 row1、结束列 col2 和结束行 row2。
2. 使用嵌套循环生成从 col1 到 col2 和从 row1 到 row2 的所有单元格。
3. 返回生成的单元格列表。

关键点:
- 使用 ord 和 chr 函数来处理字母列号。
- 使用嵌套循环生成所有符合条件的单元格。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O((c2 - c1 + 1) * (r2 - r1 + 1))
空间复杂度: O((c2 - c1 + 1) * (r2 - r1 + 1))
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def cells_in_range(s: str) -> List[str]:
    """
    函数式接口 - 生成给定范围内的所有单元格
    """
    # 解析输入字符串
    col1, row1, _, col2, row2 = s[0], int(s[1]), s[2], s[3], int(s[4])
    
    # 生成所有符合条件的单元格
    result = []
    for col in range(ord(col1), ord(col2) + 1):
        for row in range(row1, row2 + 1):
            result.append(f"{chr(col)}{row}")
    
    return result


Solution = create_solution(cells_in_range)