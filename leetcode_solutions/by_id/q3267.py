"""
2981. 找出出现至少三次的最长特殊子字符串 I - 给你一个仅由小写英文字母组成的字符串 s 。 如果一个字符串仅由单一字符组成，那么它被称为 特殊 字符串。例如，字符串 "abc" 不是特殊字符串，而字符串 "ddd"、"zz" 和 "f" 是特殊字符串。 返回在 s 中出现 至少三次 的 最长特殊子字符串 的长度，如果不存在出现至少三次的特殊子字符串，则返回 -1 。 子字符串 是字符串中的一个连续 非空 字符序列。 示例 1： 输入：s = "aaaa" 输出：2 解释：出现三次的最长特殊子字符串是 "aa" ：子字符串 "aaaa"、"aaaa" 和 "aaaa"。 可以证明最大长度是 2 。 示例 2： 输入：s = "abcdef" 输出：-1 解释：不存在出现至少三次的特殊子字符串。因此返回 -1 。 示例 3： 输入：s = "abcaba" 输出：1 解释：出现三次的最长特殊子字符串是 "a" ：子字符串 "abcaba"、"abcaba" 和 "abcaba"。 可以证明最大长度是 1 。 提示： * 3 <= s.length <= 50 * s 仅由小写英文字母组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和哈希表来记录每个字符的连续子串及其出现次数。

算法步骤:
1. 初始化一个哈希表 `count` 来记录每个字符的每个长度的特殊子串的出现次数。
2. 遍历字符串 `s`，使用滑动窗口找到所有连续相同字符的子串，并记录其长度和出现次数。
3. 遍历哈希表 `count`，找到出现至少三次的最长特殊子串的长度。

关键点:
- 使用滑动窗口找到所有连续相同字符的子串。
- 使用哈希表记录每个长度的特殊子串的出现次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2) - 遍历字符串和处理每个子串的时间复杂度。
空间复杂度: O(n^2) - 哈希表存储每个长度的特殊子串的出现次数。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 找出出现至少三次的最长特殊子字符串
    """
    n = len(s)
    count = {}

    # 记录每个字符的每个长度的特殊子串的出现次数
    for i in range(n):
        length = 1
        while i + length <= n and s[i] == s[i + length - 1]:
            key = (s[i], length)
            count[key] = count.get(key, 0) + 1
            length += 1

    # 找到出现至少三次的最长特殊子串的长度
    max_length = -1
    for (char, length), freq in count.items():
        if freq >= 3:
            max_length = max(max_length, length)

    return max_length


Solution = create_solution(solution_function_name)