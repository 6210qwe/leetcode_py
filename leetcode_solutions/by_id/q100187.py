"""
面试题 02.03. 删除中间节点 - 若链表中的某个节点，既不是链表头节点，也不是链表尾节点，则称其为该链表的「中间节点」。 假定已知链表的某一个中间节点，请实现一种算法，将该节点从链表中删除。 例如，传入节点 c（位于单向链表 a->b->c->d->e->f 中），将其删除后，剩余链表为 a->b->d->e->f 示例： 输入：节点 5 （位于单向链表 4->5->1->9 中） 输出：不返回任何数据，从链表中删除传入的节点 5，使链表变为 4->1->9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过复制下一个节点的值到当前节点，然后删除下一个节点来实现删除当前节点。

算法步骤:
1. 将当前节点的值替换为下一个节点的值。
2. 将当前节点的 next 指针指向下一个节点的 next 节点，从而跳过下一个节点。

关键点:
- 不能直接删除当前节点，因为没有前驱节点的引用。
- 通过复制和跳过的方式间接删除当前节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(node: ListNode):
    """
    函数式接口 - 删除链表中的中间节点
    """
    if node is None or node.next is None:
        return  # 无法删除尾节点或空节点

    # 复制下一个节点的值
    node.val = node.next.val
    # 删除下一个节点
    node.next = node.next.next


Solution = create_solution(solution_function_name)