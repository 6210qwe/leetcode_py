# 问题描述
# ============================================================================
"""
1667. 修复表中的名字 - 表： Users +----------------+---------+ | Column Name | Type | +----------------+---------+ | user_id | int | | name | varchar | +----------------+---------+ user_id 是该表的主键(具有唯一值的列)。 该表包含用户的 ID 和名字。名字仅由小写和大写字符组成。 编写解决方案，修复名字，使得只有第一个字符是大写的，其余都是小写的。 返回按 user_id 排序的结果表。 返回结果格式示例如下。 示例 1： 输入： Users table: +---------+-------+ | user_id | name | +---------+-------+ | 1 | aLice | | 2 | bOB | +---------+-------+ 输出： +---------+-------+ | user_id | name | +---------+-------+ | 1 | Alice | | 2 | Bob | +---------+-------+
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 的内置函数 `INITCAP` 或 `UPPER` 和 `LOWER` 来修复名字，并按 `user_id` 排序。

算法步骤:
1. 使用 `INITCAP` 函数将名字的第一个字符转换为大写，其余字符转换为小写。
2. 按 `user_id` 排序返回结果表。

关键点:
- 使用 SQL 内置函数 `INITCAP` 简化字符串处理。
- 确保结果按 `user_id` 排序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 Users 表的行数。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(1)，不需要额外的空间，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(params):
    """
    函数式接口 - 实现
    """
    # 实现最优解法
    query = """
    SELECT user_id, INITCAP(name) AS name
    FROM Users
    ORDER BY user_id
    """
    return query


Solution = create_solution(solution_function_name)