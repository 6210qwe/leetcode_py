"""
1377. T 秒后青蛙的位置 - 给你一棵由 n 个顶点组成的无向树，顶点编号从 1 到 n。青蛙从 顶点 1 开始起跳。规则如下： * 在一秒内，青蛙从它所在的当前顶点跳到另一个 未访问 过的顶点（如果它们直接相连）。 * 青蛙无法跳回已经访问过的顶点。 * 如果青蛙可以跳到多个不同顶点，那么它跳到其中任意一个顶点上的机率都相同。 * 如果青蛙不能跳到任何未访问过的顶点上，那么它每次跳跃都会停留在原地。 无向树的边用数组 edges 描述，其中 edges[i] = [ai, bi] 意味着存在一条直接连通 ai 和 bi 两个顶点的边。 返回青蛙在 t 秒后位于目标顶点 target 上的概率。与实际答案相差不超过 10-5 的结果将被视为正确答案。 示例 1： [https://assets.leetcode.com/uploads/2021/12/21/frog1.jpg] 输入：n = 7, edges = [[1,2],[1,3],[1,7],[2,4],[2,6],[3,5]], t = 2, target = 4 输出：0.16666666666666666 解释：上图显示了青蛙的跳跃路径。青蛙从顶点 1 起跳，第 1 秒 有 1/3 的概率跳到顶点 2 ，然后第 2 秒 有 1/2 的概率跳到顶点 4，因此青蛙在 2 秒后位于顶点 4 的概率是 1/3 * 1/2 = 1/6 = 0.16666666666666666 。 示例 2： [https://assets.leetcode.com/uploads/2021/12/21/frog2.jpg] 输入：n = 7, edges = [[1,2],[1,3],[1,7],[2,4],[2,6],[3,5]], t = 1, target = 7 输出：0.3333333333333333 解释：上图显示了青蛙的跳跃路径。青蛙从顶点 1 起跳，有 1/3 = 0.3333333333333333 的概率能够 1 秒 后跳到顶点 7 。 提示： * 1 <= n <= 100 * edges.length == n - 1 * edges[i].length == 2 * 1 <= ai, bi <= n * 1 <= t <= 50 * 1 <= target <= n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 来遍历树，并计算青蛙在 t 秒后位于目标顶点的概率。

算法步骤:
1. 构建邻接表表示树。
2. 使用 DFS 从顶点 1 开始遍历树，记录当前顶点、时间、概率和父节点。
3. 在每个顶点，检查是否到达目标顶点且时间正好为 t 秒，或者时间超过 t 秒但没有更多可访问的顶点。
4. 如果满足上述条件之一，返回当前概率；否则继续递归遍历未访问的子节点。
5. 如果遍历完所有可能路径仍未找到目标顶点，返回 0。

关键点:
- 使用邻接表来高效存储和访问树结构。
- 通过传递时间和概率参数来跟踪青蛙的移动和概率。
- 确保在每个顶点只访问未访问过的子节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是顶点数。每个顶点和边最多访问一次。
空间复杂度: O(n)，用于存储邻接表和递归调用栈。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def frog_position(n: int, edges: List[List[int]], t: int, target: int) -> float:
    """
    函数式接口 - 计算青蛙在 t 秒后位于目标顶点的概率
    """
    # 构建邻接表
    graph = [[] for _ in range(n + 1)]
    for u, v in edges:
        graph[u].append(v)
        graph[v].append(u)

    def dfs(node: int, time: int, prob: float, parent: int) -> float:
        # 如果到达目标顶点且时间正好为 t 秒，或者时间超过 t 秒但没有更多可访问的顶点
        if node == target and (time == t or (time < t and len(graph[node]) == 1)):
            return prob
        if time >= t:
            return 0.0

        # 计算下一个顶点的概率
        next_prob = prob / (len(graph[node]) - (node != 1))
        for neighbor in graph[node]:
            if neighbor != parent:
                result = dfs(neighbor, time + 1, next_prob, node)
                if result > 0:
                    return result
        return 0.0

    return dfs(1, 0, 1.0, -1)


Solution = create_solution(frog_position)