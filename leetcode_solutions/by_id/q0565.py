"""
565. 数组嵌套 - 索引从0开始长度为N的数组A，包含0到N - 1的所有整数。找到最大的集合S并返回其大小，其中 S[i] = {A[i], A[A[i]], A[A[A[i]]], ... }且遵守以下的规则。 假设选择索引为i的元素A[i]为S的第一个元素，S的下一个元素应该是A[A[i]]，之后是A[A[A[i]]]... 以此类推，不断添加直到S出现重复的元素。 示例 1: 输入: A = [5,4,0,3,1,6,2] 输出: 4 解释: A[0] = 5, A[1] = 4, A[2] = 0, A[3] = 3, A[4] = 1, A[5] = 6, A[6] = 2. 其中一种最长的 S[K]: S[0] = {A[0], A[5], A[6], A[2]} = {5, 6, 2, 0} 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] < nums.length * A中不含有重复的元素。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）来遍历每个起点，标记访问过的节点以避免重复计算。

算法步骤:
1. 初始化一个变量 `max_size` 来记录最大集合的大小。
2. 遍历数组中的每个元素，如果该元素未被访问过，则从该元素开始进行DFS。
3. 在DFS过程中，使用一个集合 `visited` 来记录已经访问过的节点，并更新当前集合的大小。
4. 更新 `max_size` 为当前集合大小和 `max_size` 的较大值。
5. 返回 `max_size`。

关键点:
- 使用集合 `visited` 来记录已经访问过的节点，避免重复计算。
- 通过标记访问过的节点来优化时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。每个节点最多只会被访问一次。
空间复杂度: O(n)，最坏情况下需要存储所有的节点。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def array_nesting(nums: List[int]) -> int:
    """
    函数式接口 - 找到数组嵌套的最大集合大小
    """
    def dfs(index: int) -> int:
        if index in visited:
            return 0
        visited.add(index)
        return 1 + dfs(nums[index])

    max_size = 0
    visited = set()
    for i in range(len(nums)):
        if i not in visited:
            max_size = max(max_size, dfs(i))
    
    return max_size

