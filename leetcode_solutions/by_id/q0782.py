"""
771. 宝石与石头 - 给你一个字符串 jewels 代表石头中宝石的类型，另有一个字符串 stones 代表你拥有的石头。 stones 中每个字符代表了一种你拥有的石头的类型，你想知道你拥有的石头中有多少是宝石。 字母区分大小写，因此 "a" 和 "A" 是不同类型的石头。 示例 1： 输入：jewels = "aA", stones = "aAAbbbb" 输出：3 示例 2： 输入：jewels = "z", stones = "ZZ" 输出：0 提示： * 1 <= jewels.length, stones.length <= 50 * jewels 和 stones 仅由英文字母组成 * jewels 中的所有字符都是 唯一的
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合来存储宝石类型，然后遍历石头字符串，统计宝石的数量。

算法步骤:
1. 将宝石类型字符串转换为一个集合，以便 O(1) 时间复杂度进行查找。
2. 遍历石头字符串，对于每个字符，检查它是否在宝石集合中，如果是，则计数器加一。

关键点:
- 使用集合来存储宝石类型，以实现高效的查找。
- 遍历石头字符串时，使用集合的成员检查操作来判断当前石头是否为宝石。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 jewels 的长度，m 是 stones 的长度。需要 O(n) 的时间将 jewels 转换为集合，O(m) 的时间遍历 stones。
空间复杂度: O(n)，用于存储珠宝集合。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def num_jewels_in_stones(jewels: str, stones: str) -> int:
    """
    函数式接口 - 计算石头中有多少是宝石
    """
    # 将宝石类型转换为集合
    jewel_set = set(jewels)
    
    # 初始化计数器
    count = 0
    
    # 遍历石头字符串
    for stone in stones:
        if stone in jewel_set:
            count += 1
    
    return count


Solution = create_solution(num_jewels_in_stones)