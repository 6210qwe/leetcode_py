"""
3038. 相同分数的最大操作数目 I - 给你一个整数数组 nums ，如果 nums 至少 包含 2 个元素，你可以执行以下操作： * 选择 nums 中的前两个元素并将它们删除。 一次操作的 分数 是被删除元素的和。 在确保 所有操作分数相同 的前提下，请你求出 最多 能进行多少次操作。 请你返回按照上述要求 最多 可以进行的操作次数。 示例 1： 输入：nums = [3,2,1,4,5] 输出：2 解释：我们执行以下操作： - 删除前两个元素，分数为 3 + 2 = 5 ，nums = [1,4,5] 。 - 删除前两个元素，分数为 1 + 4 = 5 ，nums = [5] 。 由于只剩下 1 个元素，我们无法继续进行任何操作。 示例 2： 输入：nums = [3,2,6,1,4] 输出：1 解释：我们执行以下操作： - 删除前两个元素，分数为 3 + 2 = 5 ，nums = [6,1,4] 。 由于下一次操作的分数与前一次不相等，我们无法继续进行任何操作。 提示： * 2 <= nums.length <= 100 * 1 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算前两个元素的和作为目标分数，然后逐对检查后续元素是否满足该目标分数。

算法步骤:
1. 检查输入数组长度是否小于2，如果是则直接返回0。
2. 计算前两个元素的和作为目标分数。
3. 初始化计数器为1（已经进行了第一次操作）。
4. 从第三个元素开始，每次取两个元素，检查它们的和是否等于目标分数。
5. 如果等于目标分数，则计数器加1，并继续检查下一组元素。
6. 如果不等于目标分数或剩余元素不足两个，则停止操作并返回计数器的值。

关键点:
- 通过逐对检查元素，确保所有操作的分数相同。
- 使用简单的循环和条件判断来实现。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 其中n是数组的长度，因为我们需要遍历数组中的每个元素。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


def max_operations_with_same_score(nums: List[int]) -> int:
    """
    函数式接口 - 计算最多可以进行的操作次数，确保所有操作的分数相同。
    """
    if len(nums) < 2:
        return 0

    target_score = nums[0] + nums[1]
    count = 1

    for i in range(2, len(nums), 2):
        if i + 1 < len(nums) and nums[i] + nums[i + 1] == target_score:
            count += 1
        else:
            break

    return count


