"""
核心思想: 使用哈希表记录每个数字的出现次数，然后遍历数组找到最大的唯一数。

算法步骤:
1. 使用一个字典记录每个数字的出现次数。
2. 再次遍历数组，找到出现次数为1的最大数字。

关键点:
- 使用哈希表记录每个数字的出现次数。
- 两次遍历数组，一次统计频率，一次查找最大唯一数。
"""

