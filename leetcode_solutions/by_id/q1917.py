"""
1792. 最大平均通过率 - 一所学校里有一些班级，每个班级里有一些学生，现在每个班都会进行一场期末考试。给你一个二维数组 classes ，其中 classes[i] = [passi, totali] ，表示你提前知道了第 i 个班级总共有 totali 个学生，其中只有 passi 个学生可以通过考试。 给你一个整数 extraStudents ，表示额外有 extraStudents 个聪明的学生，他们 一定 能通过任何班级的期末考。你需要给这 extraStudents 个学生每人都安排一个班级，使得 所有 班级的 平均 通过率 最大 。 一个班级的 通过率 等于这个班级通过考试的学生人数除以这个班级的总人数。平均通过率 是所有班级的通过率之和除以班级数目。 请你返回在安排这 extraStudents 个学生去对应班级后的 最大 平均通过率。与标准答案误差范围在 10-5 以内的结果都会视为正确结果。 示例 1： 输入：classes = [[1,2],[3,5],[2,2]], extraStudents = 2 输出：0.78333 解释：你可以将额外的两个学生都安排到第一个班级，平均通过率为 (3/4 + 3/5 + 2/2) / 3 = 0.78333 。 示例 2： 输入：classes = [[2,4],[3,9],[4,5],[2,10]], extraStudents = 4 输出：0.53485 提示： * 1 <= classes.length <= 105 * classes[i].length == 2 * 1 <= passi <= totali <= 105 * 1 <= extraStudents <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用最大堆来维护每个班级的通过率增量，每次选择增量最大的班级添加一个学生。

算法步骤:
1. 初始化一个最大堆，将每个班级的通过率增量 (delta) 和班级索引加入堆中。
2. 对于每个额外的学生，从堆中取出增量最大的班级，增加一个学生，并更新该班级的通过率增量，再将其重新加入堆中。
3. 计算最终的平均通过率并返回。

关键点:
- 通过率增量的计算公式为 (pass + 1) / (total + 1) - pass / total。
- 使用最大堆来高效地找到增量最大的班级。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O((n + k) log n)，其中 n 是班级数量，k 是额外学生的数量。每次插入和删除堆的操作都是 O(log n)。
空间复杂度: O(n)，用于存储堆。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def max_average_ratio(classes: List[List[int]], extraStudents: int) -> float:
    """
    函数式接口 - 计算最大平均通过率
    """
    # 定义通过率增量的计算函数
    def delta(passed, total):
        return (passed + 1) / (total + 1) - passed / total

    # 初始化最大堆
    max_heap = []
    for passed, total in classes:
        heapq.heappush(max_heap, (-delta(passed, total), passed, total))

    # 为每个额外的学生分配班级
    for _ in range(extraStudents):
        d, passed, total = heapq.heappop(max_heap)
        passed += 1
        total += 1
        heapq.heappush(max_heap, (-delta(passed, total), passed, total))

    # 计算最终的平均通过率
    total_pass_rate = sum((passed / total for _, passed, total in max_heap))
    return total_pass_rate / len(classes)

