"""
1470. 重新排列数组 - 给你一个数组 nums ，数组中有 2n 个元素，按 [x1,x2,...,xn,y1,y2,...,yn] 的格式排列。 请你将数组按 [x1,y1,x2,y2,...,xn,yn] 格式重新排列，返回重排后的数组。 示例 1： 输入：nums = [2,5,1,3,4,7], n = 3 输出：[2,3,5,4,1,7] 解释：由于 x1=2, x2=5, x3=1, y1=3, y2=4, y3=7 ，所以答案为 [2,3,5,4,1,7] 示例 2： 输入：nums = [1,2,3,4,4,3,2,1], n = 4 输出：[1,4,2,3,3,2,4,1] 示例 3： 输入：nums = [1,1,2,2], n = 2 输出：[1,2,1,2] 提示： * 1 <= n <= 500 * nums.length == 2n * 1 <= nums[i] <= 10^3
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法，分别指向 x 和 y 部分的起始位置，交替添加到结果数组中。

算法步骤:
1. 初始化两个指针 i 和 j，分别指向 x 部分和 y 部分的起始位置。
2. 创建一个空的结果数组 result。
3. 循环 n 次，每次从 x 部分和 y 部分各取一个元素，依次添加到结果数组中。
4. 返回结果数组。

关键点:
- 使用双指针方法可以简化代码逻辑，并且保证时间复杂度和空间复杂度最优。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的一半长度。我们只需要遍历一次数组。
空间复杂度: O(1)，除了输出数组外，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def shuffle(nums: List[int], n: int) -> List[int]:
    """
    函数式接口 - 将数组按 [x1,y1,x2,y2,...,xn,yn] 格式重新排列
    """
    result = []
    for i in range(n):
        result.append(nums[i])
        result.append(nums[i + n])
    return result


Solution = create_solution(shuffle)