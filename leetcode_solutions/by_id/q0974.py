# 问题描述
# ============================================================================
"""
937. 重新排列日志文件 - 给你一个日志数组 logs。每条日志都是以空格分隔的字串，其第一个字为字母与数字混合的 标识符 。 有两种不同类型的日志： * 字母日志：除标识符之外，所有字均由小写字母组成 * 数字日志：除标识符之外，所有字均由数字组成 请按下述规则将日志重新排序： * 所有 字母日志 都排在 数字日志 之前。 * 字母日志 在内容不同时，忽略标识符后，按内容字母顺序排序；在内容相同时，按标识符排序。 * 数字日志 应该保留原来的相对顺序。 返回日志的最终顺序。 示例 1： 输入：logs = ["dig1 8 1 5 1","let1 art can","dig2 3 6","let2 own kit dig","let3 art zero"] 输出：["let1 art can","let3 art zero","let2 own kit dig","dig1 8 1 5 1","dig2 3 6"] 解释： 字母日志的内容都不同，所以顺序为 "art can", "art zero", "own kit dig" 。 数字日志保留原来的相对顺序 "dig1 8 1 5 1", "dig2 3 6" 。 示例 2： 输入：logs = ["a1 9 2 3 1","g1 act car","zo4 4 7","ab1 off key dog","a8 act zoo"] 输出：["g1 act car","a8 act zoo","ab1 off key dog","a1 9 2 3 1","zo4 4 7"] 提示： * 1 <= logs.length <= 100 * 3 <= logs[i].length <= 100 * logs[i] 中，字与字之间都用 单个 空格分隔 * 题目数据保证 logs[i] 都有一个标识符，并且在标识符之后至少存在一个字
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用自定义排序函数对日志进行排序。

算法步骤:
1. 将日志分为字母日志和数字日志。
2. 对字母日志进行排序：首先按内容排序，如果内容相同则按标识符排序。
3. 将排序后的字母日志和原始顺序的数字日志合并。

关键点:
- 使用 Python 的 `sorted` 函数和自定义排序键。
- 保持数字日志的原始顺序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是日志的数量。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，存储字母日志和数字日志需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def reorder_log_files(logs: List[str]) -> List[str]:
    """
    重新排列日志文件 - 按照题目要求对日志进行排序。
    """
    def sort_key(log: str) -> tuple:
        # 分割日志为标识符和内容
        identifier, content = log.split(" ", 1)
        # 如果内容是数字日志，返回 (1, None, None) 以确保数字日志在字母日志之后
        if content[0].isdigit():
            return (1, None, None)
        # 否则返回 (0, 内容, 标识符) 以确保字母日志在前，并按内容和标识符排序
        return (0, content, identifier)

    # 使用自定义排序键对日志进行排序
    return sorted(logs, key=sort_key)


Solution = create_solution(reorder_log_files)