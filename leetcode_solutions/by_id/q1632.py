# 问题描述
# ============================================================================
"""
1525. 字符串的好分割数目 - 给你一个字符串 s ，一个分割被称为 「好分割」 当它满足：将 s 分割成 2 个字符串 p 和 q ，它们连接起来等于 s 且 p 和 q 中不同字符的数目相同。 请你返回 s 中好分割的数目。 示例 1： 输入：s = "aacaba" 输出：2 解释：总共有 5 种分割字符串 "aacaba" 的方法，其中 2 种是好分割。 ("a", "acaba") 左边字符串和右边字符串分别包含 1 个和 3 个不同的字符。 ("aa", "caba") 左边字符串和右边字符串分别包含 1 个和 3 个不同的字符。 ("aac", "aba") 左边字符串和右边字符串分别包含 2 个和 2 个不同的字符。这是一个好分割。 ("aaca", "ba") 左边字符串和右边字符串分别包含 2 个和 2 个不同的字符。这是一个好分割。 ("aacab", "a") 左边字符串和右边字符串分别包含 3 个和 1 个不同的字符。 示例 2： 输入：s = "abcd" 输出：1 解释：好分割为将字符串分割成 ("ab", "cd") 。 示例 3： 输入：s = "aaaaa" 输出：4 解释：所有分割都是好分割。 示例 4： 输入：s = "acbadbaada" 输出：2 提示： * s 只包含小写英文字母。 * 1 <= s.length <= 10^5
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个计数器分别记录左边和右边的不同字符数量，并在遍历过程中更新计数器。

算法步骤:
1. 初始化两个计数器 `left_count` 和 `right_count`，分别用于记录左边和右边的不同字符数量。
2. 将整个字符串的字符计数存储在 `right_count` 中。
3. 遍历字符串，逐步将字符从 `right_count` 移动到 `left_count`，并检查两个计数器中的不同字符数量是否相等。
4. 如果相等，则增加好分割的计数。

关键点:
- 使用计数器来高效地统计不同字符的数量。
- 在遍历过程中动态更新计数器，避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，因为字母表的大小是固定的（26个小写字母），所以计数器的空间复杂度是常数级的。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 返回字符串 s 中好分割的数目
    """
    from collections import Counter

    left_count = Counter()
    right_count = Counter(s)
    good_splits = 0

    for i in range(len(s) - 1):
        char = s[i]
        left_count[char] += 1
        right_count[char] -= 1

        if right_count[char] == 0:
            del right_count[char]

        if len(left_count) == len(right_count):
            good_splits += 1

    return good_splits


Solution = create_solution(solution_function_name)