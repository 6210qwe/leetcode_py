"""
2607. 使子数组元素和相等 - 给你一个下标从 0 开始的整数数组 arr 和一个整数 k 。数组 arr 是一个循环数组。换句话说，数组中的最后一个元素的下一个元素是数组中的第一个元素，数组中第一个元素的前一个元素是数组中的最后一个元素。 你可以执行下述运算任意次： * 选中 arr 中任意一个元素，并使其值加上 1 或减去 1 。 执行运算使每个长度为 k 的 子数组 的元素总和都相等，返回所需要的最少运算次数。 子数组 是数组的一个连续部分。 示例 1： 输入：arr = [1,4,1,3], k = 2 输出：1 解释：在下标为 1 的元素那里执行一次运算，使其等于 3 。 执行运算后，数组变为 [1,3,1,3] 。 - 0 处起始的子数组为 [1, 3] ，元素总和为 4 - 1 处起始的子数组为 [3, 1] ，元素总和为 4 - 2 处起始的子数组为 [1, 3] ，元素总和为 4 - 3 处起始的子数组为 [3, 1] ，元素总和为 4 示例 2： 输入：arr = [2,5,5,7], k = 3 输出：5 解释：在下标为 0 的元素那里执行三次运算，使其等于 5 。在下标为 3 的元素那里执行两次运算，使其等于 5 。 执行运算后，数组变为 [5,5,5,5] 。 - 0 处起始的子数组为 [5, 5, 5] ，元素总和为 15 - 1 处起始的子数组为 [5, 5, 5] ，元素总和为 15 - 2 处起始的子数组为 [5, 5, 5] ，元素总和为 15 - 3 处起始的子数组为 [5, 5, 5] ，元素总和为 15 提示： * 1 <= k <= arr.length <= 105 * 1 <= arr[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将数组分成 k 个组，每组的元素索引相差 k。对于每一组，找到其中位数，然后将所有元素变成该中位数所需的最小操作次数即为答案。

算法步骤:
1. 初始化一个字典，用于存储每个组的元素。
2. 遍历数组，将每个元素放入对应的组中。
3. 对于每个组，找到其中位数，并计算将所有元素变成该中位数所需的操作次数。
4. 累加所有组的操作次数，得到最终结果。

关键点:
- 使用中位数来最小化操作次数。
- 通过字典来存储每个组的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)。
空间复杂度: O(n)，需要额外的空间来存储每个组的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int], k: int) -> int:
    """
    函数式接口 - 使子数组元素和相等
    """
    # 初始化一个字典，用于存储每个组的元素
    groups = {}
    
    # 遍历数组，将每个元素放入对应的组中
    for i in range(len(arr)):
        group_index = i % k
        if group_index not in groups:
            groups[group_index] = []
        groups[group_index].append(arr[i])
    
    # 计算总的操作次数
    total_operations = 0
    
    # 对于每个组，找到其中位数，并计算将所有元素变成该中位数所需的操作次数
    for group in groups.values():
        group.sort()
        median = group[len(group) // 2]
        operations = sum(abs(num - median) for num in group)
        total_operations += operations
    
    return total_operations


Solution = create_solution(solution_function_name)