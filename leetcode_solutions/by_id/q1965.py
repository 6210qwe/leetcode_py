"""
1837. K 进制表示下的各位数字总和 - 给你一个整数 n（10 进制）和一个基数 k ，请你将 n 从 10 进制表示转换为 k 进制表示，计算并返回转换后各位数字的 总和 。 转换后，各位数字应当视作是 10 进制数字，且它们的总和也应当按 10 进制表示返回。 示例 1： 输入：n = 34, k = 6 输出：9 解释：34 (10 进制) 在 6 进制下表示为 54 。5 + 4 = 9 。 示例 2： 输入：n = 10, k = 10 输出：1 解释：n 本身就是 10 进制。 1 + 0 = 1 。 提示： * 1 <= n <= 100 * 2 <= k <= 10
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将十进制数 n 转换为 k 进制，并计算其各位数字之和。

算法步骤:
1. 初始化结果变量 `result` 为 0。
2. 使用循环将 n 不断除以 k，每次取余数并加到 `result` 中，然后更新 n 为 n 除以 k 的商。
3. 当 n 为 0 时，结束循环，返回 `result`。

关键点:
- 通过取模运算获取当前位的值，并累加到结果中。
- 通过整除运算更新 n，直到 n 为 0。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log_k(n)) - 每次迭代 n 减少一个数量级。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_digits_in_base_k(n: int, k: int) -> int:
    """
    将十进制数 n 转换为 k 进制，并计算其各位数字之和。
    """
    result = 0
    while n > 0:
        result += n % k  # 取当前位的值并累加到结果中
        n //= k  # 更新 n 为 n 除以 k 的商
    return result


