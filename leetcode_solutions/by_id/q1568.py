# ============================================================================
# 问题描述
# ============================================================================
"""
1457. 二叉树中的伪回文路径 - 给你一棵二叉树，每个节点的值为 1 到 9 。我们称二叉树中的一条路径是 「伪回文」的，当它满足：路径经过的所有节点值的排列中，存在一个回文序列。 请你返回从根到叶子节点的所有路径中 伪回文 路径的数目。 示例 1： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/05/23/palindromic_paths_1.png] 输入：root = [2,3,1,3,1,null,1] 输出：2 解释：上图为给定的二叉树。总共有 3 条从根到叶子的路径：红色路径 [2,3,3] ，绿色路径 [2,1,1] 和路径 [2,3,1] 。 在这些路径中，只有红色和绿色的路径是伪回文路径，因为红色路径 [2,3,3] 存在回文排列 [3,2,3] ，绿色路径 [2,1,1] 存在回文排列 [1,2,1] 。 示例 2： [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2020/05/23/palindromic_paths_2.png] 输入：root = [2,1,1,1,3,null,null,null,null,null,1] 输出：1 解释：上图为给定二叉树。总共有 3 条从根到叶子的路径：绿色路径 [2,1,1] ，路径 [2,1,3,1] 和路径 [2,1] 。 这些路径中只有绿色路径是伪回文路径，因为 [2,1,1] 存在回文排列 [1,2,1] 。 示例 3： 输入：root = [9] 输出：1 提示： * 给定二叉树的节点数目在范围 [1, 105] 内 * 1 <= Node.val <= 9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算来记录路径上的节点值出现次数，并检查是否可以形成伪回文路径。

算法步骤:
1. 使用深度优先搜索 (DFS) 遍历二叉树。
2. 使用一个整数 `path` 来记录路径上每个节点值的出现次数，使用位运算进行更新。
3. 当到达叶子节点时，检查 `path` 中 1 的个数是否不超过 1（即最多只有一个节点值出现奇数次），如果是，则该路径是伪回文路径。
4. 递归遍历左右子树，并回溯更新 `path`。

关键点:
- 使用位运算来高效地记录和检查节点值的出现次数。
- 通过递归和回溯遍历所有路径。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数。每个节点只被访问一次。
空间复杂度: O(h)，其中 h 是二叉树的高度。递归调用栈的深度取决于树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def pseudo_palindromic_paths(root: Optional[TreeNode]) -> int:
    def dfs(node: Optional[TreeNode], path: int) -> int:
        if not node:
            return 0
        
        # 更新路径状态
        path ^= 1 << (node.val - 1)
        
        # 如果是叶子节点，检查路径是否是伪回文
        if not node.left and not node.right:
            return 1 if bin(path).count('1') <= 1 else 0
        
        # 递归遍历左右子树
        return dfs(node.left, path) + dfs(node.right, path)
    
    return dfs(root, 0)


Solution = create_solution(pseudo_palindromic_paths)