"""
3523. 非递减数组的最大长度 - 给你一个整数数组 nums。在一次操作中，你可以选择一个子数组，并将其替换为一个等于该子数组 最大值 的单个元素。 返回经过零次或多次操作后，数组仍为 非递减 的情况下，数组 可能的最大长度。 子数组 是数组中一个连续、非空 的元素序列。 示例 1： 输入： nums = [4,2,5,3,5] 输出： 3 解释： 实现最大长度的一种方法是： 1. 将子数组 nums[1..2] = [2, 5] 替换为 5 → [4, 5, 3, 5]。 2. 将子数组 nums[2..3] = [3, 5] 替换为 5 → [4, 5, 5]。 最终数组 [4, 5, 5] 是非递减的，长度为 3。 示例 2： 输入： nums = [1,2,3] 输出： 3 解释： 无需任何操作，因为数组 [1,2,3] 已经是非递减的。 提示： * 1 <= nums.length <= 2 * 105 * 1 <= nums[i] <= 2 * 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用单调栈来维护一个非递减的子数组，并记录每个元素的最长非递减子数组长度。

算法步骤:
1. 初始化一个栈 `stack` 和一个数组 `lengths` 用于记录每个元素的最长非递减子数组长度。
2. 遍历数组 `nums`，对于每个元素 `num`：
   - 如果栈为空或当前元素 `num` 大于等于栈顶元素，则将 `num` 入栈，并更新 `lengths` 数组。
   - 否则，弹出栈顶元素直到栈顶元素小于等于 `num`，然后将 `num` 入栈，并更新 `lengths` 数组。
3. 最后返回 `lengths` 数组中的最大值。

关键点:
- 使用单调栈来维护非递减子数组。
- 动态更新每个元素的最长非递减子数组长度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 `nums` 的长度。每个元素最多入栈和出栈一次。
空间复杂度: O(n)，需要额外的空间来存储栈和 `lengths` 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算非递减数组的最大长度
    """
    stack = []
    lengths = [0] * len(nums)
    
    for i, num in enumerate(nums):
        while stack and nums[stack[-1]] > num:
            stack.pop()
        
        if not stack:
            lengths[i] = 1
        else:
            lengths[i] = i - stack[-1]
        
        stack.append(i)
    
    return max(lengths)


Solution = create_solution(solution_function_name)