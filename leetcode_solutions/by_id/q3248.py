"""
2972. 统计移除递增子数组的数目 II - 给你一个下标从 0 开始的 正 整数数组 nums 。 如果 nums 的一个子数组满足：移除这个子数组后剩余元素 严格递增 ，那么我们称这个子数组为 移除递增 子数组。比方说，[5, 3, 4, 6, 7] 中的 [3, 4] 是一个移除递增子数组，因为移除该子数组后，[5, 3, 4, 6, 7] 变为 [5, 6, 7] ，是严格递增的。 请你返回 nums 中 移除递增 子数组的总数目。 注意 ，剩余元素为空的数组也视为是递增的。 子数组 指的是一个数组中一段连续的元素序列。 示例 1： 输入：nums = [1,2,3,4] 输出：10 解释：10 个移除递增子数组分别为：[1], [2], [3], [4], [1,2], [2,3], [3,4], [1,2,3], [2,3,4] 和 [1,2,3,4]。移除任意一个子数组后，剩余元素都是递增的。注意，空数组不是移除递增子数组。 示例 2： 输入：nums = [6,5,7,8] 输出：7 解释：7 个移除递增子数组分别为：[5], [6], [5,7], [6,5], [5,7,8], [6,5,7] 和 [6,5,7,8] 。 nums 中只有这 7 个移除递增子数组。 示例 3： 输入：nums = [8,7,6,6] 输出：3 解释：3 个移除递增子数组分别为：[8,7,6], [7,6,6] 和 [8,7,6,6] 。注意 [8,7] 不是移除递增子数组因为移除 [8,7] 后 nums 变为 [6,6] ，它不是严格递增的。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针和二分查找来找到所有可能的移除递增子数组。

算法步骤:
1. 找到数组的最长严格递增前缀和后缀。
2. 使用双指针遍历前缀和后缀，计算可以移除的子数组数量。
3. 对于每个前缀结尾和后缀开头，使用二分查找来确定可以移除的子数组范围。

关键点:
- 利用双指针和二分查找来高效地找到所有可能的移除递增子数组。
- 通过前缀和后缀的性质来简化问题。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。双指针遍历的时间复杂度为 O(n)，二分查找的时间复杂度为 O(log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def count_incremovable_subarrays(nums: List[int]) -> int:
    n = len(nums)
    left, right = 0, n - 1
    
    # 找到最长严格递增前缀
    while left < n - 1 and nums[left] < nums[left + 1]:
        left += 1
    
    # 如果整个数组是严格递增的，直接返回所有子数组的数量
    if left == n - 1:
        return n * (n + 1) // 2
    
    # 找到最长严格递增后缀
    while right > 0 and nums[right] > nums[right - 1]:
        right -= 1
    
    # 计算可以移除的子数组数量
    count = 0
    for i in range(left + 1):
        j = right
        while j < n and (i == left or nums[j] > nums[i]):
            j += 1
        count += n - j
    
    return count

