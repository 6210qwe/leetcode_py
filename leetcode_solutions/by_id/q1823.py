"""
1704. 判断字符串的两半是否相似 - 给你一个偶数长度的字符串 s 。将其拆分成长度相同的两半，前一半为 a ，后一半为 b 。 两个字符串 相似 的前提是它们都含有相同数目的元音（'a'，'e'，'i'，'o'，'u'，'A'，'E'，'I'，'O'，'U'）。注意，s 可能同时含有大写和小写字母。 如果 a 和 b 相似，返回 true ；否则，返回 false 。 示例 1： 输入：s = "book" 输出：true 解释：a = "bo" 且 b = "ok" 。a 中有 1 个元音，b 也有 1 个元音。所以，a 和 b 相似。 示例 2： 输入：s = "textbook" 输出：false 解释：a = "text" 且 b = "book" 。a 中有 1 个元音，b 中有 2 个元音。因此，a 和 b 不相似。 注意，元音 o 在 b 中出现两次，记为 2 个。 提示： * 2 <= s.length <= 1000 * s.length 是偶数 * s 由 大写和小写 字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算字符串的前半部分和后半部分中元音的数量，并比较这两个数量是否相等。

算法步骤:
1. 定义一个集合来存储所有元音字符。
2. 将字符串分为两半。
3. 分别计算前半部分和后半部分中元音的数量。
4. 比较两个数量是否相等，如果相等则返回 True，否则返回 False。

关键点:
- 使用集合来快速检查字符是否为元音。
- 通过遍历字符串的一半来计算元音数量，避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们需要遍历字符串的一半来计算元音数量。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> bool:
    """
    函数式接口 - 判断字符串的两半是否相似
    """
    # 定义元音集合
    vowels = set('aeiouAEIOU')
    
    # 将字符串分为两半
    mid = len(s) // 2
    a, b = s[:mid], s[mid:]
    
    # 计算前半部分和后半部分中元音的数量
    count_a = sum(1 for char in a if char in vowels)
    count_b = sum(1 for char in b if char in vowels)
    
    # 比较两个数量是否相等
    return count_a == count_b


Solution = create_solution(solution_function_name)