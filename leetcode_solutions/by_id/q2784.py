"""
2681. 英雄的力量 - 给你一个下标从 0 开始的整数数组 nums ，它表示英雄的能力值。如果我们选出一部分英雄，这组英雄的 力量 定义为： * i0 ，i1 ，... ik 表示这组英雄在数组中的下标。那么这组英雄的力量为 max(nums[i0],nums[i1] ... nums[ik])2 * min(nums[i0],nums[i1] ... nums[ik]) 。 请你返回所有可能的 非空 英雄组的 力量 之和。由于答案可能非常大，请你将结果对 109 + 7 取余。 示例 1： 输入：nums = [2,1,4] 输出：141 解释： 第 1 组：[2] 的力量为 22 * 2 = 8 。 第 2 组：[1] 的力量为 12 * 1 = 1 。 第 3 组：[4] 的力量为 42 * 4 = 64 。 第 4 组：[2,1] 的力量为 22 * 1 = 4 。 第 5 组：[2,4] 的力量为 42 * 2 = 32 。 第 6 组：[1,4] 的力量为 42 * 1 = 16 。 第​ ​​​​​​7 组：[2,1,4] 的力量为 42​​​​​​​ * 1 = 16 。 所有英雄组的力量之和为 8 + 1 + 64 + 4 + 32 + 16 + 16 = 141 。 示例 2： 输入：nums = [1,1,1] 输出：7 解释：总共有 7 个英雄组，每一组的力量都是 1 。所以所有英雄组的力量之和为 7 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过排序和前缀和来计算所有子集的力量之和。

算法步骤:
1. 对数组进行排序。
2. 计算前缀和数组。
3. 遍历每个元素，计算以该元素为最大值的所有子集的力量之和。
4. 使用前缀和优化计算过程，避免重复计算。

关键点:
- 排序后，可以方便地确定每个子集的最大值和最小值。
- 使用前缀和来优化计算过程，减少时间复杂度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序的时间复杂度。
空间复杂度: O(n) - 前缀和数组的空间复杂度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def power_of_heroes(nums: List[int]) -> int:
    MOD = 10**9 + 7
    n = len(nums)
    
    # 对数组进行排序
    nums.sort()
    
    # 计算前缀和数组
    prefix_sum = [0] * (n + 1)
    for i in range(1, n + 1):
        prefix_sum[i] = (prefix_sum[i - 1] + nums[i - 1]) % MOD
    
    total_power = 0
    for i in range(n):
        # 计算以 nums[i] 为最大值的所有子集的力量之和
        max_val = nums[i]
        min_val_sum = (prefix_sum[n] - prefix_sum[i]) % MOD
        count = (1 << (n - i - 1)) % MOD
        total_power += (max_val * max_val * min_val_sum * count) % MOD
        total_power %= MOD
    
    return total_power


Solution = create_solution(power_of_heroes)