"""
1026. 节点与其祖先之间的最大差值 - 给定二叉树的根节点 root，找出存在于 不同 节点 A 和 B 之间的最大值 V，其中 V = |A.val - B.val|，且 A 是 B 的祖先。 （如果 A 的任何子节点之一为 B，或者 A 的任何子节点是 B 的祖先，那么我们认为 A 是 B 的祖先） 示例 1： [https://assets.leetcode.com/uploads/2020/11/09/tmp-tree.jpg] 输入：root = [8,3,10,1,6,null,14,null,null,4,7,13] 输出：7 解释： 我们有大量的节点与其祖先的差值，其中一些如下： |8 - 3| = 5 |3 - 7| = 4 |8 - 1| = 7 |10 - 13| = 3 在所有可能的差值中，最大值 7 由 |8 - 1| = 7 得出。 示例 2： [https://assets.leetcode.com/uploads/2020/11/09/tmp-tree-1.jpg] 输入：root = [1,null,2,null,0,3] 输出：3 提示： * 树中的节点数在 2 到 5000 之间。 * 0 <= Node.val <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 来遍历树，并在遍历过程中维护当前路径上的最小值和最大值。对于每个节点，计算其与当前路径上最小值和最大值的差值，并更新全局最大差值。

算法步骤:
1. 定义一个递归函数 `dfs`，该函数接受当前节点、当前路径上的最小值和最大值作为参数。
2. 对于每个节点，计算其与当前路径上最小值和最大值的差值，并更新全局最大差值。
3. 递归地调用 `dfs` 函数，更新当前路径上的最小值和最大值。
4. 返回全局最大差值。

关键点:
- 使用 DFS 遍历树，并在遍历过程中维护当前路径上的最小值和最大值。
- 递归地更新全局最大差值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点只会被访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def max_ancestor_diff(root: Optional[TreeNode]) -> int:
    """
    计算节点与其祖先之间的最大差值
    """
    def dfs(node, min_val, max_val):
        if not node:
            return 0
        
        # 更新当前路径上的最小值和最大值
        min_val = min(min_val, node.val)
        max_val = max(max_val, node.val)
        
        # 计算当前节点与当前路径上最小值和最大值的差值
        current_diff = max(abs(node.val - min_val), abs(node.val - max_val))
        
        # 递归地计算左子树和右子树的最大差值
        left_diff = dfs(node.left, min_val, max_val)
        right_diff = dfs(node.right, min_val, max_val)
        
        # 返回当前节点及其子树的最大差值
        return max(current_diff, left_diff, right_diff)
    
    # 从根节点开始遍历
    return dfs(root, root.val, root.val)

Solution = create_solution(max_ancestor_diff)