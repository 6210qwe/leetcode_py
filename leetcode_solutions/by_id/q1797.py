"""
1678. 设计 Goal 解析器 - 请你设计一个可以解释字符串 command 的 Goal 解析器 。command 由 "G"、"()" 和/或 "(al)" 按某种顺序组成。Goal 解析器会将 "G" 解释为字符串 "G"、"()" 解释为字符串 "o" ，"(al)" 解释为字符串 "al" 。然后，按原顺序将经解释得到的字符串连接成一个字符串。 给你字符串 command ，返回 Goal 解析器 对 command 的解释结果。 示例 1： 输入：command = "G()(al)" 输出："Goal" 解释：Goal 解析器解释命令的步骤如下所示： G -> G () -> o (al) -> al 最后连接得到的结果是 "Goal" 示例 2： 输入：command = "G()()()()(al)" 输出："Gooooal" 示例 3： 输入：command = "(al)G(al)()()G" 输出："alGalooG" 提示： * 1 <= command.length <= 100 * command 由 "G"、"()" 和/或 "(al)" 按某种顺序组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 字符串替换，将特定模式替换为对应字符

算法步骤:
1. 替换"(al)"为"al"
2. 替换"()"为"o"
3. 返回结果

关键点:
- 字符串替换
- 注意替换顺序
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - n为字符串长度
空间复杂度: O(n) - 新字符串空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def interpret(command: str) -> str:
    """
    函数式接口 - 设计 Goal 解析器
    
    实现思路:
    字符串替换：将特定模式替换为对应字符。
    
    Args:
        command: 命令字符串
        
    Returns:
        解析后的字符串
        
    Example:
        >>> interpret("G()(al)")
        'Goal'
    """
    return command.replace("(al)", "al").replace("()", "o")


Solution = create_solution(interpret)
