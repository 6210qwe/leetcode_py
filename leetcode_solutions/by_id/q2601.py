"""
2518. 好分区的数目 - 给你一个正整数数组 nums 和一个整数 k 。 分区 的定义是：将数组划分成两个有序的 组 ，并满足每个元素 恰好 存在于 某一个 组中。如果分区中每个组的元素和都大于等于 k ，则认为分区是一个好分区。 返回 不同 的好分区的数目。由于答案可能很大，请返回对 109 + 7 取余 后的结果。 如果在两个分区中，存在某个元素 nums[i] 被分在不同的组中，则认为这两个分区不同。 示例 1： 输入：nums = [1,2,3,4], k = 4 输出：6 解释：好分区的情况是 ([1,2,3], [4]), ([1,3], [2,4]), ([1,4], [2,3]), ([2,3], [1,4]), ([2,4], [1,3]) 和 ([4], [1,2,3]) 。 示例 2： 输入：nums = [3,3,3], k = 4 输出：0 解释：数组中不存在好分区。 示例 3： 输入：nums = [6,6], k = 2 输出：2 解释：可以将 nums[0] 放入第一个分区或第二个分区中。 好分区的情况是 ([6], [6]) 和 ([6], [6]) 。 提示： * 1 <= nums.length, k <= 1000 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i][j] 表示前 i 个元素分成两组，第一组和为 j 的方案数。

算法步骤:
1. 初始化 dp 数组，dp[0][0] = 1。
2. 遍历数组中的每个元素，对于每个元素，更新 dp 数组。
3. 最后，计算所有满足条件的 dp[n][j] 的和。

关键点:
- 使用动态规划来避免重复计算。
- 通过模运算来防止结果溢出。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)，其中 n 是数组长度，k 是给定的整数。
空间复杂度: O(n * k)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算好分区的数目
    """
    MOD = 10**9 + 7
    n = len(nums)
    total_sum = sum(nums)
    
    if total_sum < 2 * k:
        return 0
    
    dp = [[0] * (k + 1) for _ in range(n + 1)]
    dp[0][0] = 1
    
    for i in range(1, n + 1):
        for j in range(k + 1):
            dp[i][j] = dp[i - 1][j]
            if j >= nums[i - 1]:
                dp[i][j] += dp[i - 1][j - nums[i - 1]]
            dp[i][j] %= MOD
    
    result = 0
    for j in range(k, min(total_sum, 2 * k) + 1):
        if total_sum - j >= k:
            result = (result + dp[n][j]) % MOD
    
    return result

