"""
LCR 092. 将字符串翻转到单调递增 - 如果一个由 '0' 和 '1' 组成的字符串，是以一些 '0'（可能没有 '0'）后面跟着一些 '1'（也可能没有 '1'）的形式组成的，那么该字符串是 单调递增 的。 我们给出一个由字符 '0' 和 '1' 组成的字符串 s，我们可以将任何 '0' 翻转为 '1' 或者将 '1' 翻转为 '0'。 返回使 s 单调递增 的最小翻转次数。 示例 1： 输入：s = "00110" 输出：1 解释：我们翻转最后一位得到 00111. 示例 2： 输入：s = "010110" 输出：2 解释：我们翻转得到 011111，或者是 000111。 示例 3： 输入：s = "00011000" 输出：2 解释：我们翻转得到 00000000。 提示： * 1 <= s.length <= 20000 * s 中只包含字符 '0' 和 '1' 注意：本题与主站 926 题相同： https://leetcode.cn/problems/flip-string-to-monotone-increasing/ [https://leetcode.cn/problems/flip-string-to-monotone-increasing/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划

算法步骤:
1. 定义两个数组 `dp0` 和 `dp1`，分别表示以 '0' 结尾和以 '1' 结尾的最小翻转次数。
2. 初始化 `dp0[0]` 和 `dp1[0]`，根据第一个字符是否为 '0' 或 '1' 进行初始化。
3. 遍历字符串，更新 `dp0` 和 `dp1`：
   - 如果当前字符是 '0'，则 `dp0[i] = dp0[i-1]`，`dp1[i] = min(dp0[i-1], dp1[i-1]) + 1`。
   - 如果当前字符是 '1'，则 `dp0[i] = dp0[i-1] + 1`，`dp1[i] = min(dp0[i-1], dp1[i-1])`。
4. 最后返回 `min(dp0[-1], dp1[-1])`。

关键点:
- 使用动态规划来记录以 '0' 和 '1' 结尾的最小翻转次数。
- 通过遍历字符串，逐步更新 `dp0` 和 `dp1` 数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。
空间复杂度: O(n)，需要两个长度为 n 的数组来存储中间结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 实现
    """
    n = len(s)
    dp0 = [0] * n
    dp1 = [0] * n

    # 初始化
    dp0[0] = 1 if s[0] == '1' else 0
    dp1[0] = 1 if s[0] == '0' else 0

    for i in range(1, n):
        if s[i] == '0':
            dp0[i] = dp0[i-1]
            dp1[i] = min(dp0[i-1], dp1[i-1]) + 1
        else:
            dp0[i] = dp0[i-1] + 1
            dp1[i] = min(dp0[i-1], dp1[i-1])

    return min(dp0[-1], dp1[-1])


Solution = create_solution(solution_function_name)