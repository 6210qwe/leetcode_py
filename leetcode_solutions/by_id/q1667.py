"""
核心思想: 通过递归和数学推导来找到第 k 位字符，避免生成完整的字符串。

算法步骤:
1. 如果 k 位于中间位置 (即 k == 2^(n-1))，直接返回 "1"。
2. 如果 k 小于中间位置，递归调用 find_kth_bit(n-1, k)。
3. 如果 k 大于中间位置，递归调用 find_kth_bit(n-1, 2^n - k)，并翻转结果。

关键点:
- 通过递归和数学推导避免生成完整的字符串，从而优化时间和空间复杂度。
"""

# ============================================================================
