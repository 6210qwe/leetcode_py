def find_kth_bit(n: int, k: int) -> str:
    """
    递归函数 - 找到第 n 个二进制字符串中的第 k 位字符
    """
    if n == 1:
        return "0"
    mid = (1 << (n - 1))
    if k == mid:
        return "1"
    elif k < mid:
        return find_kth_bit(n - 1, k)
    else:
        return "1" if find_kth_bit(n - 1, 2 * mid - k) == "0" else "0"


