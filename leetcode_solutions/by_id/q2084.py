"""
1953. 你可以工作的最大周数 - 给你 n 个项目，编号从 0 到 n - 1 。同时给你一个整数数组 milestones ，其中每个 milestones[i] 表示第 i 个项目中的阶段任务数量。 你可以按下面两个规则参与项目中的工作： * 每周，你将会完成 某一个 项目中的 恰好一个 阶段任务。你每周都 必须 工作。 * 在 连续的 两周中，你 不能 参与并完成同一个项目中的两个阶段任务。 一旦所有项目中的全部阶段任务都完成，或者执行仅剩的一个阶段任务将会导致你违反上面的规则，你将 停止工作。注意，由于这些条件的限制，你可能无法完成所有阶段任务。 返回在不违反上面规则的情况下你 最多 能工作多少周。 示例 1： 输入：milestones = [1,2,3] 输出：6 解释：一种可能的情形是： ​​​​- 第 1 周，你参与并完成项目 0 中的一个阶段任务。 - 第 2 周，你参与并完成项目 2 中的一个阶段任务。 - 第 3 周，你参与并完成项目 1 中的一个阶段任务。 - 第 4 周，你参与并完成项目 2 中的一个阶段任务。 - 第 5 周，你参与并完成项目 1 中的一个阶段任务。 - 第 6 周，你参与并完成项目 2 中的一个阶段任务。 总周数是 6 。 示例 2： 输入：milestones = [5,2,1] 输出：7 解释：一种可能的情形是： - 第 1 周，你参与并完成项目 0 中的一个阶段任务。 - 第 2 周，你参与并完成项目 1 中的一个阶段任务。 - 第 3 周，你参与并完成项目 0 中的一个阶段任务。 - 第 4 周，你参与并完成项目 1 中的一个阶段任务。 - 第 5 周，你参与并完成项目 0 中的一个阶段任务。 - 第 6 周，你参与并完成项目 2 中的一个阶段任务。 - 第 7 周，你参与并完成项目 0 中的一个阶段任务。 总周数是 7 。 注意，你不能在第 8 周参与完成项目 0 中的最后一个阶段任务，因为这会违反规则。 因此，项目 0 中会有一个阶段任务维持未完成状态。 提示： * n == milestones.length * 1 <= n <= 105 * 1 <= milestones[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法来最大化工作周数。如果最大的项目任务数大于其他所有项目的任务数之和，则最多可以工作 (总任务数 - 最大任务数) * 2 + 1 周。否则，可以工作总任务数周。

算法步骤:
1. 计算总任务数 total 和最大任务数 max_milestone。
2. 如果 max_milestone 大于等于 total - max_milestone，则返回 (total - max_milestone) * 2 + 1。
3. 否则，返回 total。

关键点:
- 通过比较最大任务数和其他任务数之和来决定最大工作周数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 milestones 的长度。需要遍历一次数组来计算总任务数和最大任务数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def maximum_weeks(milestones: List[int]) -> int:
    """
    函数式接口 - 计算最多能工作多少周
    """
    total = sum(milestones)
    max_milestone = max(milestones)
    
    if max_milestone >= total - max_milestone:
        return (total - max_milestone) * 2 + 1
    else:
        return total


Solution = create_solution(maximum_weeks)