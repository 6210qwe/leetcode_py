"""
1051. 高度检查器 - 学校打算为全体学生拍一张年度纪念照。根据要求，学生需要按照 非递减 的高度顺序排成一行。 排序后的高度情况用整数数组 expected 表示，其中 expected[i] 是预计排在这一行中第 i 位的学生的高度（下标从 0 开始）。 给你一个整数数组 heights ，表示 当前学生站位 的高度情况。heights[i] 是这一行中第 i 位学生的高度（下标从 0 开始）。 返回满足 heights[i] != expected[i] 的 下标数量 。 示例： 输入：heights = [1,1,4,2,1,3] 输出：3 解释： 高度：[1,1,4,2,1,3] 预期：[1,1,1,2,3,4] 下标 2 、4 、5 处的学生高度不匹配。 示例 2： 输入：heights = [5,1,2,3,4] 输出：5 解释： 高度：[5,1,2,3,4] 预期：[1,2,3,4,5] 所有下标的对应学生高度都不匹配。 示例 3： 输入：heights = [1,2,3,4,5] 输出：0 解释： 高度：[1,2,3,4,5] 预期：[1,2,3,4,5] 所有下标的对应学生高度都匹配。 提示： * 1 <= heights.length <= 100 * 1 <= heights[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数排序来对高度进行排序，并比较原数组和排序后的数组，统计不匹配的元素个数。

算法步骤:
1. 使用计数排序对高度数组进行排序。
2. 比较原数组和排序后的数组，统计不匹配的元素个数。

关键点:
- 计数排序的时间复杂度为 O(n)，适用于高度范围较小的情况。
- 通过比较原数组和排序后的数组，可以快速找到不匹配的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1) （因为高度范围固定为 1 到 100，所以额外空间是常数级别的）
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def height_checker(heights: List[int]) -> int:
    """
    函数式接口 - 实现高度检查器
    """
    # 计数排序
    count = [0] * 101
    for height in heights:
        count[height] += 1
    
    # 生成排序后的数组
    sorted_heights = []
    for i in range(1, 101):
        sorted_heights.extend([i] * count[i])
    
    # 统计不匹配的元素个数
    mismatch_count = 0
    for i in range(len(heights)):
        if heights[i] != sorted_heights[i]:
            mismatch_count += 1
    
    return mismatch_count

