# ============================================================================
# 问题描述
# ============================================================================
"""
3469. 移除所有数组元素的最小代价 - 给你一个整数数组 nums。你的任务是在每一步中执行以下操作之一，直到 nums 为空，从而移除 所有元素 ：
* 从 nums 的前三个元素中选择任意两个元素并移除它们。此操作的成本为移除的两个元素中的 最大值 。
* 如果 nums 中剩下的元素少于三个，则一次性移除所有剩余元素。此操作的成本为剩余元素中的 最大值 。
返回移除所有元素所需的最小成本。
示例 1 输入：nums = [6,2,8,4] 输出：12 解释： 初始时，nums = [6, 2, 8, 4]。
* 在第一次操作中，移除 nums[0] = 6 和 nums[2] = 8，操作成本为 max(6, 8) = 8。现在，nums = [2, 4]。
* 在第二次操作中，移除剩余元素，操作成本为 max(2, 4) = 4。
移除所有元素的成本为 8 + 4 = 12。这是移除 nums 中所有元素的最小成本。所以输出 12。
示例 2 输入：nums = [2,1,3,3] 输出：5 解释： 初始时，nums = [2, 1, 3, 3]。
* 在第一次操作中，移除 nums[0] = 2 和 nums[1] = 1，操作成本为 max(2, 1) = 2。现在，nums = [3, 3]。
* 在第二次操作中，移除剩余元素，操作成本为 max(3, 3) = 3。
移除所有元素的成本为 2 + 3 = 5。这是移除 nums 中所有元素的最小成本。因此，输出是 5。
提示：
* 1 <= nums.length <= 1000
* 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，每次尽量选择最小的两个元素进行移除，以最小化每次操作的成本。

算法步骤:
1. 将数组排序。
2. 每次选择前两个元素进行移除，并计算其最大值作为本次操作的成本。
3. 更新数组，继续上述操作，直到数组为空。
4. 返回总成本。

关键点:
- 通过排序确保每次选择的都是当前最小的两个元素。
- 通过贪心策略，每次选择最小的两个元素进行移除，以最小化总成本。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度为 O(n log n)，后续遍历操作为 O(n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_minimum_cost_to_remove_array_elements(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 对数组进行排序
    nums.sort()
    
    total_cost = 0
    while len(nums) > 0:
        if len(nums) >= 3:
            # 选择前两个元素进行移除
            cost = max(nums[0], nums[1])
            nums = nums[2:]
        else:
            # 剩余元素少于三个，一次性移除所有剩余元素
            cost = max(nums)
            nums = []
        total_cost += cost
    
    return total_cost


Solution = create_solution(find_minimum_cost_to_remove_array_elements)