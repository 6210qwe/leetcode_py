"""
2486. 追加字符以获得子序列 - 给你两个仅由小写英文字母组成的字符串 s 和 t 。 现在需要通过向 s 末尾追加字符的方式使 t 变成 s 的一个 子序列 ，返回需要追加的最少字符数。 子序列是一个可以由其他字符串删除部分（或不删除）字符但不改变剩下字符顺序得到的字符串。 示例 1： 输入：s = "coaching", t = "coding" 输出：4 解释：向 s 末尾追加字符串 "ding" ，s = "coachingding" 。 现在，t 是 s ("coachingding") 的一个子序列。 可以证明向 s 末尾追加任何 3 个字符都无法使 t 成为 s 的一个子序列。 示例 2： 输入：s = "abcde", t = "a" 输出：0 解释：t 已经是 s ("abcde") 的一个子序列。 示例 3： 输入：s = "z", t = "abcde" 输出：5 解释：向 s 末尾追加字符串 "abcde" ，s = "zabcde" 。 现在，t 是 s ("zabcde") 的一个子序列。 可以证明向 s 末尾追加任何 4 个字符都无法使 t 成为 s 的一个子序列。 提示： * 1 <= s.length, t.length <= 105 * s 和 t 仅由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来检查 t 是否已经是 s 的子序列，并计算需要追加的字符数。

算法步骤:
1. 初始化两个指针 i 和 j，分别指向 s 和 t 的起始位置。
2. 遍历 s 和 t，如果 s[i] == t[j]，则同时移动 i 和 j；否则只移动 i。
3. 当遍历完 s 或 t 时，返回 t 剩余未匹配的长度。

关键点:
- 使用双指针方法可以高效地检查 t 是否已经是 s 的子序列。
- 通过计算 t 剩余未匹配的长度，确定需要追加的字符数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + m)，其中 n 是 s 的长度，m 是 t 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str, t: str) -> int:
    """
    函数式接口 - 返回需要追加的最少字符数
    """
    i, j = 0, 0
    while i < len(s) and j < len(t):
        if s[i] == t[j]:
            j += 1
        i += 1
    
    return len(t) - j


Solution = create_solution(solution_function_name)