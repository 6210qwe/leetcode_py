def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询来计算每种披萨配料的成本
    """
    # 实现最优解法
    query = """
    SELECT 
        topping_name,
        SUM(topping_cost) AS total_cost
    FROM 
        pizza_toppings
    GROUP BY 
        topping_name
    ORDER BY 
        total_cost DESC;
    """
    return query


