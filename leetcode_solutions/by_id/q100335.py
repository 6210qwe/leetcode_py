def solution_function_name(dataA: int, dataB: int) -> int:
    """
    函数式接口 - 使用位运算实现两数之和
    """
    # 32 位整数的最大值
    MAX = 0x7FFFFFFF
    # 32 位掩码
    mask = 0xFFFFFFFF
    
    while dataB != 0:
        # 计算无进位和
        sum_without_carry = (dataA ^ dataB) & mask
        # 计算进位
        carry = ((dataA & dataB) << 1) & mask
        # 更新 dataA 和 dataB
        dataA, dataB = sum_without_carry, carry
    
    # 如果结果是负数，取反加1
    return dataA if dataA <= MAX else ~(dataA ^ mask)

Solution = create_solution(solution_function_name)