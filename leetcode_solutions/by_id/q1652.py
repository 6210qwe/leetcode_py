"""
1529. 最少的后缀翻转次数 - 给你一个长度为 n 、下标从 0 开始的二进制字符串 target 。你自己有另一个长度为 n 的二进制字符串 s ，最初每一位上都是 0 。你想要让 s 和 target 相等。 在一步操作，你可以选择下标 i（0 <= i < n）并翻转在 闭区间 [i, n - 1] 内的所有位。翻转意味着 '0' 变为 '1' ，而 '1' 变为 '0' 。 返回使 s 与 target 相等需要的最少翻转次数。 示例 1： 输入：target = "10111" 输出：3 解释：最初，s = "00000" 。 选择下标 i = 2: "00000" -> "00111" 选择下标 i = 0: "00111" -> "11000" 选择下标 i = 1: "11000" -> "10111" 要达成目标，需要至少 3 次翻转。 示例 2： 输入：target = "101" 输出：3 解释：最初，s = "000" 。 选择下标 i = 0: "000" -> "111" 选择下标 i = 1: "111" -> "100" 选择下标 i = 2: "100" -> "101" 要达成目标，需要至少 3 次翻转。 示例 3： 输入：target = "00000" 输出：0 解释：由于 s 已经等于目标，所以不需要任何操作 提示： * n == target.length * 1 <= n <= 105 * target[i] 为 '0' 或 '1'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，从左到右遍历 target 字符串，记录当前字符是否需要翻转。如果当前字符与前一个字符不同，则需要一次翻转。

算法步骤:
1. 初始化翻转次数为 0。
2. 遍历 target 字符串，从第二个字符开始。
3. 如果当前字符与前一个字符不同，则增加翻转次数。
4. 返回翻转次数。

关键点:
- 通过比较相邻字符来确定是否需要翻转。
- 从左到右遍历可以确保每次翻转都尽可能覆盖更多的字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 target 的长度。我们只需要遍历一次 target 字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_flips(target: str) -> int:
    """
    函数式接口 - 计算使 s 与 target 相等需要的最少翻转次数
    """
    flips = 0
    current_char = '0'
    
    for char in target:
        if char != current_char:
            flips += 1
            current_char = char
    
    return flips


Solution = create_solution(min_flips)