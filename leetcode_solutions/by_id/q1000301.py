"""
LCR 047. 二叉树剪枝 - 给定一个二叉树 根节点 root ，树的每个节点的值要么是 0，要么是 1。请剪除该二叉树中所有节点的值为 0 的子树。 节点 node 的子树为 node 本身，以及所有 node 的后代。 示例 1： 输入: [1,null,0,0,1] 输出: [1,null,0,null,1] 解释: 只有红色节点满足条件“所有不包含 1 的子树”。 右图为返回的答案。 [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/06/1028_2.png] 示例 2： 输入: [1,0,1,0,0,0,1] 输出: [1,null,1,null,1] 解释: [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/06/1028_1.png] 示例 3： 输入: [1,1,0,1,1,0,1,0] 输出: [1,1,0,1,1,null,1] 解释: [https://s3-lc-upload.s3.amazonaws.com/uploads/2018/04/05/1028.png] 提示： * 二叉树的节点个数的范围是 [1,200] * 二叉树节点的值只会是 0 或 1 注意：本题与主站 814 题相同：https://leetcode.cn/problems/binary-tree-pruning/ [https://leetcode.cn/problems/binary-tree-pruning/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用后序遍历（自底向上）来剪枝，确保在处理当前节点时，其子树已经被处理过。

算法步骤:
1. 定义一个辅助函数 `prune_tree` 来递归地剪枝。
2. 对于每个节点，先递归地处理其左子树和右子树。
3. 如果当前节点的左子树或右子树被剪枝，则将其置为 None。
4. 如果当前节点的值为 0 且其左右子树都为空，则剪掉当前节点。
5. 返回处理后的根节点。

关键点:
- 使用后序遍历来确保子树已经处理完毕。
- 递归地处理每个节点及其子树。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是二叉树的节点数。每个节点都被访问一次。
空间复杂度: O(h)，其中 h 是二叉树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def prune_tree(root: Optional[TreeNode]) -> Optional[TreeNode]:
    """
    剪除二叉树中所有节点的值为 0 的子树。
    """
    if not root:
        return None

    # 递归处理左子树
    root.left = prune_tree(root.left)
    # 递归处理右子树
    root.right = prune_tree(root.right)

    # 如果当前节点的值为 0 且其左右子树都为空，则剪掉当前节点
    if root.val == 0 and not root.left and not root.right:
        return None

    return root


Solution = create_solution(prune_tree)