from leetcode_solutions.utils.solution import create_solution


def strobogrammatic_number(num: str) -> bool:
    """
    函数式接口 - 中心对称数
    
    实现思路:
    中心对称数旋转180度后还是自己，使用哈希表存储对称字符对。
    
    Args:
        num: 字符串数字
        
    Returns:
        是否是中心对称数
        
    Example:
        >>> strobogrammatic_number("69")
        True
    """
    mapping = {'0': '0', '1': '1', '6': '9', '8': '8', '9': '6'}
    
    left, right = 0, len(num) - 1
    
    while left <= right:
        if num[left] not in mapping or mapping[num[left]] != num[right]:
            return False
        left += 1
        right -= 1
    
    return True


