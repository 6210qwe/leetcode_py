"""
2328. 网格图中递增路径的数目 - 给你一个 m x n 的整数网格图 grid ，你可以从一个格子移动到 4 个方向相邻的任意一个格子。 请你返回在网格图中从 任意 格子出发，达到 任意 格子，且路径中的数字是 严格递增 的路径数目。由于答案可能会很大，请将结果对 109 + 7 取余 后返回。 如果两条路径中访问过的格子不是完全相同的，那么它们视为两条不同的路径。 示例 1： [https://assets.leetcode.com/uploads/2022/05/10/griddrawio-4.png] 输入：grid = [[1,1],[3,4]] 输出：8 解释：严格递增路径包括： - 长度为 1 的路径：[1]，[1]，[3]，[4] 。 - 长度为 2 的路径：[1 -> 3]，[1 -> 4]，[3 -> 4] 。 - 长度为 3 的路径：[1 -> 3 -> 4] 。 路径数目为 4 + 3 + 1 = 8 。 示例 2： 输入：grid = [[1],[2]] 输出：3 解释：严格递增路径包括： - 长度为 1 的路径：[1]，[2] 。 - 长度为 2 的路径：[1 -> 2] 。 路径数目为 2 + 1 = 3 。 提示： * m == grid.length * n == grid[i].length * 1 <= m, n <= 1000 * 1 <= m * n <= 105 * 1 <= grid[i][j] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）结合记忆化搜索（Memoization）来计算每个格子的递增路径数目。

算法步骤:
1. 初始化一个二维数组 `dp`，用于存储每个格子的递增路径数目。
2. 对于每个格子 (i, j)，使用 DFS 计算其递增路径数目，并将结果存储在 `dp` 中。
3. 在 DFS 过程中，如果当前格子的值大于相邻格子的值，则递归计算相邻格子的递增路径数目，并累加到当前格子的路径数目中。
4. 最后，遍历整个 `dp` 数组，累加所有格子的递增路径数目，得到最终结果。

关键点:
- 使用记忆化搜索避免重复计算。
- 递归过程中，确保路径是严格递增的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是网格的行数和列数。每个格子最多被访问一次。
空间复杂度: O(m * n)，用于存储 `dp` 数组和递归调用栈。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from functools import lru_cache

MOD = 10**9 + 7

def count_increasing_paths(grid: List[List[int]]) -> int:
    m, n = len(grid), len(grid[0])
    
    @lru_cache(maxsize=None)
    def dfs(i: int, j: int) -> int:
        directions = [(0, 1), (1, 0), (0, -1), (-1, 0)]
        paths = 1  # 自身也算一条路径
        for di, dj in directions:
            ni, nj = i + di, j + dj
            if 0 <= ni < m and 0 <= nj < n and grid[ni][nj] > grid[i][j]:
                paths += dfs(ni, nj)
                paths %= MOD
        return paths
    
    total_paths = 0
    for i in range(m):
        for j in range(n):
            total_paths += dfs(i, j)
            total_paths %= MOD
    
    return total_paths

