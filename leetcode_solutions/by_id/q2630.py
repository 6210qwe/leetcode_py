"""
2544. 交替数字和 - 给你一个正整数 n 。n 中的每一位数字都会按下述规则分配一个符号： * 最高有效位 上的数字分配到 正 号。 * 剩余每位上数字的符号都与其相邻数字相反。 返回所有数字及其对应符号的和。 示例 1： 输入：n = 521 输出：4 解释：(+5) + (-2) + (+1) = 4 示例 2： 输入：n = 111 输出：1 解释：(+1) + (-1) + (+1) = 1 示例 3： 输入：n = 886996 输出：0 解释：(+8) + (-8) + (+6) + (-9) + (+9) + (-6) = 0 提示： * 1 <= n <= 10^9
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历每一位数字并根据其位置决定符号，计算交替数字和。

算法步骤:
1. 将整数转换为字符串，以便逐位处理。
2. 初始化结果变量 `result` 和符号变量 `sign`。
3. 遍历字符串中的每一位数字：
   - 将当前位的数字转换为整数。
   - 根据当前位的位置（奇数或偶数）决定符号。
   - 将当前位的数字乘以符号并累加到结果中。
   - 切换符号。
4. 返回结果。

关键点:
- 使用字符串遍历可以方便地处理每一位数字。
- 通过切换符号来实现交替求和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d)，其中 d 是 n 的位数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def alternating_digit_sum(n: int) -> int:
    """
    函数式接口 - 计算交替数字和
    """
    # 将整数转换为字符串
    n_str = str(n)
    result = 0
    sign = 1  # 初始化符号为正

    # 遍历每一位数字
    for digit in n_str:
        result += int(digit) * sign
        sign *= -1  # 切换符号

    return result


Solution = create_solution(alternating_digit_sum)