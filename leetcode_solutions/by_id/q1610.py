"""
1486. 数组异或操作 - 给你两个整数，n 和 start 。 数组 nums 定义为：nums[i] = start + 2*i（下标从 0 开始）且 n == nums.length 。 请返回 nums 中所有元素按位异或（XOR）后得到的结果。 示例 1： 输入：n = 5, start = 0 输出：8 解释：数组 nums 为 [0, 2, 4, 6, 8]，其中 (0 ^ 2 ^ 4 ^ 6 ^ 8) = 8 。 "^" 为按位异或 XOR 运算符。 示例 2： 输入：n = 4, start = 3 输出：8 解释：数组 nums 为 [3, 5, 7, 9]，其中 (3 ^ 5 ^ 7 ^ 9) = 8. 示例 3： 输入：n = 1, start = 7 输出：7 示例 4： 输入：n = 10, start = 5 输出：2 提示： * 1 <= n <= 1000 * 0 <= start <= 1000 * n == nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 直接计算每个元素并进行异或操作。

算法步骤:
1. 初始化结果变量 result 为 0。
2. 遍历从 0 到 n-1 的每个索引 i，计算当前元素 nums[i] = start + 2*i。
3. 将当前元素与结果变量 result 进行异或操作。
4. 返回最终的结果 result。

关键点:
- 通过直接计算每个元素并进行异或操作，避免了显式构建整个数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def xor_operation(n: int, start: int) -> int:
    """
    函数式接口 - 计算数组中所有元素按位异或后的结果
    """
    result = 0
    for i in range(n):
        result ^= start + 2 * i
    return result


Solution = create_solution(xor_operation)