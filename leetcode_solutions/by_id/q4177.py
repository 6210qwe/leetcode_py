"""
3794. 反转字符串前缀 - 给你一个字符串 s 和一个整数 k。 反转 s 的前 k 个字符，并返回结果字符串。 示例 1: 输入: s = "abcd", k = 2 输出: "bacd" 解释: 前 k = 2 个字符 "ab" 反转为 "ba"。最终得到的结果字符串为 "bacd"。 示例 2: 输入: s = "xyz", k = 3 输出: "zyx" 解释: 前 k = 3 个字符 "xyz" 反转为 "zyx"。最终得到的结果字符串为 "zyx"。 示例 3: 输入: s = "hey", k = 1 输出: "hey" 解释: 前 k = 1 个字符 "h" 在反转后保持不变。最终得到的结果字符串为 "hey"。 提示: * 1 <= s.length <= 100 * s 仅由小写英文字母组成。 * 1 <= k <= s.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法反转字符串的前 k 个字符。

算法步骤:
1. 将字符串 s 转换为列表，以便进行原地修改。
2. 使用双指针方法反转前 k 个字符。
3. 将列表转换回字符串并返回。

关键点:
- 使用双指针方法可以在 O(k) 时间内完成反转操作。
- 字符串是不可变的，因此需要先将其转换为列表。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(k)，其中 k 是需要反转的字符数。
空间复杂度: O(n)，其中 n 是字符串的长度，因为我们需要将字符串转换为列表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def reverse_string_prefix(s: str, k: int) -> str:
    """
    函数式接口 - 反转字符串 s 的前 k 个字符，并返回结果字符串。
    """
    # 将字符串转换为列表
    s_list = list(s)
    
    # 使用双指针方法反转前 k 个字符
    left, right = 0, k - 1
    while left < right:
        s_list[left], s_list[right] = s_list[right], s_list[left]
        left += 1
        right -= 1
    
    # 将列表转换回字符串并返回
    return ''.join(s_list)


Solution = create_solution(reverse_string_prefix)