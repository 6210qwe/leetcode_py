"""
1757. 可回收且低脂的产品 - 表：Products +-------------+---------+ | Column Name | Type | +-------------+---------+ | product_id | int | | low_fats | enum | | recyclable | enum | +-------------+---------+ product_id 是该表的主键（具有唯一值的列）。 low_fats 是枚举类型，取值为以下两种 ('Y', 'N')，其中 'Y' 表示该产品是低脂产品，'N' 表示不是低脂产品。 recyclable 是枚举类型，取值为以下两种 ('Y', 'N')，其中 'Y' 表示该产品可回收，而 'N' 表示不可回收。 编写解决方案找出既是低脂又是可回收的产品编号。 返回结果 无顺序要求 。 返回结果格式如下例所示： 示例 1： 输入： Products 表： +-------------+----------+------------+ | product_id | low_fats | recyclable | +-------------+----------+------------+ | 0 | Y | N | | 1 | Y | Y | | 2 | N | Y | | 3 | Y | Y | | 4 | N | N | +-------------+----------+------------+ 输出： +-------------+ | product_id | +-------------+ | 1 | | 3 | +-------------+ 解释： 只有产品 id 为 1 和 3 的产品，既是低脂又是可回收的产品。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 查询来筛选出既是低脂又是可回收的产品。

算法步骤:
1. 从 Products 表中选择 product_id 列。
2. 使用 WHERE 子句筛选出 low_fats 为 'Y' 且 recyclable 为 'Y' 的行。

关键点:
- 使用 SQL 查询来实现高效的筛选。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 Products 表中的行数。我们需要遍历整个表来筛选符合条件的行。
空间复杂度: O(1)，查询本身不使用额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(params):
    """
    函数式接口 - 使用 SQL 查询来筛选出既是低脂又是可回收的产品。
    """
    # 实现最优解法
    query = """
    SELECT product_id
    FROM Products
    WHERE low_fats = 'Y' AND recyclable = 'Y';
    """
    return query


