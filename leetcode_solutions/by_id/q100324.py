"""
LCR 180. 文件组合 - 待传输文件被切分成多个部分，按照原排列顺序，每部分文件编号均为一个 正整数（至少含有两个文件）。传输要求为：连续文件编号总和为接收方指定数字 target 的所有文件。请返回所有符合该要求的文件传输组合列表。 注意，返回时需遵循以下规则： * 每种组合按照文件编号 升序 排列； * 不同组合按照第一个文件编号 升序 排列。 示例 1： 输入：target = 12 输出：[[3, 4, 5]] 解释：在上述示例中，存在一个连续正整数序列的和为 12，为 [3, 4, 5]。 示例 2： 输入：target = 18 输出：[[3,4,5,6],[5,6,7]] 解释：在上述示例中，存在两个连续正整数序列的和分别为 18，分别为 [3, 4, 5, 6] 和 [5, 6, 7]。 提示： * 1 <= target <= 10^5
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来找到所有符合条件的连续正整数序列。

算法步骤:
1. 初始化两个指针 left 和 right，分别指向序列的起始位置。
2. 计算当前窗口内的元素和 sum。
3. 如果 sum 小于 target，则右移 right 指针并更新 sum。
4. 如果 sum 大于 target，则右移 left 指针并更新 sum。
5. 如果 sum 等于 target，则记录当前窗口内的元素，并右移 left 指针继续寻找下一个可能的序列。

关键点:
- 使用双指针方法可以有效地找到所有符合条件的连续正整数序列。
- 通过调整指针的位置来控制窗口的大小，从而找到所有满足条件的序列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 target 的值。因为每个指针最多移动 n 次。
空间复杂度: O(1)，除了输出结果外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_continuous_sequences(target: int) -> List[List[int]]:
    """
    函数式接口 - 找到所有和为 target 的连续正整数序列
    """
    result = []
    left, right, sum = 1, 2, 3

    while left < right:
        if sum == target:
            result.append(list(range(left, right + 1)))
            sum -= left
            left += 1
        elif sum < target:
            right += 1
            sum += right
        else:
            sum -= left
            left += 1

    return result


Solution = create_solution(find_continuous_sequences)