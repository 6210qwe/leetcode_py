"""
3560. 木材运输的最小成本 - 给你三个整数 n、m 和 k。 有两根长度分别为 n 和 m 单位的木材，需要通过三辆卡车运输。每辆卡车最多只能装载一根长度 不超过 k 单位的木材。 你可以将木材切成更小的段，其中将长度为 x 的木材切割成长度为 len1 和 len2 的段的成本为 cost = len1 * len2，并且满足 len1 + len2 = x。 返回将木材分配到卡车上的 最小总成本 。如果木材不需要切割，总成本为 0。 示例 1： 输入： n = 6, m = 5, k = 5 输出： 5 解释： 将长度为 6 的木材切割成长度为 1 和 5 的两段，成本为 1 * 5 == 5。现在三段长度分别为 1、5 和 5 的木材可以分别装载到每辆卡车。 示例 2： 输入： n = 4, m = 4, k = 6 输出： 0 解释： 两根木材已经可以直接装载到卡车上，因此不需要切割。 提示： * 2 <= k <= 105 * 1 <= n, m <= 2 * k * 输入数据保证木材总存在能被运输的方案。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法来最小化切割成本。首先判断是否需要切割木材，如果需要切割，则选择最优的切割点以最小化成本。

算法步骤:
1. 检查是否需要切割木材。如果 n 和 m 都小于等于 k，则不需要切割，直接返回 0。
2. 如果 n > k，则计算将 n 切割成不超过 k 的最小成本。
3. 如果 m > k，则计算将 m 切割成不超过 k 的最小成本。
4. 返回总成本。

关键点:
- 通过贪心算法选择最优的切割点，使得切割成本最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, m: int, k: int) -> int:
    """
    函数式接口 - 计算木材运输的最小成本
    """
    def min_cost_to_cut(length: int, k: int) -> int:
        if length <= k:
            return 0
        # 贪心选择最优切割点
        for i in range(1, k + 1):
            if (length - i) <= k:
                return i * (length - i)

    # 检查是否需要切割木材
    if n <= k and m <= k:
        return 0

    # 计算切割成本
    cost_n = min_cost_to_cut(n, k)
    cost_m = min_cost_to_cut(m, k)

    return (cost_n if cost_n is not None else 0) + (cost_m if cost_m is not None else 0)


Solution = create_solution(solution_function_name)