"""
931. 下降路径最小和 - 给你一个 n x n 的 方形 整数数组 matrix ，请你找出并返回通过 matrix 的下降路径 的 最小和 。 下降路径 可以从第一行中的任何元素开始，并从每一行中选择一个元素。在下一行选择的元素和当前行所选元素最多相隔一列（即位于正下方或者沿对角线向左或者向右的第一个元素）。具体来说，位置 (row, col) 的下一个元素应当是 (row + 1, col - 1)、(row + 1, col) 或者 (row + 1, col + 1) 。 示例 1： [https://pic.leetcode.cn/1729566253-aneDag-image.png] 输入：matrix = [[2,1,3],[6,5,4],[7,8,9]] 输出：13 解释：如图所示，为和最小的两条下降路径 示例 2： [https://pic.leetcode.cn/1729566282-dtXwRd-image.png] 输入：matrix = [[-19,57],[-40,-5]] 输出：-59 解释：如图所示，为和最小的下降路径 提示： * n == matrix.length == matrix[i].length * 1 <= n <= 100 * -100 <= matrix[i][j] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们可以在原矩阵上进行修改，从而节省空间。

算法步骤:
1. 初始化一个与输入矩阵大小相同的二维数组 dp，dp[i][j] 表示从第一行到第 i 行，且第 i 行选择第 j 列的最小路径和。
2. 对于第一行，dp[0][j] 就是 matrix[0][j]。
3. 对于其他行，dp[i][j] = matrix[i][j] + min(dp[i-1][j-1], dp[i-1][j], dp[i-1][j+1])，其中需要处理边界情况。
4. 最后返回 dp[n-1] 中的最小值。

关键点:
- 在原矩阵上进行修改，避免额外的空间开销。
- 处理边界情况时，确保索引不越界。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是矩阵的边长。我们需要遍历整个矩阵。
空间复杂度: O(1)，我们在原矩阵上进行修改，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(matrix: List[List[int]]) -> int:
    """
    函数式接口 - 返回矩阵的最小下降路径和
    """
    if not matrix or not matrix[0]:
        return 0

    n = len(matrix)

    for i in range(1, n):
        for j in range(n):
            # 处理边界情况
            left = matrix[i-1][j-1] if j > 0 else float('inf')
            up = matrix[i-1][j]
            right = matrix[i-1][j+1] if j < n-1 else float('inf')

            # 更新当前单元格的值
            matrix[i][j] += min(left, up, right)

    # 返回最后一行的最小值
    return min(matrix[-1])


Solution = create_solution(solution_function_name)