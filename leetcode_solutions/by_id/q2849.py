"""
2763. 所有子数组中不平衡数字之和 - 一个长度为 n 下标从 0 开始的整数数组 arr 的 不平衡数字 定义为，在 sarr = sorted(arr) 数组中，满足以下条件的下标数目： * 0 <= i < n - 1 ，和 * sarr[i+1] - sarr[i] > 1 这里，sorted(arr) 表示将数组 arr 排序后得到的数组。 给你一个下标从 0 开始的整数数组 nums ，请你返回它所有 子数组 的 不平衡数字 之和。 子数组指的是一个数组中连续一段 非空 的元素序列。 示例 1： 输入：nums = [2,3,1,4] 输出：3 解释：总共有 3 个子数组有非 0 不平衡数字： - 子数组 [3, 1] ，不平衡数字为 1 。 - 子数组 [3, 1, 4] ，不平衡数字为 1 。 - 子数组 [1, 4] ，不平衡数字为 1 。 其他所有子数组的不平衡数字都是 0 ，所以所有子数组的不平衡数字之和为 3 。 示例 2： 输入：nums = [1,3,3,3,5] 输出：8 解释：总共有 7 个子数组有非 0 不平衡数字： - 子数组 [1, 3] ，不平衡数字为 1 。 - 子数组 [1, 3, 3] ，不平衡数字为 1 。 - 子数组 [1, 3, 3, 3] ，不平衡数字为 1 。 - 子数组 [1, 3, 3, 3, 5] ，不平衡数字为 2 。 - 子数组 [3, 3, 3, 5] ，不平衡数字为 1 。 - 子数组 [3, 3, 5] ，不平衡数字为 1 。 - 子数组 [3, 5] ，不平衡数字为 1 。 其他所有子数组的不平衡数字都是 0 ，所以所有子数组的不平衡数字之和为 8 。 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= nums.length
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和有序集合来高效计算每个子数组的不平衡数字。

算法步骤:
1. 初始化结果变量 `result` 为 0。
2. 使用两个指针 `left` 和 `right` 来表示当前子数组的左右边界。
3. 使用一个有序集合 `active` 来存储当前子数组中的元素。
4. 遍历数组 `nums`，对于每个元素 `num`：
   - 将 `num` 加入 `active`。
   - 计算当前子数组的不平衡数字，并更新 `result`。
   - 如果 `active` 中的元素数量超过 `n`，则移除 `left` 指针指向的元素，并移动 `left` 指针。
5. 返回 `result`。

关键点:
- 使用有序集合来高效维护当前子数组中的元素及其顺序。
- 通过滑动窗口来避免重复计算子数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2 log n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from sortedcontainers import SortedSet

def sum_of_imbalance_numbers(nums: List[int]) -> int:
    n = len(nums)
    result = 0
    
    for left in range(n):
        active = SortedSet()
        imbalance_count = 0
        
        for right in range(left, n):
            num = nums[right]
            if num not in active:
                active.add(num)
                # 计算加入新元素后的不平衡数字
                if num - 1 in active and num + 1 in active:
                    imbalance_count -= 1
                elif num - 1 not in active and num + 1 not in active:
                    imbalance_count += 1
            
            result += imbalance_count
    
    return result

