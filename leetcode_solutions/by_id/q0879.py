"""
849. 到最近的人的最大距离 - 给你一个数组 seats 表示一排座位，其中 seats[i] = 1 代表有人坐在第 i 个座位上，seats[i] = 0 代表座位 i 上是空的（下标从 0 开始）。 至少有一个空座位，且至少有一人已经坐在座位上。 亚历克斯希望坐在一个能够使他与离他最近的人之间的距离达到最大化的座位上。 返回他到离他最近的人的最大距离。 示例 1： [https://assets.leetcode.com/uploads/2020/09/10/distance.jpg] 输入：seats = [1,0,0,0,1,0,1] 输出：2 解释： 如果亚历克斯坐在第二个空位（seats[2]）上，他到离他最近的人的距离为 2 。 如果亚历克斯坐在其它任何一个空位上，他到离他最近的人的距离为 1 。 因此，他到离他最近的人的最大距离是 2 。 示例 2： 输入：seats = [1,0,0,0] 输出：3 解释： 如果亚历克斯坐在最后一个座位上，他离最近的人有 3 个座位远。 这是可能的最大距离，所以答案是 3 。 示例 3： 输入：seats = [0,1] 输出：1 提示： * 2 <= seats.length <= 2 * 104 * seats[i] 为 0 或 1 * 至少有一个 空座位 * 至少有一个 座位上有人
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组找到所有相邻的两个人之间的最大距离，并考虑两端的特殊情况。

算法步骤:
1. 初始化两个指针 `prev` 和 `curr`，分别记录上一个人的位置和当前人的位置。
2. 遍历数组，更新 `prev` 和 `curr` 的位置，并计算它们之间的距离。
3. 计算两端的特殊情况，即第一个空座位和最后一个空座位到最近的人的距离。
4. 返回最大距离。

关键点:
- 处理两端的特殊情况。
- 使用双指针法计算相邻两人之间的最大距离。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 seats 的长度。我们只需要一次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(seats: List[int]) -> int:
    """
    函数式接口 - 实现
    """
    n = len(seats)
    prev, curr, max_distance = -1, 0, 0

    # 遍历数组，找到相邻两人之间的最大距离
    while curr < n:
        if seats[curr] == 1:
            if prev == -1:
                # 第一个空座位到最近的人的距离
                max_distance = max(max_distance, curr)
            else:
                # 中间空座位到最近的人的距离
                max_distance = max(max_distance, (curr - prev) // 2)
            prev = curr
        curr += 1

    # 最后一个空座位到最近的人的距离
    max_distance = max(max_distance, n - 1 - prev)

    return max_distance


Solution = create_solution(solution_function_name)