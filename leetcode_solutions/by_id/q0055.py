"""
55. 跳跃游戏 - 给你一个非负整数数组 nums ，你最初位于数组的 第一个下标 。数组中的每个元素代表你在该位置可以跳跃的最大长度。 判断你是否能够到达最后一个下标，如果可以，返回 true ；否则，返回 false 。 示例 1： 输入：nums = [2,3,1,1,4] 输出：true 解释：可以先跳 1 步，从下标 0 到达下标 1, 然后再从下标 1 跳 3 步到达最后一个下标。 示例 2： 输入：nums = [3,2,1,0,4] 输出：false 解释：无论怎样，总会到达下标为 3 的位置。但该下标的最大跳跃长度是 0 ， 所以永远不可能到达最后一个下标。 提示： * 1 <= nums.length <= 104 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 贪心算法，维护能到达的最远位置

算法步骤:
1. 初始化：farthest = 0（能到达的最远位置）
2. 遍历数组，对于每个位置i：
   - 如果i > farthest，说明无法到达当前位置，返回False
   - 更新farthest = max(farthest, i + nums[i])
   - 如果farthest >= n-1，说明可以到达终点，返回True
3. 遍历完成，返回True

关键点:
- 贪心策略：每次更新能到达的最远位置
- 如果当前位置超出能到达的最远位置，说明无法到达
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 只需一次遍历
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def can_jump(nums: List[int]) -> bool:
    """
    函数式接口 - 贪心算法
    
    实现思路:
    使用贪心算法，维护能到达的最远位置，判断是否能到达最后一个下标。
    
    Args:
        nums: 非负整数数组，nums[i]表示在该位置可以跳跃的最大长度
        
    Returns:
        如果能到达最后一个下标返回True，否则返回False
        
    Example:
        >>> can_jump([2, 3, 1, 1, 4])
        True
        >>> can_jump([3, 2, 1, 0, 4])
        False
    """
    n = len(nums)
    farthest = 0
    
    for i in range(n):
        # 如果当前位置超出能到达的最远位置，返回False
        if i > farthest:
            return False
        # 更新能到达的最远位置
        farthest = max(farthest, i + nums[i])
        # 如果能到达终点，提前返回
        if farthest >= n - 1:
            return True
    
    return True


