"""
818. 赛车 - 你的赛车可以从位置 0 开始，并且速度为 +1 ，在一条无限长的数轴上行驶。赛车也可以向负方向行驶。赛车可以按照由加速指令 'A' 和倒车指令 'R' 组成的指令序列自动行驶。 * 当收到指令 'A' 时，赛车这样行驶： * position += speed * speed *= 2 * 当收到指令 'R' 时，赛车这样行驶： * 如果速度为正数，那么speed = -1 * 否则 speed = 1 当前所处位置不变。 例如，在执行指令 "AAR" 后，赛车位置变化为 0 --> 1 --> 3 --> 3 ，速度变化为 1 --> 2 --> 4 --> -1 。 给你一个目标位置 target ，返回能到达目标位置的最短指令序列的长度。 示例 1： 输入：target = 3 输出：2 解释： 最短指令序列是 "AA" 。 位置变化 0 --> 1 --> 3 。 示例 2： 输入：target = 6 输出：5 解释： 最短指令序列是 "AAARA" 。 位置变化 0 --> 1 --> 3 --> 7 --> 7 --> 6 。 提示： * 1 <= target <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和记忆化搜索来找到最短指令序列。

算法步骤:
1. 定义一个递归函数 `dp`，使用记忆化搜索来避免重复计算。
2. 在每个位置，尝试两种操作：加速 'A' 和倒车 'R'。
3. 对于加速操作，更新位置和速度，并递归调用 `dp`。
4. 对于倒车操作，更新速度，并递归调用 `dp`。
5. 返回到达目标位置的最短指令序列长度。

关键点:
- 使用记忆化搜索来优化递归过程，减少重复计算。
- 通过递归地探索所有可能的操作路径，找到最短路径。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(target * log(target))
空间复杂度: O(target * log(target))
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
from functools import lru_cache

def racecar(target: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    @lru_cache(None)
    def dp(position: int, speed: int) -> int:
        if abs(position) > 2 * target:
            return float('inf')
        if position == target:
            return 0
        # 加速
        move = dp(position + speed, speed * 2) + 1
        # 倒车
        if (position + speed > target and speed > 0) or (position + speed < target and speed < 0):
            move = min(move, dp(position, -speed // abs(speed)) + 1)
        return move

    return dp(0, 1)

