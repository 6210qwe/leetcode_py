def arithmetic_slices(nums: List[int]) -> int:
    """
    函数式接口 - 返回数组中等差子数组的总个数
    
    实现思路:
    使用动态规划思想，定义 dp[i] 表示以索引 i 结尾的等差子数组个数。
    若 nums[i-2], nums[i-1], nums[i] 构成等差，则 dp[i] = dp[i-1] + 1；
    否则 dp[i] = 0。最终结果为 sum(dp)。
    实际实现中可空间优化为单变量累加。

    Args:
        nums: 整数数组，长度范围 [1, 5000]
        
    Returns:
        int: 所有等差子数组（长度 ≥3）的个数
        
    Example:
        >>> arithmetic_slices([1,2,3,4])
        3
        >>> arithmetic_slices([1])
        0
        >>> arithmetic_slices([1,2,3,4,5])
        6
    """
    n = len(nums)
    if n < 3:
        return 0

    total = 0
    cnt = 0  # 当前连续等差延伸长度（即以 i 结尾的等差子数组个数）

    for i in range(2, n):
        if nums[i] - nums[i-1] == nums[i-1] - nums[i-2]:
            cnt += 1
            total += cnt
        else:
            cnt = 0

    return total


