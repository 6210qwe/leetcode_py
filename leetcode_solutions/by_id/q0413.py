"""
核心思想: 动态规划 / 滑动窗口 —— 利用连续等差子数组长度递推计数

算法步骤:
1. 若数组长度 < 3，直接返回 0（无法构成等差数列）
2. 遍历数组，计算每对相邻元素的差值 diff
3. 维护当前连续等差段长度 cnt（初始为 0），当 nums[i] - nums[i-1] == nums[i-1] - nums[i-2] 时，说明可延长前一段等差子数组，则 cnt += 1，该位置新增 cnt 个等差子数组（以 i 结尾的长度 ≥3 的等差子数组数）
4. 否则重置 cnt = 0
5. 累加所有 cnt 得到总数

关键点:
- 注意边界条件：i 从 2 开始遍历（需访问 i-2）
- 优化时间和空间复杂度：仅需 O(1) 额外空间，O(n) 时间
"""

