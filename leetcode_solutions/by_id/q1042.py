"""
1000. 合并石头的最低成本 - 有 n 堆石头排成一排，第 i 堆中有 stones[i] 块石头。 每次 移动 需要将 连续的 k 堆石头合并为一堆，而这次移动的成本为这 k 堆中石头的总数。 返回把所有石头合并成一堆的最低成本。如果无法合并成一堆，返回 -1 。 示例 1： 输入：stones = [3,2,4,1], K = 2 输出：20 解释： 从 [3, 2, 4, 1] 开始。 合并 [3, 2]，成本为 5，剩下 [5, 4, 1]。 合并 [4, 1]，成本为 5，剩下 [5, 5]。 合并 [5, 5]，成本为 10，剩下 [10]。 总成本 20，这是可能的最小值。 示例 2： 输入：stones = [3,2,4,1], K = 3 输出：-1 解释：任何合并操作后，都会剩下 2 堆，我们无法再进行合并。所以这项任务是不可能完成的。. 示例 3： 输入：stones = [3,5,1,2,6], K = 3 输出：25 解释： 从 [3, 5, 1, 2, 6] 开始。 合并 [5, 1, 2]，成本为 8，剩下 [3, 8, 6]。 合并 [3, 8, 6]，成本为 17，剩下 [17]。 总成本 25，这是可能的最小值。 提示： * n == stones.length * 1 <= n <= 30 * 1 <= stones[i] <= 100 * 2 <= k <= 30
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。定义 dp[i][j][m] 表示将区间 [i, j] 的石头合并成 m 堆的最小成本。

算法步骤:
1. 初始化前缀和数组 prefix_sum，用于快速计算区间和。
2. 初始化 dp 数组，dp[i][j][m] 表示将区间 [i, j] 的石头合并成 m 堆的最小成本。
3. 遍历区间长度 l，从 1 到 n。
4. 对于每个区间长度 l，遍历起点 i，从 0 到 n-l。
5. 计算终点 j = i + l - 1。
6. 如果区间 [i, j] 的长度小于 m，则直接跳过。
7. 如果 m 为 1，则 dp[i][j][1] 等于 dp[i][j][k] + 区间 [i, j] 的总和。
8. 如果 m 大于 1，则 dp[i][j][m] 等于 dp[i][mid][1] + dp[mid+1][j][m-1] 的最小值。

关键点:
- 使用前缀和数组快速计算区间和。
- 动态规划的状态转移方程。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3 * k)
空间复杂度: O(n^2 * k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def merge_stones(stones: List[int], k: int) -> int:
    """
    函数式接口 - 合并石头的最低成本
    """
    n = len(stones)
    if (n - 1) % (k - 1) != 0:
        return -1

    # 前缀和数组
    prefix_sum = [0] * (n + 1)
    for i in range(1, n + 1):
        prefix_sum[i] = prefix_sum[i - 1] + stones[i - 1]

    # 动态规划数组
    dp = [[[float('inf')] * (k + 1) for _ in range(n)] for _ in range(n)]

    # 初始化 dp 数组
    for i in range(n):
        dp[i][i][1] = 0

    # 动态规划状态转移
    for l in range(2, n + 1):  # 区间长度
        for i in range(n - l + 1):
            j = i + l - 1
            for m in range(2, k + 1):
                if (j - i + 1) < m:
                    continue
                for mid in range(i, j, k - 1):
                    dp[i][j][m] = min(dp[i][j][m], dp[i][mid][1] + dp[mid + 1][j][m - 1])
            if (j - i) % (k - 1) == 0:
                dp[i][j][1] = dp[i][j][k] + prefix_sum[j + 1] - prefix_sum[i]

    return dp[0][n - 1][1]


Solution = create_solution(merge_stones)