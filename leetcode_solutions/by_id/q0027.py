# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 双指针法，使用快慢指针，快指针遍历数组，慢指针指向下一个不等于val的元素位置

算法步骤:
1. 使用slow指针指向下一个不等于val的元素位置（初始为0）
2. 使用fast指针遍历数组
3. 如果nums[fast] != val，说明遇到不等于val的元素：
   - 将nums[fast]放到nums[slow]位置
   - slow指针右移
4. 返回slow（不等于val的元素数量）

关键点:
- 双指针法可以原地修改数组，不需要额外空间
- 时间复杂度O(n)，空间复杂度O(1)
- 元素的相对顺序保持不变
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 只需遍历数组一次，n为数组长度
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def remove_element(nums: List[int], val: int) -> int:
    """
    函数式接口 - 双指针法实现
    
    实现思路:
    使用快慢指针，快指针遍历数组，慢指针指向下一个不等于val的元素位置。
    
    Args:
        nums: 整数数组
        val: 要移除的值
        
    Returns:
        不等于val的元素数量
        
    Example:
        >>> nums = [3,2,2,3]
        >>> remove_element(nums, 3)
        2
        >>> nums[:2]
        [2, 2]
    """
    slow = 0  # 慢指针，指向下一个不等于val的元素位置
    for fast in range(len(nums)):
        # 如果当前元素不等于val，保留它
        if nums[fast] != val:
            nums[slow] = nums[fast]
            slow += 1
    
    return slow


