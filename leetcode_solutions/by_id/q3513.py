"""
核心思想: 使用单调栈来维护一个递增的子序列，并计算跳跃得分。

算法步骤:
1. 初始化一个单调栈和一个结果列表。
2. 遍历数组，对于每个元素：
   - 如果当前元素大于栈顶元素，则将栈顶元素出栈，并计算跳跃得分。
   - 将当前元素入栈。
3. 返回结果列表中的最大值。

关键点:
- 使用单调栈来维护递增子序列。
- 计算跳跃得分时，需要考虑当前元素与栈顶元素之间的距离。
"""

# ============================================================================
# 复杂度分析
