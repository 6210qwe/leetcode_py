from typing import List

def max_hopping_score(nums: List[int]) -> int:
    """
    函数式接口 - 计算最大数组跳跃得分
    """
    stack = []
    max_score = 0
    
    for i, num in enumerate(nums):
        while stack and nums[stack[-1]] < num:
            j = stack.pop()
            score = (i - j) * nums[j]
            max_score = max(max_score, score)
        stack.append(i)
    
    return max_score

