"""
1358. 包含所有三种字符的子字符串数目 - 给你一个字符串 s ，它只包含三种字符 a, b 和 c 。 请你返回 a，b 和 c 都 至少 出现过一次的子字符串数目。 示例 1： 输入：s = "abcabc" 输出：10 解释：包含 a，b 和 c 各至少一次的子字符串为 "abc", "abca", "abcab", "abcabc", "bca", "bcab", "bcabc", "cab", "cabc" 和 "abc" (相同字符串算多次)。 示例 2： 输入：s = "aaacb" 输出：3 解释：包含 a，b 和 c 各至少一次的子字符串为 "aaacb", "aacb" 和 "acb" 。 示例 3： 输入：s = "abc" 输出：1 提示： * 3 <= s.length <= 5 x 10^4 * s 只包含字符 a，b 和 c 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来找到所有包含 a, b, c 的子字符串。

算法步骤:
1. 初始化一个计数器来记录当前窗口内 a, b, c 的数量。
2. 使用两个指针 left 和 right 来表示滑动窗口的左右边界。
3. 移动右指针扩展窗口，直到窗口内包含 a, b, c。
4. 当窗口内包含 a, b, c 时，移动左指针收缩窗口，并计算以当前右指针结尾的所有有效子字符串的数量。
5. 重复步骤 3 和 4 直到右指针到达字符串末尾。

关键点:
- 使用滑动窗口可以高效地找到所有满足条件的子字符串。
- 通过移动左指针来计算以当前右指针结尾的所有有效子字符串的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。每个字符最多被访问两次（一次由右指针，一次由左指针）。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
