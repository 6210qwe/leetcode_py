def solution_function_name(s: str) -> int:
    """
    函数式接口 - 返回包含 a, b, c 各至少一次的子字符串数目
    """
    count = {'a': 0, 'b': 0, 'c': 0}
    left = 0
    result = 0
    
    for right in range(len(s)):
        count[s[right]] += 1
        
        while all(count[char] > 0 for char in 'abc'):
            result += len(s) - right
            count[s[left]] -= 1
            left += 1
    
    return result


Solution = create_solution(solution_function_name)