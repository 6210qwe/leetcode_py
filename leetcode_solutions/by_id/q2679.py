"""
2549. 统计桌面上的不同数字 - 给你一个正整数 n ，开始时，它放在桌面上。在 109 天内，每天都要执行下述步骤： * 对于出现在桌面上的每个数字 x ，找出符合 1 <= i <= n 且满足 x % i == 1 的所有数字 i 。 * 然后，将这些数字放在桌面上。 返回在 109 天之后，出现在桌面上的 不同 整数的数目。 注意： * 一旦数字放在桌面上，则会一直保留直到结束。 * % 表示取余运算。例如，14 % 3 等于 2 。 示例 1： 输入：n = 5 输出：4 解释：最开始，5 在桌面上。 第二天，2 和 4 也出现在桌面上，因为 5 % 2 == 1 且 5 % 4 == 1 。 再过一天 3 也出现在桌面上，因为 4 % 3 == 1 。 在十亿天结束时，桌面上的不同数字有 2 、3 、4 、5 。 示例 2： 输入：n = 3 输出：2 解释： 因为 3 % 2 == 1 ，2 也出现在桌面上。 在十亿天结束时，桌面上的不同数字只有两个：2 和 3 。 提示： * 1 <= n <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过观察可以发现，对于任意一个数字 n，如果 n > 1，那么最终桌面上会出现从 2 到 n 的所有数字。这是因为 n % (n-1) == 1，(n-1) % (n-2) == 1，依此类推。因此，我们只需要返回 n-1 即可。

算法步骤:
1. 如果 n 为 1，直接返回 1。
2. 否则，返回 n-1。

关键点:
- 通过数学观察简化问题，避免复杂的模拟过程。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_distinct_numbers(n: int) -> int:
    """
    函数式接口 - 统计桌面上的不同数字
    """
    if n == 1:
        return 1
    return n - 1


Solution = create_solution(count_distinct_numbers)