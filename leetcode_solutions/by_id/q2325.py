"""
2222. 选择建筑的方案数 - 给你一个下标从 0 开始的二进制字符串 s ，它表示一条街沿途的建筑类型，其中： * s[i] = '0' 表示第 i 栋建筑是一栋办公楼， * s[i] = '1' 表示第 i 栋建筑是一间餐厅。 作为市政厅的官员，你需要随机 选择 3 栋建筑。然而，为了确保多样性，选出来的 3 栋建筑 相邻 的两栋不能是同一类型。 * 比方说，给你 s = "001101" ，我们不能选择第 1 ，3 和 5 栋建筑，因为得到的子序列是 "011" ，有相邻两栋建筑是同一类型，所以 不合 题意。 请你返回可以选择 3 栋建筑的 有效方案数 。 示例 1： 输入：s = "001101" 输出：6 解释： 以下下标集合是合法的： - [0,2,4] ，从 "001101" 得到 "010" - [0,3,4] ，从 "001101" 得到 "010" - [1,2,4] ，从 "001101" 得到 "010" - [1,3,4] ，从 "001101" 得到 "010" - [2,4,5] ，从 "001101" 得到 "101" - [3,4,5] ，从 "001101" 得到 "101" 没有别的合法选择，所以总共有 6 种方法。 示例 2： 输入：s = "11100" 输出：0 解释：没有任何符合题意的选择。 提示： * 3 <= s.length <= 105 * s[i] 要么是 '0' ，要么是 '1' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用前缀和来记录每个位置之前的 '0' 和 '1' 的数量，然后通过遍历字符串来计算有效的三元组。

算法步骤:
1. 初始化两个数组 `count_0` 和 `count_1`，分别记录每个位置之前的 '0' 和 '1' 的数量。
2. 遍历字符串，填充 `count_0` 和 `count_1` 数组。
3. 再次遍历字符串，对于每个位置 `i`，计算以 `s[i]` 为中心的有效三元组的数量。
4. 返回总的有效三元组数量。

关键点:
- 使用前缀和可以快速计算任意位置之前的 '0' 和 '1' 的数量。
- 通过遍历字符串两次，可以在 O(n) 时间复杂度内解决问题。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    n = len(s)
    if n < 3:
        return 0

    # 前缀和数组
    count_0 = [0] * (n + 1)
    count_1 = [0] * (n + 1)

    # 填充前缀和数组
    for i in range(1, n + 1):
        count_0[i] = count_0[i - 1] + (s[i - 1] == '0')
        count_1[i] = count_1[i - 1] + (s[i - 1] == '1')

    result = 0

    # 计算有效三元组的数量
    for i in range(1, n - 1):
        if s[i] == '0':
            result += count_1[i] * (count_1[n] - count_1[i + 1])
        else:
            result += count_0[i] * (count_0[n] - count_0[i + 1])

    return result


Solution = create_solution(solution_function_name)