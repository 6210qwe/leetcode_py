"""
3082. 求出所有子序列的能量和 - 给你一个长度为 n 的整数数组 nums 和一个 正 整数 k 。 一个整数数组的 能量 定义为和 等于 k 的子序列的数目。 请你返回 nums 中所有子序列的 能量和 。 由于答案可能很大，请你将它对 109 + 7 取余 后返回。 示例 1： 输入： nums = [1,2,3], k = 3 输出： 6 解释： 总共有 5 个能量不为 0 的子序列： * 子序列 [1,2,3] 有 2 个和为 3 的子序列：[1,2,3] 和 [1,2,3] 。 * 子序列 [1,2,3] 有 1 个和为 3 的子序列：[1,2,3] 。 * 子序列 [1,2,3] 有 1 个和为 3 的子序列：[1,2,3] 。 * 子序列 [1,2,3] 有 1 个和为 3 的子序列：[1,2,3] 。 * 子序列 [1,2,3] 有 1 个和为 3 的子序列：[1,2,3] 。 所以答案为 2 + 1 + 1 + 1 + 1 = 6 。 示例 2： 输入： nums = [2,3,3], k = 5 输出： 4 解释： 总共有 3 个能量不为 0 的子序列： * 子序列 [2,3,3] 有 2 个子序列和为 5 ：[2,3,3] 和 [2,3,3] 。 * 子序列 [2,3,3] 有 1 个子序列和为 5 ：[2,3,3] 。 * 子序列 [2,3,3] 有 1 个子序列和为 5 ：[2,3,3] 。 所以答案为 2 + 1 + 1 = 4 。 示例 3： 输入： nums = [1,2,3], k = 7 输出： 0 解释：不存在和为 7 的子序列，所以 nums 的能量和为 0 。 提示： * 1 <= n <= 100 * 1 <= nums[i] <= 104 * 1 <= k <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算所有子序列的能量和。我们使用一个二维数组 dp，其中 dp[i][j] 表示前 i 个元素中和为 j 的子序列的数量。

算法步骤:
1. 初始化一个二维数组 dp，大小为 (n+1) x (k+1)，并将 dp[0][0] 设为 1。
2. 遍历数组 nums，对于每个元素 num，更新 dp 数组。
3. 对于每个 num，从后向前遍历 dp 数组，更新 dp[i][j] 为 dp[i-1][j] + dp[i-1][j-num]。
4. 最终结果为 dp[n][k]。

关键点:
- 使用模运算来防止结果溢出。
- 从后向前遍历 dp 数组以避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)
空间复杂度: O(n * k)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算所有子序列的能量和
    """
    MOD = 10**9 + 7
    n = len(nums)
    dp = [[0] * (k + 1) for _ in range(n + 1)]
    dp[0][0] = 1

    for i in range(1, n + 1):
        for j in range(k + 1):
            dp[i][j] = dp[i - 1][j]
            if j >= nums[i - 1]:
                dp[i][j] += dp[i - 1][j - nums[i - 1]]
                dp[i][j] %= MOD

    return dp[n][k]


Solution = create_solution(solution_function_name)