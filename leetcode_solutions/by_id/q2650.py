"""
2578. 最小和分割 - 给你一个正整数 num ，请你将它分割成两个非负整数 num1 和 num2 ，满足： * num1 和 num2 直接连起来，得到 num 各数位的一个排列。 * 换句话说，num1 和 num2 中所有数字出现的次数之和等于 num 中所有数字出现的次数。 * num1 和 num2 可以包含前导 0 。 请你返回 num1 和 num2 可以得到的和的 最小 值。 注意： * num 保证没有前导 0 。 * num1 和 num2 中数位顺序可以与 num 中数位顺序不同。 示例 1： 输入：num = 4325 输出：59 解释：我们可以将 4325 分割成 num1 = 24 和 num2 = 35 ，和为 59 ，59 是最小和。 示例 2： 输入：num = 687 输出：75 解释：我们可以将 687 分割成 num1 = 68 和 num2 = 7 ，和为最优值 75 。 提示： * 10 <= num <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将 num 的各个数位从小到大排序，然后交替分配给 num1 和 num2，使得 num1 和 num2 的和最小。

算法步骤:
1. 将 num 转换为字符串并拆分成字符列表。
2. 对字符列表进行排序。
3. 初始化 num1 和 num2 为 0。
4. 交替将排序后的字符分配给 num1 和 num2。
5. 返回 num1 和 num2 的和。

关键点:
- 通过排序和交替分配，确保 num1 和 num2 的和最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d log d)，其中 d 是 num 的位数。排序操作的时间复杂度为 O(d log d)。
空间复杂度: O(d)，需要存储 num 的各个数位。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def split_with_minimum_sum(num: int) -> int:
    """
    函数式接口 - 将 num 分割成两个非负整数 num1 和 num2，使得它们的和最小。
    """
    # 将 num 转换为字符串并拆分成字符列表
    digits = list(str(num))
    
    # 对字符列表进行排序
    digits.sort()
    
    # 初始化 num1 和 num2
    num1, num2 = 0, 0
    
    # 交替将排序后的字符分配给 num1 和 num2
    for i, digit in enumerate(digits):
        if i % 2 == 0:
            num1 = num1 * 10 + int(digit)
        else:
            num2 = num2 * 10 + int(digit)
    
    # 返回 num1 和 num2 的和
    return num1 + num2


Solution = create_solution(split_with_minimum_sum)