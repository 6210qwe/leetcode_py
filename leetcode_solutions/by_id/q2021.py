"""
1910. 删除一个字符串中所有出现的给定子字符串 - 给你两个字符串 s 和 part ，请你对 s 反复执行以下操作直到 所有 子字符串 part 都被删除： * 找到 s 中 最左边 的子字符串 part ，并将它从 s 中删除。 请你返回从 s 中删除所有 part 子字符串以后得到的剩余字符串。 一个 子字符串 是一个字符串中连续的字符序列。 示例 1： 输入：s = "daabcbaabcbc", part = "abc" 输出："dab" 解释：以下操作按顺序执行： - s = "daabcbaabcbc" ，删除下标从 2 开始的 "abc" ，得到 s = "dabaabcbc" 。 - s = "dabaabcbc" ，删除下标从 4 开始的 "abc" ，得到 s = "dababc" 。 - s = "dababc" ，删除下标从 3 开始的 "abc" ，得到 s = "dab" 。 此时 s 中不再含有子字符串 "abc" 。 示例 2： 输入：s = "axxxxyyyyb", part = "xy" 输出："ab" 解释：以下操作按顺序执行： - s = "axxxxyyyyb" ，删除下标从 4 开始的 "xy" ，得到 s = "axxxyyyb" 。 - s = "axxxyyyb" ，删除下标从 3 开始的 "xy" ，得到 s = "axxyyb" 。 - s = "axxyyb" ，删除下标从 2 开始的 "xy" ，得到 s = "axyb" 。 - s = "axyb" ，删除下标从 1 开始的 "xy" ，得到 s = "ab" 。 此时 s 中不再含有子字符串 "xy" 。 提示： * 1 <= s.length <= 1000 * 1 <= part.length <= 1000 * s 和 part 只包小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来存储字符，当栈顶元素与 part 的前缀匹配时，检查后续字符是否能形成完整的 part，如果可以则弹出这些字符。

算法步骤:
1. 初始化一个空栈。
2. 遍历字符串 s 的每个字符：
   - 将字符压入栈中。
   - 检查栈顶元素是否与 part 的第一个字符匹配，如果匹配则继续检查后续字符是否能形成完整的 part。
   - 如果形成完整的 part，则将这些字符从栈中弹出。
3. 最后将栈中的字符拼接成结果字符串并返回。

关键点:
- 使用栈来存储字符，并在匹配到 part 时弹出相应字符。
- 通过不断检查栈顶元素和 part 的匹配情况，确保每次删除最左边的 part。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是字符串 s 的长度，m 是 part 的长度。最坏情况下，每个字符都需要与 part 进行比较。
空间复杂度: O(n)，使用栈来存储字符。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_substring(s: str, part: str) -> str:
    """
    函数式接口 - 移除字符串 s 中所有出现的子字符串 part
    """
    stack = []
    part_len = len(part)
    
    for char in s:
        stack.append(char)
        if ''.join(stack[-part_len:]) == part:
            del stack[-part_len:]
    
    return ''.join(stack)


Solution = create_solution(remove_substring)