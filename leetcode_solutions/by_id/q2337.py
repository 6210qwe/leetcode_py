"""
2259. 移除指定数字得到的最大结果 - 给你一个表示某个正整数的字符串 number 和一个字符 digit 。 从 number 中 恰好 移除 一个 等于 digit 的字符后，找出并返回按 十进制 表示 最大 的结果字符串。生成的测试用例满足 digit 在 number 中出现至少一次。 示例 1： 输入：number = "123", digit = '3' 输出："12" 解释："123" 中只有一个 '3' ，在移除 '3' 之后，结果为 "12" 。 示例 2： 输入：number = "1231", digit = '1' 输出："231" 解释：可以移除第一个 '1' 得到 "231" 或者移除第二个 '1' 得到 "123" 。 由于 231 > 123 ，返回 "231" 。 示例 3： 输入：number = "551", digit = '5' 输出："51" 解释：可以从 "551" 中移除第一个或者第二个 '5' 。 两种方案的结果都是 "51" 。 提示： * 2 <= number.length <= 100 * number 由数字 '1' 到 '9' 组成 * digit 是 '1' 到 '9' 中的一个数字 * digit 在 number 中出现至少一次
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法找到移除哪个 digit 可以使结果最大。

算法步骤:
1. 遍历 number 字符串，记录每个 digit 出现的位置。
2. 对于每个 digit 的位置，生成移除该 digit 后的新字符串。
3. 比较所有新字符串，返回最大的一个。

关键点:
- 通过遍历和比较来确保找到最优解。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 其中 n 是 number 的长度，因为我们需要遍历字符串并进行比较。
空间复杂度: O(1) - 除了输入和输出外，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def remove_digit(number: str, digit: str) -> str:
    """
    函数式接口 - 从 number 中恰好移除一个等于 digit 的字符后，返回按十进制表示最大的结果字符串。
    """
    max_result = ""
    for i in range(len(number)):
        if number[i] == digit:
            new_number = number[:i] + number[i+1:]
            if new_number > max_result:
                max_result = new_number
    return max_result


Solution = create_solution(remove_digit)