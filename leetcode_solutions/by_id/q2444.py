"""
2370. 最长理想子序列 - 给你一个由小写字母组成的字符串 s ，和一个整数 k 。如果满足下述条件，则可以将字符串 t 视作是 理想字符串 ： * t 是字符串 s 的一个子序列。 * t 中每两个 相邻 字母在字母表中位次的绝对差值小于或等于 k 。 返回 最长 理想字符串的长度。 字符串的子序列同样是一个字符串，并且子序列还满足：可以经由其他字符串删除某些字符（也可以不删除）但不改变剩余字符的顺序得到。 注意：字母表顺序不会循环。例如，'a' 和 'z' 在字母表中位次的绝对差值是 25 ，而不是 1 。 示例 1： 输入：s = "acfgbd", k = 2 输出：4 解释：最长理想字符串是 "acbd" 。该字符串长度为 4 ，所以返回 4 。 注意 "acfgbd" 不是理想字符串，因为 'c' 和 'f' 的字母表位次差值为 3 。 示例 2： 输入：s = "abcd", k = 3 输出：4 解释：最长理想字符串是 "abcd" ，该字符串长度为 4 ，所以返回 4 。 提示： * 1 <= s.length <= 105 * 0 <= k <= 25 * s 由小写英文字母组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划和哈希表来记录每个字符的最大理想子序列长度。

算法步骤:
1. 初始化一个长度为 26 的数组 dp，用于记录以每个字符结尾的最长理想子序列长度。
2. 遍历字符串 s，对于每个字符，更新其在 dp 数组中的值。
3. 对于每个字符 c，检查其前后 k 个字符范围内的所有字符，更新 dp[c] 的值。
4. 最后，返回 dp 数组中的最大值。

关键点:
- 使用 dp 数组来记录以每个字符结尾的最长理想子序列长度。
- 通过遍历字符及其前后 k 个字符范围内的字符来更新 dp 数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * k)，其中 n 是字符串 s 的长度，k 是给定的整数。
空间复杂度: O(1)，dp 数组的大小固定为 26。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def longest_ideal_subsequence(s: str, k: int) -> int:
    """
    函数式接口 - 计算最长理想子序列的长度
    """
    # 初始化 dp 数组，长度为 26，初始值为 0
    dp = [0] * 26
    
    # 遍历字符串 s
    for char in s:
        char_index = ord(char) - ord('a')
        # 更新 dp[char_index] 的值
        dp[char_index] += 1
        
        # 检查前后 k 个字符范围内的所有字符
        for i in range(max(0, char_index - k), min(26, char_index + k + 1)):
            if i != char_index:
                dp[char_index] = max(dp[char_index], dp[i] + 1)
    
    # 返回 dp 数组中的最大值
    return max(dp)


Solution = create_solution(longest_ideal_subsequence)