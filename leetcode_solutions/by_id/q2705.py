"""
2568. 最小无法得到的或值 - 给你一个下标从 0 开始的整数数组 nums 。 如果存在一些整数满足 0 <= index1 < index2 < ... < indexk < nums.length ，得到 nums[index1] | nums[index2] | ... | nums[indexk] = x ，那么我们说 x 是 可表达的 。换言之，如果一个整数能由 nums 的某个子序列的或运算得到，那么它就是可表达的。 请你返回 nums 不可表达的 最小非零整数 。 示例 1： 输入：nums = [2,1] 输出：4 解释：1 和 2 已经在数组中，因为 nums[0] | nums[1] = 2 | 1 = 3 ，所以 3 是可表达的。由于 4 是不可表达的，所以我们返回 4 。 示例 2： 输入：nums = [5,3,2] 输出：1 解释：1 是最小不可表达的数字。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过检查每个可能的二进制位，找到第一个未被覆盖的位。

算法步骤:
1. 使用一个集合来记录所有已出现的二进制位。
2. 遍历数组中的每个数，将其转换为二进制表示，并将每个二进制位加入集合。
3. 从1开始，逐个检查每个整数的二进制表示，直到找到第一个不在集合中的二进制位。

关键点:
- 利用集合来高效地存储和查找二进制位。
- 通过逐个检查整数的二进制表示，确保找到最小的不可表达的整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + log(max(nums)))
空间复杂度: O(log(max(nums)))
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def minimum_impossible_or(nums: List[int]) -> int:
    """
    函数式接口 - 找到最小的不可表达的整数
    """
    # 使用集合记录所有已出现的二进制位
    seen_bits = set()
    
    # 遍历数组中的每个数，将其转换为二进制表示，并将每个二进制位加入集合
    for num in nums:
        bit = 1
        while num > 0:
            if num & 1:
                seen_bits.add(bit)
            num >>= 1
            bit <<= 1
    
    # 从1开始，逐个检查每个整数的二进制表示，直到找到第一个不在集合中的二进制位
    result = 1
    while result in seen_bits:
        result <<= 1
    
    return result


Solution = create_solution(minimum_impossible_or)