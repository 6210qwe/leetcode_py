"""
658. 找到 K 个最接近的元素 - 给定一个 排序好 的数组 arr ，两个整数 k 和 x ，从数组中找到最靠近 x（两数之差最小）的 k 个数。返回的结果必须要是按升序排好的。 整数 a 比整数 b 更接近 x 需要满足： * |a - x| < |b - x| 或者 * |a - x| == |b - x| 且 a < b 示例 1： 输入：arr = [1,2,3,4,5], k = 4, x = 3 输出：[1,2,3,4] 示例 2： 输入：arr = [1,1,2,3,4,5], k = 4, x = -1 输出：[1,1,2,3] 提示： * 1 <= k <= arr.length * 1 <= arr.length <= 104 * arr 按 升序 排列 * -104 <= arr[i], x <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二分查找和双指针来找到最接近 x 的 k 个元素。

算法步骤:
1. 使用二分查找找到一个起始位置，使得从该位置开始的 k 个元素是最接近 x 的。
2. 使用双指针从起始位置向两边扩展，比较左右指针指向的元素与 x 的距离，选择更接近 x 的元素。
3. 最后返回结果。

关键点:
- 二分查找用于快速定位起始位置。
- 双指针用于逐步扩展并选择最接近 x 的元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n + k)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def find_k_closest_elements(arr: List[int], k: int, x: int) -> List[int]:
    """
    函数式接口 - 找到最接近 x 的 k 个元素
    """
    # 二分查找找到起始位置
    left, right = 0, len(arr) - k
    while left < right:
        mid = (left + right) // 2
        if x - arr[mid] > arr[mid + k] - x:
            left = mid + 1
        else:
            right = mid
    
    # 返回结果
    return arr[left:left + k]

