"""
779. 第K个语法符号 - 我们构建了一个包含 n 行( 索引从 1 开始 )的表。首先在第一行我们写上一个 0。接下来的每一行，将前一行中的0替换为01，1替换为10。 * 例如，对于 n = 3 ，第 1 行是 0 ，第 2 行是 01 ，第3行是 0110 。 给定行数 n 和序数 k，返回第 n 行中第 k 个字符。（ k 从索引 1 开始） 示例 1: 输入: n = 1, k = 1 输出: 0 解释: 第一行：0 示例 2: 输入: n = 2, k = 1 输出: 0 解释: 第一行: 0 第二行: 01 示例 3: 输入: n = 2, k = 2 输出: 1 解释: 第一行: 0 第二行: 01 提示: * 1 <= n <= 30 * 1 <= k <= 2n - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用递归方法，通过观察可以发现第 n 行的第 k 个字符与第 n-1 行的某个字符有关。
        具体来说，如果 k 是奇数，则第 n 行的第 k 个字符与第 n-1 行的 (k+1)//2 个字符相同；
        如果 k 是偶数，则第 n 行的第 k 个字符与第 n-1 行的 (k+1)//2 个字符相反。

算法步骤:
1. 如果 n 为 1，则直接返回 0。
2. 如果 k 是奇数，则返回第 n-1 行的 (k+1)//2 个字符。
3. 如果 k 是偶数，则返回第 n-1 行的 (k+1)//2 个字符的相反值。

关键点:
- 递归调用时，通过 (k+1)//2 来减少 k 的规模。
- 通过取反操作来处理偶数位置的字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 每次递归调用都会减少 n 的值，直到 n 为 1。
空间复杂度: O(n) - 递归调用栈的深度最多为 n。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int, k: int) -> int:
    """
    函数式接口 - 返回第 n 行中第 k 个字符
    """
    if n == 1:
        return 0
    if k % 2 == 1:
        return solution_function_name(n - 1, (k + 1) // 2)
    else:
        return 1 - solution_function_name(n - 1, (k + 1) // 2)


Solution = create_solution(solution_function_name)