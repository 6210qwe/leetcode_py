"""
135. 分发糖果 - n 个孩子站成一排。给你一个整数数组 ratings 表示每个孩子的评分。 你需要按照以下要求，给这些孩子分发糖果： * 每个孩子至少分配到 1 个糖果。 * 相邻两个孩子中，评分更高的那个会获得更多的糖果。 请你给每个孩子分发糖果，计算并返回需要准备的 最少糖果数目 。 示例 1： 输入：ratings = [1,0,2] 输出：5 解释：你可以分别给第一个、第二个、第三个孩子分发 2、1、2 颗糖果。 示例 2： 输入：ratings = [1,2,2] 输出：4 解释：你可以分别给第一个、第二个、第三个孩子分发 1、2、1 颗糖果。 第三个孩子只得到 1 颗糖果，这满足题面中的两个条件。 提示： * n == ratings.length * 1 <= n <= 2 * 104 * 0 <= ratings[i] <= 2 * 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 两次遍历，先从左到右，再从右到左，满足相邻规则

算法步骤:
1. 初始化所有孩子为1个糖果
2. 从左到右遍历，如果ratings[i] > ratings[i-1]，candies[i] = candies[i-1] + 1
3. 从右到左遍历，如果ratings[i] > ratings[i+1]，candies[i] = max(candies[i], candies[i+1] + 1)

关键点:
- 两次遍历，分别满足左右规则
- 时间复杂度O(n)，空间复杂度O(n)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组两次
空间复杂度: O(n) - 糖果数组空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def candy(ratings: List[int]) -> int:
    """
    函数式接口 - 分发糖果
    
    实现思路:
    两次遍历，先从左到右，再从右到左，满足相邻规则。
    
    Args:
        ratings: 每个孩子的评分数组
        
    Returns:
        需要准备的糖果数目
        
    Example:
        >>> candy([1,0,2])
        5
    """
    n = len(ratings)
    candies = [1] * n
    
    # 从左到右遍历
    for i in range(1, n):
        if ratings[i] > ratings[i - 1]:
            candies[i] = candies[i - 1] + 1
    
    # 从右到左遍历
    for i in range(n - 2, -1, -1):
        if ratings[i] > ratings[i + 1]:
            candies[i] = max(candies[i], candies[i + 1] + 1)
    
    return sum(candies)


