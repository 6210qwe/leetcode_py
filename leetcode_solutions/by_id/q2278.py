"""
2177. 找到和为给定整数的三个连续整数 - 给你一个整数 num ，请你返回三个连续的整数，它们的 和 为 num 。如果 num 无法被表示成三个连续整数的和，请你返回一个 空 数组。 示例 1： 输入：num = 33 输出：[10,11,12] 解释：33 可以表示为 10 + 11 + 12 = 33 。 10, 11, 12 是 3 个连续整数，所以返回 [10, 11, 12] 。 示例 2： 输入：num = 4 输出：[] 解释：没有办法将 4 表示成 3 个连续整数的和。 提示： * 0 <= num <= 1015
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断 num 是否能被 3 整除，如果可以，则返回三个连续整数，否则返回空数组。

算法步骤:
1. 检查 num 是否能被 3 整除。
2. 如果可以，计算中间的整数 mid = num // 3。
3. 返回 [mid - 1, mid, mid + 1]。
4. 如果不能，返回空数组。

关键点:
- 三个连续整数的和一定是 3 的倍数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 只需要常数时间进行判断和计算。
空间复杂度: O(1) - 只需要常数空间存储结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(num: int) -> List[int]:
    """
    函数式接口 - 找到和为给定整数的三个连续整数
    """
    if num % 3 != 0:
        return []
    
    mid = num // 3
    return [mid - 1, mid, mid + 1]


