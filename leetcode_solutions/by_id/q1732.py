"""
1611. 使整数变为 0 的最少操作次数 - 给你一个整数 n，你需要重复执行多次下述操作将其转换为 0 ： * 翻转 n 的二进制表示中最右侧位（第 0 位）。 * 如果二进制表示中的第 (i-1) 位为 1 且从第 (i-2) 位到第 0 位都为 0，则翻转 n 的二进制表示中的第 i 位。 返回将 n 转换为 0 的最小操作次数。 示例 1： 输入：n = 3 输出：2 解释：3 的二进制表示为 "11" "11" -> "01" ，执行的是第 2 种操作，因为第 0 位为 1 。 "01" -> "00" ，执行的是第 1 种操作。 示例 2： 输入：n = 6 输出：4 解释：6 的二进制表示为 "110". "110" -> "010" ，执行的是第 2 种操作，因为第 1 位为 1 ，第 0 到 0 位为 0 。 "010" -> "011" ，执行的是第 1 种操作。 "011" -> "001" ，执行的是第 2 种操作，因为第 0 位为 1 。 "001" -> "000" ，执行的是第 1 种操作。 提示： * 0 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用递归和记忆化搜索来解决这个问题。通过找到最高位的 1，并将其翻转，然后递归处理剩余部分。

算法步骤:
1. 找到 n 的最高位 1 的位置。
2. 将该位翻转，并递归处理剩余部分。
3. 使用记忆化搜索来避免重复计算。

关键点:
- 通过递归和记忆化搜索来优化时间复杂度。
- 使用位运算来找到最高位的 1 并进行翻转。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)，其中 n 是输入的整数。每次递归调用都会减少一位。
空间复杂度: O(log n)，递归调用栈的深度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
from functools import lru_cache

@lru_cache(maxsize=None)
def min_one_bit_operations(n: int) -> int:
    """
    函数式接口 - 计算将整数 n 转换为 0 的最小操作次数
    """
    if n == 0:
        return 0
    # 找到最高位的 1
    highest_bit = 1
    while (highest_bit << 1) <= n:
        highest_bit <<= 1
    # 递归处理剩余部分
    return 2 * min_one_bit_operations(n ^ highest_bit) + 1

Solution = create_solution(min_one_bit_operations)