"""
3513. 不同 XOR 三元组的数目 I - 给你一个长度为 n 的整数数组 nums，其中 nums 是范围 [1, n] 内所有数的 排列 。 XOR 三元组 定义为三个元素的异或值 nums[i] XOR nums[j] XOR nums[k]，其中 i <= j <= k。 返回所有可能三元组 (i, j, k) 中 不同 的 XOR 值的数量。 排列 是一个集合中所有元素的重新排列。 示例 1： 输入： nums = [1,2] 输出： 2 解释： 所有可能的 XOR 三元组值为： * (0, 0, 0) → 1 XOR 1 XOR 1 = 1 * (0, 0, 1) → 1 XOR 1 XOR 2 = 2 * (0, 1, 1) → 1 XOR 2 XOR 2 = 1 * (1, 1, 1) → 2 XOR 2 XOR 2 = 2 不同的 XOR 值为 {1, 2}，因此输出为 2。 示例 2： 输入： nums = [3,1,2] 输出： 4 解释： 可能的 XOR 三元组值包括： * (0, 0, 0) → 3 XOR 3 XOR 3 = 3 * (0, 0, 1) → 3 XOR 3 XOR 1 = 1 * (0, 0, 2) → 3 XOR 3 XOR 2 = 2 * (0, 1, 2) → 3 XOR 1 XOR 2 = 0 不同的 XOR 值为 {0, 1, 2, 3}，因此输出为 4。 提示： * 1 <= n == nums.length <= 105 * 1 <= nums[i] <= n * nums 是从 1 到 n 的整数的一个排列。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用集合来存储所有可能的 XOR 三元组值，并利用集合的唯一性来去重。

算法步骤:
1. 初始化一个空集合 `xor_set` 来存储不同的 XOR 三元组值。
2. 遍历数组中的每个元素 `nums[i]`，计算 `nums[i]` 与所有后续元素 `nums[j]` 和 `nums[k]` 的异或值，并将结果加入 `xor_set`。
3. 返回 `xor_set` 的大小。

关键点:
- 使用集合来存储和去重 XOR 三元组值。
- 通过三层循环遍历所有可能的三元组组合。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^3)，其中 n 是数组的长度。我们需要遍历所有可能的三元组组合。
空间复杂度: O(n^3)，在最坏情况下，所有的 XOR 三元组值都是唯一的，需要存储 n^3 个值。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算不同 XOR 三元组的数目
    """
    xor_set = set()
    
    n = len(nums)
    for i in range(n):
        for j in range(i, n):
            for k in range(j, n):
                xor_value = nums[i] ^ nums[j] ^ nums[k]
                xor_set.add(xor_value)
    
    return len(xor_set)


Solution = create_solution(solution_function_name)