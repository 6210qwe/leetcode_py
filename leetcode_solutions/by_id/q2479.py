"""
2402. 会议室 III - 给你一个整数 n ，共有编号从 0 到 n - 1 的 n 个会议室。 给你一个二维整数数组 meetings ，其中 meetings[i] = [starti, endi] 表示一场会议将会在 半闭 时间区间 [starti, endi) 举办。所有 starti 的值 互不相同 。 会议将会按以下方式分配给会议室： 1. 每场会议都会在未占用且编号 最小 的会议室举办。 2. 如果没有可用的会议室，会议将会延期，直到存在空闲的会议室。延期会议的持续时间和原会议持续时间 相同 。 3. 当会议室处于未占用状态时，将会优先提供给原 开始 时间更早的会议。 返回举办最多次会议的房间 编号 。如果存在多个房间满足此条件，则返回编号 最小 的房间。 半闭区间 [a, b) 是 a 和 b 之间的区间，包括 a 但 不包括 b 。 示例 1： 输入：n = 2, meetings = [[0,10],[1,5],[2,7],[3,4]] 输出：0 解释： - 在时间 0 ，两个会议室都未占用，第一场会议在会议室 0 举办。 - 在时间 1 ，只有会议室 1 未占用，第二场会议在会议室 1 举办。 - 在时间 2 ，两个会议室都被占用，第三场会议延期举办。 - 在时间 3 ，两个会议室都被占用，第四场会议延期举办。 - 在时间 5 ，会议室 1 的会议结束。第三场会议在会议室 1 举办，时间周期为 [5,10) 。 - 在时间 10 ，两个会议室的会议都结束。第四场会议在会议室 0 举办，时间周期为 [10,11) 。 会议室 0 和会议室 1 都举办了 2 场会议，所以返回 0 。 示例 2： 输入：n = 3, meetings = [[1,20],[2,10],[3,5],[4,9],[6,8]] 输出：1 解释： - 在时间 1 ，所有三个会议室都未占用，第一场会议在会议室 0 举办。 - 在时间 2 ，会议室 1 和 2 未占用，第二场会议在会议室 1 举办。 - 在时间 3 ，只有会议室 2 未占用，第三场会议在会议室 2 举办。 - 在时间 4 ，所有三个会议室都被占用，第四场会议延期举办。 - 在时间 5 ，会议室 2 的会议结束。第四场会议在会议室 2 举办，时间周期为 [5,10) 。 - 在时间 6 ，所有三个会议室都被占用，第五场会议延期举办。 - 在时间 10 ，会议室 1 和 2 的会议结束。第五场会议在会议室 1 举办，时间周期为 [10,12) 。 会议室 1 和会议室 2 都举办了 2 场会议，所以返回 1 。 提示： * 1 <= n <= 100 * 1 <= meetings.length <= 105 * meetings[i].length == 2 * 0 <= starti < endi <= 5 * 105 * starti 的所有值 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想:
- 使用优先队列来管理会议室的使用情况。
- 先将会议按开始时间排序。
- 使用两个优先队列：一个用于存储当前正在使用的会议室，另一个用于存储当前空闲的会议室。

算法步骤:
1. 将会议按开始时间排序。
2. 初始化两个优先队列：一个用于存储当前正在使用的会议室（按结束时间排序），另一个用于存储当前空闲的会议室（按编号排序）。
3. 遍历每个会议：
   - 释放所有已经结束的会议室到空闲队列。
   - 如果有空闲的会议室，选择编号最小的会议室。
   - 如果没有空闲的会议室，选择最早结束的会议室，并更新其结束时间。
4. 统计每个会议室举办的会议次数，返回举办最多次会议的房间编号。

关键点:
- 使用优先队列来高效管理会议室的使用情况。
- 通过排序和优先队列，确保每次选择的会议室都是最优的。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m log m + m log n)，其中 m 是会议的数量，n 是会议室的数量。排序的时间复杂度是 O(m log m)，优先队列的操作时间复杂度是 O(log n)。
空间复杂度: O(n + m)，用于存储会议室的状态和优先队列。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def most_booked(n: int, meetings: List[List[int]]) -> int:
    # 将会议按开始时间排序
    meetings.sort()
    
    # 优先队列：(结束时间, 会议室编号)
    used_rooms = []
    # 优先队列：(会议室编号)
    free_rooms = list(range(n))
    
    # 统计每个会议室举办的会议次数
    room_count = [0] * n
    
    for start, end in meetings:
        # 释放所有已经结束的会议室
        while used_rooms and used_rooms[0][0] <= start:
            _, room = heapq.heappop(used_rooms)
            heapq.heappush(free_rooms, room)
        
        if free_rooms:
            # 选择编号最小的空闲会议室
            room = heapq.heappop(free_rooms)
            heapq.heappush(used_rooms, (end, room))
        else:
            # 选择最早结束的会议室
            end_time, room = heapq.heappop(used_rooms)
            heapq.heappush(used_rooms, (end_time + end - start, room))
        
        # 更新会议室的会议次数
        room_count[room] += 1
    
    # 返回举办最多次会议的房间编号
    return room_count.index(max(room_count))

