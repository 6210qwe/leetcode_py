def reach_number(target: int) -> int:
    """
    函数式接口 - 返回到达目标所需的最小移动次数
    """
    target = abs(target)
    k = 0
    while k * (k + 1) // 2 < target:
        k += 1
    
    # Check if the difference is even
    diff = k * (k + 1) // 2 - target
    while diff % 2 != 0:
        k += 1
        diff = k * (k + 1) // 2 - target
    
    return k


