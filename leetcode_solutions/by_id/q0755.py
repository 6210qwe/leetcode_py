"""
754. 到达终点数字 - 在一根无限长的数轴上，你站在0的位置。终点在target的位置。 你可以做一些数量的移动 numMoves : * 每次你可以选择向左或向右移动。 * 第 i 次移动（从 i == 1 开始，到 i == numMoves ），在选择的方向上走 i 步。 给定整数 target ，返回 到达目标所需的 最小 移动次数(即最小 numMoves ) 。 示例 1: 输入: target = 2 输出: 3 解释: 第一次移动，从 0 到 1 。 第二次移动，从 1 到 -1 。 第三次移动，从 -1 到 2 。 示例 2: 输入: target = 3 输出: 2 解释: 第一次移动，从 0 到 1 。 第二次移动，从 1 到 3 。 提示: * -109 <= target <= 109 * target != 0
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用数学方法找到最小的 k 使得 1 + 2 + ... + k >= |target|，然后根据奇偶性调整 k。

算法步骤:
1. 计算最小的 k 使得 1 + 2 + ... + k >= |target|。
2. 如果 (k * (k + 1) // 2 - |target|) 是偶数，则 k 就是答案。
3. 否则，增加 k 直到 (k * (k + 1) // 2 - |target|) 是偶数。

关键点:
- 通过求和公式 1 + 2 + ... + k = k * (k + 1) // 2 来确定 k。
- 根据奇偶性调整 k 以确保可以到达 target。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(sqrt(|target|))
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
