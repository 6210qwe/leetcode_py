"""
957. N 天后的牢房 - 监狱中 8 间牢房排成一排，每间牢房可能被占用或空置。 每天，无论牢房是被占用或空置，都会根据以下规则进行变更： * 如果一间牢房的两个相邻的房间都被占用或都是空的，那么该牢房就会被占用。 * 否则，它就会被空置。 注意：由于监狱中的牢房排成一行，所以行中的第一个和最后一个牢房不存在两个相邻的房间。 给你一个整数数组 cells ，用于表示牢房的初始状态：如果第 i 间牢房被占用，则 cell[i]==1，否则 cell[i]==0 。另给你一个整数 n 。 请你返回 n 天后监狱的状况（即，按上文描述进行 n 次变更）。 示例 1： 输入：cells = [0,1,0,1,1,0,0,1], n = 7 输出：[0,0,1,1,0,0,0,0] 解释：下表总结了监狱每天的状况： Day 0: [0, 1, 0, 1, 1, 0, 0, 1] Day 1: [0, 1, 1, 0, 0, 0, 0, 0] Day 2: [0, 0, 0, 0, 1, 1, 1, 0] Day 3: [0, 1, 1, 0, 0, 1, 0, 0] Day 4: [0, 0, 0, 0, 0, 1, 0, 0] Day 5: [0, 1, 1, 1, 0, 1, 0, 0] Day 6: [0, 0, 1, 0, 1, 1, 0, 0] Day 7: [0, 0, 1, 1, 0, 0, 0, 0] 示例 2： 输入：cells = [1,0,0,1,0,0,1,0], n = 1000000000 输出：[0,0,1,1,1,1,1,0] 提示： * cells.length == 8 * cells[i] 为 0 或 1 * 1 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用位运算来表示牢房状态，并利用哈希表检测循环。

算法步骤:
1. 将初始牢房状态转换为一个整数。
2. 使用哈希表记录每个状态出现的天数，以检测循环。
3. 模拟每一天的状态变化，直到达到目标天数或检测到循环。
4. 如果检测到循环，直接跳过循环部分，计算剩余天数。
5. 返回最终状态。

关键点:
- 使用位运算来高效表示和操作牢房状态。
- 通过哈希表检测循环，避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 最多有 2^6 种不同的状态，因此最多模拟 64 天即可找到循环。
空间复杂度: O(1) - 哈希表最多存储 64 个状态。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List


def prison_after_n_days(cells: List[int], n: int) -> List[int]:
    def to_int(cells: List[int]) -> int:
        return sum(cell << i for i, cell in enumerate(cells))

    def to_list(state: int) -> List[int]:
        return [(state >> i) & 1 for i in range(8)]

    state = to_int(cells)
    seen = {state: 0}
    day = 0

    while day < n:
        next_state = 0
        for i in range(1, 7):
            if (state >> (i - 1)) & 1 == (state >> (i + 1)) & 1:
                next_state |= 1 << i
        state = next_state
        day += 1
        if state in seen:
            cycle_length = day - seen[state]
            n = (n - day) % cycle_length
            day = 0
            seen.clear()
        else:
            seen[state] = day

    return to_list(state)


