"""
2358. 分组的最大数量 - 给你一个正整数数组 grades ，表示大学中一些学生的成绩。你打算将 所有 学生分为一些 有序 的非空分组，其中分组间的顺序满足以下全部条件： * 第 i 个分组中的学生总成绩 小于 第 (i + 1) 个分组中的学生总成绩，对所有组均成立（除了最后一组）。 * 第 i 个分组中的学生总数 小于 第 (i + 1) 个分组中的学生总数，对所有组均成立（除了最后一组）。 返回可以形成的 最大 组数。 示例 1： 输入：grades = [10,6,12,7,3,5] 输出：3 解释：下面是形成 3 个分组的一种可行方法： - 第 1 个分组的学生成绩为 grades = [12] ，总成绩：12 ，学生数：1 - 第 2 个分组的学生成绩为 grades = [6,7] ，总成绩：6 + 7 = 13 ，学生数：2 - 第 3 个分组的学生成绩为 grades = [10,3,5] ，总成绩：10 + 3 + 5 = 18 ，学生数：3 可以证明无法形成超过 3 个分组。 示例 2： 输入：grades = [8,8] 输出：1 解释：只能形成 1 个分组，因为如果要形成 2 个分组的话，会导致每个分组中的学生数目相等。 提示： * 1 <= grades.length <= 105 * 1 <= grades[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过数学公式计算最大组数

算法步骤:
1. 计算满足条件的最大组数 k，使得 1 + 2 + ... + k <= n
2. 使用二分查找找到满足上述条件的最大 k

关键点:
- 利用等差数列求和公式 S = k * (k + 1) / 2 来判断是否满足条件
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_number_of_groups(n: int) -> int:
    """
    函数式接口 - 计算可以形成的最大组数
    """
    left, right = 1, n
    while left < right:
        mid = (left + right + 1) // 2
        if mid * (mid + 1) // 2 <= n:
            left = mid
        else:
            right = mid - 1
    return left


Solution = create_solution(max_number_of_groups)