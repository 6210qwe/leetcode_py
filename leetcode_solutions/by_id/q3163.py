"""
2913. 子数组不同元素数目的平方和 I - 给你一个下标从 0 开始的整数数组 nums 。 定义 nums 一个子数组的 不同计数 值如下： * 令 nums[i..j] 表示 nums 中所有下标在 i 到 j 范围内的元素构成的子数组（满足 0 <= i <= j < nums.length ），那么我们称子数组 nums[i..j] 中不同值的数目为 nums[i..j] 的不同计数。 请你返回 nums 中所有子数组的 不同计数 的 平方 和。 由于答案可能会很大，请你将它对 109 + 7 取余 后返回。 子数组指的是一个数组里面一段连续 非空 的元素序列。 示例 1： 输入：nums = [1,2,1] 输出：15 解释：六个子数组分别为： [1]: 1 个互不相同的元素。 [2]: 1 个互不相同的元素。 [1]: 1 个互不相同的元素。 [1,2]: 2 个互不相同的元素。 [2,1]: 2 个互不相同的元素。 [1,2,1]: 2 个互不相同的元素。 所有不同计数的平方和为 12 + 12 + 12 + 22 + 22 + 22 = 15 。 示例 2： 输入：nums = [2,2] 输出：3 解释：三个子数组分别为： [2]: 1 个互不相同的元素。 [2]: 1 个互不相同的元素。 [2,2]: 1 个互不相同的元素。 所有不同计数的平方和为 12 + 12 + 12 = 3 。 提示： * 1 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来计算每个子数组的不同元素数目，并维护一个哈希表来记录每个元素的最新出现位置。

算法步骤:
1. 初始化结果变量 `result` 为 0，模数 `mod` 为 10^9 + 7。
2. 使用两个指针 `left` 和 `right` 来表示当前子数组的左右边界，初始时都为 0。
3. 使用一个哈希表 `last_seen` 来记录每个元素的最新出现位置。
4. 遍历数组 `nums`，对于每个元素 `nums[right]`：
   - 更新 `last_seen` 中 `nums[right]` 的最新出现位置。
   - 计算当前子数组的不同元素数目 `distinct_count`。
   - 将 `distinct_count` 的平方累加到 `result` 中，并对 `mod` 取余。
   - 如果 `last_seen` 中存在 `nums[left]` 且其位置等于 `left`，则移动 `left` 指针。
5. 返回 `result`。

关键点:
- 使用滑动窗口和哈希表来高效地计算每个子数组的不同元素数目。
- 通过维护 `last_seen` 来快速判断当前子数组中的不同元素数目。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是数组 `nums` 的长度。每个子数组的处理时间是 O(1)。
空间复杂度: O(n)，用于存储 `last_seen` 哈希表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 计算子数组不同元素数目的平方和
    """
    mod = 10**9 + 7
    result = 0
    n = len(nums)
    
    for left in range(n):
        last_seen = {}
        distinct_count = 0
        for right in range(left, n):
            if nums[right] not in last_seen:
                distinct_count += 1
            last_seen[nums[right]] = right
            result = (result + distinct_count ** 2) % mod
    
    return result


Solution = create_solution(solution_function_name)