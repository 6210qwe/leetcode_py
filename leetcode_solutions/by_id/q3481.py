# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 其中 n 是表中的记录数。
空间复杂度: O(1) - 不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

def solution_function_name():
    """
    函数式接口 - 实现 SQL 查询
    """
    query = """
    SELECT 
        id, 
        record_date, 
        value
    FROM 
        records r1
    WHERE 
        EXISTS (
            SELECT 1 
            FROM records r2 
            WHERE r2.record_date = DATE_ADD(r1.record_date, INTERVAL 1 DAY)
        )
    """
    return query

