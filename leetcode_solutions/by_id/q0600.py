# ============================================================================
# 问题描述
# ============================================================================
"""
600. 不含连续1的非负整数 - 给定一个正整数 n ，请你统计在 [0, n] 范围的非负整数中，有多少个整数的二进制表示中不存在 连续的 1 。 示例 1: 输入: n = 5 输出: 5 解释: 下面列出范围在 [0, 5] 的非负整数与其对应的二进制表示： 0 : 0 1 : 1 2 : 10 3 : 11 4 : 100 5 : 101 其中，只有整数 3 违反规则（有两个连续的 1 ），其他 5 个满足规则。 示例 2: 输入: n = 1 输出: 2 示例 3: 输入: n = 2 输出: 3 提示: * 1 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算不超过 n 的所有不含连续 1 的非负整数的数量。

算法步骤:
1. 将 n 转换为二进制字符串。
2. 定义 dp[i][j] 表示长度为 i 的二进制数，以 j 结尾（j 为 0 或 1）且没有连续 1 的数量。
3. 初始化 dp 数组，dp[1][0] = 1, dp[1][1] = 1。
4. 通过状态转移方程更新 dp 数组：
   - dp[i][0] = dp[i-1][0] + dp[i-1][1]
   - dp[i][1] = dp[i-1][0]
5. 遍历 n 的二进制表示，根据当前位和前一位的状态更新结果。
6. 返回最终结果。

关键点:
- 通过动态规划避免重复计算。
- 通过二进制表示的遍历来处理边界情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)，因为我们需要遍历 n 的二进制表示。
空间复杂度: O(log n)，用于存储 dp 数组。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_integers(n: int) -> int:
    """
    函数式接口 - 计算不超过 n 的所有不含连续 1 的非负整数的数量。
    """
    # 将 n 转换为二进制字符串
    binary_n = bin(n)[2:]
    length = len(binary_n)
    
    # 定义 dp 数组
    dp = [[0, 0] for _ in range(length + 1)]
    dp[1][0], dp[1][1] = 1, 1
    
    # 通过状态转移方程更新 dp 数组
    for i in range(2, length + 1):
        dp[i][0] = dp[i-1][0] + dp[i-1][1]
        dp[i][1] = dp[i-1][0]
    
    # 初始化结果
    result = 0
    prev_bit = 0
    
    # 遍历 n 的二进制表示
    for i in range(length):
        bit = int(binary_n[i])
        if bit == 1:
            result += dp[length - i][0]
            if prev_bit == 1:
                break
        prev_bit = bit
    
    # 如果没有提前退出，加上最后一个数
    if prev_bit != 1:
        result += 1
    
    return result


Solution = create_solution(find_integers)