"""
LCP 66. 最小展台数量 - 力扣嘉年华将举办一系列展览活动，后勤部将负责为每场展览提供所需要的展台。 已知后勤部得到了一份需求清单，记录了近期展览所需要的展台类型， `demand[i][j]` 表示第 `i` 天展览时第 `j` 个展台的类型。 在满足每一天展台需求的基础上，请返回后勤部需要准备的 **最小** 展台数量。 **注意：** - 同一展台在不同天中可以重复使用。 **示例 1：** >输入：`demand = ["acd","bed","accd"]` > >输出：`6` > >解释： >第 `0` 天需要展台 `a、c、d`； >第 `1` 天需要展台 `b、e、d`； >第 `2` 天需要展台 `a、c、c、d`； >因此，后勤部准备 `abccde` 的展台，可以满足每天的展览需求; **示例 2：** >输入：`demand = ["abc","ab","ac","b"]` > >输出：`3` **提示：** - `1 <= demand.length,demand[i].length <= 100` - `demand[i][j]` 仅为小写字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一个字典来记录每种展台的最大需求量，然后求和。

算法步骤:
1. 初始化一个字典 `max_demand` 来记录每种展台的最大需求量。
2. 遍历每一天的需求，更新 `max_demand` 中每种展台的最大需求量。
3. 计算 `max_demand` 中所有值的总和，即为所需的最小展台数量。

关键点:
- 使用字典来记录每种展台的最大需求量。
- 遍历每一天的需求，更新字典中的最大值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是需求列表的长度，m 是每个需求字符串的平均长度。
空间复杂度: O(1)，因为字母种类是固定的（26个小写字母）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(demand: List[str]) -> int:
    """
    函数式接口 - 计算最小展台数量
    """
    max_demand = {}
    
    for day in demand:
        day_count = {}
        for char in day:
            day_count[char] = day_count.get(char, 0) + 1
        
        for char, count in day_count.items():
            max_demand[char] = max(max_demand.get(char, 0), count)
    
    return sum(max_demand.values())


Solution = create_solution(solution_function_name)