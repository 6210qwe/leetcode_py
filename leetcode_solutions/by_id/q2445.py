"""
2368. 受限条件下可到达节点的数目 - 现有一棵由 n 个节点组成的无向树，节点编号从 0 到 n - 1 ，共有 n - 1 条边。 给你一个二维整数数组 edges ，长度为 n - 1 ，其中 edges[i] = [ai, bi] 表示树中节点 ai 和 bi 之间存在一条边。另给你一个整数数组 restricted 表示 受限 节点。 在不访问受限节点的前提下，返回你可以从节点 0 到达的 最多 节点数目。 注意，节点 0 不 会标记为受限节点。 示例 1： [https://assets.leetcode.com/uploads/2022/06/15/ex1drawio.png] 输入：n = 7, edges = [[0,1],[1,2],[3,1],[4,0],[0,5],[5,6]], restricted = [4,5] 输出：4 解释：上图所示正是这棵树。 在不访问受限节点的前提下，只有节点 [0,1,2,3] 可以从节点 0 到达。 示例 2： [https://assets.leetcode.com/uploads/2022/06/15/ex2drawio.png] 输入：n = 7, edges = [[0,1],[0,2],[0,5],[0,4],[3,2],[6,5]], restricted = [4,2,1] 输出：3 解释：上图所示正是这棵树。 在不访问受限节点的前提下，只有节点 [0,5,6] 可以从节点 0 到达。 提示： * 2 <= n <= 105 * edges.length == n - 1 * edges[i].length == 2 * 0 <= ai, bi < n * ai != bi * edges 表示一棵有效的树 * 1 <= restricted.length < n * 1 <= restricted[i] < n * restricted 中的所有值 互不相同
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索 (DFS) 来遍历树，并跳过受限节点。

算法步骤:
1. 构建树的邻接表表示。
2. 将受限节点存储在一个集合中，以便快速查找。
3. 从节点 0 开始进行 DFS，跳过受限节点。
4. 计算可以到达的节点数量。

关键点:
- 使用邻接表表示树结构。
- 使用集合存储受限节点，以便 O(1) 时间复杂度内判断节点是否受限。
- 使用 DFS 遍历树，避免访问受限节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是节点数。构建邻接表和 DFS 都是线性时间复杂度。
空间复杂度: O(n)，用于存储邻接表和递归调用栈。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional

def solution_function_name(n: int, edges: List[List[int]], restricted: List[int]) -> int:
    """
    函数式接口 - 计算在不访问受限节点的前提下，可以从节点 0 到达的最多节点数目。
    """
    # 构建邻接表
    graph = [[] for _ in range(n)]
    for u, v in edges:
        graph[u].append(v)
        graph[v].append(u)
    
    # 将受限节点存储在集合中
    restricted_set = set(restricted)
    
    # 深度优先搜索
    def dfs(node: int, parent: int) -> int:
        if node in restricted_set:
            return 0
        count = 1
        for neighbor in graph[node]:
            if neighbor != parent:
                count += dfs(neighbor, node)
        return count
    
    return dfs(0, -1)

