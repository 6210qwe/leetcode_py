"""
3340. 检查平衡字符串 - 给你一个仅由数字 0 - 9 组成的字符串 num。如果偶数下标处的数字之和等于奇数下标处的数字之和，则认为该数字字符串是一个 平衡字符串。 如果 num 是一个 平衡字符串，则返回 true；否则，返回 false。 示例 1： 输入：num = "1234" 输出：false 解释： * 偶数下标处的数字之和为 1 + 3 = 4，奇数下标处的数字之和为 2 + 4 = 6。 * 由于 4 不等于 6，num 不是平衡字符串。 示例 2： 输入：num = "24123" 输出：true 解释： * 偶数下标处的数字之和为 2 + 1 + 3 = 6，奇数下标处的数字之和为 4 + 2 = 6。 * 由于两者相等，num 是平衡字符串。 提示： * 2 <= num.length <= 100 * num 仅由数字 0 - 9 组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算字符串中偶数下标和奇数下标的数字之和，并比较它们是否相等。

算法步骤:
1. 初始化两个变量 `even_sum` 和 `odd_sum` 为 0，分别用于存储偶数下标和奇数下标的数字之和。
2. 遍历字符串中的每个字符，根据其下标是偶数还是奇数，将对应的数字加到 `even_sum` 或 `odd_sum` 中。
3. 比较 `even_sum` 和 `odd_sum` 是否相等，如果相等则返回 True，否则返回 False。

关键点:
- 通过一次遍历字符串来计算偶数下标和奇数下标的数字之和。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_balanced_string(num: str) -> bool:
    """
    函数式接口 - 检查给定的字符串是否为平衡字符串
    """
    even_sum = 0
    odd_sum = 0
    
    for i, char in enumerate(num):
        if i % 2 == 0:
            even_sum += int(char)
        else:
            odd_sum += int(char)
    
    return even_sum == odd_sum


Solution = create_solution(is_balanced_string)