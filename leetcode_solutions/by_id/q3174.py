"""
2914. 使二进制字符串变美丽的最少修改次数 - 给你一个长度为偶数下标从 0 开始的二进制字符串 s 。 如果可以将一个字符串分割成一个或者更多满足以下条件的子字符串，那么我们称这个字符串是 美丽的 ： * 每个子字符串的长度都是 偶数 。 * 每个子字符串都 只 包含 1 或 只 包含 0 。 你可以将 s 中任一字符改成 0 或者 1 。 请你返回让字符串 s 美丽的 最少 字符修改次数。 示例 1： 输入：s = "1001" 输出：2 解释：我们将 s[1] 改为 1 ，且将 s[3] 改为 0 ，得到字符串 "1100" 。 字符串 "1100" 是美丽的，因为我们可以将它分割成 "11|00" 。 将字符串变美丽最少需要 2 次修改。 示例 2： 输入：s = "10" 输出：1 解释：我们将 s[1] 改为 1 ，得到字符串 "11" 。 字符串 "11" 是美丽的，因为它已经是美丽的。 将字符串变美丽最少需要 1 次修改。 示例 3： 输入：s = "0000" 输出：0 解释：不需要进行任何修改，字符串 "0000" 已经是美丽字符串。 提示： * 2 <= s.length <= 105 * s 的长度为偶数。 * s[i] 要么是 '0' ，要么是 '1' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串并检查每对相邻字符是否相同，如果不同则需要进行一次修改。

算法步骤:
1. 初始化一个计数器 `changes` 为 0。
2. 遍历字符串，每次检查两个相邻字符。
3. 如果两个相邻字符不同，则增加 `changes` 计数器。
4. 返回 `changes` 作为结果。

关键点:
- 由于字符串长度为偶数，因此可以直接两两比较。
- 每次修改可以使一对不同的字符变为相同的字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。我们只需要遍历字符串一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_changes_to_make_beautiful(s: str) -> int:
    """
    函数式接口 - 返回使二进制字符串变美丽的最少修改次数
    """
    changes = 0
    for i in range(0, len(s), 2):
        if s[i] != s[i + 1]:
            changes += 1
    return changes


Solution = create_solution(min_changes_to_make_beautiful)