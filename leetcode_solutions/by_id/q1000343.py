# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 回溯算法，每个元素可以选择加入或不加入

算法步骤:
1. 使用回溯，对每个元素有两种选择：加入或不加入
2. 当遍历完所有元素时，将当前子集加入结果
3. 也可以使用位运算，每个子集对应一个二进制数

关键点:
- 回溯算法
- 时间复杂度O(2^n)
"""

# ============================================================================
