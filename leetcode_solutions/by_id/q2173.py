"""
2047. 句子中的有效单词数 - 句子仅由小写字母（'a' 到 'z'）、数字（'0' 到 '9'）、连字符（'-'）、标点符号（'!'、'.' 和 ','）以及空格（' '）组成。每个句子可以根据空格分解成 一个或者多个 token ，这些 token 之间由一个或者多个空格 ' ' 分隔。 如果一个 token 同时满足下述条件，则认为这个 token 是一个有效单词： * 仅由小写字母、连字符和/或标点（不含数字）组成。 * 至多一个 连字符 '-' 。如果存在，连字符两侧应当都存在小写字母（"a-b" 是一个有效单词，但 "-ab" 和 "ab-" 不是有效单词）。 * 至多一个 标点符号。如果存在，标点符号应当位于 token 的 末尾 。 这里给出几个有效单词的例子："a-b."、"afad"、"ba-c"、"a!" 和 "!" 。 给你一个字符串 sentence ，请你找出并返回 sentence 中 有效单词的数目 。 示例 1： 输入：sentence = "cat and dog" 输出：3 解释：句子中的有效单词是 "cat"、"and" 和 "dog" 示例 2： 输入：sentence = "!this 1-s b8d!" 输出：0 解释：句子中没有有效单词 "!this" 不是有效单词，因为它以一个标点开头 "1-s" 和 "b8d" 也不是有效单词，因为它们都包含数字 示例 3： 输入：sentence = "alice and bob are playing stone-game10" 输出：5 解释：句子中的有效单词是 "alice"、"and"、"bob"、"are" 和 "playing" "stone-game10" 不是有效单词，因为它含有数字 提示： * 1 <= sentence.length <= 1000 * sentence 由小写英文字母、数字（0-9）、以及字符（' '、'-'、'!'、'.' 和 ','）组成 * 句子中至少有 1 个 token
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用正则表达式来匹配有效的单词。

算法步骤:
1. 将句子按空格分割成 token。
2. 对每个 token 使用正则表达式进行验证，确保其符合有效单词的条件。
3. 统计符合条件的 token 数量。

关键点:
- 使用正则表达式来匹配有效单词，确保高效且简洁。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是句子的长度。每个字符最多被处理一次。
空间复杂度: O(1)，正则表达式的匹配不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution
import re

def count_valid_words(sentence: str) -> int:
    """
    函数式接口 - 计算句子中的有效单词数
    """
    # 定义有效单词的正则表达式
    valid_word_pattern = re.compile(r'^[a-z]+(-[a-z]+)?[!,.]?$')
    
    # 将句子按空格分割成 token
    tokens = sentence.split()
    
    # 统计符合条件的 token 数量
    valid_count = sum(bool(valid_word_pattern.match(token)) for token in tokens)
    
    return valid_count

Solution = create_solution(count_valid_words)