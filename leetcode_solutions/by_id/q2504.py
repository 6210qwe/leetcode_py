"""
2420. 找到所有好下标 - 给你一个大小为 n 下标从 0 开始的整数数组 nums 和一个正整数 k 。 对于 k <= i < n - k 之间的一个下标 i ，如果它满足以下条件，我们就称它为一个 好 下标： * 下标 i 之前 的 k 个元素是 非递增的 。 * 下标 i 之后 的 k 个元素是 非递减的 。 按 升序 返回所有好下标。 示例 1： 输入：nums = [2,1,1,1,3,4,1], k = 2 输出：[2,3] 解释：数组中有两个好下标： - 下标 2 。子数组 [2,1] 是非递增的，子数组 [1,3] 是非递减的。 - 下标 3 。子数组 [1,1] 是非递增的，子数组 [3,4] 是非递减的。 注意，下标 4 不是好下标，因为 [4,1] 不是非递减的。 示例 2： 输入：nums = [2,1,1,2], k = 2 输出：[] 解释：数组中没有好下标。 提示： * n == nums.length * 3 <= n <= 105 * 1 <= nums[i] <= 106 * 1 <= k <= n / 2
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个数组分别记录每个位置左侧连续非递增的长度和右侧连续非递减的长度。

算法步骤:
1. 初始化两个数组 `non_increasing` 和 `non_decreasing`，分别记录每个位置左侧连续非递增的长度和右侧连续非递减的长度。
2. 从左到右遍历数组，填充 `non_increasing` 数组。
3. 从右到左遍历数组，填充 `non_decreasing` 数组。
4. 再次遍历数组，找到满足条件的好下标。

关键点:
- 使用两次遍历分别计算左侧和右侧的连续非递增/非递减长度。
- 最后一次遍历检查每个位置是否满足好下标的条件。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_good_indices(nums: List[int], k: int) -> List[int]:
    """
    函数式接口 - 找到所有好下标
    """
    n = len(nums)
    non_increasing = [1] * n
    non_decreasing = [1] * n

    # 计算左侧连续非递增的长度
    for i in range(1, n):
        if nums[i] <= nums[i - 1]:
            non_increasing[i] = non_increasing[i - 1] + 1

    # 计算右侧连续非递减的长度
    for i in range(n - 2, -1, -1):
        if nums[i] <= nums[i + 1]:
            non_decreasing[i] = non_decreasing[i + 1] + 1

    # 找到所有好下标
    good_indices = []
    for i in range(k, n - k):
        if non_increasing[i - 1] >= k and non_decreasing[i + 1] >= k:
            good_indices.append(i)

    return good_indices


Solution = create_solution(find_good_indices)