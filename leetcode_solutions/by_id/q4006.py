"""
核心思想: 使用度数数组来判断是否存在一个简单无向图。如果每个节点的度数都是偶数且总度数是偶数，则存在这样的图。

算法步骤:
1. 检查每个节点的度数是否为偶数。
2. 计算所有节点度数的总和，检查是否为偶数。

关键点:
- 简单无向图中每个节点的度数必须是偶数。
- 总度数必须是偶数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
