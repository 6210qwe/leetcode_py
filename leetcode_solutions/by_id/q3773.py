"""
3507. 移除最小数对使数组有序 I - 给你一个数组 nums，你可以执行以下操作任意次数： * 选择 相邻 元素对中 和最小 的一对。如果存在多个这样的对，选择最左边的一个。 * 用它们的和替换这对元素。 返回将数组变为 非递减 所需的 最小操作次数 。 如果一个数组中每个元素都大于或等于它前一个元素（如果存在的话），则称该数组为非递减。 示例 1： 输入： nums = [5,2,3,1] 输出： 2 解释： * 元素对 (3,1) 的和最小，为 4。替换后 nums = [5,2,4]。 * 元素对 (2,4) 的和为 6。替换后 nums = [5,6]。 数组 nums 在两次操作后变为非递减。 示例 2： 输入： nums = [1,2,2] 输出： 0 解释： 数组 nums 已经是非递减的。 提示： * 1 <= nums.length <= 50 * -1000 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法找到需要移除的最小数对，使得数组变为非递减。

算法步骤:
1. 初始化一个计数器 `count` 用于记录操作次数。
2. 遍历数组，检查每对相邻元素是否满足非递减条件。
3. 如果不满足条件，移除这对元素，并增加计数器 `count`。
4. 重复上述步骤直到数组变为非递减。

关键点:
- 通过一次遍历即可找到所有需要移除的数对。
- 通过贪心策略，每次移除和最小的相邻元素对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要一次遍历数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_sort(nums: List[int]) -> int:
    """
    函数式接口 - 计算将数组变为非递减所需的最小操作次数
    """
    count = 0
    n = len(nums)
    
    for i in range(1, n):
        if nums[i] < nums[i - 1]:
            # 移除当前元素对
            nums[i] = nums[i - 1]
            count += 1
    
    return count


Solution = create_solution(min_operations_to_sort)