# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串来检查每个字符与其前一个字符是否相同。如果不同，则需要确保当前字符与下一个字符相同。

算法步骤:
1. 初始化一个计数器 `count` 为 1，表示当前子串的长度。
2. 从第二个字符开始遍历字符串：
   - 如果当前字符与前一个字符相同，增加计数器 `count`。
   - 如果当前字符与前一个字符不同，检查 `count` 是否为 1 或 4 的倍数。如果不是，返回 False。
   - 重置计数器 `count` 为 1。
3. 遍历结束后，检查最后一个子串的长度是否为 1 或 4 的倍数。如果不是，返回 False。
4. 如果所有检查都通过，返回 True。

关键点:
- 通过一次遍历即可完成检查，时间复杂度为 O(n)。
- 使用常数空间存储计数器和前一个字符，空间复杂度为 O(1)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> bool:
    """
    函数式接口 - 判断字符串是否可分解为值均等的子串
    """
    if not s:
        return False

    count = 1
    for i in range(1, len(s)):
        if s[i] == s[i - 1]:
            count += 1
        else:
            if count % 4 != 0 and count != 1:
                return False
            count = 1

    # 检查最后一个子串
    return count % 4 == 0 or count == 1


Solution = create_solution(solution_function_name)