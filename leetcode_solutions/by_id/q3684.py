"""
3407. 子字符串匹配模式 - 给你一个字符串 s 和一个模式字符串 p ，其中 p 恰好 包含 一个 '*' 符号。 p 中的 '*' 符号可以被替换为零个或多个字符组成的任意字符序列。 如果 p 可以变成 s 的 子字符串，那么返回 true ，否则返回 false 。 示例 1： 输入：s = "leetcode", p = "ee*e" 输出：true 解释： 将 '*' 替换为 "tcod" ，子字符串 "eetcode" 匹配模式串。 示例 2： 输入：s = "car", p = "c*v" 输出：false 解释： 不存在匹配模式串的子字符串。 示例 3： 输入：s = "luck", p = "u*" 输出：true 解释： 子字符串 "u" ，"uc" 和 "uck" 都匹配模式串。 提示： * 1 <= s.length <= 50 * 1 <= p.length <= 50 * s 只包含小写英文字母。 * p 只包含小写英文字母和一个 '*' 符号。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法来匹配字符串 s 和模式 p。

算法步骤:
1. 找到模式 p 中的 '*' 符号的位置。
2. 分别处理 '*' 前后的部分，使用双指针来匹配这些部分。
3. 如果 '*' 前的部分匹配成功，则继续匹配 '*' 后的部分。
4. 如果所有部分都匹配成功，则返回 True，否则返回 False。

关键点:
- 使用双指针方法可以有效地匹配字符串。
- 处理 '*' 符号时，需要考虑其前后部分的匹配情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是字符串 s 的长度，m 是模式 p 的长度。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_match(s: str, p: str) -> bool:
    """
    函数式接口 - 判断模式 p 是否可以变成 s 的子字符串
    """
    # 找到 '*' 在 p 中的位置
    star_index = p.find('*')
    
    # 如果没有 '*'，直接判断 p 是否是 s 的子字符串
    if star_index == -1:
        return p in s
    
    # 获取 '*' 前后的部分
    prefix = p[:star_index]
    suffix = p[star_index + 1:]
    
    # 计算前缀和后缀的总长度
    total_length = len(prefix) + len(suffix)
    
    # 遍历 s，尝试匹配前缀和后缀
    for i in range(len(s) - total_length + 1):
        if s[i:i+len(prefix)] == prefix and s[i+len(prefix):i+len(prefix)+len(suffix)] == suffix:
            return True
    
    return False


Solution = create_solution(is_match)