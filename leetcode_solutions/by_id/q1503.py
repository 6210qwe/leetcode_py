"""
1402. 做菜顺序 - 一个厨师收集了他 n 道菜的满意程度 satisfaction ，这个厨师做出每道菜的时间都是 1 单位时间。 一道菜的 「 like-time 系数 」定义为烹饪这道菜结束的时间（包含之前每道菜所花费的时间）乘以这道菜的满意程度，也就是 time[i]*satisfaction[i] 。 返回厨师在准备了一定数量的菜肴后可以获得的最大 like-time 系数 总和。 你可以按 任意 顺序安排做菜的顺序，你也可以选择放弃做某些菜来获得更大的总和。 示例 1： 输入：satisfaction = [-1,-8,0,5,-9] 输出：14 解释：去掉第二道和最后一道菜，最大的 like-time 系数和为 (-1*1 + 0*2 + 5*3 = 14) 。每道菜都需要花费 1 单位时间完成。 示例 2： 输入：satisfaction = [4,3,2] 输出：20 解释：可以按照任意顺序做菜 (2*1 + 3*2 + 4*3 = 20) 示例 3： 输入：satisfaction = [-1,-4,-5] 输出：0 解释：大家都不喜欢这些菜，所以不做任何菜就可以获得最大的 like-time 系数。 提示： * n == satisfaction.length * 1 <= n <= 500 * -1000 <= satisfaction[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，优先考虑满意度高的菜品，并且利用累积和来计算最大 like-time 系数。

算法步骤:
1. 对满意度数组进行降序排序。
2. 从满意度最高的菜品开始累加，同时维护当前的 like-time 系数和累积满意度。
3. 如果当前累积满意度大于等于零，则继续累加；否则停止累加，返回当前的最大 like-time 系数。

关键点:
- 通过降序排序，确保高满意度的菜品优先被考虑。
- 使用累积和来动态计算 like-time 系数，避免重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 satisfaction 的长度，主要由排序操作决定。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def max_satisfaction(satisfaction: List[int]) -> int:
    """
    函数式接口 - 计算最大 like-time 系数
    """
    # 对满意度数组进行降序排序
    satisfaction.sort(reverse=True)
    
    total_sum = 0  # 当前累积满意度
    max_like_time = 0  # 最大 like-time 系数
    
    for s in satisfaction:
        if total_sum + s > 0:
            total_sum += s
            max_like_time += total_sum
        else:
            break
    
    return max_like_time


Solution = create_solution(max_satisfaction)