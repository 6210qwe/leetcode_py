"""
2980. 检查按位或是否存在尾随零 - 给你一个 正整数 数组 nums 。 你需要检查是否可以从数组中选出 两个或更多 元素，满足这些元素的按位或运算（ OR）结果的二进制表示中 至少 存在一个尾随零。 例如，数字 5 的二进制表示是 "101"，不存在尾随零，而数字 4 的二进制表示是 "100"，存在两个尾随零。 如果可以选择两个或更多元素，其按位或运算结果存在尾随零，返回 true；否则，返回 false 。 示例 1： 输入：nums = [1,2,3,4,5] 输出：true 解释：如果选择元素 2 和 4，按位或运算结果是 6，二进制表示为 "110" ，存在一个尾随零。 示例 2： 输入：nums = [2,4,8,16] 输出：true 解释：如果选择元素 2 和 4，按位或运算结果是 6，二进制表示为 "110"，存在一个尾随零。 其他按位或运算结果存在尾随零的可能选择方案包括：(2, 8), (2, 16), (4, 8), (4, 16), (8, 16), (2, 4, 8), (2, 4, 16), (2, 8, 16), (4, 8, 16), 以及 (2, 4, 8, 16) 。 示例 3： 输入：nums = [1,3,5,7,9] 输出：false 解释：不存在按位或运算结果存在尾随零的选择方案。 提示： * 2 <= nums.length <= 100 * 1 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 检查数组中是否有至少两个偶数，因为偶数的最低位是0，按位或运算后仍然会保留尾随零。

算法步骤:
1. 初始化一个计数器来记录偶数的数量。
2. 遍历数组，统计偶数的数量。
3. 如果偶数的数量大于等于2，返回True；否则，返回False。

关键点:
- 只需要统计偶数的数量，因为偶数的最低位是0，按位或运算后仍然会保留尾随零。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中n是数组的长度，因为我们需要遍历整个数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def check_if_bitwise_or_has_trailing_zeros(nums: List[int]) -> bool:
    """
    检查数组中是否有至少两个偶数，因为偶数的最低位是0，按位或运算后仍然会保留尾随零。
    """
    even_count = 0  # 计数器，用于记录偶数的数量
    for num in nums:
        if num % 2 == 0:
            even_count += 1
            if even_count >= 2:
                return True
    return False


