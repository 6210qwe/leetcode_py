"""
3010. 将数组分成最小总代价的子数组 I - 给你一个长度为 n 的整数数组 nums 。 一个数组的 代价 是它的 第一个 元素。比方说，[1,2,3] 的代价是 1 ，[3,4,1] 的代价是 3 。 你需要将 nums 分成 3 个 连续且没有交集 的子数组。 请你返回这些子数组的 最小 代价 总和 。 示例 1： 输入：nums = [1,2,3,12] 输出：6 解释：最佳分割成 3 个子数组的方案是：[1] ，[2] 和 [3,12] ，总代价为 1 + 2 + 3 = 6 。 其他得到 3 个子数组的方案是： - [1] ，[2,3] 和 [12] ，总代价是 1 + 2 + 12 = 15 。 - [1,2] ，[3] 和 [12] ，总代价是 1 + 3 + 12 = 16 。 示例 2： 输入：nums = [5,4,3] 输出：12 解释：最佳分割成 3 个子数组的方案是：[5] ，[4] 和 [3] ，总代价为 5 + 4 + 3 = 12 。 12 是所有分割方案里的最小总代价。 示例 3： 输入：nums = [10,3,1,1] 输出：12 解释：最佳分割成 3 个子数组的方案是：[10,3] ，[1] 和 [1] ，总代价为 10 + 1 + 1 = 12 。 12 是所有分割方案里的最小总代价。 提示： * 3 <= n <= 50 * 1 <= nums[i] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 为了使总代价最小，我们需要选择三个子数组的第一个元素，使得它们的和最小。我们可以先对数组进行排序，然后选择前三个元素作为三个子数组的第一个元素。

算法步骤:
1. 对数组进行排序。
2. 选择排序后的前三个元素作为三个子数组的第一个元素。
3. 返回这三个元素的和作为最小总代价。

关键点:
- 通过对数组进行排序，我们可以确保选择的前三个元素是最小的，从而使得总代价最小。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n) - 排序操作的时间复杂度。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 对数组进行排序
    sorted_nums = sorted(nums)
    
    # 选择前三个元素作为三个子数组的第一个元素
    min_cost = sum(sorted_nums[:3])
    
    return min_cost


Solution = create_solution(solution_function_name)