"""
1550. 存在连续三个奇数的数组 - 给你一个整数数组 arr，请你判断数组中是否存在连续三个元素都是奇数的情况：如果存在，请返回 true ；否则，返回 false 。 示例 1： 输入：arr = [2,6,4,1] 输出：false 解释：不存在连续三个元素都是奇数的情况。 示例 2： 输入：arr = [1,2,34,3,4,5,7,23,12] 输出：true 解释：存在连续三个元素都是奇数的情况，即 [5,7,23] 。 提示： * 1 <= arr.length <= 1000 * 1 <= arr[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历数组来检查是否存在连续三个奇数。

算法步骤:
1. 初始化一个计数器 `count` 为 0，用于记录当前连续的奇数个数。
2. 遍历数组中的每个元素：
   - 如果当前元素是奇数，则增加计数器 `count`。
   - 否则，将计数器 `count` 重置为 0。
3. 如果在任何时候 `count` 达到 3，则返回 `True`。
4. 遍历结束后，如果没有找到连续三个奇数，则返回 `False`。

关键点:
- 通过一次遍历即可完成检查，时间复杂度为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(arr: List[int]) -> bool:
    """
    函数式接口 - 检查数组中是否存在连续三个奇数
    """
    count = 0
    for num in arr:
        if num % 2 == 1:
            count += 1
            if count == 3:
                return True
        else:
            count = 0
    return False


Solution = create_solution(solution_function_name)