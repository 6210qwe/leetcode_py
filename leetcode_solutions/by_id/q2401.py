"""
2315. 统计星号 - 给你一个字符串 s ，每 两个 连续竖线 '|' 为 一对 。换言之，第一个和第二个 '|' 为一对，第三个和第四个 '|' 为一对，以此类推。 请你返回 不在 竖线对之间，s 中 '*' 的数目。 注意，每个竖线 '|' 都会 恰好 属于一个对。 示例 1： 输入：s = "l|*e*et|c**o|*de|" 输出：2 解释：不在竖线对之间的字符加粗加斜体后，得到字符串："l|*e*et|c**o|*de|" 。 第一和第二条竖线 '|' 之间的字符不计入答案。 同时，第三条和第四条竖线 '|' 之间的字符也不计入答案。 不在竖线对之间总共有 2 个星号，所以我们返回 2 。 示例 2： 输入：s = "iamprogrammer" 输出：0 解释：在这个例子中，s 中没有星号。所以返回 0 。 示例 3： 输入：s = "yo|uar|e**|b|e***au|tifu|l" 输出：5 解释：需要考虑的字符加粗加斜体后："yo|uar|e**|b|e***au|tifu|l" 。不在竖线对之间总共有 5 个星号。所以我们返回 5 。 提示： * 1 <= s.length <= 1000 * s 只包含小写英文字母，竖线 '|' 和星号 '*' 。 * s 包含 偶数 个竖线 '|' 。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历字符串，统计不在竖线对之间的星号数量。

算法步骤:
1. 初始化一个计数器 `count` 用于记录星号的数量。
2. 初始化一个标志变量 `in_pair` 用于标记当前是否在竖线对之间。
3. 遍历字符串 `s`：
   - 如果遇到竖线 `|`，切换 `in_pair` 的状态。
   - 如果 `in_pair` 为 `False` 且遇到星号 `*`，增加 `count`。
4. 返回 `count`。

关键点:
- 使用标志变量 `in_pair` 来判断当前是否在竖线对之间。
- 仅在 `in_pair` 为 `False` 时统计星号。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历一次字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def count_asterisks(s: str) -> int:
    """
    函数式接口 - 统计不在竖线对之间的星号数量
    """
    count = 0
    in_pair = False
    for char in s:
        if char == '|':
            in_pair = not in_pair
        elif char == '*' and not in_pair:
            count += 1
    return count


Solution = create_solution(count_asterisks)