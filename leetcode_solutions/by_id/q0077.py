"""
77. 组合 - 给定两个整数 n 和 k，返回范围 [1, n] 中所有可能的 k 个数的组合。 你可以按 任何顺序 返回答案。 示例 1： 输入：n = 4, k = 2 输出： [ [2,4], [3,4], [2,3], [1,2], [1,3], [1,4], ] 示例 2： 输入：n = 1, k = 1 输出：[[1]] 提示： * 1 <= n <= 20 * 1 <= k <= n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 回溯算法，从1到n中选择k个数

算法步骤:
1. 回溯函数backtrack(start, path)：
   - 如果path长度等于k，加入结果
   - 否则，对于i从start到n：
     * 选择i，加入path
     * 递归backtrack(i+1, path)
     * 回溯，移除i
2. 调用backtrack(1, [])开始回溯

关键点:
- 从start开始选择，避免重复组合
- 每次选择后，下一个选择从i+1开始
- 时间复杂度O(C(n,k))，空间复杂度O(k)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(C(n,k)) - 需要生成所有组合
空间复杂度: O(k) - 递归栈深度和路径长度
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def combine(n: int, k: int) -> List[List[int]]:
    """
    函数式接口 - 回溯算法
    
    实现思路:
    使用回溯算法从1到n中选择k个数的所有组合。
    
    Args:
        n: 范围[1, n]
        k: 组合中数字的个数
        
    Returns:
        所有可能的k个数的组合列表
        
    Example:
        >>> combine(4, 2)
        [[1, 2], [1, 3], [1, 4], [2, 3], [2, 4], [3, 4]]
    """
    result = []
    
    def backtrack(start: int, path: List[int]):
        """回溯函数"""
        if len(path) == k:
            result.append(path[:])
            return
        
        for i in range(start, n + 1):
            path.append(i)
            backtrack(i + 1, path)
            path.pop()
    
    backtrack(1, [])
    return result


