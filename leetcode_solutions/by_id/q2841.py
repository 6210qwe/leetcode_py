def solution_function_name():
    """
    函数式接口 - 返回每辆自行车的最后使用时间
    """
    # SQL 查询语句
    query = """
    SELECT bike_id, MAX(started_at) AS last_used
    FROM Bikes
    GROUP BY bike_id
    ORDER BY bike_id
    """
    return query


