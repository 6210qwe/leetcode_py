"""
核心思想: 使用 SQL 查询来获取每辆自行车的最后使用时间。

算法步骤:
1. 从 `Bikes` 表中选择所有列。
2. 使用 `GROUP BY bike_id` 对每辆自行车进行分组。
3. 使用 `MAX(started_at)` 获取每辆自行车的最后使用时间。
4. 使用 `ORDER BY bike_id` 按自行车 ID 排序结果。

关键点:
- 使用 `MAX` 函数获取每辆自行车的最后使用时间。
- 使用 `GROUP BY` 和 `ORDER BY` 来确保结果按自行车 ID 排序。
"""

