"""
1317. 将整数转换为两个无零整数的和 - 「无零整数」是十进制表示中 不含任何 0 的正整数。 给你一个整数 n，请你返回一个 由两个整数组成的列表 [a, b]，满足： * a 和 b 都是无零整数 * a + b = n 题目数据保证至少有一个有效的解决方案。 如果存在多个有效解决方案，你可以返回其中任意一个。 示例 1： 输入：n = 2 输出：[1,1] 解释：a = 1, b = 1。a + b = n 并且 a 和 b 的十进制表示形式都不包含任何 0。 示例 2： 输入：n = 11 输出：[2,9] 示例 3： 输入：n = 10000 输出：[1,9999] 示例 4： 输入：n = 69 输出：[1,68] 示例 5： 输入：n = 1010 输出：[11,999] 提示： * 2 <= n <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过遍历从 1 到 n-1 的所有可能值，找到第一个符合条件的 a 和 b。

算法步骤:
1. 从 1 开始遍历到 n-1。
2. 对于每个 i，检查 i 和 n-i 是否都为无零整数。
3. 如果找到符合条件的 i 和 n-i，返回 [i, n-i]。

关键点:
- 使用辅助函数 `is_no_zero` 检查一个整数是否为无零整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 最坏情况下需要遍历 n-1 个数。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_no_zero(num: int) -> bool:
    """检查一个整数是否为无零整数"""
    return '0' not in str(num)


def solution_function_name(n: int) -> List[int]:
    """
    函数式接口 - 将整数 n 转换为两个无零整数的和
    """
    for i in range(1, n):
        if is_no_zero(i) and is_no_zero(n - i):
            return [i, n - i]


Solution = create_solution(solution_function_name)