"""
2643. 一最多的行 - 给你一个大小为 m x n 的二进制矩阵 mat ，请你找出包含最多 1 的行的下标（从 0 开始）以及这一行中 1 的数目。 如果有多行包含最多的 1 ，只需要选择 行下标最小 的那一行。 返回一个由行下标和该行中 1 的数量组成的数组。 示例 1： 输入：mat = [[0,1],[1,0]] 输出：[0,1] 解释：两行中 1 的数量相同。所以返回下标最小的行，下标为 0 。该行 1 的数量为 1 。所以，答案为 [0,1] 。 示例 2： 输入：mat = [[0,0,0],[0,1,1]] 输出：[1,2] 解释：下标为 1 的行中 1 的数量最多。该行 1 的数量为 2 。所以，答案为 [1,2] 。 示例 3： 输入：mat = [[0,0],[1,1],[0,0]] 输出：[1,2] 解释：下标为 1 的行中 1 的数量最多。该行 1 的数量为 2 。所以，答案为 [1,2] 。 提示： * m == mat.length * n == mat[i].length * 1 <= m, n <= 100 * mat[i][j] 为 0 或 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历每一行，统计每行中 1 的数量，并记录最大值及其对应的行下标。

算法步骤:
1. 初始化变量 `max_ones` 用于记录当前最大 1 的数量，`max_index` 用于记录最大 1 的数量对应的行下标。
2. 遍历每一行，计算每行中 1 的数量。
3. 如果当前行 1 的数量大于 `max_ones`，更新 `max_ones` 和 `max_index`。
4. 返回 `max_index` 和 `max_ones`。

关键点:
- 通过一次遍历即可找到包含最多 1 的行，时间复杂度为 O(m * n)。
- 只需要常数空间来存储最大值和对应的行下标。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 是矩阵的行数，n 是矩阵的列数。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(mat: List[List[int]]) -> List[int]:
    """
    函数式接口 - 找出包含最多 1 的行的下标及其 1 的数量
    """
    max_ones = 0
    max_index = 0
    
    for i, row in enumerate(mat):
        ones_count = sum(row)
        if ones_count > max_ones:
            max_ones = ones_count
            max_index = i
    
    return [max_index, max_ones]


Solution = create_solution(solution_function_name)