"""
2915. 和为目标值的最长子序列的长度 - 给你一个下标从 0 开始的整数数组 nums 和一个整数 target 。 返回和为 target 的 nums 子序列中，子序列 长度的最大值 。如果不存在和为 target 的子序列，返回 -1 。 子序列 指的是从原数组中删除一些或者不删除任何元素后，剩余元素保持原来的顺序构成的数组。 示例 1： 输入：nums = [1,2,3,4,5], target = 9 输出：3 解释：总共有 3 个子序列的和为 9 ：[4,5] ，[1,3,5] 和 [2,3,4] 。最长的子序列是 [1,3,5] 和 [2,3,4] 。所以答案为 3 。 示例 2： 输入：nums = [4,1,3,2,1,5], target = 7 输出：4 解释：总共有 5 个子序列的和为 7 ：[4,3] ，[4,1,2] ，[4,2,1] ，[1,1,5] 和 [1,3,2,1] 。最长子序列为 [1,3,2,1] 。所以答案为 4 。 示例 3： 输入：nums = [1,1,5,4,5], target = 3 输出：-1 解释：无法得到和为 3 的子序列。 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= 1000 * 1 <= target <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来解决这个问题。我们定义 dp[i] 表示和为 i 的最长子序列的长度。通过遍历数组中的每个元素，更新 dp 数组。

算法步骤:
1. 初始化一个大小为 target + 1 的 dp 数组，所有值设为 -1，表示初始状态下没有和为目标值的子序列。
2. 将 dp[0] 设为 0，表示和为 0 的子序列长度为 0。
3. 遍历数组中的每个元素，对于每个元素，从 target 到当前元素值进行逆向更新 dp 数组。
4. 如果 dp[target] 仍为 -1，表示没有和为 target 的子序列，返回 -1；否则返回 dp[target]。

关键点:
- 使用逆向更新 dp 数组，避免重复使用同一个元素。
- 动态规划的状态转移方程为 dp[j] = max(dp[j], dp[j - num] + 1)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * target)，其中 n 是 nums 的长度。
空间复杂度: O(target)，dp 数组的大小。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def length_of_longest_subsequence(nums: List[int], target: int) -> int:
    """
    函数式接口 - 返回和为 target 的 nums 子序列中，子序列长度的最大值。
    """
    # 初始化 dp 数组
    dp = [-1] * (target + 1)
    dp[0] = 0  # 和为 0 的子序列长度为 0

    # 遍历数组中的每个元素
    for num in nums:
        for j in range(target, num - 1, -1):
            if dp[j - num] != -1:
                dp[j] = max(dp[j], dp[j - num] + 1)

    return dp[target]


Solution = create_solution(length_of_longest_subsequence)