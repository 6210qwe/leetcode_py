"""
2216. 美化数组的最少删除数 - 给你一个下标从 0 开始的整数数组 nums ，如果满足下述条件，则认为数组 nums 是一个 美丽数组 ： * nums.length 为偶数 * 对所有满足 i % 2 == 0 的下标 i ，nums[i] != nums[i + 1] 均成立 注意，空数组同样认为是美丽数组。 你可以从 nums 中删除任意数量的元素。当你删除一个元素时，被删除元素右侧的所有元素将会向左移动一个单位以填补空缺，而左侧的元素将会保持 不变 。 返回使 nums 变为美丽数组所需删除的 最少 元素数目。 示例 1： 输入：nums = [1,1,2,3,5] 输出：1 解释：可以删除 nums[0] 或 nums[1] ，这样得到的 nums = [1,2,3,5] 是一个美丽数组。可以证明，要想使 nums 变为美丽数组，至少需要删除 1 个元素。 示例 2： 输入：nums = [1,1,2,2,3,3] 输出：2 解释：可以删除 nums[0] 和 nums[5] ，这样得到的 nums = [1,2,2,3] 是一个美丽数组。可以证明，要想使 nums 变为美丽数组，至少需要删除 2 个元素。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，遍历数组并尽量保留符合条件的元素。

算法步骤:
1. 初始化两个变量：`deletions` 用于记录删除的元素数量，`prev` 用于记录上一个保留的元素。
2. 遍历数组，对于每个元素：
   - 如果当前元素与 `prev` 相同且索引为偶数，则删除该元素。
   - 否则，更新 `prev` 为当前元素。
3. 返回 `deletions`。

关键点:
- 通过贪心策略，尽量保留符合条件的元素，从而最小化删除的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度，因为只需要一次遍历。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_deletions_to_make_array_beautiful(nums: List[int]) -> int:
    """
    函数式接口 - 使数组变为美丽数组所需的最少删除数
    """
    deletions = 0
    prev = None
    for i, num in enumerate(nums):
        if i % 2 == 0:
            if num == prev:
                deletions += 1
            else:
                prev = num
        else:
            if num == prev:
                deletions += 1
            else:
                prev = None
    return deletions


Solution = create_solution(min_deletions_to_make_array_beautiful)