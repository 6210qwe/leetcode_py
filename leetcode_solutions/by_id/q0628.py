"""
628. 三个数的最大乘积 - 给你一个整型数组 nums ，在数组中找出由三个数组成的最大乘积，并输出这个乘积。 示例 1： 输入：nums = [1,2,3] 输出：6 示例 2： 输入：nums = [1,2,3,4] 输出：24 示例 3： 输入：nums = [-1,-2,-3] 输出：-6 提示： * 3 <= nums.length <= 104 * -1000 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 最大乘积可能由三种情况产生：
1. 三个最大的正数相乘
2. 两个最小的负数和一个最大的正数相乘

算法步骤:
1. 找出数组中最大的三个数
2. 找出数组中最小的两个数
3. 计算两种情况的乘积并返回较大值

关键点:
- 使用线性扫描找到最大的三个数和最小的两个数，避免排序
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
