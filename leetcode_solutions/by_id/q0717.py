"""
717. 1 比特与 2 比特字符 - 有两种特殊字符： * 第一种字符可以用一比特 0 表示 * 第二种字符可以用两比特（10 或 11）表示 给你一个以 0 结尾的二进制数组 bits ，如果最后一个字符必须是一个一比特字符，则返回 true 。 示例 1: 输入: bits = [1, 0, 0] 输出: true 解释: 唯一的解码方式是将其解析为一个两比特字符和一个一比特字符。 所以最后一个字符是一比特字符。 示例 2: 输入：bits = [1,1,1,0] 输出：false 解释：唯一的解码方式是将其解析为两比特字符和两比特字符。 所以最后一个字符不是一比特字符。 提示: * 1 <= bits.length <= 1000 * bits[i] 为 0 或 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 从头到尾遍历数组，根据当前位决定是否跳过下一个位。

算法步骤:
1. 初始化索引 i 为 0。
2. 遍历数组直到倒数第二个元素：
   - 如果当前位是 1，则跳过下一个位（i += 2）。
   - 否则，只移动一位（i += 1）。
3. 最后检查索引 i 是否等于数组长度减 1，如果是则返回 True，否则返回 False。

关键点:
- 通过遍历数组并根据当前位决定是否跳过下一个位来判断最后一个字符是否是一比特字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组 bits 的长度。我们只需遍历一次数组。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_one_bit_character(bits: List[int]) -> bool:
    """
    函数式接口 - 判断最后一个字符是否是一比特字符
    """
    i = 0
    n = len(bits)
    while i < n - 1:
        if bits[i] == 1:
            i += 2
        else:
            i += 1
    return i == n - 1


Solution = create_solution(is_one_bit_character)