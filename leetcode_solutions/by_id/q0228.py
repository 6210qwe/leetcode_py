"""
228. 汇总区间 - 给定一个 无重复元素 的 有序 整数数组 nums 。 区间 [a,b] 是从 a 到 b（包含）的所有整数的集合。 返回 恰好覆盖数组中所有数字 的 最小有序 区间范围列表 。也就是说，nums 的每个元素都恰好被某个区间范围所覆盖，并且不存在属于某个区间但不属于 nums 的数字 x 。 列表中的每个区间范围 [a,b] 应该按如下格式输出： * "a->b" ，如果 a != b * "a" ，如果 a == b 示例 1： 输入：nums = [0,1,2,4,5,7] 输出：["0->2","4->5","7"] 解释：区间范围是： [0,2] --> "0->2" [4,5] --> "4->5" [7,7] --> "7" 示例 2： 输入：nums = [0,2,3,4,6,8,9] 输出：["0","2->4","6","8->9"] 解释：区间范围是： [0,0] --> "0" [2,4] --> "2->4" [6,6] --> "6" [8,9] --> "8->9" 提示： * 0 <= nums.length <= 20 * -231 <= nums[i] <= 231 - 1 * nums 中的所有值都 互不相同 * nums 按升序排列
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历数组，找出连续区间

算法步骤:
1. 使用双指针，start指向区间起点
2. 遍历数组，如果当前元素与下一个元素不连续，记录区间
3. 格式化区间为字符串

关键点:
- 使用双指针找出连续区间
- 时间复杂度O(n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n) - 需要遍历数组一次
空间复杂度: O(1) - 只使用常数额外空间（不包括结果数组）
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def summary_ranges(nums: List[int]) -> List[str]:
    """
    函数式接口 - 汇总区间
    
    实现思路:
    遍历数组，找出连续区间。
    
    Args:
        nums: 无重复元素的有序整数数组
        
    Returns:
        区间范围列表
        
    Example:
        >>> summary_ranges([0, 1, 2, 4, 5, 7])
        ['0->2', '4->5', '7']
    """
    if not nums:
        return []
    
    result = []
    start = 0
    
    for i in range(len(nums)):
        if i == len(nums) - 1 or nums[i + 1] != nums[i] + 1:
            if start == i:
                result.append(str(nums[start]))
            else:
                result.append(f"{nums[start]}->{nums[i]}")
            start = i + 1
    
    return result


