def divide(a: int, b: int) -> int:
    """
    函数式接口 - 实现两数相除
    """
    # 特殊情况处理
    if a == 0:
        return 0
    if b == 1:
        return a
    if b == -1:
        return -a if a > -2**31 else 2**31 - 1

    # 确定符号
    sign = -1 if (a > 0) ^ (b > 0) else 1

    # 将 a 和 b 转换为正数
    a, b = abs(a), abs(b)

    # 计算商
    quotient = 0
    while a >= b:
        temp, multiple = b, 1
        while a >= (temp << 1):
            temp <<= 1
            multiple <<= 1
        a -= temp
        quotient += multiple

    # 根据符号调整结果
    quotient = sign * quotient

    # 处理溢出
    return min(max(-2**31, quotient), 2**31 - 1)


