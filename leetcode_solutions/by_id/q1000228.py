"""
LCR 001. 两数相除 - 给定两个整数 a 和 b ，求它们的除法的商 a/b ，要求不得使用乘号 '*'、除号 '/' 以及求余符号 '%' 。 注意： * 整数除法的结果应当截去（truncate）其小数部分，例如：truncate(8.345) = 8 以及 truncate(-2.7335) = -2 * 假设我们的环境只能存储 32 位有符号整数，其数值范围是 [−231, 231−1]。本题中，如果除法结果溢出，则返回 231 − 1 示例 1： 输入：a = 15, b = 2 输出：7 解释：15/2 = truncate(7.5) = 7 示例 2： 输入：a = 7, b = -3 输出：-2 解释：7/-3 = truncate(-2.33333..) = -2 示例 3： 输入：a = 0, b = 1 输出：0 示例 4： 输入：a = 1, b = 1 输出：1 提示: * -231 <= a, b <= 231 - 1 * b != 0 注意：本题与主站 29 题相同：https://leetcode.cn/problems/divide-two-integers/ [https://leetcode.cn/problems/divide-two-integers/]
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用二进制移位和减法来模拟除法操作。

算法步骤:
1. 处理特殊情况，如被除数为 0 或除数为 1 或 -1。
2. 将 a 和 b 转换为正数，并记录符号。
3. 使用二进制移位和减法来计算商。
4. 根据符号调整结果，并处理溢出情况。

关键点:
- 使用二进制移位和减法来模拟除法操作，避免使用乘号、除号和求余符号。
- 处理符号和溢出情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log N)，其中 N 是被除数的绝对值。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

