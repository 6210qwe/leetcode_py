"""
核心思想: 使用动态规划和组合数学来计算满足条件的子序列数量。

算法步骤:
1. 计算每个元素的频率。
2. 使用动态规划来计算以每个元素作为唯一中间众数的子序列数量。
3. 对于每个元素，计算其左边和右边的子序列数量，并结合组合数学公式计算总数量。

关键点:
- 使用动态规划来避免重复计算。
- 利用组合数学公式来计算子序列数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n)
"""

# ============================================================================
