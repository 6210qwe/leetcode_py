"""
3228. 将 1 移动到末尾的最大操作次数 - 给你一个 二进制字符串 s。 你可以对这个字符串执行 任意次 下述操作： * 选择字符串中的任一下标 i（ i + 1 < s.length ），该下标满足 s[i] == '1' 且 s[i + 1] == '0'。 * 将字符 s[i] 向 右移 直到它到达字符串的末端或另一个 '1'。例如，对于 s = "010010"，如果我们选择 i = 1，结果字符串将会是 s = "000110"。 返回你能执行的 最大 操作次数。 示例 1： 输入： s = "1001101" 输出： 4 解释： 可以执行以下操作： * 选择下标 i = 0。结果字符串为 s = "0011101"。 * 选择下标 i = 4。结果字符串为 s = "0011011"。 * 选择下标 i = 3。结果字符串为 s = "0010111"。 * 选择下标 i = 2。结果字符串为 s = "0001111"。 示例 2： 输入： s = "00111" 输出： 0 提示： * 1 <= s.length <= 105 * s[i] 为 '0' 或 '1'。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法，从右向左遍历字符串，计算每个 '1' 可以移动的最大距离。

算法步骤:
1. 初始化两个变量：`max_ops` 用于记录最大操作次数，`zero_count` 用于记录当前 '0' 的数量。
2. 从右向左遍历字符串：
   - 如果遇到 '1'，则将 `zero_count` 加到 `max_ops` 中，并重置 `zero_count`。
   - 如果遇到 '0'，则增加 `zero_count`。
3. 返回 `max_ops`。

关键点:
- 从右向左遍历可以确保每次移动 '1' 时，不会影响后续的 '1' 的移动。
- 使用 `zero_count` 记录当前 '0' 的数量，以便在遇到 '1' 时计算可移动的最大距离。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需遍历一次字符串。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 返回将 1 移动到末尾的最大操作次数
    """
    max_ops = 0
    zero_count = 0
    
    for char in reversed(s):
        if char == '1':
            max_ops += zero_count
            zero_count = 0
        else:
            zero_count += 1
    
    return max_ops


Solution = create_solution(solution_function_name)