"""
1400. 构造 K 个回文字符串 - 给你一个字符串 s 和一个整数 k 。请你用 s 字符串中 所有字符 构造 k 个非空 回文串 。 如果你可以用 s 中所有字符构造 k 个回文字符串，那么请你返回 True ，否则返回 False 。 示例 1： 输入：s = "annabelle", k = 2 输出：true 解释：可以用 s 中所有字符构造 2 个回文字符串。 一些可行的构造方案包括："anna" + "elble"，"anbna" + "elle"，"anellena" + "b" 示例 2： 输入：s = "leetcode", k = 3 输出：false 解释：无法用 s 中所有字符构造 3 个回文串。 示例 3： 输入：s = "true", k = 4 输出：true 解释：唯一可行的方案是让 s 中每个字符单独构成一个字符串。 提示： * 1 <= s.length <= 105 * s 中所有字符都是小写英文字母。 * 1 <= k <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算字符串中每个字符的出现次数，判断是否可以构造 k 个回文字符串。

算法步骤:
1. 统计字符串中每个字符的出现次数。
2. 计算出现奇数次的字符数量。
3. 如果出现奇数次的字符数量大于 k，则无法构造 k 个回文字符串；否则可以。

关键点:
- 一个回文字符串最多只能有一个字符出现奇数次。
- 如果出现奇数次的字符数量大于 k，则无法构造 k 个回文字符串。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。
空间复杂度: O(1)，因为字符集大小固定为 26。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_construct_k_palindromes(s: str, k: int) -> bool:
    """
    判断是否可以用字符串 s 中的所有字符构造 k 个回文字符串。
    """
    if len(s) < k:
        return False

    # 统计每个字符的出现次数
    char_count = [0] * 26
    for char in s:
        char_count[ord(char) - ord('a')] += 1

    # 计算出现奇数次的字符数量
    odd_count = sum(count % 2 for count in char_count)

    # 判断是否可以构造 k 个回文字符串
    return odd_count <= k


Solution = create_solution(can_construct_k_palindromes)