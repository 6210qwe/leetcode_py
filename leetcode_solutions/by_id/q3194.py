"""
2942. 查找包含给定字符的单词 - 给你一个下标从 0 开始的字符串数组 words 和一个字符 x 。 请你返回一个 下标数组 ，表示下标在数组中对应的单词包含字符 x 。 注意 ，返回的数组可以是 任意 顺序。 示例 1： 输入：words = ["leet","code"], x = "e" 输出：[0,1] 解释："e" 在两个单词中都出现了："leet" 和 "code" 。所以我们返回下标 0 和 1 。 示例 2： 输入：words = ["abc","bcd","aaaa","cbc"], x = "a" 输出：[0,2] 解释："a" 在 "abc" 和 "aaaa" 中出现了，所以我们返回下标 0 和 2 。 示例 3： 输入：words = ["abc","bcd","aaaa","cbc"], x = "z" 输出：[] 解释："z" 没有在任何单词中出现。所以我们返回空数组。 提示： * 1 <= words.length <= 50 * 1 <= words[i].length <= 50 * x 是一个小写英文字母。 * words[i] 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 遍历每个单词，检查是否包含指定字符 x，如果包含则将该单词的下标加入结果列表。

算法步骤:
1. 初始化一个空的结果列表 res。
2. 遍历 words 数组，对于每个单词，检查是否包含字符 x。
3. 如果包含，则将当前单词的下标加入 res。
4. 返回结果列表 res。

关键点:
- 使用 in 运算符来检查字符 x 是否在单词中。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m)，其中 n 是 words 的长度，m 是单词的平均长度。
空间复杂度: O(1)，除了结果列表外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_words_containing(words: List[str], x: str) -> List[int]:
    """
    函数式接口 - 查找包含指定字符的单词下标
    """
    res = []
    for i, word in enumerate(words):
        if x in word:
            res.append(i)
    return res


Solution = create_solution(find_words_containing)