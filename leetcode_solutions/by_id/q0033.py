"""
33. 搜索旋转排序数组 - 整数数组 nums 按升序排列，数组中的值 互不相同 。 在传递给函数之前，nums 在预先未知的某个下标 k（0 <= k < nums.length）上进行了 向左旋转，使数组变为 [nums[k], nums[k+1], ..., nums[n-1], nums[0], nums[1], ..., nums[k-1]]（下标 从 0 开始 计数）。例如， [0,1,2,4,5,6,7] 下标 3 上向左旋转后可能变为 [4,5,6,7,0,1,2] 。 给你 旋转后 的数组 nums 和一个整数 target ，如果 nums 中存在这个目标值 target ，则返回它的下标，否则返回 -1 。 你必须设计一个时间复杂度为 O(log n) 的算法解决此问题。 示例 1： 输入：nums = [4,5,6,7,0,1,2], target = 0 输出：4 示例 2： 输入：nums = [4,5,6,7,0,1,2], target = 3 输出：-1 示例 3： 输入：nums = [1], target = 0 输出：-1 提示： * 1 <= nums.length <= 5000 * -104 <= nums[i] <= 104 * nums 中的每个值都 独一无二 * 题目数据保证 nums 在预先未知的某个下标上进行了旋转 * -104 <= target <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 二分查找，判断target在左半部分还是右半部分

算法步骤:
1. 使用二分查找，left = 0, right = len(nums) - 1
2. 计算mid = (left + right) // 2
3. 如果nums[mid] == target，返回mid
4. 判断左半部分是否有序（nums[left] <= nums[mid]）：
   - 如果有序且target在[left, mid]范围内，在左半部分查找
   - 否则在右半部分查找
5. 如果左半部分无序，则右半部分有序：
   - 如果target在[mid, right]范围内，在右半部分查找
   - 否则在左半部分查找
6. 重复直到找到或left > right

关键点:
- 旋转数组至少有一半是有序的
- 通过判断target是否在有序部分来决定搜索方向
- 时间复杂度O(log n)，空间复杂度O(1)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 二分查找，n为数组长度
空间复杂度: O(1) - 只使用常数额外空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def search(nums: List[int], target: int) -> int:
    """
    函数式接口 - 二分查找实现
    
    实现思路:
    使用二分查找，判断target在左半部分还是右半部分，通过判断有序部分来决定搜索方向。
    
    Args:
        nums: 旋转排序数组
        target: 目标值
        
    Returns:
        目标值的下标，如果不存在则返回-1
        
    Example:
        >>> search([4,5,6,7,0,1,2], 0)
        4
        >>> search([4,5,6,7,0,1,2], 3)
        -1
    """
    left, right = 0, len(nums) - 1
    
    while left <= right:
        mid = (left + right) // 2
        
        if nums[mid] == target:
            return mid
        
        # 判断左半部分是否有序
        if nums[left] <= nums[mid]:
            # 左半部分有序
            if nums[left] <= target < nums[mid]:
                # target在左半部分
                right = mid - 1
            else:
                # target在右半部分
                left = mid + 1
        else:
            # 右半部分有序
            if nums[mid] < target <= nums[right]:
                # target在右半部分
                left = mid + 1
            else:
                # target在左半部分
                right = mid - 1
    
    return -1


