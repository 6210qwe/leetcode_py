"""
867. 转置矩阵 - 给你一个二维整数数组 matrix， 返回 matrix 的 转置矩阵 。 矩阵的 转置 是指将矩阵的主对角线翻转，交换矩阵的行索引与列索引。 [https://assets.leetcode.com/uploads/2021/02/10/hint_transpose.png] 示例 1： 输入：matrix = [[1,2,3],[4,5,6],[7,8,9]] 输出：[[1,4,7],[2,5,8],[3,6,9]] 示例 2： 输入：matrix = [[1,2,3],[4,5,6]] 输出：[[1,4],[2,5],[3,6]] 提示： * m == matrix.length * n == matrix[i].length * 1 <= m, n <= 1000 * 1 <= m * n <= 105 * -109 <= matrix[i][j] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用列表推导式来生成转置矩阵。

算法步骤:
1. 获取原矩阵的行数和列数。
2. 使用列表推导式生成转置矩阵，通过交换行索引和列索引。

关键点:
- 列表推导式可以简洁地实现矩阵转置。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是矩阵的行数和列数。
空间复杂度: O(m * n)，需要额外的空间来存储转置后的矩阵。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def transpose_matrix(matrix: List[List[int]]) -> List[List[int]]:
    """
    函数式接口 - 返回矩阵的转置矩阵
    """
    # 获取原矩阵的行数和列数
    m, n = len(matrix), len(matrix[0])
    
    # 使用列表推导式生成转置矩阵
    transposed_matrix = [[matrix[j][i] for j in range(m)] for i in range(n)]
    
    return transposed_matrix


