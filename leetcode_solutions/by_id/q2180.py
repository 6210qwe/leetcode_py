"""
2071. 你可以安排的最多任务数目 - 给你 n 个任务和 m 个工人。每个任务需要一定的力量值才能完成，需要的力量值保存在下标从 0 开始的整数数组 tasks 中，第 i 个任务需要 tasks[i] 的力量才能完成。每个工人的力量值保存在下标从 0 开始的整数数组 workers 中，第 j 个工人的力量值为 workers[j] 。每个工人只能完成 一个 任务，且力量值需要 大于等于 该任务的力量要求值（即 workers[j] >= tasks[i] ）。 除此以外，你还有 pills 个神奇药丸，可以给 一个工人的力量值 增加 strength 。你可以决定给哪些工人使用药丸，但每个工人 最多 只能使用 一片 药丸。 给你下标从 0 开始的整数数组tasks 和 workers 以及两个整数 pills 和 strength ，请你返回 最多 有多少个任务可以被完成。 示例 1： 输入：tasks = [3,2,1], workers = [0,3,3], pills = 1, strength = 1 输出：3 解释： 我们可以按照如下方案安排药丸： - 给 0 号工人药丸。 - 0 号工人完成任务 2（0 + 1 >= 1） - 1 号工人完成任务 1（3 >= 2） - 2 号工人完成任务 0（3 >= 3） 示例 2： 输入：tasks = [5,4], workers = [0,0,0], pills = 1, strength = 5 输出：1 解释： 我们可以按照如下方案安排药丸： - 给 0 号工人药丸。 - 0 号工人完成任务 0（0 + 5 >= 5） 示例 3： 输入：tasks = [10,15,30], workers = [0,10,10,10,10], pills = 3, strength = 10 输出：2 解释： 我们可以按照如下方案安排药丸： - 给 0 号和 1 号工人药丸。 - 0 号工人完成任务 0（0 + 10 >= 10） - 1 号工人完成任务 1（10 + 10 >= 15） 示例 4： 输入：tasks = [5,9,8,5,9], workers = [1,6,4,2,6], pills = 1, strength = 5 输出：3 解释： 我们可以按照如下方案安排药丸： - 给 2 号工人药丸。 - 1 号工人完成任务 0（6 >= 5） - 2 号工人完成任务 2（4 + 5 >= 8） - 4 号工人完成任务 3（6 >= 5） 提示： * n == tasks.length * m == workers.length * 1 <= n, m <= 5 * 104 * 0 <= pills <= m * 0 <= tasks[i], workers[j], strength <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法和优先队列来最大化任务分配。

算法步骤:
1. 对任务和工人进行排序。
2. 使用一个优先队列来存储当前可以使用的工人。
3. 遍历任务，对于每个任务：
   - 尝试使用当前优先队列中的工人完成任务。
   - 如果无法完成，则尝试使用药丸增加工人的力量来完成任务。
   - 如果仍然无法完成，则将任务加入待处理的任务列表。
4. 返回可以完成的最大任务数。

关键点:
- 优先队列用于高效地选择合适的工人。
- 贪心策略确保每次选择最优的工人或药丸使用方式。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n + m log m)，其中 n 是任务的数量，m 是工人的数量。排序操作的时间复杂度是 O(n log n) 和 O(m log m)，优先队列操作的时间复杂度是 O(log m)。
空间复杂度: O(m)，优先队列的空间复杂度是 O(m)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def max_tasks(tasks: List[int], workers: List[int], pills: int, strength: int) -> int:
    # 对任务和工人进行排序
    tasks.sort()
    workers.sort()
    
    # 优先队列存储当前可以使用的工人
    worker_queue = []
    task_index = 0
    pill_count = pills
    completed_tasks = 0
    
    for worker in workers:
        heapq.heappush(worker_queue, -worker)
        
        while worker_queue and task_index < len(tasks):
            # 尝试使用当前优先队列中的工人完成任务
            if -worker_queue[0] >= tasks[task_index]:
                heapq.heappop(worker_queue)
                completed_tasks += 1
                task_index += 1
            # 如果无法完成，则尝试使用药丸增加工人的力量来完成任务
            elif pill_count > 0 and -worker_queue[0] + strength >= tasks[task_index]:
                heapq.heappop(worker_queue)
                completed_tasks += 1
                pill_count -= 1
                task_index += 1
            else:
                break
    
    return completed_tasks

