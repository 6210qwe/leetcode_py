"""
2457. 美丽整数的最小增量 - 给你两个正整数 n 和 target 。 如果某个整数每一位上的数字相加小于或等于 target ，则认为这个整数是一个 美丽整数 。 找出并返回满足 n + x 是 美丽整数 的最小非负整数 x 。生成的输入保证总可以使 n 变成一个美丽整数。 示例 1： 输入：n = 16, target = 6 输出：4 解释：最初，n 是 16 ，且其每一位数字的和是 1 + 6 = 7 。在加 4 之后，n 变为 20 且每一位数字的和变成 2 + 0 = 2 。可以证明无法加上一个小于 4 的非负整数使 n 变成一个美丽整数。 示例 2： 输入：n = 467, target = 6 输出：33 解释：最初，n 是 467 ，且其每一位数字的和是 4 + 6 + 7 = 17 。在加 33 之后，n 变为 500 且每一位数字的和变成 5 + 0 + 0 = 5 。可以证明无法加上一个小于 33 的非负整数使 n 变成一个美丽整数。 示例 3： 输入：n = 1, target = 1 输出：0 解释：最初，n 是 1 ，且其每一位数字的和是 1 ，已经小于等于 target 。 提示： * 1 <= n <= 1012 * 1 <= target <= 150 * 生成的输入保证总可以使 n 变成一个美丽整数。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过贪心算法逐步增加 n 的值，直到其各位数字之和小于等于 target。

算法步骤:
1. 定义一个辅助函数 `digit_sum` 来计算一个整数的各位数字之和。
2. 初始化变量 `x` 为 0，表示需要增加的最小值。
3. 使用一个循环不断检查当前 n 的各位数字之和是否小于等于 target：
   - 如果是，则返回 x。
   - 否则，增加 x 的值，使得 n 的最低位变为 0，并将 n 更新为新的值。
4. 重复上述过程直到找到满足条件的 x。

关键点:
- 通过逐步增加 x 的值，确保 n 的各位数字之和最终小于等于 target。
- 每次增加 x 的值时，确保 n 的最低位变为 0，从而减少后续的计算量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n)，因为每次增加 x 的值时，n 的位数会减少。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def digit_sum(n: int) -> int:
    """计算一个整数的各位数字之和"""
    return sum(int(digit) for digit in str(n))


def solution_function_name(n: int, target: int) -> int:
    """
    函数式接口 - 找出并返回满足 n + x 是美丽整数的最小非负整数 x。
    """
    x = 0
    while digit_sum(n) > target:
        # 计算 n 的最低位变为 0 需要增加的值
        increment = 10 - (n % 10)
        x += increment
        n += increment
        # 去掉最低位
        n //= 10
    return x


Solution = create_solution(solution_function_name)