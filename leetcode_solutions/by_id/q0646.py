"""
646. 最长数对链 - 给你一个由 n 个数对组成的数对数组 pairs ，其中 pairs[i] = [lefti, righti] 且 lefti < righti 。 现在，我们定义一种 跟随 关系，当且仅当 b < c 时，数对 p2 = [c, d] 才可以跟在 p1 = [a, b] 后面。我们用这种形式来构造 数对链 。 找出并返回能够形成的 最长数对链的长度 。 你不需要用到所有的数对，你可以以任何顺序选择其中的一些数对来构造。 示例 1： 输入：pairs = [[1,2], [2,3], [3,4]] 输出：2 解释：最长的数对链是 [1,2] -> [3,4] 。 示例 2： 输入：pairs = [[1,2],[7,8],[4,5]] 输出：3 解释：最长的数对链是 [1,2] -> [4,5] -> [7,8] 。 提示： * n == pairs.length * 1 <= n <= 1000 * -1000 <= lefti < righti <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，首先按每个数对的第二个元素进行排序，然后遍历排序后的数组，尽可能选择更多的数对。

算法步骤:
1. 按每个数对的第二个元素进行排序。
2. 初始化当前链的最后一个数对的结束位置为负无穷大。
3. 遍历排序后的数组，如果当前数对的第一个元素大于当前链的最后一个数对的结束位置，则将该数对加入链中，并更新当前链的最后一个数对的结束位置。

关键点:
- 按第二个元素排序，确保每次选择的数对的结束位置最小，从而尽可能多地选择后续数对。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 pairs 的长度，主要由排序操作决定。
空间复杂度: O(1)，除了输入和输出外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def findLongestChain(pairs: List[List[int]]) -> int:
    """
    函数式接口 - 实现最长数对链
    """
    # 按第二个元素排序
    pairs.sort(key=lambda x: x[1])
    
    # 初始化当前链的最后一个数对的结束位置
    current_end = float('-inf')
    count = 0
    
    # 遍历排序后的数组
    for pair in pairs:
        if pair[0] > current_end:
            count += 1
            current_end = pair[1]
    
    return count

