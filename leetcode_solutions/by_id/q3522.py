"""
3254. 长度为 K 的子数组的能量值 I - 给你一个长度为 n 的整数数组 nums 和一个正整数 k 。 一个数组的 能量值 定义为： * 如果 所有 元素都是依次 连续 且 上升 的，那么能量值为 最大 的元素。 * 否则为 -1 。 你需要求出 nums 中所有长度为 k 的 子数组 的能量值。 请你返回一个长度为 n - k + 1 的整数数组 results ，其中 results[i] 是子数组 nums[i..(i + k - 1)] 的能量值。 示例 1： 输入：nums = [1,2,3,4,3,2,5], k = 3 输出：[3,4,-1,-1,-1] 解释： nums 中总共有 5 个长度为 3 的子数组： * [1, 2, 3] 中最大元素为 3 。 * [2, 3, 4] 中最大元素为 4 。 * [3, 4, 3] 中元素 不是 连续的。 * [4, 3, 2] 中元素 不是 上升的。 * [3, 2, 5] 中元素 不是 连续的。 示例 2： 输入：nums = [2,2,2,2,2], k = 4 输出：[-1,-1] 示例 3： 输入：nums = [3,2,3,2,3,2], k = 2 输出：[-1,3,-1,3,-1] 提示： * 1 <= n == nums.length <= 500 * 1 <= nums[i] <= 105 * 1 <= k <= n
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口检查每个长度为 k 的子数组是否满足条件。

算法步骤:
1. 初始化结果数组 `results`。
2. 使用滑动窗口遍历数组 `nums`，检查每个长度为 k 的子数组：
   - 检查子数组是否连续且上升。
   - 如果满足条件，记录最大值；否则记录 -1。
3. 返回结果数组 `results`。

关键点:
- 使用滑动窗口可以高效地检查每个子数组。
- 检查子数组是否连续且上升时，只需比较相邻元素。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)（不考虑输出数组）
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_power_of_k_size_subarrays(nums: List[int], k: int) -> List[int]:
    """
    函数式接口 - 计算长度为 k 的子数组的能量值
    """
    n = len(nums)
    results = [-1] * (n - k + 1)

    for i in range(n - k + 1):
        if all(nums[i + j] == nums[i] + j for j in range(k)):
            results[i] = nums[i + k - 1]

    return results


Solution = create_solution(find_power_of_k_size_subarrays)