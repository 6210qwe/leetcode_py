"""
核心思想: 使用笛卡尔积生成所有可能的比赛组合

算法步骤:
1. 从 teams 表中选择所有队伍
2. 使用笛卡尔积生成所有可能的比赛组合
3. 过滤掉相同队伍的比赛

关键点:
- 使用 SQL 的 CROSS JOIN 生成笛卡尔积
- 使用 WHERE 子句过滤掉相同队伍的比赛
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是队伍的数量。因为我们需要生成所有可能的比赛组合。
空间复杂度: O(1)，除了存储结果外，不需要额外的空间。
"""

