def solution_function_name():
    """
    函数式接口 - 生成联赛的所有比赛
    """
    # 实现最优解法
    query = """
    SELECT 
        t1.team_name AS home_team, 
        t2.team_name AS away_team
    FROM 
        teams t1
    CROSS JOIN 
        teams t2
    WHERE 
        t1.team_name < t2.team_name
    """
    return query


Solution = create_solution(solution_function_name)