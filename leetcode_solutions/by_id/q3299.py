# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个数字出现的次数，并通过检查每个数字是否可以形成所需的模式来找到最大子集。

算法步骤:
1. 统计每个数字出现的次数。
2. 对于每个数字，检查其是否可以形成所需的模式。
3. 计算并更新最大子集的大小。

关键点:
- 使用哈希表高效统计每个数字的出现次数。
- 通过检查每个数字及其平方数来确定是否可以形成所需的模式。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度。最坏情况下，我们需要对每个数字进行对数级别的检查。
空间复杂度: O(n)，用于存储每个数字的出现次数。
"""

# ============================================================================
