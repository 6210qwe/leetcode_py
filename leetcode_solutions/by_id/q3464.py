# 问题描述
# ============================================================================
"""
3196. 最大化子数组的总成本 - 给你一个长度为 n 的整数数组 nums。 子数组 nums[l..r]（其中 0 <= l <= r < n）的 成本 定义为： cost(l, r) = nums[l] - nums[l + 1] + ... + nums[r] * (−1)r − l 你的任务是将 nums 分割成若干子数组，使得所有子数组的成本之和 最大化，并确保每个元素 正好 属于一个子数组。 具体来说，如果 nums 被分割成 k 个子数组，且分割点为索引 i1, i2, ..., ik − 1（其中 0 <= i1 < i2 < ... < ik - 1 < n - 1），则总成本为： cost(0, i1) + cost(i1 + 1, i2) + ... + cost(ik − 1 + 1, n − 1) 返回在最优分割方式下的子数组成本之和的最大值。 注意：如果 nums 没有被分割，即 k = 1，则总成本即为 cost(0, n - 1)。 示例 1： 输入： nums = [1,-2,3,4] 输出： 10 解释： 一种总成本最大化的方法是将 [1, -2, 3, 4] 分割成子数组 [1, -2, 3] 和 [4]。总成本为 (1 + 2 + 3) + 4 = 10。 示例 2： 输入： nums = [1,-1,1,-1] 输出： 4 解释： 一种总成本最大化的方法是将 [1, -1, 1, -1] 分割成子数组 [1, -1] 和 [1, -1]。总成本为 (1 + 1) + (1 + 1) = 4。 示例 3： 输入： nums = [0] 输出： 0 解释： 无法进一步分割数组，因此答案为 0。 示例 4： 输入： nums = [1,-1] 输出： 2 解释： 选择整个数组，总成本为 1 + 1 = 2，这是可能的最大成本。 提示： * 1 <= nums.length <= 105 * -109 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划

算法步骤:
1. 初始化两个数组 `dp` 和 `cost`，其中 `dp[i]` 表示以 `nums[i]` 结尾的子数组的最大成本。
2. 计算 `cost` 数组，其中 `cost[i]` 表示从 `nums[0]` 到 `nums[i]` 的交替和。
3. 使用动态规划更新 `dp` 数组，对于每个 `i`，找到 `j` 使得 `dp[j] + cost[i] - cost[j]` 最大。
4. 返回 `dp` 数组中的最大值。

关键点:
- 使用前缀和来计算 `cost` 数组。
- 动态规划的状态转移方程为 `dp[i] = max(dp[j] + cost[i] - cost[j])`。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def maximize_total_cost(nums: List[int]) -> int:
    """
    函数式接口 - 最大化子数组的总成本
    """
    n = len(nums)
    if n == 1:
        return 0

    # 计算 cost 数组
    cost = [0] * n
    for i in range(n):
        cost[i] = nums[i] * (-1) ** i
        if i > 0:
            cost[i] += cost[i - 1]

    # 初始化 dp 数组
    dp = [0] * n
    dp[0] = nums[0]

    # 动态规划更新 dp 数组
    for i in range(1, n):
        dp[i] = nums[i]
        for j in range(i):
            dp[i] = max(dp[i], dp[j] + cost[i] - (cost[j] if j > 0 else 0))

    return max(dp)


Solution = create_solution(maximize_total_cost)