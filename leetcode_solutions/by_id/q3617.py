"""
3330. 找到初始输入字符串 I - Alice 正在她的电脑上输入一个字符串。但是她打字技术比较笨拙，她 可能 在一个按键上按太久，导致一个字符被输入 多次 。 尽管 Alice 尽可能集中注意力，她仍然可能会犯错 至多 一次。 给你一个字符串 word ，它表示 最终 显示在 Alice 显示屏上的结果。 请你返回 Alice 一开始可能想要输入字符串的总方案数。 示例 1： 输入：word = "abbcccc" 输出：5 解释： 可能的字符串包括："abbcccc" ，"abbccc" ，"abbcc" ，"abbc" 和 "abcccc" 。 示例 2： 输入：word = "abcd" 输出：1 解释： 唯一可能的字符串是 "abcd" 。 示例 3： 输入：word = "aaaa" 输出：4 提示： * 1 <= word.length <= 100 * word 只包含小写英文字母。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划

算法步骤:
1. 初始化一个长度为 n+1 的数组 dp，其中 dp[i] 表示以第 i 个字符结尾的子字符串的方案数。
2. 遍历字符串 word，对于每个字符 word[i]：
   - 如果 word[i] 与 word[i-1] 相同，则 dp[i+1] = dp[i] + dp[i-1]。
   - 否则，dp[i+1] = dp[i]。
3. 返回 dp[n]。

关键点:
- 使用动态规划来记录每一步的方案数。
- 通过比较当前字符和前一个字符来决定是否增加方案数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 word 的长度。
空间复杂度: O(n)，用于存储动态规划数组 dp。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(word: str) -> int:
    """
    函数式接口 - 计算 Alice 一开始可能想要输入字符串的总方案数
    """
    n = len(word)
    if n == 1:
        return 1

    dp = [0] * (n + 1)
    dp[0] = 1
    dp[1] = 1

    for i in range(2, n + 1):
        dp[i] = dp[i - 1]
        if word[i - 1] == word[i - 2]:
            dp[i] += dp[i - 2]

    return dp[n]


Solution = create_solution(solution_function_name)