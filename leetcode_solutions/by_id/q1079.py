"""
1022. 从根到叶的二进制数之和 - 给出一棵二叉树，其上每个结点的值都是 0 或 1 。每一条从根到叶的路径都代表一个从最高有效位开始的二进制数。 * 例如，如果路径为 0 -> 1 -> 1 -> 0 -> 1，那么它表示二进制数 01101，也就是 13 。 对树上的每一片叶子，我们都要找出从根到该叶子的路径所表示的数字。 返回这些数字之和。题目数据保证答案是一个 32 位 整数。 示例 1： [https://assets.leetcode.com/uploads/2019/04/04/sum-of-root-to-leaf-binary-numbers.png] 输入：root = [1,0,1,0,1,0,1] 输出：22 解释：(100) + (101) + (110) + (111) = 4 + 5 + 6 + 7 = 22 示例 2： 输入：root = [0] 输出：0 提示： * 树中的节点数在 [1, 1000] 范围内 * Node.val 仅为 0 或 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用深度优先搜索（DFS）遍历二叉树，同时维护当前路径的二进制数。

算法步骤:
1. 定义一个递归函数 `dfs`，参数包括当前节点和当前路径的二进制数。
2. 如果当前节点为空，返回 0。
3. 更新当前路径的二进制数，左移一位并加上当前节点的值。
4. 如果当前节点是叶子节点，返回当前路径的二进制数。
5. 递归调用 `dfs` 函数处理左子树和右子树，并返回它们的和。

关键点:
- 使用位运算来更新当前路径的二进制数。
- 递归终止条件是遇到叶子节点。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是树中节点的数量。每个节点只访问一次。
空间复杂度: O(h)，其中 h 是树的高度。递归调用栈的深度最多为树的高度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import Optional
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution

def sum_root_to_leaf(root: Optional[TreeNode]) -> int:
    """
    计算从根到叶的二进制数之和
    """
    def dfs(node: Optional[TreeNode], current_value: int) -> int:
        if not node:
            return 0
        current_value = (current_value << 1) | node.val
        if not node.left and not node.right:
            return current_value
        return dfs(node.left, current_value) + dfs(node.right, current_value)
    
    return dfs(root, 0)

Solution = create_solution(sum_root_to_leaf)