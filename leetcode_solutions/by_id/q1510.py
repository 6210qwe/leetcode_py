"""
1394. 找出数组中的幸运数 - 在整数数组中，如果一个整数的出现频次和它的数值大小相等，我们就称这个整数为「幸运数」。 给你一个整数数组 arr，请你从中找出并返回一个幸运数。 * 如果数组中存在多个幸运数，只需返回 最大 的那个。 * 如果数组中不含幸运数，则返回 -1 。 示例 1： 输入：arr = [2,2,3,4] 输出：2 解释：数组中唯一的幸运数是 2 ，因为数值 2 的出现频次也是 2 。 示例 2： 输入：arr = [1,2,2,3,3,3] 输出：3 解释：1、2 以及 3 都是幸运数，只需要返回其中最大的 3 。 示例 3： 输入：arr = [2,2,2,3,3] 输出：-1 解释：数组中不存在幸运数。 示例 4： 输入：arr = [5] 输出：-1 示例 5： 输入：arr = [7,7,7,7,7,7,7] 输出：7 提示： * 1 <= arr.length <= 500 * 1 <= arr[i] <= 500
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表记录每个数字的出现频次，然后遍历哈希表找到满足条件的最大幸运数。

算法步骤:
1. 使用哈希表记录每个数字的出现频次。
2. 遍历哈希表，找到频次等于数值的数字，并记录最大值。
3. 返回最大幸运数，如果没有找到则返回 -1。

关键点:
- 使用哈希表统计频次，时间复杂度为 O(n)。
- 一次遍历哈希表查找最大幸运数，时间复杂度为 O(n)。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def find_lucky(arr: List[int]) -> int:
    """
    函数式接口 - 找出数组中的幸运数
    """
    # 使用哈希表记录每个数字的出现频次
    freq = {}
    for num in arr:
        if num in freq:
            freq[num] += 1
        else:
            freq[num] = 1
    
    # 遍历哈希表，找到频次等于数值的数字，并记录最大值
    max_lucky = -1
    for num, count in freq.items():
        if num == count and num > max_lucky:
            max_lucky = num
    
    return max_lucky


Solution = create_solution(find_lucky)