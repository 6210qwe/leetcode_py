"""
1652. 拆炸弹 - 你有一个炸弹需要拆除，时间紧迫！你的情报员会给你一个长度为 n 的 循环 数组 code 以及一个密钥 k 。 为了获得正确的密码，你需要替换掉每一个数字。所有数字会 同时 被替换。 * 如果 k > 0 ，将第 i 个数字用 接下来 k 个数字之和替换。 * 如果 k < 0 ，将第 i 个数字用 之前 k 个数字之和替换。 * 如果 k == 0 ，将第 i 个数字用 0 替换。 由于 code 是循环的， code[n-1] 下一个元素是 code[0] ，且 code[0] 前一个元素是 code[n-1] 。 给你 循环 数组 code 和整数密钥 k ，请你返回解密后的结果来拆除炸弹！ 示例 1： 输入：code = [5,7,1,4], k = 3 输出：[12,10,16,13] 解释：每个数字都被接下来 3 个数字之和替换。解密后的密码为 [7+1+4, 1+4+5, 4+5+7, 5+7+1]。注意到数组是循环连接的。 示例 2： 输入：code = [1,2,3,4], k = 0 输出：[0,0,0,0] 解释：当 k 为 0 时，所有数字都被 0 替换。 示例 3： 输入：code = [2,4,9,3], k = -2 输出：[12,5,6,13] 解释：解密后的密码为 [3+9, 2+3, 4+2, 9+4] 。注意到数组是循环连接的。如果 k 是负数，那么和为 之前 的数字。 提示： * n == code.length * 1 <= n <= 100 * 1 <= code[i] <= 100 * -(n - 1) <= k <= n - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口来计算每个位置的和。

算法步骤:
1. 初始化一个结果数组 res。
2. 根据 k 的正负情况，分别处理前 k 个或后 k 个元素的和。
3. 使用滑动窗口更新每个位置的和。

关键点:
- 使用取模运算来处理循环数组。
- 通过滑动窗口减少重复计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def defuse_the_bomb(code: List[int], k: int) -> List[int]:
    """
    函数式接口 - 拆炸弹
    """
    n = len(code)
    if k == 0:
        return [0] * n
    
    res = [0] * n
    window_sum = 0
    
    if k > 0:
        # 计算第一个窗口的和
        for i in range(k):
            window_sum += code[i]
        
        for i in range(n):
            res[i] = window_sum
            window_sum -= code[(i + k) % n]
            window_sum += code[i]
    else:
        # 计算第一个窗口的和
        for i in range(-1, k - 1, -1):
            window_sum += code[i]
        
        for i in range(n):
            res[i] = window_sum
            window_sum -= code[i]
            window_sum += code[(i + k - 1) % n]
    
    return res


Solution = create_solution(defuse_the_bomb)