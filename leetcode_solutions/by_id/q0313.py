"""
313. 超级丑数 - 超级丑数 是一个正整数，并满足其所有质因数都出现在质数数组 primes 中。 给你一个整数 n 和一个整数数组 primes ，返回第 n 个 超级丑数 。 题目数据保证第 n 个 超级丑数 在 32-bit 带符号整数范围内。 示例 1： 输入：n = 12, primes = [2,7,13,19] 输出：32 解释：给定长度为 4 的质数数组 primes = [2,7,13,19]，前 12 个超级丑数序列为：[1,2,4,7,8,13,14,16,19,26,28,32] 。 示例 2： 输入：n = 1, primes = [2,3,5] 输出：1 解释：1 不含质因数，因此它的所有质因数都在质数数组 primes = [2,3,5] 中。 提示： * 1 <= n <= 105 * 1 <= primes.length <= 100 * 2 <= primes[i] <= 1000 * 题目数据 保证 primes[i] 是一个质数 * primes 中的所有值都 互不相同 ，且按 递增顺序 排列
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划，类似丑数II，使用指针数组记录每个质数对应的下一个丑数位置

算法步骤:
1. 使用dp数组存储超级丑数
2. 使用指针数组index，index[i]表示primes[i]对应的下一个丑数位置
3. 每次选择最小的primes[i] * dp[index[i]]作为下一个超级丑数
4. 更新对应的指针

关键点:
- 使用多指针动态规划
- 时间复杂度O(n*k)，空间复杂度O(n+k)
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n*k) - n为要查找的位置，k为质数数组长度
空间复杂度: O(n+k) - dp数组和指针数组空间
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def super_ugly_number(n: int, primes: List[int]) -> int:
    """
    函数式接口 - 超级丑数
    
    实现思路:
    动态规划，类似丑数II，使用指针数组记录每个质数对应的下一个丑数位置。
    
    Args:
        n: 要查找的位置
        primes: 质数数组
        
    Returns:
        第n个超级丑数
        
    Example:
        >>> super_ugly_number(12, [2, 7, 13, 19])
        32
    """
    dp = [1] * n
    k = len(primes)
    index = [0] * k
    
    for i in range(1, n):
        # 找到下一个最小的超级丑数
        next_ugly = min(primes[j] * dp[index[j]] for j in range(k))
        dp[i] = next_ugly
        
        # 更新所有产生这个最小值的指针
        for j in range(k):
            if primes[j] * dp[index[j]] == next_ugly:
                index[j] += 1
    
    return dp[n - 1]


