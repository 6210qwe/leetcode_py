"""
2591. 将钱分给最多的儿童 - 给你一个整数 money ，表示你总共有的钱数（单位为美元）和另一个整数 children ，表示你要将钱分配给多少个儿童。 你需要按照如下规则分配： * 所有的钱都必须被分配。 * 每个儿童至少获得 1 美元。 * 没有人获得 4 美元。 请你按照上述规则分配金钱，并返回 最多 有多少个儿童获得 恰好 8 美元。如果没有任何分配方案，返回 -1 。 示例 1： 输入：money = 20, children = 3 输出：1 解释： 最多获得 8 美元的儿童数为 1 。一种分配方案为： - 给第一个儿童分配 8 美元。 - 给第二个儿童分配 9 美元。 - 给第三个儿童分配 3 美元。 没有分配方案能让获得 8 美元的儿童数超过 1 。 示例 2： 输入：money = 16, children = 2 输出：2 解释：每个儿童都可以获得 8 美元。 提示： * 1 <= money <= 200 * 2 <= children <= 30
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，优先让尽可能多的儿童获得 8 美元，然后处理剩余的钱。

算法步骤:
1. 计算每个儿童至少获得 1 美元后剩余的钱。
2. 计算最多可以让多少个儿童获得 8 美元。
3. 处理剩余的钱，确保没有儿童获得 4 美元。

关键点:
- 优先分配 8 美元。
- 确保没有儿童获得 4 美元。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(money: int, children: int) -> int:
    """
    函数式接口 - 实现最优解法
    """
    # 每个儿童至少获得 1 美元
    if money < children:
        return -1
    
    # 剩余的钱
    remaining_money = money - children
    
    # 计算最多可以让多少个儿童获得 8 美元
    max_children_with_8 = min(children, remaining_money // 7)
    
    # 剩余的钱
    remaining_money -= max_children_with_8 * 7
    
    # 如果剩余的钱正好是 3 且只有一个儿童没有获得 8 美元，则需要调整
    if remaining_money == 3 and children - max_children_with_8 == 1:
        max_children_with_8 -= 1
    
    return max_children_with_8


Solution = create_solution(solution_function_name)