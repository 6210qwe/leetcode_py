"""
1862. 向下取整数对和 - 给你一个整数数组 nums ，请你返回所有下标对 0 <= i, j < nums.length 的 floor(nums[i] / nums[j]) 结果之和。由于答案可能会很大，请你返回答案对109 + 7 取余 的结果。 函数 floor() 返回输入数字的整数部分。 示例 1： 输入：nums = [2,5,9] 输出：10 解释： floor(2 / 5) = floor(2 / 9) = floor(5 / 9) = 0 floor(2 / 2) = floor(5 / 5) = floor(9 / 9) = 1 floor(5 / 2) = 2 floor(9 / 2) = 4 floor(9 / 5) = 1 我们计算每一个数对商向下取整的结果并求和得到 10 。 示例 2： 输入：nums = [7,7,7,7,7,7,7] 输出：49 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数排序和前缀和来优化计算。

算法步骤:
1. 计算每个数的出现次数。
2. 使用前缀和来快速计算区间内的数的数量。
3. 对于每个数，计算其作为除数时的贡献。

关键点:
- 使用前缀和来快速计算区间内的数的数量。
- 通过遍历每个数及其倍数来计算贡献。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是 nums 的长度。因为我们需要遍历每个数及其倍数。
空间复杂度: O(n)，用于存储计数和前缀和。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution

def sum_of_floored_pairs(nums: List[int]) -> int:
    MOD = 10**9 + 7
    max_num = max(nums)
    count = [0] * (max_num + 1)
    
    # 计算每个数的出现次数
    for num in nums:
        count[num] += 1
    
    # 计算前缀和
    prefix_sum = [0] * (max_num + 2)
    for i in range(1, max_num + 1):
        prefix_sum[i] = prefix_sum[i - 1] + count[i]
    
    result = 0
    for i in range(1, max_num + 1):
        if count[i] == 0:
            continue
        for j in range(i, max_num + 1, i):
            result += count[i] * (prefix_sum[min(j + i - 1, max_num + 1)] - prefix_sum[j - 1])
            result %= MOD
    
    return result

Solution = create_solution(sum_of_floored_pairs)