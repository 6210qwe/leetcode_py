"""
2762. 不间断子数组 - 给你一个下标从 0 开始的整数数组 nums 。nums 的一个子数组如果满足以下条件，那么它是 不间断 的： * i，i + 1 ，...，j 表示子数组中的下标。对于所有满足 i <= i1, i2 <= j 的下标对，都有 0 <= |nums[i1] - nums[i2]| <= 2 。 请你返回 不间断 子数组的总数目。 子数组是一个数组中一段连续 非空 的元素序列。 示例 1： 输入：nums = [5,4,2,4] 输出：8 解释： 大小为 1 的不间断子数组：[5], [4], [2], [4] 。 大小为 2 的不间断子数组：[5,4], [4,2], [2,4] 。 大小为 3 的不间断子数组：[4,2,4] 。 没有大小为 4 的不间断子数组。 不间断子数组的总数目为 4 + 3 + 1 = 8 。 除了这些以外，没有别的不间断子数组。 示例 2： 输入：nums = [1,2,3] 输出：6 解释： 大小为 1 的不间断子数组：[1], [2], [3] 。 大小为 2 的不间断子数组：[1,2], [2,3] 。 大小为 3 的不间断子数组：[1,2,3] 。 不间断子数组的总数目为 3 + 2 + 1 = 6 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用滑动窗口和有序集合来维护当前窗口内的元素，并确保窗口内元素的最大值和最小值之差不超过2。

算法步骤:
1. 初始化两个指针 left 和 right，分别表示滑动窗口的左右边界。
2. 使用有序集合（如 Python 的 SortedList）来维护当前窗口内的元素。
3. 移动右指针 right，将新元素加入有序集合，并检查当前窗口内的最大值和最小值之差是否超过2。
4. 如果超过2，则移动左指针 left，直到窗口内的最大值和最小值之差不超过2。
5. 计算当前窗口内的子数组数量，并累加到结果中。
6. 重复上述步骤，直到右指针遍历完整个数组。

关键点:
- 使用有序集合来高效地获取当前窗口内的最大值和最小值。
- 滑动窗口的动态调整确保了时间复杂度的优化。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。每个元素最多被插入和删除一次有序集合，每次操作的时间复杂度为 O(log n)。
空间复杂度: O(n)，最坏情况下有序集合需要存储整个数组的元素。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from sortedcontainers import SortedList

def count_continuous_subarrays(nums: List[int]) -> int:
    """
    返回不间断子数组的总数目。
    """
    n = len(nums)
    left = 0
    result = 0
    window = SortedList()
    
    for right in range(n):
        window.add(nums[right])
        
        while window[-1] - window[0] > 2:
            window.remove(nums[left])
            left += 1
        
        result += right - left + 1
    
    return result

