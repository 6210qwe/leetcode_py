"""
830. 较大分组的位置 - 在一个由小写字母构成的字符串 s 中，包含由一些连续的相同字符所构成的分组。 例如，在字符串 s = "abbxxxxzyy" 中，就含有 "a", "bb", "xxxx", "z" 和 "yy" 这样的一些分组。 分组可以用区间 [start, end] 表示，其中 start 和 end 分别表示该分组的起始和终止位置的下标。上例中的 "xxxx" 分组用区间表示为 [3,6] 。 我们称所有包含大于或等于三个连续字符的分组为 较大分组 。 找到每一个 较大分组 的区间，按起始位置下标递增顺序排序后，返回结果。 示例 1： 输入：s = "abbxxxxzzy" 输出：[[3,6]] 解释："xxxx" 是一个起始于 3 且终止于 6 的较大分组。 示例 2： 输入：s = "abc" 输出：[] 解释："a","b" 和 "c" 均不是符合要求的较大分组。 示例 3： 输入：s = "abcdddeeeeaabbbcd" 输出：[[3,5],[6,9],[12,14]] 解释：较大分组为 "ddd", "eeee" 和 "bbb" 示例 4： 输入：s = "aba" 输出：[] 提示： * 1 <= s.length <= 1000 * s 仅含小写英文字母
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针遍历字符串，找到所有长度大于等于3的连续子串。

算法步骤:
1. 初始化两个指针 i 和 j，分别指向当前分组的起始和结束位置。
2. 遍历字符串，当遇到不同字符时，检查当前分组的长度是否大于等于3，如果是则记录该分组的起始和结束位置。
3. 更新指针 i 和 j，继续遍历直到字符串结束。

关键点:
- 使用双指针可以高效地找到所有符合条件的分组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要一次遍历字符串。
空间复杂度: O(1)，除了存储结果的列表外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def large_group_positions(s: str) -> List[List[int]]:
    """
    函数式接口 - 找到字符串中所有长度大于等于3的连续子串，并返回它们的起始和结束位置。
    """
    n = len(s)
    result = []
    i = 0
    
    while i < n:
        j = i + 1
        while j < n and s[j] == s[i]:
            j += 1
        
        if j - i >= 3:
            result.append([i, j - 1])
        
        i = j
    
    return result


Solution = create_solution(large_group_positions)