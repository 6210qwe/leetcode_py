"""
945. 使数组唯一的最小增量 - 给你一个整数数组 nums 。每次 move 操作将会选择任意一个满足 0 <= i < nums.length 的下标 i，并将 nums[i] 递增 1。 返回使 nums 中的每个值都变成唯一的所需要的最少操作次数。 生成的测试用例保证答案在 32 位整数范围内。 示例 1： 输入：nums = [1,2,2] 输出：1 解释：经过一次 move 操作，数组将变为 [1, 2, 3]。 示例 2： 输入：nums = [3,2,1,2,1,7] 输出：6 解释：经过 6 次 move 操作，数组将变为 [3, 4, 1, 2, 5, 7]。 可以看出 5 次或 5 次以下的 move 操作是不能让数组的每个值唯一的。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，先对数组进行排序，然后遍历数组，确保每个元素都比前一个元素大。

算法步骤:
1. 对数组进行排序。
2. 初始化一个变量 `moves` 来记录操作次数。
3. 遍历排序后的数组，对于每个元素，如果它不大于前一个元素，则将其增加到前一个元素加一，并更新 `moves`。
4. 返回 `moves`。

关键点:
- 通过排序和贪心策略，确保每个元素都比前一个元素大，从而使得数组中的每个值都唯一。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度，因为排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_increment_for_unique(nums: List[int]) -> int:
    """
    函数式接口 - 使数组唯一的最小增量
    """
    if not nums:
        return 0

    nums.sort()
    moves = 0
    for i in range(1, len(nums)):
        if nums[i] <= nums[i - 1]:
            diff = nums[i - 1] - nums[i] + 1
            nums[i] += diff
            moves += diff

    return moves


Solution = create_solution(min_increment_for_unique)