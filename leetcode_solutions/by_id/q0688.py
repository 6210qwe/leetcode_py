"""
688. 骑士在棋盘上的概率 - 在一个 n x n 的国际象棋棋盘上，一个骑士从单元格 (row, column) 开始，并尝试进行 k 次移动。行和列是 从 0 开始 的，所以左上单元格是 (0,0) ，右下单元格是 (n - 1, n - 1) 。 象棋骑士有8种可能的走法，如下图所示。每次移动在基本方向上是两个单元格，然后在正交方向上是一个单元格。 [https://assets.leetcode.cn/aliyun-lc-upload/uploads/2018/10/12/knight.png] 每次骑士要移动时，它都会随机从8种可能的移动中选择一种(即使棋子会离开棋盘)，然后移动到那里。 骑士继续移动，直到它走了 k 步或离开了棋盘。 返回 骑士在棋盘停止移动后仍留在棋盘上的概率 。 示例 1： 输入: n = 3, k = 2, row = 0, column = 0 输出: 0.0625 解释: 有两步(到(1,2)，(2,1))可以让骑士留在棋盘上。 在每一个位置上，也有两种移动可以让骑士留在棋盘上。 骑士留在棋盘上的总概率是0.0625。 示例 2： 输入: n = 1, k = 0, row = 0, column = 0 输出: 1.00000 提示: * 1 <= n <= 25 * 0 <= k <= 100 * 0 <= row, column <= n - 1
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用动态规划来计算骑士在每一步移动后留在棋盘上的概率。

算法步骤:
1. 初始化一个三维数组 dp，其中 dp[k][i][j] 表示骑士在第 k 步时位于 (i, j) 的概率。
2. 设置初始条件：dp[0][row][column] = 1.0，表示骑士在初始位置的概率为 1.0。
3. 对于每一步 k，遍历棋盘上的每个位置 (i, j)，更新 dp[k][i][j] 为其所有可能的前一步位置的概率之和除以 8。
4. 最终结果为 dp[K][i][j] 的总和。

关键点:
- 使用动态规划避免重复计算。
- 通过边界检查确保骑士不离开棋盘。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(K * N^2)
空间复杂度: O(K * N^2)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def knight_probability(n: int, k: int, row: int, column: int) -> float:
    """
    计算骑士在棋盘上移动 k 步后仍留在棋盘上的概率。
    """
    # 定义骑士的 8 种可能移动
    moves = [(2, 1), (2, -1), (-2, 1), (-2, -1),
             (1, 2), (1, -2), (-1, 2), (-1, -2)]
    
    # 初始化 dp 数组
    dp = [[[0.0 for _ in range(n)] for _ in range(n)] for _ in range(k + 1)]
    dp[0][row][column] = 1.0
    
    # 动态规划更新 dp 数组
    for step in range(1, k + 1):
        for i in range(n):
            for j in range(n):
                for move in moves:
                    ni, nj = i + move[0], j + move[1]
                    if 0 <= ni < n and 0 <= nj < n:
                        dp[step][ni][nj] += dp[step - 1][i][j] / 8.0
    
    # 计算最终结果
    return sum(sum(row) for row in dp[k])


Solution = create_solution(knight_probability)