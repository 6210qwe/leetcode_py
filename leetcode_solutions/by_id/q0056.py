"""
56. 合并区间 - 以数组 intervals 表示若干个区间的集合，其中单个区间为 intervals[i] = [starti, endi] 。请你合并所有重叠的区间，并返回 一个不重叠的区间数组，该数组需恰好覆盖输入中的所有区间 。 示例 1： 输入：intervals = [[1,3],[2,6],[8,10],[15,18]] 输出：[[1,6],[8,10],[15,18]] 解释：区间 [1,3] 和 [2,6] 重叠, 将它们合并为 [1,6]. 示例 2： 输入：intervals = [[1,4],[4,5]] 输出：[[1,5]] 解释：区间 [1,4] 和 [4,5] 可被视为重叠区间。 示例 3： 输入：intervals = [[4,7],[1,4]] 输出：[[1,7]] 解释：区间 [1,4] 和 [4,7] 可被视为重叠区间。 提示： * 1 <= intervals.length <= 104 * intervals[i].length == 2 * 0 <= starti <= endi <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 排序后合并，按区间起始端点排序，然后合并重叠区间

算法步骤:
1. 对区间按起始端点排序
2. 初始化结果列表，将第一个区间加入
3. 遍历剩余区间：
   - 如果当前区间与结果中最后一个区间重叠，合并它们
   - 否则，将当前区间加入结果
4. 返回合并后的区间列表

关键点:
- 排序后，重叠的区间必然相邻
- 判断重叠：当前区间的起始 <= 结果中最后一个区间的结束
- 合并：更新结果中最后一个区间的结束为两者的最大值
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n*log(n)) - 排序需要O(n*log(n))，遍历需要O(n)
空间复杂度: O(n) - 存储结果区间列表
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from leetcode_solutions.utils.solution import create_solution


def merge(intervals: List[List[int]]) -> List[List[int]]:
    """
    函数式接口 - 排序后合并
    
    实现思路:
    对区间按起始端点排序，然后遍历合并重叠的区间。
    
    Args:
        intervals: 区间列表，每个区间为[start, end]
        
    Returns:
        合并后的不重叠区间数组
        
    Example:
        >>> merge([[1,3],[2,6],[8,10],[15,18]])
        [[1, 6], [8, 10], [15, 18]]
    """
    if not intervals:
        return []
    
    # 按起始端点排序
    intervals.sort(key=lambda x: x[0])
    
    result = [intervals[0]]
    
    for interval in intervals[1:]:
        # 如果当前区间与结果中最后一个区间重叠，合并
        if interval[0] <= result[-1][1]:
            result[-1][1] = max(result[-1][1], interval[1])
        else:
            # 否则，添加新区间
            result.append(interval)
    
    return result


