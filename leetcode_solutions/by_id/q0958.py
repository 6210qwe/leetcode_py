"""
922. 按奇偶排序数组 II - 给定一个非负整数数组 nums， nums 中一半整数是 奇数 ，一半整数是 偶数 。 对数组进行排序，以便当 nums[i] 为奇数时，i 也是 奇数 ；当 nums[i] 为偶数时， i 也是 偶数 。 你可以返回 任何满足上述条件的数组作为答案 。 示例 1： 输入：nums = [4,2,5,7] 输出：[4,5,2,7] 解释：[4,7,2,5]，[2,5,4,7]，[2,7,4,5] 也会被接受。 示例 2： 输入：nums = [2,3] 输出：[2,3] 提示： * 2 <= nums.length <= 2 * 104 * nums.length 是偶数 * nums 中一半是偶数 * 0 <= nums[i] <= 1000 进阶：可以不使用额外空间解决问题吗？
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针方法，一个指针遍历偶数索引，另一个指针遍历奇数索引，确保每个位置上的元素符合要求。

算法步骤:
1. 初始化两个指针，分别指向偶数索引和奇数索引。
2. 遍历数组，如果当前偶数索引位置的元素是奇数，则找到下一个奇数索引位置的偶数元素，交换它们。
3. 如果当前奇数索引位置的元素是偶数，则找到下一个偶数索引位置的奇数元素，交换它们。
4. 重复上述过程直到遍历完整个数组。

关键点:
- 使用双指针在一次遍历中完成排序，避免了额外的空间开销。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需要遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def sort_array_by_parity_ii(nums: List[int]) -> List[int]:
    """
    函数式接口 - 按奇偶排序数组 II
    """
    i, j = 0, 1
    n = len(nums)
    
    while j < n and i < n:
        if nums[i] % 2 == 0:
            i += 2
        elif nums[j] % 2 == 1:
            j += 2
        else:
            nums[i], nums[j] = nums[j], nums[i]
            i += 2
            j += 2
    
    return nums

