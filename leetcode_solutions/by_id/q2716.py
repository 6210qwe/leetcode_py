"""
核心思想: 使用贪心算法和预生成的质数表来判断是否可以通过减去质数使数组严格递增。

算法步骤:
1. 生成一个包含所有小于等于 1000 的质数的列表。
2. 遍历数组，对于每个元素，使用二分查找找到一个合适的质数，使得当前元素减去该质数后仍大于前一个元素。
3. 如果找不到合适的质数，则返回 False。
4. 如果遍历完整个数组，返回 True。

关键点:
- 使用预生成的质数表来提高查找效率。
- 使用二分查找来快速找到合适的质数。
"""

