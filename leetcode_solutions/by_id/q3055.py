"""
2864. 最大二进制奇数 - 给你一个 二进制 字符串 s ，其中至少包含一个 '1' 。 你必须按某种方式 重新排列 字符串中的位，使得到的二进制数字是可以由该组合生成的 最大二进制奇数 。 以字符串形式，表示并返回可以由给定组合生成的最大二进制奇数。 注意 返回的结果字符串 可以 含前导零。 示例 1： 输入：s = "010" 输出："001" 解释：因为字符串 s 中仅有一个 '1' ，其必须出现在最后一位上。所以答案是 "001" 。 示例 2： 输入：s = "0101" 输出："1001" 解释：其中一个 '1' 必须出现在最后一位上。而由剩下的数字可以生产的最大数字是 "100" 。所以答案是 "1001" 。 提示： * 1 <= s.length <= 100 * s 仅由 '0' 和 '1' 组成 * s 中至少包含一个 '1'
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将所有的 '1' 放在前面，除了最后一个 '1' 放在最后一位，以确保结果是奇数。

算法步骤:
1. 统计字符串中 '1' 的数量。
2. 构造结果字符串，将所有 '1' 放在前面，除了最后一个 '1' 放在最后一位，其余位置用 '0' 填充。

关键点:
- 确保最后一个 '1' 放在最后一位，以保证结果是奇数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。需要遍历字符串一次来统计 '1' 的数量，然后再构造结果字符串。
空间复杂度: O(1)，除了结果字符串外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> str:
    """
    函数式接口 - 实现
    """
    # 统计字符串中 '1' 的数量
    ones_count = s.count('1')
    
    # 构造结果字符串
    result = '1' * (ones_count - 1) + '0' * (len(s) - ones_count) + '1'
    
    return result


Solution = create_solution(solution_function_name)