"""
1806. 还原排列的最少操作步数 - 给你一个偶数 n ，已知存在一个长度为 n 的排列 perm ，其中 perm[i] == i （下标 从 0 开始 计数）。 一步操作中，你将创建一个新数组 arr ，对于每个 i ： * 如果 i % 2 == 0 ，那么 arr[i] = perm[i / 2] * 如果 i % 2 == 1 ，那么 arr[i] = perm[n / 2 + (i - 1) / 2] 然后将 arr 赋值 给 perm 。 要想使 perm 回到排列初始值，至少需要执行多少步操作？返回最小的 非零 操作步数。 示例 1： 输入：n = 2 输出：1 解释：最初，perm = [0,1] 第 1 步操作后，perm = [0,1] 所以，仅需执行 1 步操作 示例 2： 输入：n = 4 输出：2 解释：最初，perm = [0,1,2,3] 第 1 步操作后，perm = [0,2,1,3] 第 2 步操作后，perm = [0,1,2,3] 所以，仅需执行 2 步操作 示例 3： 输入：n = 6 输出：4 提示： * 2 <= n <= 1000 * n 是一个偶数
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过观察每次操作后的数组变化，可以发现这是一个周期性的问题。我们可以通过模拟操作来找到最小的操作步数。

算法步骤:
1. 初始化一个长度为 n 的数组 perm，并将其初始化为 [0, 1, 2, ..., n-1]。
2. 创建一个新的数组 arr 来存储每次操作后的结果。
3. 通过循环模拟每次操作，直到 perm 回到初始状态。
4. 返回操作的次数。

关键点:
- 通过模拟操作来找到周期性。
- 使用一个变量来记录操作的次数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，因为每次操作会将数组分成两部分，最坏情况下需要 log n 次操作才能回到初始状态。
空间复杂度: O(n)，需要额外的空间来存储新的数组 arr。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def reinitialize_permutation(n: int) -> int:
    """
    函数式接口 - 返回使 perm 回到初始状态所需的最小操作步数
    """
    # 初始化 perm 和 arr
    perm = list(range(n))
    arr = [0] * n
    steps = 0
    
    while True:
        for i in range(n):
            if i % 2 == 0:
                arr[i] = perm[i // 2]
            else:
                arr[i] = perm[n // 2 + (i - 1) // 2]
        
        steps += 1
        if arr == list(range(n)):
            break
        
        perm = arr[:]
    
    return steps


Solution = create_solution(reinitialize_permutation)