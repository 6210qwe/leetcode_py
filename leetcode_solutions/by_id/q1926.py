"""
时间复杂度: O(n + m)，其中 n 是 Products 表的大小，m 是 StoresProducts 表的大小。JOIN 操作的时间复杂度通常为 O(n + m)。
空间复杂度: O(1)，查询操作的空间复杂度主要取决于数据库的实现，但通常可以认为是常数级别的。
"""

