"""
1154. 一年中的第几天 - 给你一个字符串 date ，按 YYYY-MM-DD 格式表示一个 现行公元纪年法 [https://baike.baidu.com/item/公元/17855] 日期。返回该日期是当年的第几天。 示例 1： 输入：date = "2019-01-09" 输出：9 解释：给定日期是2019年的第九天。 示例 2： 输入：date = "2019-02-10" 输出：41 提示： * date.length == 10 * date[4] == date[7] == '-'，其他的 date[i] 都是数字 * date 表示的范围从 1900 年 1 月 1 日至 2019 年 12 月 31 日
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将日期拆分为年、月、日，然后计算该日期是这一年的第几天。

算法步骤:
1. 拆分输入字符串，获取年、月、日。
2. 使用一个数组存储每个月的天数。
3. 如果是闰年，2月的天数加1。
4. 遍历前几个月，累加天数。
5. 加上当前月份的天数，得到结果。

关键点:
- 判断是否为闰年。
- 通过累加前几个月的天数来计算总天数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(1) - 固定的常数次操作。
空间复杂度: O(1) - 只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def day_of_the_year(date: str) -> int:
    """
    计算给定日期是这一年的第几天。
    """
    # 拆分输入字符串，获取年、月、日
    year, month, day = map(int, date.split('-'))
    
    # 每个月的天数
    days_in_month = [0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
    
    # 判断是否为闰年
    if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
        days_in_month[2] = 29
    
    # 计算总天数
    total_days = sum(days_in_month[:month]) + day
    
    return total_days


Solution = create_solution(day_of_the_year)