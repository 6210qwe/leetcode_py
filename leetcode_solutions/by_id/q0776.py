"""
590. N 叉树的后序遍历 - 给定一个 n 叉树的根节点 root ，返回 其节点值的 后序遍历 。 n 叉树 在输入中按层序遍历进行序列化表示，每组子节点由空值 null 分隔（请参见示例）。 示例 1： [https://assets.leetcode.com/uploads/2018/10/12/narytreeexample.png] 输入：root = [1,null,3,2,4,null,5,6] 输出：[5,6,3,2,4,1] 示例 2： [https://assets.leetcode.com/uploads/2019/11/08/sample_4_964.png] 输入：root = [1,null,2,3,4,5,null,null,6,7,null,8,null,9,10,null,null,11,null,12,null,13,null,null,14] 输出：[2,6,14,11,7,3,12,8,4,13,9,10,5,1] 提示： * 节点总数在范围 [0, 104] 内 * 0 <= Node.val <= 104 * n 叉树的高度小于或等于 1000 进阶：递归法很简单，你可以使用迭代法完成此题吗?
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用迭代法进行后序遍历。后序遍历的顺序是左-右-根，可以利用栈来实现。

算法步骤:
1. 初始化一个栈，并将根节点压入栈。
2. 初始化一个结果列表，用于存储后序遍历的结果。
3. 当栈不为空时，弹出栈顶节点，并将其值插入结果列表的开头。
4. 将该节点的所有子节点依次压入栈。
5. 返回结果列表。

关键点:
- 使用栈来模拟递归的过程。
- 将节点值插入结果列表的开头，以实现后序遍历的顺序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是节点的数量。每个节点都被访问一次。
空间复杂度: O(n)，在最坏情况下，栈中可能会包含所有的节点。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.tree import Node
from leetcode_solutions.utils.solution import create_solution

def postorder(root: 'Node') -> List[int]:
    """
    函数式接口 - 实现 N 叉树的后序遍历
    """
    if not root:
        return []

    stack, result = [root], []
    
    while stack:
        node = stack.pop()
        result.insert(0, node.val)
        for child in node.children:
            stack.append(child)
    
    return result

Solution = create_solution(postorder)