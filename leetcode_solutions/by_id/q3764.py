"""
3462. 提取至多 K 个元素的最大总和 - 给你一个大小为 n x m 的二维矩阵 grid ，以及一个长度为 n 的整数数组 limits ，和一个整数 k 。你的目标是从矩阵 grid 中提取出 至多 k 个元素，并计算这些元素的最大总和，提取时需满足以下限制： * 从 grid 的第 i 行提取的元素数量不超过 limits[i] 。 返回最大总和。 示例 1： 输入：grid = [[1,2],[3,4]], limits = [1,2], k = 2 输出：7 解释： * 从第 2 行提取至多 2 个元素，取出 4 和 3 。 * 至多提取 2 个元素时的最大总和 4 + 3 = 7 。 示例 2： 输入：grid = [[5,3,7],[8,2,6]], limits = [2,2], k = 3 输出：21 解释： * 从第 1 行提取至多 2 个元素，取出 7 。 * 从第 2 行提取至多 2 个元素，取出 8 和 6 。 * 至多提取 3 个元素时的最大总和 7 + 8 + 6 = 21 。 提示： * n == grid.length == limits.length * m == grid[i].length * 1 <= n, m <= 500 * 0 <= grid[i][j] <= 105 * 0 <= limits[i] <= m * 0 <= k <= min(n * m, sum(limits))
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，优先选择每行中最大的元素，直到达到 k 个元素或无法再选择为止。

算法步骤:
1. 将每行的元素按降序排序。
2. 使用一个最小堆来维护当前选择的 k 个最大元素。
3. 遍历每一行，尽可能多地选择该行中的最大元素，同时更新堆。
4. 计算堆中所有元素的总和。

关键点:
- 使用最小堆来动态维护当前选择的 k 个最大元素。
- 每次选择元素时，确保不超过 limits 的限制。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * m * log m + k * log k)
- n * m * log m 用于对每行进行排序。
- k * log k 用于维护最小堆。

空间复杂度: O(k)
- 最小堆的空间复杂度为 O(k)。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
import heapq

def max_sum_with_at_most_k_elements(grid: List[List[int]], limits: List[int], k: int) -> int:
    """
    函数式接口 - 从矩阵 grid 中提取出至多 k 个元素，并计算这些元素的最大总和。
    """
    # 初始化最小堆
    min_heap = []
    total_sum = 0
    
    # 对每行进行降序排序
    for i, row in enumerate(grid):
        row.sort(reverse=True)
    
    # 遍历每一行
    for i, row in enumerate(grid):
        count = 0
        for num in row:
            if count < limits[i] and len(min_heap) < k:
                heapq.heappush(min_heap, num)
                total_sum += num
                count += 1
            elif count < limits[i] and num > min_heap[0]:
                total_sum += num - min_heap[0]
                heapq.heapreplace(min_heap, num)
                count += 1
            else:
                break
    
    return total_sum

