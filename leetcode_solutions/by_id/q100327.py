"""
LCR 166. 珠宝的最高价值 - 现有一个记作二维矩阵 frame 的珠宝架，其中 frame[i][j] 为该位置珠宝的价值。拿取珠宝的规则为： * 只能从架子的左上角开始拿珠宝 * 每次可以移动到右侧或下侧的相邻位置 * 到达珠宝架子的右下角时，停止拿取 注意：珠宝的价值都是大于 0 的。除非这个架子上没有任何珠宝，比如 frame = [[0]]。 示例 1： 输入：frame = [[1,3,1],[1,5,1],[4,2,1]] 输出：12 解释：路径 1→3→5→2→1 可以拿到最高价值的珠宝 提示： * 0 < frame.length <= 200 * 0 < frame[0].length <= 200
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 动态规划

算法步骤:
1. 初始化一个与输入矩阵相同大小的 dp 矩阵，dp[i][j] 表示从 (0,0) 到 (i,j) 的最大价值。
2. 初始化 dp[0][0] 为 frame[0][0]。
3. 填充 dp 矩阵的第一行和第一列，因为只能从左边或上边到达这些位置。
4. 对于其他位置 (i,j)，dp[i][j] = max(dp[i-1][j], dp[i][j-1]) + frame[i][j]。
5. 返回 dp 矩阵的右下角值 dp[m-1][n-1]。

关键点:
- 使用原地修改 frame 矩阵来节省空间。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)，其中 m 和 n 分别是矩阵的行数和列数。
空间复杂度: O(1)，使用原地修改 frame 矩阵，不使用额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(frame: List[List[int]]) -> int:
    """
    函数式接口 - 计算珠宝的最高价值
    """
    if not frame or not frame[0]:
        return 0

    m, n = len(frame), len(frame[0])

    # 初始化第一行
    for j in range(1, n):
        frame[0][j] += frame[0][j - 1]

    # 初始化第一列
    for i in range(1, m):
        frame[i][0] += frame[i - 1][0]

    # 填充 dp 矩阵
    for i in range(1, m):
        for j in range(1, n):
            frame[i][j] += max(frame[i - 1][j], frame[i][j - 1])

    return frame[m - 1][n - 1]


Solution = create_solution(solution_function_name)