# ============================================================================
# 问题描述
# ============================================================================
"""
LCR 120. 寻找文件副本 - 设备中存有 n 个文件，文件 id 记于数组 documents。若文件 id 相同，则定义为该文件存在副本。请返回任一存在副本的文件 id。 示例 1： 输入：documents = [2, 5, 3, 0, 5, 0] 输出：0 或 5 提示： * 0 ≤ documents[i] ≤ n-1 * 2 <= n <= 100000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希集合来记录已经出现过的文件 id，当遇到重复的文件 id 时直接返回。

算法步骤:
1. 初始化一个空的哈希集合。
2. 遍历文件 id 数组，对于每个文件 id：
   - 如果该文件 id 已经在哈希集合中，返回该文件 id。
   - 否则，将该文件 id 添加到哈希集合中。

关键点:
- 使用哈希集合可以在 O(1) 时间内检查文件 id 是否已经出现过。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是文件 id 数组的长度。最坏情况下需要遍历整个数组。
空间复杂度: O(n)，在最坏情况下，哈希集合需要存储 n 个文件 id。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(documents: List[int]) -> int:
    """
    函数式接口 - 寻找文件副本
    """
    seen = set()  # 用于存储已经出现过的文件 id
    for doc_id in documents:
        if doc_id in seen:
            return doc_id
        seen.add(doc_id)
    return -1  # 理论上不会到达这里，因为题目保证至少有一个重复文件 id


Solution = create_solution(solution_function_name)