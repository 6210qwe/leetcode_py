"""
856. 括号的分数 - 给定一个平衡括号字符串 S，按下述规则计算该字符串的分数： * () 得 1 分。 * AB 得 A + B 分，其中 A 和 B 是平衡括号字符串。 * (A) 得 2 * A 分，其中 A 是平衡括号字符串。 示例 1： 输入： "()" 输出： 1 示例 2： 输入： "(())" 输出： 2 示例 3： 输入： "()()" 输出： 2 示例 4： 输入： "(()(()))" 输出： 6 提示： 1. S 是平衡括号字符串，且只含有 ( 和 ) 。 2. 2 <= S.length <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用栈来处理括号字符串，遇到'('时将当前分数压入栈中，并将当前分数重置为0；遇到')'时，弹出栈顶元素，并将当前分数乘以2加上栈顶元素。

算法步骤:
1. 初始化一个栈和当前分数。
2. 遍历字符串：
   - 如果是'('，将当前分数压入栈中，并将当前分数重置为0。
   - 如果是')'，弹出栈顶元素，并将当前分数乘以2加上栈顶元素。
3. 返回最终的分数。

关键点:
- 使用栈来存储中间结果，避免重复计算。
- 通过栈顶元素来计算当前子串的分数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度。每个字符只会被处理一次。
空间复杂度: O(n)，在最坏情况下，栈的大小可能达到字符串长度的一半。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def score_of_parentheses(S: str) -> int:
    """
    函数式接口 - 计算平衡括号字符串的分数
    """
    stack = []
    current_score = 0
    
    for char in S:
        if char == '(':
            stack.append(current_score)
            current_score = 0
        else:
            current_score = stack.pop() + max(2 * current_score, 1)
    
    return current_score


Solution = create_solution(score_of_parentheses)