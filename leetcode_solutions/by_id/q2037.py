"""
1925. 统计平方和三元组的数目 - 一个 平方和三元组 (a,b,c) 指的是满足 a2 + b2 = c2 的 整数 三元组 a，b 和 c 。 给你一个整数 n ，请你返回满足 1 <= a, b, c <= n 的 平方和三元组 的数目。 示例 1： 输入：n = 5 输出：2 解释：平方和三元组为 (3,4,5) 和 (4,3,5) 。 示例 2： 输入：n = 10 输出：4 解释：平方和三元组为 (3,4,5)，(4,3,5)，(6,8,10) 和 (8,6,10) 。 提示： * 1 <= n <= 250
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 枚举所有可能的 a, b, c 组合，并检查是否满足 a^2 + b^2 = c^2。

算法步骤:
1. 初始化计数器 count 为 0。
2. 使用三重循环枚举所有可能的 a, b, c 组合。
3. 对于每组 a, b, c，检查是否满足 a^2 + b^2 = c^2。
4. 如果满足条件，增加计数器 count。
5. 返回计数器 count 的值。

关键点:
- 通过三重循环枚举所有可能的组合。
- 优化内层循环，减少不必要的计算。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(n: int) -> int:
    """
    函数式接口 - 统计平方和三元组的数目
    """
    count = 0
    for a in range(1, n + 1):
        for b in range(a, n + 1):  # 从 a 开始，避免重复计数
            c_square = a * a + b * b
            c = int(c_square ** 0.5)
            if c <= n and c * c == c_square:
                count += 2 if a != b else 1  # (a, b, c) 和 (b, a, c) 是两个不同的三元组
    return count


Solution = create_solution(solution_function_name)