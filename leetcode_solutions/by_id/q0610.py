"""
610. 判断三角形 - 表: Triangle +-------------+------+ | Column Name | Type | +-------------+------+ | x | int | | y | int | | z | int | +-------------+------+ 在 SQL 中，(x, y, z)是该表的主键列。 该表的每一行包含三个线段的长度。 对每三个线段报告它们是否可以形成一个三角形。 以 任意顺序 返回结果表。 查询结果格式如下所示。 示例 1: 输入: Triangle 表: +----+----+----+ | x | y | z | +----+----+----+ | 13 | 15 | 30 | | 10 | 20 | 15 | +----+----+----+ 输出: +----+----+----+----------+ | x | y | z | triangle | +----+----+----+----------+ | 13 | 15 | 30 | No | | 10 | 20 | 15 | Yes | +----+----+----+----------+
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 判断三条边是否能构成三角形，需要满足任意两边之和大于第三边。

算法步骤:
1. 读取输入的三角形表。
2. 对于每一行，检查 x, y, z 是否满足三角形不等式。
3. 根据判断结果，添加 'Yes' 或 'No' 到结果表中。

关键点:
- 使用 SQL 查询来实现高效的判断。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是表中的行数。
空间复杂度: O(1)，不需要额外的空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(triangle: List[List[int]]) -> List[List[str]]:
    """
    函数式接口 - 判断三角形
    """
    result = []
    for row in triangle:
        x, y, z = row
        if x + y > z and x + z > y and y + z > x:
            result.append([x, y, z, "Yes"])
        else:
            result.append([x, y, z, "No"])
    return result


Solution = create_solution(solution_function_name)