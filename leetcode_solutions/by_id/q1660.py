"""
1556. 千位分隔数 - 给你一个整数 n，请你每隔三位添加点（即 "." 符号）作为千位分隔符，并将结果以字符串格式返回。 示例 1： 输入：n = 987 输出："987" 示例 2： 输入：n = 1234 输出："1.234" 示例 3： 输入：n = 123456789 输出："123.456.789" 示例 4： 输入：n = 0 输出："0" 提示： * 0 <= n < 2^31
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将整数转换为字符串，然后从右向左每三位插入一个点。

算法步骤:
1. 将整数 n 转换为字符串。
2. 从右向左每隔三位插入一个点。
3. 返回处理后的字符串。

关键点:
- 从右向左处理字符串，确保点的位置正确。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(log n) - 因为字符串的长度与 n 的对数成正比。
空间复杂度: O(log n) - 需要存储转换后的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def thousand_separator(n: int) -> str:
    """
    函数式接口 - 将整数 n 转换为带有千位分隔符的字符串。
    """
    # 将整数转换为字符串
    s = str(n)
    
    # 从右向左每隔三位插入一个点
    result = []
    for i in range(len(s)):
        if i > 0 and (len(s) - i) % 3 == 0:
            result.append('.')
        result.append(s[i])
    
    return ''.join(result)


Solution = create_solution(thousand_separator)