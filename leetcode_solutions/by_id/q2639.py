"""
2553. 分割数组中数字的数位 - 给你一个正整数数组 nums ，请你返回一个数组 answer ，你需要将 nums 中每个整数进行数位分割后，按照 nums 中出现的 相同顺序 放入答案数组中。 对一个整数进行数位分割，指的是将整数各个数位按原本出现的顺序排列成数组。 * 比方说，整数 10921 ，分割它的各个数位得到 [1,0,9,2,1] 。 示例 1： 输入：nums = [13,25,83,77] 输出：[1,3,2,5,8,3,7,7] 解释： - 分割 13 得到 [1,3] 。 - 分割 25 得到 [2,5] 。 - 分割 83 得到 [8,3] 。 - 分割 77 得到 [7,7] 。 answer = [1,3,2,5,8,3,7,7] 。answer 中的数字分割结果按照原数字在数组中的相同顺序排列。 示例 2： 输入：nums = [7,1,3,9] 输出：[7,1,3,9] 解释：nums 中每个整数的分割是它自己。 answer = [7,1,3,9] 。 提示： * 1 <= nums.length <= 1000 * 1 <= nums[i] <= 105
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将每个整数转换为字符串，然后逐个字符地将其添加到结果列表中。

算法步骤:
1. 初始化一个空的结果列表 `result`。
2. 遍历输入数组 `nums` 中的每个整数。
3. 将每个整数转换为字符串，并逐个字符地将其添加到 `result` 列表中。
4. 返回 `result` 列表。

关键点:
- 使用字符串操作来逐个字符地处理整数的数位。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n * d)，其中 n 是数组 `nums` 的长度，d 是整数的最大数位数（最多为 6）。
空间复杂度: O(n * d)，用于存储结果列表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def separate_digits(nums: List[int]) -> List[int]:
    """
    函数式接口 - 将数组中的每个整数进行数位分割，并按顺序放入结果数组中
    """
    result = []
    for num in nums:
        for digit in str(num):
            result.append(int(digit))
    return result


Solution = create_solution(separate_digits)