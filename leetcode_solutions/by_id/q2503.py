"""
2419. 按位与最大的最长子数组 - 给你一个长度为 n 的整数数组 nums 。 考虑 nums 中进行 按位与（bitwise AND）运算得到的值 最大 的 非空 子数组。 * 换句话说，令 k 是 nums 任意 子数组执行按位与运算所能得到的最大值。那么，只需要考虑那些执行一次按位与运算后等于 k 的子数组。 返回满足要求的 最长 子数组的长度。 数组的按位与就是对数组中的所有数字进行按位与运算。 子数组 是数组中的一个连续元素序列。 示例 1： 输入：nums = [1,2,3,3,2,2] 输出：2 解释： 子数组按位与运算的最大值是 3 。 能得到此结果的最长子数组是 [3,3]，所以返回 2 。 示例 2： 输入：nums = [1,2,3,4] 输出：1 解释： 子数组按位与运算的最大值是 4 。 能得到此结果的最长子数组是 [4]，所以返回 1 。 提示： * 1 <= nums.length <= 105 * 1 <= nums[i] <= 106
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 找到数组中最大值，然后找到该最大值的最长连续子数组。

算法步骤:
1. 找到数组中的最大值。
2. 遍历数组，找到最大值的最长连续子数组。

关键点:
- 通过遍历数组找到最大值。
- 通过滑动窗口技术找到最大值的最长连续子数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(nums: List[int]) -> int:
    """
    函数式接口 - 找到按位与最大的最长子数组
    """
    if not nums:
        return 0

    # 找到数组中的最大值
    max_value = max(nums)

    # 初始化变量
    max_length = 0
    current_length = 0

    # 遍历数组，找到最大值的最长连续子数组
    for num in nums:
        if num == max_value:
            current_length += 1
            max_length = max(max_length, current_length)
        else:
            current_length = 0

    return max_length


Solution = create_solution(solution_function_name)