"""
1827. 最少操作使数组递增 - 给你一个整数数组 nums （下标从 0 开始）。每一次操作中，你可以选择数组中一个元素，并将它增加 1 。 * 比方说，如果 nums = [1,2,3] ，你可以选择增加 nums[1] 得到 nums = [1,3,3] 。 请你返回使 nums 严格递增 的 最少 操作次数。 我们称数组 nums 是 严格递增的 ，当它满足对于所有的 0 <= i < nums.length - 1 都有 nums[i] < nums[i+1] 。一个长度为 1 的数组是严格递增的一种特殊情况。 示例 1： 输入：nums = [1,1,1] 输出：3 解释：你可以进行如下操作： 1) 增加 nums[2] ，数组变为 [1,1,2] 。 2) 增加 nums[1] ，数组变为 [1,2,2] 。 3) 增加 nums[2] ，数组变为 [1,2,3] 。 示例 2： 输入：nums = [1,5,2,4,1] 输出：14 示例 3： 输入：nums = [8] 输出：0 提示： * 1 <= nums.length <= 5000 * 1 <= nums[i] <= 104
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，逐个检查数组中的每个元素，确保当前元素大于前一个元素。

算法步骤:
1. 初始化一个变量 `prev` 用于记录前一个元素的值。
2. 初始化一个变量 `operations` 用于记录所需的操作次数。
3. 遍历数组，对于每个元素：
   - 如果当前元素小于等于 `prev`，则需要进行操作使其大于 `prev`。
   - 计算所需的操作次数并更新 `prev`。
4. 返回 `operations`。

关键点:
- 通过贪心算法，每次只考虑当前元素和前一个元素的关系，确保数组严格递增。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是数组的长度。我们只需遍历数组一次。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def min_operations_to_make_array_increasing(nums: List[int]) -> int:
    """
    函数式接口 - 使数组严格递增的最少操作次数
    """
    if not nums:
        return 0

    prev = nums[0]
    operations = 0

    for i in range(1, len(nums)):
        if nums[i] <= prev:
            # 计算所需的操作次数
            diff = prev - nums[i] + 1
            operations += diff
            # 更新当前元素
            nums[i] += diff
        # 更新前一个元素
        prev = nums[i]

    return operations


Solution = create_solution(min_operations_to_make_array_increasing)