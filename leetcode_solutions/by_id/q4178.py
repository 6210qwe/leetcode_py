"""
3813. 元音辅音得分 - 给你一个字符串 s，由小写英文字母、空格和数字组成。 令 v 表示 s 中元音字母的数量，c 表示辅音字母的数量。 元音字母是 'a'、'e'、'i'、'o' 和 'u'，而英文字母表中除元音外的其他字母均视为辅音字母。 字符串 s 的 得分 定义如下： * 如果 c > 0，则 score = floor(v / c)，其中 floor 表示 向下取整 到最接近的整数。 * 否则，如果 c = 0，则 score = 0。 返回一个整数，表示字符串的得分。 示例 1： 输入: s = "cooear" 输出: 2 解释: 字符串 s = "cooear" 包含 v = 4 个元音字母 ('o', 'o', 'e', 'a') 和 c = 2 个辅音字母 ('c', 'r')。 得分为 floor(v / c) = floor(4 / 2) = 2。 示例 2： 输入: s = "axeyizou" 输出: 1 解释: 字符串 s = "axeyizou" 包含 v = 5 个元音字母 ('a', 'e', 'i', 'o', 'u') 和 c = 3 个辅音字母 ('x', 'y', 'z')。 得分为 floor(v / c) = floor(5 / 3) = 1。 示例 3： 输入: s = "au 123" 输出: 0 解释: 字符串 s = "au 123" 不包含辅音字母 (c = 0)，因此得分为 0。 提示： * 1 <= s.length <= 100 * s 仅由小写英文字母、空格和数字组成。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过一次遍历字符串来统计元音和辅音的数量，然后计算得分。

算法步骤:
1. 初始化元音和辅音的计数器。
2. 遍历字符串，更新元音和辅音的计数器。
3. 根据元音和辅音的数量计算得分。

关键点:
- 使用集合来快速判断字符是否为元音。
- 一次遍历即可完成统计。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串的长度，因为我们需要遍历整个字符串一次。
空间复杂度: O(1)，只需要常数级的额外空间来存储计数器。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(s: str) -> int:
    """
    函数式接口 - 计算字符串的元音辅音得分
    """
    vowels = set('aeiou')
    v_count = 0
    c_count = 0
    
    for char in s:
        if char in vowels:
            v_count += 1
        elif char.isalpha():
            c_count += 1
    
    if c_count == 0:
        return 0
    else:
        return v_count // c_count


Solution = create_solution(solution_function_name)