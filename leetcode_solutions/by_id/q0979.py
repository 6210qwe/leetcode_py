"""
942. 增减字符串匹配 - 由范围 [0,n] 内所有整数组成的 n + 1 个整数的排列序列可以表示为长度为 n 的字符串 s ，其中: * 如果 perm[i] < perm[i + 1] ，那么 s[i] == 'I' * 如果 perm[i] > perm[i + 1] ，那么 s[i] == 'D' 给定一个字符串 s ，重构排列 perm 并返回它。如果有多个有效排列perm，则返回其中 任何一个 。 示例 1： 输入：s = "IDID" 输出：[0,4,1,3,2] 示例 2： 输入：s = "III" 输出：[0,1,2,3] 示例 3： 输入：s = "DDI" 输出：[3,2,0,1] 提示： * 1 <= s.length <= 105 * s 只包含字符 "I" 或 "D"
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个指针，一个指向当前最小值，一个指向当前最大值。遍历字符串 s，根据字符是 'I' 还是 'D' 来决定使用哪个指针，并更新指针。

算法步骤:
1. 初始化两个指针 low 和 high，分别指向 0 和 n。
2. 遍历字符串 s：
   - 如果字符是 'I'，将 low 指向的值加入结果数组，并将 low 加 1。
   - 如果字符是 'D'，将 high 指向的值加入结果数组，并将 high 减 1。
3. 最后将 low 指向的值加入结果数组（此时 low 应该等于 high）。

关键点:
- 使用贪心算法，通过两个指针来构建满足条件的排列。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是字符串 s 的长度。我们只需要遍历一次字符串 s。
空间复杂度: O(1)，除了结果数组外，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def diStringMatch(s: str) -> List[int]:
    """
    函数式接口 - 实现增减字符串匹配
    """
    n = len(s)
    low, high = 0, n
    result = []
    
    for char in s:
        if char == 'I':
            result.append(low)
            low += 1
        else:
            result.append(high)
            high -= 1
    
    result.append(low)  # 最后 low 应该等于 high
    return result

