# ============================================================================

from typing import List

def maximalRange(nums: List[int]) -> List[int]:
    n = len(nums)
    left, right = [0] * n, [0] * n
    stack = []
    
    # 从左到右遍历，找到每个元素的最左边界
    for i in range(n):
        while stack and nums[stack[-1]] < nums[i]:
            stack.pop()
        left[i] = stack[-1] + 1 if stack else 0
        stack.append(i)
    
    stack.clear()
    
    # 从右到左遍历，找到每个元素的最右边界
    for i in range(n - 1, -1, -1):
        while stack and nums[stack[-1]] < nums[i]:
            stack.pop()
        right[i] = stack[-1] - 1 if stack else n - 1
        stack.append(i)
    
    # 计算每个元素的最大范围
    result = [right[i] - left[i] + 1 for i in range(n)]
    return result

