"""
核心思想: 使用单调栈来找到每个元素作为最大值的最左和最右边界。

算法步骤:
1. 初始化两个数组 left 和 right，分别记录每个元素作为最大值的最左和最右边界。
2. 使用单调递减栈从左到右遍历数组，找到每个元素的最左边界。
3. 使用单调递减栈从右到左遍历数组，找到每个元素的最右边界。
4. 计算每个元素的最大范围，并返回结果。

关键点:
- 使用单调栈可以在线性时间内找到每个元素的边界。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
