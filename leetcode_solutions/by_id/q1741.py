"""
1636. 按照频率将数组升序排序 - 给你一个整数数组 nums ，请你将数组按照每个值的频率 升序 排序。如果有多个值的频率相同，请你按照数值本身将它们 降序 排序。 请你返回排序后的数组。 示例 1： 输入：nums = [1,1,2,2,2,3] 输出：[3,1,1,2,2,2] 解释：'3' 频率为 1，'1' 频率为 2，'2' 频率为 3 。 示例 2： 输入：nums = [2,3,1,3,2] 输出：[1,3,3,2,2] 解释：'2' 和 '3' 频率都为 2 ，所以它们之间按照数值本身降序排序。 示例 3： 输入：nums = [-1,1,-6,4,5,-6,1,4,1] 输出：[5,-1,4,4,-6,-6,1,1,1] 提示： * 1 <= nums.length <= 100 * -100 <= nums[i] <= 100
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用计数器统计频率，然后根据频率和数值进行排序。

算法步骤:
1. 使用 `collections.Counter` 统计每个元素的频率。
2. 将数组按频率升序排序，如果频率相同则按数值降序排序。

关键点:
- 使用 `Counter` 来高效统计频率。
- 使用 `sorted` 函数进行多键排序。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n log n)，其中 n 是数组的长度。排序操作的时间复杂度是 O(n log n)。
空间复杂度: O(n)，用于存储频率计数和排序后的结果。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List
from collections import Counter

def frequencySort(nums: List[int]) -> List[int]:
    """
    函数式接口 - 按照频率将数组升序排序
    """
    # 统计每个元素的频率
    freq = Counter(nums)
    
    # 按频率升序排序，如果频率相同则按数值降序排序
    sorted_nums = sorted(nums, key=lambda x: (freq[x], -x))
    
    return sorted_nums

