"""
2384. 最大回文数字 - 给你一个仅由数字（0 - 9）组成的字符串 num 。 请你找出能够使用 num 中数字形成的 最大回文 整数，并以字符串形式返回。该整数不含 前导零 。 注意： * 你 无需 使用 num 中的所有数字，但你必须使用 至少 一个数字。 * 数字可以重新排序。 示例 1： 输入：num = "444947137" 输出："7449447" 解释： 从 "444947137" 中选用数字 "4449477"，可以形成回文整数 "7449447" 。 可以证明 "7449447" 是能够形成的最大回文整数。 示例 2： 输入：num = "00009" 输出："9" 解释： 可以证明 "9" 能够形成的最大回文整数。 注意返回的整数不应含前导零。 提示： * 1 <= num.length <= 105 * num 由数字（0 - 9）组成
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计数来构建最大回文数。首先统计每个数字出现的次数，然后从大到小选择数字构建回文数。

算法步骤:
1. 统计每个数字的出现次数。
2. 从大到小选择数字，如果某个数字出现了偶数次，则将其全部加入回文数中；如果出现了奇数次，则只取其最大偶数部分。
3. 如果有剩余的单个数字，选择最大的一个放在中间。
4. 构建回文数时，确保没有前导零。

关键点:
- 优先选择较大的数字来构建回文数。
- 确保没有前导零。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 num 的长度。我们需要遍历字符串进行计数和构建回文数。
空间复杂度: O(1)，因为计数器的大小是固定的（最多 10 个数字）。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
