"""
2206. 将数组划分成相等数对 - 给你一个整数数组 nums ，它包含 2 * n 个整数。 你需要将 nums 划分成 n 个数对，满足： * 每个元素 只属于一个 数对。 * 同一数对中的元素 相等 。 如果可以将 nums 划分成 n 个数对，请你返回 true ，否则返回 false 。 示例 1： 输入：nums = [3,2,3,2,2,2] 输出：true 解释： nums 中总共有 6 个元素，所以它们应该被划分成 6 / 2 = 3 个数对。 nums 可以划分成 (2, 2) ，(3, 3) 和 (2, 2) ，满足所有要求。 示例 2： 输入：nums = [1,2,3,4] 输出：false 解释： 无法将 nums 划分成 4 / 2 = 2 个数对且满足所有要求。 提示： * nums.length == 2 * n * 1 <= n <= 500 * 1 <= nums[i] <= 500
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用哈希表统计每个元素的出现次数，然后检查每个元素的出现次数是否都是偶数。

算法步骤:
1. 使用哈希表统计每个元素的出现次数。
2. 遍历哈希表，检查每个元素的出现次数是否都是偶数。
3. 如果所有元素的出现次数都是偶数，则返回 True；否则返回 False。

关键点:
- 使用哈希表统计元素出现次数，时间复杂度为 O(n)。
- 检查每个元素的出现次数是否为偶数，时间复杂度为 O(k)，其中 k 是不同元素的数量。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n + k)，其中 n 是数组的长度，k 是不同元素的数量。
空间复杂度: O(k)，用于存储哈希表。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def can_divide_into_equal_pairs(nums: List[int]) -> bool:
    """
    函数式接口 - 检查是否可以将数组划分成相等数对
    """
    # 使用哈希表统计每个元素的出现次数
    count = {}
    for num in nums:
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    # 检查每个元素的出现次数是否都是偶数
    for value in count.values():
        if value % 2 != 0:
            return False
    
    return True


Solution = create_solution(can_divide_into_equal_pairs)