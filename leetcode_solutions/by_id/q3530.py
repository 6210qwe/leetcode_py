"""
3220. 奇数和偶数交易 - 表：transactions +------------------+------+ | Column Name | Type | +------------------+------+ | transaction_id | int | | amount | int | | transaction_date | date | +------------------+------+ transactions_id 列唯一标识了表中的每一行。 这张表的每一行包含交易 id，金额总和和交易日期。 编写一个解决方案来查找每天 奇数 交易金额和 偶数 交易金额的 总和。如果某天没有奇数或偶数交易，显示为 0。 返回结果表以 transaction_date 升序 排序。 结果格式如下所示。 示例： 输入： transactions 表： +----------------+--------+------------------+ | transaction_id | amount | transaction_date | +----------------+--------+------------------+ | 1 | 150 | 2024-07-01 | | 2 | 200 | 2024-07-01 | | 3 | 75 | 2024-07-01 | | 4 | 300 | 2024-07-02 | | 5 | 50 | 2024-07-02 | | 6 | 120 | 2024-07-03 | +----------------+--------+------------------+ 输出： +------------------+---------+----------+ | transaction_date | odd_sum | even_sum | +------------------+---------+----------+ | 2024-07-01 | 75 | 350 | | 2024-07-02 | 0 | 350 | | 2024-07-03 | 0 | 120 | +------------------+---------+----------+ 解释： * 对于交易日期： * 2024-07-01: * 奇数交易金额总和：75 * 偶数交易金额总和：150 + 200 = 350 * 2024-07-02: * 奇数交易金额总和：0 * 偶数交易金额总和：300 + 50 = 350 * 2024-07-03: * 奇数交易金额总和：0 * 偶数交易金额总和：120 注意：输出表以 transaction_date 升序排序。
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用 SQL 查询来计算每天的奇数和偶数交易金额总和。

算法步骤:
1. 使用 CASE WHEN 语句将交易金额分为奇数和偶数。
2. 使用 GROUP BY 语句按交易日期分组，并计算每天的奇数和偶数交易金额总和。
3. 使用 COALESCE 函数处理可能的 NULL 值，确保在某天没有奇数或偶数交易时显示为 0。
4. 按交易日期升序排序。

关键点:
- 使用 CASE WHEN 语句进行条件判断。
- 使用 GROUP BY 语句进行分组聚合。
- 使用 COALESCE 函数处理 NULL 值。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)，其中 n 是 transactions 表的行数。我们需要遍历整个表来进行分组和聚合操作。
空间复杂度: O(1)，我们只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(transactions):
    """
    函数式接口 - 实现
    """
    # SQL 查询实现最优解法
    query = """
    SELECT 
        transaction_date,
        COALESCE(SUM(CASE WHEN amount % 2 = 1 THEN amount ELSE 0 END), 0) AS odd_sum,
        COALESCE(SUM(CASE WHEN amount % 2 = 0 THEN amount ELSE 0 END), 0) AS even_sum
    FROM 
        transactions
    GROUP BY 
        transaction_date
    ORDER BY 
        transaction_date ASC;
    """
    return query


Solution = create_solution(solution_function_name)