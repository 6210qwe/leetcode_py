"""
984. 不含 AAA 或 BBB 的字符串 - 给定两个整数 a 和 b ，返回 任意 字符串 s ，要求满足： * s 的长度为 a + b，且正好包含 a 个 'a' 字母与 b 个 'b' 字母； * 子串 'aaa' 没有出现在 s 中； * 子串 'bbb' 沒有出现在 s 中。 示例 1： 输入：a = 1, b = 2 输出："abb" 解释："abb", "bab" 和 "bba" 都是正确答案。 示例 2： 输入：a = 4, b = 1 输出："aabaa" 提示： * 0 <= a, b <= 100 * 对于给定的 a 和 b，保证存在满足要求的 s
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，优先添加数量更多的字符，但确保不会出现连续三个相同的字符。

算法步骤:
1. 初始化结果字符串 `result`。
2. 当 `a` 和 `b` 都大于 0 时，循环执行以下操作：
   - 如果 `a` 大于 `b`，则添加两个 'a' 和一个 'b' 到 `result`，并相应减少 `a` 和 `b` 的计数。
   - 否则，如果 `b` 大于 `a`，则添加两个 'b' 和一个 'a' 到 `result`，并相应减少 `a` 和 `b` 的计数。
   - 如果 `a` 和 `b` 相等，则交替添加 'a' 和 'b' 到 `result`，并相应减少 `a` 和 `b` 的计数。
3. 如果 `a` 或 `b` 还有剩余，继续添加剩余的字符到 `result`，确保不会出现连续三个相同的字符。

关键点:
- 通过贪心策略，优先添加数量更多的字符，但确保不会出现连续三个相同的字符。
- 交替添加字符以避免连续三个相同的字符。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(a + b)
空间复杂度: O(a + b)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def solution_function_name(a: int, b: int) -> str:
    """
    函数式接口 - 返回一个不含连续三个相同字符的字符串
    """
    result = []
    
    while a > 0 and b > 0:
        if a > b:
            result.append('aab')
            a -= 2
            b -= 1
        elif b > a:
            result.append('bba')
            b -= 2
            a -= 1
        else:
            result.append('ab')
            a -= 1
            b -= 1
    
    if a > 0:
        result.append('a' * a)
    if b > 0:
        result.append('b' * b)
    
    return ''.join(result)


Solution = create_solution(solution_function_name)