"""
2554. 从一个范围内选择最多整数 I - 给你一个整数数组 banned 和两个整数 n 和 maxSum 。你需要按照以下规则选择一些整数： * 被选择整数的范围是 [1, n] 。 * 每个整数 至多 选择 一次 。 * 被选择整数不能在数组 banned 中。 * 被选择整数的和不超过 maxSum 。 请你返回按照上述规则 最多 可以选择的整数数目。 示例 1： 输入：banned = [1,6,5], n = 5, maxSum = 6 输出：2 解释：你可以选择整数 2 和 4 。 2 和 4 在范围 [1, 5] 内，且它们都不在 banned 中，它们的和是 6 ，没有超过 maxSum 。 示例 2： 输入：banned = [1,2,3,4,5,6,7], n = 8, maxSum = 1 输出：0 解释：按照上述规则无法选择任何整数。 示例 3： 输入：banned = [11], n = 7, maxSum = 50 输出：7 解释：你可以选择整数 1, 2, 3, 4, 5, 6 和 7 。 它们都在范围 [1, 7] 中，且都没出现在 banned 中，它们的和是 28 ，没有超过 maxSum 。 提示： * 1 <= banned.length <= 104 * 1 <= banned[i], n <= 104 * 1 <= maxSum <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用贪心算法，优先选择较小的整数，并跳过被禁止的整数。

算法步骤:
1. 将 banned 数组转换为集合，以便快速查找。
2. 初始化当前和 current_sum 和计数器 count。
3. 遍历从 1 到 n 的整数：
   - 如果当前整数不在 banned 集合中，检查将其加入 current_sum 是否会超过 maxSum。
   - 如果不会超过，则将其加入 current_sum 并增加计数器 count。
   - 如果超过 maxSum，则停止遍历。
4. 返回计数器 count。

关键点:
- 使用集合来快速查找被禁止的整数。
- 通过贪心算法优先选择较小的整数，确保选择的整数数量最多。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(m)，其中 m 是 banned 数组的长度。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def max_count(banned: List[int], n: int, max_sum: int) -> int:
    """
    函数式接口 - 返回最多可以选择的整数数目
    """
    # 将 banned 数组转换为集合
    banned_set = set(banned)
    
    current_sum = 0
    count = 0
    
    for i in range(1, n + 1):
        if i not in banned_set:
            if current_sum + i <= max_sum:
                current_sum += i
                count += 1
            else:
                break
    
    return count

