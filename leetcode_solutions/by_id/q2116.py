from typing import List

def count_k_difference(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算差的绝对值为 k 的数对数目
    """
    count = {}
    pairs = 0
    
    for num in nums:
        if num + k in count:
            pairs += count[num + k]
        if num - k in count:
            pairs += count[num - k]
        
        if num in count:
            count[num] += 1
        else:
            count[num] = 1
    
    return pairs

