"""
核心思想: 检查原始数组长度是否等于 m * n，然后按行填充二维数组。

算法步骤:
1. 检查 original 的长度是否等于 m * n，如果不等则返回空数组。
2. 初始化一个 m 行 n 列的二维数组。
3. 使用切片将 original 分割成每 n 个元素一组，并填入二维数组中。

关键点:
- 检查原始数组长度是否符合要求。
- 使用切片和列表推导式来构建二维数组。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(m * n)
空间复杂度: O(m * n)
"""

# ============================================================================
