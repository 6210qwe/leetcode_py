"""
3468. 可行数组的数目 - 给你一个长度为 n 的数组 original 和一个长度为 n x 2 的二维数组 bounds，其中 bounds[i] = [ui, vi]。 你需要找到长度为 n 且满足以下条件的 可能的 数组 copy 的数量： 1. 对于 1 <= i <= n - 1 ，都有 (copy[i] - copy[i - 1]) == (original[i] - original[i - 1]) 。 2. 对于 0 <= i <= n - 1 ，都有 ui <= copy[i] <= vi 。 返回满足这些条件的数组数目。 示例 1 输入：original = [1,2,3,4], bounds = [[1,2],[2,3],[3,4],[4,5]] 输出：2 解释： 可能的数组为： * [1, 2, 3, 4] * [2, 3, 4, 5] 示例 2 输入：original = [1,2,3,4], bounds = [[1,10],[2,9],[3,8],[4,7]] 输出：4 解释： 可能的数组为： * [1, 2, 3, 4] * [2, 3, 4, 5] * [3, 4, 5, 6] * [4, 5, 6, 7] 示例 3 输入：original = [1,2,1,2], bounds = [[1,1],[2,3],[3,3],[2,3]] 输出：0 解释： 没有可行的数组。 提示： * 2 <= n == original.length <= 105 * 1 <= original[i] <= 109 * bounds.length == n * bounds[i].length == 2 * 1 <= bounds[i][0] <= bounds[i][1] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 通过计算每个元素的有效范围来确定可能的数组数量。

算法步骤:
1. 计算原始数组的差分数组。
2. 对于每个元素，计算其在bounds中的有效范围。
3. 通过累积有效范围的交集来计算可能的数组数量。

关键点:
- 差分数组可以帮助我们快速计算每个元素的有效范围。
- 通过累积有效范围的交集，可以确保所有条件都满足。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List

def find_number_of_copy_arrays(original: List[int], bounds: List[List[int]]) -> int:
    """
    函数式接口 - 计算满足条件的数组数量
    """
    n = len(original)
    if n == 0:
        return 0
    
    # 计算原始数组的差分数组
    diff = [original[i] - original[i - 1] for i in range(1, n)]
    
    # 初始化第一个元素的有效范围
    min_val, max_val = bounds[0]
    count = max(0, max_val - min_val + 1)
    
    for i in range(1, n):
        # 更新当前元素的有效范围
        min_val = max(min_val, bounds[i][0] - sum(diff[:i]))
        max_val = min(max_val, bounds[i][1] - sum(diff[:i]))
        
        # 如果有效范围为空，返回0
        if min_val > max_val:
            return 0
        
        # 更新可能的数组数量
        count = max(0, max_val - min_val + 1)
    
    return count

