"""
3783. 整数的镜像距离 - 给你一个整数 n。 定义它的 镜像距离 为：abs(n - reverse(n)) ，其中 reverse(n) 表示将 n 的数字反转后形成的整数。 返回表示 n 的镜像距离的整数。 其中，abs(x) 表示 x 的绝对值。 示例 1： 输入： n = 25 输出： 27 解释： * reverse(25) = 52。 * 因此，答案为 abs(25 - 52) = 27。 示例 2： 输入： n = 10 输出： 9 解释： * reverse(10) = 01，即 1。 * 因此，答案为 abs(10 - 1) = 9。 示例 3： 输入： n = 7 输出： 0 解释： * reverse(7) = 7。 * 因此，答案为 abs(7 - 7) = 0。 提示： * 1 <= n <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 将整数 n 转换为字符串，然后反转字符串得到 reverse(n)，最后计算它们的差值的绝对值。

算法步骤:
1. 将整数 n 转换为字符串。
2. 反转该字符串。
3. 将反转后的字符串转换回整数。
4. 计算 n 和反转后的整数的差值的绝对值。

关键点:
- 使用字符串操作来反转整数，这样可以避免处理进位和溢出问题。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(d)，其中 d 是整数 n 的位数。因为字符串反转和整数转换的操作都是线性的。
空间复杂度: O(d)，因为我们需要存储反转后的字符串。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def mirror_distance_of_an_integer(n: int) -> int:
    """
    函数式接口 - 计算整数 n 的镜像距离
    """
    # 将整数 n 转换为字符串
    str_n = str(n)
    
    # 反转字符串
    reversed_str_n = str_n[::-1]
    
    # 将反转后的字符串转换回整数
    reversed_n = int(reversed_str_n)
    
    # 计算 n 和反转后的整数的差值的绝对值
    distance = abs(n - reversed_n)
    
    return distance


Solution = create_solution(mirror_distance_of_an_integer)