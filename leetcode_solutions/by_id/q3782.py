import pandas as pd
import re

def solution(users: pd.DataFrame) -> pd.DataFrame:
    """
    函数式接口 - 查找所有合法邮箱地址
    """
    # 定义正则表达式
    pattern = r'^[a-zA-Z0-9_]+@[a-zA-Z]+\.[a-zA-Z]{3}$'
    
    # 过滤合法的邮箱地址
    valid_emails = users[users['email'].str.match(pattern)]
    
    # 按 user_id 升序排序
    result = valid_emails.sort_values(by='user_id')
    
    return result

