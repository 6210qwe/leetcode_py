"""
核心思想: 使用正则表达式来匹配合法的邮箱地址。

算法步骤:
1. 定义一个正则表达式来匹配合法的邮箱地址。
2. 使用 pandas 库读取数据并应用正则表达式过滤合法的邮箱地址。
3. 对结果按 user_id 升序排序。

关键点:
- 正则表达式的使用可以高效地进行字符串匹配。
"""

