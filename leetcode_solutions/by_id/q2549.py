"""
2454. 下一个更大元素 IV - 给你一个下标从 0 开始的非负整数数组 nums 。对于 nums 中每一个整数，你必须找到对应元素的 第二大 整数。 如果 nums[j] 满足以下条件，那么我们称它为 nums[i] 的 第二大 整数： * j > i * nums[j] > nums[i] * 恰好存在 一个 k 满足 i < k < j 且 nums[k] > nums[i] 。 如果不存在 nums[j] ，那么第二大整数为 -1 。 * 比方说，数组 [1, 2, 4, 3] 中，1 的第二大整数是 4 ，2 的第二大整数是 3 ，3 和 4 的第二大整数是 -1 。 请你返回一个整数数组 answer ，其中 answer[i]是 nums[i] 的第二大整数。 示例 1： 输入：nums = [2,4,0,9,6] 输出：[9,6,6,-1,-1] 解释： 下标为 0 处：2 的右边，4 是大于 2 的第一个整数，9 是第二个大于 2 的整数。 下标为 1 处：4 的右边，9 是大于 4 的第一个整数，6 是第二个大于 4 的整数。 下标为 2 处：0 的右边，9 是大于 0 的第一个整数，6 是第二个大于 0 的整数。 下标为 3 处：右边不存在大于 9 的整数，所以第二大整数为 -1 。 下标为 4 处：右边不存在大于 6 的整数，所以第二大整数为 -1 。 所以我们返回 [9,6,6,-1,-1] 。 示例 2： 输入：nums = [3,3] 输出：[-1,-1] 解释： 由于每个数右边都没有更大的数，所以我们返回 [-1,-1] 。 提示： * 1 <= nums.length <= 105 * 0 <= nums[i] <= 109
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用两个单调栈来分别存储第一大和第二大的元素。

算法步骤:
1. 初始化两个单调栈 `stack1` 和 `stack2`。
2. 从右到左遍历数组 `nums`，维护两个栈：
   - 如果当前元素大于 `stack1` 的栈顶元素，则将 `stack1` 的栈顶元素弹出并压入 `stack2`。
   - 将当前元素压入 `stack1`。
3. 再次从右到左遍历数组 `nums`，构建结果数组 `answer`：
   - 如果当前元素大于 `stack2` 的栈顶元素，则将 `stack2` 的栈顶元素弹出并记录为当前元素的第二大整数。
   - 如果当前元素大于 `stack1` 的栈顶元素，则将 `stack1` 的栈顶元素弹出并压入 `stack2`。
   - 将当前元素压入 `stack1`。
4. 返回结果数组 `answer`。

关键点:
- 使用两个单调栈来分别存储第一大和第二大的元素。
- 从右到左遍历两次数组，确保正确找到第二大整数。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(n)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def next_greater_element_iv(nums: List[int]) -> List[int]:
    """
    函数式接口 - 实现
    """
    n = len(nums)
    stack1, stack2 = [], []
    answer = [-1] * n

    # 从右到左遍历数组，构建两个单调栈
    for i in range(n - 1, -1, -1):
        while stack1 and nums[i] >= stack1[-1]:
            stack1.pop()
        if stack2 and nums[i] < stack2[-1]:
            answer[i] = stack2[-1]
        while stack2 and nums[i] >= stack2[-1]:
            stack2.pop()
        stack2.append(stack1[-1] if stack1 else -1)
        stack1.append(nums[i])

    return answer


Solution = create_solution(next_greater_element_iv)