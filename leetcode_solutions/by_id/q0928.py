"""
892. 三维形体的表面积 - 给你一个 n * n 的网格 grid ，上面放置着一些 1 x 1 x 1 的正方体。每个值 v = grid[i][j] 表示 v 个正方体叠放在对应单元格 (i, j) 上。 放置好正方体后，任何直接相邻的正方体都会互相粘在一起，形成一些不规则的三维形体。 请你返回最终这些形体的总表面积。 注意：每个形体的底面也需要计入表面积中。 示例 1： [https://assets.leetcode.com/uploads/2021/01/08/tmp-grid2.jpg] 输入：grid = [[1,2],[3,4]] 输出：34 示例 2： [https://assets.leetcode.com/uploads/2021/01/08/tmp-grid4.jpg] 输入：grid = [[1,1,1],[1,0,1],[1,1,1]] 输出：32 示例 3： [https://assets.leetcode.com/uploads/2021/01/08/tmp-grid5.jpg] 输入：grid = [[2,2,2],[2,1,2],[2,2,2]] 输出：46 提示： * n == grid.length * n == grid[i].length * 1 <= n <= 50 * 0 <= grid[i][j] <= 50
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 计算每个单元格的表面积，并减去相邻单元格之间的重叠部分。

算法步骤:
1. 初始化总表面积为 0。
2. 遍历每个单元格，计算其表面积。
3. 对于每个单元格，计算其与上下左右四个方向相邻单元格的重叠部分，并从总表面积中减去。
4. 返回总表面积。

关键点:
- 每个单元格的表面积为 6 * v - 2 * (v - 1)，其中 v 是该单元格的高度。
- 相邻单元格之间的重叠部分为 min(v1, v2) * 2，其中 v1 和 v2 分别是两个相邻单元格的高度。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n^2)，其中 n 是网格的边长。我们需要遍历整个网格。
空间复杂度: O(1)，只使用了常数级的额外空间。
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def surface_area(grid: List[List[int]]) -> int:
    """
    计算三维形体的总表面积
    """
    n = len(grid)
    total_surface_area = 0

    for i in range(n):
        for j in range(n):
            if grid[i][j] > 0:
                # 计算当前单元格的表面积
                total_surface_area += 6 * grid[i][j] - 2 * (grid[i][j] - 1)

                # 减去与上方单元格的重叠部分
                if i > 0:
                    total_surface_area -= 2 * min(grid[i][j], grid[i - 1][j])

                # 减去与左侧单元格的重叠部分
                if j > 0:
                    total_surface_area -= 2 * min(grid[i][j], grid[i][j - 1])

    return total_surface_area


Solution = create_solution(surface_area)