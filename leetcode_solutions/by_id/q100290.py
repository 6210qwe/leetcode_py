"""
核心思想: 使用状态机来解析字符串，判断其是否为有效数字。

算法步骤:
1. 定义状态和转移规则。
2. 遍历字符串，根据当前字符更新状态。
3. 最终状态应为有效数字的结束状态。

关键点:
- 状态机的设计需要覆盖所有可能的情况。
- 空格、符号、整数、小数、指数等部分的处理。
"""

# ============================================================================
# 复杂度分析
