def solution_function_name(s: str) -> bool:
    """
    函数式接口 - 判断字符串是否为有效数字
    """
    # 去除首尾空格
    s = s.strip()
    if not s:
        return False

    # 定义状态和转移规则
    states = [
        {' ': 0, 's': 1, 'd': 2, '.': 4},  # 0. 开始
        {'d': 2, '.': 4},                  # 1. 符号
        {'d': 2, '.': 3, 'e': 5, ' ': 8},  # 2. 整数
        {'d': 3, 'e': 5, ' ': 8},          # 3. 小数点后有数字
        {'d': 3},                          # 4. 小数点后无数字
        {'s': 6, 'd': 7},                  # 5. 指数
        {'d': 7},                          # 6. 指数符号
        {'d': 7, ' ': 8},                  # 7. 指数数字
        {' ': 8}                           # 8. 结束
    ]

    def char_type(c):
        if c in '0123456789':
            return 'd'
        elif c in '+-':
            return 's'
        elif c == 'e' or c == 'E':
            return 'e'
        elif c == '.':
            return '.'
        elif c == ' ':
            return ' '
        else:
            return '?'

    p = 0
    for c in s:
        t = char_type(c)
        if t not in states[p]:
            return False
        p = states[p][t]

    return p in [2, 3, 7, 8]


Solution = create_solution(solution_function_name)