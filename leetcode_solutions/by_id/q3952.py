"""
3637. 三段式数组 I - 给你一个长度为 n 的整数数组 nums。 如果存在索引 0 < p < q < n − 1，使得数组满足以下条件，则称其为 三段式数组（trionic）： * nums[0...p] 严格 递增， * nums[p...q] 严格 递减， * nums[q...n − 1] 严格 递增。 如果 nums 是三段式数组，返回 true；否则，返回 false。 示例 1: 输入: nums = [1,3,5,4,2,6] 输出: true 解释: 选择 p = 2, q = 4： * nums[0...2] = [1, 3, 5] 严格递增 (1 < 3 < 5)。 * nums[2...4] = [5, 4, 2] 严格递减 (5 > 4 > 2)。 * nums[4...5] = [2, 6] 严格递增 (2 < 6)。 示例 2: 输入: nums = [2,1,3] 输出: false 解释: 无法选出能使数组满足三段式要求的 p 和 q 。 提示: * 3 <= n <= 100 * -1000 <= nums[i] <= 1000
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用一次遍历找到符合条件的 p 和 q。

算法步骤:
1. 初始化两个指针 p 和 q，分别用于标记递增和递减部分的结束位置。
2. 遍历数组，找到第一个递减的位置 p。
3. 从 p 开始继续遍历，找到第一个递增的位置 q。
4. 检查 q 之后的部分是否严格递增。

关键点:
- 通过一次遍历找到 p 和 q，确保时间复杂度最优。
- 确保 p 和 q 之间的部分严格递减，q 之后的部分严格递增。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def is_trionic_array(nums: List[int]) -> bool:
    """
    函数式接口 - 判断给定数组是否为三段式数组
    """
    if len(nums) < 3:
        return False

    p = 0
    while p + 1 < len(nums) and nums[p] < nums[p + 1]:
        p += 1

    if p == len(nums) - 1:
        return False

    q = p
    while q + 1 < len(nums) and nums[q] > nums[q + 1]:
        q += 1

    if q == len(nums) - 1 or p == q:
        return False

    for i in range(q, len(nums) - 1):
        if nums[i] >= nums[i + 1]:
            return False

    return True


Solution = create_solution(is_trionic_array)