"""
3452. 好数字之和 - 给定一个整数数组 nums 和一个整数 k，如果元素 nums[i] 严格 大于下标 i - k 和 i + k 处的元素（如果这些元素存在），则该元素 nums[i] 被认为是 好 的。如果这两个下标都不存在，那么 nums[i] 仍然被认为是 好 的。 返回数组中所有 好 元素的 和。 示例 1： 输入： nums = [1,3,2,1,5,4], k = 2 输出： 12 解释： 好的数字包括 nums[1] = 3，nums[4] = 5 和 nums[5] = 4，因为它们严格大于下标 i - k 和 i + k 处的数字。 示例 2： 输入： nums = [2,1], k = 1 输出： 2 解释： 唯一的好数字是 nums[0] = 2，因为它严格大于 nums[1]。 提示： * 2 <= nums.length <= 100 * 1 <= nums[i] <= 1000 * 1 <= k <= floor(nums.length / 2)
"""

# ============================================================================
# 实现思路
# ============================================================================
"""
核心思想: 使用双指针法检查每个元素是否为好数字，并计算其和。

算法步骤:
1. 初始化两个指针 left 和 right，分别指向 i - k 和 i + k。
2. 遍历数组，对于每个元素 nums[i]，检查它是否大于 left 和 right 指针所指向的元素。
3. 如果 nums[i] 是好数字，则将其加入结果和中。
4. 返回结果和。

关键点:
- 使用边界条件处理指针越界的情况。
"""

# ============================================================================
# 复杂度分析
# ============================================================================
"""
时间复杂度: O(n)
空间复杂度: O(1)
"""

# ============================================================================
# 代码实现
# ============================================================================

from typing import List, Optional
from leetcode_solutions.utils.linked_list import ListNode
from leetcode_solutions.utils.tree import TreeNode
from leetcode_solutions.utils.solution import create_solution


def sum_of_good_numbers(nums: List[int], k: int) -> int:
    """
    函数式接口 - 计算好数字之和
    """
    n = len(nums)
    good_sum = 0
    
    for i in range(n):
        left = max(0, i - k)
        right = min(n - 1, i + k)
        
        if (i == 0 or nums[i] > nums[left]) and (i == n - 1 or nums[i] > nums[right]):
            good_sum += nums[i]
    
    return good_sum


